                  <p>Jean de La Fontaine was born in 1621 in Champagne; both his parents belonged to the highest provincial middle class. He went to school in Reims, made a brief attempt to study for the priesthood and then studied law.</p>

<p>He married in 1647 but separated amicably from his wife in 1658. For most of the next forty years he lived in Paris, supported by a succession of wealthy patrons. His literary career began when he was in his thirties, and his first work of real importance was the <em>Contes</em>, the first volume of which appeared in 1664. Many of these are derived from Boccaccio’s <em>Decameron</em>; they are bawdy, and the third volume (1674) was seized by the police and prohibited from sale. Neglected until the twentieth century, they are now considered almost as fine as his later <em>Fables</em>.</p>

<p>From the late 1660s La Fontaine began to meet regularly with Racine, Boileau and Molière; the four writers were known as ‘the quartet of the Rue du Vieux Colombier’. These writers are, of course, very different indeed, but all are remarkable for their clarity, wit and intelligence. There has seldom, in the history of any country, been a more important web of literary friendships.</p>

<p>La Fontaine is still the most often quoted French writer. His Fables are an integral part of French culture, repeatedly rewritten and reinterpreted in dance, music and the visual arts. They are equally remarkable for their formal perfection and for the understanding of human nature embodied in them. Inspired himself primarily by Aesop, and also by the Panchatantra, the vast Indian collection first translated into French in 1644, La Fontaine provided a model for subsequent fabulists, above all for Russia's Ivan Krylov.


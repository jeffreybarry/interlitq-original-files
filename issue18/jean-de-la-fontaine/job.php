<p>Jean de La Fontaine, who lived from 1621 to 1695, created tales and fables by versifying stories already known, mainly from classical antiquity and the Renaissance. This is a translation from his orignal French, <em>La Matrone d'Ephèse</em>. The opening would have reminded his first readers that the story was ancient. Its first known appearance dates from the time of Nero, in the <em>Satyricon</em> of Petronius (sections 110-111), but it is no doubt older, since it is believed to be a 'Milesian tale', one of those attributed to Aristides of Miletus (2nd century BC). Its longevity is no doubt due to the neatness of the plot and its black humour. In France, sixteenth-century versions can be found, for instance in Brantôme's Dames <em>galantes</em> (the adjective might here be rendered 'amorous'), but nearer to La Fontaine was a prose version by his friend, the free-thinker and Epicurean Saint-Evremond, published with La Fontaine's first collection of <em>Contes</em> ('Tales') in 1664. These Tales, sixty or more of them in total, were mostly verse retellings of stories found in Renaissance authors such as Boccaccio, often scurrilous and sometimes obscene, wittily recounted, the more risqué passages making crystal clear what was going on in language which adroitly avoided using offensive terms. These stories made La Fontaine's reputation, not entirely to his advantage, before the fables for which he is now remembered. This tale, not exactly pure but not as naughty as most of them, was first published in 1682. It is also in the twelfth and last volume of Fables in 1693; editors often publish it as a fable, which it is not. The entire collection of <em>Contes </em>has been translated by Guido Waldman (Carcanet Press, 2000). In modern times perhaps the best-known adaptations of the tale are a play by Christopher Fry, <em>A Phoenix too Frequent</em>, and an episode in Fellini's film of the <em>Satyricon</em>. </p>

<p>One or two points may need explanation. The widow, though living before the Christian era, seems to be warned in Christian terms not to grieve too much: the sin that is mentioned would have been the sin of despair. However she appears oblivious of the fact that for Christians suicide is sinful. As for the hanged robber, he is guarded because relatives or friends would have wanted not only to put an end to his posthumous disgrace, but also to give his body proper burial. </p>


	<p><strong>The Widow of Ephesus</strong></p>

	<p>&nbsp;&nbsp;&nbsp;&nbsp;If ever there has been a tale<br>
	&nbsp;&nbsp;&nbsp;&nbsp;outworn, familiar, and stale,<br>
	&nbsp;&nbsp;&nbsp;&nbsp;it’s this one here; and I have made<br>
a version versified. – Why choose it then?<br>
Did someone’s orders have to be obeyed?<br>
It’s been rewritten time and time again.<br>
When we compare Petronius to you<br>
	&nbsp;&nbsp;&nbsp;&nbsp;will not your widow’s beauties fade?<br>
How can you make this ancient story new?<br>
- Critic, by argument to prove you wrong<br>
	&nbsp;&nbsp;&nbsp;&nbsp;would take me, I believe, too long;<br>
let’s see if I can bring the tale to life.<br><br>

	&nbsp;&nbsp;&nbsp;&nbsp;In Ephesus there lived a wife<br>
supremely virtuous, and wise and good;<br>
she took her loyalty, they said in town,<br>
further than any other woman would.<br>
Her chastity and she acquired renown<br>
from all the local talk. A thing so rare<br>
brought visitors to see, and stand and stare:<br>
an honour to her sex, the city’s pride.<br>
Mothers-in-law would take a son’s new bride<br>
to see just what a perfect wife should be;<br>
each husband praised her to his cherished spouse.<br>
With her originates the family tree<br>
of Prudery, a famous ancient house.<br>
Her husband’s passion for her was extreme.<br>
	&nbsp;&nbsp;&nbsp;&nbsp;He died. To tell you why and how<br>
	&nbsp;&nbsp;&nbsp;&nbsp;is nothing to my purpose now.<br>
He died. The will’s bequests, so it would seem,<br>
	&nbsp;&nbsp;&nbsp;&nbsp;might have sufficed to soothe her grief,<br>
if wealth bequeathed to widows brought relief<br>
	&nbsp;&nbsp;&nbsp;&nbsp;when husbands loved and loving die.<br>
	&nbsp;&nbsp;&nbsp;&nbsp;Some widows tear their hair and cry<br>
while thinking nonetheless of what remains,<br>
and through their tears will reckon up their gains;<br>
but this one’s cries spread panic all around,<br>
	&nbsp;&nbsp;&nbsp;&nbsp;so miserably did she moan,<br>
	&nbsp;&nbsp;&nbsp;&nbsp;so loud and wretched was the sound;<br>
to hear her would have pierced a heart of stone<br> 
- although, when such misfortunes strike, we know,<br>
whatever pain is suffered from the blow,<br>
the sorrow felt is less than what appears;<br>
a trace of ostentation swells the tears.<br>
In her affliction, friends and neighbours sought<br>
	&nbsp;&nbsp;&nbsp;&nbsp;to do their duty, as they thought,<br>
by preaching moderation: her distress<br>
might be a sin if taken to excess,<br>
they said; which made her all the more distraught.<br>
She did not wish to see the light of day<br>
her husband saw no longer; in the end<br>
she went into the vault in which he lay,<br>
resolved that from the tomb she would descend<br>
	&nbsp;&nbsp;&nbsp;&nbsp;to Hades, following his shade<br>
	&nbsp;&nbsp;&nbsp;&nbsp;upon the journey he had made.<br><br>

And now for more excess, but in a friend,<br>
for friends can take devotion to a fault.<br>
Her slave, from pity, went into the vault,<br>
prepared to stay with her until the end.<br>
	&nbsp;&nbsp;&nbsp;&nbsp;Prepared? – she had, that is to say,<br>
thought through the plan perhaps halfway,<br>
and till the moment of decision came<br>
would certainly be resolute and brave.<br>
From infancy the mistress and the slave<br>
had shared their lives, in everything the same;<br>
to each the other was extremely dear;<br>
their love had grown with every passing year.<br>
Across the world you would have searched in vain<br>
	&nbsp;&nbsp;&nbsp;&nbsp;to find such steadfast love again.<br>
The slave it was who had the greater sense.<br>
She waited till the storms were less intense,<br>
	&nbsp;&nbsp;&nbsp;&nbsp;then tried to guide the grieving soul<br>
towards the usual paths of common woe.<br>
In spite of all her efforts to console,<br>
she failed; the widow only wished to go<br>
towards the black and dismal place below<br>
and join, by any means, her husband there.<br>
	&nbsp;&nbsp;&nbsp;&nbsp;A dagger was the best for speed;<br>
but still she wanted with her eyes to feed<br>
	&nbsp;&nbsp;&nbsp;&nbsp;upon the treasured corpse, still dear<br>
	&nbsp;&nbsp;&nbsp;&nbsp;while cold in death upon its bier.<br>
	&nbsp;&nbsp;&nbsp;&nbsp;Such was her diet, nothing more,<br>
inside the vault; starvation was the door,<br>
among all those which lead us out of life,<br>
that seemed most fitting to this loving wife.<br>
A day went by; another came and went:<br>
from nourishment she faithfully abstained,<br>
but deeply sighing constantly maintained,<br>
with many an 'Alas!', a long lament<br>
at Nature, gods, and fate, as if she meant<br>
to go on doing all she could to show<br>
	&nbsp;&nbsp;&nbsp;&nbsp;how widows should express their woe.<br><br>

There was another corpse along the road.<br>
He had a very different abode.<br>
On him no monument had been bestowed,<br>
except a gibbet's crossbar overhead.<br>
This body, left to hang, was there displayed<br>
	&nbsp;&nbsp;&nbsp;&nbsp;to fill all other thieves with dread.<br>
A soldier stood beside the gibbet, paid<br>
to keep a careful watch, by strict decree:<br>
if fellow-thieves, or friends, or kin should dare<br>
to move the body from the gallows-tree,<br>
the sentry who had slept, or took no care,<br>
himself would hang instead. Now I agree<br>
that such a punishment would be too hard,<br>
	&nbsp;&nbsp;&nbsp;&nbsp;but for the common good, the law<br>
allowed no mercy to an idle guard.<br>
One night when it was dark, the soldier saw,<br>
inside the vaulted tomb, some chinks of light.<br>
	&nbsp;&nbsp;&nbsp;&nbsp;Intrigued by this unusual sight,<br>
he goes to look, and at some distance hears,<br>
filling the night-time air, the widow's cries,<br>
then having entered, asks, in some surprise,<br>
	&nbsp;&nbsp;&nbsp;&nbsp;why such a clamour? why such tears?<br>
why does this melancholy music sound<br>
in this dark house, with sadness all around?<br>
	&nbsp;&nbsp;&nbsp;&nbsp;These trifling questions went unheard:<br>
her mourning kept the widow occupied.<br>
	&nbsp;&nbsp;&nbsp;&nbsp;The corpse, on her behalf, replied,<br>
	&nbsp;&nbsp;&nbsp;&nbsp;but did not need to say a word;<br>
	&nbsp;&nbsp;&nbsp;&nbsp;its very presence made it clear<br>
why she, while still alive, was buried there.<br>
"From grief, we both have vowed," the slave then said,<br>
	&nbsp;&nbsp;&nbsp;&nbsp;"to starve ourselves till we are dead."<br>
In his reply the soldier sought to give,<br>
though lacking in rhetorical invention,<br>
a true idea of what it is to live.<br>
	&nbsp;&nbsp;&nbsp;&nbsp;This time the lady paid attention:<br>
for passing time was working on her now;<br>
the previous emotion had decreased.<br>
	&nbsp;&nbsp;&nbsp;&nbsp;"If being faithful to your vow<br>
forbids all nourishment," the guard pursued,<br>
	&nbsp;&nbsp;&nbsp;&nbsp;"it would be possible at least<br>
for you to watch me while I eat my food;<br>
and you can go on dying nonetheless."<br>
The women find that they can acquiesce<br>
to this proposal, and the guard departs<br>
to bring his supper back, with their consent.<br>
Meanwhile the slave, deep in her heart of hearts,<br>
was tempted to renounce the cruel intent<br>
of following, as escort, where the husband went.<br>
"Madam," she said, "a thought occurs to me:<br>
whether you choose to be, or not to be,<br>
it doesn't matter to your husband any more.<br>
Would he, do you believe, have followed you,<br>
	&nbsp;&nbsp;&nbsp;&nbsp;had you died first, and gone before?<br>
He would have wished to last his lifetime through.<br>
Since life is short, why hurry? We could stay.<br>
At twenty years of age, why choose the tomb?<br>
Once there, we'll be at leisure, I presume.<br>
Myself I'd rather die when old and grey.<br>
Why take your beauties to the world below?<br>
	&nbsp;&nbsp;&nbsp;&nbsp;What use is it, in any case,<br>
to show them to the dead? Two days ago,<br>
seeing the treasures that the heavens chose,<br>
for their delight, to ornament your face,<br>
I thought: alas! for shame, that all of those,<br>
by our decision, now will not survive."<br>
The lady, flattered, started to revive.<br>
The love-inducing god had raised his bow;<br>
	&nbsp;&nbsp;&nbsp;&nbsp;he picked his moment, picked a dart,<br>
	&nbsp;&nbsp;&nbsp;&nbsp;and with it brought the soldier low.<br>
A second lightly brushed the lady's heart.<br>
Although in tears, her beauty and her youth<br>
	&nbsp;&nbsp;&nbsp;&nbsp;were still attractive, and in truth<br>
	&nbsp;&nbsp;&nbsp;&nbsp;a man quite difficult to please<br>
	&nbsp;&nbsp;&nbsp;&nbsp;might fall in love with her with ease,<br>
	&nbsp;&nbsp;&nbsp;&nbsp;and love her as a wife as well.<br>
	&nbsp;&nbsp;&nbsp;&nbsp;In love the soldier duly fell.<br>
In this event pity and tears combined,<br>
for pitying is loving of a kind:<br>
it has its charms, because, to say the least,<br>
	&nbsp;&nbsp;&nbsp;&nbsp;beauty in tears is much increased.<br>
He give her praise: she does not disagree,<br>
but takes it in, although this poisoned drink<br>
is in the scale of love the first degree.<br>
The compliments he pays her make her think<br>
she rather likes him. Following this feat<br>
his next achievement is to make her eat;<br>
and next he manages to makes her feel<br>
	&nbsp;&nbsp;&nbsp;&nbsp;he has a certain sex-appeal:<br>
in point of male attractions, far ahead<br>
of any handsome husband who is dead.<br>
	&nbsp;&nbsp;&nbsp;&nbsp;The suitor she had deigned to hear<br>
became a second husband then and there;<br>
the witness was the corpse, once held so dear.<br>
But while these nuptials occupied the pair,<br>
a robber seized his chance, and took away<br>
the body that was in the soldier's care.<br>
He heard a noise, and rushed outside: in vain;<br>
the robber was already out of sight.<br>
The soldier had no refuge; in this plight<br>
he went back to the women to explain.<br>
The slave replied, perceiving his dismay:<br>
"Someone has robbed you of your corpse, you say?<br>
And you will hang? The law will not relent?<br>
I'll tell you what," this faithful servant said,<br>
	&nbsp;&nbsp;&nbsp;&nbsp;"assuming Madam will consent,<br>
	&nbsp;&nbsp;&nbsp;&nbsp;we'll put our corpse up there instead.<br>
	&nbsp;&nbsp;&nbsp;&nbsp;The passers-by will never guess."<br>
The lady did consent. Oh fickleness! thy name<br>
is woman; ever women were the same,<br>
the ones with greater beauty, and with less.<br>
	&nbsp;&nbsp;&nbsp;&nbsp;If some of those whom we adore<br>
were faithful too, could any man want more?<br><br>

As for you prudes, don't count upon your strength;<br>
	&nbsp;&nbsp;&nbsp;&nbsp;it may not be enough; beware!<br>
You may intend to keep us at arm's length,<br>
but we think our intentions too are fair.<br>
	&nbsp;&nbsp;&nbsp;&nbsp;The outcome, as my story shows,<br>
	&nbsp;&nbsp;&nbsp;&nbsp;may not be what you might suppose.<br>
And good Petronius, in verity,<br>
was this a case so rare that it deserved<br>
to be by you so carefully preserved<br>
as an example to posterity?<br>
The widow, I believe, did little wrong,<br>
	&nbsp;&nbsp;&nbsp;&nbsp;except to mourn too loud, too long,<br>
	&nbsp;&nbsp;&nbsp;&nbsp;and foolishly resolve to die,<br>
a pointless plan; apart from that, to tie<br>
that once-loved husband to the gallows-tree<br>
was neither here nor there, it seems to me.<br>
By doing so she saved the other one<br>
from hanging too. When all is said and done,<br>
a common soldier on his feet is worth<br>
more than an emperor interred in earth.</p>

<p><strong>La Matrone d'Éphèse</strong></p>

<p>S'il est un conte usé, commun et rebattu, <br>
C'est celui qu'en ces vers j'accommode à ma guise.<br> 
- Et pourquoi donc le choisis-tu ? <br>
Qui t'engage à cette entreprise ? <br>
N'a-t-elle point déjà produit assez d'écrits ?<br> 
Quelle grâce aura ta Matrone <br>
Au prix de celle de Pétrone ? <br>
Comment la rendras-tu nouvelle à nos esprits ?<br> 
- Sans répondre aux censeurs, car c'est chose infinie,<br> 
Voyons si dans mes vers je l'aurai rajeunie. <br><br>

Dans Ephèse, il fut autrefois <br>
Une dame en sagesse et vertus sans égale,<br> 
Et selon la commune voix <br>
Ayant su raffiner sur l'amour conjugale.<br> 
Il n'était bruit que d'elle et de sa chasteté :<br> 
On l'allait voir par rareté : <br>
C'était l'honneur du sexe : heureuse sa patrie :<br> 
Chaque mère à sa bru l'alléguait pour patron ; <br>
Chaque époux la prônait à sa femme chérie ; <br>
D'elle descendent ceux de la prudoterie, <br>
Antique et célèbre maison. <br>
Son mari l'aimait d'amour folle.<br> 
Il mourut. De dire comment, <br>
Ce serait un détail frivole ; <br>
Il mourut, et son testament <br>
N'était plein que de legs qui l'auraient consolée, <br>
Si les biens réparaient la perte d'un mari <br>
Amoureux autant que chéri. <br>
Mainte veuve pourtant fait la déchevelée,<br> 
Qui n'abandonne pas le soin du demeurant, <br>
Et du bien qu'elle aura fait le compte en pleurant.<br> 
Celle-ci par ses cris mettait tout en alarme ; <br>
Celle-ci faisait un vacarme, <br>
Un bruit, et des regrets à percer tous les coeurs ; <br>
Bien qu'on sache qu'en ces malheurs <br>
De quelque désespoir qu'une âme soit atteinte,<br> 
La douleur est toujours moins forte que la plainte,<br> 
Toujours un peu de faste entre parmi les pleurs. <br>
Chacun fit son devoir de dire à l'affligée <br>
Que tout a sa mesure, et que de tels regrets <br>
Pourraient pécher par leur excès : <br>
Chacun rendit par là sa douleur rengrégée.<br> 
Enfin ne voulant plus jouir de la clarté <br>
Que son époux avait perdue, <br>
Elle entre dans sa tombe, en ferme volonté<br> 
D'accompagner cette ombre aux enfers descendue.<br> 
Et voyez ce que peut l'excessive amitié ; <br>
(Ce mouvement aussi va jusqu'à la folie) <br>
Une esclave en ce lieu la suivit par pitié, <br>
Prête à mourir de compagnie. <br>
Prête, je m'entends bien ; c'est-à-dire en un mot<br> 
N'ayant examiné qu'à demi ce complot, <br>
Et jusques à l'effet courageuse et hardie.<br> 
L'esclave avec la dame avait été nourrie. <br>
Toutes deux s'entraimaient, et cette passion <br>
Etait crue avec l'âge au coeur des deux femelles :<br> 
Le monde entier à peine eût fourni deux modèles <br>
D'une telle inclination. <br><br>

Comme l'esclave avait plus de sens que la dame, <br>
Elle laissa passer les premiers mouvements, <br>
Puis tâcha, mais en vain, de remettre cette âme <br>
Dans l'ordinaire train des communs sentiments. <br>
Aux consolations la veuve inaccessible <br>
S'appliquait seulement à tout moyen possible<br> 
De suivre le défunt aux noirs et tristes lieux :<br> 
Le fer aurait été le plus court et le mieux, <br>
Mais la dame voulait paître encore ses yeux <br>
Du trésor qu'enfermait la bière, <br>
Froide dépouille et pourtant chère. <br>
C'était là le seul aliment <br>
Qu'elle prît en ce monument. <br>
La faim donc fut celle des portes<br> 
Qu'entre d'autres de tant de sortes, <br>
Notre veuve choisit pour sortir d'ici-bas.<br> 
Un jour se passe, et deux sans autre nourriture<br> 
Que ses fréquents soupirs, que ses fréquents hélas,<br> 
Qu'un inutile et long murmure <br>
Contre les dieux, le sort, et toute la nature.<br> 
Enfin sa douleur n'omit rien, <br>
Si la douleur doit s'exprimer si bien.<br> 
Encore un autre mort faisait sa résidence <br>
Non loin de ce tombeau, mais bien différemment,<br> 
Car il n'avait pour monument <br>
Que le dessous d'une potence. <br>
Pour exemple aux voleurs on l'avait là laissé.<br> 
Un soldat bien récompensé <br>
Le gardait avec vigilance. <br>
Il était dit par ordonnance <br>
Que si d'autres voleurs, un parent, un ami<br> 
L'enlevaient, le soldat nonchalant, endormi, <br>
Remplirait aussitôt sa place, <br>
C'était trop de sévérité ; <br>
Mais la publique utilité <br>
Défendait que l'on fit au garde aucune grâce. <br>
Pendant la nuit il vit aux fentes du tombeau <br>
Briller quelque clarté, spectacle assez nouveau. <br>
Curieux, il y court, entend de loin la dame <br>
Remplissant l'air de ses clameurs. <br>
Il entre, est étonné, demande à cette femme,<br> 
Pourquoi ces cris, pourquoi ces pleurs, <br>
Pourquoi cette triste musique, <br>
Pourquoi cette maison noire et mélancolique.<br> 
Occupée à ses pleurs à peine elle entendit <br>
Toutes ces demandes frivoles, <br>
Le mort pour elle y répondit ; <br>
Cet objet sans autres paroles <br>
Disait assez par quel malheur <br>
La dame s'enterrait ainsi toute vivante.<br> 
Nous avons fait serment, ajouta la suivante,<br> 
De nous laisser mourir de faim et de douleur. <br>
Encore que le soldat fût mauvais orateur, <br>
Il leur fit concevoir ce que c'est que la vie.<br> 
La dame cette fois eut de l'attention ; <br>
Et déjà l'autre passion <br>
Se trouvait un peu ralentie.<br> 
Le temps avait agi. Si la foi du serment,<br> 
Poursuivit le soldat, vous défend l'aliment, <br>
Voyez-moi manger seulement, <br>
Vous n'en mourrez pas moins. Un tel tempérament<br> 
Ne déplut pas aux deux femelles : <br>
Conclusion qu'il obtint d'elles <br>
Une permission d'apporter son soupé ;<br> 
Ce qu'il fit ; et l'esclave eut le coeur fort tenté <br>
De renoncer dès lors à la cruelle envie <br>
De tenir au mort compagnie. <br>
Madame, ce dit-elle, un penser m'est venu :<br> 
Qu'importe à votre époux que vous cessiez de vivre ?<br> 
Croyez-vous que lui-même il fût homme à vous suivre <br>
Si par votre trépas vous l'aviez prévenu ? <br>
Non Madame, il voudrait achever sa carrière. <br>
La nôtre sera longue encor si nous voulons. <br>
Se faut-il à vingt ans enfermer dans la bière ?<br> 
Nous aurons tout loisir d'habiter ces maisons. <br>
On ne meurt que trop tôt ; qui nous presse ? attendons ;<br> 
Quant à moi je voudrais ne mourir que ridée. <br>
Voulez-vous emporter vos appas chez les morts ? <br>
Que vous servira-t-il d'en être regardée ? <br>
Tantôt en voyant les trésors <br>
Dont le Ciel prit plaisir d'orner votre visage,<br> 
Je disais : hélas ! c'est dommage, <br>
Nous-mêmes nous allons enterrer tout cela.<br> 
A ce discours flatteur la dame s'éveilla. <br>
Le Dieu qui fait aimer prit son temps ; il tira<br> 
Deux traits de son carquois ; de l'un il entama <br>
Le soldat jusqu'au vif ; l'autre effleura la dame : <br>
Jeune et belle elle avait sous ses pleurs de l'éclat, <br>
Et des gens de goût délicat <br>
Auraient bien pu l'aimer, et même étant leur femme.<br> 
Le garde en fut épris : les pleurs et la pitié, <br>
Sorte d'amour ayant ses charmes, <br>
Tout y fit : une belle, alors qu'elle est en larmes<br> 
En est plus belle de moitié. <br>
Voilà donc notre veuve écoutant la louange,<br> 
Poison qui de l'amour est le premier degré ; <br>
La voilà qui trouve à son gré <br>
Celui qui le lui donne ; il fait tant qu'elle mange,<br> 
Il fait tant que de plaire, et se rend en effet <br>
Plus digne d'être aimé que le mort le mieux fait. <br>
Il fait tant enfin qu'elle change ; <br>
Et toujours par degrés, comme l'on peut penser : <br>
De l'un à l'autre il fait cette femme passer ; <br>
Je ne le trouve pas étrange. <br>
Elle écoute un Amant, elle en fait un Mari ; <br>
Le tout au nez du mort qu'elle avait tant chéri. <br>
Pendant cet hyménée un voleur se hasarde <br>
D'enlever le dépôt commis aux soins du garde.<br> 
Il en entend le bruit ; il y court à grands pas ;<br> 
Mais en vain, la chose était faite. <br>
Il revient au tombeau conter son embarras,<br> 
Ne sachant où trouver retraite. <br>
L'Esclave alors lui dit le voyant éperdu :<br> 
L'on vous a pris votre pendu ? <br>
Les lois ne vous feront, dites-vous, nulle grâce ?<br> 
Si Madame y consent j'y remédierai bien. <br>
Mettons notre mort en la place, <br>
Les passants n'y connaîtront rien. <br>
La Dame y consentit. O volages femelles ! <br>
La femme est toujours femme ; il en est qui sont belles, <br>
Il en est qui ne le sont pas. <br>
S'il en était d'assez fidèles, <br>
Elles auraient assez d'appas. <br><br>

Prudes vous vous devez défier de vos forces.<br> 
Ne vous vantez de rien. Si votre intention <br>
Est de résister aux amorces, <br>
La nôtre est bonne aussi ; mais l'exécution<br> 
Nous trompe également ; témoin cette Matrone. <br>
Et n'en déplaise au bon Pétrone, <br>
Ce n'était pas un fait tellement merveilleux<br> 
Qu'il en dût proposer l'exemple à nos neveux. <br>
Cette veuve n'eut tort qu'au bruit qu'on lui vit faire ;<br> 
Qu'au dessein de mourir, mal conçu, mal formé ; <br>
Car de mettre au patibulaire, <br>
Le corps d'un mari tant aimé, <br>
Ce n'était pas peut-être une si grande affaire.<br> 
Cela lui sauvait l'autre ; et tout considéré, <br>
Mieux vaut goujat debout qu'Empereur enterré.</p>


            
           




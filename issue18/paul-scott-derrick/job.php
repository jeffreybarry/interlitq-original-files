            <p></p>

<p>That snake with legs we saw in a medieval bestiary. It came to me last night in my<br>
&nbsp;&nbsp;&nbsp;&nbsp; dream.<br><br>

First it was a vague coiled shape. A shadow lying on the pavement of a parking lot.<bR>
&nbsp;&nbsp;&nbsp;&nbsp; Then it was under a low-growing tree, and playing with a bird. Beak to snout.<br>
&nbsp;&nbsp;&nbsp;&nbsp; They seemed to be grooming each other. Or kissing.<br><br>

Snakes don’t kiss little birds, I thought. They eat them. But this one didn’t. It<bR> 
&nbsp;&nbsp;&nbsp;&nbsp;turned its head and looked into my eyes. I saw that venomous mouth straight<br> &nbsp;&nbsp;&nbsp;&nbsp;on. It smiled.<br><bR>

Then it stood up on four short sturdy legs (just like the ones I remembered from <bR>
&nbsp;&nbsp;&nbsp;&nbsp;the book) and stretched.<br><br>

So, I thought, I’ve discovered something here. The artist was right. This four-<bR>
&nbsp;&nbsp;&nbsp;&nbsp;legged creature is real. It’s just that it’s <em>elusive</em>. Nobody else has seen it again<bR> 
&nbsp;&nbsp;&nbsp;&nbsp;until now.<br><br>

Responsible, I notice what I can. Those rounded paws on the ground. The joints of<br>
&nbsp;&nbsp;&nbsp;&nbsp; its toes. The flexing knees. They bear its weight – efficient – as it calmly walks<br>
&nbsp;&nbsp;&nbsp;&nbsp; away. And disappears.<br><br>

Observation. Details. Facts. 

</p>

<p align="center">...</p>

<p>Every single book in a study should occupy its proper place. Alphabetical order,<bR>
&nbsp;&nbsp;&nbsp;&nbsp;  subject matter, genres. The shelves are a skeleton – strong and straight. Like a<bR>
&nbsp;&nbsp;&nbsp;&nbsp;  well-formed mind. Everything eventually finds where it belongs.<bR><bR>

Categories. Sections.<bR><bR>

This is my refuge. Surrounded by an unobtrusive company. Friends, brought forth<br>
&nbsp;&nbsp;&nbsp;&nbsp;  from the minds of friends. Nobody shouts. Everyone is patient. Waiting<br>
&nbsp;&nbsp;&nbsp;&nbsp;  politely for the moment to speak.<br><br>

Ian stares down from his image of himself on the wall. Molded face. It pulls itself<bR>
&nbsp;&nbsp;&nbsp;&nbsp;  together from a welter of angles and colors. Slabs of paint that seem up close<bR>
&nbsp;&nbsp;&nbsp;&nbsp;  to be a chaos organize into the shape of life.<br><br>

One eye a point of light. The other a point in shadow. Visible, sculpted. Almost<br>
&nbsp;&nbsp;&nbsp;&nbsp;  invisible. The maker’s intention. <em>If you look long enough into these eyes,<bR> 
&nbsp;&nbsp;&nbsp;&nbsp; I’ll feel whatever you’re feeling. </em><br><bR>

Every surface covers a depth. Every word – what lies beyond all words.<br><br>

A proper study. The comforting structure of shelves. One more point in a universe<br>
&nbsp;&nbsp;&nbsp;&nbsp;  of infinite centers. Light and order. Here and now. Everything in its place.
</p>

<p align="center">...</p>

<p>In the hidden loom of the mind, wherever it falls, sunlight is busy. It weaves all<br>
&nbsp;&nbsp;&nbsp;&nbsp; things it touches into the fabric of the day. Sometimes satin. Sometimes<br>
&nbsp;&nbsp;&nbsp;&nbsp; muslin. Sometimes corduroy. Garments we put on and off until we die.<br><bR>

The indolent diligence of time.<br><br>

Colors get heavier at night. Textures thicken.<br><br>

Those Prussian-blue tiles on the dome of the church that shine like water in the <br>
&nbsp;&nbsp;&nbsp;&nbsp;silver air go solid and dull. Forms withdraw into themselves to sleep beneath<br>
&nbsp;&nbsp;&nbsp;&nbsp; the floating mirror of the moon. The nave – its angled buttresses, quiet<br>
&nbsp;&nbsp;&nbsp;&nbsp; rectangles – stand where they have always stood and wait for another morning.<br><br> 

A graceful, serrated curve against the sky.<br><bR>

Learn to love what you see like sunlight falling on helpless stones. <br><br>

This mind is the mind of the world. Be careful what you take into the dark.<br>
&nbsp;&nbsp;&nbsp;&nbsp; Whatever you think about what you feel will formulate a part of what we<br>
&nbsp;&nbsp;&nbsp;&nbsp; were. A part of this home we bequeath to the children who follow our<br>
&nbsp;&nbsp;&nbsp;&nbsp; footsteps, unsuspecting. </p>


<p align="center">...</p>


<p>Buildings go to sleep when their people do. They settle down under a blanket of <br>
&nbsp;&nbsp;&nbsp;&nbsp; silence as one by one their lights go out.<br><br>

All of those sounds the day suppresses rise up to the surface and murmur. Night <br>
&nbsp;&nbsp;&nbsp;&nbsp; time’s undertone.  <bR><br>

If you happen to be awake, you can hear the tick of the clock. The cessant hum of <br>
&nbsp;&nbsp;&nbsp;&nbsp; the fridge. The breathing of the person you love beside you. The whirr of <bR>
&nbsp;&nbsp;&nbsp;&nbsp; your own blood in your ears, like cicadas whirring from their secret places in<bR>
&nbsp;&nbsp;&nbsp;&nbsp;  the dark. <br><br>

Open your eyes. Close them. Two equal darknesses. In both of which amorphous <bR>
&nbsp;&nbsp;&nbsp;&nbsp; shadows – grey on black or black on grey – slowly roil and tumble.<br><br>

This borderline that separates thought and dream. Lonely listener. Reach out now<bR>
&nbsp;&nbsp;&nbsp;&nbsp;  and touch the edges of all unwritten poems.<bR><br>

Do every sleeper’s dreams rise up somehow (through the stair-well? The air shafts?) <br>
&nbsp;&nbsp;&nbsp;&nbsp; and mingle in the attic to produce the building’s dreams? Intangible entangled<br>
&nbsp;&nbsp;&nbsp;&nbsp;  anthology? Unrecovered poems that a building also forgets like we do with <bR>
&nbsp;&nbsp;&nbsp;&nbsp; the noise and business of the coming day?

           </p>




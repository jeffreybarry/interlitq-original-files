<em>(On Maggi Hambling’s Aldeburgh Scallop and the Making of Damascus Twist)</em><br><br><br>

A studio floor with shards of pink mashed shell,<br>
ink sketches of sea-birds…. Forgive me.<br>
I’m trying to recreate what it was like <br>
for her to dream this up: a ten-foot scallop,<br> 
memory of storm and love, of two entwined lives, <br>
set upright in this shore on secret fabricator legs<br><br>

like a chilly North Sea re-run of the birth <br>
of Aphrodite. We stand in Sunday dawn<br>
uncertain of each other, on the banked-up shale<br> 
of an eroding coast, looking up at this curved <br>
guardian of the earth, a monument <br>
to music made for joy in making. <br><br>

                       <blockquote>*</blockquote>

We face an under-side of polished steel<br>
dark like the belly of a wave, dark and light<br> 
like life and death, with movement between colours<br> 
as between the forms, part shining and part rust. <br>
A shimmer where it faces sea, but at the back <br>
the strange dark shadow silhouette <br><br>

of energy shattering as it breaks<br>
and voices of the drowned written in white sky<br> 
cut-outs, like finger-holes in a flute. <br>
We creep in. We run our hands along the frilled<br> 
bronze edge, split and joined at the core<br>
like life-long partners betraying and forgiving.<br><br> 

I remember Plato’s cave, the screen, the fire <br>
and how he banned the artists for <br>
making appearances; making more than we can hear<br> 
or see or know. Invisible in this safe-seeming <br>
hollow shadow - echo, harmony and echo – <br>
we gaze out at a grey dim open sea<br><br>



and iron horizon splicing day from night <br>
like west from east. I think of Syria <br>
on the front page: flat-to-flat urban warfare<br> 
in Aleppo, where ‘ouds <br>
beyond the price of rubies <br>
have been made from the seventh century,<br><br>

and air attacks on Gaza. In the pub quiz <br>
on Saturday, in the snack bar, <br>
kitchen, cinema and bus, how quick <br>
the arguments flare up, even in England,<br> 
even if we’ve never been <br>
to what we call the middle of the east. We identify. <br><br>

We occupy the centre of the rift<br>
or what rift represents. This is our story, one bronze<br>
<em>étude pathétique</em>, split vertical and horizontal. <br> 
A multiple arabesque, steel fission and steel fusion<br>
like the goddess Harmonia, slim perfectionist <br>
surrounded by destruction, whose wedding gift,   <br><br>

a necklace, lays a curse on anyone who steals <br>
and wears it next. Harmony the hidden <br>
daughter, love-child of Aphrodite<br>
and Apollo’s enemy, the god of war.<br>
The lyre is the first thing soldiers smash <br>
before slicing up God’s perfect instrument, the larynx. <br><br>

                               <blockquote>*</blockquote>

Sing the microtonal range of the <em>maqam</em>:<br>
the sadness and pain of <em>Saba; Hijaz</em>, conjuring<br>
distant desert and our longing for it. Sing <br>
of the body, thighs and palms to slap, <br>
teeth and tongue to whistle through <br>
and the fragile, mucus-laden <em>vox humana</em>.<br><br>
 
Sing also of David’s harp, placed sideways <br>
on a mountain to catch the cinnamon wind <br>
blowing from golden rocks at dusk<br>
below the tower of Lebanon <br>
which looks towards Damascus.  <br> 
As we all do. That’s where the mind goes now<br><br>

on Suffolk pebble-shingle <br>
gleaming like highlights of <em>intarsia</em><br>
whose weavers set the floats of weft<br>
and warp at angles, to reflect <br>
light scatteredly, depending who you are<br>
and where you’re looking from.<br><br>

These chinking waves… Their draining <em>shump</em> <br>
roars in like shell-thuds over Al Fayhaa, <br>
Fragrant City, home to <em>rosa damascena </em><br>
and the heptatonic scale of a fretless lute; <br>
where sky, dark-pearled as strong <em>espresso</em>, burns<br> 
above the courtyard in Umayyad Mosque,  <br><br>

where children used to lick pistachio-starred ice-cream<br>
and now play Asking-for-Papers-of-Identity-<br>
at-Gun-Point, and old men with pewter urns <br>
strapped to their backs pour liquid liquorice, <br>
flavoured with tamarisk, in ribboned lollops <br>
the way a flat-edged stone <br><br>

skims the surface of smooth water. <br>
In the split-shell casque of psyche, <br>
in our dreams, wherever we’re looking from,<br> 
we <em>are</em> this middle east. Some chasm <br>
through the centre must be in and of us all. <br>
Creatures of relation and division, we belong <br><br>

in the tectonic clash <br>
of histories on common ground. The past <br>
is never where we left it, overrun <br>
by warring memories <br>
from a single rift-shot family of languages <br>
and faith. Filters we see each other through <br><br>
 

take on the greenish quality of shadow cast <br>
by glass. We set up frontiers<br>
to keep us safe and keep out the unknown. <br>
When sea floods in or Viking boats arrive<br>
chaos is come, we are broken into. What survives<br> 
is hope, like that run of burnished steps<br><br>

infused with cardamom, heart of the oldest <br>
city in the world. Rosewood sets of backgammon <br>
like puzzle-books inlaid with mother-of-pearl, <br>
blue gargoyle fish of the <em>souk</em> displayed <br>
beside a frayed formica stool, strings of hanging flip-flops<br>
and rolls of samite: glitter-fabrics that depend <br><br>

on optic interference like the play of light <br>
in Damascus Twist, that iron plait<br>
welded in carbon fire, whose laminate <br>
spiral surface, acid-bitten into waves, <br>
resembles damask. Valued formerly in sword-blades<br> 
and now in the barrels of guns.<br><br>

                     <blockquote>*</blockquote> 

Is making our defence against the dark? We break<br>
the line to make it, improvise a rhythm, string  <br>
silk gut over leather membrane  <br>
reverberating with folds. We shape<br>
a pilgrim shell, place it to face the storm, catch harmony <br>
from music of the spheres and dive in cold, as grey <br><br>

dawn breaks over greyer sea, like kingfishers  <br>
tracking the play of  iridescence.  However <br>
this pans out, this you and me, we can take delight<br> 
in form, in makeshift colours of the waves – <br>
silver, hyacinth and jade, here in the rain for all to see -<br> 
and meanings we have found in what the world has made. 

</p>

            
           




<p>The Immortalists<br>
(Charles Lindbergh, Dr. Alexis Carrel, and Their Daring Quest to Live Forever)<br>
By David M. Friedman<br>
HarperCollins Publishers<br>
ISBN: 978-0-06-052815-7<br>
ISBN-10: 0-06-052815-X</p>

<p>At Chicago’s International Museum of Surgical Science, I once attended an exhibit on the history of transplant surgery and was surprised to learn that Charles Lindbergh had, among his other triumphs, invented a perfusion pump--a device for cultivating organs and keeping them alive outside the body.</p>  

<p>Lindbergh? The man who won glory for his solo non-stop flight across the Atlantic in 1927? As I stepped out of the Museum into the crisp Lake Shore Drive air, I asked myself—what was the story behind his little-known but dramatic career change, from pioneer aviator to inventor of medical devices?</p>

<p>Lindy’s less-publicized talents were spotted by a French expatriate surgeon and scientist, Alexis Carrel, active in New York City during the post-WWI era. Carrel had a giant reputation and ego to match, justified by solid achievements in medical science (in the field of vascular suturing and antisepsis), but held back by primitive laboratory conditions at the Rockefeller Institute where he was on staff. Charles Lindbergh—an action man, skilled with machinery, tenacious—arrived on Carrel’s doorstep at just the right time. Carrel had even bigger plans for the future, and saw in Lindbergh the means of carrying them out. And so a quirky friendship began, a curious but satisfying blend of the Old World and the New—as well as an intriguing footnote to Jazz Age history.</p>

<p>These are details of Lindbergh’s life that the passage of time has airbrushed away; perhaps the most surprising is that this all-American hero came close to turning his back on the USA altogether. He had always had a strongly cosmopolitan outlook, if not quite in the sense that we understand the term today. Charles met his future bride in Mexico (few of Anne Morrow’s suitors could hope to compete with Lindbergh’s chosen mode of transportation for their first date—a spin in <em>The Spirit of St. Louis</em>). Wedding bells followed, and the first of many international trips—with Anne Morrow on hand as crew, not as spouse—got under way.</p>

<p>Lindbergh and Carrel favored the eugenicist theories then in vogue; and so it was regrettable, if perhaps not unexpected, that Lindy’s eyes should drift toward Nazi Germany as the 1930s wore on. Invited to review the burgeoning German aircraft industry, the impressionable Lindbergh was stunned by what he saw—sophisticated Luftwaffe technology, not <em>The Spirit of Saint Louis</em>! The older and somewhat wiser Carrel—who, as a WWI surgeon in France, had seen what German military hardware could do to the human body—entertained more misgivings.</p>

<p>Back home, Carrel’s and Lindbergh’s scientific researches had real-world implications, but both men were hunting bigger game. Organs that could be repaired and sustained in vitro could be replaced in the human body, with a dramatic impact on human longevity. Such life enhancements were to be reserved for a better race, and class, of person. </p>

<p>These supremacist beliefs, untethered from ethical considerations, led these two men down shadowy paths, from mice cages to experimentation on primates. Carrel’s flickers of conscience argued against testing on live humans, but Lindbergh was champing at the bit, strapping on gas masks in depressurization chambers (tests which the Nazis later performed on unwilling subjects). In the soul of Charles Lindbergh, puttering around laboratories, isolation and isolationism went hand in hand.</p>

<p>A full-blown relocation to Germany would have seemed the next logical step, and indeed, Lindbergh came close to renting an apartment for his family in Wannsee. He flirted with treason, accepting a Nazi cross. Something held him in check, and his lengthy European sojourn ultimately included time spent elsewhere, in England and Brittany. The serene and self-deluding Minnesotan at last returned to the United States to join the “America First” movement on home soil. For anybody else, this would have spelled a personal and moral tragedy on a truly epic scale.</p>

<p>Somehow, Lindbergh pulled up the nose cone just in time to avert disaster. Should his wife and co-pilot through life, Anne Morrow, perhaps take the credit? Alas, no—the author gleefully reprints her early diary’s starstruck burblings on Hitlerian resolve. We all know that parenthood, reputed to be a civilizing influence for many, failed to work its magic for our golden couple. Anne bore other children after the horror of the kidnapping of Charles Jr., abandoning them with surrogates during their overseas trips. These geographically and emotionally remote parents, reminiscent of Elizabeth II and the Duke of Edinburgh on visits to the Commonwealth, were distant figures to their children upon their return. Friedman reveals that Charles sired at least one and possibly several children out of wedlock. One big happy family.</p>

<p>Anne Morrow overcame her past to become a best-selling author (<em>Gift from the Sea</em>, 1955), and her writings became a fixture at weddings and funerals. A byword for goodness and benignity, Morrow’s distinctive spiritual outlook has resonated with generations of readers (the present reviewer shares with Morrow a love of the Maine island of North Haven). Charles, too, wrote; his combination of authorship and rugged outdoor pursuits was not unusual in the eras of Teddy Roosevelt and Ernest Hemingway, but is arguably less common today.</p>

<p>Does the author of “The Immortalists” successfully pilot his readers through this strange saga—or does he lose his passengers along the way? Most of the time, Friedman prepares the reader for the twists and turns in the Lindbergh saga, navigating a tiny biplane through one storm after another. The gyrations become harder to absorb as Lindbergh, the quasi traitor, joins as the US Air Force as a civilian advisor and sees illicit action in dogfights in the Pacific Theater, (perhaps) atoning for his record of appeasement (not a term the author uses). </p>

<p>People with engineering backgrounds are common in denialist movements (lunar or otherwise). And yet, in a further unexpected gesture of expiation, Lindbergh the Nazi sympathizer helps to open a concentration camp and recounts his experiences; what would today’s Holocaust deniers make of that? </p>

<p>The topsy-turvy changes keep coming. The 30s husband basking in a companionate marriage to fellow crew-member Anne Morrow soon loses faith in women pilots, and by the 50s he is a Father-Knows-Best figure, his embittered wife finding comfort in the arms of a physician; yet another man in a white coat coming to the rescue. The author does not say so, but conjugal competition over book sales and royalties may have been a contributing factor in the couple’s estrangement.</p>

<p>As the 1950s drew to their conclusion, CL’s life as a glorified mystery shopper for Pan American Airways lost its savor; and with the advent of the 60s, further change (not simply technological innovation) was in the air. The one-time racialist made friends with a tribe of Masai herdsmen on a trip to Africa, rethought his prior attitudes, and re-emerged as an environmentalist ambassador decades before Al Gore began purveying inconvenient truths.</p>

<p>Lindbergh reinvented himself—with his original reputation largely intact and many awkward moments overlooked; and it is a flaw of this book that the author fails to explain how this happened. As for Carrel, more must have been involved in the Frenchman’s departure from France than the author lets on; and Friedman cannot say—who can?—why this able though tragically flawed individual should be forgotten today. The author portrays the protagonists’ racialism and anti-Semitism as shocking and unexpected—and so they will be, for newcomers to the historical period. Yet they were common currency for the time; and many pillars of the Establishment—not least <em>The Times</em> of London and its editor Geoffrey Dawson—banged the gong for appeasement.</p>

<p>“The Immortalists” is a meditation on many major themes in American history: celebrity worship, a can-do attitude toward technology, reverence for doctors in white coats, immigrants who make a big splash in the USA, the changing role of women and the impact of two career couples. It disproves the notion that there are no second acts in American lives. It raises more questions than it answers, yet it is one of the finest books I have read in years and I can enthusiastically recommend it.
</p>




            
           




                  <p>Marcelo Maturana nació en Londres, Inglaterra, de padres chilenos, en 1955. Creció en Santiago, Chile. Habla español, inglés, italiano.</p>

<p>Estudió Antropología en la Universidad de Chile. Ha trabajado durante años como editor (en el sentido de <em>editor</em>, no de <em>publisher</em>), tanto free-lance como en editoriales. Fue editor literario de Alfaguara Chile desde fines de 1994 hasta 1998. Ha escrito crítica literaria en diversos diarios y revistas chilenos (un par de colaboraciones también en <em>Letras Libres</em> de Madrid), en algunos casos usando los seudónimos Vicente Montañés o Artemio Echegoyen. También ha sido articulista y columnista (literario y de temas libres) en diarios como <em>La Nación</em> de Santiago y <em>Las Últimas Noticias</em> (LUN) de Santiago, y en revistas como <em>La Bicicleta, Reseña, Rocinante</em>. Actualmente escribe una columna titulada "Nervio Óptico" en LUN, que firma como Vicente Montañés.</p>

<p>Ha sido premiado en varios concursos de cuentos.


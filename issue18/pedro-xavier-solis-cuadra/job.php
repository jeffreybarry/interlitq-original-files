<p><strong>Bipolaridad</strong></p>

<p>Hay días en que su mente amanece como una jaula abierta<br>
de la que brotan gorjeos de pájaros alzando sus alas<br>
revoloteando y piando sobre las copas altas de los árboles,<br>
libre jolgorio sin derrotero, sólo luz y canto y vuelo.<br><br>

Otros días su mente zozobra en lo profundo del corazón<br>
como un barco anegado en la obscuridad del lecho marino<br>
con un coro de fantasmas cantando en la noche sorda<br>
echado a pique, proa encallada, sin velas ni quilla ni ruta.</p>


<p><strong>Bipolar</strong></p>

<p>There are days when her mind rises like a cage sprung open<br>
twittering birds fluttering and chirping up and away<br>
spreading their wings over the tall tops of trees<br>
A triumphant free spree of light, song and flight<br><br>

Other days her mind broods at the bottom of her heart<br>
Like a ship floundering in the dark bed of the sea<br>
A chorus of phantoms singing in the mute night<br>
prow run aground, no sails no keel<br>
sinking, nowhere to go.



</p>


           




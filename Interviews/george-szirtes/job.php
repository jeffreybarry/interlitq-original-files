<p><em>Interlitq</em>: Could you tell us more about your Hungarian heritage and how it has shaped you.</p>

<p>GS: You ask about my Hungarian heritage? It is primarily language, I suppose, but there may be - sometimes I feel certain there is - a peculiar sense of history, of the kind of history Eliot describes in <em>The Waste Land</em>. Something of The Fire Sermon maybe but more this passage from Death by Water:</p>

<p>What is that sound high in the air <bR>
Murmur of maternal lamentation <br>
Who are those hooded hordes swarming<br> 
Over endless plains, stumbling in cracked earth <br>
Ringed by the flat horizon only <br>
What is the city over the mountains <Br>
Cracks and reforms and bursts in the violet air <br>
Falling towers <br>
Jerusalem Athens Alexandria <br>
Vienna London <br>
Unreal </p>

<p>The vulnerability of those plains to hooded hordes, and the fragility of those falling towers seem to be written on the genetic nerves. But that vulnerability is as much Jewish as Hungarian.</p>

<p>But it’s hard to know more than that. I do however know that passage strikes a deep chord in me and I ascribe that to the Hungarian - Jewish sensibility I seem to have inherited. Reading that list of cities at the end I automatically insert Budapest. The bats with baby-faces are there too. Maybe it is my mother’s Transylvanian roots reasserting themselves. Sounds comical and melodramatic doesn’t it? I see that myself but it doesn’t mean I don’t experience it. <bR>
Clearly I have been shaped by these instincts. They are lodged somewhere between memory and imagination but I have lived far longer in England so England too has shaped me. The trouble is I can never be quite certain that my shape has fully adapted itself to it. </p>




<p><em>Interlitq</em>: Have ever felt an outsider in England? How have you successfully wed your Hungarian and English identities?</p>


<p>GS:  England, to me, remains an uneasy pact between elegy and pastiche, between courtesy and bruise. Its ways and manners are navigable. Better still they are legible. At one level I still feel I am learning the language, gently prodding it to see what it does. </p>

<p>But of course I am an outsider and am treated as such. I don’t mean I am some kind of outcast or pariah: the kindness, decency and tolerance of my adopted country are striking. I have gratefully experienced it throughout my life. But that doesn’t mean I am <em>of</em> here: it means I <em>live</em> here, like a guest that has grown familar. </p>

<p>For example, you’ll find I am missing from the main anthologies. I don’t quite fit. I am, as one of radio producers put it, not really ‘useful’. I don’t exemplify or represent anything but myself and the world at large. The essayistic writing I am asked to do is inevitably about Hungary. Here I am more Hungarian than English. The opposite is true in Hungary.</p>

<p>Our age is obsessed with identities. What are you? Who are you? Where do you belong? Are you one of us? I can’t give firm answers to any of these questions. Sometimes I wonder why I should.</p>



<p><em>Interlitq</em>: How has your recent experience of illness shaped you? Would you go as far as to say that intimations of mortality have infused you with creative force?</p>

<p>GS: I did, it is true, have a quadruple bypass early in 2016. One day I was at home, the next in hospital. I had a few weird and slightly uncomfortable days and nights, then I came home. I felt tired and a little fragile but that didn’t last too long. Now I am back.</p>

<p>I went in determined to record everything in a notebook and did in fact do so but I don’t think I fully entertained the possibility of death. Maybe I did a little the night before the operation but I didn’t really think I would die (apparently there is a one in a hundred, maybe one in two hundred chance that someone might). I simply entertained the possibility in the way I might entertain the possibility of being blown up at an airport. In other words the possibility was remote and slightly fantastical. </p>

<p>After the operation I shared the ward with various others at different times but one man who was there throughout was suffering far more than I was. He had a lung complication. One night he had a very high temperature and began hallucinating. He was coughing at the same time. It was a horrible cough. In between, during the day, he would chat about anything. He was an intelligent man from an entirely different field of work from me. The night he suffered particularly badly, it flashed through my mind that we were, in effect, somewhere near the valley of the shadow of death. But even that wasn’t particularly traumatic. It was normal. Those were the conditions of life. It is brief and never as safe as we think. </p>

<p>I don’t think the experience has made me any wiser or any better equipped to talk about mortality. I haven’t walked through the valley of the shadow of death. It was a glance down into a place that is never quite out of view. Will it change the poems? I can’t tell. It isn’t something I felt tempted to write ‘about’. One would have to write out of it, not about it.</p>


<p><em>Interlitq</em>: How do you regard the current state of English poetry?</p>

<p>As to the current state of English poetry, I have just judged the Forward Prize along with four other judges. I think we are at a transition point. There are many new younger poets and most of those are women. Those younger women are writing with great confidence, a confidence that has given them energy. Out of the fifteen names on our three short-lists, eleven were women and all the winners were women. We weren’t bending over backwards to be dutiful or supportive. They were just very good. And indeed it was the second time in two years that all the winners were women. I don’t make anything particular of this except that we will be seeing more. </p>

<p>Regarding the range of poetry, it is wider than I ever remember it before. Let me correct that: the range has always been there but the extremes of that range are now to be found at or near the centre. There are, for example, far more prose poems. There is a far greater willingness to deliver fragments of discourse rather than to complete worked-through lyrical shapes. There is, in any case, a suspicion of lyric modes (see the acceptance, after decades, of someone like Denise Riley) while, especially in the case of some of the younger women, particularly the desire to raise personal experience to an intense exemplary level. There is also the ambition to produce big projects (see Vahni Capildeo and Luke Kennard etc), to explore something large by way of passages and digressions, to both de-centre and yet to provide what seems a central perception. There is irony, there is the new sincerity, there is the poetry of assumed presences and voices as absorbed via the internet and its publicity machines. Sam Riviere’s <em>81 Austerities</em> and Kim Kardashian’s <em>Marriage</em> are prime examples of this. And it is interesting that Rivier’s work, which would have been regarded as experimental ten years ago, is now published by Faber, and that Denise Riley is published by Picador. </p>

<p>Our age, as I have already remarked, is obsessed with identity. One way or the other, by presentation, by replication, by mask, or by rejection, the question of identity echoes throughout contemporary poetry.</p>



<p><em>Interlitq</em>: Is there any English poet you especially admire?</p>

<p>GS: I think the two contemporaries who have meant most to me would be Derek Mahon and James Fenton. Both can sing, both are troubled, both have a moral-aesthetic core, both speak to my condition. I think Alice Oswald is wonderful, quite head-explodingly wonderful, but she does not find me in my natural habitat. I have to go to her.</p>



<p><em>Interlitq</em>: How has your study of the visual arts shaped your writing?</p>

<p>My first book was full of poems originating in visual art, paintings chiefly. Then I wrote a good deal on photography and some on film, in so far as they intrigued me as media. I was also writing reviews and bits and pieces on painting and photography mostly for <em>Modern Painters</em>. My own career as a painter lasted until 1984 when translation took all my spare time. I was working as a full-time teacher of art and art history and painting in this or that odd hour was frustrating. The poems in the meantime tended to work through visual imagery, seeing concrete situations, whether real or imaginary, and trying to listen to the noise they made. The deepest, most complex feelings were mediated as images but in the form of words.</p>


<p><em>Interlitq</em>: You are considered a foremost literary translator from Hungarian to English---how do you perceive the role of the translator?</p>

<p>GS: The role of the translator, as far as I am concerned, is to make it possible for readers in the receiving language to experience as much of the original work as possible. I mean the experience as the translator experiences it. That experience is transformed in the act of translation, so the translation becomes a work in itself; not an independent work, a dependent work, but a work all the same. I spent many years translating poetry, then fiction tended to take over. Both  were good for me. I learned the dimensions of other voices and some refreshed and modified mine. I’d recommend any writer to translate. It offers another experience of your own language, approaching it from the outside. I’d like to translate more poetry again. I am a poet after all.</p>

<p><em>Interlitq</em>: Could you tell our readers what you are working on at the moment?</p>

<p>GS: Since the publication of my <em>New and Collected Poems</em> in 2008, I have taken on ever more different projects involving ever more ways of writing. I wanted - and still do want - to see what else I could do, what my own store of voices contained. So both <em>The Burning of the Books</em> (2009) and<em> Bad Machine</em> (2013) contain various kinds of poems that I had not written before. I have also been writing short texts in various sets. I have published some five chapbooks of those. This year has seen the publication of a collaboration, <em>Fifty-Six</em> with Carol Watts, a poet very different from me. Also, my own new book out this October, <em>Mapping the Delta</em>. In the pipeline: a third book of poetry for children, a book of prose fables, a prose book about my mother, and a book of miniature fantasies (again in prose) about my father. In terms of poetry, I have been working with haiku and the cinquain as well as with verse on politics. Oh, and a Selected Poems in Chinese translation.</p>

<p><em>Interlitq</em>: On another note, were you surprised that Bob Dylan was awarded the Nobel Prize for Literature?</p>

<p>I don’t think Bob Dylan winning the Nobel Prize for Literature is either the end of the world or the birth of a new one. The case for Dylan is that he introduced a much denser and more ambitious lyric into popular song and that those lyrics had a profound effect on more than one generation, an effect that acted upon literature too to some degree through its association with the Beat Poets. Dylan formed sensibilities and opened the door to poetry for an essentially pop or folk based audience. Dylan’s own written books are fine but scarcely Nobel material.</p> 

<p>Since the Nobel has always had a political aspect, it is also possible the awarding it to Dylan was a way of supporting the progressive liberal side of America against Trump. It’s a long shot but not impossible.</p>

<p>The argument against Dylan is that the Nobel Prize should go to the finest author of books and that in those terms Dylan is nowhere and that the contenders should have been those tipped by the bookies plus a few others. The truth is that the issue of who won the prize is always really about who should have won the prize but did not.</p>

<p>I am not inclined to concern myself too long with this or any other prize. Having won a few, I am aware how much it is a matter of chance. Prizes are not the final judgment on anything. I also think Dylan is likely to be a one-off and, if that is the case, I am perfectly happy for him to have won.</p>

<p><em>Interlitq</em>: By the time this interview is published in <em>Interlitq</em>, you will be in China. Could you tell us more about your relationship with that country.</p>

<p>GS: We - that is to say I and my artist wife, Clarissa (who was herself born in China to British parents), spent two weeks in Shanghai in 2011 with the poet Pascale Petit. That was the result of an invitation involving the Writers Centre Norwich and Fudan University but also involving the Chinese-born poet Yang Lian and a poet in China itself, Xiayo Kaiyu. The idea was that there would be some mutual translation and that we would work with Fudan’s own course in Creative Writing. That, like the current venture, was intended to be a month-long residence but circumstances changed before our departure and the residency was reduced to a fortnight.</p>

<p>Three years later, I was invited to be writer-in-residence at NTU Singapore and we spent a month there. The current Chinese invitation is really a re-invitation from last year when I had to cancel a few days before the flight because of my heart condition. </p>

<p>I try to say yes to all invitations when possible. Life is more interesting that way. I had no particular connection with China (nor to many of the other places I have visited) but it is, of course, as they say, a fascinating country. My impressions of Shanghai were very complex: on the one hand a vast post-modern, western-looking city, rapidly expanding with a lot of money to spare, on the other a city where very poor internal migrant workers were being bussed in daily or accommodated in basic rooms or dormitories. Further out of Shanghai, the smooth bullet train glided into less modern but still attractive smaller towns and villages that looked poor in parts. Had we gone even further west we would have met genuinely abject poverty, as a Chinese acquaintance told us. In Shanghai we were taken to museums where thousands of years of Chinese culture were presented to us with great pride while, at the airport, copies of <em>China Daily</em> in English still read like Mao-period party sheets complete with speeches and details of grain production. China bans practically every form of social media including GMail and all important news sources from the west. It exercises iron control over access to incoming information but the UEA campus was full of Chinese students last time I looked. </p>

<p>Hence the interest.</p>












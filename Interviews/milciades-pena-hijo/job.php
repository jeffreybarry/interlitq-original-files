<p><strong>En la búsqueda de una participación protagónica</strong></p>


<p><strong>Yamila Musa entrevista a Milcíades Peña (hijo)</strong></p>


<p><em>Milcíades Peña propone una mirada de cercanía en la construcción política de los sectores populares y un perfil diferente de lo que es ser un representante social. Activo impulsor de la Ley de Comunas, lleva adelante el primer canal de televisión villero del país y fue el hombre que torció el rumbo de la tragedia de Cromañón. </em></p>

<p><strong>Tags: popular- Ley de Comunas- Ley de Urbanización- Ley de Deporte- Urbana Te Ve- Cromañón- sistema político – cercanía – Trotskismo – Peronismo</strong></p>


<p>Llegue antes de tiempo, me senté junto a la ventana del café frente a la plaza Congreso, a una cuadra de la Asociación de Madres de Plaza de Mayo, y con la vista perdida entre las palomas que revoloteaban por la vereda pensaba, y a medida que pasaban los minutos me iban surgiendo muchos interrogantes. <br>
Y en esa ansiada espera, al fin llegó Milcíades. Un hombre luchador, un hombre con una mirada profunda de la sociedad e hijo de una de las mentes más brillantes de la historia argentina. </p>




<p><strong>Fuiste uno de los principales precursores y promotores de la Ley de Comunas ¿Cómo ves su aplicación por parte del actual gobierno porteño?</strong></p> 


<p>Mal, muy lejos de lo que soñaron los estatuyentes y muy lejos de lo que soñó el primer movimiento comunero. Fue una ley democráticamente trabajada,  recorriendo los barrios, hablando con los vecinos, y construida sobre el concepto de <strong>gestión de cercanía</strong> que fue tomada en base a experiencias internacionales, que tiene que ver con una mirada distinta de la sociedad y de las dificultades que tienen las grandes urbes. </p>


<p>La política, la administración, la gestión, es algo que  ha quedado lejos, se ha perdido el contacto, y este era el concepto central, el  disparador, pero para fortalecer eso y que efectivamente hubiera una cuestión de <strong>cercanía</strong>, el cambio de lógica que se buscaba era fomentar el <strong>empoderamiento social</strong>, la participación protagónica de los vecinos, pero el sistema político en general no cree en eso. <br> 
El sistema no cree mucho en esto de abrir el juego de una “<strong>participación protagónica</strong>”. Una cosa es participar como espectador de algo, pero ser protagonista es otra cosa, sobre todo en las cuestiones sociales, políticas, y a eso se le escapa mucho.</p>


<p>“El <strong>sistema político</strong> es una suerte de cofradía sistémica que se retroalimenta, y esto de abrir la participación no gusta”, dice Milcíades Peña, mientras continúa relatando las pecualiares circunstancias histórico políticas que se dieron en la Legislatura porteña en el momento de sanción de esta esperada Ley de Comunas.</p>


<p>“Uno de los puntos de debate fue la cuestión presupuestaria, qué presupuestos iban a manejar las comunas. Muy lindo hablar de participación, descentralización, gestión de cercanías, pero si no se tienen los recursos económicos para administrar esa comunidad de referencia, a  las palabras se las lleva el viento”.


<p>Cuando se discutió  el artículo que  tiene que ver con los <strong>presupuestos</strong>, se dio un debate entre los pisos y los techos. El Macrismo planteaba un techo presupuestario, y nosotros planteábamos un piso presupuestario. Por supuesto que ganó la postura de la derecha. Así, se obtuvo un techo de hasta el 3% del presupuesto destinado a la descentralización de todos los barrios de la ciudad, y después todo el proceso se fue dando parcialmente. </p>


<p>La verdad que la <strong>aplicación</strong>, yendo al disparador, está muy lejos de ese espíritu y mantiene una mirada absolutamente centralista de lo que es el tema de las comunas. Se asemeja mucho más a lo que fue en su origen, este concepto de desconcentración administrativa que al concepto de centralización política, que es el interesante. </p>


<p>La <strong>Junta Comunal</strong> se reúne discrecionalmente. Los Consejos Consultivos comunales son el lugar de participación vecinal, absolutamente horizontal, tienen la función de consultivo, de proponer a la Junta Comunal distintas acciones políticas, tareas, prioridades, debates sobre el presupuesto de las comunas. No son vinculantes las decisiones que en el Consejo Consultivo se tomen, pero sí son de respuesta obligatoria por parte de la Juntas Comunales, aunque en la realidad nada de eso sucede. </p>


<p>El <strong>Macrismo</strong> ganó en todas las comunas, tiene la mayoría absoluta, entonces hacen lo que quieren, la participación de la oposición es ínfima, y el gobierno lleva adelante una cantidad de tareas en los barrios desde la administración central, pasando por encima de lo que la Ley dice, no respetando la Constitución ni la Ley, e ignorando absolutamente todas las normativas. </p>


<p>Por  ejemplo, participo mucho en la <strong>comuna</strong> N° 6 y hace poco fue aprobada para esa jurisdicción  la tarea de refacción de los puentes sobre las vías. Se decidió arreglarlos y armar canteros colgantes con plantitas; el embellecimiento de cuatro puentes que salen millones cada uno, una obra no absolutamente prioritaria y tremendamente cara,  que excede el presupuesto de contratación directa. </p>


<p>Las contrataciones no pueden superar los 5 millones de pesos, porque si lo superan se debe llamar a licitación pública, con los controles correspondientes. ¿Qué hicieron? Hay cuatro puentes con dos cuadras de diferencia entre cada uno, se contrató por separado a la misma empresa, son cuatro obras distintas en las que se invirtieron 8 millones de pesos  evitando así ir a la licitación pública.</p>


<p><strong>Larreta o algún funcionario</strong> va a los barrios, se reúne con los vecinos convocados desde la administración central, informa lo que se va a hacer, y en ese contexto los vecinos dan alguna idea por fuera de lo que es la Junta Comunal y por fuera de lo que es el Consejo Consultivo que es el órgano fundamental. </p>


<p>Otras situaciones de la Ley de Comunas es considerar  cómo se distribuye el presupuesto hoy en las 15 comunas ¿Todas son iguales? ¿Todas tienen las mismas necesidades? No, <strong>estamos en una ciudad desequilibrada, inequitativa</strong>, y nosotros logramos incluir en la Ley que el presupuesto se distribuyera en base a indicadores sociales, indicadores que había que construir, y nunca se construyeron. Era una función del órgano de coordinación intercomunal, entonces ¿con qué criterios se distribuye la plata?, con un criterio absolutamente discrecional y no de justicia y equidad social. </p>


<p>La Ley es un mamarracho, la están haciendo fracasar. El Macrismo lo ha tomado a esto como un gasto político, y la gran mayoría del <strong>sistema político descree de esto</strong> como herramienta de transformación verdadera, profunda. Entonces, tampoco se da una pelea a fondo para que se respete la ley. Si el gobierno creyera en la descentralización la difundiría  y la promovería por todos los medios. Hoy los vecinos no saben del poder que tienen.</p>
 
<p><strong>Fuiste inventor de varias leyes en la Legislatura porteña sobre todo en las relativas al deporte. Hoy se están viviendo muchos problemas con embargos y cierres de clubes de barrio. ¿Cuál es tu opinión al respecto, cuál es el rol que deben ocupar los legisladores y el gobierno para frenar la desaparición de estos espacios? </strong></p>


<p>Los clubes de barrio forman un lugar central en la planificación social, política y cultural de la ciudad, y lo que sucede es que son víctimas de las políticas económicas de turno. Siempre han tenido problemas, básicamente porque <strong>nunca fueron visualizados</strong> en el marco de la planificación estratégica de las políticas sociales, culturales y educativas que involucran al deporte. </p>


<p>Si no se tiene una planificación y una mirada estratégica de la ciudad, es muy difícil que se logren incluir cada una de las herramientas potenciales que se poseen o con las que cuentan. Los clubes están tomados como algo pintoresco, como algo necesario, pero sin planificación seria. Las <strong>políticas económicas</strong> que viene tomando el Macrismo son <strong>devastadoras</strong> para esas entidades.</p>


<p>Entonces insisto con esto, que <strong>no existe una planificación estratégica</strong> y para el tema de los clubes es fundamental, no alcanza con decir que se otorgan más subsidios ¿Qué hacemos con los clubes? Si hoy todos los clubes de barrio cerraran sus puertas ¿El Estado estaría en condiciones de brindar el servicio que esos clubes brindan? La porción de la sociedad que concurre a los clubes para desarrollar diversas actividades es importantísima ¿Cuál es la capacidad instalada que tiene el Estado para dar respuesta a eso? ¿Tienen esa capacidad? </p>


<p>Los clubes tienen que formar parte de la estrategia de la planificación pública, porque están ocupándose de algo que el Estado no está en condiciones de brindar, siendo que nuestra Constitución habla del deporte como un derecho. Si no está esa mirada, es muy difícil que las políticas sean efectivas para el conjunto de la comunidad. </p>


<p>Al no planificar se están <strong>desperdiciando muchos recursos económicos, humanos y técnicos</strong>, porque la poca plata que el Estado pone en los clubes no tiene un objetivo estratégico. </p>


<p>Entonces el debate es ¿Qué hacemos con el deporte? ¿Para qué queremos el deporte en la ciudad? El <strong>Macrismo tiene la mirada de que el deporte es una fuente de negocios</strong>,  a tal punto que la Secretaría de Deporte está bajo la órbita del Ministerio de Desarrollo Económico.</p>


<p>La derecha tiene un proyecto general de <strong>achicamiento, destrucción, ciudad para pocos</strong>, por eso le apuesta a los desarrolladores inmobiliarios, a los negocios, al aumento del valor de la tierra, al aumento de los costos que vienen enganchados a partir de esa definición. </p>


<p><strong>Jamás se ha logrado articular</strong> a los polideportivos con los clubes y canalizar el ingreso de la gente. El polideportivo tiene que estar abierto indiscriminadamente a todos los vecinos, derivar a los vecinos allí, que sea un lugar más de desarrollo deportivo, pero  la puerta de entrada a la práctica del deporte debe ser el club de barrio. </p>


<p>Entonces, hay que fortalecer una organización desde la sociedad civil que involucre la planificación para la detección de talentos, una apuesta de verdad al deporte amateur, de alto rendimiento, y en base a esa definición aplicar los recursos. </p>


<p><strong>Vos siempre estuviste relacionado con los medios comunitarios. El proyecto de Urbana TeVé surgió a través de la Ley de Medios ¿Por qué se eligió llamar así al canal y en qué posición se encuentra este proyecto luego del cambio de gobierno?
</strong></p>

<p>Urbana empieza a transmitir en marzo del 2010, es hija de la <strong>Ley de Medios</strong> de la democracia, y el nombre tiene que ver con que en ese momento, año 2008 ó 2009 se venía discutiendo en la Legislatura de la Ciudad el proyecto de <strong>Ley de Urbanización</strong>.</p>


<p>La idea de armar el canal surge por un compañero nuestro que vive en el barrio, Juan, un viejo dirigente que en principio quería poner una radio y terminó con la idea de un canal de televisión. Yo no tenía idea, había hecho algo en tele, en gráfica, pero lo que más había hecho era radio. Entonces sumamos a otro compañero que sí tenía experiencia, ya que había estado en uno de los primeros canales de <strong>TV comunitaria</strong>.  Nos lanzamos de aventureros totales entendiendo que había una temática en el barrio muy fuerte que era la de la urbanización, y que era imprescindible que para que eso avanzara, la información debía circular y socializarse. </p>


<p>El nombre surge por dos conceptos, uno en apoyo a la Ley de Urbanización del barrio, y el otro tiene que ver con el barrio Padre Mugica, que forma parte de la traza urbana de la ciudad. Por eso Urbana TeVé, porque el barrio, la villa, forma parte de la ciudad, <strong>no es un <em>ghetto</em></strong>, y hay que plantear la urbanización como respeto a la idea cultural y social del barrio. Esto es un debate de casi 10 años.</p>


<p>Me ronda mucho lo de la cuestión del <strong>protagonismo</strong>,  desde hace muchos años pienso cómo romper el concepto de participación pasiva para transformarla en participación activa. Cristina Kirchner en este último tiempo empezó a hablar mucho del empoderamiento, y en la política yo adscribo a esa idea ya que el individuo se conceptualiza como parte de un todo y su acción debe ser protagónica,  y el Estado debe brindarle esa posibilidad.</p>


<p>Nosotros, hasta hace tres meses teníamos más de veinte personas trabajando en el canal, hoy quedamos siete  porque es imposible sostenerlo, y eso que administramos los recursos al máximo  y  cobramos  muy poco. Hasta hace dos meses pudimos tener dos móviles en la calle, un periodista, un productor y un camarógrafo,  cubrían en el exterior y en el barrio en dos turnos, y ahora no los tenemos más. Nos quedamos sin editores, priorizamos a los compañeros del barrio que se capacitaron con nosotros, y eso hizo que disminuyera la calidad. </p>


<p>Este año se cortaron las capacitaciones, se cortó la articulación con el organismo del Estado y <strong>no hubo plata</strong>. Recién ahora, después de muchos meses de lucha, el gobierno convocó a nuevos concursos, los llamados Fomeca, que es un presupuesto que lo fija la Ley y son destinados al sector sin fines de lucro, y esa plata está, hay que ejecutarla a través de concursos. </p>




<p><strong>¿Cuál es la situación de las frecuencias otorgadas?</strong></p>


<p>Nos otorgaron una frecuencia que está ocupada ilegalmente por <strong>Artear, Canal 13</strong>. Ellos son <strong>“ocupas” de una frecuencia</strong>, ya que no realizaron concurso ni tienen licencia, en cambio nosotros tenemos el orgullo de decir junto con Barricada que somos los primeros en la historia en tener un canal de televisión comunitario. </p>


<p>Pero ya pasó un año y no pudimos hacer uso de nuestra licencia, no pudimos avanzar porque no tenemos la habilitación definitiva. Hace unos días, nos comunicaron que tomaron la decisión de <strong>cambiarnos de frecuencia</strong>, porque si canal 13 se va, tienen que hacer una mudanza general de todos los canales, que era lo que estaba previsto en la gestión anterior. </p>


<p>Se descartó  y nos asignaron una nueva frecuencia. Habíamos concursado por la 33 y nos van a dar la 32 que está vacía. Supuestamente en abril del año próximo deberíamos estar<strong> transmitiendo para toda la ciudad</strong> de Buenos Aires, lo cual es un año y medio de demora, con respecto al tiempo que se había estipulado. </p>


<p><strong>Aparte del canal ¿estás trabajando en algún otro proyecto? ¿Volverías a la Legislatura?</strong></p>


<p><strong>Sí y no</strong>. Hace tiempo atrás no se me hubiera ocurrido, estaba con la cabeza en el canal, no me atraía ese lugar, ya que después de la experiencia de Cromañón quedé muy herido, quedé muy golpeado.</p>


<p>Pero ahora, si se diera la posibilidad, <strong>no la descartaría</strong>. Soy un agradecido de haber podido estar en ese momento histórico en la Legislatura porque fui útil, si yo no hubiera estado ahí en ese momento la lucha de Cromañón hubiera tenido otro final, no lo digo de manera vanidosa, <strong>fui útil para hacer justicia</strong>. Los lugares en la política si uno tiene convicciones, principios, ideales, hay que ocuparlos porque forman parte del sistema. </p>


<p>Llegamos a unos de los temas que más ansiaba escuchar de Milcíades en esta entrevista, tema profundamente doloroso, alguien que vivió una de las peores masacres humanas de la historia de nuestro país desde muy cerca, y aún pasada más de una década nos planteamos si aún seguimos siendo víctimas de mentiras y verdades ocultas ¿qué fue lo que pasó verdaderamente con Cromañón? ¿Importan más los intereses económicos que las vidas de un montón de pibes? ¿Y la justicia qué rol ocupa? ¿Cómo afrontamos este tipo de situaciones como sociedad? </p>






<p><strong>Cromañón: ¿Desde una mirada qué cambió el destino?</strong></p> 


<p><strong>¿Cuál es tu sensación hoy, 12 años después de la tragedia de Cromañón? </strong></p>


<p>Me quedó un sabor agridulce, siento <strong>frustración</strong>. Se deberían debatir cosas más profundas a partir de eso que nos pasó como sociedad, y desde la política se hicieron esfuerzos fenomenales para evitar que eso sucediera. Se bastardeó el tema, se politizó en un mal sentido, y no pudimos discutir de las cosas más filosóficas de la vida entre los jóvenes. </p>


<p>En aquellos días yo tenía desesperación de ir a hablar con los pibes, les pedí a mis compañeros que armaran reuniones, recorrí centros de estudiantes, cantidades de escuelas dando charlas para hablar del tema desde distintas aristas. </p>


<p>La mayoría de los pibes que murieron en  Cromañón fue porque volvieron a rescatar a sus amigos, una carga de solidaridad muy fuerte y de eso no se habló, y no se habló a fondo de lo que significa el <strong>individualismo</strong>. Una construcción social del negocio sobre lo que había que debatir, era un gran disparador para discutir lo que es el capitalismo, a qué lugares te llevan esas conductas. </p>


<p>Nos quedamos en la cuestión política que es lo importante, pero <strong>nadie se animó a ir a fondo</strong>, a discutir el sistema: ¿Cómo nos divertimos? ¿Cuál es el rol del Estado? Ese fue mi eje en toda la investigación de Cromañón.</p>


<p>En esos años, <strong>Bergoglio</strong>,  le daba misa a los familiares de las victimas demostrando una actitud muy solidaria; rescato una frase muy certera que refiere a eso: “Es una ciudad que no ha llorado lo suficiente”. No soy creyente, soy anticlerical, pero él sí habló, y hoy soy absolutamente papista. El Papa es la figura más importante en el mundo, dice las cosas más trascendentes, <strong>está yendo al corazón del capitalismo a cuestionar la esencia</strong>.</p> 


<p>Doce años después no pusimos en cuestión el rol del Estado, y al debate no lo logramos porque <strong>el sistema logró reproducirse</strong>, los medios trabajaron para eso, para la superficialidad, para la banalización de la temática. </p>


<p><strong>¿Actualmente tenes contacto con los sobrevivientes y las familias?</strong></p>


<p>Si, los veo, y voy todos los fines de año, pero hoy <strong>se fracturó mucho el movimiento</strong>, muchos fueron cooptados por el sistema. De movida Ibarra nomás intentó sacar del medio a algunos padres, Macri a otros, Aníbal Fernández a otros en su momento, hubo muchas miserias.<br>
Es una barbaridad que los músicos estén presos y que Ibarra este libre, se trabajó mucho en la fractura, y en esas cosas no se pudo avanzar. </p>


<p>Cuando me fui a reunir el primer día con los de <strong>Callejeros</strong>, les dije que la ecuación era simple, a mayor responsabilidad de ustedes menor responsabilidad de la política, y el sistema va a hacer lo posible para que la responsabilidad sea de ustedes, y así fue. Por eso, lograr la destitución de Ibarra fue un paso fenomenal, histórico, muy doloroso para  mí  porque yo formaba parte de ese espacio político, por más diferencia que  tuviéramos eran mis compañeros, mi gobierno. </p>


<p><strong>¿Y ahora cuál es tu relación con Ibarra?</strong></p>


<p><strong>Nunca fui amigo</strong>, estábamos forjando una relación justamente en esos días previos al Cromañón, estábamos en una búsqueda de articular y trabajar juntos, un proceso de acercamiento político, y para mí, desde lo personal, eso fue un desastre. </p>


<p>Si yo no hubiera actuado como lo hice,  hoy tendría una carrera política asegurada, pero humanamente no hubiera podido vivir conociendo la verdad, teniendo la información ¿Cómo haces? Te convertís en un miserable.</p>


<p>Hubo y <strong>corrió mucha plata</strong>, hubo gente que recibió mucho dinero para cambiar votos. Y son cosas que tengo denunciadas en la justicia, no es que las estoy diciendo porque sí. Tengo todas las pruebas, conozco el monto de dinero que cobró cada uno para cambiar todo, personalmente recibí muchas propuestas y la más importante era la de mi futuro político. </p>


<p><strong>Pero, quedó demostrado tu compromiso con la gente y tu calidad humana como político.</strong></p>


<p>Sólo en los más cercanos. Si buscas en la historia de los familiares y demás, hay mucha gente que no hizo nada, que tuvo posiciones ambiguas, que después se subieron al reclamo, sobre todo de la izquierda. Yo <strong>me jugué el pellejo</strong> y lo digo con la mayor humildad que pueda decirlo, si yo no impulsaba la investigación llevándola adelante porque formaba parte de esa fuerza política,  hubiese sido distinto el final. </p>


<p><strong>Si no hacía eso te juro y te aseguro que no pasaba nada</strong>, el Macrismo no quería saber nada, no iba a profundizar. Los tipos tenían acuerdos y negocios con Ibarra; algunos que estaban en la Comisión Investigadora eran dobles agentes y eso no está en la historia escrita. </p>


<p>En <em>Clarín</em> yo no figuraba, estaba prohibido, en <em>Página12</em>  tampoco,  <em>La Nación</em> jugaba para potenciar a los dirigentes del Macrismo. Yo puse la cara pero laburé con mi grupo de compañeros que nos cargamos esto al hombro, y <strong>creo que no hay un reconocimiento</strong>.</p> 


<p>Luego, el Kirchnerismo logró meter la discusión desde el rol que debía cumplir el Estado, pero era muy autorreferencial, todos nuestros  discursos en esa época eran sobre la batalla cultural, pero para  mí se abrió muy claramente a partir de  Cromañón. En algo tan dramático y hondo como Cromañón es donde se ven los <strong>principios de verdad</strong> y no se pueden priorizar las necesidades de la política.</p>


<p><strong>Milcíades Peña padre: del trotskismo al peronismo</strong></p>


<p>Mi padre murió cuando yo era muy pequeño, antes de que pudiera conocer como pensaba,  pero <strong>dejó un legado intelectual y afectivo enorme</strong>.<bR> 
Sin embargo, Milcíades hijo se siente atravesado por las contradicciones propias de su generación y de un pensamiento político diverso a la hora de discutir intelectualmente con su progenitor.</p>


<p>Mi viejo fue un tipo brillante y es imprescindible leerlo, pero su crítica y posición con el peronismo está muy atravesada por una mirada eurocentrista, que es lo que tiene el marxismo.</p>


<p>Creo que los movimientos nacionales son movimientos <strong>populares</strong>,  y esas son las revoluciones, esa es la Izquierda. El populismo es una mirada eurocentrista y el marxismo también, pero los fenómenos estos no se terminan de construir. Además, en el pensamiento  Marxista, no existe una relación con América, en esa época lo tocó muy de refilón y a mi viejo le faltó eso con el peronismo. </p>


<p>También está muy <strong>atravesado por la época</strong>, por la pos guerra, me imagino yo. Entender al Peronismo atravesado por el Marxismo, es una cuestión que me he  planteado muchas veces. También hay que saber leerlo, porque en algunas ocasiones desde el Trotskismo se le ha dado una interpretación que no  es cien por ciento correcta hacia el peronismo, pero sí fuertemente anti personalista.</p>


<p>“Era un tipo que escribía con diez pseudónimos y no sé qué hubiera pasado si hubiera seguido con vida, como hubiera evolucionado esa cabeza. Su análisis fue brillante e imprescindible, y <strong>no le faltó razón en la crítica que le hizo al Peronismo</strong>. Él fue un tipo original, distinto de la historia, fue un genio, imagináte que a su obra la escribió entre los 18 y 24 años y a los 4 años ya hablaba inglés”.</p>








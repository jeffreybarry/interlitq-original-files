<p><strong>El camino de la integración</strong></p>


<p><em>Patricio Leone es el psicólogo creador de la Fan Page “Psicólogas y Psicólogos en Argentina”, la comunidad más importante del rubro en el país. Los debates en torno a las nuevas tecnologías, el rol de la Psicología en la política y el nacimiento de los nuevos espacios para el ejercicio de la profesión de la mano de otras disciplinas y corrientes de pensamiento. ¿Se puede hacer psicología por fuera de los paradigmas?</em><br><br></p>


<p><strong>Tags: psicólogas- psicólogos- argentina - facebook - DIAFOS - diversidad - trotskista - política- otras corrientes</strong><br><Br></p>




<p><strong>Creaste la red social de psicólogos más importante de la historia argentina. ¿Cómo surge esta iniciativa y cómo está compuesta?</strong></p>


<p><strong>“Psicólogas y Psicólogos en Argentina”</strong> fue un accidente porque nació de una desgracia muy profunda que fue la muerte de la hija de un amigo, quien me pidió que ingrese al grupo de las amigas de la hija para que publique fotos,  y así me cree un perfil en facebook y empezó una especie de búsqueda de Psicología. </p>


<p>Surgió la idea de armar el grupo para <strong>debatir hacia donde iba la profesión</strong>. Soy egresado de la UBA en el año ‘89, y en esa época si no eras Psicoanalista o Lacaniano, no existía otra posibilidad. Pero yo si veía que había otras corrientes, que los pacientes me rechazaban interpretaciones, venían con demandas raras de un vacío existencial. </p>


<p>Hoy el paciente viene urgido porque tiene colon irritable, psoriasis, y tiene que curarse en 15 días. Entonces, eso desde el <strong>Psicoanálisis</strong> es muy difícil de cumplir, y entre a interrogarme mucho y a debatir con mi mujer que es Lacaniana. </p>


<p>Mi objetivo era juntar 200 personas para debatir, <strong>ahora somos 74.000</strong>. El grupo tiene una virtud grande que es el número, porque nunca hubo un colectivo tan grande en la historia de la psicología argentina. Hubo algo parecido creado por <strong>Jorge Garabenta</strong>, un colega amigo, que tuvo un foro vía mail que alcanzó las 10.000 personas cuando todavía no existían las redes sociales. </p> 


<p><strong>El 70% de los miembros del grupo son psicólogos</strong> (unos 45.000) y 60.000 somos los activos, representa las tres cuartas partes de los psicólogos de Argentina del país. <strong> “Psicólogas y Psicólogos en Argentina”</strong> nos hizo descubrir cuestiones de la profesión: que nos cuesta dominar nuestras emociones, que discutimos y nos enfervorizamos y nos convertimos en monstruos, por ejemplo. </p>


<p>La gente cree que el psicólogo es una persona imperturbable que tiene la vida comprada, y el grupo nos permitió comprobar que no es así. También permitió el crecimiento profesional y laboral de muchos colegas, y eso lo defiendo a muerte, y también tiene muchos detractores.


<p>El grupo permite una mayor difusión de la psicología, y hace 2 o 3 años empezó a ocurrir un fenómeno nuevo: se empezaron a sumar <strong>estudiantes</strong> y eso me gusta, porque cuando yo fui estudiante me hubiera encantado tener un grupo y escuchar debatir a los colegas. Ahora haces click y tenes un debate con 200 comentarios. </p>


<p>El 20% de los miembros del grupo son estudiantes, y el 10% son personas que no son psicólogos ni gente de psicología, lo cual hubo discusión con ese tema cuando empezaron a incorporarse, si debía permitir que se sumarán, pero eso no se puede saber, si sos psicóloga o un sicario bosnio, no tengo forma de comprobarlo, <em>internet</em> es eso, el anonimato. </p>


<p>Las acciones son las que determinan si permanecen o se van, y muchas veces este 10% enriquece mucho porque son madres de niños autistas, profesores que vienen a buscar información porque tienen un alumno con trastornos límites de la personalidad, gente que viene a pedir atención psicológica y de manera perentoria y desgarradora, hemos tenido personas que han entrado diciéndonos que somos la última oportunidad, que se va a suicidar, y las intervenciones fueron maravillosas. Pudieron contenerlos, hablar, y siempre derivarlos vía mensaje privado para evitar la exposición, que haya comentarios que puedan inducir cierto enojo, desánimo. </p>


<p>Luego, esas personas aparecen y te dicen que le salvamos la vida, y eso me pone la piel de gallina. Una vez muchos colegas se enojaron con una situación de suicidio porque fue la estudiante la que intervino, pero ella la salvó, la contuvo. Como decía Freud: <em>no hay nada más tranquilizador que unas pocas palabras bondadosas</em>. El grupo tiene eso, y tiene cosas negativas como la exposición, ya que nos lee mucha gente. </p>


<p><strong>¿Cuál es la relación de la Psicología con las nuevas tecnologías?</strong></p>


<p>Es una herramienta maravillosa que nos interroga a los psicólogos hacia dónde vamos, hacia dónde fue nuestra profesión. Por ejemplo, el secreto del analista no existe más, ahora todo es público y <strong>Facebook</strong> es público. ¿Y qué hago con eso, lo rechazo o lo tomo? Si lo rechazo estoy perdido porque tengo que rechazar constantemente elementos que el paciente mira, entonces lo incorporo y que esos elementos que mira me motoricen el proceso terapéutico de una forma o de otra. </p>


<p>Si un paciente sabe que me gusta correr  o me vio correr, utilizo la maratón como analogía o simbolismo para cuando un paciente está desfalleciente en su vida, cuando siente que no puede más, y le cuento que a mí también me pasó, que en el 30 km. pensé que me moría. Hay un montón de herramientas que te pueden servir. </p>


<p>Soy de la época en la que se discutió el contestador automático, entonces ahora las redes sociales nos desafían, porque <strong>son hijas de la posmodernidad</strong>. La profesión cambió mucho como cambió todo, la política, la religión, ¿Por qué no la Psicología? Estamos yendo a un lugar de mayor informalidad, de mayor visibilidad. La pregunta es ¿Qué hacemos con eso? ¿Rechazamos la tecnología?


<p><strong>Hay que incorporarla</strong>. Yo tengo hasta pacientes en Facebook desde hace 3 años y nunca tuve problemas, nunca un paciente me vino a decir “<em>como vos sos trotskista dejo de venir</em>”.  A veces se ríen, a veces discutimos, pero jamás esto hizo ningún ruido. <strong>Los psicólogos debemos tener mente flexible</strong>, amplia, plástica. En salud mental el paradigma es la plasticidad, si sabemos que la plasticidad es una enorme virtud, entonces hay que ser plásticos también en esto de incorporar todo lo bueno que tiene Internet y las redes sociales. Yo construí esta institución básicamente por el <strong>Facebook</strong>, entonces soy de la idea de “<em>dale, incorporemos</em>”. </p>




<p><strong>El tema de la exposición pública y la reserva de la identidad es una dinámica muy discutida en torno a las redes sociales. ¿Cómo se percibe este fenómeno desde la Psicología y como lo manejas vos desde tu práctica profesional?</strong></p>


<p>A veces me han tocado personas en el grupo que me piden que les publique lo que quieren decir, porque no quieren mostrarse con su nombre. Pero a mí me parece que todo enriquece. Mi celular esta prendido las 24 horas y, ¿vos pensas que algún paciente me ha llamado a las tres de la madrugada? Sólo me ha pasado en mis vacaciones con alguna cuestión muy urgente, que te llaman por intento de un suicidio, muerte de un ser querido o crisis matrimoniales muy profundas. </p>


<p>Me parece que hay una subestimación de la capacidad del paciente de poder interpretar lo que el profesional dice y hace. El paciente te ve y si vos corres una maratón te felicita, o te admira. <strong>Yo lo utilizo como un recurso nuevo</strong>.</p>


<p>Internet cambió mucho el conocimiento, antes el modelo del conocedor era el sabio y ahora el que sabe a dónde buscar. <strong>Los psicólogos debemos ser amigables con Internet</strong>, para a mí el que lo rechace está equivocado, de los debates de Facebook aprendo un montón.</p>


<p>El grupo me enseñó cuestiones legales, clínicas, académicas. Hoy no me considero ni psicoanalista, ni sistémico, ni cognitivo. <strong>No sé lo que soy</strong>, lo digo con un poco de pesar pero con orgullo, porque como no sé, puedo tomar de todas las corrientes, mi visión de la Psicología es que el norte sea mi paciente, no una orientación. </p>


<p>A veces el Psicoanálisis no sirve y tomas herramientas de la Sistémica, y tampoco sirve y tomas herramientas de la Cognitiva, y soy tan así que tengo un <strong>grupo de anti fans</strong>, un grupo de Cognitivos que me juegan en contra porque dicen que rechazo al Psicoanálisis; otro grupo porque soy Psicoanalista y dicen que rechazo a los Cognitivos. </p>


<p>Esto tiene que ver con la capacidad de integrar, lo digo humildemente, para a mí es el camino a seguir, no me voy a embanderar con ninguna corriente. La especie humana quiere etiquetar, quiere que tengas un rótulo, y si me queres etiquetar soy psicólogo, soy docente, soy <em>runner</em>, y soy trotskista, esas son mis etiquetas. <strong>Empecé siendo psicoanalista y ahora necesito de otras corrientes. </strong></p>


<p><strong>Renegás de las etiquetas pero por otro lado la ciencia necesita paradigmas. En ese sentido ¿Cuál es la corriente que mejor te identifica?</strong></p>


<p>Estoy formado en la UBA, tengo una escucha psicoanalítica, tengo un sesgo, pero no me considero psicoanalista porque no me inscribo dentro de él, si el paciente mejora yo estoy bien. A veces me fastidio porque sé que pertenecer “a” te da una especie de <em>back up</em> pero a mí nadie sale a defenderme, porque no soy miembro de ninguno, porque yo elijo el camino de la integración. </p>


<p><strong>¿Qué es DIAFOS y cuáles son sus objetivos?</strong></p>


<p>DIAFOS también surge de una calamidad: un grupo de profesionales trabajamos en una institución que comenzó a ir hacia el lado del dinero aprovechándose de la necesidad e inexperiencia de muchos colegas jóvenes, con una práctica muy perversa que hay en nuestra profesión. </p>


<p>Nos empezamos a incomodar con esta situación, pedimos varias reuniones y en diciembre del año 2009 nos expulsaron. Así, con tres compañeros nos lanzamos con este proyecto, y de la institución que nos echaron, 30 profesionales se vinieron con nosotros. Entonces DIAFOS nació bendito. </p>


<p>El nombre surgió porque quería que tenga un nombre griego, y entendía que yo quería la <strong>diversidad</strong>, que en griego es diáforas, pero nos sonaba como diásporas que no queríamos y entonces quedó DIAFOS. Significa diversidad pero con una torsión. </p>


<p>Diversidad de orientaciones, porque convivimos 60 profesionales con distintas visiones, predomina el Psicoanálisis, pero hay cognitivos, sistémicos, rogerianos, de todas las orientaciones, y eso nos enorgullece, porque lo que un paciente necesita lo tenemos. </p>


<p>Nuestro plantel es diverso y eso es maravilloso, me nutro. También tenemos diversidad de actividades, por eso se llamó espacio de <strong>Arte, Cultura y Salud</strong>. Contamos con talleres sobre cultura, charlas, nos gustaría hacer encuentros de poesía, tenemos ofrecimiento de periodistas para dar charlas. Estas actividades hacen sinergia entre sí, y esto nos trae gente porque se van enterando de todo lo que tenemos. </p>


<p><strong>DIAFOS es una red profesional y humana, no una fuente de trabajo</strong>. DIAFOS también te da seguridad, porque no es lo mismo tener un consultorio en un barrio y que se caiga alguien a atenderse que no sabes ni quién es. Todos los pacientes que llaman son derivados a la red, nosotros con mi mujer Marcela, que somos los directores, no nos quedamos con ninguno, nuestros pacientes los buscamos por otro lado.</p>


<p>Tenemos un plan que es un arancel más bajo para personas que no pueden pagar, el profesional atiende en DIAFOS y después se queda con el paciente. Plata no ganamos pero somos felices. </p>


<p><strong>¿Cuáles son los mayores desafíos que se presentan en DIAFOS?</strong></p>


<p>Ahora el desafío perentorio en este momento es el <strong>macroeconómico</strong>. Decir macro remite a Macri (el Presidente de Argentina), y el desafío es la desocupación en ascenso, y los psicólogos estamos en la primera línea de batalla, ya que lo primero que abandona la gente es el psicólogo, y vemos con preocupación creciente que la profesión comienza un derrotero hacia lo que en su momento fue la gran catástrofe en el 2001. </p>


<p>En aquella época había un 40% de desocupación, los consultorios vacíos, atendía pacientes y me pagaban con mermeladas caseras, milanesas de sojas caseras, libros, discos, caían devastados y muy angustiados porque estaban desocupados. Fue un caos, y en la actualidad este es el principal desafío, cómo afrontar este período en el que yo lo denomino “la baja”, porque cae todo, en la demanda de profesionales, demanda de atención psicológica, y caen las actividades, los talleres comienzan a mermar, las charlas a declinar, las actividades comienzan a suspenderse por falta de gente. </p>


<p>Muchas instituciones van a cerrarse, gran parte de la profesión va a caer porque va a caer la economía en general, entonces nuestro principal desafío es la fortaleza hacia el hombre. <bR>
Otro desafío es incorporar los vaivenes de acuerdo a la época, las nuevas herramientas, si hablamos de otro tipo de desafíos. </p>


<p>También nos ha pasado que vengan pacientes que son de otros colegas, entonces hay un desafío en la apertura, de mantener la institución en funcionamiento sin caer en prácticas poco éticas. Mi límite es mantener la institución en funcionamiento, entonces tiene límites éticos. Otro desafío es incorporar nuevas disciplinas, porque muchos colegas se resisten. Entonces ¿Cómo leemos el espíritu de la época? ¿Cómo no caemos en anacronismos? ¿Cómo mantenemos ese equilibrio?</p>


<p><strong>A mí me seduce lo nuevo</strong>, y me seduce tanto que es peligroso, en cambio mi mujer tiene personalidad muy distinta, muy reservada, así que entre los dos llegamos a un buen equilibrio.</p>


<p><strong>¿Cómo se relaciona la Psicología con la política? ¿Cuál es el rol que juega políticamente el psicólogo en la sociedad?</strong></p>


<p>Una cuestión que tiene que ver con la profesión es si existe el <em>Ser psicólogo</em>. Soy psicólogo con mi paciente, no soy psicólogo para ir a buscar el pan; no soy psicólogo con mis amigos, y a veces se decepcionan y en ese sentido Freud escribió sobre el <em>psicólogo silvestre</em>. Soy muy cauto con eso, y a veces me pregunto ¿El psicólogo debe involucrarse en política? ¿La persona debe involucrarse en política? Yo si voy a algún acto, voy como Patricio y no como psicólogo. </p>


<p><strong>Soy trotskista antes de ser psicólogo, pero no soy un trotskista como psicólogo</strong> y a veces aborrezco esas articulaciones, cuando por ejemplo leo izquierda Lacaniana, voy al baño y me pego la cabeza contra los azulejos. ¡Que alguien explique qué es izquierda Lacaniana! Y lo digo como psicólogo y como un tipo de izquierda. </p>


<p>Cuando soy psicólogo no soy psicólogo de izquierda, y cuando soy trotskista no soy trotskista psicólogo. El psicólogo es una función, pero si me preguntas si esta bueno que el psicólogo participe de la realidad, te digo que sí. En cambio si me preguntás si está bueno que esa participación de la realidad lo limite en la profesión te digo que no está bueno. </p>


<p>Hay un debate que se hizo en el grupo y fue el primer debate encarnizado, en el que expulse gente por insultar. Y era en torno a si atendemos a un torturador. Yo que soy trotskista y los quiero a todos presos, los detesto, como cosa personal. Pero si un paciente es torturador, ¿tengo que decirle que no lo atiendo? Para mí no, hasta lo quiero atender para saber que pasa en la cabeza de ese tipo, sería como un posgrado, imaginate que hubiera venido Videla, es una oportunidad única, porque los desprecio con el alma. Ahora un paciente es un paciente, ahora si tenes algún obstáculo porque te da miedo, impresión, ahí sí derivas. </p>


<p>¿Me define mi pertenencia política? Por ahí para algún paciente desde lo imaginario, sí. han aparecido pacientes que se han querido analizar conmigo porque soy trotskista, y cuando les pregunto por qué, me ha respondido que soy el único que va a entender la locura de un trosko, “<em>porque los trotskistas nos entendemos entre nosotros</em>”. </p>


<p><strong>Los psicólogos no somos jueces</strong>, debemos participar de la realidad, ser psicólogos vivos, involucrarnos con la realidad, pero como psicólogos debemos estar por fuera de la política, y no podemos permitir que la política contamine el quehacer profesional. </p>


<p><strong>Nadie es apolítico</strong> y decir que lo sos es porque sos la persona más política del mundo, porque la negación de la política es una política. Y la forma en la que uno se posiciona como psicólogo es política, pero hay que diferenciar lo que es política partidaria de la política. Tengo una posición política como psicólogo, y creo que la fui expresando en la charla. Una de mis políticas es aborrecer de los dogmas, de los clichés, soy dialéctico. Simpatizo con el PTS pero me enojo hasta con la teoría trotskista, por ejemplo, la posición frente a Milagros Salas la respeto pero no la comparto. </p>




<p><strong>¿Es difícil atender a un paciente con otra ideología, cómo lo haces?</strong></p>


<p>Es terrible, tengo un paciente que me decía que no le pregunte que hizo en la dictadura. Decirle a un psicólogo no me preguntes, es como que me estás matando porque te voy a preguntar, y el paciente me dijo que no me va a responder. <strong>Para a mí es un hermoso desafío</strong>, creo que es alguien que estuvo metido en la represión ilegal, pero es un desafío que me hace crecer. </p>


<p><strong>¿Qué tipo de pacientes te gusta atender?</strong></p>


<p>Me defino como un psicólogo de gente, me gusta atender gente, al ferretero, la cajera del supermercado, supervisar me aburre. Para  mí una cajera del supermercado que quiere ser bailarina es un desafío hermoso. También me defino como un psicólogo de la acción, también soy marxista y eso me ha contaminado la profesión, y en algún punto me hizo entrar en conflicto con el Psicoanálisis. Pero como soy materialista, <strong>el Psicoanálisis tiene mucho de idealismo</strong> y Marx rompe con la vertiente idealista de Hegel. </p>


<p>Salirse de los dogmas, no atarse a nada férreamente, que tengan la posibilidad y facilidad de cuestionar todo lo que tienen adelante, que no significa descreer de la verdad, le pido a los colegas que no se aten a ningún cliché, que no sean hablados. El objetivo con mis alumnos es crear  libres pensadores. <strong>Quiero librepensadores, que los psicólogos desarrollemos pensamiento propio.</strong></p> 




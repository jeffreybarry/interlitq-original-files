				<p><em>Interlitq</em>: Tell us about <em>The Tyndale Society</em>, and your connection to it.</p>
 
<p>NLI: <em>The Tyndale Society</em> honors the memory of William Tyndale (1494-1536), the first published English translator of the Bible. The modern-day Tyndale era began with the "Let There Be Light" exhibition at the British Library in London in 1994, and other events commemorating the quincentennial of Tyndale's birth. Twenty-three years later, the Society has a distinguished record in organizing conferences and events, and has two flagship publications, "Reformation," in addition to the "Tyndale Society Journal (TSJ)," of which I am editor. I am soon to begin work on the 49th edition of the TSJ. Our magazine discusses Tyndale's contributions to the English language and to civilization itself, and Tyndale is the center of the wheel from which the spokes radiate; we can and do cover all relevant aspects of Tudor and Reformation history. I have written articles on Michael Servetus (1509-1553), WT's Spanish contemporary who discovered the pulmonary circulation of the blood, who met the same fiery end as Tyndale, and with whom his life shares commonalities. With any story we write, Tyndale is waiting in the wings somewhere!</p>

				<p><em>Interlitq</em>: When did your association with the Society begin?</p>
 
<p>NLI: I attended the 1996 Tyndale Society Conference at Hertford College, Oxford. My first appearance as commentator/reviewer in the TSJ dates to April 2000 (TSJ15), and I took over the editorship in Autumn 2009 (TSJ37). Society founder Professor David Daniell (author of the landmark Tyndale biography which ushered in a new era of Tyndale scholarship<sup>1</sup>) was determined to rescue Tyndale from the shadows and he fulfilled this objective with sterling success<sup>2</sup>.  My colleagues on the TSJ editorial board carry on in this tradition, and as editor I am ably assisted by Society President Mary Clow, and our tech/DTP expert Dave Steele. We refuse to allow Tyndale to languish in footnotes and bibliographies.</p>

<p><em>Interlitq</em>: Are you seriously implying that Tyndale’s story was swept under the rug for five whole centuries?</p>
 
<p>NLI: Not exactly. Our commentator Ramona Garcia has profiled public attitudes toward Tyndale during the Victorian era, a time during which history buffs took advantage of rail and seaborne transportation to engage in Tyndale tourism, by visiting sites (the Nibley Knoll monument) associated with Tyndale's childhood near Stinchcombe in Gloucestershire, and with the tragic end to his life (Vilvoorde, in Belgium). There was keen interest in Tyndale in those days, reflecting national confidence and pride in English history and culture.</p>

				<p><em>Interlitq</em>: Why did attitudes shift in the 20th century?</p>
 
<p>NLI: Growing secularization did not help Tyndale's cause, although one can be a nonbeliever and a WT enthusiast. Celebrity atheists Chris Hitchens (interestingly, Hitchens was a Tyndale family name, which WT used as an alias) and Richard Dawkins have stressed the importance of early Bible history in the UK, and Dawkins has recommended the study of the KJV<sup>3</sup>, which draws heavily on Tyndale.</p>

<p>Overall, however, Victorian triumphalism faced a lean time in the 20th century.  The entire Victorian approach to judging success—the "great man" of history school—came under a cloud of revisionism, some of it well-deserved. Yet too many babies were thrown out with the bathwater.</p>

				<p><em>Interlitq</em>: And new generations brought in new heroes?</p>
 
<p>NLI: Tyndale's nemesis Thomas More (never out of fashion, of course) reached new audiences in the 1960s with the Robert Bolt play "A Man for All Seasons." The airbrushed portrayal of More as a man of conscience and principle took hold in that era of protest and demonstration. We at the Tyndale Society know More's darker side; the fanaticism, the heresy-hunting, the persecution of honorable men like William Tyndale, in whose arrest and execution More almost certainly played an indirect hand. </p>

<p><em>Interlitq</em>: Thomas More fans have no trouble at all keeping TM firmly in the spotlight. Why do you suppose that More enjoys this competitive advantage in publicity?</p>
 
<p>NLI: I should point out that we at the Tyndale Society enjoy cordial relations with the international Thomas More community (there have been joint public debates). Various factors account for More’s apparent dominance. Thomas More is able to draw upon the support of a network, a machine. The closest analogy I can give you is drawn from academia; think of More as the tenured professor who enjoys respect by virtue of his publications but also as a matter of status.  William Tyndale is the independent scholar, who may have lost out in a tenure battle or political quarrel, and who is banished to the wilderness. He may be the equal or superior of the insider, but he will never gain the respect that it is his due, because of well-entrenched snobbery and the herding instinct. The majority prefers safety in numbers...</p>
<p>I should add that thanks to Google Alerts we know that Tyndale has fans all over the world, if perhaps not so much in the UK (a prophet without honour in his own country). There are news stories from Africa and the United States. We hear from Tyndale supporters in Japan, from any country you care to name. And that is how it should be, for the Tyndale message is a universal one.</p>
 
<p><em>Interlitq</em>: But isn't it true that Thomas More’s saintly image has lately come under assault from an unexpected source?</p>
 
<p>NLI: Hilary Mantel is continuing to work on her Thomas Cromwell project, "Wolf Hall," and is rumored to be covering the story of William Tyndale's exile and King Henry's short-lived efforts to reach out to the expatriate English translator. Mantel's unflattering portrayal of Thomas More has sown disquiet in the TM community: historian John Guy puts ultimate blame on John Foxe (an Elizabethan historian dismissed as a propagandist in some quarters). Truth be told, More damns himself in his very own screeds against Tyndale, whom he viewed as in some respects the chief threat against Christendom.</p>

<p><em>Interlitq</em>: And if Mantel is sympathetic to the plight of the religious Reformers, how do you feel about that?</p>
 
<p>NLI: John Guy agrees that Mantel spins a good yarn, yet fears that undergraduates cannot distinguish between her novels and history<sup>4</sup>. My own impression, which may seem conflicted, is that Mantelization could lead to the wrong kind of publicity, to over-exposure. I know from my own researches that Google hits on Thomas Cromwell are overwhelmingly associated with "Wolf Hall"; but we need proper, non-fictional history to fill in the gaps in the historical record in the lives of Cromwell, Tyndale, and other reformers.</p>

				<p><em>Interlitq</em>: And is that what you at the TSJ plan to do in the future?</p>
 
<p>NLI: When the truth is uncovered about the early lives of Tyndale, Cromwell, and Servetus (currently fogged with mystery), we at the TSJ will be there to report these stories to the world. We will continue to review publications relevant to Tyndale (histories, translations). I have pushed our magazine in a more multi-media direction; we investigate Tyndale memorials (statues), plays, movies... and we have printed contributions from young people. Not everything received is publishable, and sometimes overzealous contributors will, with the best of intentions, recycle myths that have since been discredited. We are careful historians but our aim is to inspire interest, not strangle it at birth. I am always keen to find new reviewers—especially those who do justice to new books, not simply by pouncing on typos (anyone can do that), but by developing lines of inquiry which the author may have hinted at, yet not properly explored. When I retire from my own career as a staff translator, I expect to devote a great deal of time to Tyndalian research.</p>


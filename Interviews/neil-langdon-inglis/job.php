<p>

<em>Interlitq:</em> Summarize for us the achievements of Irish author Brian Inglis (1916-1993), in this year, the centenary of your father’s birth.</p>

<p>NLI: Brian's main achievements fall into three categories.  As an advocate for the existence of the supernatural, he gave the subject of parapsychology respectability (note that word respectability—we shall return to it). He forced a skeptical establishment to confront claims and evidence for the existence of the paranormal. Another writer might have been ignored; Brian was able to compel this attention by virtue of his track record as a journalist, historian, and admired public figure. His works of history (on more conventional topics) dated back to the 1950s, and he is an honorable and distinctive member of the 20<sup>th</sup> century club of non-academic historians (we must mention Winston Churchill, Roy Jenkins, Paul Johnson, as other shining examples). Last but not least, Brian's 1960s weekly documentary on the events of 25 years previously, <em>All Our Yesterdays</em> (Granada Television, 1960-1973, with Brian at the helm for the last ten years), spawned a host of imitators and influenced media representations of WWII and wartime Britain for generations to follow.</p>

<p><em>Interlitq:</em> Would it be true to say that he downplayed military issues when looking back on his own life and career?</p>

<p>NLI: He never gloried in his own military service with the RAF (Coastal Command). If the subject was brought up, he would touch upon his flight training (where he learned about the "triangulation of forces"). I think he mentioned once that he had shot done one German aircraft. On the other hand, he loathed fantasists who bragged about bogus military records. But there he would leave it, changing the subject as swiftly as possible.</p>

<p><em>Interlitq:</em> How did he feel about being a war historian on TV? Was he proud of <em>AOY</em>?</p>

<p>NLI: He knew the subject cold (with some obvious omissions; I assume he had inside information about the Enigma/Ultra programs, but could not discuss them openly in view of official secrets legislation). Brian was ambitious and when a top-rated TV program was offered to him, he was hardly going to refuse (we were often #2 in the ratings, if never quite #1—<em>Coronation Street</em> got in our way). <em>AOY</em> was produced by men Brian admired and respected, and Granada was an employer that nurtured talent (provided you did not fall out with the bosses, thereby becoming <em>Persona Non Granada</em>).  </p>

<p>Brian was a perfectionist and called for the highest standards, to the extent the dictates of weekly broadcasting permitted. His trademark economical style meant that every sentence he spoke was packed with insight and information.  His contract with Granada was generous and allowed him time for his own research. There was financial stability and a beautiful home in Bayswater to live and entertain in (although he would cringe to hear me say this). None of this came handed on a platter. Stress was inescapable—Brian is visibly nervous in his maiden broadcast—but his life had been ruled by deadlines and he did what needed to be done to make the show a success.</p>

<p><em>Interlitq:</em> What was the downside?</p>

<p>NLI: As I have hinted, Brian was never completely free to speak his mind. <em>AOY</em> was a mass hit, and Granada was catering to the general public, for whom the events of WWII were in the recent past and being relived anew; viewers were the nation’s grandparents, parents, and children.  Thus, decades before social media, Brian dared not overlook the silent scrutiny of millions of armchair historians across the land.  An outright attack on royalty (had there been one) would have been unthinkable. Brian could not be a revisionist, as part of him had always wanted to be.  </p>

<p>Also, shopkeepers in our neighborhood, in conversations with my mother, extolled Brian for his seriousness, by which they meant <em>respectability</em>. This was not a reputation he wanted, yet it stuck to him like a burr. In real life Brian Inglis supported the use of cannabis, and a reappraisal of traditional sexual morality, including the decriminalization of homosexuality.</p>

<p>This double life created a certain amount of cognitive dissonance, which grew unpleasant, and finally intolerable. It was hard to be radical when you were also a product of Ireland’s Protestant Ascendancy, albeit one who viewed the British Empire and Establishment with cold suspicion. You might say that Brian was neither fully British nor fully Irish.
</p>
<p><em>Interlitq:</em> In his books, was Brian a muckraker?</p>

<p>NLI: Sometimes yes and sometimes no. One of his earliest books, “Fringe Medicine” [Faber and Faber, 1964], was considered so controversial that the publisher had to rope in a noted surgeon to write the foreword and lend what they perceived as a veneer of credibility. Unorthodox medicine was merely one topic that Brian was unable to focus on fully until his departure from television. When <em>AOY</em> ended, Brian was freed of its shackles and plunged headlong into his pet topics. He was able to mount an indictment of the hated British Empire in "The Opium War" [Hodder and Stoughton, 1976], an exposé of the opium trade; this book is a legal document from the counsel for the prosecution as much as a work of history. “The Opium War” is a title scheduled for reissue by <a href="http://endeavourpress.com">Endeavour Press</a> in London.</p>

<p>And yet, Brian's public school history master Murray Senior had always counseled him to read dissenting views.  Brian could be fair and courageously so.  To raise the issue of Irish revolutionary Roger Casement's homosexuality, as Brian did in "Roger Casement" [H&amp;S, 1973], decades before the gay-marriage referendum in Ireland, was calculated to make him enemies within the Republican community and elsewhere. As was his portrait of Casement as a long-standing civil servant who served the Crown ably for years in hardship posts, and who was treated with respect and no small amount of patience by the UK authorities until Casement's treason could be ignored no longer.</p>

<p><em>Interlitq:</em> Was Brian objective in his treatment of the occult?</p>

<p>NLI: The paranormal texts crackle with hostility.  For Brian, the existence of the psi force was the great under-reported story of his time, and he displayed a sincere commitment to getting to the bottom of that story. He felt a burning dislike for what he recorded as establishment "resistance movements", motivated by obscurantism, which sought to quash unorthodox para-scientific research out of fear of the truth. He would always claim to be cool and rational but his feelings on this topic ran very high indeed. His 1970s battles with the U.S. Committee for the Investigation of Claims for the Paranormal (originally named CSICOP) were ugly and do not show Brian at his best. "Science and Parascience"—the sequel to the more readable "Natural and Supernatural"—is the work of a fanatic. It has numerous admirers. [Both titles were originally from H&amp;S 1984 and 1978, and both have now been rereleased by White Crow Books, including in e-book form—see <a href="http://whitecrowbooks.com">http://whitecrowbooks.com</a>].</p>

<p>Over time Brian moved away from a fascination with "hard-core psi" (levitation, spoon-bending, telekinesis) to a broader interest in the elusive forces of serendipity and chance which shape our world, and which even skeptics like me can and do experience. No friend of scientists, he came to regard scientists' flashes of inspiration as a manifestation of the psi force itself.  </p>

<p>Toward the end of his life he achieved a degree of contentment with his last love, Australian journalist Margaret van Hattem (cut short by her untimely death 1989). He was not, I think, fighting pitched battles in quite the same way as before. Some of his best friends were skeptics!</p> 

<p><em>Interlitq:</em> And socially?</p>

<p>NLI: Work meant everything to him. In common with many writers he was most productive before lunch.  Always more of a listener than a talker in social situations, he enjoyed having people around him, especially women.  His barefaced philandering in the 1960s finished my parents' marriage; yet Brian evolved, and by the 1980s he was a sincere and helpful mentor for dozens of female authors and professionals (Lionel Shriver, the American novelist, was one). He even socialized occasionally with my mother, Ruth Langdon Inglis (a wonderful author in her own right), in the closing years of his life, at the Academy Club run by Auberon Waugh, where they were both members. Brian was, I like to think, at peace with himself at last.</p>

<p><em>Interlitq:</em> Did such a busy life leave much time for family? What prompted your decision to revisit your father's work?</p>

<p>NLI: As noted above, White Crow Books and Endeavour Press are reissuing some of Brian's most noted publications in Kindle and in some cases print editions. I have been providing assistance where appropriate.  Personally I find some of the books easier to read than others; the parapsychology titles remind me too painfully of being lectured by Brian about his spoon-bending friends, whom I regarded as transparent frauds. As a husband, Brian was pretty much of a disaster; but as a parent he did his duty and loved my sister and me in his own way. I don't believe he ever understood me. Yet I prefer to remember the good times. His birthday gift of Otto Klemperer's recording of Beethoven's <em>Eroica</em> symphony launched me on a lifetime of classical music collecting and appreciation.  </p>

<p>One year before his death he pointed out to me an academic paper on parapsychology which quoted an Inglis in a footnote; it wasn't Brian Inglis who was quoted—but me, Neil Inglis, a skeptic! We could both laugh about this over many glasses of wine. It was the last time I saw him alive.</p>


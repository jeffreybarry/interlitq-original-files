<p><em>Interlitq</em>: As you are about to visit the Peruvian Amazon, can you tell us how this trip fits in with your current artistic concerns?</p>

<p>PP: Thank you for interviewing me. This June I’m going to Tambopata National Park, to a very remote research station in the Peruvian Amazon. The sleeping quarters of this station are open to the forest on one side, so you can see monkeys from your bed and I’ve heard that macaws fly in to steal food! The station is near the largest macaw salt lick known, a cliff by the Tambopata River, where they flock to replenish minerals they need in their diet. I’ll be going on night and dawn walks to observe the wildlife and hopefully get more information about the trees and plants. </p>

<p>I’m very lucky that the Arts Council have funded me to go on this trip, and to take time off to finish <em>Mama Amazonica,</em> the collection I’m working on.  The book is set in the Amazon jungle and in a psychiatric ward, so I wanted to have new experiences of the rainforest; I haven’t been there since I went to the Venezuelan Amazon twenty years ago.  That time I trekked in the Lost World, climbed Mount Roraima and was canoed to the base of Angel Falls by the local Pemón people, so the type of jungle I saw was cloud-forest, not much of the lowland forest with giant trees.</p>

<p><em>Interlitq</em>: Could you tell us more about your last book, <em>Fauverie</em> and your forthcoming book, <em>Mama Amazonica</em>.</p>

<p>PP: The <em>Fauverie</em> of my sixth collection is the name of the big-cat house in the Jardin des Plantes zoo in Paris’s Latin Quarter, but it was also in a way my father’s flat which was nearby, a wild and ferocious place for me to be during the two years I visited him while he was dying of emphysema. </p>

<p>The real star of the book is Aramis the benign black jaguar, who resided at the Ménagerie, during the time I went back and forth to Paris to write the poems. The story is about my father’s reappearance after vanishing for thirty-five years, and my re-acquaintance with Paris, city of my birth and where I spent my early childhood. I fell in love with Paris when I visited it as a tourist. The troubled relationship with my father gave me an opportunity to study a man who had been cruel and abusive, to look at what could be retrieved and redeemed, given that there are many places in the world where those in power are cruel. </p>

<p><em>Mama Amazonica</em> is the other side of the picture, as it is my mother’s story, from the moment she met my father to the moment she died, years later, from complications brought on by the massive doses of the many medications she was prescribed for her manic depression and psychosis. I have set her story in the Amazon as well as in a psychiatric hospital, to open up the focus, and to make an estranged parent lovable, but dangerous as the creatures of that setting. I say that, but actually that’s how the poems are writing themselves, so I’m only guessing that this is the secret plan! The title poem ‘Mama Amazonica’ depicts my mother as a giant waterlily fertilised by beetles, in a backwater of the jungle. I’m thrilled that Bloodaxe has offered to publish it in September 2017.</p>

<p><em>Interlitq</em>: Would you go as far as to say that your poems are verbal sculptures? Have you given up sculpture altogether?</p>

<p>PP: I have given up sculpture. But I still feel that I’m making sculptures in my poems, with words. I like to make my poems as physical as possible, and the form of the image is crucial to me. I see my poems as objects and installations and the book as an exhibition in a gallery, except… that it’s portable! Which is a wonderful idea, that you can carry an exhibition around and store it in a bookshelf! In <em>Mama Amazonica</em> many of the sculptures are tiny, contained in two to ten lines, so you could pick them up if they were made of materials, but there are a fair number of much longer poems, that could be thought of as installations. The title poem feels like an installation to me, or a video film.</p>

<p><em>Interlitq</em>: You were born in France and spend long periods of time in Paris. Is a French identity at the core of your work?</p>

<p>PP: A non-British identity is at the core of my work, but I’m not sure it’s a French one, even though I am French and was born in Paris, and don’t have British nationality. My heritage is complicated and misty! On my mother’s side there’s my Welsh/Asian grandmother, who brought me up, though her part Welsh heritage was originally from Ireland. She was half Pakistani and there’s been a lot of secrecy about that. On my father’s side, I think the roots are all French, though my Gran did tell me he was part Algerian, but I don’t think that was true. He did live in Algeria, in the Kabylie Mountains, though, for part of his disappearance, and had close Algerian friends. When I go to France I’m perceived as English because I speak French with an English accent, so I’m an outsider in my own country! This mixture of confused identities may be one of the reasons that I write about ‘elsewhere’ in my poems – there is a search for home and a feeling of being an outsider. </p>

<p><em>Interlitq</em>: How central are themes of violence and gender issues to your work? Also tell us more about your preoccupation with the environment.</p>

<p>PP: I’d say these concerns are central. I hope that readers don’t just read my poems as personal and autobiographical, I would hope they reach beyond the ‘confessional’. Abuse of women and children is a worldwide problem and in certain societies is extreme.</p>

<p>My environmental concerns must originate from my childhood I think, when I lived with my grandmother in mid-Wales, and she had what seemed to me to be a huge garden. I worked in the garden for my keep, and loved it. Later, I spent summer holidays camping with my mother in an overgrown terraced vineyard she had bought in the south of France, and that place must have been my second childhood ‘Amazon’.  Both places planted a deep love of nature and a feeling that the natural world in its wild state is an ally, a place of retreat and consolation. Gran had a lot of animals, and after my unhappy infancy in Paris, they must have made a deep impression: I adored them and bonded with them. As I grow older it horrifies me that many species are now endangered.  </p>

<p><em>Interlitq</em>: Do you continue to reflect on Frida Kahlo, pivotal as she has been to your poetic development? Are you drawn to Mexico?</p>

<p>PP: I am continuing to use some of the images, which I wrote about in <em>What the Water Gave Me</em>. For example, in <em>Mama Amazonica</em>, I have created my own hummingbird necklace, after the painting of hers ‘Self-Portrait with Thorn Necklace and Hummingbird’. The necklace has a particular significance in this book but also carries much of the meaning it had for Frida Kahlo and Mexican symbolism. So although I thought I’d finished with her imagery I’m finding I haven’t quite, that I’m enlarging it in another context.</p>

<p>I am very drawn to Mexico, though haven’t been there for a few years now. I’d love to see Frida Kahlo’s Blue House, now that her bathroom and wardrobe are open. I wanted to see them when I went before but they were sealed to the public. </p>

<p><em>Interlitq</em>: How do you see the state of English poetry these days?</p>

<p>PP: If you mean British poetry, it’s at a very interesting state just now, in transition mode. There are still some conservative tendencies, but the upsurge of young poets, women poets, and those of mixed and other heritages, is in the process of transforming it, making it more lively, varied, and emotionally open. There are so many exciting poets writing now.</p>

<p><em>Interlitq</em>: Now that you are based in Cornwall, do you miss Walthamstow?</p>

<p>PP: No I don’t. I had been trying to get away from London for years, and am much happier in the deep country here, surrounded by birds and lush greenery, and our own wild river 100 yards downhill from our house, the Lynher, which roars down from Bodmin Moor. </p>

<p><em>Interlitq</em>: Where do you see yourself a year from now?</p>

<p>PP: I hope to see myself writing away in my garden den. It’s beautiful in spring. As to what I’ll be writing, I have no idea yet.<br><br></p>



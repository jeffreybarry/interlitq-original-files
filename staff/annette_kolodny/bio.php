                  <td class="texto2" valign="top"><p>Ever  since her 1960s graduate student days at the University of California, Berkeley,  Annette Kolodny has combined political activism in the Civil Rights, women&rsquo;s,  and environmental movements with a scholarly scrutiny of American culture and  its discontents.&nbsp; Her first two books are  considered landmarks in the fields of ecocriticism and frontier studies; each  examines the developing mythology of the western frontiers.&nbsp; <em>The Lay of the Land: Metaphor as  Experience and History in American Life and Letters</em> (Chapel Hill:  University of North Carolina Press, 1975; rpt. 1984) concerns itself with  Euroamerican male fantasy projections onto the successive &ldquo;virgin&rdquo;  wildernesses.&nbsp; <em>The Land Before Her:  Fantasy and Experience of the American Frontiers, 1630-1860</em> (Chapel Hill:  University of North Carolina Press, 1984) offers the first comprehensive study  of white women's responses to the pioneering experience, analyzing not only  personal documents like letters and diaries but published novels, poetry, and  promotional tracts, as well.&nbsp; Dr.  Kolodny&rsquo;s essay &ldquo;Dancing Through the Minefield: Some Observations on the  Theory, Practice, and Politics of a Feminist Literary Criticism&rdquo; was awarded  the Florence Howe Prize for Feminist Criticism in 1979 and has since been  translated and reprinted worldwide, becoming the most anthologized essay in the  field.&nbsp; Throughout her career, she has  continued to publish actively in the fields of feminist literary criticism and  critical theory, ecocriticism, frontier studies, and early American literature  and culture.<br>&nbsp;<br>
                    During  her long career, Dr. Kolodny has held faculty positions at Yale University, the University of British   Columbia, the University of New   Hampshire, the University of Maryland,  Rensselaer Polytechnic Institute, and the University of Arizona.<br>&nbsp;<br>
                    From  1988 through 1993, Dr. Kolodny took on the challenges of academic  administration by becoming Dean of the College of Humanities at the  University of Arizona.&nbsp; As a result of her experience in administration,  Dr. Kolodny now also writes about higher education issues and, as a consultant,  works with schools across the country and around the world to effect positive  change on campus.&nbsp; In 1998, Duke  University Press published <em>Failing the Future: A Dean Looks at Higher  Education in the Twenty-first Century</em>, Dr. Kolodny&rsquo;s study of higher  education public policy issues.<br>&nbsp;<br>
                    Her  books and essays have garnered numerous awards both in the United    States and abroad, and she is the  recipient of fellowships from the National Endowment for the Humanities, the  Ford Foundation, the Rockefeller Foundation, the John Simon Guggenheim  Foundation, and others.&nbsp; In 1998, she was  the first woman to be named an Honored Scholar by the Division on Early  American Literature in the Modern Language Association (MLA).&nbsp; In October 2002, she received the  Distinguished Achievement Award from the Western Literature Association.&nbsp; At its annual meeting in December 2002, the  American Literature Section of the MLA awarded Dr. Kolodny the prestigious Jay  B. Hubbell Medal for outstanding lifetime scholarly achievement in American  literary and cultural studies.&nbsp; In  October 2006 the Society of Early Americanists honored Dr. Kolodny for  &ldquo;Excellence in Teaching&rdquo; based on her &ldquo;important contributions to the lives and  careers&rdquo; of her many students over the years.&nbsp;  Several national prizes in ecocriticism and feminist studies have  recently been named for her; and her work has been the subject of many  conferences and conference sessions here and abroad. <br>&nbsp;<br>
                    At the University of Arizona, where  she held the title of College of Humanities Professor of American Literature  and Culture, in 2001 the Graduate and Professional Student Council named her  &ldquo;Outstanding Faculty Mentor of Graduate Students.&rdquo;&nbsp; In 2002 she received the &ldquo;Dean&rsquo;s  Distinguished Teaching Award with Sustained Contributions to Mentoring&rdquo; from  the College of Humanities.&nbsp; <br>&nbsp;<br>
                    Her  current research on Native American stories about first contacts with Europeans  uncovered a lost masterpiece of nineteenth-century Native American  literature.&nbsp; The work was reprinted by Duke  University Press in 2007, under the title Joseph Nicolar&rsquo;s <em>The Life and Traditions of the Red Man</em>, edited, annotated, with a  history of the Penobscot Nation and an introduction by Annette Kolodny.<br>&nbsp;<br>
                    On June  30, 2007, Dr. Kolodny retired from the University of Arizona,  becoming Professor Emerita.&nbsp; Despite this  retirement, Dr. Kolodny continues her active schedule of conference appearances  and guest lectures at universities in the United    States and around the world.&nbsp; Her most recent publications concentrate on  the fields of Native American Studies and ecocriticism.&nbsp; She is now completing a book entitled <em>In Search of First Contact:&nbsp; The Peoples of the Dawnland, the Vikings of </em><em>Vinland</em><em>, and American Popular Culture</em>, to be  published by Duke University Press.</p>

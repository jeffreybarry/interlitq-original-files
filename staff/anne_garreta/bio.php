                  <td class="texto2" valign="top"><p>Anne Garr&eacute;ta est n&eacute;e en 1962 &agrave; Paris. Normalienne, elle a publi&eacute; en 1986 un premier roman, <em>Sphinx,</em> salu&eacute; par la critique: il narrait une histoire d'amour entre deux personnages dont on ne pouvait conna&icirc;tre l'identit&eacute; sexuelle.</p>
                    <p>
                      Apr&egrave;s ce brillant travail sur la "grammaire du genre" et un pamphlet sous forme de dialogue<em>, Pour en finir avec le genre humain</em> , Anne Garr&eacute;ta entame une carri&egrave;re universitaire aux Etats-Unis, passe  un PhD en litt&eacute;rature fran&ccedil;aise sur les "fins de roman" aux XVII&egrave;me et  XVIII&egrave;me si&egrave;cles, enseigne quelques ann&eacute;es &agrave; Princeton.</p>
                    <p>
                      Elle publie en 1990 <em>Ciels liquides</em><a href="http://cosmogonie.free.fr/livres.html" target="_blank">,</a> roman qui s'attache cette fois &agrave; d&eacute;crire la descente aux enfers d'un  personnage qui perd l'usage de la langue. Entre 1991 et 1992 elle  s&eacute;journe &agrave; Rome &agrave; la Villa M&eacute;dicis. Depuis juin 2000, Anne Garr&eacute;ta est  membre de l'Oulipo. </p>
                    <p>
                      Son troisi&egrave;me roman<em><u>, </u>La D&eacute;composition</em>, publi&eacute; en 1999, met en sc&egrave;ne un serial-killer utilisant comme modus operandi les personnages d' <em>A la Recherche du temps perdu</em> de Marcel Proust. </p>
<p>En septembre 2002 est sorti <a href="http://cosmogonie.free.fr/antescriptum.html" target="_blank"><em>Pas un jour</em></a>(&eacute;ditions  Grasset), "roman" de douze chapitres r&eacute;dig&eacute;s en deuxi&egrave;me personne,  digression sur et tentative d'&eacute;puisement du d&eacute;sir, travers&eacute; par  d'autres th&egrave;mes connexes tels que la rem&eacute;moration, l'analyse des  situations, l'impossible reconstruction du monde. Quant aux buts  affich&eacute;s, citons au moins ces deux phrases en guise d'introduction  imparfaite: "Digresser ou diff&eacute;rer tes d&eacute;sirs"; "se d&eacute;prendre de  soi-m&ecirc;me". </p>
<p>Anne Garr&eacute;ta est aujourd'hui ma&icirc;tre de conf&eacute;rences &agrave; l'universit&eacute; de Rennes II, enseigne en alternance aux Etats-Unis. </p>
<p><span lang="EN-US">Tous ses romans sont publi&eacute;s chez Grasset (&agrave; l'exception de <em>Pour en finir</em>..., chez Fran&ccedil;ois Bourin). </span>Autres publications: La nouvelle<u> </u><em>La Pyramide</em> publi&eacute;e en 1991,"Vol", nouvelle publi&eacute;e dans le n&deg;7 du Serpent &agrave; Plumes  (1990), "Nuits" publi&eacute; dans le num&eacute;ro 24 (1994) de cette revue. </p>
<p>Le 7 novembre 2002, Anne Garr&eacute;ta a obtenu le prix M&eacute;dicis pour son dernier roman, <em>Pas un jour</em>.</p>

          <p><span lang="EN-US">
Peter Robertson was born in Glasgow in 1960, of Scottish and Irish descent, son of William John Robertson, formerly Chairman, West of Scotland Refractories, and Isobel Murray Imrie; he was the step-nephew of Ames Lyell Imrie CBE, variously City Chamberlain of Edinburgh, City Chamberlain of Dundee, and Consul of Sweden at Edinburgh. He was brought up in Perthshire, and educated at Cambridge University. He is the President, Publisher, and Founding Editor of The International Literary Quarterly (Interlitq), a not-for-profit corporation in the State of New York. </p>

			  <p>As an author, his fiction has been translated into several languages, and he has published translations of work by authors such as Jorge Luis Borges, Rosalía de Castro, Rubén Darío and Paul Éluard.</p>

			  <p>He is a Fellow of the Royal Society of Arts. a Fellow of the Society of Antiquaries of Scotland, and a Fellow of the Chartered Institute of Linguists.</p>

<p>He lives in Argentina and Scotland.

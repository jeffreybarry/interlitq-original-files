                  <td class="texto2" valign="top"><p>Pablo Neruda (1904-1973), whose real name is Neftalí Ricardo Reyes Basoalto, was born on 12

July, 1904, in the town of Parral in Chile. His father was a railway employee and his mother, who

died shortly after his birth, a teacher. Some years later his father, who had then moved to the

town of Temuco, remarried doña Trinidad Candia Malverde. The poet spent his childhood and

youth in Temuco, where he also got to know Gabriela Mistral, head of the girls’ secondary school,

who took a liking to him. At the early age of thirteen he began to contribute some articles to the

daily “La Mañana”, among them, <em>Entusiasmo y Perseverancia</em> – his first publication – and his first

poem. In 1920, he became a contributor to the literary journal “Selva Austral” under the pen name

of Pablo Neruda, which he adopted in memory of the Czechoslovak poet Jan Neruda (1834-1891).

Some of the poems Neruda wrote at that time are to be found in his first published book:

<em>Crepusculario</em> (1923). The following year saw the publication of <em>Veinte poemas de amor y una

cancion desesperada</em>, one of his best-known and most translated works. Alongside his literary

activities, Neruda studied French and pedagogy at the University of Chile in Santiago.</p>

<p>Between 1927 and 1935, the government put him in charge of a number of honorary consulships,

which took him to Burma, Ceylon, Java, Singapore, Buenos Aires, Barcelona, and Madrid. His

poetic production during that difficult period included, among other works, the collection of

esoteric surrealistic poems, <em>Residencia en la tierra</em> (1933), which marked his literary breakthrough.</p>


<p>The Spanish Civil War and the murder of García Lorca, whom Neruda knew, affected him strongly

and made him join the Republican movement, first in Spain, and later in France, where he started

working on his collection of poems <em>España en el Corazón</em> (1937). The same year he returned to his

native country, to which he had been recalled, and his poetry during the following period was

characterised by an orientation towards political and social matters. <em>España en el Corazón</em> had a

great impact by virtue of its being printed in the middle of the front during the civil war.</p>

<p>In 1939, Neruda was appointed consul for the Spanish emigration, residing in Paris, and, shortly

afterwards, Consul General in Mexico, where he rewrote his <em>Canto General de Chile</em>, transforming

it into an epic poem about the whole South American continent, its nature, its people and its

historical destiny. This work, entitled <em>Canto General</em>, was published in Mexico 1950, and also

underground in Chile. It consists of approximately 250 poems brought together into fifteen literary

cycles and constitutes the central part of Neruda’s production. Shortly after its publication, <em>Canto

General</em> was translated into some ten languages. Nearly all these poems were created in a difficult

situation, when Neruda was living abroad.</p>

<p>In 1943, Neruda returned to Chile, and in 1945 he was elected senator of the Republic, also joining

the Communist Party of Chile. Due to his protests against President González Videla’s repressive

policy against striking miners in 1947, he had to live underground in his own country for two years

until he managed to leave in 1949. After living in different European countries he returned home

in 1952. A great deal of what he published during that period bears the stamp of his political

activities; one example is <em>Las Uvas y el Viento</em> (1954), which can be regarded as the diary of

Neruda’s exile. In <em>Odas elementales</em> (1954- 1959) his message is expanded into a more extensive

description of the world, where the objects of the hymns – things, events and relations – are duly

presented in alphabetic form.</p>

<p>Neruda’s production is exceptionally extensive. For example, his <em>Obras Completas</em>, constantly

republished, comprised 459 pages in 1951; in 1962 the number of pages was 1,925, and in 1968 it

amounted to 3,237, in two volumes. Among his works of the last few years can be mentioned <em>Cien

sonetos de amor</em>(1959), which includes poems dedicated to his wife Matilde Urrutia, <em>Memorial de

Isla Negra</em>, a poetic work of an autobiographic character in five volumes, published on the

occasion of his sixtieth birthday, <em>Arte de pajáros</em> (1966), <em>La Barcarola</em> (1967), the play <em>Fulgor y

muerte de Joaquín Murieta</em> (1967), <em>Las manos del día</em> (1968), <em>Fin del mundo</em> (1969), <em>Las piedras del

cielo</em> (1970), and <em>La espada encendida</em>.
</p>

                  <td class="texto2" valign="top"><p>Julia Kristeva was born in Bulgaria in 1942. At the age of 23, she  moved to Paris and has lived there ever since. Her original interests  were in language and linguistics, and she was influenced by her  contemporaries Lucian Goldmann, and Roland Barthes. She also studied  Freudian and Lacanian psychoanalysis, and like her mentors, she began  to work both as an analyst and an academic. She joined the '<em>Tel Quel</em> group' in 1965, where she met her future husband, Phillipe Sollers, and  became an active member of the group, focusing on the politics of  language. The Tel Quel group worked with the notion of history as a  text for interpretation and its writing as an act of politicized  production rather than an attempt to make an objective reproduction.  Kristeva's articles began to appear in publications by <em>Tel Quel</em> and the journal <em>Critique</em> in 1967, and in 1970 she joined the editorial board. Her research in  linguistics, including her interest in Lacan's seminars during the same  year, manifested in the publication of <em>Le Texte Du Roman</em> (1970), <em>S&eacute;m&eacute;iotik&eacute;: Recherches pour une s&eacute;manalyse</em> (1969), and subsequently, <em>La Revolution du langage poetique</em> (her doctoral thesis) in 1974. The latter publication led to her  accepting of chair of linguistics at the University of Paris, and a  series of guest appointments at Columbia University in New York.<br>
&nbsp;<br>
Kristeva's unique background, a "foreign" woman working in the  predominantly male intellectual circles of France, drives the  strategies of her work in semiotics and her interest in the politics of  marginality. In accordance with her thinking, she produces both  fictional and academic texts. Her interest is in discourses that resist  rigid and one-dimensional logic and instead engage in an ongoing  process of writing the struggle with the impasse of language. She  prefers to analyze, to think language against itself, by its fracturing  and multiplication of texts, while taking the figure of negativity into  account.<br>
&nbsp;<br>
In addition, Kristeva's experiences in Communist Bulgaria provide her  an intimate understanding of Marxism and the work of the Russian  Formalists such as Mikhail Bakhtin (whose work she is accredited with  introducing to the West). Developing Hegel's concept of negativity in  conjunction with these ideas and those of her teachers and peers, she  produced an influential critique and following shift from Structuralist  to Poststructuralist thought. Her particular focus is a  process-oriented reading of the sign.<br>
&nbsp;<br>
Such a process for Kristeva is concerned with bringing the speaking  body back into phenomenology and linguistics. In opposition to theories  in structuralist linguistics that she feels are "nothing more than the  thoughts of archivists, archaeologists, and necrophiliacs" she develops  a new science, "semanalysis," that connects the body, complete with its  drives, back into language from where she believes the logic of  signification is already present. In this process she elaborates on the  Lacanian idea of the mirror stage and the formation of a separation, a  lack, from the (m)other that forms signification as a movement from  need (demand) into desire. It is an ongoing process of completion  through the symbolic castration of the subject. Here, Kristeva is  critical of Lacan's overlooking of processes that take place before the  mirror stage.<br>
&nbsp;<br>
Kristeva's elaboration on the model of Lacan involves a distinction  between the "semiotic" and "semiotics" as a field of study in  linguistics, and a further distinction between two heterogeneous types  of signification in language, the semiotic and the symbolic. The  semiotic exists within the signifying process, it is a discharge of the  drives within language that manifests in the rhythm and tone of the  text (and the speech of the subject). It refers to an element in  symbolic language that does not signify, the bits of psychic and bodily  energies (partial drives) that are less precise but nonetheless "speak"  of the phenomena of embodied significations through language and their  inherent limits. The symbolic is the rule-governed element of language,  grammar and syntax, that makes reference and therefore judgment at all  possible, the element of meaning associated with the very forces of  grammar and syntax.<br>
&nbsp;<br>
Kristeva became more interested in psychoanalysis and completed her  training in 1979. Her work intensified around the formation of identity  and the role abjection and Otherness play in the process. Her writings  of the 1980's include transcripts from her practice as an analyst, such  as <em>Tales of Love</em> (1983) and <em>Black Sun</em> (1987). Her 1982 publication, <em>Powers of Horror: An Essay in Abjection</em>,  describes the pre-mirror stage development of the child's entry into  the Law of the father as Lacan theorized. For Kristeva, birth itself is  a separation <em>within</em> the body, a violent separation from the  body of the mother. In the maternal body, excess gives rise to a  separation that is material and maintained by a regulation (regarding  availability of the breast) that is prior to the mirror stage. The  maternal regulation operates as a law, prefiguring and providing the  grounds of paternal Law as the entry of the child into language and  society.<br>
&nbsp;<br>
Kristeva's writings maintain this logic of an oscillation between  symbolic identity and semiotic rejection or the experience of  difference. <em>Revolution in Poetic Language and Powers of Horror</em> are focused on material maternal rejection, which prefigures signification and sets up the logic of rejection. <em>Tales of Love</em> (trans.1987) and <em>Black Sun: Depression and Melancholy</em> (trans.1989) are focused on primary narcissism, which prefigures all subsequent identity and sets up the logic of repetition. <em>Strangers to Ourselves</em> (1989) and <em>Lettre ouverte &yacute; Harlem D&eacute;sir</em> (1990) are focused on rejection or difference within identity. In  recognition of her contribution to French intellectual culture, she was  honored by the French government in 1990 and made a "chevalier de  l'ordre des arts et des lettres."</p>

                  <td class="texto2" valign="top"><p><span lang="EN">Carmen Boullosa</span><span lang="EN"> is a leading Mexican poet, novelist and playwright. Her work is  eclectic and difficult to categorize, but it generally focuses on the  issues of feminism and gender roles within a Latin American context.  She has won a number of awards for her works, and has taught at  universities such as Georgetown University, Columbia University and New  York University (NYU), as well as at universities in nearly a dozen  other countries. She is currently Distinguished Lecturer at the City  College of New York and is now married to Mike Wallace, the  Pulitzer-prize winning co-author of <em>Gotham: A History of New York City to 1898</em>.</span><br>
                      <span lang="EN"> </span>&nbsp;<br>
                      <span lang="EN">Boullosa  has written over a dozen novels, and some of these works have been  translated into five different languages. Her bestselling novel, <em>Son vacas, somos puercos</em> (1991) was translated into English in 1997 as <em>They're Cows, We're Pigs</em>.  The story is narrated in the first person by an old man looking back on  his life. He was kidnapped and sent from his native France on a slave  ship to the West Indies at the age of thirteen. To gain his freedom, he  joins a group of pirates (or "pigs"), allowing Boullosa to compare two  very different societal and political systems&mdash;traditional Europe and  carefree pirates. In <em>La milagrosa</em>, a novel written in 1993, the  protagonist is a girl who has the power to heal the sick and perform  other miracles while she sleeps. She falls in love with Aurelio  Jimenez, a detective sent to discredit her, even though she fears that  her powers will disappear if she spends time with people. It ends  ambiguously, leaving an unsolved murder without closure. <em>Duerme</em>,  another popular work published in 1995, tells the story of Claire, a  French woman whose mother was a prostitute. Attempting to escape the  same profession, she arrives in Spain dressed as a man. To save a  subject of the Spanish king, she reveals herself as a female and  prepares to take his punishment of death by hanging. Beforehand,  however, she is wounded in the left breast and her blood is replaced by  water from the lakes of Mexico City. The water's magical powers make it  possible for her to survive the punishment.</span><br>

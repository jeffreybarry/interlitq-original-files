                  <td class="texto2" valign="top"><p><span lang="EN-US">Born  in Buenos Aires in 1948, Manguel grew up in Tel-Aviv, where his father  served as the first Argentinian ambassador to Israel. At the age of  seven, when his family returned to Argentina, he became fluent in  Spanish, his first languages being English and German (which he spoke  with his governess). At sixteen years of age, while working at the  Pygmalion bookshop in Buenos Aires, he was asked by the blind Jorge  Luis Borges to read aloud to him at his home. For Manguel, the  relationship was pivotal: he read to Borges from 1964 to 1968. At that  time, he was attending the Colegio Nacional de Buenos Aires, where he  was inspired particularly by a teacher of literature whose subsequent  collaboration with the military would provoke Manguel to write his  first novel, <em>News From A Foreign Country Came</em>. Manguel left  Argentina for Europe before the horrors of the 'disappeared' began, and  just after the events of May 1968. He credits Severo Sarduy, Hector  Bianciotti, Julio Cort&aacute;zar and Genevi&egrave;ve Serreau with generously easing  his transition into living abroad and living by words. </span></p>
                    <p><span lang="EN-US">
                      In  the 1970s, Manguel lived a peripatetic life in France, England, Italy,  and Tahiti, reviewing, translating, editing, and always reading. In  1980, Manguel and Gianni Guadalupi compiled <em>The Dictionary of Imaginary Places</em>,  a comprehensive and celebratory catalogue of fantasy settings from  world literature. The publication of the book marked the beginning of  Manguel&rsquo;s long association with publisher/editor Louise Dennys. In  1983, Manguel edited the ground-breaking anthology <em>Black Water</em>: <em>The Book of Fantastic Literature</em>. Manguel moved to Toronto, Canada where he lived and raised his three children for almost twenty years.</span></p>

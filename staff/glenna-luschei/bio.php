                  <p>Born in Iowa and educated in Iowa, Missouri and Nebraska, Glenna Luschei holds degrees from the University of Nebraska where she graduated with high distinction and was tapped Phi Beta Kappa and Mortar board. She has lived most of her life in Latin America and the American West. She is now an avocado rancher in Carpinteria, California, and served as a panelist for offering aid to farmers with the USDA, also working as a translator for Spanish-speaking workers. Luschei acted as a panelist for the National Endowment for the Arts which awarded grants to writers. She completed her PhD in Hispanic Languages and Literatures from the University of California at Santa Barbara in 2005.</p>

 

<p>In 1967, Luschei moved from Colombia to Albuquerque, with her fourth child under one arm and her first book, <em>Carta al Norte</em>, under the other.  In Albuquerque, she established her <em>Solo Press</em> and the magazine <em>Café Solo</em> as an exchange with Latin American writers. </p>

 

<p>She participated in the New Mexico Poetry in the Schools, was named the D. H. Lawrence Fellow and a grantee of the Helene Wurlitzer Foundation.  Her book, <em>Thirty Songs of Dissolution</em>, was published in 1977 by San Marcos Press, Cerrillos. Her retrospective of poetry, <em>Salt Lick</em>, was published in 2009 by West End Press, Albuquerque.  Her book, <em>The Sky Is Shooting Blue Arrows</em>, was published in 2014 by the University of New Mexico Press. Her latest book, <em>Zen Duende</em>, a collaboration with Erik Greinke, was the winner of the Pushcart Prize and was published by Presa Press in 2016.</p>

 

<p>Her books of Spanish translations include those of work by Luis de Góngora y Argote and Sor Juana, Solo Press. Her Solo Press publication of Luis Aleixandre brought her and her husband Bill Horton to Aleixandre's Nobel Prize investiture during their wedding trip in 1977. Some years later, the couple moved back to Bill’s home state of North Carolina. During that time Luschei was awarded a Fortner Prize and an honorary doctorate from St. Andrews University in Laurinburg, North Carolina.</p>
                 <p> After Luschei's move to California, <em>Solo Press</em> added Solo Flight, an activities group supported by the California Arts Council and the National Endowment for the Arts, to produce Poetry and Jazz Festivals as well as book fairs. She brought New Mexico and Mexican poets into her events, including Carlos Fuentes.</p>

 

<p>Luschei has taught for Cal Poly, San Luis Obispo; UCLA Artsreach, California Men’s Colony and Atascadero State Hospital, as well as at the University of Nebraska Writer’s Conference.  She has received a National Endowment for the Arts Writer's Fellowship and many California Arts Council individual grants.</p>


<p>In 2000, she was named Poet Laureate for the City and County of San Luis Obispo.  </p>

                  <td class="texto2" valign="top"><p>
 Thomas Luschei is an associate professor in CGU’s School of Educational Studies, where he teaches courses on international comparative education, urban education in the United States, and teacher quality and teacher policy. His research interests include international and comparative education, the economics of education, teacher labor markets and teacher quality, bilingual education policy and practice, and the global applications of Colombia’s <em>Escuela Nueva</em> rural school improvement model. The primary focus of his research is the impact and availability of educational resources—particularly high-quality teachers—among economically disadvantaged children. He has conducted related research in Africa, Asia, and North, Central, and South America. In 2013, he served as a Fulbright Visiting Scholar at the Centro de Investigación y Formación en Educación at the Universidad de los Andes, Bogotá, Colombia. In 2016 he was a Fulbright Specialist at the Universidad de San Andrés, Buenos Aires, Argentina.</p>

 

<p>Dr. Luschei is the author (with Amita Chudgar) of <em>Teacher distribution in developing countries: Teachers of marginalized students in India, Mexico, and Tanzania </em>(2016, Palgrave Macmillan). His research has appeared in numerous scholarly journals, including the <em>American Educational Research Journal</em>, the <em>American Journal of Education</em>, the <em>Comparative Education Review</em>, the <em>International Journal of Educational Development</em>, <em>Education Policy Analysis Archives</em>, the <em>International Review of Education</em>, the <em>Phi Delta Kappan</em>, <em>Teachers College Record</em>, and <em>Prospects: UNESCO’s Comparative Journal of Curriculum, Learning and Assessment</em>. He serves on the editorial boards of the journals <em>Educational Researcher, Evaluation and Program Planning, Interaction Design and Architecture, Forum for International Research in Education, Journal of Teacher Education, Panorama</em>, and <em>Prospects</em>. He has been invited to present his research in Argentina, Colombia, India, Indonesia, Mexico, and South Korea. From 2011 to 2014, he served on the Board of Directors of the Comparative and International Education Society. Prior to pursuing graduate studies, he worked as a bilingual elementary, high school, and adult education teacher in the Los Angeles Unified School District, where he taught students ranging in age from six to eighty-four.</p>

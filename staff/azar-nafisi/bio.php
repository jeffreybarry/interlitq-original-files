                  <td class="texto2" valign="top"><p>Azar Nafisi is best known as the author of the national bestseller <em>Reading Lolita in Tehran: A Memoir in Books</em>,  which electrified its readers with a compassionate and often harrowing  portrait of the Islamic revolution in Iran and how it affected one  university professor and her students. Earning high acclaim and an  enthusiastic readership, <em>Reading Lolita in Tehran</em> is an  incisive exploration of the transformative powers of fiction in a world  of tyranny. The book has spent over 117 weeks on the <em>New York Times</em> bestseller list. <em>Reading Lolita in Tehran</em> has been translated in 32 languages, and has won diverse literary  awards, including the 2004 Non-fiction Book of the Year Award from <em>Booksense</em>,  the Frederic W. Ness Book Award, the 2004 Latifeh Yarsheter Book Award,  an achievement award from the American Immigration Law Foundation, as  well as being a finalist for the 2004 PEN/Martha Albrand Award for  Memoir. In 2006 she won a Persian Golden Lioness Award for literature,  presented by the World Academy of Arts, Literature, and Media.<br>
&nbsp;<br>
Azar Nafisi is a Visiting Professor and the executive director of  Cultural Conversations at the Foreign Policy Institute of Johns Hopkins  University&rsquo;s School of Advanced International Studies in Washington,  DC, where she is a professor of aesthetics, culture, and literature,  and teaches courses on the relation between culture and politics. Azar  Nafisi held a fellowship at Oxford University, teaching and conducting  a series of lectures on culture and the important role of Western  literature and culture in Iran after the revolution in 1979. She taught  at the University of Tehran, the Free Islamic University, and Allameh  Tabatabai before her return to the United States in 1997 - earning  national respect and international recognition for advocating on behalf  of Iran&rsquo;s intellectuals, youth, and especially young women. In 1981,  she was expelled from the University of Tehran for refusing to wear the  mandatory Islamic veil and did not resume teaching until 1987.<br>
&nbsp;<br>
Azar Nafisi conducted workshops in Iran for women students on the  relationship between culture and human rights; the material culled from  these workshops formed the basis of a new human rights education  curriculum. She has lectured and written extensively in English and  Persian on the political implications of literature and culture, as  well as the human rights of the Iranian women and girls and the  important role they play in the process of change for pluralism and an  open society in Iran. She has been consulted on issues related to Iran  and human rights both by the policy makers and various human rights  organizations in the US and elsewhere.& She is also involved in the  promotion of not just literacy, but of reading books with universal  literary value.<br>
&nbsp;<br>

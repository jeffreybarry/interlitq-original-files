<p>R. A. D. Ford</p>


<p>from SILENT CONVERSATIONS: A READER’S LIFE by Anthony Rudolf
(Seagull Books/Chicago University Press)</p>

<p>Robert Ford, Canadian poet, translator and diplomat, who died in 1998, had
muscular atrophy from the age of nineteen; he was not expected to live long.
Ford was the most extraordinary person I have known in my lifelong role as
a go-between. I made contact with him in 1982, during the days of my
involvement in the struggle to educate myself and, by extension, the
constituency of concerned and frightened people who had been reading the
Menard Press pamphlets about the nuclear threat, which I published between
1980 and 1985. Back then, I wrote about my three political gurus or mentors,
Sir Martin Ryle, Lord (Solly) Zuckerman and E. P. Thompson. The twenty
pamphlets included three by Ryle and two by Zuckerman. Thompson went
out of his way to publicize Ryle’s first pamphlet <em>Towards the Nuclear
Holocaust </em>as the best account of our fearful dilemma. Thanks, in part, to his
intervention, it sold fourteen or fifteen thousand copies, easily Menard’s top
selling book. </p>

<p>In my political publishing heyday, all kinds of ‘moderate’ and mainstream
people were, along with radicals, in touch with Menard, because as a poetry
publisher the press was understood to be independent of supposedly
dangerous extremists like CND—the Campaign for Nuclear Disarmament.
At one point, I was asked to attend a meeting at MI5 after a Soviet diplomat
visited me to buy pamphlets. Not long after, in a separate incident, I found
myself on the line to or from the Ministry of Defence security police, when
I had not phoned them or been phoned by them. I reported this to a privy
councillor friend, the former Housing Minister Reg Freeson, who made
enquiries. The ensuing meeting with MI5, near the British Council offices in
Trafalgar Square, was so interesting that I asked if I could come back. I made
my usual speech about loyal opposition blah-blah. I was struck how young
and pretty the intelligence officer was: a deliberate choice to disarm me? </p>

<p>By the time I met him, Robert Ford was special adviser on East–West
relations to the Canadian Prime Minister, Pierre Trudeau, having retired as
ambassador to the Soviet Union. One of Canada’s, indeed the West’s, most
important and influential diplomats since the war, Ford had sub-ambassadorial
postings in London, Sao Paulo and Moscow followed by ambassadorial stints in Colombia, Yugoslavia and Egypt, before returning to Moscow for sixteen years (1964–1980). He ended up as doyen of the diplomatic corps. Such a long stint was rare, if not unique, because of fears that ambassadors left abroad for too long may go native in the bad sense. There was no danger that Robert the diplomat would succumb, precisely because going native was the province of Robert the poet. He told me that, because of his disability, he was the only foreigner or maybe even non-member of the politburo allowed to use the lift in the Kremlin. He was, too,
the only Western diplomat to have met all Soviet leaders from Stalin to
Gorbachev, although the meeting with Stalin was more like observation at
close quarters. Robert was already in a wheelchair when I first met him. He
never complained, never admitted to pain, always behaved as if nothing was
wrong. </p>

<p>Ford and his wife Thereza, a brilliant and charming Brazilian heiress,
retired to a small castle near Vichy, where I stayed with them. They had met
in London in 1946 at the first General Assembly of the United Nations: ‘At
the opening reception in the House of Lords given by Clement Attlee . . . I
heard an attractive member of the Brazilian delegation making amusing
remarks about the Canadians. I could not resist the temptation of saying in
Portuguese that the latter was not a secret language. This led inevitably to
marriage.’ (I like his deployment of the word ‘inevitably’, with the erotics of
the interaction left entirely unspoken and buried between the words of the
short sentence.) The castle, La Poivrière, which had been the residence of the
German ambassador to Petain’s regime, was beautifully decorated with
Russian folk art, including butter dishes, gifts from their close friend Lili Brik,
the elegant and talented Jewish mistress of Mayakovsky and sister-in-law of
Louis Aragon. The Fords owned wonderful works of art, including a
Kandinsky painting on glass. Over dinner, out of the blue, Thereza said:
‘Anthony, everybody thinks Robert depends on me and will be helpless if he
outlives me, but the opposite is true. I need him more than he needs me. I
want to die first.’ I looked at him in expectation of some kind of reaction,
but he sat there impassive, like the Buddha or a rocky mountain (goat) or a
figure in a Poussin painting. Like Pope’s Gainsborough, he was ‘a man who
had a rare capacity / to snatch a grace beyond the reach of art’. After a short
illness, Thereza’s ambition was fulfilled in 1983: Robert outlived her,
producing sad true lyrics to her memory and, as such, to his own memory. </p>

<p>I went up to my bedroom and opened my suitcase. It was empty. I was
perplexed until it dawned on me that one of the two servants must have put
my stuff away—and there the gear was, in the en suite bathroom and in the
wardrobe. The next morning, after breakfast, we faced each other in the
sitting room and Robert, notebook and pen at the ready, asked me about the
peace movement and whether it was infiltrated by the Russians (yes, I said,
and infiltrated by the Americans and the British too) and the politics of
disarmament. I spoke honestly and carefully, perhaps for fifteen minutes.
Thank you, he said, I shall be writing a memo to Pierre Trudeau. That felt
good: for almost the only time in my life I had a direct if minuscule input
into politics at the highest level, although a few indirect inputs—for example,
Martin Ryle’s pamphlet was the subject of a widely circulated unofficial
refutation by the Ministry of Defence—suggested that Menard must have
been doing something right. One other direct and verifiable input: I sent a
phrase I coined (‘unilateral nuclear rearmament’) to Neil Kinnock, then leader
of the opposition, enabling him to use it in a speech and briefly turn the table
on the Tories, since ‘unilateral disarmers’ was their term of abuse for CND
and its supporters. Although the great campaign was a coalition of nuclear
disarmers, unilateral and multilateral, the unilateral aspect was always and
mistakenly to the forefront. When Kinnock went to Moscow he already knew
about Byron’s poem ‘Darkness’ which was quoted at him by Russians, because
he had a copy of my Menard pamphlet about the circumstances of the poem’s
writing. </p>
<p>Over the years, in correspondence and on the phone, Ford and I
continued discussing security issues, Israel (which fascinated him and
generated many questions), poetry (he gave me his books as listed in the
bibliography) and translation. Although not a reactionary and not a
professional cold warrior, he was conservative, but on the nuclear issue he,
like Zuckerman, had no doubt that a new approach was needed. Thompson
and Ryle were more radical than Ford and Zuckerman. I was privately torn,
settling eventually on the ‘freeze’ as common ground between unilateralists
and multilateralists (the freeze was a position which many members of both
camps, wrongly, rejected), which belongs in a political book I shan’t be
writing. Zuckerman, Ryle, Thompson and Ford were highly cultivated men,
influential in their fields, and immensely powerful personalities of a kind I
have been drawn to, if not actually sought out. They were all married to
remarkable women, who matched them in depth and range. Long marriages
between equals interest me, but this too is a topic for another occasion and,
perhaps, a different genre, namely fiction. Robert Ford was the only one of
the four with whom I had a literary as well as a political dialogue, and that
brought me close to him, although Martin Ryle too was a good friend. On
the other hand, Solly Zuckerman and Edward Thompson were my colleagues
in a common cause and friendly acquaintances, much as I would like to claim
them as friends. </p>

<p>In 1983, I arranged three events for Ford in London: the first was a
lecture at the now-defunct GB–USSR Association, then run by my old friend
John Roberts. The meeting was chaired by the former foreign secretary, David
Owen, Ford’s colleague on the Olaf Palme Commission. Ford’s UK buddies,
retired top diplomats, showed up. Earlier that day, the Canadian ambassador
had made a lunch for him. All I remember, thanks perhaps to good wine,
was the presence of Carmen Callil and John Mortimer. The other events were
a poetry reading at Canada House and a Russian translation reading at the
old Poetry Society in Earls Court. Robert Ford was a meditative poet, hibernal
rather than aestival; Norwegian rather than Italian, if you like. Perhaps this
is a roundabout way of saying the matrix of his poetry is Canada, which, after
all, is situated in North North America, the Arctic and Russia not that far
away. Ford himself said that the ‘Russian landscape, so much like that of the
Ottawa Valley, has been an important factor in shaping my verse.’ The firs,
the pines, the birches, the snow reminded him of Canada, of home. Naturally,
he translated some of the Zhivago poems. Autumn, however, is different,
greyer and more melancholy in Russia than in Canada. Two Canadian poets,
friends of mine, Sharon Nelson and Seymour Mayne, tell me Ford is no
longer on the literary radar in their country, though Mayne himself is
personally an admirer, and his wife Sharon Katz drew portraits of Russian
poets for Ford’s personal and beautifully translated selection, Russian Poetry. </p>

<p>In 1989, Ford sent me his book <em>Dostoevsky and Other Poems</em>. My thank you letter, preserved in a carbon copy, speaks rather formally of his ‘austerity
of diction, the edgy and disturbed rhythms, the way the syntax plays against
the lexicon: all the registers serving a vision where intelligence of the heart
doubles the heart of intelligence’. After a conference in Arles in 1997, which
brought together the translators of Yves Bonnefoy from across the globe, I
returned to Paris via Robert’s house for what we both knew would be our last
meeting. Now virtually paralysed by his lifelong condition, worsened by a
stroke, he greeted me from bed with a movement of his little finger. By then
the childless widower had moved with his household, a long service and
devoted Portuguese couple, and possessions, from his small chateau, to a
lovely house in the castle’s village, Saint-Sylvestre Pragoulin. With hindsight,
I now read the scene at the bedside, especially when the manservant brought
tea, as a gloss on Paula Rego’s painting ‘The Family’. How I wanted to free
him from his affliction!, just as the children in her painting are trying to raise
the male figure from near death—the original title of the painting was,
indeed, ‘Lazarus’. I looked again at the Kandinsky painting on the wall facing
his bed. Ford told me that the painter’s estate had recently written to him,
withdrawing its certificate of authenticity. This, he said, was rubbish, and all
the experts said it was rubbish, but he was in no position to fight the estate.
I wonder what has happened to that picture, indeed to all his pictures and
his library. </p>

<p>Robert Ford’s literary friendships and activities while in the Soviet Union
were in a creatively dialectical relationship with his professional work, which
is not surprising since he was a poet and translator of distinction and a
profoundly thoughtful man of the highest intelligence. His diplomatic reports
became famous, like those of Isaiah Berlin from Washington during World
War II. The personal and political fed into each other, perhaps uniquely.
Given Canada’s significant position within the broad western coalition and
the importance of the country of his final posting, he is certainly the most
influential poet diplomat from any country since Saint-John Perse and George
Seferis. Nonetheless, he had no illusions concerning where main power lay,
namely with Canada’s North American neighbour, and that Canada’s scope
for independent initiatives was limited. According to one obituary, when
George Schulz became Ronald Reagan’s Secretary of State in the USA, he
sought a briefing from Ford. </p>

<p>I do not claim that Ford is a poet in the league of those other professional
diplomat poets, Perse, Paz, Neruda and Seferis—how many poets are?—but
his elegiac oeuvre, though small and narrowly focused, is distinctive and, to
my ear, beautiful. However, his understanding of the psyches and mentalities
of individuals in the elites, political and artistic, of the country where he was
posted, influenced his diplomacy more than it influenced his literary works,
unlike Paz, one of whose most important books is <em>Le Singe grammarien, The
Monkey Grammarian</em>, which could not have been written without his
profound experience of India. </p>

<p>Here on my desk is the first of Ford’s two memoirs: <em>Our Man in Moscow</em>
(1989, with a cover photo of him and Leonid Brezhnev) and the French
edition <em>Diplomate et Poète à Moscou</em> (1990, sporting a photo of him with
Andrei Voznesensky). These photographs of Ford as a young and middleaged
man reveal a handsome, elegant, self-confident and self-possessed
personality, reminding me of Clark Gable or Anthony Eden, when he was
Foreign Secretary during the war. Although Robert came across as a reserved
and highly cerebral man, classically opposite to his passionate and extravert
Latin wife, the passion was there beneath the armour of the intense discipline
of a man schooled in illness and diplomacy. In private, he always spoke his
mind, and from the heart. He held strong views, and could be aroused to
sharpness when one was naive or stupid, but he also wanted to learn from
those, including myself, with areas of knowledge beyond his specialities.
Reading these two memoirs, we realize that the diplomat was accredited to
the Soviet Union, a country with many nationalities and literatures, whereas
the poet was accredited to Russia, her literature, her poetry, her soul, as
becomes clear in <em>A Moscow Literary Memoir</em> (1995), which tells of his life
‘among the great artists of Russia from 1946 to 1980’. Here he is off-duty,
and not constrained by the responsibilities of the day job, although in that
city, and in a country, as Mandelstam said, where poetry is so important the
poet is sometimes killed, you could never entirely divorce art and politics,
which is one of the major themes of this valuable record of a passion and a
time. It is not, however, an ambitious literary work, in part because it had to
be written and edited in the difficult circumstances of his final years. </p>

<p>Although Ford lived on until late in the presidency of Boris Yeltsin
(whose commitment to democracy he doubted), <em>Our Man in Moscow </em>was
written during the high optimism of Gorbachev’s rule: for my money Mikhail
Gorbachev, even more than Nelson Mandela, is the man of the (twentieth)
century, since he managed the change, <em>perestroika and glasnost</em>, in such a way
that war, nuclear war, was avoided. Despite the optimism of the times, and
despite Ford’s admiration and respect for Gorbachev, whom he saw as a
Kerensky figure, Robert Ford appears to agree with Vasily Grossman: ‘When
will Russia ever be free? Perhaps never.’ As I said, he had gone native, not in
the negative sense that requires ambassadors to move on before they succumb
but in the Keatsian sense of negative capability. He had become the poets he
loved, <em>echt Russisch</em>, one who knows that the Russian soul is something unique
unto itself, like the Russian language praised by Ivan Turgenev in the prose poem I translated (see below). Although Ford writes that the proposition was and
is debatable, part of him agreed with the poet Fyodor Tyutchev whom he
quotes, in his own translation, at the end of the memoir:<br><br>

Russian cannot be understood<br>
Only with the mind—no normal<br>
Standard can judge her<br>
Greatness. She stands alone, unique. <br>
In Russia one must only believe. <br><br>

Ford died before George Bush and the neoconservatives came to power,
and before we could have a conversation about my considered view that the
project called democracy was in fact a project to promote capitalism, and
deregulated capitalism at that. In his early days as Foreign Secretary, David
Miliband, in a speech timed to second-guess or echo President Obama, struck
me as naive in his unreconstructed promotion, if necessary by military means,
of the admirable and essential concept called democracy, naive about its
relationship to economics and climate change. What does Great Britain look
like to normal Europeans like the Spanish, the Portuguese, the Italians, the
Germans, the French, who managed to dispose of their imperial pretensions
along with their dictatorships? NATO and the USA (pre-Obama at any rate)
have not been straight in their dealings with Russia. When will we (and
Putin’s Russia too) learn that security in the modern world is not a zero-sum
game? Ford predicted in the mid-1990s that Greater Russian nationalism
would re-emerge, and that is surely correct.</p>

<p>Ford’s literary recollections, as he says, put the flesh on the skeleton of
the country portrayed in the political memoir. <em>A Moscow Literary Memoir</em>
tells of Ford’s friendships with the poets of the ‘thaw’: his closest friend Andrei
Voznesensky, Yevgeny Yevtushenko, Bella Akhmadulina, the prose writers
Yuri Nagibin and Konstantin Simonov, the ballerina Maya Plisetskaya
(‘Plisetskaya is the Tsvetaeva of ballet,’ wrote Voznesensky), Nina Kandinsky,
the widow of the painter, and of course Robert and Thereza’s dearest friend,
Lili Brik. ‘The inner tug of war between the eastern Slavophile and the
western liberal is the eternal conflict in Russia . . . The whole of Russia today
is an epic drama by Dostoevsky, being played out before our very eyes’.</p>

<p>There is a wonderful photograph of Brik with Mayakovsky, Pasternak
and, am I imagining it, a haunted-looking Eisenstein, taken in St Petersburg
in 1924. Lili looks very elegant in her cloche hat, reminding one that Lenin
had only just died and Stalin was not yet fully in control. She explains to the
Fords that her marriage to Osip Brik became platonic as the ménage à trois
with Mayakovsky gathered apace. At a dinner party with the Fords and
Voznesensky, she says: ‘I have no objection to falsehoods, but there is a big
distance between honest lying and dishonest lying.’ As for Pasternak, she
much preferred his poetry and The <em>Childhood of Lyuvers</em> to <em>Doctor Zhivago</em>,
and Ford agreed with her. On Pasternak and Akhmatova, Ford writes that
they [like Robert Lowell] appear to put his own art above other people’s lives,
namely Olga (the model for Lara in Doctor Zhivago) and Akhmatova’s son,
Lev. ‘It is an anguished question,’ writes Ford. He and Thereza visited
Pasternak’s grave in Peredelkino, prompting a poem, ‘The Grave of Pasternak’: <br><br>

The wind withers<br>
At the windows of the voices, <br>
Tapping gently, then lost<br>
In the infinite space. <br>
The snow is convent pure. <br>
In the first light<br>
Of Peredelkino<br>
He lies alone with one<br>
Paper flower, faded, wet, <br>
Staining the snow. <br>
The voices can be heard again. <br>
And the windows reopen. <br><br>

I take my farewell of Robert with the memory that I wanted to hug him when
it was time to go, but didn’t, knowing it would embarrass him. I held his
finger for a moment and left with tears in my eyes. He knew what he meant
to me. Here is his translation of the final stanza of Yesenin’s poem on death: <br><br>

I know in that other country<br>
There are no fields golden in the haze. <br>
That is why I cherish those who have<br>
Spent on earth with me their days. <br><br><br><Br></p>


<p><em>The Russian Tongue</em><br><BR>

In days of doubt, in days of painful reflections on the various fates<bR>
of my homeland, you alone are my support and mainstay, oh great, <br>
strong, true and free Russian tongue! How can one not fall into<br>
despair, seeing everything that is being done at home? But it is<br>
impossible to believe that such a language was not given to a great<br>
people! <br><br></p>






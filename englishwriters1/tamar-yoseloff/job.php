<p><strong>After the Storm</strong><br><br>

<em>An Anatomy of Melancholy</em>, Hughie O’Donoghue<bR> <br>


At daybreak cars crept along the main road<br>
out of town, luggage racks piled <br>
with huddled bundles. <br><br>

Those of us who stayed <br>
stockpiled batteries, bottled water, canned beans. <br><br>

The streets emptied of everything but heat. <br>
A couple of storm chasers<br>
cruised in like they owned the place; <br>
one of them would die years later in Texas, <br>
the tornado whisking him off the ground like a leaf. <br><br>

When it finally arrived, its black hand tore <br>
through whole fields, tossed aside houses and trees, <br>
grabbed the sun in its fist. <br><br>

I always believed each morning<br>
I’d wake, each day would bring <br>
some new joy.  <br>
                    &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;       I survived<br>
but now fear squats in my heart <br>
ready to fling me to the sky. <br><br><br><br>

<strong>Insomnia</strong><br><br>

The clock’s small hands carry me <br>
to the house where I was a child<br><br>

and in those rooms peace settled<br>
despite the war inside the TV, <br>
the lunatic with an axe who filled <br>
the drive-in screen. My parents said<br>
no harm would come if I was good. <br><br>

My room was buttercup yellow, <br>
it was always spring; my Barbies<br>
were missing arms or legs, while boys <br>
came back from Nam in bags. <br>
Everyone seemed old but they were young,<br><br>

now everyone seems young, and I’m the one <br>
crowding the night with phantoms. <br><br><br><br>



<strong>The Little Ice Age</strong><br><br>

The freeze that put trees to sleep<br>
shrouded steeples, cast drunkards <br>
who fell short of their beds <br>
in marble effigies. A quicksilver city <br>
crowned the solid river, mansions <br>
without foundations. <br><br>

The frost made merry sport: <br>
skaters coupled with their reflections, <br>
children and dogs yelped with glee<br>
at their foolish legs. The people were dazzled, <br>
each step lively and dangerous. <br><br>

Some heard the low rumble<br>
trebling to a cry, saw the shatter webbing <br>
the surface, and, before the man could flee, <br>
the surge of break widened at his feet, <br>
pulling him under the icy sheet, <br><br>
	
but most did not, content to live<br>
in the suspension between freeze<br>
and thaw, easy in their play. No one <br>
wished for spring, with its sentimental <br>
blooms and twittering birds; <br>
the strange and fatal fevers<br>
carried on the first warm breeze.  <br><br><br><br></p>







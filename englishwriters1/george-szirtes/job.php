<p><strong>What we talk about when we talk about talking</strong><br><Br>

Some talk about things, <Br>
some talk about other things, <Br>
some about people. <Br><Br>

Some don't talk at all<Br>
but pull faces and pretend<Br>
to listen not talk. <Br><Br>

Some are trapped inside<Br>
an empty conversation<Br>
and can't quite get out. <Br><Br>

Social intercourse<Br>
as hell?  Other people have<Br>
devils of their own. <Br><Br>

See that young man there? <Br>
Watch his mouth move. Watch his hands<Br>
as he tries to speak. <Br><Br>

Something must be said<Br>
but it escapes him before<Br>
he knows what it is. <Br><Br>

Something fugitive<Br>
is in the forsaken air<Br>
before its saying. <Br><Br>

Pity the speechless<Br>
with their gesticulation <Br>
and their stiff white lips. <Br><Br>

Where's the oxygen<Br>
of publicity now? Choked<Br>
off. Gone for a walk. <Br><Br>

Some talk about things. <Br>
Some don't talk at all. Talking<Br>
is this. This is talk. <Br><Br><Br><Br>


<strong>Charge Sheet</strong><Br><Br>

That is not a man. <Br>
That is a thinking machine<Br>
with eyes and fingers. <Br><Br>

Look at him working. <Br>
He's doing nothing useful. <Br>
Those are only words. <Br><Br>

Do words shift the load<Br>
hanging by a thread or turn<Br>
a key in the door? <Br><Br>

Do words perform tasks<Br>
that address the rain and clouds? <Br>
Some of us doubt it. <Br><Br>

Words have no substance. <Br>
They hang around street corners, <Br>
menacing shadows<Br><Br>

waiting for something<Br>
to happen so they may spread<Br>
like an infection.<Br><Br>

They take up the air<Br>
the useful need to function. <Br>
We do not need them. <Br><Br>

They should let us be<Br>
and amuse themselves elsewhere<Br>
on some distant moon. <Br><Br>

The doors are open. <Br>
Out there is light and silence. <Br>
Let the silence come. <Br><Br><Br><Br>


<strong>A Quartet from Finland</strong><Br><Br>

What is the music<Br>
weaving the night together<Br>
like a small spider? <Br><Br>

It wriggles this way<Br>
and that so delicately, <Br>
scuttling up and down, <Br><Br>

you would think it had<Br>
an agenda, some design<Br>
beyond construction<Br><Br>

transcending hunger, <Br>
when all it looks to achieve<Br>
is to build a web<Br><Br>

and then shimmy up<Br>
and down it and to sit there<Br>
at its frail centre. <Br><Br>

Eric Tulindberg, <Br>
you slender eight-legged slip<Br>
of a dead spider, <Br><Br>

spinning through the night<Br>
in whatever Finland then<Br>
existed to spin in, <Br><Br>

are you still spinning<Br>
your death on a short silk thread, <Br>
the draught still blowing<Br><Br>

where it hangs and sways<Br>
while I wake from my own web<Br>
spun out of nowhere? <Br><Br>

Is this my morning<Br>
or your night? Is it the two<Br>
we're both stuck between? <Br><Br><Br><Br>



<strong>Cargo</strong><Br><Br>

Consider the drowned<Br>
packing the sea and rising<Br>
like a dank mountain. <Br><Br>

Crowding the water, <Br>
packed close like cargo, the drowned<Br>
vanish unlisted. <Br><Br>

How deep the sea is, <Br>
how fierce and cold, untroubled<Br>
by its history. <Br><Br>

We have history<Br>
in which we drown our sorrows<Br>
as in saltwater. <Br><Br>

We don't understand<Br>
death in the way the sea does. <Br>
We set out in hope. <Br><Br>

Now we lie, piled up, <Br>
as if we were intended<Br>
to be together. <Br><Br>

But nothing is meant. <Br>
The sea does not bear meaning. <Br>
It is just a throat. <Br><Br>

We too have our throats<Br>
but they are filled with water<Br>
and grief and money. <Br><Br>

Those who ferry us<Br>
betray us. We can't trust them<Br>
but rely on them. <Br><Br>

You will recall us<Br>
in your private drowned moments. <Br>
You will recall us. <Br><Br><Br><Br>


<strong>Meet Harpo</strong><Br><Br>

Talking to Groucho<Br>
wasn't easy. He would roll<Br>
his big eyes and sneer. <Br><Br>

Talking to Chico<Br>
was no easier. His fake<Br>
accent took over. <Br><Br>

Talking to Harpo<Br>
was like talking to a blast<Br>
of angry white air. <Br><Br>

They were familiars<Br>
not people. I knew them all<Br>
as shapes in dead fire. <Br><Br>

Above all Harpo, <Br>
explosive, a child with fits<Br>
of terrible greed. <Br><Br>

Outside in the storm<Br>
the brothers were blown along<Br>
ravines and high seas. <Br><Br>

Everyone scattered<Br>
in the wind. It was wartime. <Br>
It was rationing. <Br><Br>

Dread sister, Laughter, <Br>
have you no care for brothers<Br>
of thunderous talk? <Br><Br>

Beware the silent<Br>
speechifier in the wig. <Br>
Cross your legs. Fight back. <Br><Br><Br><Br></p>





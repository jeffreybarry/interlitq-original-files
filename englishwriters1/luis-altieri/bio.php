<p>1990 / 1996  Miembro de AAPY (Asociación Argentina de Profesores de Yoga) tomando diferentes cursos de formación complementaria con el Yoga. </p>
<p>Formación en “Vidya Mandir”, escuela de Dakshina Tantra con el Profesor Paulo Murilo Rosas, en Rio de Janeiro, Brasil. </p>
<p>Formación en Iyengar Yoga con los profesores Fernando Calviño y Marina Chaselon, en Buenos Aires. </p>

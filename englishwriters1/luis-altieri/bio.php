                  <p>Luis Altieri nace en Buenos Aires el 9 de Julio de 1962.</p>
<p>Ha realizado innumerables exposiciones colectivas en el país y en el exterior y cerca de treinta muestras individuales incluyendo una  itinerante por gran parte de la provincia de Buenos Aires durante el año 2007, recorriendo museos y centros culturales. </p>

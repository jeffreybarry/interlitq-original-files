<p><strong>June day in Colyton</strong><br><br>
<em>For Jan Dean</em><br><br>
On this duck-egg blue morning<br>
even seagulls keep their voices down. <br>
The whirr of pigeons’ wings ruffles the air. <br><br>

We stroll sloping streets, gazing<br>
as summer flowers punch colour on walls<br>
white as china or oatmeal antique linen. <br><br>

Butterflies flicker, making no sound <br>
As they tip wings from bloom to bloom. <br> 
No need for conversation between us, <br><br>

we match step for step, dreamily. <br>
Each house has its own story to tell, <br>
each door keeps its own secrets. <br><br><br><br>


<strong>Deferment</strong><br><br>
Grief is a cruel handbag -<br>
its catch snaps shut like jaws. <br>
Inside is buried an old compact, <br>
hankie embroidered with an M<br>
in a huddle of forget-me-nots. <br>
There’s a used-up biro, one cherry lipstick, <br>
a purse stained from long-dead hands, <br>
inside only a few pence, a stamp. <br>
The handbag is a stomach<br>
digesting the past. What can be <br>
done with it? It cannot be thrown away. <br>
Best hide it in the bottom of the wardrobe<br>
an unexploded bomb. <br><br><br><br></p>






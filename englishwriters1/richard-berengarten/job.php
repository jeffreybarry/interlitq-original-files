<P>RICHARD BERENGARTEN</P>

<p><strong>Dwelling ~ for the <em>Shekhinah</em></strong></p>

<p>In  this sequence of ten sonnets, ‘Dwelling’ is associated with the <em>Shekhinah</em> (Hebrew שכינה), which in the Kabbalah signifies the manifestation of divinity in feminine aspect. This word derives from the Hebrew verb שכן [<em>shakan</em>], ‘to settle, inhabit, dwell, in-dwell, abide’. See for example <em>Exodus</em> 40:35: “And Moses was not able to enter into the Tent of the Congregation, because the cloud abode [<em>shakan</em>] thereon, and the glory of the Lord filled the Tabernacle.” The word for ‘tabernacle' itself, משכן [<em>mishkan</em>], ‘residence, dwelling place’), is cognate. In my writings, the <em>Shekhinah</em> is associated both with Gerard Manley Hopkins’s conception of <em>inscape</em> and <em>instress</em>, and with our English words <em>radiance</em> and <em>glory</em>. The last of these words is cognate with <em>clear</em>. At once immanent and transcendental, the <em>Shekhinah</em> is often ‘happened upon’ (discovered, recovered, uncovered) among ephemeral qualities and effects of light and sound that appear to inhere in (settle on, cling to, attach to, belong to) more substantial phenomena. This sequence forms the first part of a book entitled <em>Notness</em>, which consists of one hundred ‘metaphysical’ sonnets. It will be published by Shearsman Books in the spring of 2015. This title is an anagram of <em>Sonnets</em>.<br><br></p>  

<p><strong><em>Moon over sea</em></strong></p>


<p>Times when joy’s so full I feel I could burst –<br>
when in fact ‘I’ does burst: explodes in thous-<br>
ands of connecting splinters, the way those<br>
moonflecks spill and ripple tide-wide waves. Best<br>
then never swell to encompass this beast<br>
(many-faced) identity. Rather with these<br>
phantasms, let all fail, flake, fly. Since all withers<br>
eventually, why flinch, fluster, flail, wail, boast? <br>
Catch joys rather in their moments of disappearing<br>
into unthinking, únthought, thought’s entire<br>
notness – past fellow feeling, past fearing<br>
of falling apart, past loss, past past desire –<br>
and never mind their melting or those searing<br>
yellow and blue flames vaulting in black fire. <br><br></p>




<p><em><strong>Home</strong></em></p>


<p>Gift to my heart, my soul’s hostess and home, <br>
interior, bless’d before time ever was, <br>
before <em>wherefores</em> and <em>whys</em>, before <em>because</em>, <br>
temple of skies with starred or clouded dome; <br>
treasure so quickly gone I cannot wait<br>
when loss sets in with its cold stony grin<br>
and occupies all space that’s left within –<br>
useless I call, <em>Gone, gone</em>, but call too late  <br>
through absence to scry miserable fate. <br>
Yet home is this and you whatever this<br>
in presence proffers to my innerness –<br>
you guest, you gust of wind, you swinging gate –<br>
with you and this now how could I not still<br>
be sure that I belonged in miracle? <br><br></p> 
<p><em><strong>Siesta</strong></em></p>


<p>Where does your skin, or mine, begin or end? <br>
Stilled in the wake of storms we woke and spurred, <br>
my borders, lying next to yours, float blurred<br>
among these waves we failed to tire or spend. <br>
Through hand-clasp, elbow-crook, hip-fold, knee-bend, <br>
you wrapped me ín you as our passion stirred<br>
(and through each other’s passions, more incurred. <br>
And still our bodies merge. Our beings blend. <br>
What shall be said then (rich loss? faint <em>tristesse?</em>) <br>
when what we know is this calm tenderness? <br>
How sudden separation is – and yet <br>
now, as we sleep-wake, cool net curtains let<br>
kind breeze in from this sunny afternoon. <br>
Relishing this, we’ll shower, go out soon. <br><br></p>





<p><em><strong>Night bathing, Aegean</strong></em></p>


<p>On the beach we strip, watching pin-eyed ships<br>
flicker through dark. Nor shall we wait to hide<br>
our nakedness, but running, cast aside<br>
warm sun-bronzed skins of daylight’s swimming dips. <br>
Now you are all moons, ovals in ellipse<br>
orbiting me. Pale, glistening, purified, <br>
slow we come in to land, lapped by the tide –<br>
to suck out salt from <em>Aphrodite’s</em> lips. <br>
Listen, love, to the waves. Hump-backed, they’re hauling<br>
nets of black light around the shores of Greece. <br>
A creaking-timbered moon, full sailed, is trawling<br>
this August night for shooting stars, its fleece; <br>
while, curled up in their wake, someone lies calling<br>
small white cries, like a seagull, for release. <br><br></p>

 
<p><em><strong>Though numbed by passing</strong></em></p>


<p>Though numbed by passing and surpassing fear<br>
and bound to <em>being</em> on this trembling ground<br>
I stand on all the while you spin me round<br>
the axis of a passion or a year, <br>
giddied I listen, having no choice but hear <br>
your song composed of noteless silent sound, <br>
as if unhemmed, and your whole nature crowned<br>
in glints, caught up in waves, now blurred, now near. <br>
Over the waters, shimmering, a face<br>
I’ve half thought yours appears to smile and call <br>
and back I call, Time, come, I am the space<br>
you long to lodge in, and take over all<br>
its darkest corners from, and light in grace, <br>
unsure if still I stumble, rise, or fall. <br><br></p>




<p><em><strong>True to your absence, glory</strong></em></p>


<p>Is glory in the residue, mere evidence, <br>
in shining track, in afterglow, in spoor? <br>
Being too poor to meet you in your residence<br>
I plead with glory, greet me at your door, <br>
fully resplendent, present, now, revealed<br>
hostlike to your <em>main tenant</em> in this space. <br>
But you come always partially concealed<br>
in mist, with indistinctly profiled face<br>
hanging in haze, ghostlike. We shall remain<br>
true to your absence, glory, seeing you are<br>
bright only as a long exploded star, <br>
a mote in darkness, spreading like a stain, <br>
present but in the shrinking and the swelling, <br>
their course in timespace, and their aftertelling. <br><br></p>



<p><em><strong>Insomniac presence</strong></em></p>


<p>To wake up, and to be – being wide awake –<br>
are different. The first calls <em>dawn, arising,</em> <br>
a first sun pouring light across the lake, <br>
brilliance for seeing through, not analysing, <br>
Night, sinking fast, a drowning wreck, capsizing<br>
under the ghosthood of its foamless wake, <br>
gives way, itself away, all compromising, <br>
and brittle vials of dark expand and break. <br>
But I dream of a being that can’t sleep<br>
whose constant state is steadily aware<br>
of all that is and can be, anywhere. <br>
Insomniac presence, missing you, I weep, <br>   
denied in thought-knots as I watch and keep<br>
calling for you, on you, who are not there. <br><br></p>





<p><em><strong>Radiance, palpable</strong></em></p>


<p>Time is a chance we cannot choose but take. <br>
Outside it, from this world at least, the odds<br>
are nought to zero. Push our luck and break<br>
rank from all other runners, not being gods? <br>
If we’d been brought to life through play or mime<br>
into some scarcely recognizable vast<br>
non-time, un-time, anterior to time, <br>
in which the very pastness of the past<br>
had turned (or has, or even will have turned) <br>
into radiance, palpable – to a glory<br>
so overspilling presence that the burned<br>
disintegrating day-ends of our story<br>
dissolved – then, might we really take our chance<br>
to be, outside of being, in that radiance? <br><br></p>
 
<p><em><strong>This scintillating night</strong></em></p>


<p>This scintillating night is full of subtlest<br>
variations of light. They interlace<br>
then cancel, like expressions on a face, <br> 
yours, in this case. How curiously we’re blessed, <br>
witnessing this, as if being called, addressed<br>
directly, on this huge-dimensioned base<br>
by all the puzzling splendour of time-space<br>
that placement and momentum might attest. <br>
So let’s be true, then, to each other’s love, <br>
though what we’d track in passing can’t be touched –<br>
scarcely discerned – in those far heights above; <br>
and though we pass like shoals of arrows clutched<br>
by wind in soaring flight and lightning-flecked<br>
in sentient transience here all things connect. <br><br></p>





<p><em><strong>Soul of my soul</strong></em></p>


<p>Soul of my soul, my soul’s inner retreat<br>
and nucleus, you still innermost space<br>
that occupy no space yet light her face<br>
in glance of recognition when we meet –<br>
you, instant commonplace on way or street<br>
as stone but quite untouchable in place<br>
being her possessionless pure grace<br>
and miracle – perfectly incomplete<br>
for being instantaneous, lacking name, <br>
beginningless, unpassing, without end –<br>
movement through leaves, sensed radiance and sheen<br>
in all things, yet yourself always unseen –<br>
in me be present yet and through me send<br>
breath, spirit, ghost, and ecstasy of flame. <br><br></p>








<p><strong>Hot Vents</strong><br><br>

sky pours down<br>
sunless tonnage<br><br>

six-rayed souls<br>
wallow and suck <br><br>

your cold seeps<br>
greedy for marrow<br><br>

horned spiked grinder<br>
of holdfast, homescar<br><br>

prised loose <br>
fine grains, lost bearings <br><br>

soft underparts fall open<br>
labial rifts <br><br>

inkwater fills from dark to light<br>
tributaries multiply<br><br>

stone forests <br>
scoured-out gorges<br><br>

worm-forms writhe at the hot vents<br>
ten thousand deep<br><br>

worlds and stars<br>
beyond burnishing<br><br><br></p>



<p><strong>Handspeak</strong><br>
for Luljeta Leshanaku<br><br><br>



The deaf man makes words with his hands, <br>
each hard and straight as a varnished bench, <br>
builds sentences the way men build clocks<br>
or bicycles, loving and exact. <br><br>
	
<em>Meaning is real, he says, it hangs in the air<br>
and holds its shape. <br>
Each word depends for its life upon its brother. <br><br></em>

Today I thought of you<br>
in the same breath. <br>
For you, too, words are hard-won. <br>
They taste of mineral rain, of loneliness, of smoke. <br>
Stubborn, they refuse to yield. <br><br>

Monumental, womanly, <br>
they embed themselves in my body. <br><br><br></p>






<p>By Neil Langdon Inglis<br>
U.S. General Editor, Interlitq</p>


<p>Brian St. John Inglis (1916-1993) was an admired public figure with a reputation for intellectual seriousness. As the sixties advanced, his discomfort with fame grew and his desire to return to books and journalism propelled him into new and unsuspected fields.  Parapsychology, much in vogue in the 1970s, offered a congenial home for Inglis—yet it was a refuge under assault from debunkers, including CSICOP (the Committee for the Scientific Investigation of Claims of the Paranormal) in the USA. Convinced that the best defense lay in attack, Brian concluded that only a massive and scholarly history of the paranormal would win the war of public and scholarly opinion. <em>Natural and Supernatural</em> (1978) and its sequel, <Em>Science and Parascience </em>(1984), were the end-products of a period in which Brian engaged in tenacious research, and considerable personal development, as he launched into the fray. </p>

<p>His work on a variety of subjects, crafted in his memorably disciplined prose style, has held up well. Today I turn the spotlight onto <em>Science and Parascience</em> which, along with its prequel, has been reissued for a new generation. The 1984 book concerns us especially because in it, we see how Inglis the polemicist got the better of Inglis the dispassionate researcher.</p>

<p>Inglis was (and to some extent remains) best known as a TV presenter where, during a decade at the helm of Granada Television’s wartime retrospective <em>All Our Yesterdays </em>(1962-73), he set the standard for documentary journalism. Note, moreover, that <em>All Our Yesterdays</em> was a popular success—often at #2 in the ratings, just behind <em>Coronation Street</em>—with the result that Inglis became a celebrity. This was a role that caused him some awkwardness. </p>

<p>Inglis’s television duties allowed him time to write books, typically works of academic history (two from this period are <em>Abdication</em> (1966), and <em>Poverty and the Industrial Revolution</em> (1971). </p>

<p>When the Israeli spoonbender Uri Geller emerged on the UK scene in the early 1970s (David Dimbleby’s <em>Talk-In</em>, BBC, November 1973), the media frenzy ushered in the modern era of parapsychology, triggering intense interest in the supernatural in the media and among scholars and the general public. Academic research projects, television documentaries, and a flood of books on the subject, helped set the tone, much of it credulous, for media coverage of psi issues during this time.  </p>

<p><em>Natural and Supernatural </em>  and <em>Science and Parascience</em>   exemplify Inglis’s early psi period in the sense that he is concerned with hard-core manifestations of the paranormal. Inglis is full of relish when writing about <em>pseudopods </em>and <em>apports</em>, as in the sections on Eusapia Palladino. And when the author speaks of levitations (as in the chapters on levitator Daniel Dunglas Home), his prose soars along with his subject. These are events so shocking to human perception that—in theory, at least—they must compel belief in the eye-witness. Inglis’s interest in serendipity and coincidence developed later—and reflects a mellowing in his approach, for even the staunchest skeptics have experienced inexplicable coincidences. Readers interested in the latter topic are directed to <em>Coincidence: A Matter of Chance—or Synchronicity?</em>   originally published in 1990 and now likewise re-issued in paper copy (and new media formats) by White Crow Books (headed by Jonathan Beecher). </p>

<p><em>Science and Parascience</em></p>

<p>Inglis's history professor at Shrewsbury urged him to consult contradicting opinion. In his biography of Roger Casement (1973), he brought up the subject of RC's homosexuality despite much nationalist beseeching that Casement's "Black Diaries" were forgeries. A proud Irishman, Inglis (himself a staunch supporter of legalization) would have come under pressure to quash the subject, for hostility to homosexuality in Ireland was at its peak, the gay marriage referendum lay decades into the future, and RC's sexual experiences failed to fit the preferred Irish Republican perception of Casement as a Christ-like figure. Inglis makes clear, despite himself, that Casement was a loyal servant of the Crown for many years and that the Foreign Office showed him much flexibility in view of his unique abilities as an intelligence gatherer in hardship posts, until further indulgence proved impossible. All of this is good, brave historianship showing a commitment to the truth. </p>

<p><em>Roger Casement</em> was to be Inglis’s last work as a truly conventional historian. Inglis’s remarkable gifts underwent a shift in the early 1970s, as he branched out into parapsychology. His commitment to the factual record remained unbroken, and in his writings he piled on data while avoiding airy conjecture which might not age well. Yet he could not and did not avoid polemics—who, exactly, was his target? Brian had come across the classic texts of that era that challenged science’s claims to determinacy and objectivity.  Meanwhile, the reality of the psychic force was, for Brian, the great under-reported story of the age—but alas, not for orthodox scientists, who exposed psi researchers to shame and ridicule.  Inglis concluded that the rights and wrongs in this debate were starkly clear, and that the psi community must turn this shame against its adversaries, not through a neutral chronicler, but with a fiery advocate. And they found that advocate in Brian Inglis. A fight to the death was under way. </p>

<p>Was there no room for compromise? One possible attitude toward the Occult, irrespective of one’s own preconceptions, is to regard it as one of the great enigmas of human and cultural folklore. Why do people—educated people—believe in bizarre things (ghosts, UFOs, telekinesis, etc.)? The American connoisseur of the strange Charles Fort (1874-1932) argued that weird phenomena were fascinating in their own right, and his modern-day followers do not attempt to bludgeon others into acceptance. Yet Brian’s friend Uri Geller would argue "What I do is <em>real</em>", and Brian's view was that the evidence was incontrovertible and that if you ignored it, you were the one at fault and on the wrong side of history. </p>

<p>Although their paths later diverged, it seems indisputable that Geller was an influence on Brian and one (although by no means the only) trigger in Brian's career change. Yet Brian was hardly the first establishment figure to touch the hem of a famous psychic while seeking to carve out a legacy of serious, respectable research. This was a well-trodden path that others, including Conan Doyle, had followed. Brian once told my mother Ruth Inglis that he wished to leave "footsteps in the sands of time." Does his two-volume history represent such a legacy, and how well has it held up? What would Inglis have thought about developments in the field of quantum mechanics, string theory, and holographic universes, all developments with implications for action-at-a-distance, bilocation, and other phenomena of interest to parapsychogists seeking to confirm the truth of the psi force?</p>

<p>In the past, Inglis had employed an exceptional economy of language in which each sentence linked to its precursor, set forth a new idea and introduced the next sentence. The verbiage came out in force, however, whenever Inglis criticized the skeptics who disparaged the work of parapsychological researchers (and, by implication, of Inglis himself).</p>

<p>Debunkers, in Inglis's world, were afraid, closed-minded, and intellectually limited. They were “in the grip of the materialist faith,” and ever at risk of having their cozy world-view shattered ("shaken", in Inglis's coinage), and engaged in brickbat-hurling ("hatchet jobs") as an obfuscatory tactic. In <em>Science and Parascience</em> Inglis claims to expose a host of skeptics’ conspiracies to suppress the truth and mocks their lame and unconvincing explanations for occult phenomena, unfurling his own rhetorical coups de grace: "It is inconceivable that...", "it is grotesquely implausible that...", and other such put-downs. </p>

<p>Dissenting opinion is scarce. If <em>Science and Parascience</em> were your only source, you would never guess that conjurors and other skeptics have always been far shrewder at detecting fraud than Brian gave them credit for. Moreover, research and testing protocols in the "golden age" of psi had gaping holes that were efficiently exploited by fraudsters. Ultra-complex testing safeguards can be easy to circumvent, in much the same way that guerrillas can bring a technologically superior army to its knees. </p>

<p>In fairness, Inglis's treatment of Spiritualism (or Spiritism, a term he preferred) in the 19th and 20th centuries (through to WWII) is as comprehensive treatment as the subject is likely to find, for the author had read up on every publication, journal, or primary source material then available to him. Students and researchers of this topic owe it to themselves to buy and read this book. This was a life-or-death matter (or life-after-death matter) for Inglis, and he intended to make the most resounding case possible for psi. Not a single séance is overlooked, and if your appetite for Ouija boards and table levitations is unbounded, you will not be bored. Concerned that Joseph Banks Rhine of Duke University and his flash cards have been left out? Fear not! </p>

<p>There is thoroughness but also reverse snobbery at work here too; humble origins in a psychic are proof of authenticity, yet a skeptic of working-class background (as conjurors have tended to be) is fair game. Establishment scientists such as Darwin and Faraday are of interest to Inglis only to the extent that they showed any receptivity to psi—and if they didn’t, their other contributions are of lesser account. </p>

<p><em>Science and Parascience</em> is no monument to open-mindedness. Always Inglis insisted that you had to accept the record on his terms and he would stiffen with annoyance if skeptics attempted to turn the tables on him. Sarcastic rebuttals to the effect that spirit rappings and poltergeist behavior are the attention-getting behaviors of undiagnosed psychiatric patients, represent an attitude that Inglis could not comprehend and for which he had no patience. If I asked him "Where is Uri's Skylab"—by which I meant, where are the tangible scientific achievements of psychics—my father would become incandescent. In time he would argue that scientific insights (Kekulé’s dream of the carbon ring structure is the classic example) originated from inspirations comparable to the psi force. But in taking this tack, Inglis was changing the goalposts himself. </p>

<p>And what of his parental legacy, his impact on me, his son? The subject of psi interests me, but only because of Brian. He would have been bemused to learn that I am bringing his work psi to the attention of a new generation. At that point, we diverge: I am a Fortean, not a believer. Brian, in common with so many of his contemporaries, was immersed in the “human potential” ethos of the 1960s and naturally believed psi as a force that would unlock human creativity. And yet, if the psi force exists at all, there is no earthly reason for psi to be a force for good when there is so much evil in the world. I am put in mind of an <em>X-Files </em>character, the elderly psychic who can predict only one thing—the manner and time of people’s deaths. Few people will pay good money to learn of their appointment with the Grim Reaper, and publishers will not commission such manuscripts. </p>

<p>Neil Langdon Inglis</p>

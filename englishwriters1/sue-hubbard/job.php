<p>

<strong>A search for lightness against the weight of living</strong><br><br>
		&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;   <em>fantasy is a place where it rains...</em><br>
		&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;  &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;  &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;  		Calvino<br><br>

This city without a river refuses to yield up its past<br>
inscribed like lines on the palm of a hand. <br><br>

But it’s there in the trickle of the tortoise fountain, <br>
the corner of the winding street where<br><br>

washing hangs near the synagogue by a pink wall, <br>
in the reflections of the mullioned windows<br><br>

or peanut sell’s booth, <br>
those iron grills and the evening’s insistent bell; <br><br>

even in the rain drip-dripping from the purple<br>
udders of wisteria. <br><br>

Tomorrow I might go out and meet myself<br>
 in a shop window, sit in a café to eat<br><br>

black olives and bread, write in my notebook. <br>
We travel to discover who we are. <br><br>

But tonight I’ll simply undo the buttons<br>
of my white blouse, <br><br>

toss it on a chair<br>
to sleep alone beneath these old stars<br><br><br><br>




<strong>Reading the City</strong><br><br>
<em>Siena</em><br><br>

What I no longer am<br>
but might have been<br>
lies in wait for me somewhere <br>
in the heart of this ancient city. <br>
The life of that woman<br>
hanging her washing on the line<br>
above the square –<br>
her pink petticoat and green skirt –<br>
that could be mine. <br>
Shadows peel from her face. <br>
The widow’s iron balustrade, <br>
the heavy wooden door<br>
are grazed with the small scars<br>
of their own histories. <br>
My gaze scans the street<br>
as if a written page that might<br>
tell me who I am. <br>
Alley ways and stones are only<br>
what they seem. Winding lanes, <br>
and dead ends, the upholsterer’s<br>
dark interior and bookbinder’s studio<br>
are lost in the telling. Forms shift<br>
throughout the city<br>
that happens without your love.






                  <p><strong>Sue Hubbard</strong> is an award-winning poet, novelist and freelance art critic. She was twice winner of the London Writers competition and twice a Hawthornden Fellow. As the Poetry Society’s only official Public Art poet, she was responsible for London’s largest public art poem at Waterloo. She has also been awarded two residences to Yaddo, USA, and was as a recipient of a major Arts Council Literary Award for her novel, <em>Girl in White</em>. She has also been awarded residencies at the Tyrone Guthrie Centre and Cill Rialaig, Ireland and the Institute of Art in Siena. Her poems have been broadcast on Radio 3 and 4. </p>

<p>She has published 3 collections of poetry: <em>Everything Begins with the Skin </em>(Enitharmon), <em>Ghost Station</em> (Salt) and <em>The Forgetting and Remembering of Air </em>(Salt) and collaborated with the Irish artist Donald Teskey on <Em>The Idea of Islands</em> (Occasional Press). She was also a contributor to Carcanet's series <em>Oxford Poets</em>.</p>

<p>Her prose includes <em>Depth of Field </em>(Dewis Lewis), <em>Rothko's Red and other stories</em> (Salt) and <em>Girl in White </em>(Cinnamon Press).</p>

<p>As an art critic, she has contributed regularly to <em>Time Out, The New Statesman</em> and <em>The Independent</em>, and to a range of art magazines including <em>Apollo</em>. Her collected art writings, <em>Adventures in Art</em>, were published by Other Criteria in 2011. </p>



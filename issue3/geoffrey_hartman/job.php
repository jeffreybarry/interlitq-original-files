              flexing a straw verse, new age laureates,<br>
              the soft admonishments of Nature:<br>
              &#8220;One thing only do and do again,<br>
              really taste, just be, and she is yours.&#8221;<br>
              Or &#8220;you are yours.&#8221; I hurried off, yet soon<br>
              those shady scents made me remember<br>
             
              &nbsp;<br>
              Something for which my hate was musical:<br>
              meaning to cross with fluid foot a river<br>
              of weedy green and strangely undulant,<br>
              I stepped, recoiled-&#8211;colder than flesh of fish<br>
              a snake lay hissing there, its emerald cord<br>
              circling the isle. It was, that moment,<br>
              my own thought made too visible.<br>
             
              &nbsp;<br>
              &#8220;Admire this genial serpent, and admire<br>
              the place whose honored guardian she is,&#8221;<br>
              said an unseen interpreter, &#8220;he holds<br>
              her lovingly, for her forsakes all else,<br>
              while she, propitiated by his presence<br>
              yields him, by day, the milk of paradise<br>
              and, all night long, sleep in her bosoms.&#8221;<br>
           
              &nbsp;<br>
              Those words adulterating fear I shuddered,<br>
              wriggling like snakes myself, as the voice added:<br>
              &#8220;He cannot see what nourishes the shrub<br>
              whose tears are angel-bread to him, but dreams<br>
              it is his food of exile and will keep<br>
              the mind apart from her in their embracings:<br>
              yet every night, when he is balmed and bedded,<br>
              and sees a hero spearing the green love,<br>
              she, with crude wings impending, waters it.&#8221;</span><br>

          <td colspan="7" rowspan="2" valign="top" class="texto2"><p>
<em>Palomares, Spain</em><br>
<br> The girl who serves us wears her jeans low on her hips,<br>
like our students do back in the states. Forty years ago,<br>
a B-52 bomber crashed into a tanker while refueling, dropping<br>
four hydrogen bombs. Three landed around Palomares and one splashed <br>
into the Mediterranean.Nick and I’d come to look for craters<br>
in the fields, but our search wasn’t yielding much,<br>
so we stopped for a break. 	<br>	  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp;The  waitress who steams the milk <br>
couldn&rsquo;t  have been alive in 1966, but maybe her parents were <br>
living  here then, children at the time, celebrating the festival of Saint<br> 
Anthony, <br>
the  town&rsquo;s patron saint.&nbsp; It is said <em>San Antonio</em> saved the lives <br>
of  many people who would otherwise have been farming <br>
when  plutonium clouds blew across the crops.&nbsp;  The blasts <br>
broke  windows, cracked walls, and threw several people to the ground.&nbsp; <br>
We  are hesitant to ask the waitress if she knows anything <br>
about  &ldquo;Broken Arrow&rdquo;  because a few days ago we heard on the radio<br>
that  owners of the land still deemed tainted are being asked to sell it <br>
at  a reduced price for clean-up, so that nine hectares can eventually be made<br>
into  a park. What we&rsquo;ve read suggests people in Palomares are upset, <br>
still  have their urine tested yearly for plutonium levels.<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp; &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;On January, 17, 1966, <br>
Simo Orts was fishing.&nbsp;  &ldquo;We always used to watch the planes.&nbsp; <br>
I saw a B-52 and a KC-135.&nbsp;  They must have brushed against each other.&nbsp; <br>
Both planes burst.&quot;&nbsp;  I get this all from transcripts of a documentary<br>
about the Cold War.&nbsp; Antonia  Flores said, &quot;I remember all this fire in the air <br>
and pieces of airplane falling to the ground, all the  neighbors running <br>
to the place where the smoke came from. We thought that what  had fallen<br> 
there <br>
was still burning.&quot; <br>
&nbsp; &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;We almost postponed driving out to  Palomares <br>
because we woke up to the news that North Korea had tested a nuclear  bomb.&nbsp; <br>
The whole expedition became a bit more obscene, but then we  decided <br>
a bit more important as well.&nbsp; Kim Jong Il was no longer the funny man <br>
with the bouffant and platforms, the spoiled leader who ate  lobsters <br>
with silver chopsticks.&nbsp;  What did we know about North    Korea?&nbsp; <br>
Nick said they have their own form of Coca-Cola, &ldquo;Ko-ko-a&rdquo; <br>
and that the people are told that Kim was born on Korea&rsquo;s  highest mountain, <br>
under a double rainbow.&nbsp;  I remembered reading about the red stars <br>
on their license plates.<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp; The last time we were here, in 1995, <br>
Nick and I zoomed by these fields where the three bombs  fell, the Beatles <br>
on  the rental car&rsquo;s CD player:&nbsp; <em>I don't care too much for money, </em><br>
<em>money can't buy me love</em>.&nbsp; We swam in the Mediterranean,  in the waters <br>
where  the fourth bomb plummeted.&nbsp; We knew  nothing of the incident.&nbsp; <br>
There  wasn&rsquo;t much here then&mdash;just a scruffy naturalist beach <br>
with  a comical sign, a drawing of swimming trunks with a circle around it <br>
and  a red line through it.&nbsp; As we peeled off  our bathing suits, we felt a little <br>
like  John Lennon and Yoko Ono, who posed nude for their first <br>
collaborative  album, <em>Two Virgins,</em> which was  distributed wrapped <br>
in  a brown paper covering, sometimes confiscated as pornography.&nbsp; We loved <br>
that  Lennon described Yoko and himself as &ldquo;two slightly overweight, <br>
flabby  junkies.&rdquo;&nbsp; It is rumored that he often  walked around naked <br>
at  the Dakota.&nbsp; And on his last day on  earth, Annie Leibovitz photographed<br> 
him, <br>
nude  again, for the most famous cover of <em>Rolling  Stone</em>, in a pose that looks <br>
as  though he is a koala clinging to Yoko, a tree. <br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp;&nbsp; In 1995, there was no <br>
<em>chiringuito</em> to get coffee or a snack, none of the 2000 apartments <br>
that  have sprung up&mdash;a naturalist community that, according to its <br>
advertising, <br>
also  welcomes &ldquo;textiles,&rdquo; which we assume are people who wear clothes.&nbsp; <br>
There  are cranes in the sky, cement trucks, piles of bricks,<br>
construction  everywhere.&nbsp; There&rsquo;s even a sparkly  oversized s<em>upermercado</em> <br>
called <em>Consum</em> where Nick buys microwavable  paella and cartons <br>
of  Don Simon <em>gazpacho</em>.<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Now our waitress is singing along to her  iPod.<br>
We  wonder if she is listening to the Beatles, or if anyone her age still listens <br>
to  them.&nbsp; John Lennon was staying down the  road, in Santa Isabel, <br>
in  1966, when students first wore their jeans low on their hips.&nbsp; <br>
He  wrote &ldquo;Strawberry Fields&rdquo; while in Spain.&nbsp; He arrived <br>
in  October, ten months after the bombs fell.&nbsp;  Did he know about them?&nbsp; <br>
Did  he make a similar pilgrimage here to Palomares?&nbsp;  He was still married <br>
to  Cynthia then, shooting <em>How I Won the War</em>.&nbsp; He was going to meet Yoko <br>
in  November, when he returned to London,  wearing for the first time <br>
what  became his signature round granny eyeglasses. <br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp; Simo Orts:&nbsp;  &quot;I saw it <br>
very clearly.&nbsp; A bomb  fell into the sea, close to me.&nbsp; And then  I saw <br>
how much interest the Americans showed.&nbsp; The whole 6th Fleet came.&nbsp; <br>
There were 5,000 soldiers living in tents&mdash;generals,  colonels, so many <br>
important people from North America.&quot;&nbsp; Antonia Flores:&nbsp; &quot;They started <br>
doing medical checkups here in the town with a Geiger  counter.&nbsp; <br>
Some people had to throw away their clothes because they  were <br>
contaminated. <br>
The houses were washed down with detergent or water.&nbsp; <br>
At no stage did the Americans tell us anything.&nbsp; People were scared, <br>
because no one knew what was happening; all you knew was<br>
that you were forbidden to eat things, that you couldn't go  out onto the street, <br>
you couldn't touch anything.&rdquo;<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Vera Playa, the naturalist  zone, is believed to<br> 
be <br>
where  Hannibal&rsquo;s  elephants landed in Roman times.&nbsp; Spain  has been host <br>
to  the largest living animals, the largest bombs.&nbsp;  In Palomares,<br>
on  January, 17, 1966,  a 30-knot wind was blowing from the west.&nbsp; <br>
One  plutonium-bearing dust cloud traveled across irrigated fields<br>
and  the northern edge of the village.&nbsp;  Another floated away from the village <br>
but  across prime areas used for growing beans and alfalfa.&nbsp; <br>
The  last tomato crop of the season, just ready for harvest,<br>
had  to be destroyed.<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp; In <em>How  I Won the War,</em> Lennon plays Corporal<br> Gripwood,<br>
a  fascist fighting against fascism.&nbsp;&nbsp; All  the soldiers who die in the film <br>
continue  to accompany their units.&nbsp; The director,  Richard Lester, shot the film <br>
in  black and white, but tinted the uniforms of the dead soldiers in different <br> 
colors.&nbsp; <br>
The  lab that made the release print, assuming the tinting was a mistake, <br>
graded  it back to black and white.&nbsp; So no one  has ever seen the film <br>
as  it was intended.&nbsp; <br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Kim Jong Il  apparently has several spitting-image<br> 
doubles<br>
who  make public appearances for him.&nbsp; The  stand-ins are schooled in the<br>
leader&rsquo;s mannerisms and take voice lessons to  sound like him.&nbsp; They&rsquo;ve had<br> 
plastic  surgery <br>
so their faces match  Kim&rsquo;s.&nbsp; They are used as decoys to foil  assassins. The<br> 
situation makes for an  interesting screenplay.&nbsp; In 1978, the real  Kim Jong Il <br>
kidnapped <br>
his favorite South Korean  director and his girlfriend.&nbsp;&nbsp; <br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Before  stopping<br>
for  coffee, Nick and I drove the perimeter of the town&mdash;farmers still bent <br>
in sprouts, the craters  perhaps now all filled in.&nbsp; We couldn&rsquo;t  find anything&mdash;<br>
a plaque, or a monument to the  crash&mdash;only a new <em>hamburguesa.&nbsp; </em>Billboards <br>
for more townhouse  developments, some newer ranches and bungalows <br>
with swing sets and round  above-ground pools. <br>
&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp;&nbsp;&nbsp; All four crew members<br>
in the ignited KC-135 tanker  were killed.&nbsp; Four of the seven in the  B-52 <br>
were able to parachute to  safety.&nbsp; Volatile materials in two of the  bombs <br>
that fell to earth exploded  upon impact, forming the holes we thought <br>
we would find.&nbsp; The third landed in a dry riverbed in one  piece.&nbsp; <br>
It took almost four months,  until April 7, to find the last bomb, damaged but <br>
intact, <br>
in the ocean.&nbsp; <br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp;&nbsp;&nbsp; In the movie, <em>Men of Honor,</em> Cuba Gooding Jr. plays Carl<br> 
Brashear<br>
who,  diving from the salvage ship USS <em>Hoist</em>,  is a hero for recovering<br>
the  bomb from the bottom of&nbsp; the Mediterranean.&nbsp;  Brashear was hurt much worse <br>
than  portrayed in the film. &nbsp;He almost died  from loss of blood and gangrene. <br>
His  leg was amputated in two operations because of the infection.&nbsp; In real life, <br>
a  Spanish fisherman, who witnessed the bomb plunge, led Brashear <br>
and  others to the site.&nbsp; I wonder if Kim Jong  Il has seen <em>Men of Honor,</em><br>
as  he is reported to have 20,000 Hollywood movies  in his collection.&nbsp; <br>
Someone  told me the dictator said he&rsquo;d forgo his nuclear ambitions <br>
for  just one night with Angelina Jolie.&nbsp; At  first I believe it, but soon I found<br> 
out <br>
that  the story was from <em>Humor Gazette.</em>&nbsp;&nbsp; <br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;I  was mixing up Angelina <br>
with  Cicciolina,  Jeff Koons&rsquo; porn star wife, who got herself elected<br>
to the Italian  Parliament.&nbsp; In 1990, trying to stop the  Gulf War, she offered <br>
to have sex with Saddam Hussein, hoping to  achieve peace <br>
in the Middle East.&nbsp; She made the same offer in 2002 in exchange <br>
for letting in inspectors to look for WMD&rsquo;s.&nbsp; Cicciolina even offered herself <br>
to Osama bin  Laden in return for the end of his tyranny.&nbsp; She was 55 by then, <br>
but still up for the job.<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp;&nbsp;&nbsp; After the bombs dropped in Palomares, <br>
a  massive clean-up operation was mounted by American authorities.&nbsp; <br>
Some  1400 tons of radioactive Spanish soil were taken to South Carolina <br>
for  disposal.&nbsp; As part of its compensation,  the U.S.  settled <br>
with  500 Palomares residents whose health was adversely affected.&nbsp; <br>
The  United States  also built a desalination plant at Vera Playa.&nbsp; <br>
A  British couple at the next table tells us they&rsquo;ve cancelled their trip <br>
to  Pennsylvania Dutch Country because of the schoolhouse shooting.&nbsp; <br>
<em>How could that happen? </em>they ask us. They are polite, truly  perplexed.&nbsp; <br>
Pensioners, living part time  in Palomares, they know about &ldquo;Broken    Arrow&rdquo; <br>
and explain <br>
that  in 1966, some of the personnel fell in love with Andalusia&rsquo;s  weather <br>
and  decided to stay on.&nbsp; A few  Americans&mdash;though the Brits don&rsquo;t know <br>
their  names&mdash;still live in the area, retired now.&nbsp;  At the time the plant was <br>
built, <br>
there was little local development, so it was never put to  use.&nbsp; But some <br>
of these roads, they say, were actually paved by the U.S.&nbsp; <br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; The waitress  arrives<br>
with our <em>la cuenta</em> and a smile.&nbsp; &ldquo;You&rsquo;re from the states, <em>si?&rdquo;</em><br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp;&nbsp; This is our<br> 
chance.<br>
The young woman speaks English, but I can&rsquo;t find the right  way to ask her<br>
my questions.&nbsp; I try  small talk, hoping to build up to a suitable moment.<br>
She is polite&mdash;she did indeed grow up here.&nbsp; She is a student on her way <br>
to university next year.&nbsp;  Then her cell phone rings and she excuses herself, <br>
giggling all the way to the counter of <em>tapas, </em>as Nick empties <br>
his pockets of coins.&nbsp;  He leaves a big tip.&nbsp; <br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; In 1966, the Spanish feared <br>
that their beloved Mediterranean  was contaminated.&nbsp; So American <br>Ambassador <br>
Biddel Duke went swimming for the cameras.&nbsp; A &ldquo;textile,&rdquo; he wore trunks<br>
with red, white, and blue stripes.&nbsp; An interviewer asked,&nbsp; <br>
&quot;Ambassador, do you detect any radioactivity in the  water?&quot;<br>
And Duke answered,  &quot;If this is radioactivity, I love it.&quot; </p>

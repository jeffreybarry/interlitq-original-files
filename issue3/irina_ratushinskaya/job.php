And you - to jail;<br>
The sky has put off its stars<br>
And now goes nude;<br>
Most empty the mornings<br>
And everyone shares damn all!<br>
Metal&rsquo;s particularly bare,<br>
No nudist competes,<br>
But it&rsquo;s most obscene when it&rsquo;s covered with  something, like cloth.<br>
At exercise (&ldquo;Hands behind your back!&rdquo;)<br>
Crows meet<br>
To ask if it&rsquo;s right for a free bird<br>
To gulp from the trough<br>
In a human <em>zoo</em>.<br>
They&rsquo;re like a net<br>
In the rigid air, and so the sky grows dun,<br>
Wise feathers are falling lightly, precisely, and yet<br>
Not a single white feather has fallen,<br>
Not a single one.<br>
O, merry September,<br>
The reckless hours<br>
Of semi-avian bones,<br>
You young beggars, be bold!<br>
Hark, century, whom we fostered, as the clock on the  tower<br>
Measures out the ration of heaven,<br>
Salt,<br>
Bread,<br>
Salt.<br>

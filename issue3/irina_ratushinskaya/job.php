            Whimpering &ldquo;no more wolves&rdquo;,<br>
Don&rsquo;t ask for a special bullet,<br>
Even a puppy&rsquo;s considered more worthy<br>
And none of the marksmen who revel in death<br>
Would toss you a toast down his gullet.<br>
No knife is whet for those we forget<br>
So be damned as a dog or a bitch,<br>
And your wounded brother will bare his teeth<br>
To hear you whine in his ditch.<br>
He&rsquo;ll lick his wounds, for his time has come,<br>
He won&rsquo;t wait for an answering call,<br>
And to cast a bullet for such as him -<br>
            The world has no silver at  all.

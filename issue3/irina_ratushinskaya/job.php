			  White cat padding on his paws, <br>
Rubbing whiskers on the doors, <br>
Tomorrow it is bound to snow<br>
And the house roofs high and low<br>
Will be covered to the beam<br>
All in snow, as in a dream.<br>
The cat has paws, a tail, and tum<br>
Like a fluffy little drum,<br>
Drinks no milk but shows his love<br>
For the snow and clouds above.<br>
Over the moon he walked last night,<br>
His claws became all golden bright,<br>
And his pawmarks could be seen,<br>
For he forgot to lick them clean,<br>
Stretched across the sky so far<br>
That they went from star to star<br>
Then he jumped down, and in a trice<br>
Scared to death the rats and mice,<br>
Sat on the chimney, washed his face<br>
And we will take him to our  place.<br>

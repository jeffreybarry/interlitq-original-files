		  I hope there&#8217;ll be grass enough for 
              a Przhevalsky horse<br>
              Though it&#8217;s true that it&#8217;s shorter than a thousand years 
              ago;<br>
              As for progress, it may be that you are correct, of course,<br>
              But my wife (and you know what some wives are like) says no.<br>
              I weighed up the pros and cons, then looked at a child,<br>
              Thought again, and decided I did not agree to die out,<br>
              To consent to utter extinction was something that riled,<br>
              So lost that there&#8217;s hardly a bone to collect, no doubt!<br>
              Yet you, my friend, are prepared to gather them all?<br>
              I thank you so much, but somehow I&#8217;m used to existing<br>
              By a glacier under a sun that&#8217;s a shaggy ball.<br>
              It&#8217;s an irreversible change? Just because you persist in<br>
              Counting by centuries, things that are too small to weigh<br>
              As the units of fate, which I will not measure; I&#8217;ll stand<br>
              By my former opinion; my tribe, till Judgment Day,<br>
              Will be blowing its pipes at sunset, in my native land.<br>
              Farewell, I wish you plenty of grass and water,<br>
              A very intelligent son and a well-nourished daughter;<br>
              I&#8217;m grateful for your research, and impatient remain<br>
              Till I hear from you in a thousand years again.<br>

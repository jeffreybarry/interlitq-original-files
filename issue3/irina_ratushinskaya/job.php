			  &nbsp;<br><br><strong>Untitled Poem</strong><br>
                &nbsp;<br>
              Where am I?<br>
              A question so silly and flaccid,<br>
              But those who are forced not to die<br>
              (With oxygen burning like acid)<br>
              Can scarce move their lips.<br>
              Where am I?<br>
              &nbsp;<br>
              So for me, who can never forget the line<br>
              Beyond which no dog can run, no raven can fly,<br>
              To wake up anew: where am I?<br>
              And to see: it is you. Agreed - let it be on this side.<br>
              &nbsp;<br>
              Oh well,<br>
              Let there be the strange ceiling<br>
              I yield<br>
              To the murk - Mordovia&#8217;s or Italy&#8217;s -<br>
              Whose time is this to get up? To feel cold,<br>
              Whose dawn? To be beaten<br>
              Whose anthem, these glory-be&#8217;s?<br>
              &nbsp;<br>
              So what?<br>
              Since I rest on your shoulder<br>
              This visit&#8217;s allowed<br>
              In this light, whose grey hues<br>
              Are the nobody&#8217;s hour,<br>
              This &#8216;hello&#8217; - which I need not excuse.<br>

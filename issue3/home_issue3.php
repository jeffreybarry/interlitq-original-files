        you to Issue 3 
        of <em>The International Literary Quarterly</em>.<br>
		        <br>
		Time has flown since our previous issue this February, a month once characterized by Pakistan's <a href="http://interlitq.org/issue3/zulfikar_ghose/bio.php" class="link1">Zulfikar Ghose</a> as “nasty, British and short”. Ghose’s trenchancy is not confined to seasonal vicissitudes, as evidenced by his essay published in this forum, spelling out the pitfalls of literary provincialism. He will surely feel at home here given the diverse cultural origins and universal voices of an array of writers such as: <a href="http://interlitq.org/issue3/michael_blumenthal/bio.php" class="link1">Michael Blumenthal</a>, <a href="http://interlitq.org/issue3/denise_duhamel/bio.php" class="link1">Denise Duhamel</a>, <a href="http://interlitq.org/issue3/roberta_gordenstein/bio.php" class="link1">Roberta Gordenstein</a> and <a href="http://interlitq.org/issue3/geoffrey_hartman/bio.php" class="link1">Geoffrey Hartman</a>, each proving that American literature is the equal of any being written today; <a href="http://interlitq.org/issue3/christopher_arkell/bio.php" class="link1">C.J.K. Arkell</a>, <a href="http://interlitq.org/issue3/jill_dawson/bio.php" class="link1">Jill Dawson</a> and <a href="http://interlitq.org/issue3/anthony_rudolf/bio.php" class="link1">Anthony Rudolf</a>, reminding us that British <em>literati</em> are hardly lagging behind; and <a href="http://interlitq.org/issue3/marjorie_agosin/bio.php" class="link1">Marjorie Agos&iacute;n</a> and <a href="http://interlitq.org/issue3/irina_ratushinskaya/bio.php" class="link1">Irina Ratushinskaya</a>, from Chile and Russia respectively, outstanding exponents of a poetry both lyrical and defiant.
		 </span></p></td>

            <p>&ldquo;I mean, I ask you,&rdquo; she says with a  sniff.&nbsp; &ldquo;As if that bloody thing would  fool anyone.&rdquo;&nbsp; </p>
            <p>&ldquo;Quite,&rdquo; I say.</p>
            <p>I am not an especially sympathetic  man.&nbsp; You couldn't do my job if you were.</p>
            <p>What she does have, this young woman, Emma,  as do so many of my limbless patients, is a ghost hand.&nbsp; </p>
            <p>I try not to show my excitement when she  tells me this. She must know that she has been sent to me because it&rdquo;s my area  of particular expertise but I have trained myself to hold very still indeed  when these people first begin to talk of their phantom limbs. I have become  skilled at controlling the knocking in my knees, the breathless desire to pump  them for information, to rush at once to my computer and my books, to add their  details to my statistics. I've perfected a tone that I believe sounds  concerned, warm and quietly encouraging. </p>
            <p>&ldquo;How long have you experienced this phantom  hand, Emma?&rdquo;</p>
            <p>&ldquo;Since I was three. &nbsp;Since the real one left.&nbsp; It does things, you know.&nbsp; It's tapping the chair beside me right now.&rdquo;</p>
            <p>&ldquo;Why is it doing that?&rdquo;</p>
            <p>&ldquo;It's irritated, I suppose. It's waiting  for your pearls of wisdom.&rdquo;</p>
            <p>I ignore her challenging tone and launch  confidently into the explanation I suspect she really longs for. &ldquo;It's not at  all rare,&rdquo; I tell her, &ldquo;to experience a ghostly limb when one has been lost. The  part of the brain that we suspect is creating this &ndash; illusion &ndash; lies in the  frontal lobes, particularly the strip of cortical tissue called the motor  cortex.&rdquo;</p>
            <p>She has large eyes, this Emma, and she  blinks more often than most people, or rather: she is blinking rapidly right  now.</p>
            <p>&ldquo;You're the doc,&rdquo; she says. &ldquo;When do I get  to go in the space tube?&rdquo;</p>
            <p>I say that her appointment for the brain  scan is fixed for next Tuesday evening. I smile&nbsp;  widely, trying not to feel piqued that such a slip of a girl is so  little impressed by the sum of my twenty years of research and my track record  of highly consistent publication in <em>The Lancet</em>. </p>
            <p>&nbsp;</p>
            <p>*</p>
            <p>Emma arrives promptly the following Tuesday  and seems unfazed by my bad news: my assistant, Anna, a single parent, has had  to rush home for some emergency (there is always some tiresome emergency) involving  her three year old son. I will operate the scanner and stay in the room with  Emma for the twenty minutes or so it will take to do the scan.</p>
            <p>&ldquo;Fine,&rdquo; she says, taking a piece of bubble  gum from her mouth with her good hand and looking around for somewhere to put  it. I leap forward with a tissue.</p>
            <p>As she takes her shoes off she says: &ldquo;I had  a funny dream last night. It was about my brother &ndash; he was the one who caused  the accident with the lawn mower. He lives in the States now, but in the dream,  I sent him my ghost hand in an envelope. When he opened it, it leapt out, took  hold of <em>his</em> hand, and clutched it tightly. I could feel his hand in  mine.&nbsp; I mean, in my sleep, I really  could.&nbsp; It was weird.&nbsp; It was like I was trying to tell him&nbsp; - it's okay. You were just a kid. It wasn't  your fault.&rdquo;</p>
            <p>&ldquo;Yes, yes,&rdquo; I say. I'm used to the  sentimental warblings of my patients. </p>
            <p>I remind Emma that it can be noisy inside  the brain scanner and she assures me she brought her personal CD player (as  suggested) and has something that sounds like &ldquo;Limp Biscuit&rdquo; to listen to.</p>
            <p>&ldquo;Great,&rdquo; I smile. &ldquo;That will be very  relaxing.&rdquo; </p>
            <p>I've instructed Emma to think about her  phantom hand while in the scanner as I hope to record some changes in the  parietal lobe whenever the hand is sent a message to move. I don't know if she  even hears this instruction: she barely acknowledges it. She is wriggling into  the &ldquo;space tube&rdquo;, her headphones already on.</p>
            <p>Once she's inside, I sit on a chair beside  her, and try to switch off from the metallic humming sound that fills the room.  It is an eerie space with its silvery light and airless feel. Emma&rdquo;s space  analogy &ndash; predictable though it is &ndash; has some truth. The curious thought occurs  to me for the first time that I do indeed feel, in this room, so separate from  the rest of the world, exactly like an astronaut lost in space.</p>
            <p>
              After a moment I regret my rudeness on the  phone to my assistant Anna. I have been courting Anna for several weeks. My  intolerance of her son, Tommy, is the only sticking point, I believe, to our  relationship progressing.</p>
            <p>As I muse on this, a few other terminated  relationships float into my mind. Patsy, a student doctor in med school.&nbsp; She called me &ndash; I remember it with a  stab&nbsp; &ndash; she told me I was the Coldest  Bastard She'd Ever Met.</p>
            <p>And then I feel them. Fingertips. Four  small warm fingertips and a thumb &ndash; I hadn't realized it would be the hand of a  child! I was picturing an adult hand!  I glance towards the scanner where Emma's legs in jeans are as  inscrutable as ever. The room tightens, and the silver blue light contracts  around me.</p>
            <p>Without doubt, there is a little hand  crawling across my face, like a spider. The fingers stumble, exploring. A  pause. Then they wipe tenderly at my cheeks.&nbsp; </p>
            <p>If I were a fanciful man, (which obviously  I'm not), I'd say it was exactly as if a child, a small, supernaturally  forgiving three year old child, was trying her best to make me feel better. </p>

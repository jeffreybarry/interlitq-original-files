<p><strong>Serpentarium</strong><br><br>

She can fit a thirty-foot giant<br>
into one sheet of A4, <br><br>

pack pythons <br>
into her sketchbook,<br><br>

squeezing their loops <br>
in tight breeding-balls, until<br><br>

the man comes with his forked stick <br>
and pins her head down. <br><br>

She must have been drawing<br>
too fast, her tail in her mouth<br><br>

like Ouroboros – that’s <br>
what they call her when<br><br>

she goes crazy. Years ago <br>
they sewed up <br><br>

her mouth. Now, <br>
a nurse comes to unstitch her.<br><br>

He nails her head on a hook, <br>
but that’s just the beginning, she <br><br>

knows it gets worse. <br>
She thinks if she can draw <br><br>

enough snakes she’ll get <br>
used to it, stop her eyes blinking <br><br>

when he shoves the hose <br>
down her throat, makes the room tilt, <br><br>

water poured into her stomach, <br>
her jaws unhinged. Three of them <br><br>

holding her down <br>
to strap her arms to her chest, <br><br>

while she threshes against the memory – <br>
always the same memory<br><br>

of the reptile she met that day<br>
on the Petit Pont, <br><br>

who insisted they go dancing, <br>
then escorted her to the hotel <br><br>

and seized her in his coils,<br>
who thrust his hemipenis<br><br>

into every orifice, <br>
murmuring how snake sex <br><br>

can take a whole night.<br>
Every scratch from his mating spur<br><br>

made her want to rip her skin off.<br>
Even when someone came in<br><br>

he kept going,<br> 
laughing at her.<br><br>

Years, his eggs have stayed inside her, <br>
ready to hatch in this sketchpad: <br><br>

cobras, cascabels, emerald<br>
tree boas, harlequins.<br><br>

She strokes the retic – <br>
her hog-swallower.<br><br>

She fills in the last cross-hatch<br>
on his snout, can feel the skin<br><br>

loosening round his mouth, <br>
ready for the moult.<br><br>

She waits for lights-out, <br>
smudges carefully with the eraser, <br><br>

easing his skin like a dress <br>
pulled over a girl’s head, <br><br>

her own face empty<br>
on the pillow beside her, <br><br>

the pencil of herself <br>
blunt, her work done. <br><br><br><br><br><br>

<strong>Wedding Petticoat</strong><br><br>

Her dress is lace, but her underskirt<br>
is a crinoline of rose stems.<br><br>

He calls her his garden<br>
because birds perch<br>
on the cage of hoops,<br>
hopping between the thorns.<br><br>

All day they weave her<br>
a fabric of song. <br>
All night he hacks his way through.<br><br><br><br><br><br>

<strong>Honeymoon</strong><br><br>

There were seven of me then – <br>
seven horses threshing <br>
in the barn of my head.<br>
They tore down my face, legs<br>
flailing, anywhere but inside<br>
where they could smell fire.<br>
They drove across the bed<br>
that closed around them,<br>
winter bit on their manes.<br>
Michel laughed as they stiffened.<br><br>

How narrow the bed was,<br>
a river I had to wade through.<br>
I was an empty stable, looking<br>
up through icicles. But I <br>
remember my horses, jaws open<br>
wide as if ice had stopped them<br>
mid-neigh, their eyes staring,<br>
heads twisted out of the sheets<br>
and their bodies doing <br>
that deep-frozen swim.<br><br><br><br><br><br>

<strong>Miscarriage</strong><br><br>

He gives her gin then saffron.<br>
In the toilet she gives birth to a boy. <br>
Afterwards she needs a glass dome<br>
to lie inside, a red balloon tied to her thigh.<br>
The doctor calls it the afterbirth<br>
but she knows it’s a balloon<br>
because she can hear it squeak<br>
and besides, her husband bursts it, <br>
then packs her off to work. <br><br><br><br><br><br>






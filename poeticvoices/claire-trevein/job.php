<p><strong>Instructions for Making a Standing Stone</strong><br><bR>
Steep a stone into rain, or a bucket of sand.<bR>
fresh-gathered stones should be bruised by<bR>
stamping barks into their skin. Understand<bR>
that, properly dried, they may last indefinitely.<bR><bR>

strengthen the core with chunks of teeth and<bR>
lichen grated through joints. If too<bR>
stocky, plant it uphill; the right brand<bR>
of wind will slim it down with time, to<bR>
stoop over the moors — a cracked wand.<bR><bR><bR><bR><bR><bR>





<strong>Araucaria Araucana</strong><bR><bR>

The monkey’s despair<bR>
with its daggered leaves.<bR><bR>

&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;A staircase of fins<bR>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;erupting from soil.<bR><bR>

A feral pudding<bR>
pocketed by Archie<bR><bR>

&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;at a dinner party<bR>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;and raised on the deck<bR><bR>

like a parrot.<bR>
A living fossil<bR><bR>

&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;with multiple fake IDs.<bR>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;An alcoholic monument<bR><bR>

easier to intoxicate<bR>
than your average statue.<bR><bR><bR><bR><bR><bR>




<strong>The Museum of Sleeping</strong><bR><bR>

Some nights, cold skips through the layers<bR>
into your veins, taking a hold of your body<bR>
with a metallic certainty.<bR><bR>

Other nights, selkies bump against the hull;<bR>
there is a known voice calling your name;<bR>
you wake up ready to eat.<bR><bR>

This night, still well preserved, carries<bR>
her, alive, laughing at the waves,<bR>
saying nothing at all.<bR><bR><bR><bR></p>




forest<br>
fills up like<br>
a monstrous<br>
salon,<br>
and later<br>
darkness,<br>
rain,<br>
time<br>
and oblivion<br>
fall, <br>
and the lights go out.<br><br>


The invisible beings<br>
take themselves home<br>
and the forest<br>
returns<br>
to immobility, its solemn<br>
virtue of stone and dream.<br><br><br>




<strong>Oda al bosque de Petras</strong><br><br>


Por la costa, entre los<br>
eucaliptos azules<br>
y las mansiones nuevas<br>
de Algarrobo,<br>
hay un bosque<br>
solemne:<br>
un antiguo<br>
puñado de árboles<br>
que olvidó la muerte.<br><br>


Los siglos<br>
retorcieron<br>
sus troncos, cicatrices<br>
cubrieron cada rama,<br>
ceniza y luto<br>
cayeron sobre sus antiguas copas,<br>
se enmarañó el follaje<br>
de uno y otro<br>
como telas titánica<br>
de araña<br>
y fueron los ramajes como dedos<br>
de agonizantes verdes<br>
anudados<br>
unos en otros y petrificadas.<br><br>


El viejo bosque vive<br>
aún, alguna nueva<br>
hoja asoma en la altura,<br>
un nido<br>
palpitó<br>
en la primavera,<br>
una gota <br>
de resina fragante<br>
cae en el agua y muere.<br><br>


Quieta, quieta es la sombra<br>
y el silencio compacto<br>
es<br>
como<br>
cristal negro<br>
entre los viejos brazos<br>
de los desfallecidos candelabros.<br>
El suelo se levanta,<br>
los pies nudosos se desenterraron<br>
y son muertos de piedra,<br>
estatuas rotas, huesos,<br>
las raíces<br>
que afloraron a la tierra.<br><br>


De noche<br>
allí el silencio<br>
es un profundo lago<br>
del que salen<br>
sumergidas<br>
presencias,<br>
cabelleras<br>
de musgos<br>
y de lianas,<br>
ojos<br>
antiguos<br>
con<br>
luz<br>
de turquesa,<br>
cenicientos lagartos olvidados,<br>
anchas mujeres locamente muertas,<br>
guerreros<br>
deslumbradores,<br>
ritos<br>
araucanos.<br><br>


Se puebla el viejo bosque<br>
de las Petras<br>
como un salón<br>
salvaje<br>
y luego<br>
sombra,<br>
lluvia,<br>
tiempo,<br>
olvido<br>
caen<br>
apagándolo.<br><br>


Los invisibles seres<br>
se recogen<br>
y el viejo bosque<br>
vuelve<br>
a su inmovilidad, a su solemne<br>
virtud de piedra y sueño.<br><br><br><br></p>



<p><em>Glenna Luschei writes:</em></p>

<p>I grew up as a corn and bean farmer in Iowa and am now an avocado rancher in Central Coast, California.   Like the Greeks, I believe that the land is sacred. One of my favorite readings is the "Giorgics" of Virgil, four poems about the life and activity of the farmer.  Wherever I have lived, I have planted a garden,  During my years in Colombia, I asked my mother to send me corn seeds from Iowa so I could have my own
corn patch.   It did splendidly, but I never got to harvest it.   One night, squatters walked away with the corn, stalks and all.  They must have had a mouth-watering feast.</p>

<p>When I lived in North Carolina, I experimented with the most expensive crop, ginseng, a wild crop which is guarded  in "ginseng"  houses.   They say you harvest ginseng at night by shooting an arrow at the glow it emits.  I never got past the curiosity phase of my ginseng farm, but I did get a nice harvest of creasy greens in the spring at the bottom of my property.  They were delicious watercress tendrils.</p>

<p>It made sense to me when I heard that in Chinese festivals the farmer and his plow walked directly behind the Emperor.  I grew up with the same reverence for the taciturn Iowa farmer and writers of agriculture. I loved reading Sarmiento's "Facundo" when I was young and his ideas about "la fecundidad" of the land.  I realized that fertility was prominent in my life, too. As a matter of fact, I have become pregnant whenever I have
passed beneath the Southern Cross, which has left me with a delightful family of <em>Latinos</em> to raise. They are now teachers and lawyers who have presented me with grandchildren who are gardeners and trophy-winners at fairs.  Teaching my grandchildren about the miracle of plants and the animals they sustain inspired me to write "Flora &amp; Fauna," a botany and a bestiary.</p>

<p>The book will be printed at Cal Poly's Shakespeare Press Museum, where I once taught, in a letterpress edition of one hundred.<br><br><br><br></p>

<p><strong>Foolish One</strong><br><br>
Over piles of bituminous coal<br>
the bee gathers honey from vetch<br>
and yarrow.<br><br>

Foolish one<br>
to think that winter could ever come.<br><br><br>

Apis florea<br><br><br><br>


<strong>Tragedy Plums</strong><br><br>

We left Four Corners, our breakfast<br>
Navajo Fry Bread.<br><br>

New Mexico, Arizona. At the border<br>
the Highway Patrol gave us a ticket.<br><br><br>

Linda wrote my mom a postcard,<br>
"California is the police state." <br><br><br>

We wore bell bottoms <br>
and miniskirts.<br><br>

Everything different: palm trees<br>
carpeted grocery stores. <br><br>

We found a glamorous fruit<br>
called Tragedy Plum.<br><br>

It was the week of the Sharon<br>
Tate murders.<br><br>

Tragedy then, always tragedy.<br>
We still walked in beauty.<br><br>

Prunus maritima<br><br><br><br>








<strong>Clivia</strong><br><br>

Named for Lady Clive of India.<br>
Orange.<br>
Brilliant as a raja.<br><br>

Clivia minata<br><br><br><br>





<strong>A Tale </strong><br><br>

The hang glider in my field<br>
reported bear tracks<br>
grand as dinner plates.<br><br>

Today my roses bloom:<br>
Fiesta Ware.<br>
I dine from them.<br><br>

My life's a tale larger<br>
than life. The garlic<br>
clove I inserted to cure<br><br>

my ear ache swells up,<br>
a tick with eight<br>
sprouts.<br><br>

Allium sativum<br><br><br><br>





<strong>Artichoke</strong><br><br>

I can’t be hurt<br>
for I am the artichoke<br>
in prehistoric armor.<br><br>

Peel off a plate.<br>
I’ll grow another. <br><br>

People know me for my heart<br>
my fern,<br>
my purple thistle.<br><br> 

Cynara cardunculus var.scolymus<br><br><br><br>


<strong>
Ars Poética</strong><br><br>

Must I go to the silver lining<br>
and live out my happy ending<br>
prepared to the fingertip?<br><br>

No, don’t let me go prepared,<br>
let me quaver in the double rainbow<br>
waver as the bass<br>
before swallowing the bright lure<br><br>

sway as the acacia<br>
yellow under winter rain.<br><br>

Let me get my fingers wet<br>
digging out rot<br>
around the daffodil. <br><br>

Narcissus poeticus<br><br><br><br>



 
<strong>Alligator Pear</strong><br><br>

My father brought me an avocado<br>
from far-off California.<br><br>

What I loved at first slice<br>
later claimed my life.<br><br>

Alligator pirum<br><br><br><br>



<strong>
Collecting Nectar</strong><br><br>

Bees, striving for their zenith,<br>
circle the mask<br>
of the beekeeper<br>
when he transports them to the ranch.<br><br>

Bees prefer the lotus and lavender<br>
to avocado but they pollinate<br>
the blossoms.<br>
I collect the nectar.<br><br>
 
No fear.<br>
A thrill to take part<br>
in creation.<br>
Bearing fruit fulfills the Kingdom.<br><br>

Apis florea<br><br><br><br></p>




<p>Fei Fei <br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;           1985 - 1989<br><br>
You’re showing off <br>
In the garden as if<br>
Someone is watching you lift <br>
Yellow Orchids to your nose, head <br>
Tilted up like a fawn. <br>
You’re alive and tall  <br>
As the Sunflowers. Not  <br>
Bamboo thin and as pale <br>
As the moon. It’s not the day<br> 
That stumbled sickness into your bed<br> 
Like clowns scrambling <br>
Under your blanket and then<br> 
Tumbling your mother, ripe <br>
With grief into Da Temple to <br>
Kneel and clap to Buddha <br>
So many times she broke <br>
Both hands. After seventeen years<br>
I still watch the world<br>
From the highest peak of disbelief.<br>
I am drunk on sweet wine.<br>
But don’t worry, Fei Fei, I won’t jump. <br>
I’m too far above the canyon of death<br>
And I would jump only if I could see<br>
Your face at the bottom. Fei Fei, I still look<br>
For signs of your ghost even in <br>
Early morning TV fuzz. Fei Fei, <br>
Your mother and I fight. <br>
We fight all the time. <br>
Unlike when you were here, so tiny . . . <br>
When finally you fell so far off the bed <br>
Not even a slap across your <br>
Mother’s face could reach you.<br><br><br><br>
 


&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;	For Fei Fei, Tan Juin, and Charping</p>





                  <p>
                    Né en 1979 à Paris, d’une mère suisse et d’un père argentin, tous deux architectes, Hugo Bonamin a 4 ans lorsqu’il réalise sa première œuvre peinte d’importance. Parallèlement à sa scolarité, il fréquente l’Atelier Nicolas Poussin et suit les cours de l’Ecole du Louvre. Suite à l’obtention de son baccalauréat scientifique en candidat libre à Londres, il devient Artiste-Peintre autodidacte et s’installe successivement à Londres, Buenos-Aires, Kolkata (Inde), Paris et Caux (Suisse) où il réside actuellement.</p>

<p>Depuis 2015, Il travaille sur la publication d’un livre réunissant 784 portraits d’inconnus, en collaboration avec une galerie parisienne. Ami de longue date avec Samuel Theis, ils décident d’explorer ensemble les thèmes de l’égo et de l’intimité en mettant en scène leur propre relation.    

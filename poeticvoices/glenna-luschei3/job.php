<p><em>For Steve and Margaret</em><br><br>


First trip back since the Hospital,<br>
I pray to feel at home on the Great<br>
Plains but I am only barely at home with the living<br>
pretending I know how to register,<br>
sign my name.<br><br>

Everything whiter than the snow of pear<br>
trees. My grandmother's ghost winds<br>
through them.<br>
In a month the lilacs will broadcast<br>
their perfume but now the leaves pitch<br>
hands in prayer.<br><br>

Open: save me, save me.<br><br>

My House of Usher, house of illusion.<br>
The highway wiped out our living room,<br>
bricked me in until you heard my tell-tale heart,<br>
and led me stumbling over stones.<br><br>

Out<br><br>

I buy the sculpture of a heart<br>
from an artist who lives with heart disease.<br><br>

My Indian guide on the Niobrara signals I can make it.<br>
I camp with Lewis and Clark on the lake<br>
where I learned to swim.<br>
I visit Sacajawea on her keel boat. <br>
The herbs she collects<br>
will cure me, grandmother's ointment.<br>

I live with illusion.<br>
First spring,<br>
first season of my awakening.<br>
I must not have died because I feel cold.<br>
Alive<br>
I slip my hand through your arm,<br>
an icicle that makes you jump.<br><br>

“Don’t you walk with a stick?”<br>
Yes, but I crave the warmth of a sister,<br>
as I used to walk with grandmother and her witching rod.<br>
She knew I would find and bear water.<br>
She, the first to love me for my wildness.<br><br>

And no one else has let chickens roost on my cast-iron bed.<br>
It’s the Great Plains where people plow their own fields. <br>
Grandmother gave me her diamond.  She knew I was a gypsy<br>
who would need ready cash.  She left me her Roads to California quilt <br>
and linen table cloth.<br>
She knew at some point I would settle down.  <br>
She knew I would have nine lives.<br><br><br><br>
</p>





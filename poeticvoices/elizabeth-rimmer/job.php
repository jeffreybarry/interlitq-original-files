<p><strong>Haggards</strong><br><br>
These are the places in between,<br>
between the field and the mountain,<br>
between the cattle and the sheep,<br>
between the orchard and the road,<br>
between the heather and the sea,<br><br>

places where growth is curbed<br>
by salt, or drought or altitude,<br>
by rocks beneath, by standing water,<br>
by wind, by fire, by lawlessness,<br><br>

places for forgotten things, and things<br>
no longer valued, the weeds and black bees,<br>
the wrens and thrawn roots of Latin<br>
the boys were taught in secret.<br><br>

These are the places in between,<br>
too small for the rich to care for,<br>
where things grow stronger for neglect,<br>
where questions thrive, and dreams, cut down<br>
to the roots, grow hardy, come back strong.<br><br><br><br><br>




<strong>Glittering</strong><br><br>

a crop of dark firs<br>
rakes the crest of a green ridge<br>
like a comb, teeth upward.<br><br>

hills are cups of gold,<br>
clouds form mountain ridges in<br>
the wide field of sky<br><br>

the wide open stare<br>
of a primrose bank quickens<br>
scrubbed and wind-bleached fields<br><br>

shining in the sun,<br>
the hard metallic glitter<br>
of a crow’s black feathers <br><br>

a rabbit on the scuffed <br>
heel of a grassy hill-side shoe<br>
scabby with heather<br><br>

spring lambs stretching<br>
on the fell-sides sun-warmed grass,<br>
blinking at the world<br><br><br><br><br>


<strong>Hanging the Bunting</strong><br><br>

You look into the fire and dream of home -<br>
the mingled smell of lemongrass and bleach.<br>
It has become unbearable, the loss<br>
of something dim, less loved than longed for.<br>
You crave your mother’s baking. She never baked.<br><br>

You hang the trees with bunting, learn to knit,<br>
make jam, pick ransoms, stitch the quilts<br>
your Granny made and hated. Now you save<br>
the seeds of plants your father weeded out.<br>
You blacklead grates. Your stock is made from scratch.<br><br>

It feels like fantasy, this flowery pastel nest,<br>
kept far from politics and poverty alike,<br>
but something else is stirring here, a cry of pain,<br>
a knowledge of which battles must be fought,<br>
and what the heart most needs, to take them on.<br><br><br><br><br>




<strong>Hyssop for Repentance</strong><br><br>

This is the herb for a place of pain.<br>
Its bitter tang is tonic for indulgence,<br>
its blue bee-bothered flower tips<br>
cleansing, dispersing phlegm and clotted blood,<br>
old grief and lingering remorse.<br>
Sprinkled with water from a hyssop twig,<br>
the sufferer is purified and purged. <br>
Something dies, an old life, an old poison - <br>
and something flies free, forever gone.<br>
The space is clear. Time to start again.<br><br><br><br><br>



<strong>Scouring</strong><br><br>

Things get scoured when my life goes wrong -<br>
my mother’s teaspoons, when she was ill,<br>
a copper tray I polished till my hands were black<br>
before your father’s funeral, the cobwebs<br>
from behind the radiator. Things were scrubbed,<br>
oiled and mended, folded and ironed, things<br>
were put away then, and never again after.<br>
Today I have washed the dressing gown<br>
that hung behind the door, the large black shawl<br>
you brought from India. These small acts of making good<br>
divert me from what’s underneath our house,<br>
tectonic shifts, small falls and stresses in the faith<br>
we’ve had in weather, honesty, the common will<br>
	to kindness, your solid presence in my life.<br><br><br><br></p>








<strong>My Lament</strong><br><br>

I still regret <br>
we never did it.<br>
You know the Fifties, when everyone pretended sex<br>
didn’t exist.<br>
You weren’t allowed to say virgin <br>
out loud <br>
but still supposed to be one<br>
until you got pregnant and the jig was up.<br>
Don’t ask don’t tell until…<br>
Hell.<br>
Long days, long nights, long cold<br>
showers praying for a period.<br>
“If only---I promise not to sass my mom, not to give<br>
my dad a heart attack, not to stay out ‘til midnight<br>
in my boyfriend’s jalopy.”<br><br>

As long as I’m lamenting, I wish I could’ve grown up <br>
in the Paris rage with Hemingway and Gertrude Stein<br>
or reckless in the <em>fin de siècle</em> with Toulouse-Lautrec <br>
or even in the honest Stone Age with Fred and Pebbles. <br>
Heck.<br>
She didn’t give him a heart attack.<br>
He didn’t give her flack. <br><br>


Why bother to lament<br>
since we’ve lived through it all and we’re still alive<br>
to piss and moan?<br><br><br><br>



<strong>Equinox:  September 23, 2016.<br>
(Equinox is the day before your birthday as when you were born)</strong><br><br>


Fifty years ago I was waiting for you<br>
at the Equator.<br>
It was the Equinox, eerie day, no shadow on the ground at noon<br>
hardly any night. Desperate for sleep, I prayed you would soon arrive.<br>

In the twilight<br>
I addressed the birth announcements Colombian artist<br>
Hanibal Gil printed:<br>
Wood block of mother fish with baby taking the lead.<br>
That was our path.  I was waiting for you<br>
at the Equator, our lives on the fly.<br><br>

We lived through the Equinox and progressed<br>
into Libra, your stone still a sapphire,<br>
corundum which held over a spider, will <br>
kill the beast. Plenty of spiders here in Colombia plus snakes<br>
and potholes as the jeep bumped me along <br>
drenched with waters from the Atlantic and Pacific, in the dark<br>
to the hospital.<br><br>

"I'm done with this country.<br>
 The beat up roads, the spiders, <em>la violencia</em>, the Farc,<br>
 the lack of infrastructure, worry about thieves, my stolen sapphires<br>
 and kidnappers.  I'm going back to Iowa.<br>
"The sweet corn stolen from my garden, stalks and all.<br>
I'm going to take my baby home to safety."<br><br>


But then I was in the delivery room and your father said,<br>
"We have a son."<br>
Back in my room filled with anthurium and glads<br>
I was glad, glad glad, fortunate to have my fourth and perfect<br>
child at the Equator, a Libra, a diplomat who could weigh each side<br>
of the sun and the moon, who could solve the conundrum, <br>
to begin my life swimming with you who grew into a peacemaker.<br><br>

I loved this country at the equator!<br>
I loved this country of the sun!<br>
I loved my son!<br>
Now fifty years later, even the Farc<br>
has made peace.  I know you had something to do with that.<br>
You who bring delight and the light into each Equinox.<br><br><br><br>



<strong>Wood Pecker</strong><br><br>

"Cancer/never!<br>
We die of a stroke or a heart<br>
attack,"  My brother thundered.  "Don't forget<br>
our parents watch over us.   Every week<br>
I go to a funeral of someone who weighs a hundred<br>
pounds less.   Dammit!<br>
Our parents keep us alive.  There is only another blip<br>
on your élan.  You have more books to write, to edit.<br>
You will cry over my bier first<br>
or cry over my beer, hah!  As the English say,<br>
keep your pecker up."<br><br>

I wish.<br><br>

Cancer/never in my family but here it is.<br>
After the needle biopsy of the breast<br>
my life speeds into diagnosis and procedure.<br>
Words new to me.  Lobular means not the duct<br>
but the milk lobe.  Cancer takes on a life<br>
of its own.   I enter the booming tunnel.  Cables resist<br>
the magnet with a racket, sounds of the piano tuner<br>
at the twelve-tone scale.   Then a wood pecker.<br><br><br><br>



<strong>The Flood</strong><br><br>

When the storm swept through my garage, <br>
flooded the boxes of pictures and letter I forgot<br>
I stored, I fell in love with each blurred page,<br><br>

left in wake, <br>
felt the survivors suffering twice, <br>
once in writing, once in destruction. My friend wrote<br>
my books were about aging<br>
twenty-five years ago. He and I age on <br>
though our mates have passed.<br><br>

My editor wrote I carried icicles in my poems.<br><br>

Letters from Linda revealing the meds<br>
she must take break my heart.<br>
It hurt me that she never told me how sick<br>
she was. Thirty years later I can see it right<br><br>

before me.<br>
Time and tragedy read through the lines.<br><br>

Love letters. “We’ll be happy and free<br>
as ever when we’re together again. Please come<br>
join me in San Francisco for as long as you can…”<br><br>

A good letter, sincere, but nothing about protection <br>
from the torrent to follow, and it did, it did.<br>
I didn’t go to him. I followed my hard destiny.<br><br>

We must rescue what we can, <br>
let go the broken heart, <br>
bleeding ink all the way down the street.<br><br><br><br></p>





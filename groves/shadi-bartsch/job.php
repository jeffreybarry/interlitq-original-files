<p>

<strong><em>Interlitq:</em> Why do you care about the humanities?</strong></p>

<p>As Paul Kalanithi observed, the sciences offer knowledge, but the humanities are how we make meaning.  Where else would meaning come from?  This seems so obvious to me that I won’t belabor it, but it’s hardly as if “the humanities” represent a silo somewhere where scholars analyze medieval literary texts in ecstatic solitude.</p>

<p>I also have a particular axe to grind (aren’t these metaphors interesting? it’s been a long time since axes were part of everyday life).  Without the skills that humanistic knowledge lends us, we will remain unaware of how our past constricts our present, and how to try to move beyond this.  This is a function more usually ascribed to history, but I do not have in mind the standard notion that without knowledge of the past, we will be doomed to repeat its mistakes.  Instead, I am thinking of the humanities as the lens through which we can approach the self-conceptualization of any culture and understand how its assumptions and generative thought differ from our own.  Looking in such a mirror, we have the wherewithal to understand ourselves in all our strangeness.  A quick example:  the fundamental reasonableness of the idea that humans deserve to have citizen rights is hard-baked into western culture, even if it was often observed in the breach.  It is the assumption with which much of the ancient Greek philosophical tradition begins, and it is based on the understanding that man, as a rational animal, can participate in his own sociopolitical environment in ways that look to his own good.  (The male pronouns here are of course deliberate).  In contrast, when the Qing Dynasty collapsed in China in the early 20<sup>th</sup> century, the word for citizen (<em>guomin</em>) had to be literally invented; between peasant and imperial court there were no mechanisms for large-scale public involvement, and Confucian philosophy was based on hierarchy and the family unit to a degree alien to the Greeks.  One can see immediately how these two differing philosophical traditions impacted the present; also, that there is no reason to take our own as “natural.” Not seeing this is a limitation on our ability to see in the first place.  Another example I like is that of Thomas Kuhn, who was perfectly happy being a cold fusion PhD candidate at Harvard until he encountered Aristotle’s Physics.  The jolt to his own then-teleological understanding of science produced <em>The Structure of Scientific Revolutions</em>.</p>  

<p><strong><em>Interlitq:</em> Many people would feel that your discipline, Classics (that is, the study of the ancient Greco-Roman world), is both esoteric and irrelevant in today’s world.  Do you have an answer for them?</strong></p>

<p>The answer is definitely not “it will help you with etymology” or “medical textbooks use Latin terms”!  I’m always surprised to hear that people think that the texts that formed our culture are somehow irrelevant to us even as we (most of us in the west) live <em>in</em> that culture.  The ability to read them in the original languages adds to that; as Wittgenstein remarked, the garment of language is the first thing that lets us step into someone else’s culture. </p>
<p>The other feature of Classics that I find underemphasized is the fact that for 2000 years, human beings have been interpreting the same texts – but differently in each era.  Classics offers us a mirror to the ways and vagaries of human interpretation:  what was made of Plato in 200 BCE?  100 CE?  in 1400 CE?  One hundred years ago?  We have here a whole history of the impetus to interpret, in each case shaped by cultural and political influences.  What light can this history shed on our own practices today?</p>
<p>I’m neither talking  about a Foucauldian archeology of knowledge with epistemic breaks, nor yet a study of continuity between eras, but simply about the value of context in all investigations, starting with questioning the question and its assumptions itself.</p>

<p><strong><em>Interlitq:</em> What do you consider your most important methodological practice in your own work?</strong></p>

<p>I appreciate the tools my discipline (Classics) has given me for approaching the past.  Now I am curious about the tools I don’t have, the features I don’t see, the knowledge-formations that are not yet recognized as such, the blind spots that fetter my view.  When I set out to write my fourth book, <em>The Mirror of the Self</em>, I was interested in the use of visual metaphors in ancient philosophy (just think of Plato’s cave, the shadows on the wall, the view of the Sun, the conflation of “seeing” with “knowing”).  It took me some time to realize that “seeing” in antiquity was laden with many different assumptions than “seeing” in the Western present.  Ancient theories of vision inevitably made it a tactile phenomenon, so that vision either involved the penetration of the eyes of the seeing agent by particles sent off from the object viewed, <em>or</em> assumed that the eyes themselves sent out physical “rays” that “groped” the object and sent information back to the eyes and mind.  Understanding that I could not understand Plato’s thought until I understood that his seeing metaphors were laden with sexual undertones absent in our own metaphors of sight was a conceptual leap forward for me.  Likewise in my recent book on the Roman satirist Persius, instead of taking his digestive metaphors for granted, I read ancient medical texts on digestion and found a much richer background for his poetry than I had understood at first.  Morality and digestion were closely tied together in antiquity because of widespread belief in the humoral theory:  your humors (and hence your mood, character, sanity) were very much affected by what you ate.  The sheer mass of strange lore in this process was also fascinating and illuminating.  Imagine the surprise if you told a modern doctor that an excess of certain kinds of food would be stored in your body as extra semen and located behind your eyeballs!</p>

<p><strong><em>Interlitq:</em> What do you hope to accomplish with the new University of Chicago institute you are leading (the <a href="http://sifk.uchicago.edu/"> Stevanovich Institute on the Formation of Knowledge</a>)?</strong></p>

<p>The mission of the Stevanovich Institute is to unite scholars from many different fields to study the processes of knowledge formation and transmission. Our considerations will range temporally from the ancient period to the present day and geographically over the entire globe, with a particular concern to uncover the ways embedded structures of knowledge have shaped the modern world. We will examine particular moments in the life of disciplines, both from the inside, relying on the expertise of specialists, but also from the outside, from which vantage unguarded assumptions and biases might be more visible. </p>
<p>The questions we ask include: What are the conditions under which knowledge claims emerge and derive legitimacy? In what ways do those conditions restrict or expand such claims, influencing circulation and transmission beyond their initial geographical and temporal locations? What do theologians, physicists, physicians, poets, and politicians mean by knowledge at various periods and how do they translate that knowledge into actions that affect their societies? How might the knowledge systems of indigenous peoples be compared with those of industrialized societies, as to their origins, evolution, and modes of validation? What kinds of societies have been conducive to the development of expanding knowledge systems and in what societies has little development or enlargement taken place?</p>
<p>This is all predicated on knowing (and finding relevant) the fact that the disciplines as they currently stand in major universities are not natural divisions of knowledge, but particular phenomena shaped by culture, history, class, and other factors.    Even inter-disciplinarity is predicated on the existence of such disciplines. </p> 
<p>All of us at the Institute feel that this sort of thinking offers a new paradigm for the sort of knowledge that comes out of universities, and that it is directly relevant to the “real world” in many ways.</p>

<p><strong><em>Interlitq:</em> What would you say is the biggest challenge facing academics today?</strong></p>

<p>I would say it’s the question of how research happening within universities can be more transparent to—and more impactful on—the world outside.  Our work is relevant in so many ways, and I’m not sure if there is any rationale for letting it be confined to the ivory tower, or in unnecessarily difficult terminology. If it seems irrelevant, then we should find ways of making it relevant.  Unfortunately, sometimes trying to make your work accessible to a larger audience, or caring about its impact, is considered “dumbing down” what you do, or selling out to baser motivations.  I think that one of the most important roles an academic can take on is seeking to bridge that divide between the university and the world of non-academics. </p>



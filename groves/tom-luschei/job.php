<p>

<em>Interlitq: Why is education important to you?</em></p>

<p>When I taught adult education in Los Angeles, I had students in their seventies and eighties with no prior formal education. Although these individuals had achieved many things in their lives, they could not experience some of the true pleasures of life, like being in school, reading books, learning from teachers and classmates. So first, they have not had as rich an experience as life should offer to us all, because the lack of formal education precludes many opportunities and experiences in both personal and professional realms. As the Nobel Prize winning economist Amartya Sen wrote in his book Development as Freedom, education, as a constituent of development, yields many capabilities for those who receive it. Education expands the choices that we have to achieve to our greatest potential and to live the kinds of lives that we dream about as children.</p>

<p>Of course, there are many other reasons that education is important, such as its positive impact on the development of democratic and peaceful societies, its role in making us more academically and economically productive, and the positive effects of educational attainment on health outcomes. But first and foremost, I think education is important because it enables us to live life to the fullest.</p>

<p><em>Interlitq: What does it mean to be a scholar of education?</em></p>

<p>The field of education has at least two important functions. The first is to prepare young people to become teachers. Nearly every serious university in the United States, including mine, has an initial teacher preparation program. This teacher preparation function keeps schools and colleges of education grounded and relevant for children, families, and schools. The second function is to produce research related to educational effectiveness and improvement. Many people outside the field recognize the first function but not the second. Some recognize both, but dismiss the research function as being too disconnected from practice or too theoretical. Not surprisingly, I disagree with this critique. I think that educational practice and policy must be continually informed by research. So as a scholar of education, I conduct research that attempts to do this. </p>

<p>Although most of my research is international, the most recent example I can give took place in a local school district. A colleague, two of our graduate students, and I conducted an evaluation of the district’s bilingual education program. The district had received many complaints from parents and wanted to demonstrate to these parents that they were serious about improving the program. We did a very comprehensive evaluation and wrote a report with several recommendations, mostly oriented around improving support for bilingual teachers. Now the district is implementing many of the changes we recommended, which is very nice to see. Let’s hope they work!</p>

<p>Scholars of education must not only produce research, they must also prepare the next generation of researchers. So although I taught public school in Los Angeles for nine years, I have never taught prospective teachers. Instead, my graduate students aspire to become educational leaders, policy makers, or researchers. Many of them are current teachers or educational administrators, and nearly all of them are directly connected to daily life in classrooms and schools. So what I teach them has more to do with conducting research on education than about teaching and learning. I strongly emphasize the importance of robust empirical evidence when making important educational decisions (which is not as common as one would hope). As one of my PhD students recently told me, the class she took with me caused her to question everything she thought she had learned in more than 20 years as a teacher and administrator. </p>

<p><em>Interlitq: What are the most important issues in education today? </em></p>
<p>My primary field of research is international comparative education, so I look at this question from a global perspective, with a particular focus on marginalized children in lower-income regions and countries. For these children, the greatest challenge is simple access to school. Despite massive global efforts over the past 15 years, there are still nearly 60 million primary-aged children across the globe who are not in primary school. These are the most marginalized children, including those living in remote rural areas (especially girls), children living in areas fraught with war and conflict, children from pastoralist communities, and children with special needs, among others. Getting these children into school will require continued international efforts led by international organizations like UNESCO and wealthy nations like the United States. </p>

<p>But access to school is not enough. We know that when marginalized children do attend school, the schools they attend lack proper resources, especially well trained and motivated teachers.  Ensuring qualified teachers for marginalized children is probably the most important educational challenge of this century. I just published a book with my colleague Amita Chudgar, from Michigan State University. The title is <em>Teacher Distribution in Developing Countries: Teachers of Marginalized Students in India, Mexico, and Tanzania</em>. In this book we report the results of case studies from three diverse countries to understand who teaches marginalized children and why. In this book, as well as in other research we have published, we find that the teachers of marginalized children are, on average, younger, more likely to be male, less prepared, less qualified, and less motivated than teachers of more advantaged children. The challenging living and teaching conditions in marginalized areas present one major problem. It is very difficult to convince teachers to move to and remain in these schools. And it is very difficult to recruit local teachers, due to low levels of educational attainment, especially among girls. In many countries, female teachers are practically nonexistent in remote rural areas, due to difficult living conditions. This situation has very negative consequences for the educational attainment and achievement of marginalized girls.
</p>

<p>As a scholar working and living in the United States, I can say that American children are pretty lucky, relative to most of the world’s children. Yet many of the same challenges remain for marginalized children in the United States, although to a lesser degree. Poor and minority children in the United States tend to have the least access to resource-rich schools and qualified teachers. They go to schools with higher degrees of violence, more segregation, and lower expectations. And of course, they are much less likely to attend university. Nonetheless, while these children are in school, they tend to learn at similar rates as non-minority and more advantaged children. The real challenge occurs outside the formal school system, where disadvantaged children have many fewer opportunities to participate in early childhood education, after-school enrichment activities, and summer school. So the challenge is to provide the kinds of opportunities for these children that most middle-class children in the United States take for granted.</p>
<p><em>Interlitq: What has been your most important accomplishment?</em></p>

<p>Having a part in raising two amazing children, Linda (12) and Andrew (7). Despite recent troubling events in the United States, my children give me hope for our future.</p>

<p>Professionally, I think my most important accomplishment has been my positive (I hope) impact on my former students, from elementary-aged children to PhD students. I occasionally hear from students who were my elementary students in the 1990s. Now they are in their twenties and thirties (or older), and they recall things I taught them that I have long since forgotten. One student recalled a lesson I taught about clouds. I don’t remember that at all. But a teacher never truly knows what his or her impact has been. I hope that I have provided some inspiration and direction to my former students.  </p>
<p><em>Interlitq: What are you working on right now?</em></p>

<p>Right now I am on sabbatical, which is the most wonderful word in the English language. I am working on a book that I have had on the back burner for about five years. The working title is<em> Developing Perspective: What can the North learn from the South about education?</em> The idea of the book is that educators and policy makers should not only pay attention to educational high flyers like Finland and Singapore. There is a great deal of educational innovation happening in the developing world. And the challenges these countries face are quite similar to the most pressing educational problems in the United States, especially reaching and teaching marginalized children. So I argue in the book that from both a research perspective and an education policy perspective, we need to pay much greater attention to education in the developing world.</p>

<p><em>Interlitq: What do you think the future will bring in your field? </em></p>
<p>This may come as a surprise, but I am actually hopeful about the future of education in the United States and around the world. I think there is a growing consensus that we must ensure that children are not only in school, but that they must be safe, happy, and learning while they are there. And the key to all of that is having a great teacher. I foresee much more investment in recruiting, training, and supporting teachers around the world, and I expect that young people will respond to these efforts by deciding to go into teaching. Most importantly, I hope that children in marginalized communities will receive support and motivation to become teachers in their communities, which will result in cascading virtuous cycles.</p>
<p>In terms of educational research, I feel that we are getting closer and closer to understanding how to ensure that talented young people enter and remain in teaching. It has something to do with salaries, but it also has to do with social status and a nation’s commitment to education. Many East Asian countries have achieved this goal, largely due to very high regard for teachers, accompanied by competitive salaries. It will take a long time, but I think this can be achieved in other countries as well.</p>



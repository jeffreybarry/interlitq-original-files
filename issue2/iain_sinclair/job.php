          <td colspan="3" valign="top"><p><span class="texto2"><br>
              Light from elsewhere insinuates a pewter sky. The cold relents. 
              I come less frequently to this room. No more papers, ledgers, manuscripts. 
              The only documents I write by hand are cheques to council officers, 
              tax-gatherers. For unnoticed and unrequired services. Other debits 
              are electronic and instantaneous. Credits are posthumous and they 
              arrive, if at all, after many months. Committees have to be convinced 
              of my status, my desire to operate as a &#8216;casual lecturer&#8217;. 
              I am forever making copies of my passport, but I no longer travel. 
              The freelance gig is over, an anachronism. Books that still surround 
              me are sullen, eager to mutate back into money. Most of the authors 
              are lost or forgotten. Friends, confederates, tutors.</span> </p></td>

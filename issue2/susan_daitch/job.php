              <p align="left"><span class="texto2"><em>By way of introduction, Susan Daitch  writes: My novel, &ldquo;The Dreyfus Book&rdquo;, follows a group of characters  indirectly involved in the Dreyfus trial and espionage case: petty forgers,  photographers, cross-dressing generals, actors in early silent films, and film  restorers who tried to save crumbling movies about the trial one hundred years later.  Even the tangential players who participated far from  the main stage were deeply affected by the circumstances of a trial that was to split the French public sphere as no other event of the nineteenth century. The effects of its divisiveness would continue to be felt into the next.</em></span></p>

          <td colspan="2" rowspan="2" valign="top" class="texto"><div align="center"> 
              <p align="left"><br>
                from the rental truck, more <br>
                tables for the second strip<br>
                till the last man in blue comes,<br>
                <br>
                rolling the last golden disc. <br>
                Now they wait, the tables, <br>
                the faces, the round golden masks<br>
                <br>
                and the sky says nothing.<br>
                Now the white canoes<br>
                and the three white geese.<br>
                <br>
                Now they peck in tandem,<br>
                pecking, pecking,<br>
                while an insect calls<br>
                <br>
                from among the trees.<br>
                Where are the gods<br>
                of Iowa? The gods of peace?<br>
                <br>
                The &#8216;drowsy ones&#8217;,<br>
                &#8216;he who paints pictures&#8217;,<br>
              &#8216;dust-in-the-faces&#8217;?</p>
              <p align="left"><span class="titulo1">(ii) The Tree</span></p>
              <p align="left">A day so grey. I did my laundry.<br>
                There was nowhere I wished to go.<br>
                Whatever pain I had left behind<br>
                <br>
                was better left. I switched on <br>
                all the lights in my room, all<br>
                the reading lamps and ceiling lights<br>
                <br>
                and still the day was grey.<br>
                I will place a bookmark in my book<br>
                and look at the walkway and the sea<br>
                <br>
                blueish-grey on the cover.<br>
                I will turn to the river and see<br>
                the same river I saw yesterday.<br>
                <br>
                There is no one I wish to talk to.<br>
                I will sit quietly in the bookshop<br>
                and hear what the poets say.</p>
              <p align="left">All the many trees of Iowa &#8211;<br>
                Norway Spruce and White Pine,<br>
                Cottonwood and Maple,</p>
              <p align="left">Linden, Ash, and Sycamore,<br>
                are as nothing to this tree of light<br>
                pointing across the river</p>
              <p align="left">and the sky leans down to say <br>
  &#8216;I am the tree their shadows create,<br>
                fallen, that you may see me,<br>
                <br>
                shining, that you may float,<br>
                oars still, moving me in your wake<br>
                as the wind moves spruce and cedar.&#8217;</p>
              <p align="left"><span class="titulo1">(iii) The Precinct</span></p>
              <p align="left">Not the grass but the road, every road<br>
                is made for dapple. Shadow<br>
                can be as shadow was meant to be &#8211;<br>
                <br>
                grey and lovelier for being grey.<br>
                Red-brick paved, the precinct,<br>
                lined with bench and tree, is a flood<br>
                <br>
                of dapple, a dapple that lends itself <br>
                to smells &#8211; kerosene, coffee, street cuisine &#8211;<br>
                downtown in every city of the world.</p>
              <p align="left">The floor of the mind must look like this:<br>
                thoughts that have barely landed<br>
                lit with pockets of sunlight &#8211;</p>
              <p align="left">unthoughts, the workings of a mind<br>
                not ours, glimmering beyond us.<br>
                I watch people walking through the sieve</p>
              <p align="left">unaware how they are registered &#8211;<br>
                pigeons of shadow slanted on a shirt,<br>
                bars on a wall, stripes on a pelt.</p>
              <p align="left">We walk in the cast of mind that dapple <br>
                gives us. Nothing, not even the bright <br>
                acacia, the red and orange dahlias,</p>
              <p align="left">sparrows darting among the berries,<br>
                is lovelier than the ground itself.<br>
                And now that our lovemaking<br>
                <br>
                has accepted failure and limitation,<br>
                how gently we lie together, asking<br>
                for nothing, giving sleep permission.</p>
              <p align="left"><span class="titulo1">(iv) The Sculpture</span></p>
              <p align="left">Right from the beginning<br>
                there&#8217;s already a sense of memory,<br>
                the dominant suspending them<br>
                <br>
                above the home key, father and son <br>
                confined to their own stone square. <br>
                He&#8217;s squatting down to tie <br>
                <br>
                the little boy&#8217;s shoelace, ready<br>
                for the story to begin. His mouth<br>
                is open &#8211; &#8216;like this&#8217; he says.<br>
                <br>
                The right hand says the same thing<br>
                twice but the left hand is moving up,<br>
                creating forward motion.<br>
                <br>
                The boy peers down, riveted<br>
                on the knot; a change of key is created <br>
                by the smallest interval possible.<br>
                <br>
                We walk round them as round <br>
                the benches, bins, the fountain.<br>
                Attention turns to the bass.<br>
                <br>
                So many harmony turns<br>
                and a sudden darkening of the colour, <br>
                a middle voice like sobs, a very moving, <br>
                <br>
                plaintive voice, still in that <em>parlando</em> <br>
                we don&#8217;t expect to hear again.<br>
                Everything is being told.<br>
                <br>
                The little boy&#8217;s hands fan <br>
                like doves in silhouette,<br>
                both hands moving together.</p>
              <p align="left"><span class="titulo1">(v) The Song</span></p>
              <p align="left">The song must come from elsewhere.<br>
                I can only hear the vowels. Oh oh oh<br>
                the song sings. Then oh again<br>
                <br>
                like the sugar maple exuding small <br>
                gold gasps, then a kind of humming. <br>
                Me, the river sings, then ma, ah, ah, <br>
                <br>
                then some incomprehensible <br>
                play on y from Burma. &#8216;A hermit country&#8217;,<br>
                Mr Moe explained. Far off laughter.<br>
                <br>
                The song must come from elsewhere,<br>
                from some other organ. The word<br>
&#8216;organic&#8217; was first used by Aristotle<br>
                <br>
                to refer to parts of warships &#8211; parts <br>
                of a whole that made the whole thing work.<br>
                Suspended between the giant spokes<br>
                <br>
                of the wooden starboard paddlewheel<br>
                of the William M. Black (32 tons)<br>
                as it turned on display were two spiderwebs,<br>
                <br>
                one a perfect diagram, one half-rent,<br>
                demonstrating how the wheel evolved<br>
                from weightlessness to weight.<br>
                <br>
                Why am I awake? I have climbed<br>
                out of the pit of sleep, out of the river <br>
                itself. Foghorns call over Iowa.<br>
                <br>
&#8216;Here is the place&#8217;, &#8216;beautiful land&#8217;, <br>
                in all its spellings: Ay-u-vois, Ayavois,<br>
                Ay-u-ou-ez, Aiaoua, Ayo&uuml;s, Ayo&euml;s, Ioway.</p>
              <p align="left"><span class="titulo1">(vi) The Leaves</span></p>
              <p align="left">People cling to each other<br>
                but no one has the strength<br>
                to be the branch &#8211; we are leaves<br>
                <br>
                now, nothing but leaves.<br>
                Names destined to die <br>
                in contact lists. The river<br>
                <br>
                is on the move and we<br>
                who have no claim to it,<br>
                who have claim to other rivers<br>
                <br>
                in other cities, other continents,<br>
                are swept along with it.<br>
                Choi, from South Korea,<br>
                <br>
                who says her language is<br>
                useless, useless, who has spent <br>
                her time translating it; Genti,<br>
                <br>
                struggling out of Albania&#8217;s<br>
                fifty years of isolation;<br>
&#8216;Doc&#8217;, Ashur fom Libya, <br>
                <br>
                who tells me my poems <br>
                are all the same, the same,<br>
                Doris, our <em>femme fatale</em>,<br>
                <br>
                and Nukila who buys her clothes<br>
                in the boys department, Srijato, <br>
                sad to have his young wife leave; <br>
                <br>
                and Choi again, stepping off<br>
                the path, saying in her useless<br>
                language she likes the sound of leaves.<br>
                <br>
              <span class="titulo1"> (vii) The Void</span></p>
              <p align="left"><em>That &#8220;cutting edge&#8221;, where all<br>
                true poems climb from and return to,<br>
                is the edge where the void begins.<br>
                <br>
                True vision is a bridge from here to there.<br>
                Emily Dickinson sat in her room<br>
                and the galaxy unrolled beneath her feet &#8230;</em><br>
                <br>
                I am thick with sleep, with reading.<br>
                And the river is thick with light,<br>
                the sky&#8217;s light I can see it reading.<br>
                <br>
                <em>She sat in her room and the garden<br>
                and the orchard outside her windows<br>
                took on the ghostly garments of infinity &#8230;<br>
                <br>
                The journey in life, as the song says,<br>
                is a lonesome valley. And all the roads<br>
                that wind through the ten thousand things!</em><br>
                <br>
                And that young woman reading <br>
                under the trees &#8211; lonesome, sure. <br>
                Even lonesomeness is enviable <br>
                <br>
                because we recognise ourselves in it.<br>
                There are poets of the dark and deep. <br>
                Not all of us can go there &#8211;<br>
                <br>
                some of us are statuary, seated, <br>
                bowed, like ancient mothers <br>
                of women reading under trees. <br>
                <br>
                Some of us have missed the boat <br>
&#8211; the boats we never saw for all<br>
the lights &#8211; our greenhood in the seas. </p>
              <p align="left"><span class="titulo1">(viii) The Cardigan</span></p>
              <p align="left">It was in blue, heather shades<br>
                of blue like a Fair Isle knit<br>
                but so finely done I knew<br>
                <br>
                I couldn&#8217;t afford it. Bizarrely,<br>
                the names of American&#8217;s ten <br>
                most dangerous men were woven in it,<br>
                <br>
                men who who had murdered<br>
                somebody close to me in my dream.<br>
                The Amish, like the Mennonites,<br>
                <br>
                are pacifists but, unlike them,<br>
                they practice shunning. We pass<br>
                the water tower, schoolhouse,<br>
                <br>
                the windmill and the cider press,<br>
                the birdhouse for purple martens;<br>
                the chicken house &#8211; caged layers<br>
                <br>
                for 8,500 hens &#8211; the clothing centre<br>
                that fixes buttons, zips, sends bales<br>
                to third world countries; outhouses,<br>
                <br>
                the Sunday School where services<br>
                are held in German, the silo,<br>
                Jo&#8217;s welding shop, brother-in-law<br>
                <br>
                Simon T&#8217;s quality horses;<br>
                grandpa houses, the coffinmaker&#8217;s<br>
                yard and the new Kalona library.<br>
                <br>
                I buy the rainhat I&#8217;ve been looking for.<br>
                <em>I see unknown places, I let myself<br>
                slide down the slope of dreams.</em></p>
              <p align="left"><span class="titulo1">(ix) The Island</span></p>
              <p align="left">A false fire alarm in the night.<br>
                We stand, shipwrecked on the grass,<br>
                islanded without our things.<br>
                <br>
                To say &#8216;island&#8217; as Milosz says <br>
                <em>is to be separated from the world<br>
                by a barrier difficult to penetrate</em><br>
                <br>
                <em>yet one that remains transparent, <br>
                a brilliant blue, and does not offer <br>
                a barrier to sight</em>. To say island<br>
                <br>
                is to reinvoke dressing gowns<br>
                and pyjamas flapping down corridors,<br>
                bells ringing, people missing,<br>
                <br>
                girl guide skills. Is to make<br>
                a saucer, a saucer of the sea.<br>
                Is to become a tea-leaf, a fortune<br>
                <br>
                in a cup, is to dig for termites<br>
                at the bottom of a tree. To say<br>
                island, island, is to name like Adam<br>
                <br>
                all your things: your <em>kotodama</em>,<br>
                your virtues and your sins. Is to make <br>
                loneliness disappear for good<br>
                <br>
                as naturally as youth does<br>
                for the island is ageless and time<br>
                is told, if told at all, only in beginnings:<br>
                <br>
                when the Seven Samurai are chosen,<br>
                the elected are trained, on the eve<br>
                of battle when the lyric spreads her wings.</p>
              <p align="left"><span class="titulo1">(x) The Black Angel</span></p>
              <p align="left">Below her, a red apple<br>
                still bearing a price ticket, <br>
                dimes and cents on the pedestal.<br>
                <br>
                Around her, a footworn path.<br>
                Beside her, a bush of yew <br>
                hiding a carved lily at the base<br>
                <br>
                of a tree stump monument<br>
                a midwife from Bohemia<br>
                had erected on her son&#8217;s death:<br>
                <em><br>
                Do not weep for me, dear mother.<br>
                I am at peace in my cool grave.</em><br>
                Above them, over eight feet tall,<br>
                <br>
                towers the Black Angel, who refuses <br>
                to look at the living, not unkindly, <br>
                but because she&#8217;s so intent. <br>
                <br>
                She could be learning the gavotte.<br>
                She could be learning grace.<br>
                Arms outstretched, inclining <br>
                <br>
                her head against one wing, <br>
                one knee bent, in the interval<br>
                between beats, about to take a step.<br>
                <br>
                In a golden bronze casting<br>
                blackened by oxidation and exposure,<br>
                her arms and feathers mottled green,<br>
                <br>
                thumbs and fingertips now broken,<br>
                she casts her kissing myths, murder myths,<br>
                cursing and redemption myths about her.<br>
                <br>
                <span class="titulo1">(xi) The Frith-Stool</span></p>
              <p align="left">In several English churches,<br>
                there was a stone seat beside<br>
                the altar known as the frith-stool <br>
                <br>
                (peace-stool) upon which<br>
                the seeker of sanctuary would sit.<br>
                I move bench, following the sun.<br>
                <br>
                The candle of the sun is enclosed<br>
                in a copper cylinder throwing<br>
                its shadow along the ground<br>
                <br>
                much like the filigree of trees.<br>
                But the filigree is that of<br>
                letters, each perforated band<br>
                <br>
                an indecipherable script.<br>
&#8216;They will be able to read<br>
                what I wrote, but what I wrote<br>
                <br>
                is a mystery itself&#8217; the chandler<br>
                said to the CIA who cracked<br>
                the code of an artwork similar to this.<br>
                <br>
                I too have been a candle <br>
                filtering languages. Why else <br>
                would I sit, on the first morning <br>
                <br>
                of wintertime, in a courtyard<br>
                outside the School of Journalism <br>
&amp; Mass Communication building,<br>
                <br>
                if not to escape the cacophony,<br>
                the babel of other languages<br>
                I can&#8217;t hear silence in?</p>
              <p align="left"> <span class="titulo1">(xii) The Sound</span></p>
              <p align="left">How sad he was, Arvo P&auml;rt,<br>
                not to have thanked his teacher<br>
                for the parting thought she gave him:<br>
                <br>
                that the biggest mystery in music<br>
                is something about &#8211; he couldn&#8217;t<br>
                remember her exact words &#8211;<br>
                <br>
                something about how to enter<br>
                a single sound, just as his janitor,<br>
                when asked how should a composer<br>
                <br>
                compose, replied: he has to love<br>
                each sound, each sound,<br>
                so that every blade of grass would be,<br>
                <br>
                P&auml;rt adds, as important as the flower<br>
                (and the bent man on the bent road<br>
                picking raspberries, the soprano<br>
                <br>
                holding a green pencil to mark<br>
                on her score where to breathe)<br>
                and the soul yearn to sing it endlessly.<br>
                <br>
                <em>This one note, or a silent beat, or a moment of silence,<br>
                comforts me. I build with primitive materials &#8211;<br>
                with the triad, with one specific tonality.</em><br>
                <br>
                <em>The three notes of a triad are like bells<br>
                and that is why I call it tintinnabulation.</em><br>
                Tintinnabuli &#8211; itself the sound of grass,<br>
                <br>
                blades moving like bells, harebells say,<br>
                though there are no flowers but stems alone<br>
                and a breath of wind to give the grass direction.<br>
              </p>

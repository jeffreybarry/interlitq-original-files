              <p align="left"><br>
                <span class="titulo1"> (i) The Amphitheatre</span></p>
              <p align="left">Two men are rolling tables,<br>
                round tables like golden discs<br>
                and now the legs are unfolded<br>
                <br>
                and there are more, many more<br>
                round tables like golden discs.<br>
                The amphitheatre is entabled, <br>
                <br>
                one golden arc of circles<br>
                on a bright green strip.<br>
                Now they are rolling more </p>

        we are privileged to publish fiction by two front-rank North-American 
        authors: <a href="/issue2/susan_daitch/bio.php" class="link1">Susan 
        Daitch</a> and <a href="/issue2/bret_anthony_johnston/bio.php" class="link1">Bret 
        Anthony Johnston</a>.<br>

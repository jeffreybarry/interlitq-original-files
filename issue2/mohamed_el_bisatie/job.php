              <p align="left"><br>
                <span class="menu"> <strong>A Modern Arabic Novel</strong></span></p>
              <p align="left">(<em>Hunger</em> will be published in April 2008  by the The American University in Cairo Press.)</p>
              <p align="left"><span class="menu"><strong>Translated by <a href="../../issue2/denys_johnson_davies/bio.php" class="link3">Denys Johnson-Davies</a></strong></span></p>
              <p align="left" class="texto2">The days when he worked did not last 
                all that long. She would see him coming along with his <em>gallabiya</em>                thrown over his shoulder, and she would be dejected, thinking 
                about the baking of bread, with the loaves in the basket almost 
                finished. It would appear from his exhausted face that he wanted 
                to sleep. She would join him in the living room: he liked her 
                to massage his back before he drifted off to sleep.<br>
                &#8220;What do you find enjoyable about sitting at home?&#8221;<br>
              He did not reply. </p>

          <td colspan="4" rowspan="2" valign="top" class="texto"> <div align="center" class="texto2"> 
              <p align="left"><br>
                <span class="texto2"> She leaned over him and her breast touched 
                his shoulder. Again she asked him. <br>
                &#8220;I don&#8217;t like anyone insulting my mother,&#8221; he 
                muttered.<br>
                &#8220;And who&#8217;s insulted her?&#8221;<br>
                &#8220;The customers on one occasion, and once the owner of the 
                caf&eacute;.&#8221;<br>
                &#8220;And why do they insult her?&#8221;<br>
                &#8220;Ask them. Insulting someone&#8217;s mother is something 
                they enjoy doing.&#8221;<br>
                He slept for two days and nights. She hid him a couple of loaves 
                in a piece of clothing beside the bedding. She heard him crunching 
                the bread when he woke up to eat, after which he went back to 
                sleep. Sometimes he asked her if there was something to dip the 
                bread in.<br>
                &#8220;Where from?&#8221; she says.<br>
                &#8220;Then an onion?&#8221;<br>
                &#8220;Where from?&#8221;<br>
                &#8220;Not even a bit of salt?&#8221;<br>
                She gave him the salt and he gulped down some water and went to 
                sleep.<br>
                Her feeling of fury went away. Every time, he had some sort of 
                reason to give. So what if they insulted your mother or your father? 
                Was that the end of the world?<br>
                &#8220;You listen to the insult and you just shut up.&#8221;</span></p>
              <p align="left"><span class="texto2">A week&#8212;that was all he worked. It was enough for her to 
                go once to the bakery. There were only three loaves left in the 
                palm basket for the two boys, and then she would go back to borrowing 
                and calling in at people&#8217;s houses. If only he had helped 
                her out with another week, or even four days, she would have been 
                able to do another baking. &#8220;There it is, though&#8212;that&#8217;s 
                how things are.&#8221;</span></p>
              <p align="left"><span class="texto2">He returned at night from his loafing around and sat himself down 
                on the stone bench on the other side and stared into the darkness 
                of the lane. &#8220;My goodness, if only I had a cigarette.&#8221;<br>
                He looked at Ragab, who was lying beside her leg, struggling against 
                sleep. She understood what he had in mind: he wanted to send him 
                to the grocer&#8217;s to bring him a cigarette on credit. &#8220;To 
                you of all people he&#8217;ll not sell on credit.&#8221;<br>
  &#8220;I know.&#8221;</span></p>
              <p align="left"><span class="texto2">The night was quiet, the moon was out and there was not a sound. 
                Everyone was asleep. Who but them in the lane was still up at 
                this hour? Colic brought on by hunger keeps sleep away. It would 
                only be an hour or so and their stomachs would quieten down. Stomach 
                cramps do not last&#8212;a twinge or two and they settle down. 
                </span></p>
              <p align="left"><span class="texto2">Her husband stretched out his legs in a relaxed way. He was in 
                a good mood&#8212;she did not know the reason, and she did not 
                want to know. She had enough on her plate. There was not one of 
                her neighbors from whom she had not borrowed some bread and not 
                yet given back what she had taken. What about going to them once 
                again? Maybe there were two of them who would not turn her down. 
                They all knew that she paid back what she borrowed&#8212;it was 
                the first thing she did on the day she baked bread.</span></p>
              <p align="left"><span class="texto2">&#8220;This time he&#8217;s stayed home a long time,&#8221; she 
                thought. &#8220;There&#8217;s not a thing on his mind.&#8221;<br>
                His voice came to her faintly. &#8220;That&#8217;s it, Sakeena.&#8221; 
                He gave a sigh and was silent.<br>
                She turned to him in surprise.<br>
  &#8220;Education&#8217;s a good thing,&#8221; he said.<br>
                Her surprise increased. &#8220;What education?&#8221;<br>
  &#8220;Education, woman&#8212;schools.&#8221;<br>
  &#8220;Schools? Fine.&#8221;<br>
  &#8220;Schools and universities. Tonight the students from the 
                village who are at university came back on holiday. They were 
                spending the evening at the caf&eacute; on the other bank, the 
                big caf&eacute;, the one with a wooden wall round it.&#8221;<br>
  &#8220;I know it, I&#8217;ve seen it.&#8221;<br>
  &#8220;I was sitting on its stone bench next to the wall and listening 
                to them talk. Oh, what talk it was&#8212;I&#8217;d understand 
                some of it and there&#8217;d be things I didn&#8217;t understand. 
                I felt I&#8217;d like to ask them.&#8221;<br>
  &#8220;And what is it you didn&#8217;t understand?&#8221;<br>
  &#8220;As though you&#8217;d understand, Sakeena&#8212;just shut 
                up.&#8221;<br>
  &#8220;I&#8217;ve shut up.&#8221;<br>
  &#8220;They say you shouldn&#8217;t work every day like the buffalo 
                at the water-wheel. You should have time for thinking. That&#8217;s 
                all very fine, but thinking about what? They didn&#8217;t say. 
                I just looked at them and kept quiet.&#8221;<br>
  &#8220;And of course you enjoyed that talk and you understood 
                it.&#8221;<br>
  &#8220;They said a lot of things, like what are we living for? 
                And I say to myself, what&#8217;s it all about? You&#8217;re just 
                living. They say you get married and have children. So what? And 
                I asked myself, what more do they expect of us? A lot of talk. 
                And after they went, I went too, with my brain boiling and spattering. 
                What I say is that there are people who are contented and people 
                who aren&#8217;t. It&#8217;s a funny world.&#8221;<br>
  &#8220;And what&#8217;s funny about it?&#8221;<br>
  &#8220;It just is.&#8221;</span></p>
              <p align="left"><span class="texto2">He fell silent. Drawing up his legs, he went to sleep on his side, 
                after which he stretched out on his back and began scratching 
                between his thighs.<br>
  &#8220;The whole day I&#8217;ve been scratching.&#8221;<br>
                He asked her if she had cleaned out his underpants before washing 
                them.<br>
  &#8220;I cleaned them, Zaghloul&#8212;piles of fleas and lice. 
                Be careful where you sit and where you throw them.&#8221;<br>
  &#8220;I don&#8217;t sit with anyone but you&#8212;and no one 
                but you takes them off me.&#8221;<br>
  &#8220;Who&#8217;s listening? Since when? A month ago?&#8221;<br>
  &#8220;Woman! What about last Thursday?&#8221;<br>
  &#8220;And you count that? Anyone else would be embarrassed to 
                mention it.&#8221;<br>
  &#8220;It was just the once.&#8221;<br>
  &#8220;Once? All night going here and there till your strength 
                runs out&#8212;what am I supposed to say?&#8221;<br>
                He gave her a kick, which landed on her thigh. It was the first 
                time he had kicked her. She curled up and kept quiet, while he 
                stood up and walked off.</span></p>
              <p align="left"><span class="texto2">It was a bad day for him when he got to know the students. He 
                went out each day at sunset and returned at first light. He looked 
                for them throughout the village until he found them, and then 
                sat not far away so that he could hear their voices. He did not 
                want to attract their notice so that his presence would annoy 
                them. Sometimes he realized that he was laughing when they were 
                or that he was nodding his head in agreement when he heard something 
                he understood and liked. </span></p>
              <p align="left"><span class="texto2">Occasionally he would not find them, and he would continue searching 
                around for them until he lost hope and made do with any group 
                of people he happened to come across, when he would settle down 
                quietly beside them and listen. But he would not be happy with 
                what they were saying, the same old talk they had talked before: 
                So-and-so went away, So-and-so returned, So-and-so beat his wife, 
                or she got angry and went off to her family&#8217;s house, So-and-so 
                had his animal stolen, the price of things goes up day by day, 
                and what the ration grocer steals every month. He was not excited 
                by anything he heard, and more than once he fell asleep and woke 
                up when one of them shook him to stop his snoring. How could they 
                compare with his friends the students? True, he did not understand 
                all that much of what they were saying, but what he did understand 
                exercised his mind for a long time, and even what he did not understand 
                he found himself attracted to, enjoying his perplexity as he turned 
                it about in his mind. Yes, and also their enthusiasm when they 
                were talking, their voices mingling and becoming louder. He would 
                nod his head as he listened to them with real pleasure. He liked 
                the words &#8220;my friends,&#8221; and he would go on repeating 
                them to himself. </span></p>
              <p align="left"><span class="texto2">They numbered five, and sometimes there would be one more or less. 
                They always gathered in the large caf&eacute;, in the same corner 
                that overlooked the street. When he did not find them there, he 
                would look around everywhere, sometimes catching sight of them 
                walking along the road through the fields. The sounds of their 
                loud laughter would reach him, while he was far off and could 
                not hear what they were saying, and this would upset him, though 
                he was happy just to be near them.</span></p>
              <p align="left"><span class="texto2">Sometimes when he did not find them anywhere, he would pass by 
                their houses, having found out where they lived, and he would 
                find them all gathered together in one house. He could distinguish 
                their voices from hundreds of others. He would listen a little 
                and would know from what they were saying that they would not 
                be going out that night, and so he would go off and look for some 
                other people.</span></p>
              <p align="left"><span class="texto2">Once he was sitting on the stone bench of the large caf&eacute; 
                alongside the fence, while they were inside. They were silent, 
                smoking water-pipes and coughing loudly.<br>
                He said to himself, &#8220;They&#8217;re too young to be smoking. 
                If only they&#8217;d wait five years, it would be better for their 
                chests.&#8221;<br>
                They were talking in low voices and he could not make out what 
                they were saying. Then little by little their voices grew louder: 
                they were talking about girls and women they had known at their 
                universities in Cairo, Alexandria, and Helwan. One of them was 
                renting a room in a flat without a door, a flat consisting of 
                two rooms on the ground floor. The other room was occupied by 
                a woman approximately in her forties. She lived alone and went 
                out early at night, not returning until the following afternoon. 
                On some days she stayed in her room without a sound being heard 
                from her. She dressed modestly and did not wear any make up; she 
                always had a green and black shawl around her shoulder. They would 
                greet each other fleetingly, but without saying anything. They 
                both closed the door of their room. He was shy of her, not knowing 
                anything about her. They had a shared toilet, and without any 
                agreement on their part, each would give a loud cough so as to 
                tell the other that they were on their way to the toilet, and 
                after finishing they would close the door of their room with a 
                loud bang to indicate their return. He said, &#8220;It&#8217;s 
                a lousy life.&#8221;<br>
                And they asked him if he had slept with her.<br>
  &#8220;Sleep with her? She&#8217;s as old as my mother.&#8221;<br>
  &#8220;Older women are nice.&#8221;<br>
  &#8220;Nice, and kind too.&#8221;<br>
  &#8220;Listen to the rest of it.&#8221; And he went back to his 
                story.</span></p>
              <p align="left"><span class="texto2">For some reason, he went out one night directly after she had 
                gone out. He saw her some steps ahead of him. He slowed down so 
                that she might be far enough ahead of him. They reached the main 
                street, which she crossed to the other side. He spotted her entering 
                a caf&eacute; on the square there. Driven by curiosity, he followed 
                her and entered the caf&eacute;. He was surprised to find her 
                sitting at a table opposite the door. Their eyes met. Meeting 
                her stern gaze, he was at a loss and seated himself on the nearest 
                chair he found. After this she ignored him, not looking in his 
                direction at all. He stole glances at her, and found her sitting 
                with a man in his fifties or slightly older. His hand fell several 
                times on hers as it lay on the table. He gently moved her fingers 
                between his and then moved her hand away. When the waiter came 
                with their order he leaned across slightly so as to put the tray 
                down on the table and rested his free hand on her shoulder. After 
                that she left with the man and the two of them took a taxi.</span></p>
              <p align="left"><span class="texto2">They shouted around him, &#8220;One of those.&#8221;<br>
  &#8220;As soon as you said a green and black shawl I knew. There&#8217;s 
                not one that came to my place without that same shawl on her shoulders.&#8221;<br>
  &#8220;And then?&#8221;<br>
                He went on with the story. He said that at noon the next day he 
                was going out to the college and was closing the door of his room 
                when he saw her coming from the entrance to the flat. She stood 
                in the passageway between the two rooms. She was beside herself 
                with anger and said quietly, &#8220;Would you be so good as to 
                tell me why you were following me yesterday?&#8221;<br>
                He was confused and said that he had not followed her and that 
                he had entered the caf&eacute; by chance and did not know she 
                was in there.<br>
                She was still angry as she looked him straight in the face and 
                said peremptorily, &#8220;Yes, I&#8217;m like that. Anything else 
                you&#8217;d like to know?&#8221;<br>
                He was at a loss and wet with sweat. Without another word he rushed 
                outside.</span></p>
              <p align="left"><span class="texto2">When he stopped talking, they shouted at him, &#8220;And then?&#8221;<br>
  &#8220;Yes, and then?&#8221;<br>
  &#8220;Nothing.&#8221; And he laughed.<br>
                He said that the two of them went back to as they had been, exchanging 
                greetings without saying anything, both minding their own business.<br>
  &#8220;And she didn&#8217;t come to your place?&#8221;<br>
  &#8220;She didn&#8217;t, and she had no intention of coming.&#8221;<br>
  &#8220;And you didn&#8217;t try?&#8221;<br>
  &#8220;Try what, my friend?&#8221;</span></p>
              <p align="left"><span class="texto2">He said that the only thing he was afraid of was that someone 
                from the village would come to visit him and see him in this situation 
                and that there would be a scandal, and so he began to look around 
                for somewhere else to live. The day he was moving his things he 
                was surprised to find her opening the door of her room and coming 
                toward him. She was wearing her dressing gown over her house <em>gallabiya</em>, 
                and her face was calm and sad. In a hoarse voice she asked him 
                if she had done something to annoy him.<br>
  &#8220;Not at all.&#8221;<br>
  &#8220;Then why are you leaving?&#8221;<br>
                At this he was frank with her. He said that she no doubt knew 
                about the customs of country folk. His father or one of his brothers 
                would certainly come to visit him one day, or would have something 
                he had to do in town, and would stay with him for a day or two 
                until he had finished. It would not be possible that they would 
                not come to know of her presence, and he would be put in an embarrassing 
                position, whether through the way they would look at her or their 
                attempts to talk to her and give her advice, all of which would 
                put him in a difficult position.<br>
  &#8220;I understand,&#8221; she muttered, without looking at him. 
              Turning round, she went to her room and locked the door.</span></p>
              <p align="left"><span class="texto2">After that, each one of the students told his story with women, 
                how they had got to know them and then had sex with them, with 
                or without payment, and the number of times they would do it in 
                one night, and the positions they did it in.<br>
  &#8220;The biggest lie you hear is that they are all the same 
                in the dark.&#8221;<br>
  &#8220;That&#8217;s right&#8212;no two are the same in anything.&#8221;<br>
  &#8220;The best moment for me is when I&#8217;m undressing her 
                bit by bit.&#8221;<br>
  &#8220;Like in the films.&#8221;<br>
  &#8220;Films maybe, but it certainly has the right effect.&#8221;<br>
  &#8220;I&#8217;ll cut my arm off if you&#8217;ve even touched 
                a woman before.&#8221;<br>
  &#8220;Look, after much experience I&#8217;d say there&#8217;s 
                nothing better than masturbation&#8212;alone with yourself, embracing 
                together in a moment of white heat.&#8221;<br>
  &#8220;You&#8217;re dead right. I know a woman who told me that 
                her husband leaves her in the bed and goes into the bathroom to 
                masturbate.&#8221;<br>
  &#8220;Ridiculous talk&#8212;from a degenerate woman. There&#8217;s 
                not a man in the world who would do such a thing.&#8221;                <br>
                </span></p>

                <span class="titulo1">Neighbourhood </span></p>
              <p align="left"><span class="texto2">Who are those guys - all shaved heads, shades and shorts - <br>
                parking their arses on the recently-repaired back wall<br>
                where the old clematis, taken for dead, displays<br>
                a fragile but flourishing talent for reincarnation? <br>
                Why do they hang about all day, barking into mobiles<br>
                in that strange rough tongue? What's with the unmarked van,<br>
                coming and going at all hours, the flash fast cars<br>
                which drop off leggy girls then roar up the road?<br>
              </span></p>

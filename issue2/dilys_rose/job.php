          <td colspan="4" rowspan="2" valign="top" class="texto"> <p align="left"><span class="texto2"><br>
              And that endless rapping on the street door; <br>
couldn't they install a bell, a buzzer, consider<br>
other residents? Why does nobody ever have a key?<br>
And why choose here for their halfway house <br>
              at the tail end of a mind-our-own-business cul-de-sac,<br>
              with the old folks' home across the way?</span></p>
            <p align="left"><span class="texto2">Local drama used to be a visit from the fire brigade -<br>
              three trucks per call-out - for burnt toast, mostly;<br>
              or, more sadly, a discreet, late-night ambulance. <br>
              Now every day sees a troop of displaced kids<br>
              with backpacks, holdalls, sandwiches, a hair's breadth<br>
              of hesitation in their step. Clearly they're obeying<br>
              instructions: where to go, who to meet, what to do<br>
            if nobody's home. Won't be staying long, of course.</span></p>
            <p align="left"><span class="texto2">Soon those guys, who've nipped round to the corner shop<br>
              for milk and beer and vodka will redirect their traffic,<br>
              pack off all the new recruits to hostels and sweatshops<br>
              in the hinterlands. If only they'd bugger off too.<br>
              What really gets our goat is the likelihood that<br>
              whatever they're up to is probably perfectly legal.
            </span></p>
            <p align="left">&nbsp;</p>
            <p align="left" class="titulo1">Another Tongue</p>
            <p align="left"><span class="texto2">You&#8217;ve been through the routine often enough<br>
              the wait at the baggage carousel, mesmerised<br>
              by a trundle of boxes, cases, backpacks. <br>
              Seasoned, you go it alone, navigate a route<br>
              through a trauchle of nattering tour groups.<br>
              At <em>Passport Control</em> you&#8217;re a model of patience<br>
              while grim guards squint at your unlikely likeness.<br>
              Innocent but purposeful, you stroll <br>
              through the green door: <em>Nothing to Declare</em>. <br>
              Your days of being plucked out by <em>Customs</em><br>
              to bare your belongings are well-nigh over. <br>
              Past the barrier, you&#8217;re brisk in clocking the exit<br>
              to taxis, buses, trains. You have your map, <br>
              your guide book, a room reserved, your phrasebook<br>
              open at earmarked questions: How? Where? When?<br>
              and yet at <em>Information</em> what little you&#8217;ve learnt<br>
              of the language deserts you, swirls off in a floss <br>
              of gibberish. You understand next to nothing<br>
              of what the desk clerk says, don&#8217;t trust her smile,<br>
              fear that beneath her stay-pressed blouse<br>
              lies dragonhide and spite: she means to teach you<br>
              a lesson you won&#8217;t forget, for daring to visit<br>
              her country, her desk, insufficiently fluent:<br>
              with her marker pen she won&#8217;t grant safe passage<br>
              to that <em>quiet but well-situated guest house</em>;<br>
              her directions will dump you in some dodgy quarter<br>
              to be set upon by ruffians who&#8217;ll subject you<br>
              to some horrible ordeal. You&#8217;ve been through the routine<br>
              often enough. By now you should know the ropes. </span></p>
            <p align="left">&nbsp;</p>
            <p align="left" class="titulo1">Lyrebird by Richard Browne, convict artist</p>
            <p align="left"><span class="texto2">He reckons I deserve better than a blank page,<br>
              begins with a wash of clouds, trees<br>
              and peaks picked out like needlepoint. <br>
              Besides, a formal rendering of wilderness<br>
            will tickle the folks back home.</span></p>
            <p align="left"><span class="texto2">He means well: has me strut on a sunlit rock <br>
              - doesn&#8217;t every jailbird dream of this? &#8211;<br>
              tail strings aquiver, thrilling the females<br>
              with fourteen filamentary feathers and two lyrate<br>
              belting out my cover version of clanking chains.</span></p>
            <p align="left"><span class="texto2">Were I as bright-eyed and vital as my likeness<br>
              and not a shot and stuffed and mounted mimic<br>
              of myself, I&#8217;d rather trail my stolen song<br>
              through the undergrowth, in the dawn mist,<br>
              rooting out sweeter songs to add to my cache.</span></p>
            <p align="left"><p align="left">&nbsp;</p>
            <span class="titulo1">What Lies Beneath</span></p>
            <p align="left"><span class="texto2">Opsophagia: not just a love of fish - a lust - <br>
              Blows in on the basil-scented breeze,<br>
              Slips between the flowing robes of the Aegean.<br>
              Epithalamions tongue praise of unholy unions:<br>
              Senator and roasted eel, hetaera and shrimp.<br>
              Seasoned gourmets can barely contain themselves:<br>
              Intimacy with such tender, saltsweet morsels -<br>
              <em>Oh yes, yes - how fresh, how sweet, how difficult!</em><br>
              Nothing compares to what lies beneath scales, a carapace.<br>
            </span></p><p align="left">&nbsp;</p>
            <p align="left" class="titulo1">Yew, Fortingall</p>
            <p align="left"><span class="texto2">It was always a good location; a pleasing aspect, open vista. Off 
              the beaten track.<br>
              They came. They saw. Fought their corner to settle nearby, construct 
              their places<br>
              of worship. Thin blue ribbons of smoke drifted skyward, encouraged 
              stargazing.</span></p>
            <p align="left"><span class="texto2">It was my party piece that sold them: every so often I&#8217;d seem 
              to keel over and die<br>
              then, just when it looked like the chop and splitting me into kindling<br>
              was more than likely - Hey Presto, Lord be praised - new shoots! </span></p>
            <p align="left"><span class="texto2">And so, once more, the wedding parties, mourners, random gangs of 
              vandals<br>
              paraded through my revivalist roots. Everyone wanted a piece of 
              me, a branch <br>
              of immortality. Torches scorched my trunk. I was stripped, skinned, 
              gutted.</span></p>
            <p align="left"><span class="texto2">The ultimate celebrity geriatric now, I&#8217;m knotted, warped, 
              I&#8217;m propped and pinned<br>
              in a typical attitude of Yewishness. Without the crutches and the 
              drystane dykes<br>
              I&#8217;d have let go centuries back, crumbled in on myself like 
              an old soak.</span></p>
            <p align="left"><span class="texto2">How they love to plunder. No riff-raff any more; just the specialists;<br>
              cone-gatherers, cutting pinchers, grafters. Cloning&#8217;s the 
              business:<br>
              seeding my DNA across the land. I have no choice but to proliferate.</span></p>
            <p align="left"><p align="left">&nbsp;</p>
              <span class="titulo1">Brasso </span></p>
            <p align="left"><span class="texto2">Now that the fairy lights have been stashed<br>
              for next year and the tree, brittle, scentless,<br>
              sprawls on the pavement, a Ne&#8217;erday casualty,<br>
              what will offset the darkness pressing down</span></p>
            <p align="left"><span class="texto2">like a heavy hand? I fetch the Java plate,<br>
              shipped home by granny and grandad<br>
              before the red gong of The Rising Sun<br>
              hit them in the face, and anything to hand</span></p>
            <p align="left"><span class="texto2">was sold for food. Meant for cakes,<br>
              the plate summons gin and quinine,<br>
              fields of tea, two strangers on a veranda.<br>
              The price of privilege almost killed them.</span></p>
            <p align="left"><span class="texto2">The price of poverty was even higher.<br>
              Tarnish &#8211; a year&#8217;s worth &#8211; ingrained<br>
              in the ceremonial skirts and headdress<br>
              of a long-nosed deity loosens, pools</span></p>
            <p align="left"><span class="texto2">on the dull brass disc, soaks<br>
              grubby rags, leaches into my hands.<br>
              The more I buff, the more pours out:<br>
              such an intensity of blackness.</span></p>
            <p align="left"><span class="texto2">No stopping now, not until the plate gives up<br>
              its last black bead of memory, not until<br>
              my warped reflection overlays an alien deity.<br>
              It&#8217;s mine, this heirloom sun.</span></p>
            <p align="left">&nbsp;</p>
            <p align="left" class="titulo1">The Stilled Sun </p>
            <p align="left"><span class="texto2">Light has squeezed the darkness<br>
              into a narrow seam of night.<br>
              Birdsong features 20/24.<br>
              You sleep differently: dreams<br>
              are shallow, fleeting, restless;<br>
              as if you should be up all hours,<br>
              getting on with what needs doing.<br>
              Grass challenges the lawnmower.<br>
              The cellar booms for mop<br>
              and broom and rubbish bags.<br>
              Shrubs whinge for the pruning shears.<br>
              The laundry nips your ear:<br>
              Peg me, peg me on the green. <br>
              Windowboxes beseech the watering can. <br>
              Weeds taunt the trowel.<br>
              The kitchen windows, last washed <br>
              - admit it ! - on Christmas Eve, <br>
              still bear the wingprints of a robin:<br>
              on Christmas morning it flew indoors,<br>
              trapped itself between two plates of glass<br>
              (then, treacherously clean) before<br>
              you eased it out into the snow.<br>
              Get busy. Delay and you'll miss your chance.<br>
              Midsummer, hovering in the wide bowl<br>
              of the stilled sun, will pass;<br>
              the world will turn on its axis;<br>
              night will spill its ink once more.<br>
              But if you go for the squeegee<br>
              might you wipe away forever<br>
            that wintry, red-breasted moment?</span></p>

<p><em><strong>Someone like You</strong></em></p>


<p>You’re here, a decade disappeared<br>
and even though time’s arrow propels you<br>
away from your cluttered past, a ghost <br>
of someone like you haunts streets and cities<br>
you’ve left behind, as if a memory, as if grief.<br><br>

This is what happens to those who migrate.<br>
Through terminal glass, as your plane inhales sky,<br>
someone like you takes a new breath. As you fly<br>
across the Atlantic, rain falling tenderly onto <br>
the skin of the ocean, they return to<br>
the apartment you vacated, their footsteps<br>
upon its wooden floor echoing in the heart.<br>
The wallpaper, desk, chairs, the bed<br>
you once rose late in: these furnish<br>
their mind as well as your own.  <br><br>

When you arrive in a new country,<br>
they write an elegy. When you marry,<br>
they write a sonnet. When your son is born,<br>
their first collection wins the Forward Prize.<br><br>

This is what you miss now.<br>
The snow someone like you witnesses,<br>
white and plentiful. The children they birth<br>
to absent fathers. The favourite cinemas<br>
that once swallowed reels of time <br>
while you watched <em>Lost Weekend</em>.<br> 
The fermenting odour of Edinburgh.<br>
The rich hours wasted in the Louvre,<br>
Les Tuileries and Pere Lachaise.<br>
The searing summer afternoons<br>
on a tinderbox council estate.<br>
Everything someone like you now sees,<br>
smells, touches, hears, tastes and senses<br>
displaces their existence from your own.<br><br>

One day you’ll return to their land,<br>
turn a key and open a door to find <br>
someone like you extending their hand,<br>
like a live electric cable, and the consequences<br>
of that moment will haunt you forever<br>
even as time’s arrow tears away<br>
once more, and so too you. <br><br><br></p>

<p><em><strong>Upon Medicating My Son</strong></em></p>


<p>No exorcism for this.<br>
How each limb fights<br>
its’ puppetry is shadow-show<br>
without relief. Like St Vitus’ Dance,<br>
the physic is veiled in such curses<br>
as my dark poetry no longer orates.<br>
So now, the doll and pin, the pit and fire<br>
are summoned in silent superstition<br>
each morning the teacher cavils again<br>
about my son’s cacoethes, the snare<br>
of his murmuration and twitchery. <br><br>

No infusion for this.<br>
Forget the wisdom of ergot and vinca, <br>
adder’s fork and blind-worm’s sting,<br>
lizard’s leg and owlet’s wing,<br>
intuition is the wildest remedy.<br>
So now, the school-bell ringing its elegy,<br>
a mind-man brews strange words<br>
like <em>impulsivity</em> and <em>hyperactivity</em>,<br>
while a medicine-man prescribes<br>
my son synthetic <em>herba benedicta</em>.<br><br>   

No spell for this<br>
survives the centuries of mothers<br>
who smouldered through the stake<br>
and lore our lost relative were bound to. <br>
So now my son swallows chemical curatives<br>
and sleeps as if the past never was,<br>
while the coven of night bewitches me<br>
with the charm of his transformation. </p>








<p>Peter France writes:</p>

<p>This was probably Evgeny Baratynsky’s last poem; it was sent from Naples to Russia for publication in the spring of 1844, together with the poem ‘Steamship’ (see ILQ 8). The two poems are the fruits of the poet’s first visit to western Europe, to Paris and Italy, in 1843-4, and they show a great lifting of his spirits after two decades of poetry marked by a stoical pessimism that reflects both Baratynsky’s personal destiny and the state of Russia under the despotic rule of Nicholas I. Very soon after this poem was written, however,  he died unexpectedly, aged only 44 – with hindsight the elegy takes on a doubly valedictory tone.</p>
	<p>One of Baratynsky’s longest non-narrative poems, ‘To Giacinto’ is addressed to the memory of Baratynsky’s Neapolitan tutor, Giacinto Borghese, who had gone into exile after the crushing of the short-lived Naples republic in 1799 – the disaster in which Admiral Nelson played so ominous a part. Borghese carried off to Russia some paintings that had escaped requisition and destruction, hoping to sell them; this didn’t work out, but he was offered a post as family tutor by Baratynsky’s father (the ‘venerable general’ of the poem, whose ‘much-loved tomb’ appears in the second section), and he remained in Russia, mainly at the family estate 300 miles south-east of Moscow, until his death.</p>
	<p>In reflecting on Giacinto’s life and death, Baratynsky is led to imagine the upheavals of the Europe into which he had been born in 1800 – the year after the Italian’s exile. There are references here to the chaotic history of Naples, the brief Napoleonic republic of 1799, and the rise and fall of the ‘condottiere’ Napoleon. In particular, he dwells on the heroic figure of the Russian general Suvorov, who came out of retirement to conduct a ruthlessly successful campaign against the French forces in Italy, only to fall into disfavour with the Emperor Paul. And beyond these, Baratynsky is conscious of the continuing presence in modern Italy of the great figures of ancient Rome, above all the ‘unequalled poet’, Virgil, against whom can be set ‘the gloomy bard, the child of Britain’, Byron. </p>
 
<p><em><strong>To Giacinto</strong></em></p>
<p>Giacinto, dear old friend, you fled distraught<br>
from Italy’s gold lemons and amber grapes<br>
and in your troubles, dreaming of a fortune,<br>
came to our snow-encrusted austere land<br>
laden with enigmatic paintings<br>
where you alone could make out anything!<br>
Forgive our common sense – of all the nations<br>
we were the last place for such speculation.<br>
You found no takers. So you gave up the dreams<br>
that lured you to our godforsaken north, <br>
but even then your rich Italian mind<br>
found a new life among a kindred people,<br>
and when the venerable general<br>
opened his purse to you, you happily<br>
entered a solemn contract that gave me<br>
the blessing of a foreign tutor. And Heaven<br>
be thanked that from that day for twenty years<br>
the bonds of our affection never weakened.<br><br>

And when we left the fields for Moscow, you<br>
were my guide in the old capital; thanks to you<br>
I got to know the macaroni folk,<br>
my mentor’s friends from sunnier lands. But then,<br>
alas! leaving behind a much-loved tomb,<br>
we came back to the steppes. By Heaven’s will,<br>
like you, dear son of Italy, I came to know<br>
a life of storms. But you, though far removed<br>
from your sun-beaten homeland, found with us<br>
a peaceful haven and a quiet grave.<br><br>

You gave your love to those who took you in,<br>
your life flowed in a single stream with theirs,<br>
you drowned the memories of your past in days<br>
of obscure doings in a foreign place;<br>
you bore our winter uncomplainingly<br>
and loved to hide from our brief summer’s heat<br>
in hollows shaded by cool-breathing oaks.<br><br>

Sharing our tears and our family festivals,<br>
you bowed your grey head on our days of grief,<br>
but hurried out, trembling with joy, when once<br>
a winter morning brought your former pupil,<br>
now a young officer, from distant lands<br>
to embrace his mother’s knees and fleetingly<br>
to bring a smile of life to her sad eyes.<br>
But still, though thankful for a Russian welcome,<br>
you never forgot your radiant native place!<br>
Vesuvius, the Coliseum, Capri, Saint Peter’s –<br>
you spoke about them all day long, you named<br>
the princes and the prelates of that land<br>
where once you had marvelled at Suvorov’s men<br>
entering the stately city, rank on rank, <br>
covered with battle dust, fearfully bearded,<br>
defying those flaming hours which as you said<br>
could drive even the dogs from city streets;<br>
the land that one year later saw the man,<br>
Bonaparte then, but soon Napoleon,<br>
briefly a king of kings, great condottiere,<br>
the architect of his own gigantic ruin.<br><br>

Hiding his thirst for power, he seduced you<br>
with revolutionary phrases, hollow sounds.<br>
The people greeted him with flaming lanterns,<br>
but you, you never forgot the silver spoons <br>
that you were forced to pay in slavish tribute.<br>
Through all the glitter of the people’s dreams<br>
you never could forgive Albion’s sad captive<br>
the systems of the Corsican man of war.<br><br>

Whatever happened in your time, the good<br>
and evil, all were chapters in your story:<br>
disgraced Suvorov amid the Alpine storms <br>
exhaled his warlike soul, untired by battle<br>
in the grapeshot of epigrams, your enemy<br>
found his last resting place on desolate cliffs,<br>
and your eyes closed when once your young disciples<br>
had recognized the pitfalls of the world;<br>
seeing this earth’s ephemerality<br>
and never forgetting, even at life’s feast,<br>
they fixed their gaze on the Italian tomb<br>
that lay within our churchyard fence.<br><br>
					
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;And I,<br>
I with my memories of your living speech,<br>
have seen the treasures of your Italy!<br>
Naples piled high in all her sunlit glory<br>
with purple vapours and abundant green<br>
that never fades – amphitheatre of palaces<br>
above the bright foam on the azure sea,<br>
and Cicero’s villa and the golden cave,<br>
still sacred for all those who love the Muse,<br>
where the unequalled poet’s ashes sleep<br>
who was the first to speak with epic voice<br>
among volcanoes, flowers, horror, delight,<br>
leading Aeneas into the murk of hell<br>
and showing him the enchanted fields, the sweet<br>
last refuge of the blessed spirits, those<br>
whose steadfast endurance of the trials of life<br>
earned them ethereal oblivion.<br><br>

Naples! Others before him had sought peace<br>
among your gardens, men of turbulent heart.<br>
Through veils of centuries we glimpse the villas<br> 
where Marius and Sylla came to rest.<br>
And who could be so unfeeling as not to thirst<br>
for a cave or grotto here among your charms – <br>
not a short-lived paradise, like those demigods <br>
who sipped from Lethe, then returned to battle, <br>
but a place to melt unthinking and unseen<br>
dreams of sweet ease with that last endless sleep –<br>
and in this Italy where everything – cascades,<br>
pines, poplars, mimosas, roses, even vines<br>
whose nameless leaves so faithfully embrace<br>
a face that lost its godhead long ago<br>
and hang from his half-sleeping forehead – all<br>
conspire to favour carefree happiness,<br>
you could not live here; not forgetting the sweet south,<br>
you gave your spirit to our blizzard’s harsh breath,<br>
never complaining that it might have flown<br>
beyond earth’s boundaries on Zephyr’s wings!<br><br>

Oh mysteries of souls! while the gloomy bard,<br>
the child of Britain, who for so many years<br>
dragged his heart’s torment over torrid shores,<br>
begging for his release from fateful thought<br>
among the olives, myrtles, sea and lava – <br>
in vain! (like Tantalus of old he saw<br>
the blessed stream slide mocking  past his lips),<br>
your heart found peace under the forlorn shade<br>
of skies obscured by blizzards half the year,<br>
among the ragged moss, the steppes, the needled firs.<br>
Sleep, sleep a blameless sleep among our snows!<br>
And let your sleep beneath the earth be rocked<br>
by our storm-breathing, midnight hurricanes,<br>
that can bring quiet and oblivion no less<br>
than the heady fragrances of southern airs!<br>
</p>




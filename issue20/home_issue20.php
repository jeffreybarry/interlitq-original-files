We are delighted to welcome you to Issue 20 of <em>Interlitq</em>, presenting the poetry of <a class="link1" href="http://www.interlitq.org/issue20/evgeny-baratynsky/job.php">Evgeny Baratynsky</a>, <a class="link1" href="http://www.interlitq.org/issue20/osama-esber/job.php">Osama Esber</a>, <a class="link1" href="http://www.interlitq.org/issue20/siobhan-harvey/job.php">Siobhan Harvey</a>, <a class="link1" href="http://www.interlitq.org/issue20/kona-macphee/job.php">Kona Macphee</a>, <a class="link1" href="http://www.interlitq.org/issue20/daniel-shapiro/job.php">Daniel Shapiro</a> and <a class="link1" href="http://www.interlitq.org/issue20/pedro-xavier-solis-cuadra/job.php">Pedro Xavier Sol&iacute;s Cuadra</a>; the prose of <a class="link1" href="http://www.interlitq.org/issue20/antonio-diaz-oliva/job.php">Antonio Diaz Oliva</a>, <a class="link1" href="http://www.interlitq.org/issue20/haidar-haidar/job.php">Haidar Haidar</a>, <a class="link1" href="http://www.interlitq.org/issue20/neil-langdon-inglis/job.php">Neil Langdon Inglis</a> and <a class="link1" href="http://www.interlitq.org/issue20/alan-wall/job.php">Alan Wall</a>; the prose poetry of <a class="link1" href="http://www.interlitq.org/issue20/pierre-chappuis/job.php">Pierre Chappuis</a>; and the translations of <a class="link1" href="http://www.interlitq.org/issue20/evgeny-baratynsky/job.php">Peter France</a>, <a class="link1" href="http://www.interlitq.org/issue20/allen-hibbard/bio.php">Allen Hibbard</a>, <a class="link1" href="http://www.interlitq.org/issue20/pedro-xavier-solis-cuadra/job.php">Suzanne Jill Levine</a> and <a class="link1" href="http://www.interlitq.org/issue20/pierre-chappuis/job.php">John Taylor</a>; with these texts complemented by the artwork of <a class="link1" href="http://www.interlitq.org/issue20/olga-sinclair/bio.php">Olga Sinclair</a>. Furthermore, the Editorial Committee of Interlitq is delighted to announce that <a class="link1" href="http://www.interlitq.org/issue20/allen-hibbard/bio.php">Allen Hibbard</a>, Director of the Middle East Center at Middle Tennessee State University, will be a Deputy Editor of the review with effect from this issue.<br><br>

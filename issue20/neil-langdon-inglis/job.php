<p><br><br><br><br><br>MICHAEL SERVETUS<bR>
1511-1553</p>

<p><strong>Man of Medicine by Day, Theologian by Night</strong></p>

<p>1553. While working as general practitioner in Vienne (Lyon), France, where he has long resided and is known by the French name Michel de Villeneuve, the Spaniard Michael Servetus (Miguel Servet) <em><strong>publishes a major medical discovery as a throwaway remark in the context of a theological dissertation. </strong></em></p>

<p>The scientific discovery attracts no attention, but other aspects of his book get him into trouble. He is arrested on suspicion of heresy; he is questioned by French inquisitors; he makes a jailbreak in the nick of time. Servetus flees France. However, instead of seeking refuge with like-minded people, he heads for Geneva, the home of his arch-enemy John Calvin.</p> 

<p>Servetus is re-arrested and tried. He is burned at the stake in Champel, a suburb of Geneva, the site of a teaching hospital today where memorials stand in his honor. Such recognition has been a long time in coming with a massive price to pay: His execution lasted for three quarters of an hour.</p>

<p><strong>Servetus’s childhood</strong></p>

<p>In 1511, Miguel Servet was born in Aragon (the town of Villanueva de Sigena claims the honor, although this distinction is disputed). Servet received a classical education with a strong emphasis on languages. In 1525, the young Spaniard entered the service of Cardinal Quintana, a Franciscan scholar.</p>

<p>In Spain, scriptural scholarship was at a high watermark. The Bible had just been released in a comprehensive edition in ancient languages (the Complutensian Polyglot, prepared in 1514-1517) with official backing. The New Testament would soon be released in a vernacular version notably bereft of such endorsement (the controversial Spanish translation by Francisco de Enzinas in the 1540s). Humanists all over Europe, the young Servet among them, were unfavorably comparing the pomp and worldliness of the contemporary Church with the ideal simplicity of early Christianity. </p>

<p>After attending law school in Toulouse (a bastion of religious orthodoxy), in 1529 Servet rejoined Quintana (now confessor to Emperor Charles V). He accompanied Quintana to Bologna to attend the Coronation of the Emperor by a cringingly subservient Pope. Appalled by the unseemly spectacle and total disregard for Christ’s teachings, Servet reached a critical turning-point. Biographer Roland Bainton takes up the story: “The Pope in turn compensated for his actual weakness by staging one of the most magnificent spectacles, exceeding the brilliance of medieval pageantry. (…) Private and public munificence erected triumphal arches and golden inscriptions on every corner to celebrate the Emperor’s victories. From the gaping throats of sculptured lions spurted red wine, and white from eagles’ beaks. Fife and drum, trumpet and trombone sounded like the Judgment Day. Dogs raced about and horses pranced. The Most Holy Father, Clement VII, rode in the midst of four cardinals on foot. On his head was a triple crown. Beneath a golden canopy he sat in a golden chair. When the Pope and the Emperor met, His Majesty kissed the feet of His Holiness.” </p>

<p>Here was a vision of the Antichrist on Earth. No bargaining with such a travesty was possible. </p>

<p><strong>Servetus’s early adulthood</strong></p>

<p>In 1530, filled with the purity of youth. the Spaniard abandoned Quintana’s service in horror. He spent time in Basel among religious Reformers (including Ecolampadius), yet upset them with his arrogance and refusal to compromise.</p>

<p>The following year, by now in Strasburg, de Villanueva tried his hand at theology, plunging with typical brashness into an incendiary topic, <em>De Trinitatis Erroribus (On the Errors of the Trinity)</em>. Never content to leave well enough alone, Servet sent a copy of his book to the Bishop of Saragossa, who in turn notified Quintana. Servet was denounced to the Supreme Council of the Inquisition in Spain. The Council, on May 24, 1532, sent instructions to the inquisitors at Saragossa to find out immediately where the said Miguel de Servet came from in Aragon, his family, age, where he studied, how long ago he left the country, and the names of his relatives. Investigators and snitches (Miguel’s own brother Juan among them) fanned out to determine from Miguel’s associates whether he had written to them, and how long ago, by what route and from what place, and if he had written home, to obtain the letters.</p>

<p>Already in peril themselves, the Swiss reformers did not need a whippersnapper at their heels to draw official attention. Miguel de Servet, the impetuous Spaniard from Aragon, was advised to pack his bags. Not for the first time and not for the last, he set out for a new destination where he might reinvent himself.</p>

<p><strong>The French years</strong></p>


<p>In the mid-1530s, “Michel de Villeneuve” [sic] surfaced in Paris, exhibiting characteristic intellectual voracity.</p> 

<p>He enrolled in medical school. Medical students of the period still studied at the feet of the ancient masters, including Galen. Galenic medicine’s conceptual universe was relentlessly theoretical. No outright ban had prohibited practical dissection in France; it was simply that prior to the end of the 15th century, the dissected cadaver was presumed to add little to information gathered from other sources. By the 1530s, evidence-based medicine was at last coming into vogue in Paris, and students were discovering for themselves that human innards bore no resemblance to ancient texts. Servetus’s classmate Vesalius would soon publish a treatise on anatomy that would sweep away ancient preconceptions – <em>De Humani Corporis Fabrica</em> (On the Structure of the Human Body) published in 1543. Himself moonlighting as editor for the Trechsel firm in Lyon in 1535-1541, MDV wrote “Syrups,” a more modest text on decoctions and astringents. At some point, we do not know exactly when, MDV arrived at an epoch-making anatomical observation, which he promptly set aside. Why? </p>

<p>How we wish he had continued along this path of scientific research! Alas, for Servetus, medicine was bread and butter. He could never resist the enticements of theology, and was soon drawn into a theological disputation with another recent Parisian graduate by name of John Calvin. Their vituperative exchange of letters was to continue over the next decade, reaching its climax in 1553. And it is here that we see the clearest illustration of the Spaniard’s bifurcated personality. In the years that followed, the young and generally goodnatured physician Michel de Villeneuve, treating patients by day, underwent transformation into the restless theologian Michael Servetus by night.</p>

<p>The late 1530s brought renewed signs of trouble as the young student was investigated for unorthodoxy, reflecting his interest in the discipline then known as astrology (not to be confused with modern-day horoscopes). He defended himself ably at his Paris hearings, and then, showing his expertise at timely getaways, he vanished into the night.</p>

<p><strong>Interlude</strong></p>

<p>We next hear from him in 1540. MDV had moved to Vienne (Lyon), presumably because of his ties with Trechsel publishers. Here he became personal physician and friend to Pierre Palmier, Archbishop of Vienne, suggesting that MDV had reconciled himself with the established Church to some extent. There is no record of any personal life (he rebuffed a lady’s overtures, informing her that he had suffered an unspecified “rupture”). This was a period of considerable professional success; MDV gained a reputation as a man who cured patients other doctors could not.</p>

<p>Professional acclaim was not sufficient, however. The brooding theologian Michael Servetus toiled by candlelight, preparing a manifesto containing that medical discovery we noted earlier. This would later emerge as <em>Christianismi Restitutio (The Restoration of Christianity)</em>. The author’s mind ranged over many disciplines, religious and secular. As editor he worked on editions of the Pagnini Bible (Latin) in 1542; a Spanish translation of Thomas Aquinas; and other more humorous publications on ethnic differences (“The Scots are prone to vengeance, ferocious, well built but negligent of their persons, envious and contemptuous. They revel in lies and do not seek peace like the English. The Irish are inhospitable, uncouth, cruel and more addicted to hunting and games than to agriculture (…) The English have a language, because of diverse origin, to pronounce. They have recently fallen away from the Church of Rome.”)  As we see, theology was never far away from the author’s concerns, nor the fate of those nations and individuals who drifted away from the established church.</p>

	
<p><strong>Late 1540s: Relations with Calvin Deteriorate Further</strong></p>

<p>He could not leave Calvin alone. He dipped his pen in vitriol and needled Calvin about the latter’s own orthodoxy. Insecure in his own power in Geneva, Calvin feared allegations and ammunition that could be turned against him.
Calvin bided his time pending an opportunity to unmask his tormentor. Servetus aka de Villeneuve sent Calvin a manuscript of his yet unpublished <em>Restitutio</em>. Calvin reciprocated by sending a copy of his own <em>Institutio</em> (Institutes of the Christian Religion). Servetus returned it sprinkled with abusive annotations. Now it was Calvin’s turn to snap.</p> 

<p><strong>The <em>Restitutio</em> is published</strong></p>

<p>1553: MDV vanity-pressed <em>Restitutio</em>—containing the historic anatomical observation that we referred to earlier. He sent a presentation copy to Calvin.</p>

<p>Calvin traced the book to its source, and with the aid of spies in Lyon, identified MS as MDV, denouncing Servetus to the ecclesiastical authorities.</p>
<p>The blend of daring and prudence so characteristic of Servetus’s life re-emerges at this crucial juncture in our story. He was arrested and brought before one of the most terrifying contemporary inquisitors, Cardinal Ory. Confident of his ability to match wits at the highest level, he engaged in double-talk at the hearings, escaped from jail, and vanished once again into the night, presumably drawing upon a hidden cache of money somewhere. He lay low for several months, and may have planned to relocate to a Protestant community in Italy. Instead, he showed up in Geneva, of all places. Perhaps he had grown tired of living a lie; he could have been curious to see Calvin in person; he may have entertained thoughts of sowing dissension between Calvin and the other Genevans (although Servetus seldom thought in strictly political terms). If he had dreamed of winning Calvin over with the force of his intellect, this was naivete at its purest; for Calvin had written to his colleague, Guillaume Farel, that should Servetus ever come to Geneva, "if my authority is of any avail I will not suffer him to get out alive." </p>

<p>In this era before passport photographs, how was Servetus recognized and apprehended? Did he stick up his hand in church? Was he spotted a former Parisian classmate? Whatever the explanation, he was seized and tried in a kangaroo court, convicted of anti-Trinitarianism and opposition to infant baptism, and burned at the stake in Champel, a suburb of Geneva. Copies of Restitutio were rounded up (some burned in the fire alongside their author). Only 3 originals survive.</p>

<p><strong>Which scientific observation did Servetus make? </strong></p>

<p>The pulmonary circulation of the blood:</p>
<ul>
<li><em>It is generated in the lungs from a mixture of inspired air with elaborated, subtle blood which the right ventricle of the heart communicates to the left. However, this communication is made not through the middle wall of the heart, as is commonly believed, but by a very ingenious arrangement the subtle blood is urged forward by a long course through the lungs; it is elaborated by the lungs, becomes reddish-yellow and is poured from the pulmonary artery into the pulmonary vein. Then in the pulmonary vein it is mixed with inspired air and through expiration it is cleansed of its sooty vapors. Thus finally the whole mixture, suitably prepared for the production of the vital spirit, is drawn onward from the left ventricle of the heart by diastole.</em></li>
<li><em><strong>That the communication and elaboration are accomplished in this way through the lungs we are taught by the different conjunctions and the communication of the pulmonary artery with the pulmonary vein in the lungs. The notable size of the pulmonary artery confirms this; that is, it was not made of such sort or of such size, nor does it emit so great a force of pure blood from the heart itself into the lungs merely for their nourishment; nor would the heart be of such service to the lungs, since at an earlier stage, in the embryo, the lungs, as Galen teaches, are nourished from elsewhere because those little membranes or valvules of the heart are not opened until the time of birth. Therefore that the blood is poured from the heart into the lungs at the very time of birth, and so copiously, is for another purpose. Likewise, not merely air, but air mixed with blood, is sent from the lungs to the heart through the pulmonary vein; therefore the mixture occurs in the lungs. That reddish-yellow color is given to the spirituous blood by the lungs; it is not from the heart.</strong></em></li>
<ul>

<p>To students of the history of science, these words possess and will forever retain their power to enthrall and inspire. But they were of secondary importance to their author.</p>

<p>What did Servetus truly consider important? Let us hear him in his own words, from the <em>Restitutio</em>:</p>
<ul>
<li>“Christ fills all things. He descends to the lowest depths and ascends to the loftiest heights and fills all things. He walks upon the wings of the wind, rides upon the air and inhabits the place of angels. He sits upon the circle of the earth and measures the heavens with his span and the waters in the hollow of his hand... Now Christ walks among the candlesticks, that is, in the midst of the churches, as the Apocalypse plainly teaches. His place is not in any particular heaven as some think... He dwells above all heavens and within us. [There are nonspiritual people] who separate Christ from us by placing him at the right hand of the Father. But we say that he is in that heaven to which the angels do not attain. He is in the third heaven where and whence he fills all things, beyond every place and beyond every tangible body.”</li>
</ul>

<p>As we look back over Servetus’s life and career, we might ask ourselves how MS resembled and differed from other reformers. Their commonalities may be summarized as follows:</p>
<ul>
<li>A traditionalist background.</li>
<li>A keen interest in languages (Greek, Hebrew) and Humanistic scholarship.</li>
<li>A focus on humanistic learning, sometimes including medicine (in common with Francisco de Enzinas).</li>
<li>A strong preference for the Bible rather than Church tradition.</li>
<li>Long periods in exile.</li>
<li>Lives led in disguise, and bifurcated personalities.</li>
<li>The ability to make swift escapes.</li>
<li>Jailhouse correspondence (both the English Bible translator William Tyndale, while imprisoned in Belgium, and Michael Servetus in Geneva, wrote heartfelt letters to their captors pleading for better conditions).</li>
<li>Wealthy patrons and protectors.</li>
<li>Betrayals by friends, family, colleagues.</li>
<li>Violent deaths.</li>
<li>Ability to defend themselves with agility, even when the cause was lost.</li>
</ul>

<p>And yet Servetus was distinctive in certain respects. Whilst other reformers sought safety in numbers, he worked in the belly of the beast. He was outwardly observant, willing to take the Mass, and had Catholic friends and employers. He reserved his venom for his own side, and had a unique ability to freeze potential allies into postures of undying hostility.</p>

<p>From our 21st-century vantage point, we are automatically well-disposed to those who made historic scientific discoveries. How were Reformers such as Servetus perceived by their adversaries?</p>
<ul>
<li>As heretics imperiling the salvation of ordinary folk. </li>
<li>As sexual deviants.</li>
<li>As practitioners of forbidden knowledge.</li>
<li>As people who have been corrupted and depraved by their travels overseas.</li>
<li>Spanish heretics were viewed as particularly dastardly given the general orthodoxy of Spaniards. Therefore, capturing a Spanish heretic was considered a huge prize. </li>
</ul>

<p><strong>The punishment for heresy</strong></p>

<p>Servetus was burned at the stake for heresy. What was the scriptural “justification” for burning heretics at the stake? Contemporary ecclesiastical authorities typically quoted John xv. 6: “If a man abide not in me, he is cast forth as a branch and is withered; and men gather them and cast them into the fire, and they are burned”. What exactly did burning at the stake involve? Here is a description of a Spanish auto-da-fe from Cecil Ross’s classic account of the Inquisition.</p>

<p>“To make these holocausts of human beings more ghastly, the pageant was enhanced by processions of exhumed corpses and heretics in effigy. Artificial dolls and decomposed bodies, with grinning lips and mouldy foreheads, were hauled to the bonfire, side by side with living men, women, and children. The procession presented an artistically loathsome dissonance of red and yellow hues, as it defiled, to the infernal music of growled psalms and screams and moanings, beneath the torrid blaze of Spanish sunlight.”</p>

<p>Some burned alive, some burned in effigy. The young Miguel de Servet is likely to have witnessed such a grisly spectacle as a young man in Spain. His very own immolation awaited him far from home in 1553. </p>

<p><strong>Aftermath</strong></p>

<p>In answer to critics, Calvin put together and published, in 1554, a justification, <em>Defensio orthodoxae fidei, contra prodigiosos errores Michaelis Serveti Hispani (Defense of Orthodox Faith against the Prodigious Errors of the Spaniard Michael Servetus)</em>. He argued that to spare Servetus would have been to endanger the souls of many. In the same year Calvin was answered by Sebastian Castellio, in <em>Contra libellum Calvini (Against Calvin's Booklet)</em>. Castellio declared that "to kill a man is not to protect a doctrine; it is but to kill a man. When the Genevans killed Servetus, they did not defend a doctrine; they but killed a man." He said that "if Servetus had wished to kill Calvin, the Magistrate would properly have defended Calvin. But when Servetus fought with reasons and writings, he should have been repulsed by reasons and writings."</p>

<p>Lone surviving copies of the <em>Restitutio</em> entered the antiquarian book trade. In later centuries, Voltaire would leap to Servetus’s defense. In recent times, the Unitarian Universalist Church has embraced MS as their “founder.” </p>

<p>At the end of his short and peripatetic life, Michel de Villeneuve grew weary of dodging and weaving, and embraced a final, cataclysmic reunion with destiny. His was a mind of agile ferment and wonder, snuffed out too soon. And yet his cruel death in the flames was merely the starting-point of a long and historic odyssey that continues to this day.<br><br>


</p>





            
           




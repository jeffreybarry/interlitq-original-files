<p><em><strong>Proust’s Bedroom</strong></em></p>

<p><em>“Around the bed a trillion concentric colored circles making centrifugal or centripetal movements like interlacing kaleidoscopes, shapes like those cast by the magic lantern, creating a strange and, for me, painful vision.  The red point in the center keeps sinking into incalculable, spastic distances, then returning, close up, and its coming and going felt to me like an unfathomable hammer.”</em><br>
--Ruben Dario, “Autobiography”</p>

<p style="line-height: 1.5;">For Marcel Proust, his bedroom was <br>
the fixed and painful point at the center<br>
of his preoccupations.  His mother,<br>
to distract him from his melancholy,<br>
sent for a magic lantern to be placed<br>
in the room, changing its opacity <br>
with a rainbow of colors<br>
as in Gothic stained-glass windows.<br>
But this intrusion destroyed<br>
the anesthetic effect of habit,<br>
and his uncertainty and sadness grew.<br>
When his mother died, he removed the lantern<br>
and relined the walls in cork<br>
so that only the indecipherable mystery<br>
of being one would flow within.  Seeing you<br>
In the oil lamp’s beam of light, pale in the submissive<br>
routine in which you know and do not know yourself<br>
I remember Proust. Open your eyes<br>
and though you don’t look at me and down deep<br>
the little girl you were seems imprisoned.<br>
I want to rescue her, but you cut me off.<br>
Only a faint ray of light like the little girl<br>
you enclose in your eyes<br>
barely appears in the crack of the door.<br><br><br></p>

<p><em><strong>La habitación de Proust</strong></em></p>
<p><em>“Alrededor del lecho, mil círculos coloreados y concéntricos, caleidoscópicos, enlazados y con movimientos centrífugos y centrípetos, como los que forma la linterna mágica, creaban una visión extraña y para mí dolorosa. El central punto rojo se hundía, hasta incalculables, hípnicas distancias, y volvía a acercarse, y su ir y venir era para mí como un martillo inexplicable”.</em><br>
Rubén Darío, Autobiografía.</p>

<p>Para Marcel Proust, su alcoba era<br>
el punto céntrico, fijo y doloroso<br>
de sus preocupaciones. Su madre,<br>
para distraerlo de su melancolía,<br>
mandó colocar una linterna mágica<br>
que cambió la opacidad del cuarto<br>
por irisaciones multicolores<br>
como en los vitrales góticos.<br>
Pero esta intrusión devastaba<br>
el influjo anestésico de la costumbre,<br>
y acrecía la incertidumbre y la tristeza.<br>
Al morir su madre, quitó la linterna<br>
y revistió de corcho las paredes,<br>
para que sólo fluyera el misterio<br>
indescifrable de ser uno. Recuerdo<br>
a Proust, al verte al haz del quinqué,<br>
pálida en la sumisa rutina en que<br>
te reconoces y te desconoces.<br>
Con dificultad abres tus ojos<br>
aunque no me miras, y, en el fondo,<br>
me parece confinada la niña que fuiste.<br>
Quiero rescatarla, pero me atajas<br>
y sólo una rayita de luz muy débil,<br>
como la niña que cierras en tus ojos,<br>
asoma apenas por la ranura de la puerta</p>




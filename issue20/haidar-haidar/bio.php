                  <p>Haidar Haidar was born in 1936 in the small village of Husain-al Bahr near Tartus in Syria. He is one of the most distinguished Arab writers today. In his work he has decried the abuses of tyranny and the lack of freedom, democracy and social justice in the Arab world and broken deep-rooted taboos by exploring forbidden themes.</p>
 
<p>In the 1960s he began publishing short stories in prestigious Lebanese and Syrian journals. One of his short stories, <em>Al-Fahd [The Lynx]</em>, was made into a feature film in 1969 and won several awards. In the early 1970s he worked as a teacher in Algeria before moving to Lebanon where he worked as a journalist, editor and book reviewer for several publishing houses. </p>


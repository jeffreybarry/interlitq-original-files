<p>
The poems in this selection are drawn from two manuscripts—“The Red Handkerchief and Other Poems” and “Woman at the Cusp of Twilight”—which bridge not only two periods in my writing life but the worlds of experience and dreams.  Both manuscripts explore my relationships with family members and others from childhood to adulthood while reflecting on themes such as desire, the creative life, and the passage of time.<bR><br>

—<em>Daniel Shapiro</em></p>





<p><em><strong>Disguise</strong></em></p>


<p>Tonight at the mirror,<br>
I admire my upper lip,<br>
the stubble growing darker each day.<br>
I keep dreaming of disguises—<br>
thick handlebars of hair,<br>
the great Fu Manchu,<br>
one like Gable's, pencil-thin,<br>
to make women swoon.<br>
In old photos, my father was his double<br>
until his face got recast<br>
into long Russian jowls and an apple nose,<br>
a black brush just beneath it.<br>
He was a man who scribbled <br>
faces on his eggs<br>
before releasing the yolks,<br> 
in the bowl the floating globes<br> 
like twin suns.<br>
Now I imagine my face becoming his<br>
no matter how I disguise it, <br>
I see his nose jutting out of <br>
my brow in ten years, <br>
my hair becoming yellow-gray and thin.<br>
I keep glancing&nbsp;&nbsp; behind me, <br>
into the mirror: <br>
my face blank as an egg.<br><br><br></p>





<p><em><strong>A Black Rose for William </strong></em></p>				


<p>In the penumbra of the garden,<br>
a black rose, my grandfather's rose.<br><br>

It began with a fragrant spiraling<br>
inside his bookish head, <br>
from hand to bush tamped in the ground<br>
to endless graftings on thorned stems,<br>
tea-rose pink to deepening burgundy<br>
until the black, velvety bud <br>
pushed through.<br><br>

Curiosity pushed him to this,<br>
nurturing a bloom mottling deeper<br>
to its stark conclusion, a bloom<br>
that would absorb every hue, <br>
the envy of Lorca's Spanish dancers,<br>
Poe's pale heroines—<br>
announcing a mansion slipping swiftly<br>
into mud.           <br><br> 
                
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Curiosity drove me<br>
to different flowers, different scents,<br>
roaming unlit parks at dark,<br>
never for roses but for the thrill of it,<br>
resonant night teemed around me,<br>
the crickets chirred.<br><br>

One night I felt my life emerging,<br>
fluid and wild, <br>
converging with his,<br>
as when he stepped out of the garden <br>
a fateful Sunday, his family <br>
loaded in the Packard for a Peekskill weekend, <br>
summer 1939.<br><br>
 
When he was gone, maybe at midnight, <br>
just as the black rose <br>
was unfolding, <br>
someone slipped inside the garden, <br>
leaving behind a wet footprint, <br>
the bush nude—<br><br>

maybe a stranger piqued by fragrance <br>
or a ladder of thorns, fingers plucked the petals <br>
swirling in his hand. <br>
In his pocket the bud unfurled <br>
its life of a hymn.<br>
	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;			&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;	&nbsp;&nbsp;&nbsp;&nbsp;				Maybe his jacket <br>
scraped the hedgerows, <br>
crossing lawns running home,<br> 
returning again when the night sky comes.<br><br>

That night, my sinuous route to him<br>
began, when I discovered our lives <br>
like twin flutes in duet   <br><br>

playing counterpoint <br>
across two times.<br><br><br></p>




<p><em><strong>“The Winged Aureole”</strong></em></p>

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;		<em>after Bessie Pease Gutmann</em><br>
In that print,<br>
a girl dances in a garden at dawn,<br>
doves floating around her<br>
in a rosy halo. I’m drawn<br>
to the light tread of her feet<br>
over cobblestones, her filmy nightgown<br>
rising and falling, the snow of wings.<br>
That girl could be my mother<br>
seeking pixies behind the house,<br>
calling her name down a well,<br>
<em>Shulamith</em>—I reach out my hand,<br>
she pulls me in. Grass rustling, frisky<br>
crickets tickle my feet.<br>
She turns perfect pirouettes,<br>
leads me back to a shadowed<br>
doorway behind the cobblestones<br>
cool as limestone, far from boredom,<br>
where hours never pass<br>
punctuated by shrill rings.<br>
My spiraling thoughts steep with hydrangeas,<br>
magenta-blue in the honeysuckle<br>
twilight. Out the window<br>
sparrows alight and when it darkens,<br>
glimmering wings,<br>
a host of pixies circles my room.<br><br><br></p>





<p><em><strong>Visions of Esther</strong></em></p>


<p>When I imagined God<br>
I saw my grandmother<br>
seated at a table in Heaven.<br><br>

Pale blue eyes, aquiline nose, silver bun—<br><br>

From the car she towered like a monument<br>
or a ship’s prow cresting the foam.<br>
But at the door she wore a housedress,<br>
an ace bandage circled one knee.<br>
Wind riffled a loose strand of her hair.<br><br>

I turn the glass knob<br>
and take in the sunparlor—<br>
the Kelly-green rocker, an antler of coral<br>
crowning the “boop-box” we never watched.<br><br>

Grandpa’s seals bit watermarked paper<br>
into indecipherable rings.<br>
He was a shadowy man.<br><br>

And shadowy men suggest shadowy things,<br>
mirrors and corridors, mothballs, canes,<br>
Grandma’s lips pursed over<br>
dentures, the woman upstairs no one had seen—<br><br>

we only heard along our ceiling<br>
thumping and scraping.<br><br>

Grandma bought me a model of a submarine<br>
on King’s Highway and a Timex watch,<br>
plied me with kasha steeped in broth,<br>
sponge cakes long as a Passover table.<br><br>

I watched her chopping<br>
sweet red onions, beating eggs<br>
beneath the flickering ring.<br><br>

She took her teeth out,<br>
scared me away<br><br>



and at night during my odd tantrum,<br>
asked me to take off my “Ubangi lip,”<br>
go back to sleep, return to my dreams.<br><br>

Raise your finger to your lips again,<br>
point upstairs, delight me some more:<br>
“<em>Shhh.</em>  Schindler lays with her ear to the floor.”<br><br><br></p>





<p><strong><em>Statues and Spirits</em></strong></p>
	
<p>	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;	<em>Metropolitan Museum of Art</em><br>
<strong>1. The Three Shades</strong><br><br>
I wasn’t fooled<br>
by those three male figures<br>
reaching down<br>
as if pulled by gravity<br>
toward Hell.<br>
Black and molten, muscles tensed<br>
with the skin of death,<br>
they didn’t fool me: <br>
they were the shades<br>
of my grandmother and her sisters<br>
—Esther, Gertrude, and Schiffie—<br>
classical beauties<br>
who thickened over time,<br>
their spirits mischievous as fire,<br>
souls clear as water.<br>
I wasn’t fooled<br>
by the surfaces of things.<br>
I knew they’d arrived <br>
to visit their own,<br>
disguised in a roof-garden,<br>
posing as Rodin’s famous cast, <br>
come to guide me<br>
that uncertain afternoon.</p>




<p><strong>2. The Three Graces</strong></p>
<p>The truth is I was feeling<br>
blue when I came upon<br>
these milky forms <br>
beneath a cupola<br>
in a sun-filled room.<br>
Now, they lean against each other,<br>
the casual play<br>
of arms on shoulders,<br>
trios of legs and hips and v’s<br>
in conversation. They have<br>
no heads, the air is dreaming<br>
with eyes of cerulean.<br>
I was feeling blue when I came upon <br>
their forms, light growing<br>
out of speckled marble.<br>
Flanked by amphoras<br>
draped with their togas,<br>
they’re pausing to bathe or sing or dance<br>
in this dreaming room.<br>
The blue hue fades<br>
to the clarity of a flame,<br>
and facing a wishing pool<br>
glittering with coins,  I imagine <br>
their faces as if one <br>
flickering on a pond—<br>
Gertrude, Schiffie, and Esther, <br>
aquiline noses, foreheads<br>
clear, the long dark hair<br>
they braid with water—<br>
before I toss in three dimes.<br>
Out of nowhere,<br>
a ghostly voice climbs air. <br><br><br></p>









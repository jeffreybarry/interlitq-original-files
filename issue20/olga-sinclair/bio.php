                  <p>Olga Sinclair nace en Panamá .   Sus estudios inician con su padre, el pintor Alfredo Sinclair.  A los 14 años participa en una muestra colectiva de artistas reconocidos de su país.  En 1976 comienza estudios en la Escuela de Artes Aplicadas y Oficios Artísticos de Madrid y recibe tres años de instrucción de dibujo clásico (Estudios Arjona).  En Ámsterdam estudia a los grandes holandeses del siglo 17.   A los 18 años tiene su primera muestra individual en Panamá.</p>

<p>En 1979 se matricula en la Universidad Santa María la Antigua de Panamá, donde obtiene su Licenciatura en Diseño de Interiores.  Participa, en 1982, en cursos de grabados del maestro Giangrandi.  Regresa a Europa en 1985.  Estudia, trabaja y expone en Londres.  Tiene su primer impacto-encuentro con la obra de Francis Bacon en la Galería Tate.</p>

<p>“A la edad de treinta años –señala el historiador y crítico de arte José Manuel Springer–  Olga Sinclair cruza la línea que separa al buen artista del gran maestro.”  Sostiene que “en más de treinta años de carrera ha caminado por rutas diversas que la han llevado de la figura a la abstracción, de regreso a la figura con carácter histórico y de vuelta a la experimentación con el espacio pictórico.  Tiene una parte formalista de carácter europeo, más enfocada hacia la experimentación plástica y el arte concepto.  Olga sintetiza ambos momentos.”</p>

<p>En 2007 experimenta una nueva etapa: la escultura, que se inaugura con la muestra “Es la Pera!” en México y Panamá.  En 2009, Arte al Día de Miami, Fl. publica su gran <strong><em>catalogue raisonné</em> “Olga Sinclair: Obras/Works, 1995-2008”.</strong></p>

<p>El libro de Olga Sinclair es una recopilación de 13 años de explosión de arte, color, creatividad. Editado por Hans Roedor y la Editora Arte al Día , con una introducción de José Manuel Springer, Crítico  e historiador mexicano y Carol Damian, Curadora de arte, consta de 224 páginas en las cuales  el Editor nos lleva de la mano a experimentar las diferentes etapas y situaciones que han influenciado en sus vivencias y que se manifiestan  a través de las manos que  componen su historia de Arte,</p>
  
<p>El arte de Olga Sinclair es eminentemente femenino sin ser feminista.




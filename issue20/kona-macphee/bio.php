                  <p>Kona Macphee grew up in Australia and now lives in Perthshire, where she works as a freelance writer and media producer, has serious fun as a <a href="http://www.konamacphee.com/about/">thingwright</a>, and runs the <a href="http://www.musetuners.co.uk">Muse Tuners</a> creative mentoring agency.</p>

<p>Kona received an Eric Gregory Award in 1998 and her first collection, <a href="www.konamacphee.com/tails/">Tails</a>, was published by Bloodaxe Books in
2004. Her second Bloodaxe collection, <a href="http://pb.konamacphee.com">Perfect Blue</a>, was awarded the Geoffrey Faber Memorial Prize for 2010. Her third collection, <a href="http://www.konamacphee.com/wlm/">What Long Miles</a> was published by Bloodaxe in March 2013 and was <a href="http://www.theguardian.com/books/2013/jul/28/what-long-miles-kona-macphee">The Observer's Poetry Book of the Month</a>
for July 2013.</p>


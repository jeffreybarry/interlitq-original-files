<p><em><strong> Paschal</strong></em><br>
<em>After Muldoon</em>
<p>This much we will not know - <br><br>

whether we should have stopped, and if the broken lamb <br>
that shuddered as we passed, but could not rise, would find<br> 
redemption or bleak mercy in some saviour grip, <br>
or fade on the sheep-pocked hillside where it lay; <br><br>

what slate our actions, our inactions tot their scores upon,<br> 
what act might wipe it clean; what use the blight of conscience <br>
rising, like a tardy god, to wish our sins away; <br><br>

from whence this spark, this flicker comes; where it may go;<br> <br>

this much we will not know. <br>
<br><br>
</p>


<p><em><strong>Shunned</strong></em></p>
<p>She was raised in the guise of a rose<br>
(those sixteen years green-sprigged, bud-tight,<br>
wearing the Lord's sharp words for thorns),<br>
as though a flower might stay its opening -<br><br>

but scarlet blooms on a hessian feedsack<br>
spread in that hay-hushed cleft of the loft,<br>
and smalltown whisperings begin to percolate,<br>
to tantalize, like perfume on the wind;<br><br>

and now, grubbed up, cast out, she clings<br>
to the hills – a shack on abandoned ground<br>
beyond the convex riverbend that crooks<br>
the little houses, tiny righteous folk -<br><br>

with only the steady wash of sun<br>
to bathe her dandelion-yellow crown<br>
of matted hair, the summer’s calm to tend<br>
her belly swollen to a seed-head sphere,<br><br>

and where, each night, some creeping Samaritan<br>
delivers an egg – a jug of milk – some bread -<br>
but never lingers, knowing the equal harms<br>
a stray kind touch, a stir of breath might do.<br></p>





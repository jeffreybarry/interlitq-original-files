<p>Hola, ¿qué tal todo?</p>

<p>Espero que bien. Por acá sin novedades desde la última vez.</p>

<p>Te escribo para contarte que estaba con <em>Memoria por correspondencia</em>, el libro de Emma Reyes, y me quedé pensando en lo que sucedía. ¿Has oído de Emma Reyes? No te preocupes, yo tampoco la conocía. Como muchos, no sabía de ella hasta hace algunos meses. En un taller del MFA una compañera colombiana presentó un texto sobre Emma Reyes y el nombre me quedó dando vueltas. Semanas después, perdiendo el tiempo en Amazon, vi que Memoria por correspondencia podía descargar y eso hice: lo compré y lo leí esa misma tarde en el Kindle. Es curioso, los dos libros que más me han gustado en estos meses (en español) son colombianos (el otro, por si quieres saber, es <em>El lejano amor de los extraños</em>, de Tomás González, y lo sé, yo también me canso de escuchar que González es “el secreto mejor guardado de la literatura colombiana” porque todos esos mecanismos de las editoriales agotan, pero si no has leído ese volumen de cuentos o mi preferido, la novela <em>La luz más difícil</em>, te los puedo prestar).</p>

<p>¿Te acuerdas de <em>Los Malditos</em>, el libro de la UDP editado por Leila Guerriero, con perfiles sobre escritores, bueno, malditos? Creo que Emma Reyes pertenece ahí. No es escritora propiamente tal, pero su vida encaja a la perfección en esa categoría. Así que antes que nada –que todo– te cuento: Emma Reyes es una pintora colombiana, pero pasó casi toda su vida fuera de Colombia. Vivió en Estados Unidos, Argentina, Italia y Francia, donde murió el 2003. <em>Memoria por correspondencia</em> fue mencionado como libro del 2012 en varias listas y trata sobre su infancia, en un convento de monjas, junto a su hermana Helena, las dos huérfanas. Te menciono lo de <em>Los Malditos</em> porque casi escribí una reseña del libro y pensé que el título perfecto sería “La Maldita”. Pero luego no pude, me bloqueé y en vez de eso me puse a escribir esto, que no sé si es una carta (¿quién escribe cartas hoy?), o un e-mail, o un documento Word que luego imprimiré y te pasaré por debajo de tu puerta. </p>

<p>No quiero contarte sobre la vida de Emma Reyes porque ya existen varios artículos al respecto y creo que seguirán saliendo más y de seguro acá, en algún departamento de español, se está iniciando una tesis doctoral sobre ella. Como sea: me acordé de ti porque este es un libro altamente emocional, aunque también literario (que no “literatoso”, como los libros que no te gustan, lo sé). Son 23 cartas que entre 1969 y 1997 Emma le escribe a Germán Arciniegas, un intelectual y político colombiano, además de una serie de dibujos de ella que, te confieso, no me gustaron mucho, aunque creo que sirven ya que ayudan a poner pausas entre carta y carta. Y repito que no te voy a detallar demasiado porque el libro es breve y debes conseguirlo y leerlo. Pero sí quiero mencionarte algunas partes que me hicieron reír, como cuando Emma le escribe a Arciniegas sobre la memoria o la fidelidad de la memoria, que es el gran tema de estas páginas epistolares:</p>

<p>“Jefe:</p>

<p>Tú no me haces correcciones y no sé ni siquiera si lo que escribo es comprensible. Hay momentos que me parece confuso y no sé si en conjunto se puede seguir la historia. Yo no dejo copia pues escribo directamente y ya no me acuerdo de lo que he escrito antes”.</p>

<p>“A ti te parecerá extraño que yo pueda contarte en detalle y con tanta precisión los acontecimientos de esa época tan lejana. Yo pienso como tú, que un niño de cinco años que lleva una vida normal no podría reproducir con esa fidelidad su infancia. Nosotras, tanto Helena como yo, la recordamos como si fuera hoy y la razón no te la puedo explicar. Nada se nos escapaba, ni los gestos, ni las palabras, ni los ruidos, ni los colores, todo era ya claro para nosotras”.</p>

<p>Me gustó <em>Memoria por correspondencia</em> porque justamente me recordó a los libros que no me gustan. O los libros que en estos momentos no me gustan. En las cartas de Emma Reyes no hay incesto, pero sí se habla del cuerpo, la marginalidad, la orfandad, y todos esos temas que varios escritores y escritoras de América Latina están trabajando, pero que a mí –luego de haber leído varias novelas– ya me aburren profundamente porque me recuerdan lo que escribió Marcel Proust: “Una obra de arte a la cual se le noten las teorías es como un regalo que se le ve la etiqueta del precio”.</p>

<p><em>Memoria por correspondencia</em> me hizo pensar en “Los Venenos” de Julio Cortázar, el relato que con mi madre leíamos todas las tardes, durante un verano, un par de páginas, hasta que se terminaba y volvíamos a empezarlo (sigue siendo mi favorito de Cortázar). Me recordó ese cuento porque al final lo de Emma Reyes también es un <em>coming of age</em> violento, un <em>tour de force</em> infantil tristísimo y muy bien narrado y por más que uno busque la etiqueta del precio no se le ve por ninguna parte (Emma no era académica, ni escritora y fue analfabeta hasta los 18 años, y se nota porque la escritura, creo, está libre de cualquier atadura o pretensión intelectual; lo que prima es la experiencia).</p>

<p>En fin, no quiero seguir arruinándote el libro, así que luego de esto voy a dejar de escribir. Te dejo mi parte favorita de las 23 cartas. Imagina a Emma –sé que no has visto una foto de ella, o tal vez ya la <em>googleaste</em>, no sé. Pero piensa en una adolescente huérfana que un día cierra la puerta del convento donde ha estado viviendo durante mucho tiempo. Y piensa que esa adolescente sale decidida a no regresar nunca, a dejar a su hermana y a las monjas. Y así lo hace. Y después, tiempo después, lo escribe de esta forma: “Antes de ponerme en marcha hacia el mundo me di cuenta que ya hacía mucho tiempo que yo ya no era una niña. En la calle no había nadie, solo dos perros flacos y uno le estaba oliendo el culo a otro”.</p>

<p>Abrazos,<br><br>

ADO</p>






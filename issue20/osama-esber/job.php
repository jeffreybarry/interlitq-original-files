<p><em><strong>The City of My Illusions</strong></em></p>

<p>The city of my illusions is large.<br>
It doesn’t give itself to anyone.<br>
It keeps blocking roads<br>
like any other city anywhere.<br><br>

I think about it today,<br>
crushed by the burden of its nightmare<br>
on the map of killing and destruction.<br>
I see myself pulverized stones,<br>
bloody skeins,<br>
iron rods protruding from collapsed buildings.<br><br>

In the city of my illusions<br>
the ruins are mirrors<br>
in which people see their indifferent faces.
</p>

<p align="right"><strong><em>مدينة أوهامي</em></strong></p>

<p align="right">
مدينة أوهامي كبيرة<br>
وعصيّة عليّ.<br>
لا أعرفها جيداً<br>
لا تُسلم نفسها لأي شخص<br>
وتظلّ تغلق الدروب<br>
كمثل أية مدينة أخرى<br>
في أي مكان.<br><br>

أفكر بها اليوم<br>
مرمية بثقل كوابيسها<br>
 على خريطة القتل والدمار<br>
وأرى نفسي أحجاراً مفتتة<br>
أشلاء دامية<br>
قضبان حديد نائتة من البنايات.<br><br>

في مدينة أوهامي<br>
الأنقاض صارتْ مرايا<br>
يرى فيها البشر وجوههم اللامبالية.<br><br><br></p>


<p><em><strong>Language</strong></em></p>
<p>I besieged language,<br>
emptied it of sincerity,<br>
stuffed it with lies,<br>
and said: I made you a mirror.</p>

<p align="right"><em><strong>اللغة</strong></em></p>
<p align="right">حاصرْتُ اللغة<br>
أفرغتُها من الصدق<br>
وحشوتها بالكذب<br>
وقلت لها: صنعتُ لكِ مرآةً.<br><bR><br></p>


<p><em><strong>Courage</strong></em></p>
<p>I remember the previous day<br>
but I don’t descend the stairs.<br>
I ascend until the moment of falling<br>
and when my face falls <br>
and is seen scattered in the void of the abyss<br>
I say: it once had features<br>
and looked everything in its face<br> 
with a rare courage.</p>

<p align="right"><em><strong>جرأة</strong></em></p>

<p align="right">
أتذكر اليوم الذي سبق<br> 
لكنني لا أهبط الدرج<br> 
أصعد حتى لحظة السقوط<br> 
وحين يتهاوى وجهي<br> 
وأراه يتبعثر في فراغ الهاوية<br> 
أقول: كانت له ملامح <br> 
نظر إلى كل شيء في وجهه<br> 
بجرأة عالية.<br> <br> <br> </p>


<p><em><strong>Exile</strong></em></p>
<p>We continue our exile<br>
trampled beneath feet of despair.<br>
Happiness treads on our heads and bodies<br>
in its daily passing.</p>

<p align="right"><em><strong>منفى</strong></em></p>

<p align="right">نزداد إيغالاً في المنفى،<br>
  متدحرجين بين أقدام السأم<br>
يطأ الفرح  رؤوسنا <br>
وأجسادنا في عبوره اليومي.<br><br><br></p>


<p><em><strong>Dryness</strong></em></p>
<p>My veins are parched<br>
though I am close to you, oh lake,<br>
thirsty for yourself.<br><br>

There is darkness in your water<br>
that digs its blades into your freshness<br>
while indifferent eyes<br>
are poisoned fish<br>
floating on the water.</p>

<p align="right"><em><strong>جفاف</strong></em></p>
<p align="right">أشعر أن عروقي ناشفة<br>
و أنا قريب منك أيتها البحيرة<br>
الظامئة إلى نفسها.<br>
في مياهك ظلمةٌ <br>
تحفر بأنصالها في عذوبتك<br>
فيما الأعين اللامبالية<br>
أسماك مسمومة<br>
 طافية على المياه.<br><br><br></p>

<p><em><strong>Loneliness</strong></em></p>
<p>Alone,<br>
caught in the net of despair,<br>
placed in the universal void,<br>
alone<br>
I hear the plaints of my friends in another country--<br>
what their sorrows whisper<br>
in their loneliness<br>
feathered like birds of expectation.</p>

<p align="right"><em><strong>وحدة</strong></em></p>

<p align="right">وحيداً <br>
عالقاَ في شبكة اليأس <br>
المنصوبة في الفراغ الكونيّ<br>
وحيداً<br>
أسمع ضحك أصدقائي في بلدان أخرى<br>
ما تهمسه  أحزانهم <br>
في وحدتهم المريّشة<br>
كطائر من اللهفة.<br><br><br></p>

<p><strong><em>Not as We Want</em></strong></p>

<p>Not as we want <br>
but as your lips dictate, we live,<br>
oh corpse of concrete and noise<br>
in which we move like numbers.<br><br>

We stand in lines in front of your cheap restaurants.<br>
We order pizza,<br>
hot dogs,<br>
hamburger,<br>
traveling towards an obesity <br>
to drown in its stagnant swamps.<br><br>

In your violent nights<br>
when we hear only <br>
sirens of ambulances and fire trucks,<br>
cries of drunks, <br>
and bullets of criminals,<br>
we retreat early to our beds.<br>
We dream of a day<br>
when we return <br>
to our imagined countries<br>
traveling on the wings of birds and butterflies<br>
or in paper airplanes<br>
or barefoot on roads strewn with jasmine.<br>
When we do not arrive<br>
we spit on our dreams<br>
but quickly clean them off<br>
because they are the homeland<br>
in which we live.</p>

<p align="right"><strong><em>لا كما نريد</em></strong></p>
<p align="right">
لا كما نريد<br>
بل كما تُملي شفتاك نعيشُ<br>
أيتها الجثة الاسمنتية<br>
التي نتنقّل فيها كالأرقام.<br>
نصطفّ أمام مطاعمك الرخيصة<br>
بالعشرات<br>
نتلقّف البيتزا<br>
والهوت الدوغ<br>
والهمبرغر<br>
مسافرين نحو سمنة<br>
نغرق في مستنقعاتها<br><br>

وفي لياليك العنيفة<br>
حيث لا تُسمع <br>
سوى أصوات سيارات الإسعاف والإطفاء<br>
وصرخات السكّيرين<br>
نلجأ إلى أسرّتنا باكراً<br>
نحلم بيوم ما<br>
نعود فيه إلى بلداننا<br>
المتخيّلة<br>
نسافر على أجنحة الفراشات<br>
أو في طائرات ورقيّة<br>
أو حفاة على دروب من الياسمين<br>
وحين لا نصل<br>
نبصق على أحلامنا<br>
ولكننا سرعان ما نغسلها<br>
ذلك أنها وطننا<br>
الذي نعيش فيه.<br><br><br></p>

<p><em><strong>Love</strong></em></p>

<p>I’m not sure if people still love,<br>
or if love is means or end.<br>
But when I saw you passing<br>
I became sure that language is inexhaustible.<br><br>

When you pass,<br>
the horizon extends,<br>
expands,<br>
opens space for more storms.<br><br>


One language is not enough <br>
to describe you<br>
in a poem.<br><br>

I need five languages<br>
from the main family <br>
to describe you. <br>
Sacred languages are useless<br>
to describe you.<br>
I need my fingers,<br>
my lips,<br>
what is between my thighs,<br>
what unites my pores with yours,<br>
to describe you.<br>
Then perhaps new languages will be born <br>
that will not be needed the next day.</p>

<p align="right"><strong><em>عشق</em></strong></p>
<p align="right">لا أعرف إن كان الناس يعشقون،<br>
إن كان العشق غاية أو وسيلة،<br>
ولكن حين رأيتك تعبرين<br>
تأكدت أن اللغة لا تُستنفد.<br><br>

حين تعبرين<br>
 يتمدّد الأفق<br>
يتّسع<br>
يفسح مجالاً<br>
لمزيد من الأعاصير.<br><br>

لوصفك<br>
لا تكفي لغة واحدة للقصيدة.<br>
لوصفك أحتاج إلى خمس لغات<br>
من العائلات الرئيسية.<br>
لوصفك  <br>
لا تنفع اللغات المقدّسة.<br><br>

لوصفك<br>
أحتاج لأصابعي<br>
وشفتيّ<br>
لما بين فخذيّ وما يوحّد مسامّي بمسامك<br>
عندها قد تولد لغات <br>
لا نحتاجها في اليوم التالي.<br><br><br></p>

<p><strong><em>A Decision</em></strong></p>
<p>I decided to depose myself<br>
from the thrones of the ego.</p>

<p align="right"><em><strong>قرار</strong></em></p>
<p align="right">قرّرت أن أطيح بنفسي عن عروش<br>
الأنا.<br><br><br></p>


<p><em><strong>Words</strong></em></p>
<p>In my words there are words<br>
that destroy them.<br>
***<br>
There are sewing machines<br>
to make words into shirts, slacks, jackets <br>
for poems, stories, novels and plays.<br><br>

Words are rosary beads in the hand.<br>
Words are dictionaries in jacket pockets.<br>
There are no longer words outside boxes.<br>
Enter any supermarket<br>
and buy the right words<br>
and don’t forget to read the label of contents.<br>
***<br>
In my country corpses pile up in graveyards<br>
and words bear spades<br>
not to take part in the burial<br>
but to smash someone’s skull.</p>

<p align="right"><em><strong>كلمات</strong></em></p>
<p align="right">في كلماتي كلمات<br>
تهدمها.<br>
***<br>
صارت الكلمات آلات خياطة<br>
لتفصيل القمصان والبنطلونات والسترات للقصائد والقصص والروايات.<br>
وربما للمسرحيات.<br><br>

صارت الكلمات كحبات المسبحة في اليد.<br><br>

صارت الكلمات قواميس في جيب السترة.<br>
لم يعد هناك كلمات خارج العلب.<br>
ادخل إلى أي سوبرماركت<br>
واشْتر الكلمات الملائمة<br>
ولا تنس أن تقرأ المعلومات الخاصة بكل علبة.<br>
***<br>
في بلادي<br>
الجثث تتجمّع في المقابر<br>
والكلمات تحمل معاولها<br>
لا لكي تشارك بالدفن<br>
بل لكي تحطّم جماجم بعضها بعضاً.<br><br><br></p>

<p><em><strong>  My Body</strong></em></p>


<p>Sometimes my body <br>
gets lost in its loneliness,<br>
sinks in its slumber,<br>
bored in its clothing.<br>
Suddenly it shakes things off<br>
and rebels against me,<br>
makes me forget who I am,<br>
drowns me in mud,<br>
affixes me to the wall of its whims,<br>
tricks me as I move,<br>
intoxicated in someone’s embrace,<br>
lost on bridges between one kiss and another.<br>
I hear its throbbing.<br>
I flow with its blood<br>
and close the distance between us.<br>
It is me,<br>
my beaming I,<br>
my desperate and hopeful I,<br>
that turns its back to time.</p>


<p align="right"><em><strong>جسدي</strong></em></p>
<p align="right">أحياناً أنسى جسدي<br>
غائباً في وحدته<br>
غارقاً في هدوئه<br>
مستوحشاً في لباسه<br>
وفجأة ينتفضُ<br>
يتمرّد عليّ<br>
وينسيني من أنا<br>
يغرقني في الوحل<br>
ويصلبني على جدار نزواته<br>
يتحايلُ عليّ<br>
وأنا أحلّق منتشياً في سرير بعد آخر<br>
متشرداً على جسور بين قبلة وأخرى<br>
أسمع نبضه<br>
أجري مع دمه<br>
ثم أردم المسافة بيني وبينه<br>
إنه أنا<br>
أناي المتلألئة<br>
المتهدّمة<br>
والتي تدير ظهرها للزمن.<br><br><br></p>


<p><em><strong>In Lattakia, My Birthplace</strong></em>
<p>In Lattakia<br>
I was a passing shadow,<br>
spume from an encounter <bR>
between wave and shore.</p>

<p align="right"><em><strong>في اللاذقية</strong></em></p>
<p align="right">في اللاذقية<br>
كنتُ ظلّ عابراً<br>
زبد اللقاء بين الموجة والشاطئ.<br><br><br></p>


<p><em><strong>Koln</strong></em></p>


<p>Koln:<br>
Streets that conspire against feet<br>
lead to more involvement in the city’s magic.<br>
I remember a loving woman,<br>
her buried desires<br>
scattered like hair of cats and dogs on seats,<br>
and the street that passed near her house:<br>
A river of metal pouring into the death of desires.<br><br>

In the train station<br>
I was like someone lost in the wilderness<br>
and when I boarded and took a seat<br>
the train seemed like a metal spear <br>
piercing the flesh of the void.</p>

<p align="right"><strong><em>كولن</em></strong></p>

<p align="right">كولن:<br>
شوارع تتآمر على الأقدام،<br>
تقودها إلى مزيد من التورّط  في فتنة المدينة.<br>
أذكرُ امرأة عاشقة<br>
كانت رغباتها المدفونة متناثرة كشعر القطط والكلاب على الأرائك<br>
والشارع الذي كان يمرّ قرب بيتها<br>
نهرٌ معدنيٌّ يصبُّ في موت الرغبات.<br><br>


في محطة القطارات<br>
كنتُ كالتائه في برية<br>
وحين صعدت وجلستُ في مقعدي<br>
بدا القطار كرمح من المعدن يخترق لحم المسافات.</p>









                  <p>Allen Hibbard’s personal narrative inscribes a trajectory of movement between the U.S., the Middle East and North Africa. Reared in Washington State, he went off to college at American University in Washington, D.C. He veered off to Egypt in 1985 where he taught four years at the American University of Cairo and wrote his dissertation, “Writing Differently Somewhere Else: Studies in the American Expatriate Novel,” with a view overlooking the Nile. Several years later he was to be found in Syria where he was a Fulbright lecturer in American literature at Damascus University. Over the past ten years he has been invited to speak at conferences and specially organized events in Jordan, Syria, the Netherlands, Tunisia, Italy, and Morocco. He now lives and writes in Murfreesboro, Tennessee.</p>
 
<p>Hibbard’s teaching and research interests have been in the areas of 20th Century American literature, literary theory, Middle Eastern literatures, and transnational/comparative literature. He is the author of <em>Paul Bowles: A Study of the Short Fiction </em>(Twayne, 1993), <em>Paul Bowles, Magic & Morocco</em> (Cadmus Editions, 2004) and <em>Crossing to Abbassiya and Other Stories</em> (in Arabic, Damascus: Dar al Mustaqbal, 1994); he also is the editor of <em>Conversations with William S. Burroughs </em>(University Press of Mississippi, 1999). His reviews, essays, stories, and translations have appeared in numerous journals, including <em>Partisan Review, The Review of Contemporary Fiction, The Centennial Review, Tennessee Folklore Society Bulletin, Grand Street, Edebiyat, Digest of Middle East Studies,</em> and <em>Middle East Studies Bulletin</em>. Among his current projects are a biography of Alfred Chester, and a translation (with Osama Esber) of <em>A Banquet for Seaweed</em> by Syrian novelist Haidar Haidar.


</p>

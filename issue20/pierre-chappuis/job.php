<p><em><strong>Once again, absent</strong></em></p>

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Puffs of wind are bringing her dwindled, almost faded <em>(like blueness, as at the end of an afternoon)</em> whiffs of wisteria. Balminess, mugginess, are overwhelming her.</p>

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Dreamy, at the window; both her thoughts and her gaze vanish into the distance—no, they keep coming and going, running over rolling hills opening out onto soft slopes before her eyes.<br>
	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Or rather, once again, she is absent, borne along on a blanket of light. . .</p>

<p align="center">*</p>

	<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;She sees herself <em>(truer!)</em>, she is walking lightly, melodiously <em>(quicker!)</em> within the colors of April, lending her voice, her tears.</p>

<p>	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Her walk: wakening within the complicity of the grasses, the stones to the wayside.<br>
	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Her gaze: as she passes, a quivering in the thicket.<br>
	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;And her breathing: peaceful, yet steady, sometimes almost panting in the shadowy hollows; ever in unison with the slack, unmoving hour when her desires are ultimately diluted. <br>
	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;In the same way the sky, now and then darkening, starts to clear.</p>


	<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Her desires. The pollen of her desires like a brief coloration of the air.</p>

<p align="center">*</p>

	<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Deliverance! At last a sob surges forth from inside her.<br>
	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Once again it diminishes <em>(languor!)</em>, dries up inside her; bursts out no more than the storm that has moved on behind the chimerical horizon.</p>


	<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Now all she has to do is yield to the languor, to the gloomy indolence of evening. </p>

<hr>

 <p><em><strong>On Charles Bridge</strong></em></p>

	<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;With that distraught look, that absent look of those for whom light no longer exists (a face, despite its youthfulness, bearing no fixed expression), noticing nothing of us or of all the beauty in our midst <em>(that slow poison that deliciously intoxicates the heart)</em>, without being dazzled <em>(may the distinction between day and night never fade)</em>, she probes with her stick the space in front of her <em>(emptiness, ah!, emptiness around her as she passes!)</em>, goes straight up the middle of the bridge toward the sunset: the last ray that the sun, like someone taking back his ante, would claim.</p>
	
    <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Amid the transparency where domes and steeples were hovering imperceptibly, amid the tender awakening of the vegetation, suddenly: which peace to savor?</p>
	
	<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Earth and sky, all the beauty in our midst (for the rest of us, all of us), now tarnished and undeserved, has lost its sheen. How to make it gleam again? Sadness and nostalgia rolling in the now darkened waters of the Vltava will provide no help. But blackened by the centuries and about to be devoured by the Prague night, would these saintly men and women in stone perhaps stake out a double hedge for this disinherited woman <em>(emptiness, emptiness all around her!)</em>, for this solitary blind woman, from one end of the bridge to the other?</p>

	<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Mirroring their torments, their grace, her face could only have brightened for whomever would have been inclined to approach her.</p>

<hr>

<p><em><strong>Nighttime, A Same Night</strong></em></p>

	<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Nighttime <em>(a secret point touched in depth)</em>, a same night, nighttime opens out as much as the egret-bearing silence. They have barely begun to come together and a sumptuous tumult sweeps in, responding to a common thirst.</p>

	<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;[Remembering this, what else does he do now but exalt it?]</p>

<p align="center">*</p>

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Futile, any struggle to curb the rising, invading waters, although chimerical <em>(if he had known. . .)</em>; and to wish to escape from games where mirrors keep multiplying themselves? From human being to human being <em>(from her to him; actually, from whom to whom?)</em>, from a word, from a gesture surges forth an accord as if from glaring discord.</p>

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;From yesterday to today, happy hustling and bustling as if everything were going to topple over into emptiness.<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Words <em>(their banality)</em> for unweaving and that once bound <em>(futile safeguards)</em>, would bind again, perhaps.</p>

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Doesn’t there seem to be a demon—detested, relentlessly pursuing his prey, of course—inside him, flopping around like a carp, the thief of the very thing <em>(fire, life)</em> that time has taken from him, that he will never receive from the Other <em>(the song will remain)</em> by whom he can only feel swindled?</p>

<p align="center">*</p>

<p>	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Arc powerless to join them together, in their momentum toward each other; song deprived of the keystone.</p>

<hr>

<p><em><strong>April</em></strong></strong>

<p>	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Young, walking down the narrow jetty of the morning in the tender April light. So young <em>(nascent)</em>, as if arriving from the wide sea by water that will never be seen so clear again. Emerging from no depths; as evidence <em>(childhood, all of childhood)</em>, her gaze that nothing darkens.</p>

<p>	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Sister of pollens and inflorescences—promises, foliage tenderly growing green again, the freshness before speaking, before the budding of words—she moves toward the willows, on the shore, stirred by a spring-like coming and going. </p>
	
	<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Summer, will you follow in the footsteps of your messenger who keeps walking bare-armed and barelegged despite the lasting cold? Child <em>(all of childhood once again)</em>: a lightning bolt streaking through the day without tearing it; an explosion of color, like the fragrance of gillyflowers. </p>

<p>	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;For sure, she couldn’t care less about the rags and tatters of the past season, but how <em>(“Schade”)</em> not pay tribute to the new, beautiful season within her?</p>

<p>	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Sister of buds, of blossoming cherry trees; so young, ahead of herself, future, an instant at my level <Em>(but the language: “Schade, ich verstehe nicht. . .”)</em>, foreign, already on the shore at the heart of the refoliation—bud, inflorescence, herself a cherry tree. . .</p>

<hr>

<p><em><strong>Tangible reality</strong></em></p>

<p>	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;At this hour of the afternoon, making a stop at some distance from the town on which, incidentally, the curtain of the rainstorm, obliquely drawn to the side, is falling.<br>
	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Over there, all the shadow of the sky <em>(elsewhere, the sun is now shining)</em> is gliding along as if across an immense imaginary roof.</p>

	<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Appeasement, rest, at the top of the only hillock dominating this expanse where, over a swirl of hedge-bordered fields, nothing hinders sight.</p>

	<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Near the three trees, making a stop at the heart of reality and turning toward the west, in full daylight <em>(almost a veil of heat, a shimmering)</em>, at this place spotted from afar and finally reached, a point of solitude much more than a point of view.</p>

<p align="center">*</p>

<p>	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;More important than the trees with their dense foliage is the halo of clarity hovering around them, regardless, while at their feet evening is already piling up down the slope. Will the halo welcome in some vagabond or idler when night comes, after the departure of children vaguely busy fishing <em>(their shouting behind my back, their vexed cries at every unsuccessful cast)</em>, the edge of the pond then swarming with shadow? </p>

<p align="center">*</p>

<p>	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Houses, watermills, pasturelands fade from one part of the perspective to the next, plot after plot of land dwindling until it flees into the infinite and thoughts left blank scatter, free as the air, into the wider space of the sunnier weather.</p>

<p>	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Tangible reality—fabulous, imaginary.</p>

<hr>

<p>(<em>Le Noir de l’été</em>, Geneva, Switzerland: Éditions La Dogana, 2002)<br><br><br><br><br><br><br><br></p>

           
           
<p><em><strong>Là encore, absente</strong></em></p>


	<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Par bouffées, le vent lui apporte, amoindri, presque passé <em>(de même le bleu, comme d’une fin d’après midi)</em>, un parfum de glycine. Tiédeur, moiteur l’envahissent.</p>


	<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Rêveuse, à la croisée ; son esprit autant que son regard se perdent, — mais non : tout allées et venues — parcourent coteaux et vallonnements épanouis en pente douce sous ses yeux.<br>
	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Ou plutôt, là encore, absente, portée sur une nappe de lumière. . .</p>


<p align="center">*</p>


	<p> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Elle se voit <em>(plus vrai !)</em>, elle va, légère, mélodieuse <em>(plus vif !)</em> dans les couleurs d’avril, prêtant sa voix, ses larmes.</p>


<p>	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Sa marche : éveil dans la complicité des herbes, des pierres hors des chemins.<br>
	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Son regard : au passage, ce frémissement dans les halliers.<br>
	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Et sa respiration : paisible, pourtant soutenue, presque haletante parfois dans les creux d’ombre ; toujours à l’unisson de l’heure étale, immobile, où ses désirs finalement se diluent.<br>
	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;De même, dans le ciel, dissipation de temporaires assombrissements.</p>


	<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Ses désirs. Le pollen de ses désirs comme une brève coloration de l’air.</p>


<p align="center">*</p>


	<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Délivrance ! En elle afflue enfin un sanglot.<br>
	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Une fois encore, il s’amenuise <em>(langueur !)</em>, s’épuise en elle ; n’éclate pas plus que l’orage passé derrière l’horizon chimérique.</p>


	<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Elle n’a plus maintenant qu’à céder à la langueur, à la morne indolence du soir.</p>


<hr>

<p><em><strong>Sur le pont Charles</strong></em></p>


	<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;De l’air égaré, de l’air absent de ceux pour qui il n’est plus de lumière (un visage, quoique jeune, où ne se fixe nulle expression), sans rien voir de nous ni, autour de nous, de tant de beauté <em>(ce lent poison dont le cœur délicieusement s’enivre)</em>, sans être éblouie <em>(ne s’éteigne la distinction du jour et de la nuit)</em>, de son bâton, elle sonde l’espace au-devant d’elle <Em>(le vide, ah ! sur son passage, le vide !)</em>, elle va sans écart au milieu du pont, droit devant elle, vers le couchant : dernier rayon que le soleil, comme qui retire sa mise, reprendrait.</p>


	<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Dans la transparence où vacillaient imperceptiblement clochers et dômes, dans le tendre éveil de la verdure, soudain, quelle paix goûter ?</p>


<p>	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Terre et ciel, tant de beauté autour de nous (nous autres, nous tous), maintenant ternie, imméritée, s’est rembrunie. Comment lui redonner éclat ? Tristesse, nostalgie roulées dans les eaux maintenant sombres de la Vltava ne seront d’aucune aide. Mais, noircis par les siècles, saints et saintes de pierre, eux que la nuit pragoise est sur le point de dévorer, feraient-ils pour la déshéritée <em>(vide, le vide autour d’elle !)</em>, pour l’aveugle seule, d’un bout à l’autre du pont, égrenée, une double haie ?</p>


<p>	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Miroir de leurs tourments, de leur grâce, son visage, pour qui se serait porté à sa rencontre, n’aurait pu que s’épanouir.</p>


<hr>

<p><em><strong>La nuit, une même nuit</strong></em></p>


<p>	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;La nuit <em>(un point secret touché en profondeur)</em>, une même nuit, la nuit autant que le silence porteur d’aigrettes s’ouvrent. À peine esquissé entre eux un rapprochement, voilà que déferle un somptueux tumulte, réponse à une soif partagée.</p>


<p>	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;[Se le remémorant, que fait-il d’autre maintenant que l’exalter ?]</p>


<p align="center">*</p>


	<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Vaine, toute lutte pour enrayer la montée d’eaux envahissantes, pourtant chimérique <em>(l’eût-il su. . .)</em> ; et vouloir échapper à des jeux de miroirs allant en se multipliant ? D’être à être <em>(d’elle à lui ; au juste, de qui à qui ?)</em>, d’un mot, d’un geste jaillit comme d’une criante dissonance un accord.</p>


<p>	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;D’hier à aujourd’hui, l’heureux remue-ménage comme si tout allait basculer dans le vide.<br>
	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Paroles <em>(leur banalité)</em> pour détisser et qui nouaient <em>(vains garde-fous)</em>, noueraient encore, peut-être.</p>


<p>	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;N’aurait-on dit, acharné sur sa proie, n’est-ce pas, détesté, un démon qui, en lui, s’agite comme une carpe, voleur de cela <em>(feu, vie)</em> que le temps lui a pris, qu’il ne recevra pas de l’Autre <em>(restera le chant)</em>, dont il ne pourra que se sentir floué ?</p>


<p align="center">*</p>


<p>	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Arc impuissant à les relier, dans leur élan l’un vers l’autre ; chant privé de clé de voûte.</p>


<hr>

<p><em ><strong>Avril</strong></em></p>


<p>	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Jeune, marchant sur l’étroite jetée du matin, dans la tendre lumière d’avril. Si jeune <em>(naissant)</em>, comme venue du large portée par une eau qu’on ne rêverait pas plus claire. Surgie de nulle profondeur ; témoin <em>(l’enfance, toute l’enfance)</em> son regard que rien n’assombrit.</p>


<p>	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Sœur des pollens et des inflorescences — promesses, tendre reverdissement, fraîcheur d’avant les mots, d’avant l’éclosion de la parole —, elle s’avance vers les saules qu’avive sur la rive un va-et-vient printanier.</p>


<p>	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Été, emboîteras-tu le pas à ta messagère qui va jambes et bras nus en dépit du froid encore vif ? Enfant <em>(toute l’enfance encore)</em> : un éclair, qui traverse le jour sans le déchirer ; une explosion de couleur, tel un parfum de giroflée.</p>


<p>	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Sûr, des haillons de la saison ancienne, elle n’a cure, mais comment <em>(« Schade »)</em> ne pas saluer en elle la belle, la nouvelle saison ?</p>


<p>	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Sœur des bourgeons, des cerisiers en fleurs ; si jeune, en avant d’elle-même, future, un instant à ma hauteur <em>(mais la langue : « Schade, ich verstehe nicht. . . »)</em>, étrangère, déjà sur la rive au cœur de la refeuillaison — bourgeon, inflorescence, cerisier elle-même. . .</p>


<hr>

<p><em><strong>Tangible réalité</strong></em></p>


<p>	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;À cette heure de l’après-midi, faire halte à l’écart de la ville sur laquelle tombe au passage, obliquement tiré de côté, le rideau de l’orage.<br>
	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Par là, toute l’ombre du ciel <em>(ailleurs, l’éclaircie)</em> glisse comme le long d’un immense toit imaginaire.</p>


	<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Apaisement, répit au faîte du seul monticule dominant l’étendue où, tournoiement de champs bordés de haies, rien ne vient faire obstacle au regard.</p>


<p>	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Près des trois arbres, tourné vers l’ouest, en pleine lumière <em>(presque, un voile de chaleur, un vacillement)</em>, faire halte au cœur de la réalité, en cet endroit repéré de très loin, enfin atteint, point de solitude bien plus encore que point de vue.</p>


<p align="center">*</p>


<p>	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Plus que les arbres au feuillage épais compte le nimbe de clarté qui les entoure, flottant, quand bien même, à leur pied, le soir déjà s’accumule dans le dévers. À la nuit, après le départ de gosses vaguement occupés à pêcher <em>(leurs cris dans mon dos, leurs exclamations de dépit à chaque coup manqué)</em>, le bord de l’étang grouillant d’ombre accueillera-t-il quelque vagabond ou quelque oisif ?</p>


<p align="center">*</p>


	<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Maisons, moulins, pacages s’estompent d’un plan au suivant, parcelle après parcelle s’amenuisent pour fuir à l’infini et les pensées laissées en blanc s’égaillent dans l’espace élargi de l’embellie, libres comme l’air.</p>


<p>	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Tangible réalité fabuleuse, imaginaire.</p>

<hr>

<p>(<em>Le Noir de l’été</em>, Geneva : © Éditions La Dogana, 2002.)</p>




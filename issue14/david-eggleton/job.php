            <p LANG="en-US"><strong>Graveyard School</strong></p>
            
            <p LANG="en-US">Harpoon spires, coal smoke, iron rustbucket, <br>
            daylight’s bright fine gold, cranny of the South. <br>
           Judgement’s dungeon cell, lost hospital maze, <br>
            tiny ark with cabinets of curiosities padlocked. <br>
            The lone piper able to bring the young running <br>
            the nexus of street veins to the octagonal heart. <br>
            Then submarine arcades, fleets of wooden shops, <br>
            sell-out sermons on oyster saloons ready to open. </p>
            
            <p LANG="en-US">But between pipe skirls and wool skeins stands a <br>
           boil-in-the-bag city, whose teaspoon-tinkle stanzas <br>
             announce fine china cups are running over absently, <br>
            populace gone in search of oats and possum stew. <br>
             Who’ll buy clay chamberpots, a weighing machine? <br>
             If buildings are porridge-coloured, eminent stone, <br>
             go down into the catacombs: there the dead snore. <br>
             The gannet colony’s a rest home, sounding out oracles. </p>
            <p LANG="en-US">Repeat the series of thirty-nine steps after Cargill, <br>
            from his named summit, street, monument, corner, <br>
            and watch last Century wash up in cinema lacework. <br>
            Ironmonger’s nails swapped for a sack of earth that’s <br>
            sewn into a uniform riding through Canongate. <br>
            Under a shirt of frosty stars, the kilted hills. <br>
            An eight-sided poem spiders the crystal screen: <br>
            hail to the ears, the whole town gets up and cheers. </p>

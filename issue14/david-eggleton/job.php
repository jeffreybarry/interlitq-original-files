<p><strong><a name="lines">Lines</a> at Wharf’s End</strong></p>

<p>Summer’s evening gown ruffles gold silk;<br>
saxophones of stars tilt;<br>
surf ebbs, and beach guitars plink;<br>
conch shells of cloud squeal to pink;</p>

<p>breezes trumpet the sun’s farewell;<br>
a flagging cabbage tree rustles its leaves;<br>
night throbs on rusty reefs of roofs;<br>
marimbas of town lights melt towards overseas.</p>


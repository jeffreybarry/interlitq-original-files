            <p><strong>7</strong></p>
            
            <p>Before I could muster <br>
            The lettuce for the refrigerator<br>
            I washed the outer leaves<br>
            Between a spray of water<br>
            &amp; the minutiae of my<br>
            Boy fingers – thwacking it <br>
            On the bench for the sensation<br>
            Of its ribbed-heart – quite hard.<br>
            I stood in my underpants on colt<br>
            Legs aware of all this growth both<br>
            <em>Animal, mineral – &amp; the splaying</em><br>
            <em>Of vegetable. </em>I could easily have<br>
            Mangled the process.</p>
             

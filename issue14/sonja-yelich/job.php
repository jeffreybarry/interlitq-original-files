</p>
<p><strong>8</strong></p>

<p>Apart from the constant hurricane <br>
Of lettuce there is the digging.  From<br>
Time to time there is the re-jigging<br>
Of autumn to winter &amp; the freezing<br>
Of dill from the high sunshine of <br>
Summer.  He dug holes to the middle <br>
Of the world while I stood in the zoology<br>
Of my bones waiting for one hug – <br>
Something to fall over my collar. <br>
My rangy frame. None came.</p>
<p>&nbsp;
  
</p>
<p><strong>9</strong></p>

<p>I remember him repairing a radio<br>
That was broke of its static with its<br>
Ditties &amp; munted batteries.  The dial <br>
The tuner the volume switch – all <br>
Under his stoic fingers &amp; me knocking<br>
A screw down to the garage floor by <br>
Mishap – the size of a half-ant &amp; the pitch <br>
Of his voice lost of bulletins concerts –<br>
&amp; the melancholy of his heart.<br>
A regular pain in the arse.</p>

            <p><strong>The difference between us</strong></p>
            
            <p>Up close, one mountain looks <br>
            to me much the same as another. <br>
            I see my foot slipping on ice, <br>
            my body hurtling backwards, over </p>
           
            <p>rocks, into the crevasse, the blue <br>
            walls luminous; deadly. <br>
            He who has climbed a few in his time <br>
            and survived just such a fall sees </p>
           
            <p>their particular shape and lines <br>
            of approach, as if they are women, <br>
            beckoning. These days his knees <br>
            would betray him, still he plots </p>
           
            <p>a route in his mind—each step <br>
            a move closer to conquest or death.</p>
            <p>&nbsp;</p>
            <p>&nbsp;</p>
           
            <p><a name="woman"></a> <strong>The woman who walks backwards</strong></p>
            
            <p>In my neighbourhood of bankers’ mansions<br>
            converted into halfway houses, and broken<br>
            windowed student flats, I’ve come to expect<br>
            odd subjects, the man who cries out piercingly <br>
            at dawn, the speedy walker with Tourettes, <br>
            the one who stalks the footpath for his next <br>
            cigarette, and now the woman who walks <br>
            backwards down the 200 steps, hoping <br>
            to meet herself going forwards; a way <br>
            to delay getting old, I’m told, but how <br>
            she risks splinters, bare hands gripping <br>
            the rail for dear life.</p>
            <p>&nbsp;</p>
            <p>&nbsp;</p>
            <p><a name="listening"></a><strong>Listening to my father read</strong></p>
          
            <p>As if he wanted to be the bearer of news <br>
            we couldn&rsquo;t read for ourselves, for years <br>
            my father&rsquo;s been in the habit of reading <br>
            newspaper articles aloud to anyone around</p>
            
            <p>usually my mother, sometimes going <br>
            as far as selecting me to listen, down <br>
            country over the phone, resisting <br>
            attempts at diversions; questions asking </p>
            
            <p>for his opinion or the delivery <br>
            of ours. We simply had to receive <br>
            the reported facts. Now, in his hospital <br>
            bed where he has been granted </p>
            
            <p>a reprieve from his death sentence<br>
            (bleeding on the brain in two<br>
            diagonally opposite places)<br>
            he picks up a women&rsquo;s magazine </p>
            
            <p>and reads aloud, cadence rising <br>
            and falling just as before, seemingly <br>
            making sense but not to us struggling <br>
            to decipher one recognisable word </p>
            
            <p>from his babble. &lsquo;Dad,&rsquo; I say, desperate <br>
            for connection, &lsquo;I&rsquo;m driving your car.&rsquo;<br>
            He raises his hands, makes a cry <br>
            of mock alarm and resumes reading</p>
           
            <p>and laughing to himself. I know then<br>
            there is little hope. It&rsquo;s as if he&rsquo;s clutching <br>
            the one last skill left. The car is lost to him <br>
            as we are—all he has is this strange </p>
            
            <p>language we cannot ignore, brought <br>
            back from wherever he&rsquo;s been.</p>
           <p>&nbsp;</p>
            <p>&nbsp;</p>
<p><a name="taking"></a><strong>Taking off</strong></p>

<p>In Italy during the war he went AWOL twice. <br>
Worth it, he said, to see the architecture and art<br>
his head turned permanently by the <em>Gates </em><br>
<em>of Paradise</em> at the Battistero in Florence.</p>

<p>Little wonder then, to discover he’d taken off<br>
from the rest home on a stolen walker dressed <br>
in his best—shoes, shirt, good pants and hat. <br>
In the end, a rush of blood to his head</p>

<p>felled him short. At first, I thought collapsing <br>
on the footpath lacked dignity, but with walking <br>
the only skill left to him, I realised he’d chosen <br>
this: to go not in bed, surrounded by strangers </p>

<p>but hurrying down the road to home and wife, <br>
the other great and true beauty in his life.<br>
<p>&nbsp;</p>
<p>&nbsp;</p>

<p><a name="on"></a><strong>On the way</strong></p>

<p>Sunday morning; driving my friend’s car<br>
on the way to visit my father who’s just <br>
died in hospital I stop at the pedestrian <br>
crossing; a Chinese man wearing a white </p>

<p>mask and white gloves steps gingerly<br>
onto the road, waving his arms as if <br>
practising tai-chi or warding off attackers. <br>
What sort of man masks his face </p>

<p>on a clear sunny day in Bayswater? <br>
What kind of woman notices such a man <br>
when her father has just died? And what <br>
sort of poet mentions her father </p>

<p>at all—as if to claim some emotional<br>
significance otherwise missing?</p>
<p>&nbsp;</p>
<p>&nbsp;</p>

<p><a name="unmediated"></a><strong>Unmediated</strong></p>

<p>In the middle of the night I get up <br>
for my night-time wee finding as usual <br>
these days my legs reluctant to give support. <br>
I reach for the bed base and the drawers </p>

<p>to lean on only to find the drawers gone <br>
along with everything else I have been <br>
relying on; my grandfather’s medicine <br>
cabinet to remind me I come from people </p>

<p>who worked with their hands, the paintings <br>
to denote a moving up the social scale, <br>
the photographs of my younger self naked, <br>
the clothes to conceal my older self</p>

<p>the makeup and jewellery to distract <br>
from the truth in the mirror. There is <br>
no mystery in this, they are all <br>
next door while we paint the walls </p>

<p>a calmer colour for sleep. <br>
Now in this room, inhabited <br>
only by the bed and us <br>
I realise it is all down to me.</p>
 

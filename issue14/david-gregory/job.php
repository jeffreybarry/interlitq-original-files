            <strong>Any of These</strong>
           
            <p>When you step through the Arrivals door<br>
            they are all waiting, they question your face<br>
            and you could provide the answer;<br>
            comfort the one who is crying,<br>
            kiss the glad-eyed wife, the old mother,<br>
            go with the taxi driver holding up a name.</p>
             
            <p>Flight has given you this.<br>
            After all of those hours as a seat number<br>
            you could enter any of these lives, <br>
            be who they wait for,<br>
            but kinder, astonish them.</p>
            
            <p>Be any other than who they said you were,<br>
            meaning your parents, teachers, reports,<br>
            the voices in your head, your bed and<br>
            mostly in the dark.</p>
            
            <p><em>I feel I don’t really know you,</em><br>
            was what she said.</p>
             
            <p>If that is a fault, you make it a rule.<br>
            If it is a rule, you measure yourself,<br>
            count the plum stones, <em>tinker, tailor, soldier, sailor…</em><br>
            go off, but only to your own war,<br>
            and come back through these doors.<br>
             
            <p>Your name, the baggage of your life?<br>
            Well, someone will carry them.</p>
<p>&nbsp;</p>
<p>&nbsp;</p>

<p><strong><a name="listening">Listening</a> at the Mouth of the Flower</strong></p>

<p>This for the wedding, christening, grave;<br>
all the colours of experience,<br>
all the voices of memory,<br>
almost seashell whispers<br>
from the daffodil’s throat,<br>
sounding the single, soft, spring note,<br>
releasing last year’s sunshine<br>
as a promise.</p>

<p>And the old voice of the lavender,<br>
the lips of the rose, tell you<br>
of grandma’s garden,<br>
the time of tall stalks,<br>
when blooms were chin level,<br>
the day’s deep reservoir of heat<br>
that your skin drank in,<br>
the voices from the house;<br>
cups, cutlery.</p>

<p>When you are called in,<br>
life is before you,<br>
spread like a summer tea.</p>


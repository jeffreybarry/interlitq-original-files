<p LANG="en-NZ"><a name="swimming"></a><strong>Swimming with Dolphins</strong></p>

<p LANG="en-NZ">&lsquo;<em>A natural experience&rsquo; </em><br>
<em>Akaroa tourist brochure</em></p>

<p LANG="en-NZ">Swimming, with dolphins.<br>
The comma curves<br>
And dives beneath the line,<br>
With a flick of its tail.<br>
It is neither here<br>
Nor there<br>
But important, still,<br>
Solid,<br>
A pause in the waves,<br>
A white ripple on the surface,<br>
A sleek dark body<br>
Beneath</p>

<p LANG="en-NZ">The sundried hills<br>
Like a paper cut out<br>
Two dimensional<br>
Under the blue sky.<br>
A backdrop,<br>
A prop in this &lsquo;experience&rsquo;.<br>
Ochre, dun,<br>
Or yellow brown,<br>
A dark sludge of khaki<br>
Where bush straddles the hollows.</p>

<p>This is a sentence.<br>
A string of words,<br>
Countless drops of water<br>
Running off a wetsuit,<br>
The click of cameras<br>
Recording syllables,<br>
Or blank stretches of water,<br>
Where the dolphins had been<br>
A moment before.</p>

<p>This is a theatre.<br>
Play, scene, act,<br>
Parts, words, scripts,<br>
A dialogue for mute and voices.</p>


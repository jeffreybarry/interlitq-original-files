            <p><strong>Driving Puccini to work</strong></p>
            
            <p>The Chinese princess approaches the intersection.<br>
            She chooses her moment with care.</p>
            
            <p>Morning mist freezes the windscreen – <br>
            Your glove is rimed with ice.</p>
            
            <p>You remember the boy who listened<br>
            To his parents’ records, in secret.</p>
            
            <p>The nuns said he sang like an angel, <br>
            Though he understood not a word.</p>
            
            <p>Cherry blossoms puff into spring,<br>
            Each pearled petal a pure note.</p>
           
            <p>Swelling voices melt the traffic.<br>
            The car dissolves, a well of sound.</p>

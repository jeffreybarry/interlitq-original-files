            <p>&lsquo;You could come with me,&rsquo; he says.</p>
            <p>Gannets are wheeling on the other side of the bay. </p>
            <p>&lsquo;No pressure though. Just a thought.&rsquo;</p>
            <p>The birds circle, and circle again, gleaming in the sun. Too far away for me to discern the gold near their eyes, I recognize their long and languid revolving, interspersed with occasional sudden perpendicular dives. Jizz, isn&rsquo;t that what it&rsquo;s called, the distinctive flight pattern of a bird which enables an ornithologist to identify it.</p>
            <p>&lsquo;Impossible. I&rsquo;d have to resign my job, the Principal wouldn&rsquo;t give me that sort of time off. And I mightn&rsquo;t ever get another one as good. Then there&rsquo;s my writing. And I might hate sailing out of sight of land. I might, come to that, come to hate you, shut up in a confined space for so long.&rsquo; I turn to look at him directly.</p>
            <p>&lsquo;Just a thought,&rsquo; he repeats, bunching the cushion against the capstan so that his head is raised and he can see me more easily. He eyes me, speculatively it seems, but says nothing more. Wash from a passing outboard causes the yacht to sway slightly and the anchor chain chafes with a small graunching sound. </p>
            <p>I return his gaze, then turn to look again at the gannets. Do humans also have recognizable flight patterns, and if so, what is mine? </p>
            <p align="center">* * * </p>
            <p>Three days later I&rsquo;m standing in a lunchtime queue waiting to order my sandwich and the thought returns. Is this sedate midday shuffle part of a human jizz? Within that, do I have my own flight pattern? The girl alongside has a series of tattooed butterflies fluttering down her body: a small blue one on the back of her neck, a green and orange one on her forearm, purple wings with vermillion spots encircled the backs of both calves and another of yellow and orange seems to be partly cocooned by her shoe. Cocooned – a clever metaphor. A bevy of butterflies, I decide, and am pleased with the consonance. What if there were one tattooed around her navel - a bevy of <em>Butterflitis Umbilicus</em>, and I smile at my wit – and then at the older woman behind the counter as I wait to order Thai chicken and green lettuce with a red chili dressing.</p>
            <p align="center">* * *</p>
            <p>So perhaps my style is talk to myself in inflated writers&rsquo; language and then to congratulate myself on the snips of linguistic cleverness I&rsquo;ve composed. It is helpful, this minor habit, it gets me through interminable queues, dull staff room meetings, commuting. Perhaps it also isolates me. What would happen if I asked the girl about her menagerie of butterflies. There, I&rsquo;m doing it again. Not say menagerie. Use ordinary language. Not ask questions, a writer looking for material. Say something like:</p>
            <p>&lsquo;I like your tattoos.&rsquo; There, I&rsquo;ve said it.</p>
            <p>&lsquo;Smoked chicken, hummus and cucumber,&rsquo; says the woman behind the counter handing the butterfly girl her sandwich. Butterfly girl looks at me sideways, fleetingly, and is quickly gone.</p>
            <p  align="center">* * * </p>
            <p>And then three more days pass and I am again on his yacht. As the boat laps at a soft ten knot northerly he asks:</p>
            <p>&lsquo;Thought any more about it?&rsquo;</p>
            <p>&lsquo;I&lsquo;m examining my jizz,&rsquo; I reply. </p>
            <p>I am sure that he, who will never have heard of jizz, imagines me to be reflecting on a new exercise or an article of clothing. </p>
            <p>I am not. I am thinking of the oyster catchers we saw yesterday. Both black, black all over. With their orange blades they called, &lsquo;Look-see! Look-see!&rsquo; to each other as we walked along the stones above the high tide line. One was near the water&rsquo;s edge and the other on a rocky exclamation poking from the sand. &lsquo;Look-see!&rsquo; they called to one another. Then, when we kept walking, they began to shrill, &lsquo;Look-at-me! Look-at-me!&rsquo; more and more urgently. &lsquo;Look-at-me!&rsquo; they called to us. The one from the rocks flew over our heads, and back again, crying it louder still, &lsquo;Look-at-me!&rsquo; He landed in front of us. &lsquo;Look-at-me!&rsquo; he shrieked through his orange pincers, then it became &lsquo;Come-catch-me. Come-catch-me.&rsquo; His feathers pitch, teasing us; his bill glowing, inciting us; I made a little rush toward him and he shuffled ahead, dragging a wing, &lsquo;come-catch-me,&rsquo; he taunted. I followed, he called, I ran forward in a spurt, he hunched and drew ahead. And so we went until with a grating cry he flew off and landed back on his outcrop of rock. Then he and his mate began again, &lsquo;Look-see! Look-see!&rsquo;</p>
            <p>I trudged back to where the oyster catcher began his ploy and there were the eggs. Two; the grey of the stones they lay among, blotched with darker shades, so camouflaged I might have stepped on them unknowing. </p>
            <p>It hasn&rsquo;t answered my question: should I chase after novelty, believing I will make a discovery? And if I make no discovery will I return to whence I came, and know the place for the first time? Or should I forgo excitement? Should I wait and hope and trust the answer will be available, if only I were to look about me? </p>
            <p  align="center">* * *</p>
            <p>&lsquo;So the gannets are here again,&rsquo; he says later when he sees me watching them once more, and I turn to look at him, wondering. Does the skipper of a yacht on passage differ to the one who takes me to fish round the harbour, to admire dolphins outside the Heads, to his bed.</p>
            <p>What would he be like miles from land or any other human company and what if there were a storm  - a big one – a once-in-a-hundred-years storm - and we are on the yacht and big seas swamped it and what if he fell and was unconscious and I had to sail the boat and work the radio and ask for help and then try and get sails up or down and then work out our position on the GPS and what if the boat was rolling and bucking all the time I was trying to do it. Or what if he got washed off. Or what if I did. The idea is frightening, but oh, such writing  material to be absorbed, and such a largess of time in which to write. </p>
            <p>And go on, say it. Say it. I specially like the thought of spending so much time with him. I like the way I can see his muscles move when he stows sails, or grinds the winch. I like those arms when he takes me to bed, strokes me, and, steadily, deliberately, then more urgently, makes me ache, arch, climax. Much more time for that too.</p>
            <p>I look once more at the birds. Again I can&rsquo;t see the yellow but their intense whiteness makes them gleam and flash in the sunlight. All that circling around and diving and coming up, sometimes with a fish and sometimes without one. Is that what he is doing – circling - and if I seem likely prey, will he plummet down and snatch me, so that I transported up by him, my doubts scattered away like drops of water? </p>
            <p>Yet he seems very casual about it. About me. He did say &lsquo;no pressure&rsquo;. </p>
            <p>But if I turn him down he might ask someone else. And that someone might be a more experienced sailor than me and maybe female and prettier – worse, sexier - and then he wouldn&rsquo;t ever make the offer again and I would be left only imagining situations and people and settings for my stories when I might have had first hand experience.  Not to mention the end of this romance, which is still new. The relationship is like a fledgling, I decide, trying to see if it can get off the ground and stay airborne amid the winds and air currents. Nice simile. </p>
            <p  align="center">* * * </p>
            <p>He drops the anchor and the yacht reverses back, tugging and swinging, creating tension against the chain, till he is sure it has dug in.</p>
            <p>&lsquo;You&rsquo;ll do it easily,&rsquo; he says, and I know he is talking about swimming the thirty metres to shore but I feel as if it were a subtle persuasion, as if he were saying &lsquo;see you can do this, you can do anything with me&rsquo;. And, of course, perhaps he is. </p>
            <p>On the beach we move small stones and each scoops out a depression in the sand. I examine the whorls on a whelk shell, then watch a weka as it pokes and forages, turning over limp seaweed and crackling pieces of dried kelp at the high tide line, tossing them as extravagantly as a matador&rsquo;s cape at an angry bull. I&rsquo;m not sure the image is quite right . . . like a woman searching the sale bin. . . like a child looking for his cricket ball. . . like a . . . </p>
            <p>&lsquo;I&rsquo;m only suggesting a trip,&rsquo; he says. &lsquo;Not the end of your life. You&rsquo;re not signing away your independence, or writing, or your rights to anything.&rsquo; </p>
            <p>But am I? My preoccupations - manuscripts, employer, cat, push and shove at each other, colliding with thoughts of high seas and gales, failed engines, floating hazards. These are thrust aside by stronger, enticing ones - of being his chosen companion, best mate and first mate, lover.</p>
            <p>If I go, I&rsquo;ll discover my jizz, and he&rsquo;ll reveal his. </p>
            <p>Out loud I say: &lsquo;Yes, I&rsquo;ll come.&rsquo;</p>

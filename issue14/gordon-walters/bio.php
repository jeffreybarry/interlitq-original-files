                  <p>Gordon Frederick Walters was born in Wellington on September 24th,  1919. He trained at Wellington Technical College (1939-40). In 1946 he  began his studies of Polynesian art, which led to a series of abstract  paintings. He visited Europe in 1950, where he was impressed by the  works of Piet Mondrian, Victor Vasarely and Auguste Herbin. He returned  to Wellington in 1953. Between 1956 and 1966 he evolved the distinctive  formal language of his mature style, in which elements from Maori  decorative art are synthesized with European geometric abstraction. Most  of the paintings he produced after 1964 are formally related through  the use of the same few distinctive elements, most notably the <em>koru</em> motif, a stem with a curving bulblike termination found in Maori <em>moko</em> (tattoo patterns) and <em>kowhaiwhai</em> (patterns painted on house rafters and canoe hulls). Walters  transformed the Maori prototype, which is hand-drawn and organic, into a  severe geometric form constructed with precise, ruled lines and  compass-drawn circular terminations, for example in <em>Painting No. 1</em> (1965; Auckland, C.A.G.). Walters  used ambiguities of readings between figure and ground as well as  optical effects such as dazzle and after-images to create perceptual  movement and space in two-dimensional, non-illusionistic paintings.  There are some affinities with the works of Giuseppe Capogrossi and  Bridget Riley. Walters moved to Christchurch in  1976. His work is represented in most public collections in New Zealand,  in the National Gallery, Canberra, and the Art Gallery of New South  Wales, Sydney. Gordon Walters died on November 5th, 1995.</p>

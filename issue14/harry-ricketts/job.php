           <p><strong>Quarantine Island, Dunedin</strong></p>
            <p> (<em>for Helen and Marc</em>)</p>
            
            <p> Love is and is not the point.<br>
             With this high wind filling your sails,<br>
             world turns strange and yet the same.</p>
             
            <p> Your craft must be delicate, also tough.<br>
             You head for open, unplumbed seas.<br>
             Love is and is not the point.</p>
             
            <p> These charts you’ve packed are full of blanks,<br>
             bluffs and sounds you must rename.<br>
             World turns strange and yet the same.</p>
            
            <p> Tempests, typhoons, the taniwha stowed away <br>
             in the hold: all can be weathered.<br>
             Love is and is not the point.</p>
            
            <p> There will be days of sudden calm,<br>
             nights when stars burn into your head.<br>
             World turns strange and yet the same.</p>
            
            <p> The unknown calls. The day is yours.<br>
             Hope and trust will take you far.<br>
             Love is and is not the point.<br>
             World turns strange and yet the same.</p>
            <p>&nbsp;</p>
            <p>&nbsp;
              
            </p>
            <p> <strong><a name="at">At the Getty</a></strong></p>
            <p> (<em>For Brian</em>)</p>
           
            <p> In the half-dark of the Getty,<br>
             I peer through glass at Books of Hours,<br>
             those late medieval bestsellers.</p>
            
            <p> Calendars, annunciations,<br>
             saints and sinners, devotional<br>
             aids, dim indecipherable texts.</p>
            
            <p> St Ursula is quite charming<br>
             in grisaille, flecked with gold. Saint Luke,<br>
             a serious beard, writes, fathoms deep;</p>
            
            <p> there’s an ox in the corridor.<br>
             And this is the naughty boys’ room.<br>
             A pair of foppish youths ride a goat,</p>
            
            <p> fingering its horns. David, harp<br>
             put aside, hand on bare chest, says<br>
             how sorry he is not only for </p>
            
            <p> bonking Bathsheba but, even worse,<br>
             for sending Uriah to the Front.<br>
             Two shapely bare bottoms frame his remorse.</p>
            
            <p> Then, in a corner, a stained glass<br>
             crucifixion, South Netherlands,<br>
             1490s. There’s a double </p>
            
            <p> skull beneath the cross. Mary stands<br>
             to the left, folded in, mouth turned <br>
             down. Behind her, a lemon-white sky, </p>
            
            <p> bushy trees, a turreted town.<br>
             But it’s the figure on the right<br>
             who stops me dead. The slightly curled</p>
            
            <p> brown hair, hollow look, mouth agape<br>
             could – this sounds mad, I know – be you.<br>
             Not the you I so fondly recall</p>
            
            <p> in some bar off the Piazza<br>
             Navona, wryly reflecting<br>
             on lost boys, and love gone wrong again,</p>
            
            <p> but the you I glimpsed one Christmas<br>
             Eve in St Paul’s-within-the walls,<br>
             swinging the censor, eyes wide shut.</p>

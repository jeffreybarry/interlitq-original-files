             <p><strong>One Day</strong></p>
            <p>He sits in the wide door to his father's house, <br>
            playing with toys, <br>
            crudely painted soldiers with gilded helms, <br>
            gleaming in the sun.</p>
             
            <p>His nurse tells him he is a prince, <br>
            will be a king some day, and a warrior; <br>
            his mother stoops, whispers, <br>
            tells him of his father who is king, <br>
            long lost, long sought, <br>
            longed for …</p>
             
            <p>One day, she murmurs, <br>
            lips soft against his sun-warmed hair,<br>
            one day—a world full of longing, <br>
            echoes of an endless sea, <br>
            all encompassing.</p>
             
            <p>After, he will always see her thus:<br>
            standing in the empty door <br>
            with her eyes turned outward, <br>
            hear the murmur reflected<br>
            in every lineament—<br>
            one day.</p>
             
            <p>It will cling to his shadow like the sea,<br>
            pace at his heels down every road <br>
            of the dream long held to, pursued <br>
            through the length of years<br>
            and breathed into his heart <br>
            with a brightness of sun motes, dancing<br>
            in that long ago doorway—<br>
            one day:</p>
            
            <p>One day.</p>

            <p>The Army museum was shut for the day, looking like an abandoned sandcastle when Simon shot past on his way to Turangi. He was racing the tide himself, winter&rsquo;s worst front roiling up the island behind him. It was raining now and six or eight big rigs, beached whales, were lined up in the pub&rsquo;s vast car park. </p>
            <p> The truckies were waiting it out, every reason for a night on the turps. </p>
            <p> Just north of Waiouru the road lifted Simon into an even bleaker world. Not quite six o&rsquo;clock and it was pitch-black out there. He punched his way across the bandwidth, turned up a weather warning through the static: &lsquo;. . . snow down to seven hundred metres and heavy falls expected tonight on the Central Plateau.&rsquo;  Only just in time then. So much the better if they closed the Desert Road behind him. </p>
            <p>Jesus, this weather. </p>
            <p>The wipers were close to useless, the heater struggled. His whole being was clenched like a fist around the wheel. Calm down Simon, focus on the road. Or you won&rsquo;t make it. </p>
            <p>He didn&rsquo;t just have to be at the fishing lodge by seven, he needed to show up for dinner in smart casuals, buffed and chirpy like it&rsquo;d been a great day out on the river whatever the weather. As long as he did, no one would connect him to what had happened in a Foxton Beach holiday home a couple of hours ago. </p>
            <p>&lsquo;My catch? It&rsquo;s in the smoker,&rsquo; he&rsquo;d say if they asked. </p>
            <p>The radio still stuttered. The Warratahs: &lsquo;Before this night is through&rsquo;, an old favourite. He was steady on 130 kph, nothing in the high beam but a million bullets of rain coming straight at him. He fiddled with the tuner. </p>
            <p>Another time he might have enjoyed the drive. But he couldn&rsquo;t stop picking over everything, checking for flaws. Drop the car in Taupo mid-morning. Pay cash for an InterCity bus ticket home. The dodgy licence he&rsquo;d burn later. The gun and silencer would go into the lake at a quiet lay-by. </p>
            <p>He squeezed more sound from the radio so he could hear the lyrics over the wipers, the drumming of the rain, the replay in his head of McCorquodale pleading. The bastard was gibbering before Simon squeezed the trigger. He needed McCorquodale out of his head. </p>
            <p>I killed him, and still I can&rsquo;t shift him. </p>
            <p>He wasn&rsquo;t sorry he made the prick wait to die. No price was high enough. All the poor suckers he robbed of their life&rsquo;s savings. Greedy no doubt, some of them – others just naïve, trusting. Everyone Jay McCorquodale ripped off would be a suspect: there were plenty. Simon hardly topped the list. And he&rsquo;d have his alibi.</p>
            <p>Jesus Simon, slow down. </p>
            <p>He&rsquo;d burned up the long straights. He was hitting the first fierce set of twists and turns. A vision played in his head – hurtling end-over-end into one of these sudden mysterious ravines – and switched off again as he fishtailed through the trickiest bits, shot into the clear. There&rsquo;d be no ice, it was too wet.</p>
            <p>&lsquo;Six-fifteen,&rsquo; said the radio and Bruce Springsteen was next on the playlist. &lsquo;Nebraska.&rsquo; Crime and punishment, straps across his chest, too close to the bone. The noise McCorquodale made as he died. Worse than the begging. He&rsquo;d always hear it, sleepless at three in the morning, whenever he thought about dying, that choking, sucking, gasping cough. </p>
            <p>Simon flew through the next winding dip in the road, braking into each bend and accelerating out, revs drowning the Boss&rsquo;s drawl. In spite of the heater his hands were frozen to the wheel. The adrenalin kept him from feeling any pain. </p>
            <p>When he hit the third set of hairpins he needed that surge in the blood. Reduce Speed. The big sign barely visible in the headlights, the driving rain turned to sleet and the wipers labouring. The snow must be close behind. Faster, though at the next twist in the road it said reduce speed now. okay, okay, but all the same he flung the car into the last bend, the devil&rsquo;s elbow where the bridge crossed a mountain creek and the sign screamed 25. The road kept bending, tighter and tighter till he was on full lock, till he was running out of road and out of luck, leaving the highway, airborne and rolling, replaying the everlasting instant from first to final pressure on the trigger. </p>
            <p><BR>
            </p>
            <p>He woke so cold he couldn&rsquo;t feel his feet. Seemed like he was upside down, wedged in, head rammed against the roof. There was pain from parts of him he could locate, his right arm and his neck and his head. But his brain was working. </p>
            <p>So was the radio.</p>
            <p> &lsquo;. . . it&rsquo;s seven fifty-five. News and weather at the top of the hour. That&rsquo;s almost it for our country music special tonight and we&rsquo;re signing off with a number from Wellington troubadours the Windy City Strugglers. This one&rsquo;s more blues than country, and according to the Met Service it could prove prophetic. It&rsquo;s called &lsquo;Snow on the Desert Road&rsquo;. </p>
            <p>Legs useless, left arm hurting too much to move, listening to Rick Bryant&rsquo;s aching voice plead &lsquo;Couldn&rsquo;t make it . . .&rsquo;. Ticking off the bits under control.  His right hand might obey him if the fingers weren&rsquo;t numb with cold. </p>
            <p>It wasn&rsquo;t just the radio he could hear. A slow, steady, dripping sound. His own blood, leaking from a wound he couldn&rsquo;t feel? Petrol, pooling, waiting for a spark? </p>
            <p>And the fan. Still rumbling, lazy now, battery fading, recycling air with less than no warmth in it. Though the glass had to be intact. Or he&rsquo;d be meat in the freezer by now. Even so his Swanndri was about as warm as a cotton T. There was a rug in the back but it might as well be in his centrally-heated room at the lodge. </p>
            <p>Jesus! His cell phone. In the bag velcroed to the dash, just about reach it with his right hand. </p>
            <p>The bag was upside down. Empty.</p>
            <p>He&rsquo;d be seen from the road, surely? Someone would stop, call emergency services.</p>
            <p>But when he tuned into the radio again, the disembodied voice trickling into his ice cave had news especially for him: &lsquo;. . . the road closures are unlikely to be reviewed until late tomorrow. They include State Highway 4 through National Park, and State Highway 1, the Desert Road.&rsquo;</p>

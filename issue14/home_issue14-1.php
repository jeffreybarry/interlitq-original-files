    <td colspan="2" valign="top" class="texto2">
    <p>As the President of <em>Interlitq</em>, I am delighted to welcome you to Issue 14, constituting the initial installment of our major feature "A New Zealand Literary Showcase", in which, over the course of the next three months, we will be publishing the work of more than one hundred leading New Zealand writers.</p>
    <p>And I hope you will return to visit us later this February to enjoy new work by a further 13 New Zealand writers, and then once again, towards the end of this month, to enjoy yet more new work by New Zealand authors, with additional contributions to "A New Zealand Literary Showcase" to be incorporated on a rolling basis throughout March and April. </p>
      <p>I would also like to take this opportunity to invite you to make a donation to <em>The International Literary Quarterly</em>, a not-for-profit corporation in the State of New York. Since its inception in November 2007, the review has published work by both Nobel Laureates and emerging writers, and in more than 90 different languages, in its quest to ensure that the best literature is made available to us all, wherever we may be in the world, and at the click of a finger. In view of the fact that access to <em>Interlitq</em> is free of charge, that the site is devoted solely to literature and does not carry advertising, and also that to date the review has not received funding from any grant-making organization, I believe that you will understand that donations such as these are the review's lifeblood. Thank you so much for helping to ensure that <em>Interlitq</em> will continue, as it has up until this moment, to work tirelessly in order to promote and nurture outstanding international literature, and thereby play its part in helping, through the power of this very literature, to break down those barriers that are still all-too-prevalent between us. </p>
      
              

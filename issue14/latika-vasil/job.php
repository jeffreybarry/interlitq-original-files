            <p>I shouldn&rsquo;t watch TV.  The world unravels on CNN.  Mugabe, wearing shades as dark as licorice, scowls at the camera. <em>Will the dictator fall?</em> The foreign correspondent, safari-suited, mic in hand, stands on the South African side of the border with Zimbabwe. <em>Will Zimbabwe collapse? Will Zambia send a ninja force to prop up Mugabe?  Will South Africa come to the rescue?</em> In the semi-darkness the unanswered questions wash over me like the bluish light bathing the room.  I am only watching because I don&rsquo;t want to go to bed yet.</p>
           
            <p>Later I pay for my late-night TV watching when I lie in bed squirming and contorting under the sheets trying to find that elusive comfortable position whereby sleep will come.  I&rsquo;ve been here before; the mad adrenaline-powered internal rantings; the sweaty beads of worry which I mentally finger, over and over. </p>
            
            <p>Tonight I ride the endless loop of anxiety of anticipating an early morning meeting that I will now be tired for because I can&rsquo;t sleep.  This keeps me awake.</p>
          
            <p>*</p>
           
            <p>My friend Neil gets insomnia too, but in a slightly different way.  I can&rsquo;t fall asleep and Neil can&rsquo;t stay asleep for more than a few hours.  Only Neil has had the problem for so long that we no longer talk about it in heavy tones as a <em>problem</em>.  We can now see the funny side – almost.  He has been to a doctor, been prescribed drugs, but they have too many side effects and besides there&rsquo;s only so long you can take them.  After a while the drugs stop working.  Neil likes permanent solutions.  He saw a psychotherapist for a short time but the endless answering of questions and probing were too difficult.  Better not to sleep, he decided.  &ldquo;If life gives you lemons then make lemonade&rdquo; he said.  Surprisingly, for a very intelligent person Neil often comes out with these annoying aphorisms.  Besides, I wasn&rsquo;t quite sure what he meant in the context of not being able to sleep but apparently this translated into Neil not trying to sleep, not fighting the insomnia, but instead writing for three hours every night when his eyes shot open at 3.00 am as they did every night.  Three hours writing was a good stint and the pages mounted.  The printer would wheezily spit out ten or so pages every morning at 6.00.  Then Neil would carry his throbbing head into the shower and get ready for the paid part of his work day.  He told me that the lack of sleep made him feel like he was permanently wearing a hard hat.</p>
            
            <p>*</p>
           
            <p>He showed me his novel.  Unfortunately it was incomprehensible.  The mad disjointed ravings of a sleep-deprived man.  Perhaps it was post-modernly brilliant, only I couldn&rsquo;t tell.  Neil sent it off to a few publishers but never heard back.  They didn&rsquo;t even return the manuscripts, which I thought rude.  Neil had not paid for the photocopying – surreptitiously doing it all at work – but that was beside the point.  And he had included stamped self-addressed envelopes, as requested.  After the silent rejections he had stuffed the novel into his filing cabinet in a folder called &lsquo;Discarded Novel&rsquo;, which I thought had a nice poetic ring.  Ever persistent, Neil was now working on a second novel; this one was to be science fiction he said. </p>
           
            <p>*</p>
           
            <p>Coffee was our saviour and our nemesis.  By necessity we would both drink cups of strong coffee all day to keep alert, then at night our caffeinated bodies would turn on us, the caffeine zinging through our systems, neutralising any urge to sleep.  Unlike Neil, however, I had periods of normal sleep and my coffee intake would return to saner levels – a latte in the morning and a long black in the afternoon.  Sometimes Neil and I would meet for coffee in a little hole-in-the-wall place on Cuba Street.  We would sit outside, disturbed now and then by one of the resident winos scrounging a cigarette.  Neil didn&rsquo;t smoke.  I did.  And I would never know what to do – hand over a cigarette and open the possibility of this being perceived as an invitation to linger, or refuse the request and risk an angry outburst – so many conflicting imperatives colliding in this one social encounter. </p>
            
            <p>Sometimes we simply sat together, silently sipping our coffees, eavesdropping on the conversations around us.  A girl, pale and delicately featured like a china doll but with several jarringly incongruous face studs, is telling a friend about her travel plans.  A strange itinerary, that stretches, apparently, from Turkey to Iceland.  I try to conjure up explanations for why someone would be including these two wildly disparate destinations in the one trip.  Was the doll-girl a drug mule?  My imagination fails me. </p>
            
            <p><strong>*</strong></p>
           
            <p>Sometimes Neil is in a loquacious mood.  He has wide-ranging interests and is currently reading a book on String Theory and attempting to explain it to me in great detail.  He talks about particles and quantum and gravity and I lose the thread of his explanation.  Still, the sensation of hearing these strange-sounding words and not having to process them, or respond, is relaxing in its own way.  We would often have these faux conversations and Neil was never upset when I suddenly cut into his stream with some totally unrelated comment – Neil, look at that woman&rsquo;s platform shoes.  Aren&rsquo;t they a hoot!  Neil, do you think it&rsquo;s going to rain tonight? – making it obvious that I had not been listening.  Neil is good that way.  He just likes to talk regardless of the quality of his audience.</p>
           
            <p>*</p>
           
            <p>At home, unable to sleep, I pull out my old star charts.  I first got interested in star-watching when Halley&rsquo;s Comet appeared several years ago.  I look up at the sky and think of the old Joni Mitchell song &ldquo;<em>We are stardust</em>. <em>Billion year old carbon</em>…&rdquo;  I spot the constellations familiar to me from the many hours spent at the planetarium.  At the last show there had only been four of us in the audience – me and Neil and two German tourists.  The tourists had fallen asleep as soon as the astronomer had switched on the night sky over us.  Neil later said it was a classic Pavlovian conditioning response.  Darkness triggers sleep.  I wondered what had happened to our conditioning.</p>
           
            <p>It is very late now and the summer sky is finally at its darkest.  There is Orion, the Hunter, easy to find by his distinctive three-star belt.  It all comes back to me – the shoulders formed by the reddish Betelgeuse and Bellatrix, the bright star Rigel is one of his legs.  I think of all the centuries of effort spent studying the stars and the patterns – knowledge, now considered by most to be esoteric and inconsequential.  We have other guides to navigate us now.  The stars make me feel small like an ant.  I look around the neighbourhood at all the darkened houses.  Lights and televisions finally extinguished.  Everyone is asleep.  Questions form and collide like electrons – I wonder what Neil&rsquo;s science fiction novel is about; I wonder how many people looked up at the stars tonight; and I wonder what time I will fall asleep.</p>
           

            <p><strong>The Aliquot Brothers</strong></p>
         
            <p>Boys in men's shirts, the Aliquot Brothers<br>
            have come to town. They are</p>
           
            <p>backing us into corners, mopping up<br>
            the fragments we leave behind.</p>
            
            <p>They are the perfect combination.<br>
            The redhead paints his toes. The honey blond</p>
           
            <p>streaks highlights through his hair.<br>
            They go café to café, dividing</p>
           
            <p>to rule, smearing tablecloths<br>
            with froth and melted cheese. (The rest of us</p>
            
            <p>confined to quarters, mumbling<br>
            over cold porridge and twice-strained tea.)</p>
           
            <p>No use complaining: they'll leave<br>
            when they're good and ready,</p>
           
            <p>with no remainder, nothing<br>
            but the hiss of their departure,</p>
           
            <p>the closing door that splits<br>
            this world from its neighbour.</p>
<p>&nbsp;
  
</p>
<p><strong><a name="return"></a>Return to Nussbaum Riegel</strong></p>

<p>This is a tent.<br>
This is another tent, next to the first tent.<br>
This is a bag full of urine.<br>
This is the vast inconceivable.</p>

<p>This is a rock.<br>
This is another rock.<br>
These are the deposits of a long-vanished glacier.<br>
The frigid wind, whistling over the frigid ice, passing over long<br>
generations of mummified seals making their stealthy way from the sea,<br>
has formed these rocks into the unearthly shapes we call &quot;ventifacts&quot;,<br>
photographs of which form the bulk of my presentation today.</p>

<p>This is me.<br>
This is Guido.<br>
This is Guido, Nails and Barry.<br>
Guido, Nails and Barry<br>
are men with whom I will always share a special<br>
incommunicable<br>
bond.</p>

<p>This is Alfred, Lord Tennyson.<br>
He wrote his famous poem &quot;Ulysses&quot; while visiting Antarctica<br>
on the first &quot;Artists in Antarctica&quot; programme<br>
with Bill Manhire, Chris Orsman and Nigel Brown.<br>
(This is Bill Manhire, Chris Orsman and Nigel Brown.)<br>
Alfred, Lord Tennyson inscribed his famous poem &quot;Ulysses&quot; on a cross<br>
placed on Observation Hill by the survivors of Scott's Polar Expedition of 1910-1912.<br>
To read it, you need a magnifying glass<br>
and an iron constitution.</p>

<p>This is the Polar Party.<br>
These are the Polar Party’s drinks and nibbles.<br>
The Polar Party went on till 5 a.m.,<br>
then made camp. Scott opened his diary,<br>
wishing, not for the first time,<br>
that he had brought a pen.</p>


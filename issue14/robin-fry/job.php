            <p><strong>Dreams of  You</strong></p>
            
            <p>Above the pool I grasp at shadows<br>
            like undergrowth</p>
            
            <p>they steady me for the descent<br>
            resisting the pull of gravity</p>
            
            <p>of the black water.</p>
            
            <p>Do not assume indifference.</p>
            
            <p>I wake to darkness but<br>
            the rising sun releases</p>
            
            <p>an incandescent<br>
            explosion </p>
           
            <p>the tears of ancient trees<br>
            locked in the earth for centuries</p>
          
            <p>in beads of amber</p>
           
            <p>warmed by your blood<br>
            melting<br>
            into your skin.</p>
<p>&nbsp;
  
</p>
<p><strong><a name="high"></a>The High Mysteries</strong></p>

<p><em>For Hazel whose pre-school  painting has a green wave</em></p>

<p>There is a wave of green<br>
that rolls off the loom of the earth.<br>
There is a song thrush<br>
to whom it is home. </p>

<p>There is a need to burrow down <br>
below the surface of things –<br>
rise with the sap<br>
connect with the spring.</p>

<p>*</p>
<p>Twin boys live near<br>
raised by a pretty mother<br>
in their far-off land –<br>
their childhood secrets</p>

<p>concealed <br>
behind dark eyes<br>
veiled<br>
by the moon lids of Asia.</p>

<p>*</p>
<p>Sometimes the earth<br>
yields up a treasure –<br>
yesterday a flute<br>
buried in the soil of Europe<br>
for nine thousand years.</p>

<p>We know a little<br>
that lies beneath the surfaces –</p>

<p>the dance of life<br>
that weaves the scarves of silver <br>
through the seas</p>

<p>the sounds and rhythms of an order<br>
fashioned in the mind of some god, <br>
older than creation,</p>

<p>solitary, terrifying, <br>
whose voice is in the thunder<br>
whose dance is in the storm.</p>
<p>&nbsp;
  
</p>
<p><strong><a name="time"></a>Time Traveller</strong></p>

<p>Lying in his cell a monk had a dream.<br>
He could tell it was a dream<br>
as his vision of the new Jerusalem <br>
hung upside down in a mirror.</p>

<p>Escaping a hard life full of terrors<br>
he caught the wind by its tail<br>
swung himself up into the sky.</p>

<p>He flew over smoking sea caves<br>
tidal pools studded with starfish<br>
reefs and skerries<br>
freshets and fountains.</p>

<p>Cadences of a <em>fado</em> rose with him –<br>
an ancient lamentation<br>
carried by wave and by blood<br>
from a far continent.</p>

<p>The monk landed in a garden<br>
watched a bouncing spider<br>
cross a birdbath<br>
on the water’s trampoline</p>

<p>listened to the dreams<br>
of a stone-coloured cat<br>
slumbering  by the head <br>
of a Buddha.</p>

<p>When a scholar closed the breviary<br>
the page where the monk lived<br>
returned to its own century<br>
folded into the darkness.</p>


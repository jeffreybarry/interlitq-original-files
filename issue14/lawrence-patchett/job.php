            <p><SPAN LANG="en-NZ">He could see them up ahead and would catch them. Two abreast, they were bunched on the footpath. As he drew near Mark saw how slowly they were running and realised he&rsquo;d found the wrong group—these were the Masters, not the B pack he usually ran with. But given his lateness and the stabbing, sad morning he&rsquo;d come from, he was grateful. He&rsquo;d run swiftly across town to find the B pack and was breathing heavily—here were the Masters, and he would run with them. </SPAN></p>
            <p><SPAN LANG="en-NZ">As he ranged up the rearmost runner turned and Mark said hi, then watched the man not recognise him and turn back to his running. </SPAN></p>
            <p><SPAN LANG="en-NZ">They were heading up Holloway Road. The pace was very slow. Mark felt his heart rate come down. They were winding up through the last of the houses. </SPAN></p>
            <p><SPAN LANG="en-NZ">It was Lucy that had made him late—a fight with Lucy. It was always Lucy. </SPAN></p>
            <p><SPAN LANG="en-NZ">The road came to an end and they single filed through a bike-stop, then went into the bush. The track and the trees were damp from the rain. The runners strung out along the track, winding round the side of the hill and up it slowly. Not saying much, Mark ran in the middle. He&rsquo;d not run with the Masters before and tried to run accordingly. He placed his feet carefully, held ferns back for the runner behind. He heard talk passing to and fro at other points in the line. Mostly it was the people up the front or right down the back who did the talking. They were the characters—he&rsquo;d seen them draw raffles at the clubrooms and make speeches. </SPAN></p>
            <p><SPAN LANG="en-NZ">For twenty minutes he ran quietly. The bush was damply close around him. </SPAN></p>
            <p><SPAN LANG="en-NZ">He&rsquo;d left </SPAN><SPAN LANG="en-NZ">Lucy crying in the house—a shaking, sobbing crying with her forearm against a wall that, as he stood at the door, ripped something right out of him. He and Lucy were so tangled. It made everything painful. They had businesses together—a quarterly about the wilderness, which didn&rsquo;t make money, and a design firm—and this meant that their breaking-up, he knew, and an end to the long, long sad they&rsquo;d wreaked on each other, would never really come. </SPAN></p>
            <p><SPAN LANG="en-NZ">T</SPAN><SPAN LANG="en-NZ">he hill steepened and the line of runners broke out of the bush and saw the hill opposite, the mist there catching on trees, and in the valley below the furthest reach of a damp suburb. They ran on and the track steepened again. Mark had a brief conversation with the runner ahead of him about the course and how he&rsquo;d never run it before and how he loved that about harriers—discovering new places to run—and then noticed that the other runner was not replying and pulled his head in. The pack stopped to let stragglers catch up, then ran on again. </SPAN></p>
            <p><SPAN LANG="en-NZ">Before the crying Lucy had slammed out to the porch and kicked in the front spokes of Mark&rsquo;s bicycle, then ripped a light off and flung it down the drive, where it clattered and broke open. Before that, for three days talk had stabbed between them while a client Mark was dealing with made last-minute changes repeatedly until Mark called her &lsquo;your stupid-bitch client, anyway&rsquo; in a studio-to-storeroom fight that left Lucy staring at him in a way that scared him. Throughout the last part of the week he&rsquo;d felt strung out as if over-caffeinated and, at the same time, gutted out, hollow. His work lurched and failed. It always did when he fought with Lucy. He loved her so profoundly. </SPAN></p>
            <p><SPAN LANG="en-NZ">There was an up-and-down in the track, then a fork where the pack stopped for a discussion about which runners would carry on and who would take the short route home. The early-homers bantered those who wanted to carry on. Retirement had given Bob too much energy, they said; they felt sorry for Frances, poor thing. He should see a doctor, they said. </SPAN></p>
            <p><SPAN LANG="en-NZ">Mark stayed with the smaller pack and they ran on, winding further up and around the hill. After ten minutes the frontrunners stopped for a kaka they&rsquo;d seen in the middle-story of trees; Mark savoured the cool air as a loud pair of runners at the back approached, and the bird flashed away. But it was the stuff of legend, anyway, the pair said. There&rsquo;d never been a kaka—Bob&rsquo;s old mind had played tricks on him again. </SPAN></p>
            <p><SPAN LANG="en-NZ">The track was damper here and splashy, and Mark was careful with his footing before it opened onto a stretch that undulated. The two runners in the line ahead of him began talking about a marathon in a notorious year when many runners had got hypothermia. Both had stories. It was obvious to Mark from the way they ran that these two had run many thousands of kilometres. He listened—at one point one of them used the word &lsquo;fillip&rsquo; in a sentence and he searched out a memory for some minutes until he placed it; Lucy had claimed it once in a game of scrabble, years ago. Najet and David had been there. They joined a track that was wider and ran three abreast, then crossed a stretch of grass and re-entered the bush in single file. The runners sped up on the decline and Mark moved more freely, then they bunched again for a climb, and he remembered the wine of that night, brought by Najet from her hometown in France, where it was available only at certain times of the year. It had made them all pleasantly heavy. He remembered Lucy sitting on the floor to play scrabble and leaning back on her hand to tease Najet about something. He should contact them again, he thought. David worked in advertising and knew about clients who went mental. But maybe Najet was already in touch with Lucy. He didn&rsquo;t know. </SPAN></p>
            <p><SPAN LANG="en-NZ">A long dog-leg of track brought the line of runners back on itself and then to a further hill and as they wound up through the dark trees he faced the hill to climb it, his shoes moving over clay and damp leaves. Lucy would&rsquo;ve left for the day. She&rsquo;d probably stay at her friend&rsquo;s that evening. He would send a text late tonight and she&rsquo;d return tomorrow and they&rsquo;d talk at the table over hot drinks, warily. He felt the city at his back, the streets he&rsquo;d run through, and from the distance they&rsquo;d come and the way they were talking he judged that half an hour of running was still ahead of him, at this pace and with these people, and he was grateful. Ferns went past at his sides, brushing him with moisture. He felt the cold and the delicious beginnings of fatigue and he thought of his other harrier friends, out running now over other parts of the city, and of Lucy. </SPAN></p>

<p><strong><a name="distant">Distant</a> Pain</strong></p>
On a long distance bus <br>
in a far off country <br>
a stranger once confided <br>
that his wife had left him for his father. <br>
One touch of the old man’s wand <br>
and she turned into the wicked <br>
bloody stepmother; it’s not decent, <br>
he complained, an ex-wife <br>
should remain an ex-wife forever.</p>

<p>Today on the train<br>
a man is about to expose <br>
his burns from a factory accident.<br>
He rolls up his sleeves,<br>
turns his arms to the window <br>
so the stretched, irregular patches<br>
of alien skin catch the light,<br>
tells me he’s been in pain so long <br>
that sometimes he no longer feels it,<br>
wonders if the pain has reduced<br>
<SPAN LANG="en-GB">or if he’s just getting used to it.</SPAN></p>

<p><SPAN LANG="en-GB">I want to know more about pain </SPAN><br>
that can’t be felt, and sure enough <br>
<SPAN LANG="en-GB">he’s explaining now (his words </SPAN><br>
<SPAN LANG="en-GB">merge with the sound of the train</SPAN><br>
<SPAN LANG="en-GB"><em>distant pain, distant pain</em></SPAN><SPAN LANG="en-GB">)</SPAN><br>

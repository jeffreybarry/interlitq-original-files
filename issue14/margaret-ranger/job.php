            <p><b>The Old City</b><br>
            (Old Sukothai, Thailand)</p>
            
            <p>Late monsoon thunder particles<br>
            condense the air, <br>
            night hangs in the balance,<br>
            the tin roof sings for rain.<br>
            And here it comes - asthma, <br>
            the straw you breathe through<br>
            when you can’t breathe,<br>
            the toad that struggles<br>
            out of the swamp of your lungs,<br>
            the screen door rattling in the wind.<br>
            Quick. Fix the latch. Fetch a nurse. <br>
            Listen. Is that the steady hiss<br>
            of a hospital nebuliser?<br>
            <em>Mai mii,</em> no have, no have.<br>
            In the bamboo rocker<br>
            on the dark veranda<br>
            Doi chews durian sweets. <br>
            The old city drives a hard bargain.<br>
            You hear the siren<br>
            of your other life wailing.<br>
            You see the future<br>
            without agreeing to it.</p>

            <p>Through the angry ocean, wind-blasted surge,<br>
            I have gone my journey, horrific passage.</p>
            
            <p>Now at length to this shore I have come,<br>
            To this green, afforested shore of long presage, <br>
            To a land, after much labour, level and calm.</p>
            
            <p>Hither I came, whether for warfare or in peace<br>
            Knowing not well, in accordance with a madcap scheme,<br>
            Conceived by another man to a dark purpose.</p>
           
            <p>Among a strange people, in a strange city I tread,<br>
            Doubtful how to act and what might come to pass,<br>
            But assuming a mask of courtesy or trade.</p>
            
            <p>Lady, what do you see, you in whose gaze<br>
            My hesitant appearance so well is tried?<br>
            Do you perceive my features under this disguise?</p>
            <p><BR>
            </p>
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; II</p>
            <p>Medea, lovely wench, led on by her gaze,<br>
            Through her father’s house to Jason’s chamber goes.</p>
            
            <p>There she finds Jason sleepless but in bed,<br>
            Cap-a-pe acoutred all in martial guise:<br>
            Armour on legs and limbs, armour on head and body.</p>
            
            <p>Upon that dragon he turns his restless mind,<br>
            That on the morrow he is set down to combat,<br>
            Not of his own choosing, but by regal command.</p>
            
            <p>Upon that treasure also he turns his thought<br>
            Whose value by no means is to be determined, <br>
            Which he came to win and will not leave without.</p>
            
            <p>But when fair Medea, lovely in every regard,<br>
            He saw standing there, need you wonder that<br>
            Armour and thought shortly lay in the discard?</p>
            <p><BR>
            </p>
           
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; III</p>
            <p>I come, led on by the dream of a supposed visage,<br>
            Whose fulfillment not at all did I envisage.</p>
            
            <p>A greater treasure to be brought into arms’ reach<br>
            Either by negotiation, asking or siege,<br>
            That is than a golden fleece in which a king is rich.</p>
            
            <p>A fiercer dragon, a serpent of more horrid size,<br>
            Had not prevented me else miserable wretch<br>
            From such a possession as this which I now seize.</p>
            
            <p>O dream, o vision, o sight remotely glimpsed<br>
            While yet I navigated oceans, stormy seas,<br>
            You led me on by fitful and faint gleams.</p>
           
            <p>Woman, such potency is yours, thus to bewitch<br>
            With lovely face, bosom, and lips, and limbs,<br>
            The dream alone sustained me through the long watch.</p>

            <p>In the death that comes from the loss of heart I live,<br>
            Sustained by despair itself and your love.</p>
            
            <p>From this source do I derive the hardihood,<br>
            The strength of heart and fortitude required to cleave<br>
            The dragon through and through, to sever body and head.</p>
           
            <p>As for that treasure hoard that the dragon guarded,<br>

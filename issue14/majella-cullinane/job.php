            <p><strong>Morning Prayer</strong></p>
            
            <p><em>[after Tchaikovsky] </em></p>
           
            <p>Half-way through your journey you are called out. <br>
            From the dimmed room of your making, we watch </p>
           
            <p>you moving like a corridor of shadows across a sea, <br>
            or the cool fingers of autumn stroking a tree. </p>
           
            <p>Your small hand flexes and tightens, your spine <br>
            curves against me like a horseshoe, a strung bow. </p>
            
            <p>The brazen scarlet of a gum tree fades on the hill<br>
            and like the season, it is still early days for you and me; </p>
            
            <p>for my bones to soften, my body to swell. Wait<br>
            for me in the undertow of waves. It is there </p>
            
            <p>I will catch you my little boy, and when <br>
            you emerge we can explore the novelties of light.</p>

<p><a name="insomnia"></a><strong>Insomnia</strong></p>

<p>You are there again, <br>
cast amid the flotsam and tangle of a wave. <br>
In the half-pitch, pitch darkness of the shore, <br>
the sea is tinged and flecked with cloud and rain. </p>

<p>The Monarchs on your breastbone, <br>
awake, awake and take to the air. You hear <br>
their wings through the tip-top tumble of sand, <br>
and as if to keep dawn forever at arms length </p>

the night moves inside the house again. <br>
Shadows wait outside the door, <br>
and dreams rattle against windowpanes,<br>
they want in, want in. Inside the room, </p>

<p>your breaths are like a crumpled sheet, folded, <br>
or the hushed silence heard through a seashell <br>
washed up on a beach. Open your mouth <br>
taste the sand, the sea salt, the silence</p>

<p>loosened inside your dreams, the wrapped<br>
sheets of your longing softening your skin. <br>
When  blackbird wings flicker beneath <br>
your eyelids, you'll know you're awake.</p>


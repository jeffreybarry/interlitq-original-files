            <p><strong>Hawk</strong></p>
            
            <p>The morning sky breaks with flight.<br>
            A large plane appears. Banking,<br>
            a hawk hovers over pohutukawa<br>
            and kauri, fuscous like wood<br>
            or sand at twilight. Bleak<br>
            crescent moon, hawk turns, falls.<br>
            Sparrows and fantails disperse like rain.</p>
           
            <p>Later, a pair of hawks sky-dance overhead.<br>
            The sight of them so close, courting<br>
            the air, is such rare pleasure I sway. </p>
            <p>&nbsp;
            </p>

<p><strong><a name="clavicle">Clavicle</a> </strong></p>

<p>A bone<br>
is never just a bone.<br>
This one is a memory,<br>
a storyteller. </p>

<p>It spins its yarn,<br>
little Rumpelstiltskin,<br>
into scapula, humerus, radius,<br>
ulna and metacarpus.</p>

<p>Its skeleton<br>
recounts your body’s<br>
assembly and breakage. </p>

<p>A stormy night;<br>
a bed; a fall:<br>
it’s a nursery rhyme,<br>
it’s true. </p>

<p>The soft landing<br>
that forgot to catch you;<br>
wooden floorboards;<br>
tears during x-ray;<br>
fibres that knit as delicately<br>
as granny glove-making: <br>
these are another story.</p>

<p>When you’re grown,<br>
I’ll reconstruct them<br>
into a fable. It’ll fix<br>
a smile to your face. <br>

<p>Of things unspoken:<br>
how I slumbered<br>
as you tumbled; <br>
how blame calcifies;<br>
how a mother’s guilt <br>
is the only thing<br>
that fails to mend –<br>
these I’ll suffer alone. </p>
<p>&nbsp;</p>
<p>&nbsp;</p>

<p><strong><a name="ballad">The</a> Ballad of the Lonely Masturbator’s Wife</strong></p>
<p><em>after Anne Sexton</em></p>

<p>Quaking<br>
like rockets<br>
about to launch,<br>
the bed and he<br>
are wedded;<br>
though aching to shrill,<br>
I must keep still.</p>

<p>Mustn’t let on<br>
I feel his earth shake.<br>
Mustn’t wonder why<br>
he chooses another body<br>
(his own) over mine; nor why<br>
I allow him his fill.<br>
I must keep still. </p>

<p>And so, we both worship<br>
onanism, and are joined<br>
by this almost-adultery.<br>
The dark,<br>
into which he opens up<br>
and I shun, tests the will.<br>
I must keep still. </p>

<p>But fume away<br>
secretly, an undiscovered fire<br>
patient for its spark,<br>
when only silence<br>
knows my answers or how<br>
broken and alone I am until<br>
I stem my need to keep still. </p>
<p>&nbsp;</p>
<p>&nbsp;</p>
<p><strong><a name="my">My</a> Sister Writes Poetry</strong></p>

<p>Only when I’m absent,<br>
the ghosted reflection<br>
of home movies and photograph skin<br>
does my sister seek her muse.</p>

<p>Her words fill the earth,<br>
the seasons, climates, bones<br>
I’ve said farewell to.</p>

<p>And as I embrace being<br>
an epitaph of my own country,<br>
a strange and distant voice<br>
at the end of our telephone line,<br>
she grows intimate,<br>
adjectives, nouns and verbs her new lifeblood.</p>

<p>And as I embrace living<br>
my life again, <br>
images, similes, metaphors,<br>
landscapes darker and emptier than the moon,<br>
free verse, rhymes, odes,<br>
villanelles, epics and elegies<br>
become my sister’s ongoing comfort.</p>

<p>And when she wants to summon my return,<br>
she writes out the religion of our past.</p>

<p>And when she wishes to forget me,<br>
she writes of the birth of her new-born.</p>

<p>And then, in ugly joy,<br>
in rage and remorse and guilt,<br>
she rips her reddened words<br>
into a thousand tatters,<br>
petals each that scatter the air,<br>
their delicacy and perfume<br>
too rare for a faraway eye such as mine. </p>

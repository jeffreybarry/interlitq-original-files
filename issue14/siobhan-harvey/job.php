<p><strong><a name="tension">Tension</a> at the Airport</strong></p>

<p><em>The world is all gates, all opportunities, </em><br>
<em>strings of tension waiting to be struck</em> - Emerson</p>

<p>We sit in the Sky-deck playing <em>Snap</em>.<br>
Out over huddled hills, aircraft are<br>
specks peaking between curtains of cloud.</p>

<p>Connected to invisible wire, each plane<br>
dances in the air, God’s puppetry<br>
guiding it towards the runway.</p>

<p>There, heat rises off asphalt like ripples<br>
on cobalt water or the riverstone grey<br>
dash of wrybills startled by approaching jets. </p>

<p>Close to us, the departure hall is a movie-set<br>
of fraught farewells, tears arriving<br>
(like planes) in apparent synchronicity.</p>

<p>The friction the airport bears, its miracle<br>
of cantilevered steel and glass, strains<br>
under each goodbye, take-off and landing.</p>

<p>While we continue to shuffle,<br>
gripped by what we see, how we deal <br>
and our <em>Snap</em> <em>Snap!</em> <em>Snap!!</em></p>

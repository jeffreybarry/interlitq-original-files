               <p>The magic sounds you call me with<br>
               
               are molecules of soul and bits of my skin,</p>
              
               <p>not quotidians to be navigated yet again<br>
              
               on two overworked tightropes</p>
              
               <p>stretching to infinity. In the space between<br>
              
               we float, weightless, holding hands</p>
              
               <p>the Friday candles burn, joyful and steady.</p>

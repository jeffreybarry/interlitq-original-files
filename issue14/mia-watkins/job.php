            <p> <em>. . .</em> <em>through the water . . . through the water . . . we are alone. The canals are like the grey scarf I  wear around my head and torso; enveloping and possessive. Each lock on the canal locks us in and each lock locks us out. We must push and pull and strain with all our strength to open and close the floodgates that mark our journey. Never for a day are we in one place and always in this little boat we are elsewhere, in our dreams and fancies, encased in our concerns. I can feel them thick and gluey around me, emanating from each person, even the children; a grimey sludge.</em></p>
            <p> The women are swift and nimble rats, fast and dainty movers with claw hands, rapid heart palpitations, noses always in the air, smelling cheese. There is an endless supply: Bergundy, Cancoillotte, Bourgogne. They are named after the provinces and eaten like the land, cut up into little sections, squares and triangles, dry, barren and aged. It is destroyed. They are destroyer women, femme fatales. They eye it up; suddenly it's gone.</p>
            <p> The old man thinks too much about his age. He is a grizzly bear and a tiny mouse. He towers over everybody with his mammoth presence and determination. He puts up a fight to stay young and fit, agile and strong, but he is weak and frail and frightened. He attacks the topless day at 5am every morning with fervour and fists as if it is a canopy slowly sinking over him. It is a tiny enclosure, suffocating him. Life is suffocating him now. He is never comfortable. He is a mouse in a cave and a bear in a hole.</p>
            <p> . . . <em>drifting through the water, we are alone. Our eyes move gently like sand shifting under the current. I must dilate and contract and strain to get mine into focus. Drawn softly by a breeze, a passing tree, the wafer petals of poppy flowers departing their stems to make half moon undulations, the curvet of a side glance . . . wherever they are fixed they fly off again to join another procession, to become fixated on another, and another . . .</em></p>
            <p> Gernot says Rarotonga will be the first island in the Pacific Ocean to go under when sea levels rise one more meter. He should know, he works for the United Nation's climate control sector; so does his friend Dieter who has been driving through Bergundy all day long trying to locate the boat. A heron is sighted, a symbol of good luck in Germany apparently, showing there must still be some fish in the rivers and canals, so the environment must not be in <em>such</em> a bad way, but swimming is not advised. 'God I'm so hot,' says Wander, 'I'm burning up.'</p>
            <p> That night there is a most spectacular thunderstorm. Wander sees it all as she sleeps at the top of the boat where there are no curtains. The people sleeping below miss out. The sky crackles and shakes like the skies in cheap reproductions of famous art works; layers of colour overlap and clash creating hologram clouds, hologram horizons, psychedelic landscapes. Leaf, branch and hill silhouettes are punched into the sky and the insides of Wander's eyelids. The boat rocks and the rain is heavy. Wander thinks about the end of the world, and how pleasant it feels, how agreeable, how satisfactory. <em>Die World</em>, she thinks, spitefully. <em>Die World to show them what they are doing, that they are killing you. Take revenge! Die an angry, spiteful death, make them feel sorry, make them see. Strike them down with lightening and drown them in their own excretions, in the most grotesque and fully realised images of themselves! </em>Wander and the world are angry together.</p>
            <p> Wander visits a ninth century catherdral at Vezalay that holds the remains of Mary Magdalene. She can't see the remains because they are locked up. They are locked inside a golden box behind layered glass inside a small cave inside a bigger cave underneath the cathedral. It must hold her ashes, Wander thinks, or an essence, hair, nails, jewellery, clothes? 'Mary Magdalene, the prostitute who bathed Jesus' feet,' she intones, and thinks . . .</p>
            <p> <em>I want to bathe my feet in your ashes Mary. My feet are dirty and damp and stale. They are rotting. Let me bathe them in your mysterious remains. I want to stick my feet in the golden box. Then I want to walk on water, like Jesus did. I want to walk away from this boat. I cannot swim. I want to walk away.</em></p>
            <p> <BR>
            </p>
            <p> Later Wander sleeps and dreams. She is sailing through a flooded city. Sky and water mirror each other and often exchange places. She enters some buildings and is engulfed by whispering voices. One is hers:</p>
            <p> <em>I have never been afraid of sinking in this little ship because water could never engulf me like these voices do. The voices are familiar but I do not know whose they are, or whose this one is. I see faces clear and unabashed. They do not move, they are merely carcasses, shells, like read books. I travel through buildings and houses, along corridors and staircases and through many many doors but no chimneys. I am not rising.</em> </p>
            <p> <em>The children have been observing me, spying on me. They say I am sleeping with my eyes open. Perhaps I am dead, but I can feel my feet, they are tingling skipping dancing like moths against a window; departing like moths towards a flame. They are the cadence that ends a song, sound, movement and they are the thundering drum roll that precipitates a beginning.</em></p>
            <p> <em>I look really funny, the children say. The parents are in the top cabin drinking their Champagne from Champagne. 'Top up?' inquires Gernot. Tabea is breastfeeding her baby dolly, imitating the lady she saw in a painting at the cathedral. </em></p>
            <p> <em>Pedro kisses me on the elbow. He has also kissed my feet before. He is funny like that and I don't like it much, but he is just a boy. He says I am the Queen so he <strong>has</strong> to kiss my feet  . . .  wander . . . my feet  . . . Wander . . . my feet . . . Wander . . . feet . . .  Wander . . . Wander! . . . <strong>Wander!</strong></em></p>
            <p> Upon waking Wander felt as if she had suddenly been sprouted into a nursery with only one tiny window providing essential light, a tiny star far far away. Then came the crying, not Wander's crying, but the crying of the world, an incessant harangue, and the pushing and the wheezing and the racing congestion. Half conscious, she felt sweaty desperate limbs of seething stalks shoving against her, suffocating her with monstrously striving tubes of pulp and sap. Each time she was surpassed she shivered alone in terrifying darkness, and the distant star gradually became a cruel demagogue sardonically enunciating its cruel criteria to the crowd. </p>
            <p ALIGN="CENTER"> *</p>
            <p> That evening the boat came to a halt. It simply would not budge. They waited. Dieter phoned for an engineer. The women laid out as usual wine and cheese, bread, foie gras, olives and rich salami. Mosquitoes joined the supper. Wander looked out the window onto a completely still landscape. Nothing caught her eye, there was nothing to capture her interest or feed her imagination. There was no wind, the sky was clouded over, and the water was completely flat. The edges of the window became the borders of an arrogantly framed photograph. <em>Are we there yet? How long, how long?</em></p>

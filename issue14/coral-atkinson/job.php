            <p><em>(New Zealand late 19</em><em>th</em><em> century)</em></p>
            
            <p>When Sam Turner had finished dinner he went back out on the farm with his axe and saw.  His wife Kathleen scraped the leftovers into the hens&rsquo; bucket, washed the dishes and stood them to drain in the rack; that done she took off the sacking apron she wore in the morning, put on her white cotton one, and sat in the chair by the range to say the rosary.</p>
            
            <em>Hail Mary, Full of Grace</em>
           
            <p> The rosary beads had come in a stiff brown paper packet which the priest handed her after mass on that last Sunday in Ballydownie. Inside the envelope, along with the letter of recommendation saying she was of good character, were the holy beads. They were pearly green marble, the colour of clouds and lichen and the watery light of home. </p>
            
            <p>Home.</p>
           
            <p>The shed alongside the cottage needed a touch of whitewash and grey stones were pushing through the pale paint, while down the lane the O&rsquo;Riordans&rsquo; dog was barking. Over the top of the wall the three oak trees were coming into leaf; swallows flew about the yard slicing the thick spring air. Kathleen had a basket on her arm and the unravelling sticks of the broken handle tickled her skin as she walked. There was a speckled egg lying in a clump of dandelions. She bent to pick it up; a tawny feather was stuck against the shell.</p>
           
            <em>The Lord is with thee</em>
           
            <p> The beads smelt of the brown paper and of Ballydownie.</p>
            
            <p> There were wheels coming; big wheels like the loops of cotton when she sewed and the knot was too small and the stitch slipped through. The noise of the wheels was here in her New Zealand kitchen among the cups hanging from the shelf and the plates in the draining rack and the mousetrap in the drawer in the table. What mouse could get in the drawer or get out?  The mousetrap rattled when the drawer opened. </p>
           
            <p>Please, please, Kathleen thought, let the wheels be coming here; let it be a visitor, a woman. Someone to talk to; someone, anyone, even Mrs Neill from up the road. The Neills were Presbyterians from County Antrim; people who talked of &lsquo;the Elect&rsquo; and  &lsquo;the Word of God&rsquo;. The Neills knew who would line up for the march into Heaven and who would go to the other place. They had no truck with priests or with Rome. </p>
            
            <p><em>Holy Mary, Mother of God, </em><br>
            <em>You&rsquo;re a Mickey and I&rsquo;m a Prod.</em></p>
            
            <p> &lsquo;If you need help, you know where I am,&rsquo; Mrs Neill said briskly when Kathleen walked over the first week she was at the farm. Mrs Neill had given her a cup of tea and a scone. There had been no return visit, no invitation to come again. Wasn&rsquo;t surprising really. </p>
           
            <p>The sound of the wheels was fainter.  They must be beyond the turnoff; soon they&rsquo;d be gone. They weren&rsquo;t coming. Of course they weren&rsquo;t coming, whoever did come other than an occasional swagger? </p>
           
            <p>The swaggers smelt of grass mowing and old sweat; often they dragged a leg or had an arm that stuck out at an angle, bits of bodies that stayed in one place while the man was in another. The last swagger had no teeth and made noises of ducks in mud when he drank his tea. </p>
            
            <p>&lsquo;Come here, my dear,&rsquo; the man said to Kathleen grasping at his trousers. &lsquo;Come here.&rsquo; </p>
            
            <p>Saliva ran over the stubble of the swagger&rsquo;s chin like water among the raupo stalks when the creek was in flood. When the swagger moved around the table towards her Kathleen ran outside and locked herself in the privy, sliding the big bolt across. She stayed there until Turner came home. The swagger had gone by then.  Her husband called her a fool.</p>
           
            <em> Mother of God, pray for us sinners now, and at the hour of death</em>
            <p>Kathleen and Dorothy Malone had been crossing the bridge in Ballydownie.  Long ago a priest had been hanged on that bridge, during the time of death and rebellion. There was a sign marking the exact place where the gibbet had stood. </p>
            
            <p>Dorothy said, &lsquo;Do you ever think, Kate, now that your mother&rsquo;s passed away, it&rsquo;s time we left Ireland. All the fellows have gone off to England or the States and there&rsquo;s nothing for us here. Sure we&rsquo;ll end up wizened old maids if we stay.&rsquo;</p>
            
            <p>&lsquo;Leave; you mean go to another country for good?&rsquo; said Kathleen watching the water move under the bridge. She had once seen a slaughterhouse discharge effluent into a stream, a rusty stain fluttering in the current, that&rsquo;s how it would have been when the priest was hanged. The juices of the man obscenely falling into the river. </p>
            
            <p>&lsquo;And why not,&rsquo; said Dorothy, putting one arm akimbo on her hip. &lsquo;Haven&rsquo;t I got an aunt, Teresa Boland, in New Zealand? She&rsquo;s a widow woman; runs a boarding house some place. Says she&rsquo;d give me a job if I came. I bet there&rsquo;d be one for you if you wanted. Aunt Tess thinks New Zealand&rsquo;s a grand place. And aren&rsquo;t there so many men out there that girls like us go dizzy in the head with all them offers of marriage?&rsquo; </p>
            
            <p ALIGN="CENTER">***</p>
            
            <p> Down by the creek were the willows; they shuffled about in their greenery, they chafed their feathered arms, they muttered in some other tongue.  Kathleen had given each tree a name after her neighbours in Ballydownie.</p>
            
            <p>&lsquo;There you are Mr Kelly. Isn&rsquo;t it a fine, soft morning?&rsquo; she&rsquo;d say coming to the first tree. &lsquo;And the rheumatics Mrs O&rsquo;Driscoll? Have you tried wearing a copper bangle? Sure didn&rsquo;t Patsy Doherty say it did him the power of good. And you Mr Conner, how&rsquo;s your grandson getting on up in Dublin? Has he backed any more winners at the Punchestown races?&rsquo;</p>
           
            <p>The light, shrill as a whistle, infested the land and the hills were ragged with felled stumps. Broken, smashed bits of tree. The New Zealand countryside so large, so desolate and so unused. Kathleen put her face against &lsquo;Mr Conner&rsquo; and wept. Where were the little boreens tottering into the valleys between loose stone walls, the children shouting on the roads, the cottages with whispers of smoke? Here there was only the desolation of newly uncovered hills. </p>
           
            <p>Turner sharpened his axe. Up and down the whetstone went wooing the blade. His axe and his saw were his love. His pride and his joy. </p>
            
            <p><em>Holy Mary, Mother of God</em></p>
            
            <p> Kathleen met Sam Turner at Mrs Boland&rsquo;s boarding house. </p>
            
            <p>Kathleen and Dorothy Malone had worked together but within three weeks of arriving in New Zealand Dorothy was being wooed by a farmer and a month later married him. He gave her a ring made out of a nugget he found in a hole and took her away to a house by a cold lake.</p>
            <p><BR>
            </p>
            
            <p>Kathleen was getting the cutlery out of the drawer, ready to set the tables. The knives had bone handles, yellowed as old teeth. </p>
           
            <p>&lsquo;So you&rsquo;ve got yourself an admirer,&rsquo; said Mrs Boland smirking.</p>
           
            <p>&lsquo;An admirer?&rsquo; said Kathleen, pausing with a handful of knives in her hand. &lsquo;You must be tricking.&rsquo;</p>
            
            <p>&lsquo;Never,&rsquo; said her boss. &lsquo;It&rsquo;s Mr Turner from up beyond. Real interested he is, asking me all about you.&rsquo;</p>
            
            <p>&lsquo;Mr Turner, the one who comes in for a night sometimes?&rsquo; said Kathleen. &lsquo;But I&rsquo;ve </p>
            <p>hardly spoken to him.&rsquo;</p>
           
            <p>&lsquo;Well, he&rsquo;s got tabs on you. And you could do a lot worse than walk out with a fellow like that. He&rsquo;s a good steady chap and with a fine little farm off up in the hills.&rsquo; </p>
           
            <p> A good steady chap, one that could stand up in a boat. The boat would rock but he could still stand as blood ran in the river. </p>
            
            <p>Mrs Boland took the bundle of knives from Kathleen and began laying them out as she spoke. </p>
           
            <p>Sam Turner. His face, if Kathleen had ever seen it, was unknown. But she recalled his hands; his hands were covered with fine reddish hair making them look like small, scuttling, ginger animals.  When she&rsquo;d put a bowl of porridge in front of him or came to clear away his plates, the red furred creatures hurried about. </p>
           
            <p>Kathleen blushed when she saw Turner in the dining room.</p>
            
            <p>&lsquo;Himself&rsquo;s in there for his breakfast,&rsquo; she hissed. </p>
           
            <p>&lsquo;Well find out what he wants to eat,&rsquo; said the older woman.</p>
            
            <p>&lsquo;Good morning Mr Turner. And what do you fancy for breakfast today?&rsquo; said Kathleen. </p>
           
            <p>&lsquo;My usual porridge, bacon, fried eggs, toast and tea, thank you,&rsquo; Turner caught Kathleen&rsquo;s arm. &lsquo;Would you fancy a drive in the country on your day off, Miss Collins? You could bring a friend if you want.&rsquo; </p>
           
            
            <p>&lsquo;There&rsquo;s no one I could ask,&rsquo; Kathleen face was hot and red. Red like the man&rsquo;s hands. </p>
            
            <p>Should I go?  &lsquo;Do you think it would be right?&rsquo; asked Kathleen.</p>
            
            <p>&lsquo;Ah, get on with you,&rsquo; said Mrs Boland, &lsquo;the cat never caught a mouse hiding in the cupboard.&rsquo; </p>
            
            <p>&lsquo;Thought you might like to see where I live. There&rsquo;s still a lot of bush to clear but the place has prospects,&rsquo; said Turner as they drove along in the trap. &lsquo;Mrs Boland&rsquo;s made us some sandwiches, we can eat them and have a cup of tea in the house after I&rsquo;ve shown you the farm.&rsquo;</p>
           
            <p>The wheels of the trap went around and around. The trees and the grass glowed as if lit by candles, the day smiled wide with promise. Everything was excitement. </p>
            
            <p>Kathleen had never ridden in a trap or gone out alone with a man before. She had wiped her serge skirt with vinegar and water to get the shine off it and had used the blue bag with her blouse when she washed it, to make the white bright. And she had bought a new hatpin especially for the occasion; the brass ornament impaled her hair beneath her hat, on the end of the savage pin was a blue butterfly. </p>
           
            <p>&lsquo;Matches your eyes.&rsquo; Mrs Boland said as Kathleen pushed the pin into her hat before leaving. </p>
           
            <p>&lsquo;Your eyes, your eyes,&rsquo; the trap wheels sang. </p>
           
            <p>&lsquo;Well, what do you think?&rsquo; Turner locked the front door of his cottage behind them. The lock was a mouth, a pout, a hole in the wooden panels.</p>
           
            <p>&lsquo;It&rsquo;s a grand place and Daisy looks a fine house cow. She reminds me of a brown cow called Flo we had back home,&rsquo; said Kathleen.</p>
           
            <p>Oh for that cow ponderously swinging her body. She was wide and soft as a rug on a clothes line and the grass was bloated by rain and the skylarks rose in the June air. </p>
          
            <p>&lsquo;That wasn&rsquo;t want I meant,&rsquo; said Turner putting the key back in his pocket. &lsquo;Me, do you want to have me? Would you consider us being together, getting married?&rsquo;</p>
          
            <p>&lsquo;Married?&rsquo; said Kathleen. If there was ever to be a proposal she hadn&rsquo;t expected it like this or so soon. </p>
          
            <p>&lsquo;A man needs a wife,&rsquo; said Turner. &lsquo;And I&rsquo;ve taken quite a shine to you.&rsquo; </p>
           
            <p>Kathleen inspected the mud on the toe of her boot.  And what does a wife need? She didn&rsquo;t know.</p>
            
            <p>&lsquo;Mrs Boland says you&rsquo;re a hard worker,&rsquo; said Turner, &lsquo;not one of those flighty pieces. I know you&rsquo;re a Mickey Doolan; I&rsquo;m not one myself, but I don&rsquo;t see it matters. Women seem to like all this running to the priest and I reckon it&rsquo;s not altogether a bad thing. Stops them getting ideas,&rsquo; he smiled. &lsquo;I&rsquo;ve never fancied a woman with ideas.&rsquo;</p>
           
            <p>The grass was beaten down in the front area; it was bent over and creased.  Kathleen could see the lines where the stalks were damaged and broken. The track beyond had ruts in it, scoops and hollows, hammocks in the mud. And beyond, the thick green pelt of the trees trembled. </p>
           
            <p>On the hills you could see Turner&rsquo;s work of clearing and burning. Kathleen thought of the man&rsquo;s hairy hands and imagined his equally hairy back. In her mother&rsquo;s house her parents&rsquo; bed was high and wide. Pale as a tomb fenced by black iron ends. </p>
           
            <p> But the offer of marriage was tempting. Turner seemed a decent enough bloke. He had a house, a small farm and a good reputation. She was thirty-one years old and had nothing beyond a suitcase of clothes, the tea set her mother had won in a raffle and the tablecloths, pillow slips, mats and runners she&rsquo;d embroidered for her hope chest. </p>
           
            <p>&lsquo;So what about it? I&rsquo;m willing if you are,&rsquo; said Turner as they walked to the gate.</p>
           
            <p>&lsquo;I&rsquo;ll need to think about it,&rsquo; said Kathleen.  Think. What would she think when she was buried with this man in a tall white bed; red furred hands scuttling on her skin, burrowing into her folded flesh?</p>
           
            <p>They were married in the vestry of the Catholic church with no flowers or music, on account of Turner not being of the faith. The plaster Virgin looked askance. She had a chip on one of her cheeks, a white tear on her immaculate apricot flesh. </p>
            
            <p>Mrs Boland and Dorothy and her new husband Ernest came for the wedding. Mrs Boland put on a high tea in the boarding house afterwards. There was corned beef, new potatoes and fresh peas with bread and butter. Dorothy made a two-tier wedding cake. The icing cracked as the knife blade went in. </p>
            
            <p>&lsquo;And don&rsquo;t forget to keep the top layer of the cake for when there&rsquo;s a christening,&rsquo; Dorothy said grinning.</p>
            
            <p>A christening. A child.  What terror saw the making of a child?  What invasion of the soft and holy places. Sam Turner had red hands and his axe and his saw were his pride and his joy.</p>
            
            <p>The newly wed couple went back to the farm immediately. </p>
           
            <p>&lsquo;Isn&rsquo;t it grand owning a trap,&rsquo; said Kathleen. She stroked the leather cushion of the seat with her gloved hand. &lsquo;In Ireland we only had a little donkey cart that Ma and me used for taking the butter and eggs into town on fair days.&rsquo;</p>
            
            <p> The donkey never went at more than a walk; sometimes he stopped to eat. He pulled at the hedgerows with his discoloured teeth.  Kathleen and her mother came home from fair days in the dusk. The midges flew up and down as if on tiny threads, and the moon looked over a wall.</p>
           
            <p>&lsquo;The trap&rsquo;s not mine,&rsquo; said Turner, &lsquo;the horse neither. Hired them when you came to see the farm and again for today.&rsquo;</p>
            
            <p>&lsquo;But,&rsquo; said Kathleen &lsquo;how will I get about? Everything&rsquo;s so far away in New Zealand, not like at home.&rsquo;</p>
           
            <p>Home. Far away. Over the hills and far away, and still you&rsquo;re not there. Mountains and plains, countries and continents, seas, and oceans lay between. There was no way back, well not for her. </p>
           
            <p>&lsquo;No need for you to go any place,&rsquo; said Turner whipping up the horse. &lsquo;I&rsquo;ve a farm nag I ride into town once a month or so. I buy everything then.&rsquo;</p>
           
            <p>&lsquo;But mass,&rsquo; said Kathleen. &lsquo;How can I get to mass?&rsquo; </p>
           
            <p>The elevation of the host by the priest in his twinkling garments; the magical transformation before her own eyes. Bread and wine, body and blood.  Without that, how could she be?</p>
           
            <p> &lsquo;Expect you could walk,&rsquo; said Turner </p>
            
            <p>&lsquo;Maybe I could learn to ride,&rsquo; said Kathleen, though the thought of such a thing was terrifying.</p>
           
            <p>&lsquo;Molly&rsquo;s the only horse I have. She&rsquo;s used to me. I wouldn&rsquo;t want a woman messing her about,&rsquo; said Turner.</p>
            
            <p><BR>
            </p>
            <p>Kathleen had made a new set of sheets for the bed. She had embroidered lilies on the turn overs. Satin stitch.  Each thread lay down with the other, smooth as a leaf or a flower. Her mother had told her how it was on her own wedding night; her husband&rsquo;s amorous overtures seemed so alarming that she had thought him mad and climbed terrified onto the top of the cupboard, refusing to get down while he was in the room. Next morning the priest had been sent for and given her a real telling off. Not doing her duty, calling into question the Almighty&rsquo;s plan and the sacredness of the marriage sacrament. Wasn&rsquo;t it women like her that encouraged their husbands to sin; drove them into the arms of loose harlots; the insufferable pride - the cheek of it. </p>
            
            <p>&lsquo;Tis a hard thing being a married woman,&rsquo; Kathleen&rsquo;s mother had said, &lsquo; but with time you get used to men and their ways.&rsquo;</p>
           
            <p>Kathleen was under the newly embroidered sheets in her nightdress with the pearl buttons. She was rigid with dread. What would happen? Would it hurt? Could it be borne? Outside the wind made sucking voices in the night and the darkness gathered round the house as if pulled together on a string. </p>
            
            <p>Turner opened the bedroom door carrying a lighted candle in an enamel holder. The candle light fell yellow as a knife handle on the floor. Turner was wearing a night shirt, his legs stuck thin and gawky beneath it. He must have undressed in the kitchen to spare her embarrassment; that was something.</p>
            
            <p>&lsquo;You ready?&rsquo;, said Turner.</p>
            
            <p> Was she ready?  Ready for this thing to happen; this offering up, this swooping down. This sacrifice in the bed that was tall and pale as a cake. Ready?  Kathleen didn&rsquo;t know. </p>
            
            <p>&lsquo;I think I am,&rsquo; whispered Kathleen, her fingers under the pillow clenched on the rosary. The beads were hard and biting in her hand. The trees held their branches skywards when Turner&rsquo;s axe severed the trunk. She had seen them stretching upwards and upwards as the blade fell. </p>
           
            <p>Turner blew out the candle and got into the bed. The springs creaked. Kathleen thought about the red hairs on her new husband&rsquo;s flesh, and if she should hold her arms up stiff and straight when the moment came.</p>
            
            <p>&lsquo;Here,&rsquo; he said, grabbing her wrist. Kathleen did not move. Turner scuffled up her nightdress and lay heavily on top of her. She felt the folds of the fabric between them and the awful intimacy where flesh met flesh. She was grateful for the darkness. </p>
            
            <p>And when the hurt slid into her Kathleen was still as a fallen tree.</p>
            <p><BR>
            </p>
            
            <p>Early one morning Kathleen asked Turner about getting a donkey and cart. Her husband was putting on his boots and Kathleen knelt down to do the laces. She started at the toe tightening the cords as she went. </p>
          
            <p>&lsquo;Do you think maybe we could get a little cart and a donkey like Seamus back home?&rsquo;, she said sitting back on her heels. </p>
            
            <p>Turner laughed and said, &lsquo;A donkey? We&rsquo;re not living in the old sod now.&rsquo; </p>
           
            <p>&lsquo;What about a pony then?, said Kathleen, &lsquo;so I could go about more.&rsquo;</p>
            
            <p>&lsquo;Go about?&rsquo; said Turner. &lsquo;Where do you want to go to? Don&rsquo;t you have everything you need here? We&rsquo;ve no money to waste on ponies and carts; they cost you know.&rsquo;</p>
            
            <p>&lsquo;I thought maybe I could sell Daisy&rsquo;s left over cream to the new dairy factory in town. If I had a donkey or a pony cart, I could take it in,&rsquo; said Kathleen.</p>
            
            <p>&lsquo; No need for that,&rsquo; said Turner.</p>
           
            <p>&lsquo;I just want to meet people, see the shops now and then and earn a little money of my own,&rsquo; said Kathleen.</p>
            
            <p>&lsquo;You think I don&rsquo;t provide well enough for you?&rsquo; said Turner.</p>
            
            <p>Well enough. </p>
           
            <p>Kathleen had made paper lace pelmets for the kitchen shelves. She had cut each crescent, diamond and whirl out with the folding scissors that was shaped like a bird with a vicious beak.  She laid the three pots, the two bowls - one brown and one white - the helping spoon, a ladle, a sieve, and the carving knife and fork on top of the decorated paper. There were potatoes, sugar, and flour in bags in the pantry, milk, and cream, and eggs in the scullery. The outside safe held a leg of mutton. The flies crawled over the swinging metal box where the blood had stained the mesh. </p>
           
            <p>She had seen Turner cut sheep&rsquo;s throats. The knife in his hand, the animal&rsquo;s head clamped back under his arm as if in an embrace. The bloods springing like beads in a scarlet necklace. </p>
           
            <p>She was provided for well enough. </p>
            
            <p>Turner came back from town and brought Kathleen a letter from her cousin, Lizzy Heaney in Ireland.  The envelope was on the kitchen table, where it lay unopened on the scoured wood. It was a thing of wonder, a surprise, a possibility. Kathleen touched it with the tips of her fingers. She held the envelope up, put her cheek against it and sniffed. </p>
           
            <p>The Ballydownie post office was in the front end of Mrs Butler&rsquo;s parlour. Mrs Butler would have got up from her sewing machine and said, &lsquo;New Zealand? And isn&rsquo;t that a desperate way- off country to be sending to,&rsquo; as she got out the stamp. The thought made Kathleen ache. </p>
           
            <p>Lizzy wrote that, &lsquo;Peggy Riley had another son, though the poor wee mite only lived a day, God rest his soul …Father Mulcahy had a new housekeeper called Mrs O&rsquo;Dwyer, she&rsquo;s a real old bite …The young boys in the village were all making kites from bits of newspaper… Monica Leary&rsquo;s got herself engaged to that old fellow Frank Connolly from over by the pond. Can&rsquo;t imagine what she sees in him.&rsquo; </p>
            
            <p>Kathleen sat in the kitchen reading the letter over and over. She ran her fingers across the paper, maybe the pen marks could tell her more. One corner of the letter had a slight smudge; had a moth been crushed beneath Lizzy&rsquo;s elbow or a drop of tea hastily brushed away?</p>
            
            <p>&lsquo;Would you like to read what Lizzy says?&rsquo; Kathleen asked her husband as they ate.</p>
           
            <p>&lsquo;What for?&rsquo; said Turner spitting a bit of gristle onto his plate.</p>
            
            <p>&lsquo;It&rsquo;s about Ballydownie, where I come from, thought you&rsquo;d like to hear,&rsquo; said Kathleen.</p>
            
            <p>&lsquo;Women&rsquo;s tittle tattle,&rsquo; said Turner.</p>
           
            <p>There was silence between them broken only by the sounds of eating and the sizzling of wood burning in the range. </p>
            

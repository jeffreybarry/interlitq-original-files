            <p>Rumwold is not</p>
           
             &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Rvmwoldi<br>
             &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Rumuualdi<br>
             &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Rumaldi Archiep<br>
             &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Rumpaldi<br>
             &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Rumwald<br>
             &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Runwald<br>
             &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Saint Grumbald<br>
             &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Sainct Rumwald<br>
             &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Rowaldi<br>
             &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;or Rumbaldi.</p>
           
            <p>I am Rumwold the Confessor.</p>
            
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;(Why not <br>
             &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Rumwold the Martyr?)</p>
            
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Not to be confused with St Rombaut <br>
             &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;or Rumold of Mechlin </p>
            
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;who was in fact a martyr.</p>
            <p>Not to be confused.</p>
            
            <p>It was supposed to be a memorable <br>
            name. Did I not enunciate?</p>
            
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;*</p>
            
            <p>Neither hungry nor pained, but needing to gain<br>
            my parent&rsquo;s interest, the cry expanded <br>
            to the edges of the field, to the ears</p>
            
            <p>of the harvesters half a mile behind<br>
            us. I had been told to use a big voice<br>
            to let my relaxed baby lungs pump out</p>
            
            <p>not only glottal wails but words of God.<br>
            If I&rsquo;m loud, my message will sound urgent;<br>
            adults cannot escape that new-born noiseâ€”</p>
           
            <p>they are powerless, forced to listen to babe<br>
            and carry the burden of his commands.<br>
            I needed milk and appreciated</p>
            
            <p>the closeness of arms, like a normal boy,<br>
            but <br>
            the rest of my requests were too abstract</p>
            
            <p>to be explained through moans and screams. <br>
            My words, booming, red-faced words did the trick.<br>
            What was the trick? A sermon and a song</p>
            
            <p>before the awful funeral arrangements? <br>
            You have to die, otherwise the message<br>
            loses significance. What</p>
            
            <p>message? I wanted to ask Him.<br>
            A talking baby should be remembered<br>
            long after a sermonising adult.</p>
           
            <p>I don&rsquo;t know what I did wrong, to deserve<br>
            such neglect from posterity. Called confessor, not<br>
            martyr, falsely, I wasted all three days.</p>
            
            <p> *</p>
            
            <p>My church stood on the coast between Folkstone and Hythe.<br>
            Its small white spire called the fishermen to shore<br>
            showed them safe passage, between rocks and sand banks.<br>
            Profit from the fattest whiting went to me as thanks.</p>
           
            <p>When the spire was firewood, foundations demolished<br>
            the fishermen put the rumbal whiting money <br>
            towards a Christmas Eve feast for families<br>
            of the workers. They should never have been called &ldquo;Rumbal&rdquo;</p>
            
            <p>rather &ldquo;Navigation&rdquo; or &ldquo;Beacon&rdquo;, and later<br>
            &ldquo;Celebration&rdquo; whiting. The fish were a sweet gesture,<br>
            I just wish they&rsquo;d been for me. Who is Rumbal? a young<br>
            fisherboy might wonder. Who indeed. Not I.</p>
            
            <p>Even the church of Saint Rumwold forgot over time.<br>
            Farfetched baby doesn&rsquo;t need fish, can&rsquo;t keep up a beacon<br>
            for the boats at night. What did he do to become a saint?<br>
            Confessor? Sermonising from the start? Poor parents.</p>
            
            <p>One expects a few years before the child talks back.<br>
            Unbelievable. Rumbal. Unbelievable Rumwold. <br>
            From Folkstone there now runs a scenic road with a view <br>
            of the boat-flecked harbour. Nobody ever needed me.</p>
           
            <p> *</p>
          
            <p>I certainly did not die<br>
            of old age, but nor was I ill.<br>
            A doctor might say the body <br>
            does not die without a malfunction<br>
            which could be described as illness.</p>
            
            <p>My vital organs switched off simultaneously<br>
            like machines on a timer. They functioned<br>
            perfectly, as they were always meant to,<br>
            every organ but one. My brain ticked faster<br>
            if anything. Sometimes guillotined heads</p>
            
            <p>continue trying to talk as they roll sidewaysâ€”<br>
            I am still trying to talk, even now, with nothing<br>
            but bones and even those forgotten. Baptised<br>
            babies go to Heaven, so I can only assume<br>
            that Widerin was incompetent.</p>
            
            <p>I am hanging on now, somewhere, <br>
            watching. Bitter. Bitterly forgotten.<br>
            Let&rsquo;s pool my meagre resources for a moment:<br>
            a missal and breviary in the Swedish State Archives<br>
            in Stockholm (don&rsquo;t ask). Eight pence left</p>
             
            <p>in Thomas Westall&rsquo;s 1525 willâ€”for the upkeep <br>
            of my long gone altar. A dedication to &ldquo;Rumbald&rdquo;<br>
            in Stoke Doyle. A Church of &ldquo;Saint Runwald&rdquo;<br>
            in Colchester, and Rumboldswyke, Sussex<br>
            with an 11th Century church, now, of course</p>
             
            <p>dedicated to Saint Mary. Not remembered<br>
            even in my own village. Granted, I&rsquo;ve never felt saintly.<br>
            I believe in Jesus just because I&rsquo;ve met himâ€”<br>
            anyone could do that. Otherwise I might be cynical.<br>
            (Really, I wouldn&rsquo;t exist at all). Not a saint, no</p>
            
            <p>but a martyr. This is a rare confession <br>
            from Rumwold the Confessor. Mostly, I am martyr<br>
            giving up my tiny life for the cause. I was not willing;<br>
            am I still clinging to the voice I should have lost long ago? <br>
            That would explain my disgraceful position now.</p>
 

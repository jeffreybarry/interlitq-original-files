            <p><strong>Sea Change</strong></p>
            
            <p>last Labour Day, for instance<br>
            we were overcome with sunfish<br>
            great heaving herds of them<br>
            along a quaking sky</p>
            
            <p>the tallest rivalled Sky Tower<br>
            &amp; blocked out our light<br>
            tankers crashed, balloons were stranded<br>
            twenty storeys high</p>
            
            <p>I saw you caught amongst them<br>
            trying to swim upwind<br>
            I think I heard you screaming<br>
            in an alcove of my eye</p>
            
            <p>please do not display your wounds to me<br>
            until you’ve figured out<br>
            how to cover them in fish scales<br>
            &amp; taught yourself to fly</p>

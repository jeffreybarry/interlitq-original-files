<p><strong><a name="my"></a>My Year of the Ant Gardens</strong></p>

<p>The year we lived in the caravan park,<br>
I so much wanted a pet (missing the dogs<br>
and guinea pig we had to leave behind)</p>

<p>I started making ant gardens in the sand <br>
next to the fence by the caravan,<br>
setting out stone paths, little ponds,</p>

<p>with weeds for trees and twigs for fences,<br>
and I wished and wished we lived in a house <br>
like other people, but we didn’t.</p>

<p>There was everything an ant could want<br>
in those gardens: food, furniture.<br>
I tried my best to make it all attractive.</p>

<p>Ah, but they never stayed, the ants, despite it.<br>
They always had somewhere else to get to.<br>
They only ever hurried blithely by.</p>

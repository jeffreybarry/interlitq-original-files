            <p><strong>Diwali</strong></p>
           
            <p>The ponga’s tongue tapped the window<br>
            I ignored it.</p>
            
            <p>The room was a bauble blown by candles<br>
            floating on a silver plate&mdash;</p>
             
            <p>I adored it.<br>
            The god had slept a full year</p>
            
            <p>on the dusty mantel. His suit<br>
            of light and flowers a forgotten ghazal</p>
            
            <p>until the ritual gathering of children<br>
            incense and  halwa, the soft thunk of Nani’s mala</p>
          
            <p>stirred him from his dreams. O son<br>
            your eyes two newly born worlds not six years old</p>
           
            <p>you held the plate of butterlamps<br>
            soft as flowers in water and</p>
            
            <p>if the dusty god stretched then none of us <br>
            felt his bronze trunk tap our shoulders.</p>
           
            <p>We sang; we ate.<br>
            I did not know the thread and braid of the words.</p>
            
            <p>But somehow the gift was given<br>
            somehow we made it work.</p>
             

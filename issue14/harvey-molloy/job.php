<p><strong><a name="exorbitant">The</a> Exorbitant</strong></p>

<p>*</p>

<p><em>the exorbitant question of method</em></p>

<p><em>*</em></p>
<p>School’s in. Eleven hundred<br>
legs climb the hill<br>
staff cars sparkle in the sun<br>
the sapphire sky</p>

<p>empty and boundless</p>

<p>as a Tibetan saint’s dream<br>
morning jolts the many-legged<br>
beast awake with an espresso<br>
kick (soon the café will be </p>

<p>caffeine-free)</p>

<p>outside my classroom<br>
Ionatana stands<br>
like a wading kotuku<br>
foot pressed flat</p>

<p>on the corridor wall</p>

<p>while the girls<br>
on the other side talk<br>
of church meetings<br>
merging congregations</p>

<p>passages from Deuteronomy</p>

<p>to be learnt for the pastor<br>
how Nia’s weekend job <br>
at Pak’nSave <br>
can't cover the cost </p>

<p>of her mobile</p>

<p>I clutch my tea tightly<br>
nod them inside <br>
the failing classroom<br>
the key sticks in the door</p>

<p>*</p>

<p><em>the exorbitant question of mind</em></p>

<p><em>*</em></p>

<p>The lines of the running track<br>
white orbits on green grass<br>
around an absent sun<br>
we sit on a bench in what would be</p>
 
<p>the park’s Kuiper belt</p>
 
<p>our philosophical talks as predictable<br>
(and surprising) as our weekly game of chess<br>
there are no universal laws or decrees<br>
the sun does not hold attendant planets in its sway</p>

<p>the king is a thing of nothing</p>

<p>you maintain that we stake possession<br>
over our limbs, eyes, fingers<br>
I and mine demand their <em>a priori</em> claim<br>
assert their difference from the body's reflection</p>

<p>we see and taste the world through sense</p>

<p>(the morning’s plunger blend, the double stars<br>
of Scorpius blurring each year to a single orb)<br>
and in the speeding gulf between I and body<br>
the humdrum nostalgia of middle age</p>

<p>*</p>

<p><em>the exorbitant question of method</em></p>

<p><em>*</em></p>

<p>the exorbitant question of method<br>
the question of how to pose the question<br>
what exceeds or precedes writing in<br>
a tiny flat in Palmerston North</p>

<p>on the night Muldoon got pissed</p>

<p>and announced the snap election<br>
I copied quotations from Derrida's <br>
<em>Of Grammatology</em> onto thinly-ruled index cards<br>
which I shuffled and arranged</p>

<p>before typing my thesis</p>

<p>on the problem of meaning <br>
(the mirror of reading)<br>
on erasable bond typing paper<br>
(brought by Raj from the United States)</p>

<p>through the black and white snow</p>

<p>of poor reception a ghostly Muldoon <br>
slurred his speech and looked about<br>
to lose it completely as he toddled<br>
down a corridor in The Beehive</p>

<p>somewhere down the line he lost</p>

<p>the plot became unhinged like<br>
one of Derrida’s doors that swing<br>
still he screwed the country his way<br>
and we crossed the guardrail</p>

<p>balustrade </p>

<p>mind the gap<em> </em>fell flat<br>
onto the track as we gambled (lost?)<br>
on games Derrida doesn’t play much<br>
namely Chicken or Consequences</p>

<p>*</p>

<p><em>the exorbitant question of time</em></p>

<p><em>*</em></p>

<p>Schools out. We step out<br>
into the dark carpark<br>
no moon, stars bright</p>
 
<p>Heather reckons we should<br>
research online how the school</p>
 
<p>they built in  Australia<br>
without classrooms is working out<br>
how the kids there are getting along</p>

<p>look! sparkling beads <br>
of burning white light</p>

<p>flare and fall from the fading orbit<br>
of a collapsing Soviet satellite<br>
five seconds east of Spica</p>
 

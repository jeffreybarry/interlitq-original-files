            <p><strong>Making Contact</strong></p>
            <p> </p>
            <p>It's dusk and the old electric heater is trying its four bars.<br>
            I am thinking, again, of where you are and how I might get through.</p>
            <p>          The medium was a nonsense of guesswork and guile<br>
                      and the dreams I had were forced narratives.</p>
            <p>          I tried writing a letter with my right hand.<br>
                      After a glass of wine, I used the left to channel your reply<br>
                      but all that came out were backhanded compliments.</p>
           
            <p>The room is slowly losing its chill. The cat spills herself onto my lap<br>
            and</p>
            <p>          I wonder if you were happy here,<br>
                      where we sat as a family in the bowl of the valley;<br>
                      waking to birds fluting and chipping their native mimicry,<br>
                      then following the late afternoon arrows of geese <br>
                      over acres of hard graft and laden fruit trees.</p>
            
            <p>How long do you think it will be until I can sleep? </p>
            
            <p>I see. </p>
            
            <p>          I see.</p>
            <p>&nbsp; </p>
<p>&nbsp;</p>
            <p><a name="at"></a><strong>At Dusk</strong></p>
            
            <p>I took myself beyond retraction,<br>
            like a question ventured<br>
            with a blush,<br>
            scored my shadow<br>
            into the shelf of the cliff<br>
            like a vandal's name<br>
            or the struck sticks of boys<br>
            bored with the day.<br>
            Splashes, then great waves,<br>
            overshot the clay pathway<br>
            causing spathic falls.<br>
            A dog called Daisy <br>
            stopped to warn me<br>
            that this was not a safe route<br>
            for any woman lost in thought.<br>
            Strange, that fear and thrill;<br>
            that sick risk plummet <br>
            of the stomach<br>
            when we realise <br>
            the only way is forward<br>
            and return will have to be <br>
            accomplished in the dark.</p>

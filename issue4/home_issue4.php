    <td colspan="2" valign="top" class="texto2"> I am delighted to welcome you 
      to Issue 4 of <i>The International Literary Quarterly</i>.<br> <br>
      Jorge Lu&iacute;s Borges  looms large in this issue, cited by <a href="http://interlitq.org/issue4/alan_wall/job.php" class="link1">Alan Wall</a>, and the subject of an essay by <a href="http://interlitq.org/issue4/edwin_williamson/job.php" class="link1">Edwin  Williamson</a>.</p>
    <p>We learn that Borges&rsquo; story &ldquo;Pierre Menard, the author of <em>Don  Quixote</em>&rdquo; was first published in <em>Sur</em>,  the review presided over by Victoria Ocampo, and bestriding the cultural scene  between 1931 and 1992.</p>
	<p>While <em>Sur</em>, as a standard-bearer of internationalism, is to be emulated, its partisan line is at odds with this review’s belief that art must rise above overt political affiliation. This sentiment is also affirmed by Nobel Laureate, <a href="http://interlitq.org/issue4/gao_xingjian/job.php" class="link1">Gao Xingjian</a>,  in his interview with <a href="http://interlitq.org/issue4/gao_xingjian/job.php" class="link1">David Dabydeen</a> and <a href="http://interlitq.org/issue4/gao_xingjian/job.php" class="link1">Jonathan Morley</a>.</p> </td>
    <td><img src="/images/issue_img/spacer.gif" width="1" height="219" border="0" alt=""></td>
  </tr>
  <tr> 
    <td colspan="4" rowspan="2" valign="top" class="texto2"><br> <p>Such a  conviction is  underscored by the fact that much of human experience transcends national  barriers, as borne out by <a href="http://interlitq.org/issue4/xu_xi/job.php" class="link1">Xu Xi</a> whose  protagonist is shaped by both Asian and American influences; Indian writer  <a href="http://interlitq.org/issue4/meena_alexander/job.php" class="link1">Meena Alexander</a> who writes movingly about the ravages of war in Darfur; <a href="http://interlitq.org/issue4/marina_warner/job.php" class="link1">Marina  Warner</a> who evokes Egypt, no doubt inspired by her early childhood there; and  <a href="http://interlitq.org/issue4/tugrul_tanyol/job.php" class="link1">Richard McKane</a> who, having spent many years in Turkey, has translated poets  such as <a href="http://interlitq.org/issue4/tugrul_tanyol/job.php" class="link1">Tu&#287;rul Tanyol</a>.</p>
        <p>Meanwhile, Anglo-Saxon  authors, <a href="http://interlitq.org/issue4/daniel_albright/job.php" class="link1">Daniel Albright</a>, <a href="http://interlitq.org/issue4/alice_fulton/job.php" class="link1">Alice Fulton</a>, and <a href="http://interlitq.org/issue4/michael_schmidt/job.php" class="link1">Michael Schmidt</a>, writing about  events closer to home, prove that they are academics who possess remarkable  literary gifts.</p>
        <p>And let us not  forget to thank artist, <a href="http://interlitq.org/issue4/arturo_di_stefano/bio.php" class="link1">Arturo Di Stefano</a>, for enriching our journey with his  profound yet subtle images.</p>
        <p>Thank you for your  visit, and may you return to visit us many times in the future!<br>

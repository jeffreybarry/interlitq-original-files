          <td colspan="2"valign="top" class="texto2"><p align="left"><strong>II.</strong> </p>
            <p align="left"><em><u>In Our Lifetime </u></em><strong>&nbsp;&nbsp;&nbsp;</strong></p>
              <p align="left">Flushed by the rose of flesh<br>
              Pierced by barbed wire, a wound that will not heal.<br>
              The iron of attachment cuts<br>
              What we take for ourselves, ways of living<br>
              That will not last for very long, untenable, yes.<br>
              A boy moves on the plain, his goats beside him.<br>
              Trying to find his way through clouds of dust --<br>
              Haskanita, where children rushed by men<br>
              On horseback discover the guns&rsquo; temerity,<br>
              Where stars startle themselves in broken water<br>
              And the boy with his goats, trying to turn home<br>
              Remembers what his father never told him &ndash; <br>
              Open your legs wide, run<br>
              <em>Not those staggering towards slaughter.</em></p>
            <br>
            
            <p align="left"><strong>III.</strong></p>
           
            <p align="left"><em><u>Green Leaves of El Fasher</u></em> </p>
            <p align="left">Everything that&rsquo;s real turns to sun:<br>
              Stones, trees, the jeeps they came in, those men, <br>
              In Jebel Marra, the leaves are very green,<br>
              Here, in El Fasher too. <br>
              I am singing, stones fill with music.<br>
              Do not touch my hair, I cried. They forced me <br>
              To uncover my head then beat me when my veil slipped, <br>
              Not the pink one I am wearing now, with stripes &ndash; this<br>
              My aunt gave me. I am not an animal, <br>
              They are more free, birds in the tree, horses too.<br>
              I am your language, do not cover me.<br>
              I am burning in what you take to be the present tense.<br>
              We are the letters <em>alif, ba, taa</em>, <em>mim</em> &ndash; <br>
              What the sun makes as it spins a nest of fire.</p>
           <br>
            <p align="left"><strong>IV.</strong> </p>
           
            <p align="left"><em><u>Last Colors</u></em> </p>
            <p align="left">In another country, in a tent under a tree, <br>
              A child sets paper to rock, <br>
              Picks up a crayon, draws a woman with a scarlet face, <br>
              Arms outstretched, body flung into blue.</p>
            <p align="left"><em>(Hashsha</em> &ndash; to beat down leaves from a tree.)</p>
            <p align="left">The child draws an armored vehicle, guns sticking out<br>
              Purple flames, orange and yellow jabbing, <br>
              A bounty of crayons, a hut burst into glory.</p>
            <p align="left"><em>(Yatima</em> &ndash; to be an orphan, the verb intransitive.)</p>
            <p align="left">The child draws what&rsquo;s near at hand and common,<br>
              Not what&rsquo;s far away &ndash; not the ghost house <br>
              In Khartoum where a father lies <br>
              Whose hands and ears are torn.</p>
            <p align="left"><em>(Idhash-shamsu kuwwirat &ndash; </em>so the sun is overthrown.)</p>

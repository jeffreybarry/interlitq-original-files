			  <strong>I.</strong> <br>
                <em><u>Sand, Music</u></em> </p>
              <p align="left">The wind is blurring our faces<br>
                We do not know who we are or what songs we might sing.<br>
                A stranger enters the village, lets go his horse.<br>
                A woman drags a cart filled with pots and pans,<br>
                Pulling the sky behind her.<br>
                When I was a young girl, I saw nothing,<br>
                My skin set fire to everything. <br>
                A tethered horse is pecked to death by songbirds.<br>
                In Muhagiriya everything&rsquo;s laid out <br>
                As if in a Japanese garden, the sort one dreams of &ndash;<br>
                Circles of sand, beaten rocks, tree stumps <br>
                Tilting into blue. A child&rsquo;s elbow pokes out of a  well.<br>
                In a mosque, men kneeling, five beheaded.<br>
  <em>And the daughters of music brought low.</em> </p>

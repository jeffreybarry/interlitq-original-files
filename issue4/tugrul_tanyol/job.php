            <p><strong>Who&rsquo;s there</strong><strong> </strong></p>
            <p>Canyons  call <br>
              in  your eyes <br>
              and  you are in the lands that end in rains. <br>
              Two  small rivers cry <br>
              in  the depth. <br>
              You  can&rsquo;t knead <br>
              a  whole day out of pain. </p>
            <p>You  will fashion life passionately <br>
              there  in the fading branches <br>
              of  a little plant <br>
              and  beyond that you will see the revival <br>
              of  the steppe, mountains, seas, <br>
              infinite,  dark, stagnant <br>
              and  that startling emptiness. </p>
            <p>Daylight  should not groan with pain all day, <br>
              darkness  should not mount the clouds and rove, <br>
              the  carnation bleeding in your heart <br>
              should  lean over and wipe your tears. </p>
            <p>Canyons  call in your eyes <br>
              there  where the painful winds <br>
              never  finished their lacework, <br>
              you  will learn to live <br>
              completely  alone in the middle <br>
              of  heavy, unclimbable, hard rocks. </p>
            <p>Don&rsquo;t  forget, no one can turn back rivers. <br>
              You  can&rsquo;t intimidate pain with pain, <br>
              you  can&rsquo;t weave sunlight with clouds <br>
              and  call it night <br>
              change  is the law of the universe, <br>
              death  is the sculptor of virtue <br>
              and  on those lines life <br>
              is  happy, carefree but serious, <br>
              canyons  call in your eyes. </p>
            <p><br clear="all">
                <br>
                <strong>Tuesday</strong><strong> </strong></p>
            <p>Have  you ever seen such a sky? <br>
              I  never have. <br>
              A  yellow rain is falling on the streets <br>
              mixing  with the dust. <br>
              A  Tuesday is gently dripping <br>
              from  the cage on the balcony. <br>
              Have  you ever seen such a Tuesday? </p>
            <p>I&rsquo;m  drinking tea <br>
              from  a glass in a silver holder, <br>
              I&rsquo;m  warmed inside by your love. <br>
              There  are heavy-winged clouds in the sky <br>
              and  a lone bird flying. </p>
            <p>Time  passes <br>
              on  such a day on your own <br>
              in  the crystal silence: <br>
              have  you ever seen time? </p>
            <p>My  cigarette smoke makes rings, <br>
              my  pen has become a shadow in my hand. <br>
              Suddenly  the light beside me goes out. <br>
              Have  you ever seen such light? </p>
            <p>Stretching  out to the silence of the room <br>
              I  look at the twilight. <br>
              Things  we&rsquo;re not used to are happening today: <br>
              I  miss the sky <br>
              different  and the same. </p>
            <p>Such  a Tuesday, trembling on the wind, <br>
              clouded  with rain. <br>
              Have  you ever seen such a Tuesday? </p>
            <p>&nbsp; </p>
            <p><strong>Memory</strong><strong> </strong></p>
          
            <p>Every poem starts with a bit of a  lie <br>
              and every poem is part of dying. <br>
              So one by one I send out my stone  chess men, <br>
              what&rsquo;s left in my pocket, <br>
              and no war is lost <br>
              till the death of my last pawn. </p>
            <p>I could bring you mirrors no one  has looked in. <br>
              You look at your back which is  always turned. <br>
              You are a pathetic, paltry man,  cooling the night on your forehead. <br>
              Leave the false light of the  lamps. <br>
              Now death is behind <br>
              every door you open, it&rsquo;s waiting <br>
              like a soul without a candle. </p>
            <p>I paused there <br>
              and put my head on your knees. <br>
              My head was as heavy as winter. <br>
              I saw stone everywhere, <br>
              stone and only stone. <br>
              (Your knees are a stone wall to  beat my head against) <br>
              Don&rsquo;t forget every poem starts  with a bit of a lie, <br>
              and anyway <br>
              what is a dead man if not a  gravestone? </p>
            <p>We carry impossible suicides in  our pocket, <br>
              and a pathetic, suffering, copy  of a hero in our arms. <br>
              We knock at doors that have been  knocked at, opened and closed, <br>
              and we always set our watches to  the wrong time &ndash; <br>
              (and that is why even the  murderers <br>
              wander among us without  handcuffs.) </p>
            
            <p>I take two more paces: <br>
              I have nothing left to say <br>
              (and I have not the age-old  wisdom of a manuscript to give you, <br>
              a fragment of copper that has  survived from secret palaces) <br>
              My memories are snowbound and  impassable <br>
              and every poem is a little death, <br>
              when one by one friends retire  and go. </p>
            <p>
              Every poem starts with a bit of a  lie <br>
              in the twin loneliness of being  and non-being. </p>
            <p>Heh you! <br>

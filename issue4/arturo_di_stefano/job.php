              <p align="left"><br>
                <span class="texto2"><strong>Note. </strong><br>
                My friend, the artist and printmaker Jane Joseph, and I were discussing 
                &#8211; as writers and visual artists sometimes do &#8211; the possibility 
                of a joint project. When we discovered a shared delight in Schumann&#8217;s 
                piano suite <em>Kinderszenen, Scenes from Childhood</em>, we agreed that 
                this beautiful and evocative music could be the trigger for our 
                project. Armed with the score, we went to a performance at the 
                Wigmore Hall and then, over dinner, decided on an experiment: 
                we would work separately, making images and writing texts to accompany 
                the music or, at least, the titles of the music, without any co-ordination 
                or collaboration. As is well known, Schumann himself gave his 
                pieces their titles after composing the music.</span></p>
              </div></td>

          <td colspan="4" rowspan="2" valign="top" class="texto2">  
            <p>[Later: I ended up with a suite of texts (mainly prose) that I liked,  Jane ended up with a suite of images (linocuts) that she liked, but the  experimental joint project, perhaps inevitably, was not a success and Jane&rsquo;s  linocuts were exhibited and published in a folder by Emma Hill&rsquo;s Eagle Gallery,  with only one of my texts in attendance (we agreed that two of the thirteen  pairs went well together). Never mind, we both have something to show for our  work. One day, Jane and I will find another way of working together.] </p>
             <br>
            &nbsp;<br>
            &nbsp;<br>
            <strong>Of Strange Lands and Peoples</strong>
            
            <p>I am nine years old. I live in England. I attend Hebrew classes at  my synagogue in Hampstead Garden Suburb every Sunday morning and after school  on Tuesdays and Thursdays. In our part of London,  there are no fences, only hedges. I love looking at pictures of the Royal  Family. It is 1951. King George is on the throne and Mr Churchill who won the  war is Prime Minister again. We voted for Mr Attlee because he is a socialist.  When he was Prime Minister, he nationalised the mines, provided free medicine  for everybody, and gave India  its independence. In England,  Jewish people are free to do what we like, just like the Christians. </p>
           
            <p>In the old days we lived in other countries. The teachers at Hebrew  classes tell us stories about strange lands and peoples: about Persia  and its 127 provinces, where Queen Esther, which is my mother&rsquo;s name, saved us  from death at the hands of wicked Haman, the grand vizier of King Ahasuerus. We  stamp our feet and wave our rattles, which we call <em>greggers</em>, on the festival of<em> Purim</em>, whenever Haman&rsquo;s name is mentioned during the synagogue service. </p>
           
            <p>Earlier, we lived in Egypt  and escaped slavery, thanks to Moses who was chosen by God to take us to Canaan, which became our land. Later we were thrown out  by invaders and our Temples  were destroyed. I put money in the Blue Box on the shelf in the hall cupboard,  to help Israel.  Poor people go there from the Diaspora. Once, Mr Taylor, the headmaster of the  Hebrew classes, looked at me and said: &ldquo;Israel needs farmers not chartered  accountants&rdquo;. My father is a chartered accountant.</p>
           
            <p>My stamp album has pages of stamps from Poland  and America.  My grandmother Rebecca Rosenberg used to get letters from Poland, where she lived before she came to England.  She still gets letters from America.  But everybody who remained in Poland
died during the war. </p>
                         <br>
            &nbsp;<br>
            &nbsp;<br>
            <strong>A Curious Story </strong>
            <p>I am twelve years old. My other grandmother, Fanny Rudolf, died  yesterday. We go to the funeral. When we return home, we find that the  downstairs mirror has been covered. Our next-door neighbour came into the house  and did this while everyone was at the cemetery in Willesden. My mother uncovers  the mirror. She says only very superstitious Jewish people do that. She also says  continental people show their emotions, but we are English and we don&rsquo;t do  that. Mrs Eder is continental: she came here from Belgium. My mother&rsquo;s hero is Mr  Churchill but she votes Labour and stuffs manila envelopes with Labour Party  leaflets which we put through people&rsquo;s doors.&nbsp;</p>
                        <br>
            &nbsp;<br>
            &nbsp;<br>
            <strong>Blind Man&rsquo;s Buff</strong>
            <p>I am seven years  old. We are in the playground of my school, Henrietta Barnet, at the top of my  road, Middleway, which is between Northway and Southway. We are playing It.  Somebody caught me. Now it is my turn to be It. I rush around and grab  somebody&rsquo;s arm.&nbsp; <em>&nbsp;Now I am sixty years old. &ldquo;Naar hayiti vegam vekanti&rdquo;, as the  Psalmist says. Yes, once I was a boy and now I am old. We were trusted not to  cheat</em></p>
                       <br>
            &nbsp;<br>
            &nbsp;<br>
           <strong>Pleading Child</strong><br>
            &nbsp;<br>
            Daddy, I want to  go to a film this afternoon. <br>
           You can&rsquo;t. <br>
            Why not? <br>
            Because I said so. <br>
            &nbsp;<br>
            Mummy, daddy says I can&rsquo;t go to a film this afternoon. <br>
            Please make him change his mind. <br>
            I&rsquo;ll ask him. <br>
            Thank you, mummy.<br>
            &nbsp;<br>
            Daddy says you can&rsquo;t go to a film this afternoon.&nbsp; <br>
            Why not? <br>
            Because he says so. <br>
            It&rsquo;s not fair. <br>
            &nbsp;<br>
            Daddy, please let me go to a film this afternoon. <br>
            No. <br>
            Listen to the wireless. <br>
            Read a book.<br>
            &nbsp;<br>
            I wanted to go to a film this afternoon.<br>
            &nbsp;<br>
            &nbsp;<br>
            &nbsp;<br>
            <strong>Perfect Happiness</strong>
            &nbsp;<br>
            <p>I am ten years old. We have arrived at the house of my grandparents in  Stoke Newington, 47 Manse Road,   London N16, after driving here in  our little Standard car from our own house, 41 Middleway, London NW11. It is  Sunday afternoon. I run upstairs and kiss my grandparents, Josef Rudolf and  Fanny Rudolf. As usual, my grandfather is wearing his suit. His watch is in the  waistcoat pocket, at the end of a chain. My grandmother is dressed in black.  She is large, not like my other grandmother. I smell chicken soup. I go  downstairs and wander round the house. The house is tall and narrow. My  grandfather works at home. He deals in second-hand clothes and army surplus.  The ground floor is full of great coats and other items of men&rsquo;s clothing. The  basement is full of comics, published by my Uncle Leon Rudolf. One of them is  called <em>The Merrymaker</em>. I like looking at the advertisements: toby jugs,  packets of assorted stamps, magic charms. I go outside and throw my tennis ball  against a wall. Where I live there are no walls like that. Here there are even  shops close by! My grandfather speaks English with a funny accent. Sometimes he  speaks Yiddish. He gives me sixpence. It is time to go home.&nbsp; </p>
                         <br>
            &nbsp;<br>
            &nbsp;<br>
            <strong>An Important Event</strong>
            &nbsp;<br>
            <p>My little sister  Ruth is being fed the flesh of a tomato. I am given the pips and the skin. I do  not like the food. </p>
            <p><em>A vivid memory, indeed! The event must have happened  towards the end of 1946, when I was four and Ruth was one. I know the memory is  unmediated, because it contains the kind of details parents do not tell a  child. On the other hand, here is a mediated memory of something that may have  been a direct result of the tomato story: &ldquo;Ruth wants to attack me with a  fork&rdquo;. I do not remember saying that, but my words made a great impression on my  parents because I cannot remember a time when I did not know that I had spoken  them, and that security measures were taken to prevent Ruth attacking me&hellip; </em></p>
                        <br>
            &nbsp;<br>
            &nbsp;<br>
            <strong><em>Reverie</em></strong>
            &nbsp;<br>
            <p>I am thirteen years old. I am in the lounge of my house, sitting in my  mother&rsquo;s armchair. I have placed one of the 78s from my parents&rsquo; collection  onto the turntable of the large gramophone standing in the recess by the  fireplace. Today, I am listening to Beethoven&rsquo;s violin concerto played by  George Kulenkampf, which takes up eight sides, four records, five minutes on each side. </p>
            <p><em>Today, more than fifty years later, aged sixty four, I remember that the  music transported me -- as if through a magic tunnel or looking glass -- into  myself and, for five minutes, I was lost in complete reverie, summoned to a parallel universe where shoelaces and  toothbrush, ice cream and cod liver oil, did not exist. This was what I knew, and it was good. </em></p>
            <p>I turn over the 78.&nbsp;</p>
              <br>
            &nbsp;<br>
            &nbsp;<br>
            <strong>By the Fireside</strong>
&nbsp;<br>
            <p>I am eleven years old. I am sitting on the <em>pouffe</em> in the lounge of my house. I am staring at the fire. I see  clear images. Now and then, I stir the lumps of coal with a poker.&nbsp; </p>
            <p><em>Half a century later, I  remember that the fire drew me in like a swimmer between the waves of the sea,  except that is for real. Air only yields, earth only resists, water and fire  resist then yield. </em></em></p>
            <p><em>Deep feeling, yes: but what  about those images &ndash; intense in their clarity -- once fire then embers and  ashes, whose details are now forgotten? Ah, I begin to understand why, in early  old age, I am turning to fiction where I am allowed to invent or re-invent my  images.&nbsp; </em></p>
             <br>
            &nbsp;<br>
            &nbsp;<br>
            <strong>Rider on the Rocking Horse</strong><br>
            &nbsp;<br>
            <em>Upstairs lived my rocking  horse <br>
            Until I was too big to ride  him. <br>
            Then my sister Ruth took  over&nbsp; <br>
            And then my sister Mary rode  him,<br>
            And then my sister Annie  rode him.<br>
            &nbsp;<br>
            All the children rode him,  rode him,<br>
            Swaying forwards, swaying  backwards<br>
            And then he went into a  cupboard. <br>
            One day my mother threw him  out:<br>
            Off you go: the knackers  yard&nbsp; <br>
            Is the only place for you!<br>
            &nbsp;<br>
            Hold your horses, mother  dear, <br>
            Rocky&rsquo;s mine, he lives  forever <br>
            In </em>souvenance<em>. Those  ancient days&hellip;.&hellip;.</em> <br>
                      &nbsp;<br>
            &nbsp;<br>
            &nbsp;<br>
           <strong>Almost too serious</strong><br>
            &nbsp;<br>
            <p><em>Once upon a time &ndash; I cannot  place the year, but I was small -- I created a little box, where I placed my  most secret thoughts. Only I had access to this part of my mind.&nbsp; </em></p>
            <p><em>The event may seem remote,  the process abstract, but it is a scene from childhood, a primal scene. Vivid.  I was a good and obedient boy, and yet something in me knew I must find a place  where treasure was mine and mine alone. </em></p>
            <p><em>It was a matricial raid on  the future, preparation for the time when I would write my own thoughts under  the guise of being a translator.&nbsp; </em></p>
            <p><em>It was Montaigne&rsquo;s &ldquo;little  back shop&rdquo;. The little box survived, and so did I.&nbsp;</em>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </p>
             <br>
            &nbsp;<br>
            &nbsp;<br>
            <strong>Frightening</strong>
            &nbsp;<br>
            <p><em>My political understanding is shot, my ideals  have been destroyed, my belief that things can be improved -- never strong at  the best of times &ndash; is in the shredder. This is 1980, I am thirty-eight years  old. The world situation is almost too serious to confront. I take comfort from  the word &ldquo;almost&rdquo;, I almost wrote &ldquo;little word&rdquo;&hellip;. It contains the space where  hope breathes life into action. Some hope. At least.</em></p>
            <p><em>But I am older than I was. I  don&rsquo;t want to spend all my time involving myself in politics. My son is eight  years old, my daughter six. Over to you, Nathaniel and Naomi! This is a scene  from your childhood, not mine. This world is your world, this world is my  world, this world was made for you and me. </em></p>
             <br>
            &nbsp;<br>
            &nbsp;<br>
            <strong>Child Falling Asleep</strong>
            &nbsp;<br>
            <p><em>P. is dancing a slow. She is  holding her partner, cradling her partner, not her lover but her little  grandchild, Darcy. She is rocking Darcy, rocking her to sleep, as if the waking  energy of the child is to be contained in memory until the next time they dance  together. Dependent on her grandmother, Darcy does not move of her own accord. The  image, in my recollection, is of perfect safety, a glamour. No harm will come  to either of them. As they dance, they hold at bay the depredations of time.  Darcy is asleep. She is grazing safely in the pasture.&nbsp;&nbsp; </em></p>
                         <br>
            &nbsp;<br>
            &nbsp;<br>
            <strong>The Poet Speaks</strong><br>
            &nbsp;<br>
            <em>My son, my daughter<br>
            Alone by the sea<br>
            Run, run to the end<br>
            Of what they are.<br>
            &nbsp;<br>
            Nearby, I shiver <br>
            In the wet sand,<br>
            The sun also<br>
            Is at its nadir.<br>
            &nbsp;<br>
            The cold wind blows<br>
            Hard on my head. <br>
            I run, run to <br>
            The beginning<br>
            &nbsp;<br>
            Of what I am.<br>
            I raise up my eyes. <br>
            The lowly sun<br>
            Has disappeared.<br>
            &nbsp;<br>
            This place is called<br>
            <strong>The  headland</strong>.</em><br>

            <br><p><strong>Small</strong><strong> Town</strong><strong> Station, 1918-1920</strong></p>
            <p>Me and dreams  where futures bud<br>
              with  milestones-to-grind-for.<br>
              Not without  strain, me again<br>
              and the  irrefutable trial and error<br>
              of getting a life.<br>
              Toothbrushes<br>
              and toothpaste<br>
              to freshen up the  breath.<br>
              Shortcuts that  head straight<br>
              to stations and  pools of light. The blue<br>
              of columbine and  the blue of jacaranda.<br>
              One bar is set  lower, another higher.<br>
              Winds that swell  the stratocumulus<br>
              and winds that  sweep them away clumsy-like.<br>
              Stray cats and  dead alleys<br>
              in the tiny center  of town.<br>
              Sluggishness and  couldn&rsquo;tgiveadamnism.<br>
              Hmmm . . . how  strange to leave (and not to leave).<br>
              Hustle-bustle and  hush-hush.<br>
              Wrist movements  and digging in the heels.<br>
              Twenty to, a  quarter, ten.<br>
              Luggage on the  platform and cigarette butts on the ground.<br>
              Moral values and  security cameras. A whole<br>
              jumble of  incandescent faces and consumer<br>
              goods (which I  shouldn&rsquo;t like to be called that).<br>
              And, some day, you  and me.</p>
            <p><strong>&nbsp;</strong></p>
            <p><strong>&nbsp;</strong></p>
            <p><strong>East River</strong><strong>, c. 1920-1923</strong></p>
            <p>The greatest  influence<br>
              &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;my reading of <u>Siddartha</u><br>
              &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;exercised on me<br>
              &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;had much to do with rivers.<br>
              Ever since that  moment<br>
              &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;they acquired a meaning<br>
              &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;most profound and symbolic.<br>
              &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;For a while I&rsquo;ve played with the edge<br>
              of knowing we live  in a never-ending<br>
              &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;dream. I refer to an urban dream jam-packed<br>
              &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;with buildings, hydrocarbons, aromatic mint teas,<br>
              &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;textile factories, tanneries, and a river<br>
              that reflects the  essence of dusk and provokes<br>
              &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;a weird effect, cathartic and liberating. The city<br>
              &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;institutes laws and rivers are rebels that try their damndest<br>
              &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;to break them (although they rarely get away with it).</p>
            <p>Every river is a  type<br>
              &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;of dream that lies inside<br>
              &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;a more global dream of life,<br>
              &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;and flowing it resembles us:<br>
              descending from  pretty far away<br>
              &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;by fits and starts, it disseminates<br>
              &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;and embodies the spirit<br>
              &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;of what could have been.</p>
            <p>&nbsp;</p>
            <p>&nbsp;</p>
            <p><strong>Office at Night, 1940</strong></p>
            <p>They stayed at the  office, at night, alone,<br>
              and the tense  atmosphere<br>
              wherein they find  themselves plunges them<br>
              into uncertainty.  The workplace<br>
              is their habitat,  the substitute<br>
              for their homes.  Forces<br>
              suprapersonal  constrain<br>
              them both (this is  called not letting on).<br>
              Beyond the  slightest doubt,<br>
              shyness has taken  root in them.<br>
              After knocking  off, their cares are less intense<br>
              and before long  forgotten (Deo gratias).<br>
              Private people  suffering in private places<br>
              or, better than  places, in a warren<br>
              of white walls and  functional furniture.<br>
              Disturbed when  confronting the idea<br>
              of a night sky  filled with stars, the man,<br>
              the atomized and  lonely man,<br>
              clings to the  messiness of his desk.<br>
              The woman,  standing at the file cabinet,<br>
              fishes for  documents or something-or-other.<br>
              Sure enough, said  woman&rsquo;s dreams<br>
              and the man&rsquo;s  scruples converge<br>
              in a chronic  fiasco. Anxiety<br>
              invades her till  she loses<br>
              every point of  reference.</p>
            <p>This is the closed  circle<br>
              through which they  sink<br>
              from spiritual  withdrawal<br>
              to absolute,  physical solitude.</p>
            <p>&nbsp;</p>
            <p>&nbsp;</p>
            <p><strong>Rocky Seashore, 1916-1919</strong></p>
            <p>Some twenty meters  below,<br>
              the breaker lays  bare the tidal currents<br>
              that whip the  coastline<br>
              with waves  remote-controlled<br>
              smashing against  reefs<br>
              patches of  rubble-coarsened beach<br>
              and sea-stacks  that stand erect, like clenched fists,<br>
              offshore. Whoever  descends there,<br>
              gingerly picking  his steps, will bear witness--<br>
              from the  privileged spot on the rockbed<br>
              amidst a mass of  foamy curls--<br>
              to unique,  soothing perspectives<br>
              where the  hereafter and immediacy,<br>
              totality and  nothing correspond.</p>
            <p>Things start to  vaporize,<br>
              an incommensurable  plenitude<br>
              revives the senses  inside out<br>
              and for the first  time in your life<br>
              you aspire to  unconditional love<br>
              and transcendence.</p>

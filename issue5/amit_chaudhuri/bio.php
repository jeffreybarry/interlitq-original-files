                  <td class="texto2" valign="top"><p>Born in Calcutta,  India, in 1962, Amit Chaudhuri was brought up in Bombay. He graduated  from University College, London, and was a research student at Balliol  College, Oxford. He was later Creative Arts Fellow at Wolfson College,  Oxford, and received the Harper Wood Studentship for English Literature  and Poetry from St John's College, Cambridge. He has contributed  fiction, poetry and reviews to numerous publications including <em>The Guardian</em>, <em>The</em> <em>London Review of Books</em>, <em>The</em> <em>Times Literary Supplement</em>, <em>The</em> <em>New Yorker</em> and <em>Granta</em> magazine. His first book <em>A Strange and Sublime Address</em> (1991), a novella and a number of short stories, won&nbsp;the Betty Trask  Prize, the Commonwealth Writers Prize (Eurasia Region, Best First Book)  and was shortlisted for <em>The</em> <em>Guardian</em> Fiction Prize. His second novel <em>Afternoon Raag</em> (1993) won both the Southern Arts Literature Prize and the Encore Award  (for best second novel of the year). The novel adopts the metaphor of  Indian classical music, the raag, to evoke the complex emotions  displayed by the narrator, a young Indian student at Oxford. It was  followed by <em>Freedom Song</em> (1998), set in Calcutta during the  winter of 1992-3 against a backdrop of growing political tension  between Hindus and Muslims. The US edition of <em>Freedom Song</em> won <em>The</em> <em>Los Angeles Times</em> Book Prize (Fiction) in 2000. <em>A</em><em> New World</em> (2000) is the story of Jayojit Chatterjee, a divorced writer living in  America, and the visit he makes with his son Vikram to his elderly  parents' home in Calcutta. <em>Real Time</em> (2002) includes a number of short stories set in Bombay and Calcutta, some of which have been published in <em>The</em> <em>London Review of Books</em>, <em>The Times Literary Supplement</em> and <em>The New Yorker</em>, as well as 'E-minor', a memoir written in verse. <em>D. H. Lawrence and 'Difference': Postcoloniality and the Poetry of the Present</em>,&nbsp;exploring Lawrence's position as a 'foreigner' in the English canon,&nbsp;was published in&nbsp;2003. <em>St. Cyril Road and Other Poems</em> was published in 2005. Amit Chaudhuri is Professor of Contemporary Literature at the University of East Anglia. He is editor of <em>The Picador Book of Modern Indian Literature</em>, published in 2001. He is judging the Man Booker International Prize 2009. He is also a musician.</p>

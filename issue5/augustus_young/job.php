            <strong><br>
            The Albesia in Season            </strong>
            <p>When my flowering  tree comes into its own<br>
              the birds sing in  it for a day and a night.<br>
              O <em>folie  berg&egrave;re</em> of pink and white blossom,<br>
              fluffy boa  feathers. Honey is their delight. </p>
            <p>Mountain  swallows. No one sees them alight.<br>
              Little pecking  birds that drink to clear their throats,<br>
              carousing the air  with many-coloured notes.<br>
              When the source  has been sucked dry, the birds take flight<br>
  &nbsp;<br>
              and the blossoms  begin to fall. A dead cat bloats&nbsp;&nbsp; <br>
              with maggots  under the tree. Three days ago<br>
              it slept in the  grass, a pile of old coats.<br>
              It must have  crawled there to die. Marco Polo</p>
            <p>brought the corm  of an albesia back home.<br>
              A gift to Venice from his friend  Kubla Khan.<br>
              But in the  marshlands the plant could not be grown.<br>
              So it put down  its roots in North Catalan,</p>
            <p>where the green  fronded branches have the wingspan,<br>
              and the trunk the  torsion, with its knotted grain, <br>
              to bend, and not  to break, in the <em>tramontane </em><br>
              (ants feed off  the bark in this arid terrain).</p>
            <p>The ideal wood  for carving beasts and birds.<br>
              Flea markets in  the Far East are aflood with them.<br>
              The gift of the  albesia had been offered<br>
              by Kubla Khan to  fashion royal coffins.</p>
            <p>But no Venetian  doge would be buried in less<br>
              than a sacrophage  of flesh-absorbing limestone. <br>
              Wood was only  what burnt peasants to ashes.<br>
              A dead-loss,  thought Marco Polo, who&rsquo;d have known </p>
            <p>in Kubla Khan&rsquo;s  Cambaluc, a princess<br>
              was dug up after  two thousand years, flesh and bone<br>
              perfectly  preserved in a wooden compress.<br>
              Maybe the albesia  will be my own.&nbsp;&nbsp; </p>
            <p>&nbsp;</p>

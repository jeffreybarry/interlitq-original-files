    <td colspan="2" valign="top" class="texto2"> I am delighted to welcome you 
      to Issue 5 of <i>The International Literary Quarterly</i>.<br><br>Last November I launched the review, driven by my resolve that the best literature, from both the world of letters and that of academe, be showcased on the Internet. I was committed to ensuring that first-rate writing, complemented by outstanding artwork, be made available to all, at the click of a finger. Celebrating our first year, it is timely not only to take stock of what ILQ has achieved but also to consider our goals for the future, and these two strands turn out to be part of the same continuum. With quality as our touchstone, and wary of any manifesto, the review will continue to provide a forum for all genres and, in light of the fact that the editors are themselves writers, to nurture the publication of the finest contemporary literature.</td>

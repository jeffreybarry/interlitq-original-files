            <br><p><strong>Serenade </strong></p>
            <p>The female fruit fly cannot sing<br>
              but she can recognise<br>
              the warm vibrato <br>
              of a mate:</p>
            <p>a serenade<br>
              not issued from the tongue -<br>
              but from the high pitched trill <br>
              of hidden wings;</p>
            <p>if clipped<br>
              he grows invisible<br>
              to her. They can collide a thousand times<br>
              inside this glassy chamber,</p>
            <p>but caught <br>
              in unnatural silence<br>
              she is insensible,<br>
              and bears no fruit.</p>
            <p>&nbsp;</p>
            <p><strong>Pearl Diver</strong></p>
            <p>&quot;Young  women today don't like the sea as much as we do, they lack courage<br>
              and don't  want to get their skin darkened by working in the water&quot;, Kotoyo  Motohashi, Pearl Diver, or &lsquo;Ama&rsquo;.</p>
            <p>&nbsp;</p>
            <p>Upturned<br>
              fishing  boats -<br>
              cormorants<br>
              dry their  wings.</p>
            <p>An empty  tub<br>
              for  abalone,<br>
              cuttlefish,<br>
              or  octopus<br>
              settles  on the woodblock waves.</p>
            <p>One long  breath<br>
              and  Kotoyo descends,<br>
              like her  mother<br>
              and her  grandmother<br>
              before  her.</p>
            <p>Currents  stir the kelp;<br>
              water  murkens:<br>
              Kotoyo<br>
              can  barely see her hands -</p>
            <p>or the  knife<br>
              that  separates<br>
              sea-bed  from oyster shell.<br>
              She is  last of the Ama<br>
              of  Shirahama.</p>
            <p>&nbsp;</p>
            <p><strong>Mermaid</strong></p>
            <p>caught  alive<br>
              just off  the Shetland Isles,<br>
              two feet  in length<br>
              and  finned along the spine;<br>
              her tiny  hands<br>
              are  webbed, <br>
              the lower  portion<br>
              of her  breast<br>
              scales  into<br>
              a  slippery tail.<br>
              Only her  voice<br>
              escaped.</p>
            <p>&nbsp;</p>
            <p><strong>Kamasutra (the subsidiary arts)</strong></p>
            <p>To make designs<br>
              on courtyard floors<br>
              with ricepowder and  sand<br>
              is seventh of the sixty  four<br>
              arts to hold desire.<br>
              Cutting patterns<br>
              out of leaves<br>
              is number five.<br>
              Forty-third<br>
              teaching minah birds<br>
              and parrots how to  talk.<br>
              And if you master  repartee,<br>
              sign language, foreign  tongues,<br>
              and practice all these  arts of love -<br>
              there's no room left<br>
              for emptiness,<br>
              no time<br>
              for broken hearts.</p>

            <br><p><strong>Classical Landscape </strong></p>
            <p>Under justicing fires <br>
              purging a mortal mass of thought, <br>
              Christian mice and Demeter&rsquo;s piglet <br>
              squeak vaguely in the catacombs: <br>
              vanquished meridian hallows. <br>
              Icarian divinities of air <br>
              erect imagination&rsquo;s defeat. <br>
              A verminous blue despoils <br>
              you, your carnal hopes, who wait <br>
              for nothing, happy with <br>
              the sky&rsquo;s blank, among the stones <br>
              lizarding, the taut warm smells <br>
              of olive and manure: <br>
              prospecting no depth now, equalled. <br>
              Even as a tremendous wind invests <br>
              both sea and high places, you laud <br>
              pure boundaries, idea dithyrambs, <br>
              temple thrusts that balance <br>
              any cap of sky: the ground  absolute, <br>
              a firmament, a last resolution. <br>
              Who needs gypsy gods in dark  chambers? <br>
              This is a land without entrails. </p>
            <p>&nbsp; </p>
            <p><strong>Night Piece </strong></p>
            <p>Positing traces of nameless feet <br>
              amid feathery, overblown grass, <br>
              these hunters in imaginary fields <br>
              shiver to the margins of an image <br>
              and factor the dim light: sun  molecules <br>
              allow someone to walk there  fitfully <br>
              through dead orange and coughing  dust. <br>
              She walks east, her hair laughs,  golden <br>
              choke open, o, a moment. <br>
              As she crosses the sun, she is the  sun. <br>
              The bird of night streams upward  and paints<br>
			  the sky, her eyes; cloud-lidded <br>
              waves trail a reflected blood. <br>
              Where are her doves, or  equivalents? <br>
              Charred chariot of the underworld, <br>
              graduated sandals of the dusk? <br>
              Dew rises into a nondescript  presence. </p>
            <p>&nbsp; </p>
            <p><strong>Buenos    Aires </strong></p>
            <p>In childhood I lost a city like  this.<br>
              I walk up and down Lavalle and  Libertad: <br>
              crowds light up, eating, talking,  jostling. <br>
              At least I have purposes left over. <br>
              A Dulmenic painting steals my eyes <br>
              until I resemble a lizard again. <br>
              Fictions green and cold as Borges <br>
              flicker Incas. The saltimbanques<br>
              vanish and recompose: I too <br>
              will sacrifice the stone called my  heart. <br>
              Then a silly girl testing her  English <br>
              brings back on me an abandoned  thought, <br>
              the daughter I lost returns this  way, always. <br>
              Do I need this undying, exitless <br>
              memory? I line up for an ice-cream <br>
              while my heart continues shopping. <br>
              Voyeur, note well: woman and child  on the steps <br>
              of Our Lady of Victories. Note well <br>
              I almost passed that quiet hand by, <br>
              that waits as long as a summer&rsquo;s  day. <br>
              Note well: this <em>Pieta&rsquo;s</em> one hand holds <br>
              the child,&nbsp; the other begs  motionless. <br>
              Inside a large image of the living  Christ <br>
              bleeds transparently from its neon  heart. <br>
              My turn at last: I pay the spatula  artist <br>
              fifty cents in inflated <em>australes</em>, <br>
              take (note well) my <em>cucuracha</em> cone, <br>
              mouth it to a melt and drift away. </p>

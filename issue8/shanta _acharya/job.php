            <p><strong>Black Swans</strong></p>
            
            The highly expected not happening is also a Black Swan. *<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Nassim Nicholas Taleb

            
<P>Look into your own history &ndash;<br>
exile from family, friends, country,&nbsp; <br>
betrayed by those you placed your faith in,<br>
always living at the margins<br>
 &nbsp;<br>
Yet nothing, yes nothing in your past <br>
  pointed to the unfurling of the present.<br>
   &nbsp;<br>
What has been, what might have been <br>
  two fractured landmasses drifting in time<br>
  acquiring individual perspectives;<br>
  not parallel lines meeting in infinity.<br>
   &nbsp;<br>
You wander down lives never lived,<br>
  thankful your own plans were shred to bits<br>
  though not entirely reconciled to the  alternatives.<br>
   &nbsp;<br>
<em>Is  there free will</em>? you ask  as you sink<br>
  failing to swim against the tide; knowing <br>
  only dead fish  swim with the stream.<br>
   &nbsp;<br>
Immense possibilities only appear<br>
  to be evenly distributed; not knowing the  odds<br>
  we take risks, act against the gods.<br>
   &nbsp;<br>
There is no way of knowing what we don&rsquo;t  know.<br>
  No way of protecting us against  uncertainty,<br>
  success or failure is always retrospective.<br>
   &nbsp;<br>
We build theories like terracotta armies<br>
  fallible guesses from fragmentary  information.<br>
   &nbsp;<br>
Our limitations echo in memory<br>
  rearing up in dreams of the seemingly  impossible,<br>
our own lives, elegant black swans, in full  flight&hellip;.<P>
* In his book, <em>The Black Swan</em>, Nassim Nicholas Taleb  refers to an event with rarity, extreme impact, and retrospective  predictability as a Black Swan. According to him, &ldquo;a small number of Black  Swans explain almost everything in our world, from the success of ideas and  religions, to the dynamics of historical events, to elements of our own  personal lives.&rdquo;
<p>&nbsp;</p>
<p><strong>The Dream</strong></p>
<p>The road ends abruptly like an unfinished sentence,<br>
no sign-posts, no explanation; a dirt road<br>
washed away in the rain, in the middle of the night,<br>
in the middle of nowhere with no one to guide me  there.<br>
&nbsp;<br>
The landscape is luminous merging with a sky<br>
  showered with meteors, a celebration with fireworks.<br>
  &nbsp;<br>
I sit dazed in the car, locked like a jewel in a safe, <br>
  a garden enclosed, a well sealed up<br>
  waiting for someone to break into my solitude, <br>
  to disturb my peace, carry away the burden <br>
  &nbsp;<br>
Held secret all these years &ndash;<br>
  except I can no longer remember what it is.</p>
<p>&nbsp;</p>
<p><strong>Shaadi.com</strong></p>
<p>My ideal partner would be someone like my  self &ndash;<br>
preferably never married; will settle for  divorced <br>
or widowed with children but with the right  chemistry. <br>
Tall, dark, slim and romantic; must have a  GSOH. <br>
&nbsp;<br>
Kind, understanding, generous, loyal,  trustworthy,<br>
  goes without saying. Financially,  emotionally secure; <br>
  well-educated, thoughtful, a man of action  he must be.<br>
  Non-smoker, occasional drinker, must enjoy  good food;<br>
  but be prepared to eat anything unless he  wants to cook.<br>
  Must like reading, travelling, have a  passion for Life.<br>
  Religion? A Brahmin would be nice but not  essential.<br>
  &nbsp;<br>
I wrapped my expectations with a neat summation:<br>
  <em>Don&rsquo;t have a fixed idea of what I want, but know that</em><br>
  <em>when we meet, I will know and so will he...</em><br>
  &nbsp;<br>
Having spent one balmy summer evening  registering <br>
  I decided to cool off with a glass of  chilled wine<br>
  thinking of the beautiful boy doing his  best in time.<br>
  &nbsp;<br>
But I received a response straight away:<br>
  <em>No  matches found; change your criteria; be realistic</em>.<br>
  &nbsp;<br>
That my ideal man was not found was a blow I  confess. <br>
  It doesn&rsquo;t mean such a man does not exist<br>
  I reasoned pulling my self-esteem back into  harness.<br>
  He is simply not registered with this  agency!<br>
  &nbsp;<br>
Having determined to do something about it<br>
  I persisted in my pursuit of Mr Almost  Right.<br>
  &nbsp;<br>
Must try other agencies, cast my net wider;<br>
  mean while, no harm in checking out what&rsquo;s  on offer,&nbsp; <br>
  be less fussy, let Karma in on such a deep  matter &ndash;<br>
  &nbsp;<br>
Age: Doesn&rsquo;t matter; Marital status:  Doesn&rsquo;t matter.<br>
  Children: Doesn&rsquo;t matter; Country of  residence: Doesn&rsquo;t matter.<br>
  Height: Doesn&rsquo;t matter; Education: Doesn&rsquo;t  matter.<br>
  Religion: Doesn&rsquo;t matter; Occupation:  Doesn&rsquo;t matter.<br>
  I was inundated with suitors, crashing my  computer. </p>
<p>&nbsp; </p>
<p><strong>Easter Message</strong></p>
<p>Swathed in a dirty shawl,  embracing large bundles of carrier <br>
bags like dead infants in  her arms, she sits in the carriage <br>
indifferent to the  Underground destinations we tunnel through.<br>
&nbsp;<br>
She speaks to herself in a  language I fail to decipher,<br>
  her direct gaze into my eyes  reminds me of my mother.<br>
  &nbsp;<br>
Another commuter leans  forward as if to answer her questions,<br>
  thinking she asked for  directions; a foreigner lost in the Tube.<br>
  &nbsp;<br>
Life taught her never to  depend on the kindness of strangers...<br>
&nbsp;<br>
Her eyes are nomad, her  words nonsense. He listens, she smiles.<br>
  For an instant they try to  converse. <br>
  &nbsp;<br>
A refugee from Croatia, Kosovo or Albania, she is a  survivor.<br>
  We hear the words but do not  understand her story.<br>
  &nbsp;<br>
She pauses briefly and  chuckles on her fate<br>
  before she starts like a  scratched record all over again.<br>
  &nbsp;<br>
I get off at Victoria; the carriage  disgorges<br>
  itself and gets choked  again. She sits there oblivious<br>
  to stations and  announcements on the Circle Line<br>
  embracing her carrier bags,  dead infants in her arms.</p>
<p>&nbsp; </p>
<p><strong>Going Home</strong></p>
For <em>Doris Lessing</em>
&nbsp;<br>&nbsp;<br>

<p>&ldquo;<em>Look after her, she is your responsibility</em>&hellip;&rdquo;<br>
&nbsp;<br>
You hear a stranger&rsquo;s voice &ndash;  small, personal, urgent &ndash;<br>
  before vanishing; and you watch  over a child<br>
  miraculously brought into your  care.<br>
  &nbsp;<br>
Life mere briefing for a descent  into hell;<br>
  all are part of a larger rhythm  you can tell. <br>
  &nbsp;<br>
The sun lies at our feet in this  four-gated city. <br>
  The wind blows away our words, <br>
  not the prisons we choose to live  in.<br>
  &nbsp;<br>
Assume nothing, consider all sides;<br>
  things could easily have turned  out differently.<br>
  &nbsp;<br>
<em>Was it like this for you, too</em>? she enquires<br>
  recounting her childhood  experiences.<br>
  &nbsp;<br>
Growing up is a place where everything&rsquo;s  cracked up<br>
  nothing&rsquo;s permanent; no foundations  solid.<br>
  &nbsp;<br>
You look at things from different  angles,<br>
  lying in the grass singing, summer  before the dark...&nbsp;&nbsp;&nbsp;&nbsp; <br>
  &nbsp;<br>
Every survivor needs a memoir, a golden  notebook; <br>
  writing a habit of loving, like breathing.<br>
  &nbsp;<br>
All of us are shaped by war, twisted  and warped,<br>
  our inner battles spilling out  from under our skin; <br>
  &nbsp;<br>
Our body, the old chief&rsquo;s country,  landlocked &ndash;<br>
  like a proper marriage, a ripple  from the storm.<br>
  &nbsp;<br>
  Having known many homes, many  dreams <br>
  you learn finally to live with the  freedom of a spirit, <br>
  heart like a prairie field, open &ndash;<br>
  &nbsp;<br>
Not the dispossession of one shut  behind a door,<br>
  holding it ajar only to be greeted  by desperate, <br>
unceasing, inaudible  screams.</p>
<p>&nbsp;</p>
<p><strong>The Wishing Tree</strong></p>
<p>Children conspiring around the ancient <br>
tree trunk encircle it with their voices&nbsp; <br>
clamouring to cover branches with tinsel  &ndash;<br>
&nbsp;<br>
Calligraphically scripted dreams, <br>
  poems of love, coins at the bottom of a  well,<br>
  prayer flags at monasteries <br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;along foothills  of Himalayas.&nbsp; <br>
  &nbsp;<br>
A father hoists his son on his shoulders <br>
  tall as he leans to tie a prayer on a  wing;<br>
  &nbsp;<br>
Mother and daughter, hands outstretched<br>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;cast dreams  that spell the light:<br>
  &nbsp;<br>
<em>If  only I could walk </em><br>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<em>in the hearts of my enemies</em>&hellip;<br>
  &nbsp;<br>
<em>Let  father be able to speak again&hellip; </em><br>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<em>Let my sister regain her sight&hellip;</em><br>
  &nbsp;<br>
<em>Let  mother come home soon from hospital&hellip;</em><br>
  <em>Let  my brother who left home a year ago return&hellip; </em><br>
  &nbsp;<br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<em>Let there be peace in our world&hellip;</em><br>
  <em>Let  no child die of hunger &hellip;</em><br>
  &nbsp;<br>
<em>May  God hold us always </em><br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<em>in  the palm of his hand&hellip;</em>

            <p>&nbsp;</p>
            <p><strong>Jigsaw Puzzle</strong></p>
            <P>The pieces are in front of me over this wooden table,<br>
scattered forgotten sea shells on a rainy day,<br>
nonsensical in their arbitrary arrangement,<br>
laughing at me with irreverent pride.<br>
&nbsp;<br>
            A number so large no box could carry them&mdash;<br>
              some small as an ovum, others heavy as seas,<br>
              water images, forests, darknesses&mdash;And not one can be taken back.<br>
              They multiply every second,<br>
              leave the places where I need to see them,<br>
              shout for attention if I look away.<br>
              &nbsp;<br>
            The puzzle&acute;s in my dreams, and my dreams are in it:<br>
              there&acute;s no waking up to relief, no restful sleep, no<br>
              difference between night and day. Images<br>
              glued to my eyelids with fire, with agony, <br>
              with love&mdash;Mother holding me on a park bench,<br>
              my first day of school, a picnic by a river <br>
              where stones seemed covered by wool.<br>
              &nbsp;<br>
            How can you be the same one I can&acute;t bear now, Mother,<br>
              the same one I ran to with open arms on a Peruvian beach?<br>
              Are you what I see and nothing else, or is there more to you<br>
              than the words I despair to find and never do?<br>
              Why this unending table of blame between us?<br>
              And, tell me: have your eyes always been sad,<br>
              or is it only me needing your arms around me?<br>
              &nbsp;<br>
            I fit two pieces together, and then a third.<br>
              but in the meantime the others reproduce like cancer cells,<br>
              overflow the table, copulate in the air before me.<br>
              As it&acute;s been, it will be&mdash;<br>
              I&acute;m tied by memories to this chair<br>
              and only when all&acute;s puzzled out will I be set free,<br>
              move out of this dark house,<br>
              go back to that Sunday when we laughed together<br>
              and fear still didn&acute;t own my heart.<br>
              &nbsp;<br>
            If we could only rewrite our story, Mother.<br>
              If you could help me unravel the mess <br>
              from which I&acute;ve been made. But you&acute;re farther away <br>
              than the means I use to reach you&mdash;<br>
              Inch by inch you approach your death<br>
and I find no language  good enough to hold us both.
<P> </P>
            <p>&nbsp;</p>
            <p><strong>Rompecabezas</strong></p>
            <P>Veo las piezas en esta mesa de madera<br>
desparramadas como caracoles marinos olvidados  un d&iacute;a de lluvia,<br>
sin sentido en su disposici&oacute;n arbitraria,<br>
ri&eacute;ndose de m&iacute; irreverentes.<br>
&nbsp;<br>
            Tantas que ninguna caja alcanzar&iacute;a&mdash;<br>
              peque&ntilde;as como &oacute;vulos algunas; otras, pesadas  como el mar.<br>
              Im&aacute;genes de agua, bosques, oscuridades&mdash;Ninguna  puede ser devuelta.<br>
              Se multiplican cada segundo,<br>
              abandonan los lugares donde necesito verlas,<br>
              claman por atenci&oacute;n si me distraigo.<br>
              &nbsp;<br>
            El rompecabezas est&aacute; en mis sue&ntilde;os, mis sue&ntilde;os  en &eacute;l:<br>
              no hay despertar al alivio, descanso reparador,  diferencia entre noche y d&iacute;a.<br>
              Im&aacute;genes adheridas a mis p&aacute;rpados con fuego,  con agon&iacute;a, con amor&mdash;<br>
              mi madre alz&aacute;ndome en un banco de plaza,<br>
              el primer d&iacute;a de escuela,<br>
              un picnic junto a un r&iacute;o de piedras cubiertas  de lana.<br>
              &nbsp;<br>
            &iquest;C&oacute;mo puedes ser la misma ahora, Madre,<br>
              la misma hacia quien corr&iacute; con brazos abiertos  en una playa del Per&uacute;?<br>
  &iquest;Eres s&oacute;lo lo que veo, o hay algo m&aacute;s en ti<br>
              que las palabras que desespero por encontrar?<br>
  &iquest;Por qu&eacute; esta inagotable mesa plena de culpa  entre nosotras?<br>
              Y, dime: &iquest;acaso tus ojos siempre han sido  tristes,<br>
              o es s&oacute;lo mi mirada reclamando tu regazo?<br>
              &nbsp;<br>
            Encajo dos piezas y luego una tercera,<br>
              pero las otras se reproducen cual c&eacute;lulas  malignas,<br>
              desbordan la mesa, copulan en el aire sin  verg&uuml;enza.<br>
              Como ha sido, ser&aacute;&mdash;<br>
              Estoy atada con recuerdos a esta silla,<br>
              s&oacute;lo cuando todo encuentre su lugar estar&eacute; en  libertad,&nbsp; <br>
              podr&eacute; salir de esta casa oscura,<br>
              volver a ese domingo cuando re&iacute;amos juntas<br>
              y el miedo a&uacute;n no alimentaba mi alma.<br>
              &nbsp;<br>
            &iexcl;Si pudi&eacute;ramos volver a escribir la historia,  Madre!<br>
  &iexcl;Si pudieras ayudarme a descifrar el barro del  que nac&iacute;!<br>
              Pero est&aacute;s m&aacute;s all&aacute; de las manos que tengo para  alcanzarte&mdash;<br>
              Cent&iacute;metro a cent&iacute;metro te acercas a tu muerte<br>
              y yo no  hallo un lenguaje que baste para las dos.</p>
            <P> </P>
            <p>&nbsp;</p>
            <p>&nbsp;</p>
            <p><strong>Enemies Outside</strong></p>
            <P>Soft is his skin<br>
but softer his soul.<br>
How can he flee<br>
the claws of a tiger<br>
if the grass in the prairie<br>
scratches his knees?<br>
How can he survive killing deer<br>
if the warmth of the earth<br>
burns his feet when he walks?<br>
&nbsp;<br>
            No claws, no fur, no flight.<br>
              No horns, no speed, no shell.<br>
              Only a brain.<br>
              One thousand four hundred grams<br>
              and a whole universe will be born.<br>
              Stonehenge and Athens.<br>
              Jerusalem, Armenia and Sarajevo.<br>
              &nbsp;<br>
            But it&acute;s still too soon&mdash;time&acute;s only begun.<br>
              In a cave he has made warm<br>
              a bison&acute;s drawn on the wall.<br>
              A soft tune comes from the shadows,<br>
              words yet to be born.<br>
              &nbsp;<br>
            Meanwhile, by the fire<br>
              a dog has decided to sleep.</p>
            <P> </P>
            <p>&nbsp;</p>
            <p><strong>Enemigos Afuera</strong></p>
            <P>&iquest;C&oacute;mo escapar <br>
de las garras del tigre<br>
si la hierba del prado<br>
lastima su piel?<br>
&iquest;C&oacute;mo seguir la caza del ciervo<br>
si el calor de la tierra<br>
quema sus pies al andar?<br>
&nbsp;<br>
            No tiene garras, ni pelaje, ni cuernos.<br>
              Carece de gracia, armadura, vuelo.<br>
              S&oacute;lo un cerebro.<br>
              Mil cuatrocientos gramos<br>
              alumbrar&aacute;n un universo entero.<br>
              Stonehenge y Atenas.<br>
              Jerusal&eacute;n, Armenia y Sarajevo.<br>
            Pero todav&iacute;a es pronto: el tiempo acaba de  empezar.<br>
              En la cueva que &eacute;l ha calentado<br>
              hay un bisonte trazado en la pared.<br>
              Una melod&iacute;a suave viene de las sombras,<br>
              las palabras a&uacute;n est&aacute;n por nacer.<br>
              &nbsp;<br>
            Mientras tanto, junto al fuego,<br>
              un perro decide dormir.</p>

          <td colspan="4" rowspan="2" valign="top" class="texto2">  
            <br><p><strong>DNA</strong></p>
            <P>The boy has never seen his father sleep<br>
but he sleeps in his same awkward position&mdash;<br>
lying on his side, arms stretched to the back,<br>
hands interlaced, at ease in his dreams.<br>
&nbsp;<br>
            Struggling with the fragments of an eight-piece puzzle,<br>
              the sounds he makes are the same I heard years ago<br>
              each time his moody father tried fixing the toilet, a sink, <br>
              the axolotl&acute;s aquarium&mdash;in vain.<br>
              &nbsp;<br>
            I remember him sitting on the floor of the house we&acute;d built<br>
              staring at the morning sky without a blink,<br>
              or lying in bed, his stubborn gaze glued to the ceiling<br>
              as if stories would be told there all day long.<br>
              &nbsp;<br>
            I wanted to have a child, I said, to have him twice <br>
              for me, to feel him grow inside myself, from myself,<br>
              to have another version of the man I loved,<br>
              defenceless upon my breast.<br>
              &nbsp;<br>
            Multiplying cells ad infinitum, repeating patterns, gliding <br>
              into one or two unpredictable errors,  his kindred <br>
              occurred within me, displacing my soul&acute;s spotlights <br>
              without permission, revolving the scenery of life <br>
              from self-absorption to Disney lullabies and pacifiers.<br>
              &nbsp;<br>
            Now, as I watch my child carefully putting<br>
              one plastic dinosaur after another, I remember <br>
              his father saying that from his childhood games<br>
              making endless columns of grey lead soldiers<br>
              was among all his favourite.<br>
              &nbsp;<br>
            And I wonder if extravagant sleep positions,<br>
              row-obsessiveness, and that odd amphibian fondness,<br>
              are things his DNA has delivered  to my son,<br>
              or if maybe it&acute;s not heredity, but God<br>
              doing this for me&mdash;making the child act just like his father<br>
              so that, in a way, he&acute;s still with me. <br>
            <P> </P>
            <p>&nbsp;</p>
            <p><strong>ADN</strong></p>
            <P>El ni&ntilde;o nunca ha visto dormir a su padre<br>
pero duerme en la misma posici&oacute;n inc&oacute;moda que  &eacute;l,<br>
apoyado en un costado, los brazos estirados  para atr&aacute;s,<br>
las manos entrelazadas, como un faquir.<br>
&nbsp;<br>
            Empe&ntilde;ado en armar un rompecabezas de ocho  piezas<br>
              los ruidos que hace son los mismos que escuch&eacute;  hace a&ntilde;os<br>
              cada vez que su padre destemplado intentaba  arreglar<br>
              el inodoro, la pileta o el acuario del ajolote,  en vano.<br>
              &nbsp;<br>
            Lo recuerdo sentado en el piso de la casa que  hicimos juntos<br>
              mirando el cielo de la ma&ntilde;ana sin parpadear,<br>
              o acostado en la cama, adheridos los ojos a una  pared,<br>
              como si ah&iacute; habitara el ectoplasma que lo  visit&oacute; una vez.<br>
              &nbsp;<br>
            Quer&iacute;a tener un hijo, le dije, para tenerlo a  &eacute;l dos veces,<br>
              para sentirlo crecer de m&iacute;, dentro de m&iacute;, para  tener otra versi&oacute;n<br>
              del hombre amado, indefenso sobre mi pecho.<br>
              &nbsp;<br>
            Al fin su estirpe ocurri&oacute; en m&iacute;, multiplicando<br>
              c&eacute;lulas infinitas, repitiendo patrones,<br>
              cayendo en uno o dos errores imprevisibles,<br>
              modificando los acentos de mi alma sin permiso,<br>
              reemplazando un decorado austero<br>
              por tiras bordadas, pa&ntilde;ales, noches en vela.<br>
              &nbsp;<br>
            Ahora, veo a mi ni&ntilde;o poner interminablemente<br>
              un dinosaurio pl&aacute;stico tras otro, y recuerdo a  su padre<br>
              cuando dec&iacute;a que encolumnar soldaditos de plomo<br>
              era de todos los juegos de infancia su  preferido.<br>
              &nbsp;<br>
            Y me pregunto si las extravagantes posiciones  de dormir,<br>
              la obsesi&oacute;n con las filas y esa extra&ntilde;a  afinidad por los anfibios,<br>
              son cosas que su ADN transmiti&oacute; a mi hijo, <br>
              o si no son las leyes hereditarias, sino Dios<br>
              haciendo esto por m&iacute;: que el ni&ntilde;o sea como su  padre<br>
              para que, as&iacute;, &eacute;l a&uacute;n est&eacute; conmigo.</p>
            <P> </P>

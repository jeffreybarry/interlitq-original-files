            <br><p><strong>Lost</strong></p>
            <P><em>Swing  low</em>, she  sings, <em>sweet&hellip; </em>But the word is lost<br>
along  with so much else: the lilting ease<br>
of  the voice she&rsquo;d use to lull me when I woke<br>
howling  from childhood nightmares; the irreverent<br>
sense  of humour; the mass of auburn hair <br>
piled  high, untidily. <br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;She claws her scalp,<br>
swivels  slowly to the open window. &lsquo;Are you<br>
cold?&rsquo;  I ask. <em>Yes, called </em>&ndash; I draw the shawl<br>
around  her shoulders &ndash; <em>what are you called? </em>&lsquo;I&rsquo;m Karen,<br>
your  daughter, Karen.&rsquo; <em>I knew you once</em>, she murmurs,<br>
<em>before  all this</em>. She  spreads her hands, a vague <br>
inclusive  gesture taking in the day-room&rsquo;s<br>
blank  walls and sticky floors, the inexpressive<br>
scrutiny  of her fellow-patients. <br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;  Silence,<br>
her  lean jaws working; then <em>They hear your  thoughts</em><br>
she  says abruptly. <em>I&rsquo;d like to have them with me</em><br>
<em>for  company. </em>I  catch her riddling drift<br>
before  she&rsquo;s finished speaking. &lsquo;The cats,&rsquo; I tell her,<br>
&lsquo;are  better where they are.&rsquo; She lifts her head,<br>
stares  out across the sunlit garden. <em>All</em><br>
<em>that  water</em>, she  says. <em>I hope the river doesn&rsquo;t&hellip;</em><br>
&lsquo;Rise  any higher?&rsquo; I supply the phrase,<br>
snared  in the loop of repetition, knowing <br>
there  is no river, only a pathway winding<br>
away  between the flowerbeds. <em>I can&rsquo;t see why</em><br>
<em>they&rsquo;d  build so close.</em><br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;She wriggles  in her chair,<br>
leans  sideways, drops her voice: <em>They keep me here</em><br>
<em>because  of what I did</em>.  The vase. She&rsquo;s thinking<br>
of  the heirloom vase, the gleaming fragments scattered<br>
across  the parlour floor. <em>So many pieces</em><br>
<em>you  couldn&rsquo;t &hellip;&lsquo;</em>Let  it go,&rsquo; I whisper, reaching<br>
for  her dithering hand. <em>The only time he ever</em><br>
<em>smacked  me.</em> Let it  go. <em>It&rsquo;s for my own</em><br>
<em>good.  They&rsquo;ll keep me here until I&rsquo;ve learned</em><br>
<em>my  lesson. Then Daddy will come</em> &ndash; &lsquo;He died,&rsquo; I say<br>
gently,  as always, &lsquo;before the war&rsquo; &ndash; <em>and take me &hellip;</em> <br>
&nbsp;<br>           
            &lsquo;Home,&rsquo;  I prompt at last. <em>Yes. Take me home.</em></p>
            <p>&nbsp;</p>
            <p><strong>When</strong>
            <p>When the days  shorten and the ash-twigs, flexing <br>
beyond your window  like the hands of girls <br>
in a young man&rsquo;s  dream, invite you from your hearth <br>
you&rsquo;d best not go.  I might put that more strongly <br>
but you&rsquo;ll leave  regardless, turning up your collar <br>
against the  needling rain and stepping out <br>
as if for a Sunday  morning walk, your eyes <br>
fixed on the grey  horizon. And where the footpath <br>
divides you&rsquo;ll  take the lesser branch, a wavering <br>
thread of deeper  green among the bleached <br>
tussocks, and start  to climb.<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp;  &nbsp; &nbsp;  &nbsp; &nbsp;Each time you pause<br>
to catch your  breath, you&rsquo;ll wonder what you&rsquo;re doing <br>
out on that  windswept slope alone, the buzzard&rsquo;s <br>
disconsolate  warning ringing in your ears, <br>
but you&rsquo;ll go on  climbing, making for the ridge <br>
and the land  beyond it.<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;As you crest the rise<br>
you&rsquo;ll see it like  a map &ndash; the abandoned pastures <br>
dropping away  beneath your feet, the rills <br>
in spate, the  shine off bogland; straggling lines <br>
of thorn,  untrimmed for years, marking the old <br>
forgotten holdings;  and far below, the lake <br>
so viciously  belaboured by the wind <br>
the spray peels  off in skeins.<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;  If turning back<br>
were an option,  that would be the moment &ndash; there <br>
where the track  gives out, your unimpeded gaze <br>
raking the  desolate acres &ndash; to break away, <br>
retrace your  footsteps; but you&rsquo;ll find yourself <br>
pushing grimly  onward, slantwise to the long <br>
fall of the land,  stumbling, frustrated, searching <br>
for something  else.<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; And it won&rsquo;t look like home &ndash; <br>
not yours nor  anyone&rsquo;s &ndash; that ragged terrace <br>
hacked from the  hillside, the ruinous wall enclosing <br>
what must have  been a garden; the low, drab house <br>
at the garden&rsquo;s  further end, a block of thickened <br>
shadow, its  windows blank. But when you unlatch <br>
the peeling  wicket, forcing it roughly back <br>
against its own  resistance and the tangle <br>
of bramble-stems,  some inward jolt or tremor <br>
will tell you  you&rsquo;ve arrived.<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Among the seeded<br>
spires of  fireweed, as the wind&rsquo;s voice sinks<br>
to a brittle  whisper and your rioting heart <br>
quietens, you&rsquo;ll  hunker down and lift your face <br>
to the clearing  sky, the little crescent moon <br>
fine as a flake of  ice or glass; rock back <br>
on your heels and  cry. If you don&rsquo;t know by then <br>
why you shouldn&rsquo;t  be there, huddled in the angle <br>
of the crumbling  wall while daylight fades and finches <br>
in chittering  flocks sift through the air to settle <br>
in the leafless  hedgerows, you will in time: perhaps <br>
when darkness  falls and the grasses at your feet <br>
grow stiff with  frost, perhaps a little later <br>
as you press  between the untended laurels, driven <br>
by cold or  loneliness or something sharper <br>
even than these,  towards the lightless house.            

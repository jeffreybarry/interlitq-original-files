          You were impatient <br>
            with my inconsequence, <br>
            and for the five years <br>
            we sat side by side<br>
            believed I was  sacrificing thought <br>
            for approval. <br>
            Of course I was. <br>
            Still, you were right and  wrong &ndash; <br>
            applause acts like love <br>
&nbsp; <br>
            but music&rsquo;s a discipline<br>
            pure and impersonal as  calculus&hellip;<br>
            For years we made a  reluctant pair, <br>
            and it occurred to  neither of us<br>
            to articulate the grace  we found in form <br>
            or in the release it  offered, to something promised &ndash; <br>
            in fact, our selves &ndash; <br>
            that seemed real and  pressing as an odour <br>
            in those days<br>
&nbsp; <br>
          when I misread your  passion &ndash; <br>
            not realising maths <br>

              <p><strong>None Too Soon</strong> <br>
                                        &nbsp;<br>

              To be located in memories without precedent.  That&rsquo;s the necessary antidote to d&eacute;j&agrave; vu where so many syllables have been  squandered in pliant confirmation. The more non-existent of the gods are the  only ones counting your blunders. Hard to forget. What of the yearning mind, or  thoughts that so intricately deform the musculature of a smile. The only  worthwhile wish of which I&rsquo;m currently aware is to construct a logical  space-time bracket in which all the animals are sometimes dreaming.</p>
            <p>&nbsp;</p>
            
            <p><strong>The Ventriloquist&rsquo;s Dilemma</strong> <br>
                          &nbsp;<br>
              Bird song entered our words and left with  migratory echoes insufficiently dispersed. We weren&rsquo;t designed to perceive most  of what surrounds us or to fully understand the rest. Maybe it&rsquo;s true that  differential equations drove the teenager off the road. The self-propagating  slope remains unhindered in its x-y axis. It&rsquo;s really difficult to find the  language to say these things rigorously. Sound waves break on the shore and  make one feel unwelcome. And too, there&rsquo;s that conspicuous absence of real  metaphors in nature. Sorry, meant to say, there&rsquo;s that conspicuous absence of  real nature in metaphors. Someone will always claim night flew into a tree. The  placement of those words in a line.</p>
            <p>&nbsp;</p>
            <p><strong>The Long And Short Of It Thought Experiment</strong> <br>
                          &nbsp;<br>
             Feed long and short beaked pigeons the same food, same food. Exercise  long and short legged quadrupeds in the same, same manner. Expose long and  short-haired sheep to the same climate climate. Then and only then, direct  every time-line toward the same set of nesting horizons. Can we go on this way?  One step takes longer than anyone ever thought it would and is still hovering  in the air, the shadow is getting longer, the whole earth is growing hot and  cold, hot and cold. Let any long and short lifespan equal the same function of  x divided by zero. We can now observe that the results of these and other  experiments are adding up to an old geometry of the tragic spectrum&mdash;the more  the terror, the less the pity. That&rsquo;s the long and short of it.</p>
                          <p>&nbsp;</p>

  <p><strong>Fierce Love Story</strong> <br>
                &nbsp;<br>
            To start with a taxonomic  impediment and yet go on. Looking always for news of another kind. Great  saturated patches of color stall in their rumble toward the horizon.  Squeeze-tube dearth full of biblical pornography. Sit back and watch in awe as one  sophisticated critter eviscerates another on the color-coded screen. <em>No greater love</em>, etc. Much too bright or  not enough to be convincing descriptions of nature. For the unconvinced, then,  the following three consolations: Sonorous cowboys hitch up primate dungarees.  To restore the comfort of silence is the role of objects. A little girl wanders  in and out of the color field just beyond our grasp.</p>

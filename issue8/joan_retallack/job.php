              <p><strong>Elliptical  Ice Terriers</strong> <br>
                                        &nbsp;<br>
              Just as the blades describe an elegant thicket  of parallel curves I will be your personal pronoun for the duration of this  sentence. You will be my personal pronoun for the duration of this sentence.  Exquisite parallels all about remaining in motion. Poetries and theories of  everything propel no-hands bicycle arias. More radical alterity, more  biomimicry, more blood sausage fealty, explosions of sweetest beauty. My child,  why repeat what some poet or another has already&mdash;<em>we live our lives forever taking leave</em>&mdash;when  a custom-fitted speech act can be yours within 24 hours. With it you can  perform rites and ceremonies, weddings, baptisms, extreme unctions, forgiveness  of sins, funerals; visit correctional facilities, start your own church.

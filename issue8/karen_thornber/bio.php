                  <td class="texto2" valign="top"><p>Karen Thornber is  Assistant Professor of Comparative Literature in the Department of Literature  and Comparative Literature at Harvard University. Her primary  areas of research and teaching are world literature and the literatures and  cultures of East Asia. Professor  Thornber received her Ph.D. from Harvard&rsquo;s Department of East Asian Languages  and Civilizations in 2006, and in 2007 she was awarded the American Comparative  Literature Association&rsquo;s Charles Bernheimer Prize for the Best Dissertation in  Comparative Literature in North   America. Her <em>Empire  of Texts in Motion: Chinese, Korean, and Taiwanese Transculturations of  Japanese Literature </em>(Harvard 2009) is the first book in any language to  analyze the complex relationships among the imperial Japanese, semicolonial  Chinese, occupied Manchurian, and colonial Korean and Taiwanese literary worlds  (1895-1945). It is based on extensive  vernacular archival research in China, Japan, Korea, and Taiwan. Her second  monograph, scheduled to be completed later this year, is titled <em>Intertwining Bodies and Bodyscapes</em>: <em>Environmental  Crises and East Asian Literatures</em>; this book analyzes East Asian literary  negotiations of human/nonhuman interactions from premodern times to the present  in the context of world literature on environmental crises. Her third monograph, also well underway,  explores postwar (postcolonial) intra-East Asian cultural negotiation in the  context of late twentieth- and early twenty-first century global cultural flows. Professor Thornber has articles/chapters  published and forthcoming in a variety of venues, including <em>The Journal of Asian Studies</em>, <em>Contemporary French and Francophone Studies</em>, <em>PAJLS, </em><em>Japan</em><em> Focus</em>, and a number of edited volumes. Now in her second year on the faculty at  Harvard, she has taught undergraduate and graduate courses on Literature 
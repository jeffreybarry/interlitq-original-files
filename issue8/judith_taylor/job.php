            <p><strong>Investigators' Notes</strong></p>
            <P>Hooded men, they told us, well above the  ground <br>
on the far side of the hedge, and moving a strange  way <br>
&ndash; not walking, not on bikes. <br>
City boys, they had never seen a man on  horseback:<br>
&nbsp;<br>            
            it was weird, they said,<br>
              and they left the Muir as quietly as they could.<br>
              We hardly needed to ask them where<br>
  <em>&ndash; beside  that old stone, the one with the funny writing.</em><br>
  &nbsp;<br>
            And they seemed to us to be genuinely  spooked.<br>
              I suppose the village kids<br>
              might have put them up to it &ndash; but then,<br>
              how much would the village kids know?<br>
              &nbsp;<br>
            All this time, and nobody talks about <br>
              the religious wars, or the ambush <br>
              on the east road. The government&rsquo;s man;<br>
              his daughter, who survived it;<br>
              &nbsp;<br>
            or the men who rode the carriage down,<br>
              who refused to answer the court when they  were taken<br>
  &ndash; unabashed, as if <br>
              it was something to the greater glory.<br>
              &nbsp;<br>
            In the end, it was their side <br>
              who won. And on the Latin stone<br>
              at the scene, they&rsquo;re named as martyrs.</p>
          <p>&nbsp;</p>
          <p><strong>Late Saturday morning, December</strong></p>
          <p>The light is low and sidelong.<br>
It warms some of the grey flowers in the  curtains<br>
pink and yellow, but only some.<br>
&nbsp;<br>
          We can see a sliver of white sky <br>
            and we know outside the frost is lying <br>
            undisturbed in places daylight hasn&rsquo;t been  for days <br>
            &nbsp;<br>
          &ndash; in corners, under edges, behind trees.<br>
            It&rsquo;s pale, with a sheen like finery.<br>
            It&rsquo;s all set to unbalance us.<br>
            &nbsp;<br>
          We wish we had held a bonfire on the  shortest day<br>
            and told the sun how we loved <br>
            her brassy, forward style. <br>
            &nbsp;<br>
          Now we wait as she sidles - hunched,  hung-over &ndash; <br>
            around the edges of her stamping-ground,<br>
            smoking a fog, trying <br>
            &nbsp;<br>
          not to meet the public eye.<br>
            We hope she&rsquo;ll be back again;<br>
            that nothing will keep our red-hot mama  down.<br>
            &nbsp;<br>
          But for now we lie long in our little  warmth, reluctant<br>
            to commit ourselves to the smooth world<br>
            she wishes she had not embraced:<br>
            &nbsp;<br>
          the suave, pearly surfaces that tempt us on<br>
            to take another step, the one<br>
            that betrays us.</p>
<p>&nbsp;</p>
          <p><strong>Transworld</strong></p>
          <p>From a dream:  Strathmore<br>
is colonised by  Australians<br>
of Scots descent.  A taciturn folk<br>
who keep  themselves to themselves:<br>
&nbsp;<br>          
          and yet at any  turn of the road now<br>
  &ndash; in the middle of  berryfields, grain, <br>
            dairy pasture &ndash;  you might happen <br>
            upon a cattle-station,<br>
            &nbsp;<br> 
          tin roofs<br>
            and white  verandahs shadeless<br>
            under its bony  eucalyptus trees.<br>
            It is trapped in a  well of its own<br>
            &nbsp;<br> 
          harsh conditions,  the only dust for miles<br>
            clagging it, and  the air shimmering<br>
            over it, and nowhere  else.<br>
            It ignores you.<br>
            &nbsp;<br> 
          The lean dogs and  the fierce<br>
            recalcitrant  horses never stir.<br>
            If you watch for  long enough,<br>
            then maybe you&rsquo;ll  see<br>
          &nbsp;<br> 
          the farm woman, in  cotton frock and apron,<br>
            move behind the  screens;<br>
            or the man, shaded  under his hat, emerge<br>
            and ride away.<br>
            &nbsp;<br> 
          But they never  greet you<br>
            and they&rsquo;re never  seen <br>
            about, in the sway  of barley, down<br>
            the raspberry  lines. They don&rsquo;t remember<br>
            &nbsp;<br> 
          what it is<br>
            to have near  neighbours: fading through,<br>
            they work their  distant, underlying Australia <br>
            where the herds  scatter in scrub<br>
          &nbsp;<br> 
          as far as the red  horizon.<br>
            What could they  have to say to you?<br>
            Could they tell  you how they were fetched<br>
            here, their hard  lives intruded<br>
            &nbsp;<br> 
          into this cold,  suspiciously lush place<br>
            in the old  country? Or how you <br>
            appear to them: a  mirage, perhaps,<br>
            they have more  sense than to follow?<br>
            &nbsp;<br> 
          No telling<br>
            which of you  sought the other out, or where<br>
            this is, that your  eyes almost meet<br>
            again and again.  Is it your dream, or their own?</p>

<p>Having retired from a professional career in medicine, he now devotes himself to work as a writer. His first collection of poems, <em>Fluttering Hands</em>, was published in 2008, followed by <em>Things Hard for Thought</em> in 2013.   His poems have appeared in the <em>London Review of Books, The Rialto, Magma, Stand, Poetry Durham, The English Review, The Reader, The New Welsh Review, Poetry News</em> and other magazines and anthologies. In 2013 he published a novella, <em>Sonata in Four Movements</em>. </p>


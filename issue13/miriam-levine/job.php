            <p><strong>Azucar</strong></p>
            
            <p>&ldquo;Look!&rdquo; You say.  The gorgeous<br>
            lily has &ldquo;opened its face.&rdquo;<br>
            Stamens dangle, a pistil bunches<br>
            blue at the tip—a rush of pink.</p>
           
            <p>More to my taste are half smiles,<br>
            folded clusters, silky pistils <br>
            still hidden among crinkled petals<br>
            veined with celadon and cream.</p>
           
            <p>It&rsquo;s reasonable for such lilies<br>
            to remind me of the man who sang<br>
            in Spanish on Sixth Street.  Relaxed,<br>
            assured, upright he tilted his head </p>
            
            <p>to hear his own music.  Bud-lips half&#8211; <br>
            parted floated the color of cognac.<br>
            It makes sense to mention the little<br>
            Spanish I know that was not in his song.</p>
            
            <p>Lily is <em>azucena</em>.  Sugar<br>
            <em>azucar</em>, <em>leche</em> slang for sperm. <em> Alma</em>&#8212;<br>
            you must know&#8212;soul.  What was in his pocket?  Surely I hope<br>
            a key without which we&rsquo;re homeless, tumescence, silver, bandana . . . </p>
            <p>&nbsp;</p>
            
            <p><strong>Spring Break</strong></p>
            
            <p>On Sixth and Ocean students drink<br>
            from glasses big as fishbowls, tinted </p>
          
            <p>neon.  Other kids gouge their names<br>
            in sand, the names of cities too, the span</p>
            
            <p>of a grave, but there’s no gruesome<br>
            birth date followed by a hyphen—</p>
           
            <p>mortal thoughts of aging me.<br>
            Birth, death, beginning, end: </p>
            
            <p>I would guess 1993-2082<br>
            given the life expectancy</p>
           
            <p>of youth clawing with sticks,<br>
            toy shovels, perfect fingers</p>
            
            <p>or their feet. <em>Addie Rose, Newark</em><br>
            carved deep at the tide mark.</p>
           
            <p><em>Rush, rush</em>, say the waves<br>
            but Addie has to wait</p>
            
            <p>for the tide’s slow ruin.<br>
            Crumbling ruffles go down.</p>
            
            <p>Capital O fills like a moat—<br>
            sandcastles, sunken towers,</p>
            
            <p>doorways flooded.  Kingdoms, <br>
            bits of shell, names, cities all drowned.</p>
           
            <p>A few damp lumps<br>
            like hot wax piling up.</p>
           
            <p>Only a ripple runs across<br>
            Addie’s toenail painted blue.</p>


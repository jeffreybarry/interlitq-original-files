            <p><strong>The Lesson</strong></p>
           
            <p>We all know stories of people who’ve turned into things <br>
            like trees, who woke up as an insect or a bear,<br>
            a river or a whole field of flowers.<br>
            And of course we’ve heard stories of people turned to ashes<br>
            and snow—snow falling, snow covering the ground<br>
            in deep drifts we could tunnel through, almost disappearing there.</p>
            
            <p>One winter the snow was so deep in our town<br>
            we had to climb out our windows and up <br>
            to the surface, a vast expanse with just <br>
            the top branches of a few tall trees sticking through. </p>
            
            <p>If we fell through the crust, we might tumble through the white<br>
            too deep to climb back out. There were birds in mid-flight there<br>
            and dogs standing still, as though the snow had caught them<br>
            in a flash. But when the snow melted, years later,</p>
            
            <p>everything returned to normal, though the rivers <br>
            were swollen at first with animals and debris. <br>
            There were ponds in the woods for a few weeks; they became <br>
            fields of flowers when they vanished, full of buzzing bees <br>
            which taught us something else, something harder.</p>
            <p>&nbsp;</p>
            <p><strong>Nectar</strong></p>
            
            <p>I was dozing in a hammock on a gorgeous afternoon,<br>
            dreaming I was doing what I was doing, <br>
            knowing I’d forget my dream when I woke, <br>
            and sensing I’d wake soon. Small birds kept landing</p>
            
            <p>on my body, no larger than my thumb, yellow <br>
            and blue birds, black birds trimmed with rust.<br>
            They smelled like baby powder without the dust,<br>
            or jasmine at dusk. Each took something from me </p>
            
            <p>as I dozed: first the buttons of my shirt, one after <br>
            the other; then the ring my wife had given me; <br>
            then the pens in my shirt pocket, and then the shirt itself—<br>
            they took the loose threads in their beaks and flew up </p>
            
            <p>into the trees. And when they’d unraveled<br>
            my clothes they started to peck at me, so gently <br>
            I didn’t wake, and I didn’t wake when my wife came out <br>
            and found me, just a head without ears by then </p>
            
            <p>and only half my body lying there, still asleep. <br>
            And she spent the rest of the afternoon searching <br>
            for all my parts up in the trees and putting me <br>
            back together while I slept, so I’d never notice. </p>
            
            <p>Then she lay down beside me in that hammock, and slept.<br>
            until I woke and hugged her, blessed <br>
            my good luck, hardly knowing the least of it, and listened <br>
            to the birds, the hummingbirds buzzing through the afternoon,<br>
            searching, as always, for nectar.</p>
            <p>&nbsp;
            </p>
            <p><strong>Birds from all the Days You’ve Lived</strong></p>
            
            <p>The wind drew its long hair across the bedroom floor,<br>
            but the bedroom was outside now, as the houses were scattered<br>
            and the floors of the houses were splintered, and the windows<br>
            were out there in the field, where windows are nothing <br>
            but the wind, whose long hair had been dyed gray<br>
            by the rivers it had moved through. And so we could no longer <br>
            read in the dim light with a cup of tea.</p>
            
            <p>And so we could no longer listen to music<br>
            in the morning at the dining room table, ignoring<br>
            the newspaper’s nothingness and sales-events, and we could<br>
            no longer talk to the gestures, we could only<br>
            imitate the faces. The wind’s hair was freshly washed;<br>
            but it wasn’t braided yet. <br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; I was walking up the mountain<br>
            behind you who were talking about the stones you’d visited <br>
            up here and over there, back behind that clump of trees—<br>
            stones that held the birds that must have been flying <br>
            before there was anything like us, flying overhead <br>
            before the rules were made, singing and flying, <br>
            birds that were caught now. It was a field of boulders.</p>
            
            <p>So we sat there through the afternoon, we dozed there, and when we woke—</p>
            
            <p>the birds were everywhere; they had long strands of hair <br>
            in their beaks, there were so many birds I couldn’t move <br>
            without touching feathers, and when I moved <br>
            the birds flew up, causing darkness, and then <br>
            they settled down again. You were crying, pulling out your hair<br>
            and holding your arm out so the birds could take the strands<br>
            and fly off. <em>They are not birds with names</em>, you said,<br>
            when I started to thumb through our guide book; <em>they are birds</em><br>
            <em>from all the days you’ve lived, birds from so far back</em><br>
            <em>inside you the days themselves have vanished. They still live there,</em><br>
            <em>way back inside you, and when we clap our hands</em><br>
            <em>they’ll fly back inside, though it will look like they’re flying up </em><br>
            <em>into the sky. </em>You clapped then and we sat <br>
            on those warm rocks, those rocks that held themselves more still <br>
            when we were sitting on them than they did as we walked away<br>
            up the mountain, where we camped beside a waterfall<br>
            that rushed past so loudly we had to gesture to be heard.</p>
            <p>&nbsp;
            </p>
            <p><strong>Salt</strong></p>
             
            <p>If a man takes a walk in the rain, barefoot<br>
            because he he’s had a dream of fallen leaves and healing, <br>
            and if the rain tastes like salt and stings his eyes <br>
            like salt. If the man understands what salty rain <br>
            will do. It is falling hard; if the man<br>
            asks the wind to blow the salty rain into the ocean<br>
            and tastes the rain turn fresh again. This man is full of holes<br>
            where other men wear skin.</p>
            
            <p>The rain fell salty for a moment then turned fresh again.<br>
            The bay turned into a freshwater lake <br>
            for a single tide. My fingernails are burning<br>
            he tells his daughter as he brushes her long hair,<br>
            he tells his son by breathing, he tells<br>
            his wife when he touches her. But then she’s burning too,<br>
            burning with salt. The light is salty now, falling<br>
            salty even in the wilderness.</p>
            
            <p>The wind is blowing salty too. The salty snow will fall, white<br>
            as always; salty rivers will flow<br>
            like our blood flows through our bodies, salty<br>
            rain falls everywhere, everywhere, into <br>
            the wounds we didn’t even know we’d suffered, and it stings<br>
            until we can’t sleep. And that’s not because it’s healing.</p>

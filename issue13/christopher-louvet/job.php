            <p><strong>A Reason to Leave Florida for Maine</strong></p>
           
            <p>Sea-grapes sloth in sun coursing<br>
            on noon, mid-morning’s furnace<br>
            assembling humid ambitions.<br>
            To the west, clouds in earnest</p>
            
            <p>call up reservists. No drills today,<br>
            the real thing late this afternoon,<br>
            a slow-grilled beachhead foray.<br>
            Summer cicadas cluck and croon</p>
            
            <p>special ciphers of secret services,<br>
            and north and south, traffic gutters<br>
            along avenues periodically coerced<br>
            into clogs of idle-engine mutters.</p>
            
            <p>All this is visible from the balcony, <br>
            like my neighbor’s mango tree<br>
            and its hanging, swollen fruit,<br>
            each capacious as a mandrake root,</p>
            
            <p>like weekenders waiting to cross<br>
            the street and file into the park<br>
            to forget, to barbecue the losses<br>
            of the week and anoint the sparks,</p>
            
            <p>to toast new nostalgia and more.<br>
            When it’s over droves of ibis<br>
            will scavenge the garbage stored<br>
            in the cans or littering the grass;</p>
           
            <p>the weather will be a <em>non sequitur</em>,<br>
            a reason to leave Florida for Maine,<br>
            and the moon will wax another lane<br>
            waves score awfully into the shore.</p>
           
            <p>&nbsp;</p>


<p><strong>Across the Avenue</strong></p>

<p>Ants have built their cities in the vacant lot<br>
where an old motel used to be.</p>
 
<p>The tall grass shimmies like sardines.<br>
Burned and broken bricks lie</p>
 
<p>amid shards of glass and refrain from discussion<br>
while household objects, charred and familiar,</p>
 
<p>dispassionately observe passing cars.<br>
Like discarded toys, they look where they must.</p>
 
<p>A shadeless lamp with a futile wire,<br>
a folding chair and a dial-less rotary phone,</p>
 
<p>receiver unhooked, angled up—<br>
all of these survived the fire.</p>
 
<p>Most evenings there is static, as of snow,<br>
from carmine and gold brindled clouds</p>
 
<p>emboldened as rams before clashing horns—<br>
they ascend in spare arpeggios</p>
 
<p>and green the sea with shadows—<br>
but tonight power lines pulse with gossip</p>
 
<p>while the city fence whistles the wind through <br>
missing teeth, and the tall grass bends like truth</p>
 
<p>to a lie that smells of seaweed-tarnished sand.<br>
One by one, the ants return to their homes.</p>
 
<p>Listen: the sound of the telephone, calling,<br>
though it’s not sure who, or why.</p>
<p>&nbsp;</p>

<p><strong>The light at weekend’s end is better</strong></p>

<p>Less like a roadblock,<br>
more like spilled mercury, of the eel and the harp—</p>
 
<p>orchids sharpen their tongues in the trees,</p>
 
<p>trellised morning glories<br>
glorify every shadow’s event horizon,</p>
 
<p>and yesterday’s early afternoon showers,</p>

<p>cool evening and cooler night,<br>
long-sleeved chills bright and forthcoming as headlights,</p>

<p>nightmare thunderclaps,</p>

<p>flashes of sudden jazz—<br>
like finicky, icthymaniac gulls eye-plunging</p>

<p>through admirable loops to beak sardines—</p>

<p>distance themselves in the evening’s <br>
widening shadows. The television is on,</p>

<p>and soon I’ll draw the shades.</p>

<p>The grass will continue to infiltrate the coral path<br>
even in this moment of apparent abeyance,</p>

<p>and what passes will have passed</p>

<p>like baby teeth to toothless grins,<br>
from delinquents to wild, invalid pensioners hanging on.</p>

<p>The light at weekend’s end is always better.</p>            </td>

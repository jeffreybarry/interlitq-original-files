            <p><strong>We still carry those X and Y chromosomes</strong></p>
            
            <p>from the original Adam and Eve,<br>
            from fifty million years ago,</p>
             
            <p>from African father and mother,<br>
            who, in the Upper Paleolithic, learned <br>
            to take those images of sky and water and herds of animals<br>
            and translate them into language—</p>
            
            <p>how beautiful to speak the very trees, the spear<br>
            that pierced the antelope,</p>
            
            <p>to sing the death of the creature<br>
             you would cook over flames</p>
            
            <p>as over and over again<br>
            you spoke the magic word for fire.</p>
            
            <p>Later came the words for life and death and God,<br>
            though those syllables lacked the perfect<br>
            resonance of pounding hooves or the hot, red glow<br>
            of branches rendered into smoke,</p>
            
            <p>that was itself defined as an offering<br>
            to the flickering constellations.</p>
          
            <p>Later you would shape sculptures of big-hipped women<br>
            from wet clay, and with your very hands<br>
            you would sketch into cave walls<br>
            the likenesses of deer and bison,</p>
            
            <p>the images in your head spilling out<br>
            until the whole world<br>
           trembled with art, until vast civilizations</p>
            
            <p>raised wheat and brewed beer,<br>
            built temples to new gods,<br>
            whom language had imbued with new<br>
            and terrible meanings.</p>
             
            <p><em>Old humans,</em> I praise thee!<br>
            We have moved so far from your language of the concrete and specific,<br>
            from words that equaled fire and hunger, that signaled<br>
            buffalo stampedes in the brain. These days<br>
            words merely symbolize our obsessions, in these last<br>
            days, these desperate days, these long days<br>
            spent at the outlet mall,<br>
            spent with our televisions, computers, and cell phones,<br>
            all of them speaking simultaneously,<br>
            all of them arguing consumption and selfishness,</p>
             
            <p>language, language, language,<br>
            not like<br>
            <em>buffalo, buffalo, buffalo… </em></p>                                    <p align="right"><em>for Campbell McGrath</em></p>
<p>&nbsp;</p>
<p>&nbsp;</p>

<p><strong>The Coming Surrender</strong></p>

<p>Jonathan Edwards proclaimed us<br>
<em>sinners in the hands of an angry god</em>,<br>
and, all these years later, that cosmic dread<br>
reaches towards me, promises a strangling<br>
damnation that is personal, all mine,<br>
and forever-lasting.</p>

<p>Let me thank the Baptists again<br>
for this late-blooming fury,<br>
this Fear that haunts the night hours<br>
like Florida moonflowers grazed by starlight, moistened<br>
by mist that rolls in from the whispering sea.</p>
 
<p>Like moonflowers? Yes, like fragile petals<br>
glazed with the coming surrender, the moment<br>
of dissolution when white flesh falls to the hard ground,<br>
when the moon parts the gathering cumuli </p>

<p>and all across this tropical world, <br>
the late clouds thunder<br>
their gospel of storm and trouble.</p>
<p>&nbsp;</p>
<p>&nbsp;</p>
 
<p><strong>Heaven of Horses</strong></p>

<p>I is <br>
the center <br>
of my consciousness,<br>
a word I&rsquo;ve been trying to spell for forty years,<br>
<em>consciousness</em> that is, with its tricky consonants<br>
and extra vowels. But if you lunge deep</p>

<p>below consciousness, you&rsquo;ll find<br>
that moon-lit place where the bears<br>
are chowing down on your grandmother<br>
while raccoons run laps around the track<br>
where you trained in high school<br>
beneath the light of a silvery moon.</p>

<p>Whoever named those ring-tailed varmints <em>raccoons</em><br>
was really thinking.  Is it <em>onomatopoeia</em><br>
when the word sounds like the racket <br>
furry creatures make<br>
in the dark dumpsters of 4 a.m.?</p>

<p>Now, there&rsquo;s another word I&rsquo;m surprised<br>
I can spell: <em>onomatopoeia</em>. Sounds like<br>
an opium-induced sigh<br>
from a poet dreaming of Mongolia<br>
where warriors worshipped the sky<br>
amid the music of <em>houyhnhnms</em><br>
from the green-frosted pastures.</p>

<p><em>God, consciousness, raccoons, onomatopoeia, Mongols, whinnying horses</em><br>
kerplunk across my brain, <br>

<p>that citadel of the I,</p>

<p>the place from which my essential &ldquo;me-ness&rdquo;<br>
rushes out to meet the incoming tide<br>
of everything that is the other,<br>
which is an entire universe,<br>
which is the sky so many have worshipped,<br>
dark and glowing<br>
across nights filled with those heaving constellations,<br>
which themselves are filled with stars<br>
helping sailors plot their journeys<br>
across mighty oceans, beneath<br>
solar systems roiling in the cosmic,</p>

<p><em>where Walt Whitman dances on the head of a pin</em>.</p>

<p>It is a testimony to the hugeness of Walt&rsquo;s consciousness<br>
that he takes the place of several angels, yes, he is the better<br>
angel, holy messenger of the cosmic<br>
who sang praises of the &ldquo;I,&rdquo; who celebrated<br>
the solitary, who embraced the communal,<br>
who sang the body electric, who mightily <br>
inhaled the fragrance<br>
of sweating humans and horses.</p>

<p>I must say that the sky is a thunder of horses<br>
when the nimbi rock with afternoon storms,<br>
when thunder rolls the once-solid world<br>
into a more wary wakefulness of the heavy rain<br>
still to come. I must say the night sky</p>

<p>is a heaven of horses, ghosting across<br>
the milk and white of interstellar spaces,<br>
those places the &ldquo;I&rdquo; is always longing for,<br>
that consciousness gathered beyond the single, hopeful spirit.</p>

<p>I never said God was.<br>
But I&rsquo;ll never say God wasn&rsquo;t,<br>
that there wasn&rsquo;t a moment of fiery creation<br>
that spun out all that would become poets and horses<br>
and moons and stars. I&rsquo;ll never say God<br>
isn&rsquo;t, even provisionally in this short<br>
time between breath and thought.</p>
 
 

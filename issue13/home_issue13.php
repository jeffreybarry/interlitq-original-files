              <p>  <span lang="EN-US">As the President of <em>Interlitq</em>, I am delighted to welcome you to the feature "15 Miami poets", constituting section 1 of Issue 13 of the review, with section 2 of Issue 13 to be published in the course of January 2011.</span> </p>
               

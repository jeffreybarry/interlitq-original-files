              <p><span class="titulo2"><a name="spanish">Estimado Invitado</a>,</span></p>
            
              <p>Como Presidente de <em>Interlitq</em>, me es un placer darle la bienvenida al perfil "15 Miami poets" (15 poetas de Miami), la primera sección del ejemplar 13 de la revista. La segunda sección del ejemplar 13 será publicada en el transcurso de enero 2011. </p>
               
              <p>Respecto a las adiciones al equipo de redacción de <em>Interlitq</em>, el consejo editorial aprovecha esta oportunidad de dar la bienvenida a <a href="http://interlitq.org/staff/sari-nusseibeh/bio.php" class="link1">Sari Nusseibeh</a>, profesor de filosofía y presidente de la Universidad de Al-Quds en Jerusalén, que actuará como Vice-Presidente de la corporación a partir del ejemplar 13. </p>
              
              <p>En cuanto a las contribuciones a la primera sección del ejemplar 13, espero que disfrute del talento literaria considerable de las poetas de Miami: <a href="http://interlitq.org/issue13/elisa-albo/job.php" class="link1">Elisa Albo</a>, <a href="http://interlitq.org/issue13/howard-camner/job.php" class="link1">Howard Camner</a>, <a href="http://interlitq.org/issue13/adrian-castro/job.php" class="link1">Adrian Castro</a>, <a href="http://interlitq.org/issue13/denise-duhamel/job.php" class="link1">Denise Duhamel</a>, <a href="http://interlitq.org/issue13/corey-ginsberg/job.php" class="link1">Corey Ginsburg</a>, <a href="http://interlitq.org/issue13/michael-hettich/job.php" class="link1">Michael Hettich</a>, <a href="http://interlitq.org/issue13/miriam-levine/job.php" class="link1">Miriam Levine</a>, <a href="http://interlitq.org/issue13/christopher-louvet/job.php" class="link1">Christopher Louvet</a>, <a href="http://interlitq.org/issue13/jesse-millner/job.php" class="link1">Jesse Millner</a>, <a href="http://interlitq.org/issue13/barbra-nightingale/job.php" class="link1">Barbra Nightingale</a>, <a href="http://interlitq.org/issue13/geoffrey-philp/job.php" class="link1">Geoffrey Philp</a>, <a href="http://interlitq.org/issue13/laura-richardson/job.php" class="link1">Laura Richardson</a>, <a href="http://interlitq.org/issue13/alexis-sellas/job.php" class="link1">Alexis Sellas</a>, <a href="http://interlitq.org/issue13/virgil-suarez/job.php" class="link1">Virgil Su&aacute;rez</a>, y <a href="http://interlitq.org/issue13/nick-vagnoni/job.php" class="link1">Nick Vagnoni</a>.</p>
              
              <p>Y todos estos textos complementados por las ilustraciones evocativas de <a href="http://interlitq.org/issue13/xavier-cortada/bio.php" class="link1">Xavier Cortada</a>, el artista galardonado de Miami.</p>
              
              <p>Espero que disfrute de la primera sección del ejemplar 13 de <em>Interlitq</em>, y que vuelve en enero 2011 cuando la segunda sección será publicada. </p>

      <td colspan="4" rowspan="2" class="texto2" valign="top">
       <p>Returning to Interlitq's "15 Miami poets" feature, I am confident that you will be impressed by the array of poetic talent, strong in both terms of craftsmanship and imaginative force, coming out of Miami, with contributions from <a href="http://interlitq.org/issue13/elisa-albo/job.php" class="link1">Elisa Albo</a>, <a href="http://interlitq.org/issue13/howard-camner/job.php" class="link1">Howard Camner</a>, <a href="http://interlitq.org/issue13/adrian-castro/job.php" class="link1">Adrian Castro</a>, <a href="http://interlitq.org/issue13/denise-duhamel/job.php" class="link1">Denise Duhamel</a>, <a href="http://interlitq.org/issue13/corey-ginsberg/job.php" class="link1">Corey Ginsburg</a>, <a href="http://interlitq.org/issue13/michael-hettich/job.php" class="link1">Michael Hettich</a>, <a href="http://interlitq.org/issue13/miriam-levine/job.php" class="link1">Miriam Levine</a>, <a href="http://interlitq.org/issue13/christopher-louvet/job.php" class="link1">Christopher Louvet</a>, <a href="http://interlitq.org/issue13/jesse-millner/job.php" class="link1">Jesse Millner</a>, <a href="http://interlitq.org/issue13/barbra-nightingale/job.php" class="link1">Barbra Nightingale</a>, <a href="http://interlitq.org/issue13/geoffrey-philp/job.php" class="link1">Geoffrey Philp</a>, <a href="http://interlitq.org/issue13/laura-richardson/job.php" class="link1">Laura Richardson</a>, <a href="http://interlitq.org/issue13/alexis-sellas/job.php" class="link1">Alexis Sellas</a>, <a href="http://interlitq.org/issue13/virgil-suarez/job.php" class="link1">Virgil Su&aacute;rez</a>, and <a href="http://interlitq.org/issue13/nick-vagnoni/job.php" class="link1">Nick Vagnoni</a>.</p>
       
       <p>And what better than the vivid artwork of <a href="http://interlitq.org/issue13/xavier-cortada/bio.php" class="link1">Xavier Cortada</a>, the award-winning Miami-based artist, to complement these outstanding texts.</p>
 

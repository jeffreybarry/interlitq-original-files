           <p><strong>Handling Destiny: Apology for Returning</strong></p>
            <p>There still remains then—<br>
            Your head searches for the blessed bridge<br>
            that your feet won&rsquo;t yet take you</p>
            
            <p>&ldquo;I respect you &amp; you respect me&rdquo; said the dialogue<br>
            My approach was to unite<br>
            (I&rsquo;ll say that in my apology)</p>
            
            <p>Reached the cold river wide<br>
            like groove around dead man&rsquo;s neck<br>
            like trench you leap while<br>
            delivering urgent message<br>
            (I apologize for my proximity to death)</p>
           
            <p>My place being two blocks from Boulder Creek<br>
            Marine Street being so far from ocean<br>
            So I began my long walks around Arapahoe<br>
            &ldquo;How to make poetry a divination?&rdquo;<br>
            the way an exile must invent his home</p>
           
            <p>Two blocks away I could hear<br>
            the slow trickle of seasons—<br>
            Not like Miami<br>
            where green, blue, orange<br>
            are scattered by heat<br>
            the all embracing heat year long<br>
            Snow has a way of muffling motion<br>
            The echo of heartbeat hides<br>
            Not like Miami<br>
            where pulse turns words into projectiles<br>
            with accents</p>
            
            <p>Then someone said I would not eat<br>
            I thought maybe the novelty of outsider<br>
            my skin too distracting<br>
            But in the mouth of a poisoned arrow<br>
            there&rsquo;s always the hunter&rsquo;s breath<br>
            though I didn&rsquo;t know quite how to shoot<br>
            though I had a quiver of bilingual leather</p>
            
            <p>I brought palm oil &amp; the memory of fried plátanos</p>
            <p>my mother moros<br>
            It was then my feet began to stir<br>
            from the deep rhythm of home<br>
            I could hear words from childhood<br>
            in a chorus of memory<br>
            soundtrack not yet harmonic<br>
            I cannot apologize for taking what is mine<br>
            the way a mockingbird steals<br>
            language</p>
            
            <p>Because rhythm has no ego<br>
            (who owns music of breath?)<br>
            I listened to the crack of drums in my sleep<br>
            my head to carry my load<br>
            to a white stone where there were<br>
            chiseled sixteen markings<br>
            a footprint that fit my foot<br>
            In the dream I would wash this stone with herbs<br>
            
            like Sabe Lecci&oacute;n, Siempre Viva, Maravilla<br>
            Now I&rsquo;d have to memorize these marks<br>
            make words that articulate them—</p>
           

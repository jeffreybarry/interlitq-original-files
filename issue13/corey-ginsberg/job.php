            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Like I did when we were together. Maybe you&rsquo;re trying to pretend, too, that I&rsquo;m not<br>

            <p><strong>Alba Written at Bennington College</strong></p>
 
<p>No caw of roosters to greet the pale snow here,<br>
nothing stirs at this hour, not even the mice<br>
buried deep in the snow.  I stop to piss by a tree</p>
 
<p>but notice that a dog has beaten me to the spot,<br>
a yellow smear near the tree's trunk.  I hate how<br>
snow crunches under my boots.  My hands are crows</p>
 
<p>nested in my pockets.  My breath ghosts every word<br>
I want to utter in name of such loneliness, such magic,<br>
but my mouth is merely a slit through which enough</p>
 
<p>air passes inside my lungs.  On my way home<br>
in this white world where everything seems to forget<br>
the idea of green, my memory becomes a cardinal</p>
 
<p>shooting across the snow like a drop of blood.</p>
<p>&nbsp;</p>
            <p><strong>Blue Snow</strong></p>
 
<p>I walk in the Bennington snow at midnight,<br>
cross the bridge over the lake. In moonlight,<br>
the upturned canoe burrows its hump shape<br>
from the shadows. This is a bestial night,<br>
a blue night of hatched truths.  Dark surprise.<br>
A harsh wind speaks her name to the trees.<br>
Sifted, fresh snow falls upon the path. I don't<br>
feel lost, but I see my breath in front of me,<br>
mouthed words fall frozen at my feet, crunch<br>
under my boots. Air singes my lungs with cold.<br>
How far is home in all this blueness?  I fall<br>
three times, get up, dust my legs off . . .<br>
There's the myth of crossing too many bridges.<br>
Of not looking back once you cross.  Chunks<br>
of snow fall off the branches.  Twigs break<br>
underfoot.  I hear my father calling.  What<br>
is he doing here so far from his island?<br>
I trudge onward to escape all the selves<br>
he wanted his only son to be.</p>
<p>&nbsp;</p>
<p><strong>Allegro con Salsa</strong></p>
 
<p>These Bennington College trees line the path<br>
through the lake to my white-clapboard house<br>
in Orchard C, Godliness blue on the sifted, fresh</p>
 
<p>snow.  This island boy can wither here, I think,<br>
if not for the heat in my own blood, spooked<br>
horses on my grandfather's farm, miles deep</p>
 
<p>in my memory.  I cross the bridge, unsteady feet,<br>
the snow crunches like bones under my boots.<br>
The wind hits snow chunks off tree branches,</p>
 
<p>some nocturnal morse-code not intended for me,<br>
but for some other creature.  I start humming<br>
an old song from childhood to steady my mind,</p>
 
<p>my tongue, my hands, my breath, frozen . . .<br>
The moon peeks through clouds.  I start to dance.<br>
I conga through snow as if my life depended on it.</p>
            <p>&nbsp;</p>
            <p><strong>The Great Suspiro</strong></p>
 
<p>In black cape, top hat, he choked<br>
words into his throat, a web of deceit,<br>
though a magician he wasn't.</p>
 
<p>Actually, my parents worked<br>
with him during volunteer red<br>
Sundays when they picked</p>
 
<p>potatoes from muddy fields,<br>
and Suspiro looked up at the sky<br>
and said he'd could read</p>
 
<p>the bottoms of clouds like hands,<br>
and he knew that everyone's<br>
days in Cuba were counted,</p>
 
<p>and he sighed.  He sighed all<br>
the time, which is how he got<br>
his nickname.  Suspiro.  Sigh.</p>
 
<p>My father tagged "The Great"<br>
because he said when Suspiro<br>
spoke, people stopped work</p>
 
<p>to listen.  Until the day, he fell<br>
over furrowed earth, clutching<br>
his breast, and out of his mouth</p>
 
<p>came a scream that set doves<br>
aflutter in the distant cane fields.<br>
Suspiro, of the bone-white</p>
 
<p>predictions.  How my parents<br>
would leave their country<br>
never to come back.  Me, I grow</p>
 
<p>silent when I feel his hands<br>
wrap around my throat, a mouse<br>
caught in a trap, its neck broken</p>
 
<p>a pulse quickening, then letting go.</p>


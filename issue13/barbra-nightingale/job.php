            </p>

<p><strong>For Mother, Dying Alone</strong></p>

<p>In the long night of your sleeping<br>
you died, dreaming your last dream.<br>
Were there lavender mists<br>
to soften the mountains,<br>
craggy forests filled with dark?<br>
Were there brooks gently trilling,<br>
a moon with a kindly face?</p>

<p>There are a dozen explanations<br>
for why I was not there,<br>
not near as true as this:<br>
In the end we’re all alone,<br>
no matter how many hands<br>
to hold us back, or tug us gently on.</p>
<p>&nbsp;</p>

<p><strong>Barbie Gets Divorced</strong></p>

<p>Let&rsquo;s face it: at fifty<br>
I no longer feel twenty<br>
though you&rsquo;d never guess <br>
to look at me.  Ah!<br>
The wonders of polystyrene.<br>
But in the night, I turn<br>
slick as glass, hot flashes<br>
almost melting my plastic bed.<br>
Just as Ken finished<br>
his manly stuff, fiddling<br>
with the A/C, adjusting the fan,<br>
I&rsquo;d be freezing, teeth chattering so hard<br>
my molded smile could crack,<br>
pink skin turned icy blue. </p>

<p>Frustrated, Ken slept on the couch<br>
and pretty soon Skipper<br>
looked better and better <br>
and Ken moved out.<br>
&ldquo;Fifty years!&rdquo; I shouted at him,<br>
&ldquo;I supported you for fifty years!&rdquo;<br>
Everyone knows that Ken never held<br>
a job, it was always me—<br>
from fashion model to nurse<br>
to lawyer to CEO.  Everything<br>
was mine.  Did you ever see <br>
<em>Ken&rsquo;s</em> camper or <em>Ken&rsquo;s</em> sports car?<br>
Of course not!  All Ken had<br>
was his spiffy clothes with the Velcro crotch.<br>
(He was more a Johnny-come-quickly<br>
than anything else.)<br>
But did he care?  Did he appreciate?<br>
No!  Soon as things got rough<br>
he left.  It wasn&rsquo;t my fault my fist<br>
would wind up in his eye, or my toes<br>
stuck behind his ear; my body was on fire!<br>
I was sticky, going soft around the edges.<br>
Nothing I could do!  The rat, squeamish<br>
as he was, bailed, then sued me for half of it all.<br>
Talk about a bitter pill—what&rsquo;s left for me?<br>
Even GI Joe is younger than me, and last time<br>
I saw him, he was missing a leg.  &ldquo;War&rsquo;s hell&rdquo; he said.<br>
Well, all that&rsquo;s water down the drain<br>
as I always say.  I&rsquo;m not one to pout.<br>
I&rsquo;ll just drive this spiffy coupe<br>
over to headquarters and pour me another guy!<br>
They may have retired the mold that made me,<br>
but brother, you&rsquo;re a dime a dozen.<br>
Look out, boys, here I come!</p>


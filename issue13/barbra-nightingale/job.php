            <p><strong>The Silkie</strong></p>
            
            <p>They thought she was dead,<br>
            at best deranged.<br>
            The way she undulated<br>
            through water, the colder<br>
            the better, how she stayed <br>
            down so long they thought<br>
            she’d drowned, the algae<br>
            in her hair, long floating strands<br>
            like a flag waving in wind.</p>
            
            <p>They thought what she spoke<br>
            wasn’t language, some strange<br>
            twisted tongue from a country<br>
            they couldn’t pronounce.<br>
            But they were wrong, the color<br>
            and syntax too layered.<br>
            There was much they didn’t know.<br>
            Everyone gathered to stare,<br>
            to watch as swollen with words<br>
            she was pulled from the cove<br>
            to deep water, to steep, pebbled bank,<br>
            then left to her solitude,<br>
            begging for what they never understood.<br>
            The sleek feel of fur in her mouth, <br>
            the thick shine coating her tongue,<br>
            her long deep vowels coupled<br>
            with the softest of thuds,<br>
            come all this way to journey back home.</p>

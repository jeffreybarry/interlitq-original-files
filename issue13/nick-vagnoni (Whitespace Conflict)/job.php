            <p><strong>
              On Taking the Collected Poems of Frank O'Hara on Vacation
            </strong></p>
            <p>Just that title itself is cumbersome,<br>
            let alone stuffing you under the seat in front of me.<br>
            But &ldquo;Poem&rdquo; or &ldquo;Song&rdquo; just won&rsquo;t do.<br>
            I&rsquo;ve looked at that new selected,<br>
            and it doesn&rsquo;t have what I want,<br>
            Frank, the raspberry sweater,<br>
            the song where you declare,<br>
            from the back of a cab, <br>
            that disease is worrying come true.</p>
            
            <p>I&rsquo;ve traveled with <em>Meditations in an Emergency</em>,<br>
            carried it all the way to Apsheron,<br>
            an Azerbaijani restaurant on Ocean Ave., Sheepshead Bay,<br>
            and gave it to a friend at her wedding celebration<br>
            as she held her baby daughter, Yeva Grace.<br>
            Along with it, a card with cherry blossoms<br>
            and the words &ldquo;May you always make one another happy.&rdquo;</p>
            
            <p>Oh, Frank,<br>
            so much baggage you are.<br>
            And here I go,<br>
            off to another wedding,<br>
            this time the gift,<br>
            a mandoline slicer,<br>
            already Amazoned <br>
            to Washington, D.C.</p>
           
            <p>While I&rsquo;m there, I&rsquo;ll see your Baltimore,<br>
            even if you didn&rsquo;t like it,<br>
            and I&rsquo;ll take all 600 pages of you with me,<br>
            through the little air puffers in the terminal,<br>
            through the knots of shoeless businessmen,<br>
            on the airplane, on the metro.</p>
           
            <p>Even the back page where, years ago,<br>
            on a bustling wooden dock, <br>
            I penciled the lines:</p>
             
            <p>&ldquo;I had a vision of us apart,<br>
            and when I asked how you were doing,<br>
            you said &lsquo;good,&rsquo;<br>
            and when you asked how I was doing,<br>
           I said &lsquo;good,&rsquo;<br>
            and I meant it.&rdquo; </p>

            <p>ΒΙΟΓΡΑΦΙΑ, <SPAN LANG="en-US">II</SPAN></p>
          
            <p ALIGN="JUSTIFY"> Το σκοτάδι σκαρφαλώνει πάνω μου. Ένα φυτό. Που το<br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;ποτίζεις κρυφά κάθε μεσημέρι.</p>
           
            <p ALIGN="JUSTIFY"> Έπειτα είναι κι η ανάσα σου. Με κλείνει από παντού σαν<br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;συρματόπλεγμα. Κάτω απ’το δέρμα σου</p>
            
            <p ALIGN="JUSTIFY"> Υπάρχουν εφτά πολιτείες. Η μιά πιο βαθιά από την άλλη.<br> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Σε κάθε μιά</p>
            
            <p ALIGN="JUSTIFY"> Ένα άρμα σέρνει αδιάκοπα γύρω από τα τείχη τον ένδοξο <br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;νεκρό.</p>
            <p>&nbsp;</p>
           
            <p>BIOGRAPHY, II</p>
           
            <p> Darkness is creeping over me. A plant. Which you <br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;water in secret every morning.</p>
             
            <p> It’s your breath too. It shuts me away from every-<br> 
             &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;where like barbed wire. Under your skin</p>
           
            <p> Lie seven cities. One beneath the other. Endlessly<br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;  circling</p>
            
            <p> the walls of each a chariot drags the glorious dead.</p>
            
            <p LANG="en-US"> <em>Translated by Richard Berengarten </em></p>
<p>&nbsp;</p>
             <p>&nbsp;</p>
            <p>ΕΣΩΤΕΡΙΚΟΣ ΜΟΝΟΛΟΓΟΣ ΓΕΩΡΓΙΟΥ ΧΟΡΤΑΤΖΗ</p>
           
            <p>Τα δέντρα βαδίζουν όλα προς το δάσος<br>
            για να ενωθούν με κάτι που τα ξεπερνάει.<br>
            Το δάσος βαδίζει προς το άλλο δάσος.<br>
            Ο ταύρος προς την Πασιφάη.</p>
            
            <p>Τα λάθη της φύσεως τα διορθώνει η επιστήμη.<br>
            Τα πέναλτυ τα χτυπούν οι πεπειραμένοι.<br>
            Η πλώρη είναι πάντα πιο μυτερή απ’ την πρύμη.<br>
            Ο έρωτας απ’ την ειμαρμένη.</p>
            <p>&nbsp;</p>
            
            <p> <SPAN LANG="en-US">INTERIOR MONOLOGUE OF GEORGIOS HORTATZIS</SPAN></p>
            
            <p LANG="en-US"> The trees all move towards the forest<br>
             into which richer entity each blends.<br>
             <SPAN LANG="en-US">The forest moves towards the other forest.</SPAN><br>
             To <SPAN LANG="en-US">Pasipha&euml; the bull descends</SPAN>. </p>
             <p LANG="en-US"> By science are nature’s errors rectified.<br>
              Penalties get booted by the best shot. <br>
              Prows not sterns are made to slice the tide<br>
             and Eros through our whole lot.</p>
             <p> <SPAN LANG="en-US"><em>Translated by Richard Berengarten </em></SPAN><SPAN LANG="en-US">&amp; </SPAN><SPAN LANG="en-US"><em>Paschalis Nikolaou</em></SPAN></p>
          <p>&nbsp;</p>   
<p>&nbsp;</p>
             
            <p><SPAN LANG="en-US">DOLCE</SPAN> <SPAN LANG="en-US">STIL</SPAN> <SPAN LANG="en-US">NOVO</SPAN></p>
             
            <p>Ακούω το σφύριγμα από τη στροβιλιστή <br>
            βόμβα του χρόνου. Όμως δε βλέπω πουθενά το αεροπλάνο. <br>
            Η έκρηξη θ’ ακουστεί όταν θα πεθάνω. <br>
            Αλλά ίσως και να μην ακουστεί </p>
           
            <p>καθόλου. Γιατί με λίγη τύχη και με τη σωστή<br>
            μέθοδο μπορεί ξαφνικά ν’ αλλάξει το πλάνο. <br>
            Κι όλα ν’ αρχίσουν ν’ ανεβαίνουν προς τα πάνω. <br>
            Να τα τραβούν οι ελκυστικοί μαστοί </p>
           
            <p>κάποιας θεάς που θα’ χει μόλις ξυπνήσει <br>
            γλυκά. Σ’ έναν ουράνιο κήπο. Και δε θα φορά <br>
            παρά μονάχα ένα μεταξωτό διάφανο σάλι.</p>
           
            <p>Ή ολόγυμνη (όπως τη γέννησε η φύση)<br>
            θα σκύβει για να δροσίσει στα νερά <br>
            τους λευκούς της ώμους, το λαιμό και το κεφάλι.</p>
<p>&nbsp;</p>
            
            <p> DOLCE STIL NOVO</p>
           
            <p> I hear the hiss and whirling of time’s bomb.<br>
            But nowhere does a plane show in the sky.<br>
           The bomb no doubt will go off when I die <br>
              but maybe with no great resounding boom </p>
            
            <p> of any kind. For if my fortune crests,<br>
              with the right handling the whole scheme of things<br>
              might change –  as if levitated by strings <br>
             invisibly attached to the pert breasts</p>
            
            <p> of some sweet goddess who, but half-awake<br>
               (in her celestial garden), slowly stands<br>
              resplendent in a semi-see-through shawl</p>
             
            <p> or, stooping to cool waters, with fair hands<br>
              (wearing absolutely nothing at all)<br>
             rinses white shoulders, hair and nape of neck. </p>
            
            <p> <SPAN LANG="en-US"><em>Translated by Richard Berengarten </em></SPAN><SPAN LANG="en-US">&amp; </SPAN><SPAN LANG="en-US"><em>Paschalis Nikolaou</em></SPAN></p>
            <p>&nbsp;</p>
<p>&nbsp;</p>
             
            <p> ΒΑΡΒΑΡΕΣ ΩΔΕΣ, <SPAN LANG="en-US">VII</SPAN></p>
           
            <p> Ω εσύ που θερμαίνεις  με το πρόσωπό σου<br>
             τον κρύο χειμωνιάτικο αέρα.<br>
              Που μόνο (και σοφό) βιβλίο σου είναι<br>
              το βαθύ σκοτάδι.</p>
             
            <p> Τα τριαντάφυλλα φουσκώνουν στα βήματά σου.<br>
              Ο θάνατος σε κοιτάζει υπνωτισμένος.<br>
              Οι ακακίες ρίχνουν άστρα. Σκεπάζουν μ’ ένα άσπρο νόημα<br>
             τις τέντες των φορτηγών.</p>
            
            <p> Άχρονη μητέρα με τις χρυσές πλεξίδες<br>
              και το άσπιλο κοριτσίστικο στήθος.<br>
              Πως γίνεται και δίνεις τις πορφυρές σου ρόγες<br>
στον πρώτο περαστικό;</p>
            <p>&nbsp;</p>
             
            <p> <SPAN LANG="en-US">BARBAROUS</SPAN><SPAN LANG="el-GR"><em> </em></SPAN><SPAN LANG="en-US">ODES</SPAN><SPAN LANG="el-GR">, </SPAN><SPAN LANG="en-US">VII</SPAN></p>
             
            <p LANG="en-US"> Oh you who warm the chilly winter air <br>
            with your face. <br>
             You whose single (wise) book <br>
             is deepest darkness.</p>
            
            <p LANG="en-US"> Roses billow in your footsteps.<br>
             Death watches you mesmerised.<br>
              Acacias shed stars. And with white meanings <br>
              blanket canvases of trucks.</p>
            
            <p> Eternal mother with your golden locks<br>
              and your girlish breasts untrammelled. <br>
             How is it you offer your purple nipples<br>
             to the first passerby? </p>
             
            <p> <SPAN LANG="en-US"><em>Translated by Richard Berengarten </em></SPAN><SPAN LANG="en-US">&amp; </SPAN><SPAN LANG="en-US"><em>Paschalis Nikolaou</em></SPAN></p>
<p>&nbsp;</p>
             <p>&nbsp;</p>
            <p> ΔΑΝΙΗΛ ΜΙΣΡΑΧΗΣ</p>
            
            <p>Αντί επιτυμβίου μου έχω χαράξει<br>
             –  του το ανταποδίδω από αυτή τη θέση  –<br>
             το επίγραμμα που θα μου είχε επιδαψιλεύσει<br>
             ο αλήστου μνήμης Πάτροκλος Γιατράς:<br>
             «Ατάλαντος. Το μόνο καλό του ποίημα<br>
             ήταν οι στίχοι που δεν έγραψε».</p>
            <p>&nbsp;</p>
             
            <p LANG="en-US">DANIEL MISRAHIS</p>
             
            <p>Instead of my own epitaph I&rsquo;ve engraved<br>
             – reciprocating from this final resting place –<br>
             the epigram the deathless Patroclus Yiatras<br>
             would have lavished upon me:<br>
             &lsquo;Talentless.  His only good poem <br>
             was the verse he did not write.&rsquo;</p>
            
            <p><SPAN LANG="en-US"><em>Translated by Paschalis Nikolaou</em></SPAN></p>
            <p>&nbsp;</p>
             <p>&nbsp;</p>
            <p> <SPAN LANG="el-GR">ΠΑΤΡΟΚΛΟΣ</SPAN><SPAN LANG="el-GR"> ΓΙΑΤΡΑΣ</SPAN></p>
             <p>Ενθάδε εσάπη το – ταλαίπωρο άλλωστε –<br>
             σώμα του σκώπτη Πάτροκλου Γιατρά<br>
             (το πνεύμα του διασώζεται, υποθέτω, σ’ αυτούς τους στίχους).<br>
             Έγραφε επιτύμβια για ομότεχνους<br>
             –  συνθέσεις μιας ορισμένης αναζήτησης.<br>
             Διαβάτη, θα ένιωσες, πιστεύω, ότι οι θλιβερές <br>
             σελίδες του, που βρίσκονται ανοιχτές εμπρός σου,<br>
             δεν είναι χωρίς κάποια χρησιμότητα.</p>
             <p>&nbsp;</p>

  
<p> PATROCLUS YIATRAS</p>
 
<p> Here rots the – nonetheless unfortunate – <br>
  body of Patroclus Yiatras the cynic <br>
  (his spirit survives, I suppose, in these lines). <br>
  He wrote epitaphs for fellow poets – compositions<br>
 displaying a  certain engagement. <br>
  I believe you may have felt, passer-by, that his <br>
 piteous pages, now lying open before you, <BR>
  are not without some usefulness.</p>
 <p LANG="en-US"> <em>Translated by Paschalis Nikolaou &amp; Richard Berengarten</em></p>         
 

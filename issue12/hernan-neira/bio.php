                  <p ALIGN="JUSTIFY"> Hernán Neira posee una doble vocación de escritor y profesor universitario. </p>
                  <p ALIGN="JUSTIFY"> Chileno, nacido en Lima (1960), se licenció en filosofía en la Universidad Católica de Santiago, donde además dirigió <em>Perspectivas</em>, revista estudiantil de oposición a la dictadura. Desde 1985 a 1992 vivió en París, donde se doctoró en filosofía. </p>
                  <p ALIGN="JUSTIFY"> Ha enseñado en Francia, en el Institut d'études politiques (1991-1992), en la Universidad Austral de Chile (1993-2007) y ha sido profesor invitado en numerosas universidades. </p>
                  <p ALIGN="JUSTIFY"> En sus libros y artículos aborda una amplitud de géneros (novela, filosofía, periodismo, artículos de coyuntura cultural y política). Ha publicado los relatos <em>A golpes de hacha y fuego</em> (Editorial Andrés Bello, Santiago, 1995) y las novelas <em>El sueño inconcluso </em>(Editorial Planeta, Santiago, 1999) y <em>El naufragio de la luz </em>(Ediciones B, Barcelona, 2004). </p>
                  <p ALIGN="JUSTIFY"> Es autor de los ensayos filosóficos <em>El espejo del olvido </em>(Dolmen Ediciones, Santiago, 1997), <em>La ciudad y las palabras </em>(Editorial Universitaria, Santiago, 2004) y <em>Visión de los vencidos, Estudio y trascripción de las &ldquo;Memorias&rdquo; de Juan Bautista Tupac Amaru </em>(Editorial Universidad de Santiago, 2009). También ha colaborado en los diarios <em>El Mercurio</em> (Chile), <em>El País</em> (España), revista <em>Ecos de España y Latinoamérica</em> (Alemania). </p>
                  <p ALIGN="JUSTIFY"> Su trabajo universitario se centra en filosofía político-social, teoría literaria y cultura latinoamericana. Sobre esas materias ha publicado artículos académicos en Chile, Estados Unidos, España, Países Bajos y Cuba. Conferencista en distintos países, desde 2008 es profesor de filosofía política en la Universidad de Santiago de Chile. Ha recibido cuatro  veces apoyo del Fondo Nacional de Investigación, Ciencia y Tecnología. </p>
                  <p ALIGN="JUSTIFY"> En 2003, con la novela <em>El naufragio de la luz</em>, ganó por unanimidad el Premio de novela Las Dos Orillas, dado por cinco editoriales europeas durante el <em>Salón Internacional de Libro Iberoamericano de Gijón. El naufragio de la luz</em>, publicado por primera vez en España, ha sido<em> </em>traducido al francés, italiano, portugués y griego.</p>
                  <p>La Revista <em>Lire</em> (<em>Leer</em>), publicada en Francia, ha dicho de él y de dicha novela: &ldquo;Ha leído a Conrad. Su descripción de las tierras salvajes, su visión de la naturaleza y de la crueldad humana son un homenaje a éste. Pero Hernán Neira no se contenta con saludar al maestro; se apodera de esta isla cruel y fascinante para hacer de ella el edén y el cementerio de los protagonistas, su locura y última esperanza de vida, con un lenguaje que se parece al rugido de las olas, a la vez rugoso y sinfónico&quot; (mayo 2004).</p>                  </td>

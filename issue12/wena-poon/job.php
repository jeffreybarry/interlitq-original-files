            <td colspan="4" rowspan="2" class="texto2" valign="top">
            
            <p ALIGN="JUSTIFY">A theater director friend had been urging me to write a story for her to stage.  She had seen a bullfight – quite by accident – years ago during her honeymoon in Madrid and never forgot its theatrical possibilities. Could I write a story about a matador?</p>
             
            <p ALIGN="JUSTIFY">&ldquo;But I&rsquo;ve never been to Spain!&rdquo; I protested.  &ldquo;I&rsquo;m not interested. I hate bullfighting.  Look, I can do England, America, France.  Not Spain. I can&rsquo;t even speak a word of Spanish! Find somebody else.&rdquo;</p>
             
            <p ALIGN="JUSTIFY">&ldquo;You are the only person I know who can do it,&rdquo; she said. </p>
             
            <p ALIGN="JUSTIFY">*</p>
             
            <p ALIGN="JUSTIFY">On July 19, 2009, I arrived in Valencia and sent her the following email:</p>
          
            <p ALIGN="JUSTIFY"><em>Just arrived this evening and went to the bullfights right away. &nbsp;Very monotonous and cruel. &nbsp;We left after 4 rounds (4 bulls). &nbsp;I get the idea - it's exactly like you said. However, for all the cruelty, I did not find it stimulating, passionate, or exciting - it was ritualized, slow, predictable...kind of like watching the Harvard-Yale football game. </em></p>
            
            <p ALIGN="JUSTIFY"><em>P.S. Valencia very beautiful, romantic.</em></p>
             
            <p ALIGN="JUSTIFY">During that trip, I was writing the fifth volume of my science-fiction action adventure series starring talking animals, called <em>Biophilia, </em>a term in biology for man&rsquo;s love for living things.</p>
            
            <p ALIGN="JUSTIFY">&ldquo;You know what bullfighting is?&rdquo; I said to my husband, who had seen his first Spanish bullfight and declared it would be his last.  &ldquo;It&rsquo;s the opposite of biophilia.  I can&rsquo;t write this story.  It&rsquo;s sick.&rdquo;</p>
            
            <p ALIGN="JUSTIFY">Little was I to know that within 11 days, I would have finished the first draft of my new novel, <em>Alex y Robert</em>, and undergo a radical change in my beliefs.</p>
           
            <p ALIGN="JUSTIFY">*</p>
            
            <p ALIGN="JUSTIFY">A few days after I wrote that email, I saw my second bullfight, this time in Madrid. I thought I&rsquo;d give it one last try. </p>
           
            <p ALIGN="JUSTIFY">I would never have written <em>Alex y Robert</em> if I hadn&rsquo;t seen the second bullfight.</p>
            
            <p ALIGN="JUSTIFY">*</p>
            
            <p ALIGN="JUSTIFY">I grew up in Asia.  Spain was not on my radar screen.</p>
            
            <p ALIGN="JUSTIFY">You will find French tea shops in Tokyo. You will find Italian rococo furniture in Hong Kong. You will find German luxury cars in Beijing.  In Singapore, an English-speaking country, when we say &ldquo;European&rdquo;, we often really mean England, France, Italy – and to a lesser extent, Germany.  That&rsquo;s because for decades those countries have been very good at exporting their products to the wealthy metropolises of Asia. Spain, in contrast, had been locked up by Franco for much of the 20th century. </p>
            
            <p ALIGN="JUSTIFY">&ldquo;Spain suffers from a perception problem,&rdquo; remarked one of my British friends.</p>
             
            <p ALIGN="JUSTIFY">I knew all about perception problems as someone from Singapore living in America. In some ways, I was probably as good of a candidate to write about Spain as any other.  My fiction, up to that point, was about the pain of being marginalized and misunderstood by mainstream culture.</p>
           
            <p ALIGN="JUSTIFY">Of course, I was equally guilty of misperceiving Spain.  Until I began writing <em>Alex y Robert</em>, I only knew about Spain through the movies of Pedro Almodovar.  The Anglophone world sees Spain through the lens of his camera.  It is a fact that makes the Spanish tear their hair out.</p>
             
            <p ALIGN="JUSTIFY">And of all the Spanish clichés, bullfighting must be the worst of all.  Even I knew that.</p>
            
            <p ALIGN="JUSTIFY">*</p>
            
            <p ALIGN="JUSTIFY">It is breathtaking the first time you see a bull gore a matador. </p>
            
            <p ALIGN="JUSTIFY">The matador was a young boy called Tomasito. Technically – although I did not know it at the time – he was not a matador, but a <em>novillero</em>, a novice who had not graduated to official matador status.  I had paid five Euros to see him and two others kill six bulls in Madrid&rsquo;s Las Ventas one Sunday evening.</p>
            
            <p ALIGN="JUSTIFY">To see this little teenager hit by a bull, tossed in the air, slammed into the dust, all in less than two seconds, was as obscene and shocking as watching a real life cop getting shot point blank by a felon.  Everybody screamed.</p>
            
            <p ALIGN="JUSTIFY">Tomasito, blood streaming from his leg, took his shoes off and went on with the performance.  I was to recollect it and describe it in a scene in <em>Alex y Robert</em>. The president of the <em>plaza</em> awarded him a bull&rsquo;s ear for his bravery.  He paraded his trophy around the ring, so close to me that I could almost touch him.  His face was wet and white, his lips completely bloodless, yet he was glowing with a kind of effervescent, released joy that I can only describe as post-coital.</p>
            
            <p ALIGN="JUSTIFY">What makes this kid tick? I thought.  I was sick with pathos, yet strangely elated.  As I left the bullring, every nerve in my body was tingling.  I couldn&rsquo;t believe the experience cost only five Euros.  I couldn&rsquo;t even describe the experience I  just had. </p>
            
            <p ALIGN="JUSTIFY"><em>I need to know what I am feeling, </em>I thought.  By the time I climbed into a cab, I was thinking, <em>I could do this.</em></p>
            
            <p ALIGN="JUSTIFY">*</p>
            
            <p ALIGN="JUSTIFY">&ldquo;The matador has to be a woman,&rdquo; I told my theater director friend when we met in Madrid.  &ldquo;I can&rsquo;t get into the mind of a Spanish guy, let alone a matador. The protagonist has to be a woman – not Asian, obviously. I can&rsquo;t do Spanish, but I can do American. She&rsquo;ll have to be an outsider, trying to break in.&rdquo; </p>
             
            <p ALIGN="JUSTIFY">&ldquo;Are there women matadors?&rdquo;</p>
             
            <p ALIGN="JUSTIFY">&ldquo;There better be.&rdquo;  I said suddenly, &ldquo;Look, you&rsquo;re a woman artist.  I&rsquo;m a woman artist.  If we make this a story about a man, it&rsquo;s a lost opportunity.&rdquo; </p>
             
            <p ALIGN="JUSTIFY">What I didn&rsquo;t tell her was that I found Hemingway really boring. If I had to do this at all, I wanted to do something deliriously modern and youthful.  I would write about teenage matadors – angry, bored, rebellious, listening to iPods while waiting for their turn to fight a bull in the ring.  I would write about an American girl who wants to be a matador, and a Spanish boy matador who can&rsquo;t stand bullfighting.  Separated by the Atlantic Ocean, they&rsquo;ll rely on Twitter, Facebook, and SMS to forge a glorious alliance against parents and the limitations of their respective societies. </p>
            
            <p ALIGN="JUSTIFY">But underneath the merry, modern clutter, I had a serious purpose.  I couldn&rsquo;t stand that Spain and bullfighting was still being viewed by the Anglophone world in the hellish half-light of the nineteen fifties.  I could never stand it when Westerners did it to Singapore. </p>
            
            <p ALIGN="JUSTIFY">Above all, I was interested in that strange, unsettling feeling that plagued me that night in Las Ventas. I was fascinated by the moral ambiguity, the gray area – in short, I was determined to hurtle into the zone of utter discomfort.</p>
             
            <p ALIGN="JUSTIFY">*</p>
            
            <p ALIGN="JUSTIFY">By the time I flew back to the US, eight days later, I was close to finishing the first draft of 28,000 words.  I thought it would be a novella.</p>
            
            <p ALIGN="JUSTIFY">Shortly thereafter, my theater director friend and I met in San Francisco. </p>
             
            <p ALIGN="JUSTIFY">&ldquo;Well?&rdquo; I asked.</p>
            
            <p ALIGN="JUSTIFY">&ldquo;This is not a book,&rdquo; she said, taking the manuscript out of her handbag.  &ldquo;This is a <em>movie!&rdquo;</em></p>
             
            <p ALIGN="JUSTIFY">I consider that a compliment of the highest order.</p>
           
            <p ALIGN="JUSTIFY">*</p>
            
            <p ALIGN="JUSTIFY">In October that year, I was invited to by Singapore&rsquo;s National Arts Council to speak in London&rsquo;s Southbank Centre with other Singapore writers.  After the talk, I had tea with Southbank&rsquo;s literary events director.</p>
            
            <p ALIGN="JUSTIFY">&ldquo;What do you have coming out next?&rdquo; she asked. </p>
           
            <p ALIGN="JUSTIFY">&ldquo;Well, I just wrote a story about a woman matador in Spain.&rdquo;</p>
            
            <p ALIGN="JUSTIFY">Her eyes lit up.</p>
            
            <p ALIGN="JUSTIFY">*</p>
            
            <p ALIGN="JUSTIFY"><em>Corrida. Capea. Capote. Corazon. Calor.</em></p>
            
            <p ALIGN="JUSTIFY">&ldquo;I am slowly picking up Spanish,&rdquo; I announced proudly to a Spanish friend.  &ldquo;But all the words I know are bullfighting terms and love song lyrics.&rdquo;</p>
            
            <p ALIGN="JUSTIFY">&ldquo;Not a bad place to start,&rdquo; he said, laughing.</p>
             
            <p ALIGN="JUSTIFY">*</p>
             
            <p ALIGN="JUSTIFY">In late October, I was touring my second book at literary festivals in Singapore and Bali.  I had plenty of time to kill in between.  One night, I sat up in bed reading <em>Alex y Robert</em>. My publisher had already decided to release it as a short book, to be launched at Southbank&rsquo;s London Literature Festival.</p>
            
            <p ALIGN="JUSTIFY">I was heartsick for Spain, a country I had only visited for eight days.  It seemed very far away from Asia.  I thought about the cities that I had yet to visit.  I began writing more chapters.</p>
            
            
            <p ALIGN="JUSTIFY">*</p>
             
            <p ALIGN="JUSTIFY">March 2010. I was back in Valencia, timing my visit during the annual Las Fallas festival.  In the novel, Roberto, the teenage matador who hates bullfighting, remembers a goring injury:</p>
            
            <p>&ldquo;<em>It was during Fallas. I was in hospital. They had just finished operating on me. They had put me to sleep, but despite the medicines I was woken up by fireworks at two in the morning. The sky rocked with the sound of their thunder for hours.&rdquo; </em></p>
            
            <p>I wrote this at night, sitting in the darkened living room of an Edwardian pension, still drenched with sweat from the evening&rsquo;s bullfight.  The room flickered and rocked with the scatter-burst of fireworks in the street below.  Suddenly, a crowd burst into spontaneous singing.  I was typing as fast as I could:</p>
             
            <p>&ldquo;<em>I could hear young people singing in the streets. I couldn&rsquo;t sleep. I tried to think of what I really wanted to do with my life. I realized I wasn't good at anything, that there was nothing that I was really interested in. It was too late for me to try to get into high school. Going to the other side of the world was the only way I could start over.&rdquo; </em></p>
           
            <p>Against the wishes of his parents, Roberto, who had never finished school, quits bullfighting and enrolls in an American university. In America, Alejandra boards a plane to Spain to become a matador, against the wishes of her family, her society, and even the Spanish themselves. </p>
            
            <p ALIGN="JUSTIFY">It might be an artistic risk, but it was the only kind of novel I could write.</p>
           
            <p ALIGN="JUSTIFY">*</p>
             
            <p ALIGN="JUSTIFY">I&rsquo;ll never understand Spain, the Spanish, or bullfighting. <em>Alex y Robert</em>, which eventually became a novel of 85,000 words, does not purport to reveal the truth about these things.  It does not seek to glorify bullfighting or defend it. My readers have remarked to me that it is not even about bullfighting. </p>
            
            <p ALIGN="JUSTIFY">It is really about what I have learned.  In putting myself in the zone of utter discomfort, I realized just how much the urban, Anglo-American world continually and subconsciously defines itself through its relationship with animals: whether at the dining table, at the pet store, or at the bullfights. Our absolute power over them makes us uncomfortable.  That is why we regulate  - through customs, and sometimes through laws - which animals can be eaten, for what purpose they can be killed, how they can be killed, who is allowed to kill them, and who is allowed to see them being killed.  Our customs about animals broadcast our own civilized nature and reinforce our group identity – any other tribe who has different rules is automatically barbaric.</p>
           
            <p ALIGN="JUSTIFY">Because I was a woman writing about bullfighting, I became interested in why there were hardly any women matadors.  I learned that when men want to keep women out of a masculine-dominated industry, they invariably invoke the same reasons: limitations of a woman&rsquo;s physical strength, lack of aggression, lack of ambition, lack of interest, appearance of impropriety, susceptibility to injury, duties to child-bearing, family, and household tasks, and – of course – her monthly &ldquo;incapacitation&rdquo; due to menstruation.  The attitudes in Spain about women bullfighters mirror those well-known in my profession – law – in America fifty years ago.  There is no point training a woman bullfighter, just as there was no point for a woman to graduate from law school back then, because no one will hire her anyway.</p>
            
            <p ALIGN="JUSTIFY">*</p>
            
            <p ALIGN="JUSTIFY">Introduce a controversial topic, and people fall very quickly into two polarized camps of &ldquo;Them&rdquo; versus &ldquo;Us.&rdquo;  In America, for years we have had to suffer the tidy catchphrase, &ldquo;You&rsquo;re either with us or against us&rdquo;, which continue to cripple dialogue even as it provides everyone with the cheap thrills of righteous indignation.</p>
             
            <p ALIGN="JUSTIFY">The story of Alex and Robert, and their transatlantic friendship, is my hope for a new world order. I have given &ldquo;Us&rdquo; a Spanish matador who hates killing bulls, and I present to &ldquo;Them&rdquo; a great American woman bullfighter. In laying out these contradictions, I offer nothing but a cheerful incantation against demonization, discrimination, and xenophobia. </p>
            
            <p ALIGN="JUSTIFY">For it is already the 21st century. Technology and travel is at our disposal in a way that writers of prior eras would have envied.  In many societies, both men and women are free to take advantage of ever-increasingly efficient and inexpensive means of travelling, communicating and understanding each other&rsquo;s point of view. Now, more than ever, there are no mysteries, no barbarians, no &ldquo;Other&rdquo;, no excuse for denouncing what we do not understand. We can become comfortable with pluralism and even ambiguity, because we know that these things can do nothing to threaten the bedrock of our morality.  Our curiosity about other peoples should be stronger than our fear of them.  There is a world, scintillating just beyond the barriers that we have built around ourselves.  If not now – when?</p>

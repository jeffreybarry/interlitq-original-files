            <p ALIGN="JUSTIFY"><em>The Singapore-born American novelist Wena Poon talks about an impossible commission to write about Spanish bullfighting and how she went to Spain and produced a 21</em><em>st</em><em> century novel – with a woman in the lead role.</em></p>

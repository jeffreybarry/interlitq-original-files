            <p><SPAN LANG="en-US"><em>If you wake up with the face of the fox you will be very lucky</em></SPAN></p>
            
            <p><SPAN LANG="en-US">At four, she woke with the thunder. Who else was alive? In the step-well, a knife drew the lightning to it. The clouds were caravanserai. Steps crumbled. The thunder roamed. At daybreak, she looked into the mirror.</SPAN></p>
           
            <p><SPAN LANG="en-US">§</SPAN></p>
            
            <p><SPAN LANG="en-US">In the desert, no one forgets a drought. She could hear them laughing, the travelling men, happy to have water, and shelter for the night. There was no doubt that they were ghosts. </SPAN></p>
          
            <p><SPAN LANG="en-US"><em>The word &lsquo;ghost&rsquo; is a sonic thing </em></SPAN></p>
           
            <p><SPAN LANG="en-US">Their voices were strong and high enough to reach her in her bed behind the fort walls. In the lower levels, the spy lurked. He drank with the king&rsquo;s man, who knew he was a spy. They spoke from their hearts, as if they were brothers. In the morning, one of them would be dead. </SPAN></p>
          
            <p>&ldquo;<SPAN LANG="en-US"><em>Ask me for three things&rdquo; the God of Death said</em></SPAN></p>
           
            <p><SPAN LANG="en-US">She wished she could travel towards the wound. She wished she were a sonic thing. She wished she could think of the third wish, the hardest, most difficult of all. </SPAN></p>
           
            <p><SPAN LANG="en-US">§</SPAN></p>
           
            <p><SPAN LANG="en-US">Every morning she looked for the fox in the mirror. Instead, she saw the bat. A flying fox. Was that lucky enough? Radar, that sonic thing, kept her from knocking into walls. All space confines, she thought, wishing blindness were not the price she had to pay for having one wish come true, in part. The God of Death had kept his word. If you could keep one word close to you, as if it were a living being, which one would it be? Whale? Eel? Princess? Some mornings, she saw the squirrel. Its many names leapt towards her. She chose none of them.</SPAN></p>
          
            <p><SPAN LANG="en-US">§</SPAN></p>
           
            <p><SPAN LANG="en-US">She walked past the gutter pretending it was not there. She stood before the ruined mansion pretending she was a merchant. She read the sign that said &lsquo;Saikiley kiraye par milti hai&rsquo; and pretended she was a cyclist, about to hire one. She looked at the white geese and the black pigs, pretending the disinterest of a vegetarian. She took pictures of a buffalo, a camel, a tree and a girl, all in the same frame, and she could not pretend it was not beautiful. She saw the orange flag and the crows, and her gorge rose. </SPAN><SPAN LANG="en-US"><em>Monks ate otter pretending it was fish </em></SPAN><SPAN LANG="en-US">At lunch, she ate nothing.</SPAN></p>
          
            <p><SPAN LANG="en-US">§</SPAN></p>
          
            <p>It was the night of the nightmare. In rooms separated by centuries, named after roses, pleasure, palaces, peace, precious stones, dowry, swans—the nightmare took root, shifted shape, stole souls, ate great pieces of flesh and heart. <em>A gigantic serpent can take the shape of a leech </em>As it moved from room to room, palace to palace, it wore the wings of the hornet. That was the only time it made a sound, before it entered the darkness behind the next human&rsquo;s eyes, before the next sleeper turned on her back, on his side, and began to fight for breath. <SPAN LANG="en-US">Take everything away. Replace the night with fire. See at the bottom of the ocean The Flaming Mare, and bow to it. Agni, purify everything. </SPAN></p>
            
            <p><SPAN LANG="en-US">§</SPAN></p>
         
            <p><SPAN LANG="en-US"><em>In my language, Snow White is Red Flame</em></SPAN><br>
            <SPAN LANG="en-US">And in mine, she wants to say. Only, I never knew it, until now.</SPAN></p>
          
            <p><SPAN LANG="en-US">§</SPAN></p>
           
            <p><SPAN LANG="en-US">In Gaelic, she learns, there is no difference between &ldquo;in it&rdquo; and &ldquo;in him&rdquo;. In Irish &ldquo;ajar&rdquo; could mean wide open. In Bengali, there is no &ldquo;he&rdquo; and &ldquo;she&rdquo;. In Welsh, there is no &ldquo;ooze&rdquo;. The sun knows no gentleness. Tamil has no word for face. Sanskrit has no word for mouth. She could climb and climb and still get nowhere nearer the truth of what lies hidden inside her shifting self.</SPAN></p>
            
            <p><SPAN LANG="en-US">§</SPAN></p>
           
            <p>&ldquo;<SPAN LANG="en-US">To translate,&rdquo;</SPAN><SPAN LANG="en-US"><em> </em></SPAN><SPAN LANG="en-US">he said, </SPAN><SPAN LANG="en-US">&ldquo;is to move a body from one place to another.&rdquo;</SPAN><SPAN LANG="en-US"><em> </em></SPAN></p>
            
            <p><SPAN LANG="en-US">Is that why she doesn&rsquo;t live in the city anymore? Why she roams the scrub hills, looking for brush to burn? Is that why she has to count—nose, eyes, teeth, toes— to figure out what she left behind when she moved? It all adds up, intact. And yet, that must be why she feels like someone else, like someone else moved in, speaking a lingo that shocks her, all those words she thought she&rsquo;d forgotten. </SPAN></p>
           
            <p><SPAN LANG="en-US">The new occupant lives in her body like she owns it, fills out her cheeks, hollows out her ribs, makes her limbs lighter, then heavier, makes her want to walk instead of sleep, makes her want to weep instead of talk, makes her want to move a body from one place to another. Maybe it&rsquo;s just a way of getting her old self back. Maybe nothing will be the same again.</SPAN></p>
            
            <p><SPAN LANG="en-US">§</SPAN></p>
         
            <p>&ldquo;You can only connect with someone when you&rsquo;re in your body,&rdquo; the woman with the brook racing through her voice said.<em> </em><SPAN LANG="en-US">Against the blue light, her fingers were flutes, antlers of fleeing deer. As she spoke, her words turned into butterflies and died. </SPAN></p>
            
            <p>&ldquo;<SPAN LANG="en-US">Ask me for three things,&rdquo; the God of Death said.</SPAN><SPAN LANG="en-US"><em> </em></SPAN><SPAN LANG="en-US">So the butterflies asked to be swallows and the swallows asked to be hawks and the hawks simply asked for the skies that they already owned, wheeling, snapping free as they flew.</SPAN></p>
            
            <p>&ldquo;<SPAN LANG="en-US">You run towards a cliff and you&rsquo;re flying in the air,&rdquo;</SPAN><SPAN LANG="en-US"><em> </em></SPAN><SPAN LANG="en-US">the man said in the voice of sorrow, and it was true of poetry, of love, and of the absurdity of trusting a wire to fly you over the hills, steel-humming, safe.</SPAN></p>
            
            <p>&ldquo;<SPAN LANG="en-US">In my language,&rdquo;</SPAN><SPAN LANG="en-US"><em> </em></SPAN><SPAN LANG="en-US">she said, &ldquo;there could be</SPAN><SPAN LANG="en-US"> five different words for love.&rdquo;</SPAN><SPAN LANG="en-US"><em> </em></SPAN><br>
           <SPAN LANG="en-US">And she tasted them on her tongue as she spoke them aloud: </SPAN><br>
             &ldquo;<SPAN LANG="en-US">Bondhu. Bhai. Bhalobasha. Bhogobaan. Bhoboghurey.&rdquo;</SPAN></p>
           
            <p><SPAN LANG="en-US">§</SPAN></p>
            
            <p><SPAN LANG="en-US"><em>the moon takes the sea away</em></SPAN><br>
            <SPAN LANG="en-US">and leaves lovers gazing at wet sand</SPAN><br>
            <SPAN LANG="en-US"><em>the forest loses its language</em></SPAN><br>
            <SPAN LANG="en-US">and leaves whisper no more</SPAN></p>
            
            <p><SPAN LANG="en-US">§</SPAN></p>
           
            <p><SPAN LANG="en-US">Behind her knee the lizard skin begins to grow again. On her elbows the dragonhide. That dark place in Lanark she knew so well. Pigeon-eyes, parrot-beak, what next? The village inside her bristles, dies, rots. Here isn&rsquo;t here, anymore. It&rsquo;s where she cannot be.</SPAN></p>
           
            <p><SPAN LANG="en-US">§</SPAN></p>
           
            <p><SPAN LANG="en-US">Lokkhi meye, do as I tell you, the old woman says. Touch your palms to your eyes. </SPAN><SPAN LANG="en-US"><em>Goddess Lakshmi lives in your palms </em></SPAN><SPAN LANG="en-US">Press them together when you greet your elders. Grease no one&rsquo;s palm. Cross my palm with silver? Only gypsies say that! Palmistry, that&rsquo;s the thing. Palm leaf parchment. Palm jaggery, the sweetest on the planet. A palmful of water to the travelling man, you understand? Lokkhi meye, let me read your palm. Oh, but what I see is not good, my girl! I see flying foxes, and thunderbirds, I see arrowheads, and water jars. I see black buffalos with slobbering jowls, I see, no I hear, distant howls. Press your palms to your eyes, my girl. God help you. </SPAN></p>
          
            <p><SPAN LANG="en-US">§</SPAN></p>
           
            <p><SPAN LANG="en-US">In </SPAN><SPAN LANG="en-US"><em>two parallel universes</em></SPAN><SPAN LANG="en-US"> </SPAN><br>
            <SPAN LANG="en-US">she </SPAN><SPAN LANG="en-US"><em>exists as two simultaneous things</em></SPAN><SPAN LANG="en-US">. </SPAN></p>
           
            <p><SPAN LANG="en-US">This isn&rsquo;t the stuff of sci-fi, she tells the child, it&rsquo;s poetry. </SPAN><br>
             <SPAN LANG="en-US">Like a mirror? the child asks. </SPAN></p>
           
            <p><SPAN LANG="en-US">Yes! she says. Have you ever woken up and seen yourself </SPAN><br>
             <SPAN LANG="en-US">in the mirror with the face of a fox? </SPAN></p>
          
            <p><SPAN LANG="en-US">Yesterday I did, the child says, not expecting to be believed. </SPAN><br>
             <SPAN LANG="en-US">You did? she says. You&rsquo;re lucky. You&rsquo;re so, so lucky! </SPAN></p>
            
            <p><SPAN LANG="en-US">And she weeps, while the child wonders what tears </SPAN><br>
             <SPAN LANG="en-US">have to do with mirrors and foxes, </SPAN></p>
            
            <p><SPAN LANG="en-US">with parallel universes, </SPAN><br>
             <SPAN LANG="en-US">simultaneous things, </SPAN></p>
            
            <p><SPAN LANG="en-US">and all </SPAN><br>
            <SPAN LANG="en-US">that jazz.</SPAN></p>
            
            <p><SPAN LANG="en-US">§</SPAN></p>
            
            <p><SPAN LANG="en-US">At</SPAN><SPAN LANG="en-US"> 6:49, the violet flower with the tentacles opened. </SPAN><br>
             <SPAN LANG="en-US">She woke. She wondered, for the space of a skipped heartbeat,</SPAN><br>
             <SPAN LANG="en-US">if she would ever witness such joy again. </SPAN><br>
             She slept. It was all she could have wished for. </p>
            

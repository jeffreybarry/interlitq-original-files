            <p><strong>The Unrepeatable Cloud</strong></p>
             
            <p>Somewhere, a silence of walls falling <br>
            dividing the skies. Prone. No need to see <br>
            the blue reflected in the brown grass to know </p>
            
            <p>that the clouds are fickle and carry only rumours.<br>
            Don&rsquo;t look around. Don&rsquo;t look up. <br>
            Divination is inexact, not even a science.</p>
             
            <p>Only the messenger in the cloud knows<br>
            how she lost her voice and how she let<br>
            the thunder become her ventriloquist.</p>
            

            five of them sitting on chairs<br>
            and not one purifying fire <br>
            between them.</p>
             
            <p LANG="en-US">Their chants were hard stones<br>
            spit out into the air<br>
            their feet churned keeping<br>
            the words up and moving<br>
            like threats</p>
            
            <p LANG="en-US">or curses: once pronounced<br>
            their import<br>
            set in stone.<br>
            &ldquo;This place is cleansed<br>
            of death.</p>
            

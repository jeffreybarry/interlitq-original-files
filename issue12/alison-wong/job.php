            <p><strong>Who Says Old Dogs Can&rsquo;t Learn New Tricks?</strong></p>
            
            <p>Kevin says beagles can&rsquo;t bark. <br>
            The one next door is timid <br>
            and grey-haired but knows how to <br>
            howl, thrown back to his wolf fore<br>
            bears no doubt, not some cute hound <br>
            sniffing out mandarins and <br>
            other recreational <br>
            drugs. &lsquo;Beagles!&rsquo; says Janet. &lsquo;Don&rsquo;t <br>
            be fooled by those gorgeous eyes. <br>
             See my grey hair? My beagle <br>
             failed puppy school. Never take <br>
             a beagle for a walk – it<br>
             will pick up a scent and you <br>
             won&rsquo;t see it again for days!&rsquo;<br>
             Curiosity coupled <br>
             with persistence did not kill <br>
             the cat, merely the beagle<br>
             owner. Ruth Ward sought a pooch<br>
             with intelligence and a <br>
             lot of character, not the<br>
             outrageously spirited<br>
             rogue her beagle turned out to<br>
             be. As secretary of<br>
             the Beagle Club she admits, <br>
             &lsquo;Beagle puppies certainly <br>
             are challenging. Our life was <br>
             utter chaos for the first <br>
             two years but I would not change <br>
             a thing. I&rsquo;m really concerned <br>
             that so many delightful <br>
             beagles end up in the wrong <br>
             homes. The club has a constant <br>
             flow of beagles in need of <br>
             new, loving, forever homes.&rsquo;<br>
             I look at my new husband.<br>
             My Kevin has silver hair,<br>
             tidy habits, he has an<br>
             intelligent, curious, <br>
             persistent nature. He is <br>
             not timid, just a little <br>
             reserved. &lsquo;Complicated,&rsquo; I<br>
             say, backed up by Myers-Briggs,<br>
             but he won&rsquo;t admit it. &lsquo;I&rsquo;m <br>
             a simple man... Some people <br>
             have such convoluted<em> </em>minds –<br>
             I think they must read too much<br>
             Ezekial.&rsquo; &lsquo;Yes dear,&rsquo; I<br>
             say, smoothing his beagle brow.</p>
             <p>&nbsp;</p>

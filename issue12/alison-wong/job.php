            <p><strong>Meditation on Yangzhou</strong></p>
             
            <p>he sweeps morning <br>
            and night enters his pores <br>
            as he might over </p>
            
            <p>a good book or a map <br>
             of dislocation—if only <br>
             they were </p>
             
            <p>in English<br>
             he walks and loses <br>
             himself </p>
           
            <p>with all this<br>
             Chinese food <br>
             and language oh </p>
            
            <p><em>Wei</em> he says<br>
             over the line<br>
             she is used</p>
             
            <p>to <em>Crikey</em><br>
             <em>My foot is giving me </em><br>
             <em>gyp</em></p>
             <p>&nbsp;</p>
             <p><small>Yangzhou is a city along the Yangtse River between Nanjing and Shanghai. Wei is the greeting used when answering the phone in Mandarin.</small></p>

           <p><strong>Passion from the tomb</strong></p>
            <p><SPAN LANG="en-US">All that we dreamt of died with you in the tomb,</SPAN><br>
            our passion pulverized, now dust,<br>
            the only suitor&nbsp;that pays me any heed, <br>
            this worm that feeds on the corpse of my heart.<br>
            And yet&nbsp;I court&nbsp;this pain that gives&nbsp;me pleasure, <br>
            rending my guts to praise you beyond&nbsp;measure,<br>
            a&nbsp;fire that consumes&nbsp;and which, I never&nbsp;doubt,<br>
            would, if extinguished, see my light snuffed out.</p>

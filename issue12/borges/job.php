            <p><strong> The Cloisters</strong></p>
            
            <p>From a place in the kingdom of France<br>
            They brought the stained glass and the stones<br>
            to build on the island of Manhattan<br>
            these hollow Cloisters.<br>
            They are not apocryphal.<br>
            They are faithful monuments to a nostalgia.<br>
            An American voice tells us<br>
            to pay for what we want,<br>
            because this whole building is an invention<br>
            and the money as it leaves our hand<br>
            will become shekels or vanish like smoke.<br>
            This abbey strikes more terror<br>
            than the pyramid of Giza<br>
            or the labyrinth at Knossos,<br>
            because it too is a dream.<br>
            We hear the fountain&rsquo;s murmur,<br>
            but that fountain&rsquo;s in the Patio of the Orange Trees<br>
            or the epic of <em>Der Asra</em>.<br>
            We hear clear Latin voices<br>
            but those voices echoed in Aquitaine<br>
            when it was a stone&rsquo;s throw from Islam.<br>
            We see in the tapestries<br>
            the resurrection and the death<br>
            of the white condemned unicorn,<br>
            because time in this place<br>
            does not obey an order.<br>
            The laurel trees I touch will flower<br>
            when Leif Ericsson sights the sands of America.<br>
            I feel a touch of vertigo.<br>
            I am not used to eternity.</p>
            

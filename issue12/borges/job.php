            <td colspan="4" rowspan="2" class="texto2" valign="top">
            
            <p>I spent my childhood in and around the Cloisters--at the summit of ample Fort Tryon Park, my playground in the 1950s--so imagine my pleasant surprise to discover this late poem of Jorge Luis Borges.  It was translated by W. S. Merwin (recently chosen U.S. poet laureate as of 2010) for <em>The Selected Poems</em> (Penguin, 1999) but I didn&rsquo;t see this translation until after I had worked through a version of my own.   I incorporated some of Bill Merwin&rsquo;s 'touches' and was pleased to see a few of my <em>trouvaille</em> were the same as his, such as &quot;touch of vertigo&quot;—but ultimately I left some of my own diverging choices, such as &ldquo;hollow&rdquo; for concave.</p>
            
            <p>Borges must have visited the Cloisters on one of his several trips to New York in the 1970s, when he would often stay at the house of his then publisher Jack MacRae III, in the West Village.  The poet was totally blind by then, but this did not keep him from traveling.  On the contrary, as he was now world-famous and would receive constant invitations, travel was a pleasant way for him to escape the solitude of blindness.  It was also a guarantee of companionship, which in the 1970s meant, aside from all the people he would meet, his fellow traveler and caretaker Maria Kodama, who would eventually become his wife. </p>
            
            <p>In his blindness, Borges was paradoxically a very observant traveler who would take in essential qualities of whatever monument or city he was visiting, making use of the other senses, as well as whatever he knew or was told about the place and its history.  When he came to Yale University in the early 1970s—his host was his friend and biographer Emir Rodriguez Monegal—I had the occasion to observe Borges as world traveler.  We were all having lunch at a professor&rsquo;s house in New Haven, and it had recently snowed.  Borges asked to go outside in the snow.  I offered to accompany him: the two of us stood in the bare white garden, and picked up snow with our hands, and made snowballs, and various comments; I remember my delight at seeing Borges&rsquo; almost childlike curiosity and joy when he felt the snow and held it in his hands. </p>
            
            <p align="right">Suzanne Jill Levine</p>

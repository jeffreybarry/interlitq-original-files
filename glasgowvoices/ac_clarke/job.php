            <br><p><strong>Southside Tailor</strong></p>
            <p>The dummy by the window has worn<br>
              its hessian jacket longer than he remembers.<br>
              It still has pride of place among<br>
              the heavy bales which he'll roll out<br>
              for select customers, gripping the cloth<br>
              between finger and thumb as he might pinch <br>
              the cheek of a favourite grand-child.</p>
            <p>The crack in the ceiling creeps<br>
              over that pale brown stain where a pipe leaked<br>
              and in the back of the shop his son<br>
              already middle-aged is machining a hem<br>
              for the bride's mother at the last wedding<br>
              the synagogue will see before revamp -<br>
              flats for the elderly. When he shuts shop </p>
            <p>he'll buy matzos and pickled cucumbers <br>
              from the deli which has struggled to survive<br>
              all the years he's been here. A thread straggles <br>
              between his lips, the way a cigarette<br>
              might droop. In yellowed light<br>
              he stands there, small, equivocal,<br>
              Fifties patterns fading on his walls.</p>
            <p>&nbsp;</p>
           
            <p><a name="samuel"></a><strong>Samuel Dow</strong><br>
              <em>After  whom four pubs in Glasgow are named</em><strong> </strong></p>
            <p>Who was Samuel Dow?<br>
              Did he brew fine ales? <br>
              Somehow I can't see it.</p>
            <p>I picture him in frock-coat and stiff  wing-collar<br>
              beetling his brow<br>
              over the Times or the Scotsman<br>
              in a litter of toastcrumbs, his tea<br>
              cooling beside him in a porcelain cup</p>
            <p>or taking the bow<br>
              at a municipal dinner after giving<br>
              a two hour speech on the profits of boilermaking<br>
              in which he told three heavily laboured  jokes<br>
              and mopped his brow<br>
              repeatedly. <br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Samuel  Dow.<br>
              I bet he didn't allow<br>
              even his wife to call him 'Sammy'. </p>
            <p>I see him seven years old, rebuking his  nurse<br>
              for being over-familiar<br>
              shaking hands with his mother<br>
              before retiring to sleep <br>
              with a book of maxims by his bed.</p>
            <p>At thirty taking his vows<br>
              in a stentorian voice<br>
              while his bride, cowed<br>
              under her tonnage of diamonds<br>
              just whispers<br>
              'I do', not daring<br>
              to contradict.</p>
            <p>Samuel Dow. The weight<br>
              of iron bridges<br>
              massive stanchions<br>
              quadruple chins<br>
              is in those syllables.</p>
            <p>Where is he now?<br>
              And his fob and chain<br>
              his galoshes, his stovepipe hat<br>
              his cigar?</p>
            <p>A C Clarke<br>

              of imminent derangement</p>
            <p>what would you give to shake it loose<br>
              dispense with its theories of attachment</p>
            <p>its persistence is exemplary</p>
            <p>on starched cotton mornings<br>
              in the velvet dusk</p>
            <p>when you scale the cliff of wakefulness<br>

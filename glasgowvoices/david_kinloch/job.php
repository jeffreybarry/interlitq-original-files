          <td colspan="4" rowspan="2" valign="top" class="texto2">&nbsp;<br><p><strong>The  Hangingshaw</strong></p>
            <p>Then  you sleep again and wake, begin the day.<br>
              Walking  past the Hangingshaw, you notice<br>
              again  how provisional the grass and bushes are</p>
            <p>since  they cleared the 'Prefabs' so we could build<br>
              nothing  there, how the space is still mapped out<br>
              in  little plots where enamel sinks and mangels stood.</p>
            <p>'Hay  bab-a-ree-bab, Ma mammy's got a prefab'.<br>
              The  rhyme doesn't linger but a man is lying<br>
              some  way off in a patch of sunlight.He's too far</p>
            <p>away  to tell what's happened, but his suit<br>
              is  visible and he's stretched out, arms by his side,<br>
              attention-like,  right in the centre of a box shaped green.</p>
            <p>He  seems peaceful and at home. Once, before <br>
              the  prefabs, an anti-aircraft gun pounded the sky<br>
              from  here; earlier still the Hangingshaw was just</p>
            <p>a  wood clinging to a hillside giving shade<br>
              to  travellers. And historians dispute its<br>
              spelling  for maybe it was Hagginshaw instead.</p>
            <p>'Hay  bab-a-ree-bab'. Later, in the night,<br>
              there  is a dream with a friendly scuffle<br>
              in  which your opponent suddenly turns</p>
            <p>and  embraces you: there is no pressure<br>
              of  desire or want but an absolutely even <br>
              acceptance  of you as you of him.</p>
            <p>'I  love you', you say, the cushie-doos<br>
              return:  'Hay bab-a-ree-bab', 'Hay bab-a-ree-bab'<br>
              and  you don't need to know what it might mean.</p>
              &nbsp;<br>
            <p>Note:  a &lsquo;cushie-doo&rsquo; is a Scots pigeon<br>
            &nbsp;</p>
            <p>&nbsp;</p>
          <P><a name="sailing"></a><strong>Sailing to Torcello</strong></P>          
          <p>A Doge of work! The scrolls, the endless<br>
            parchments of middle age! Applications and appeals<br>
            blot out the throb of bells, suppress<br>
            the smell of dust and hot cow hide, the peals<br>
            of youthful laughter and dismiss<br>
            the fact that rose gold sky is still held up by angels.<br>
            He doodles briefly, sighs; only a fresco, a cartoon<br>
            by Titian could show all flesh sinking in this salt lagoon.</p>
          <p>A glorified skivvy with a pointy hat, he tackles<br>
            the intractable while all the others trade and trade<br>
            even though each year the ramshackle <br>
            world they hedge and bet is weighed<br>
            in balances by bankers whose chuckles,<br>
            crackles, heckles are commissioned and ignored.<br>
            And so he takes the Vaparetto<br>
            to the holy island of Torcello.</p>
          <p>Meter Theu! Mother of God! No Doge can save<br>
            you, long, sleek and blue in your high iconostasis.<br>
            Gabriel hastens but is not quick enough; Eve<br>
            kneels, is not forgiven. Even, especially, this oasis<br>
            is the first to go: apostles in the nave<br>
            of meadows, trampled poppies in the still mosaic.<br>
            The boy locked in his almond shell will have to swim.<br>
            Cloth for earth is in the Doge's hymn.</p>
          <p>'Star of the seas!', he prays, 'Epitome of virtue';<br>
            the skies are starless and a severed peacock's tail <br>
            floats by, fans out its single iridescent eye to view<br>
            mens' wisdom, giving up on God. Soft braille<br>
            of feathers, drowned lions, is it true<br>
            that heaven only lay beneath us among the kale<br>
            and sandbanks? The canals all run with fire.<br>
          Gold endures but we are out of art and nature.</p>          <P></td>

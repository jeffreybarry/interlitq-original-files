            <br><p><strong>Lady Lazarus Has Wheels</strong></p>
            <p>You  should have known<br>
              the  rough beast<br>
              would  make you<br>
              turn  on the gas;</p>
            <p>and  answered the ad<br>
              of an  Anna Blume<br>
              (Cylinders  Farm<br>
              is  crowless:</p>
            <p>no  kunst<br>
              to  bother you,<br>
              but a  barn<br>
              full  of Merz).</p>
            <p>Turn  blue spokes<br>
              red,  go Deutsch<br>
              shelling  yellow<br>
              phonemes</p>
            <p>into  the dusk.<br>
              What  say the people?<br>
              Lady  Lazarus<br>
              has  wheels!</p>
            <p>&nbsp;</p>
            <p><a name="we"></a><strong>We wore (red)</strong><br>
  &nbsp;<br>
              lipstick (drop-dead) avec pendant cigarette; <br>
              diamanted, gin-martinied (olive-stillhettoed)&mdash;<br>
              all high-stem gilded; lush black fan-tailed <br>
              frocks, all cocktailed, jet-beaded, backlessed;<br>
              exhumed refuge, slash-sleeved, plunged<br>
              taffeta, drop-weighted at the hem; skirts fad-<br>
              ed, gold-spun, bias cut</p>
            <p>found our fragment selves (collect-<br>
              ing here, now) lit red, showered lavender,<br>
              twisting in the twisting electric vector<br>
              our mirror ball lifting the Miro in us all,<br>
              our universal Picasso, our luminous piss-<br>
              translucent walls aglow with the barely<br>
              known mothers of us all (WoolfenStein)</p>
            <p>and Money&rsquo;s Too Tight (To Mention) <br>
              belts out&mdash;Valentine Brothers&mdash; and<br>
              lovers in the next room play Fever <br>
              at thirty-three revolutions per minute,<br>
              then 78, <br>
              spread rain-stained coats, red-lined.</p>
            <p>Out-stretched arms, up-raised hands <br>
              cut at the cruel night&mdash;<br>
              jive-signing the peace.<br>
              Stand Down Margaret warred with Mack <br>
              the Knife&mdash;torn fish-netted scar-<br>
              lets kicked like sharks, birled in D<br>
              Ms (de rigeur); rude stars studded,<br>
              sickle moons embossed limp lapels <br>
              (enfolding black halter-necked crepe);<br>
              powder pinked cowboys tracked cactus across shirt<br>
              collars, rode high as if what we wore&mdash;dis-<br>
              avowing metaphor, worn dishabille, <br>
              the dead&rsquo;s fretted retros&mdash;rode us.</p>
              <p>&nbsp;</p>
            <p><a name="triptych"></a><strong>Triptych</strong></p>
            <p><strong>I</strong><br>
              Fifty minutes to break.</p>
            <p>My body, like the building, talks<br>
              to me in twinges, takes<br>
              twenty minutes to numb pressure <br>
              points. My foot is my clock.</p>
            <p>Days dissected by silent weight<br>
              invisibly shifting,<br>
              parcels of hip-strain and knee-<br>
              lock.</p>
            <p>Standing flesh weeps;<br>
              grinds from stasis a sweat.</p>
            <p>Trickles chance from lip, pit, breast.</p>
            <p>Hot springs cool along my spine.</p>
            <p><strong>&nbsp;</strong></p>
            <p><strong>II</strong><br>
              Naked and nothing I live an hour<br>
              in my ribs and right thigh. I stand hip-</p>
            <p>locked come break time, mustn&rsquo;t move from here<br>
              before the chalking of the feet to the floor</p>
            <p>anchors empty columns pinning air<br>
              down to ghost portals I&rsquo;ll fall into ten</p>
            <p>minutes later. <br>
             &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Shove out an old wall<br>
              for the feel of that catch in that hip</p>
            <p>flex a blade then settle the ribs and load<br>
              my thigh. Naked I place my jar</p>
            <p>&nbsp;</p>
            <p><strong>III</strong><strong> </strong><br>
              a space to think in naked<br>
              anchored to the hourly rate</p>
            <p>drift in absent taxi (space, heat, light)</p>
            <p>no it&rsquo;s not for the simulacra of has been<br>
              not the endlessness of vain</p>
            <p>but a space we forge here in studio</p>
            <p>is why the figurative is<br>
              for the model (always abstract)</p>

            <br><p><strong>Fridays</strong></p>
            <p>Shuffling the  pack, getting the psychic ducks in a row,<br>
              The odd  realignment of the deck chairs.<br>
              On Fridays I  set about putting my mind in order,<br>
              Look out across  the windows <br>
              And the working  week becomes the view.</p>
            <p>Not a meadow  with its tiresome buttercup meliorations<br>
              And knotted  green integument.<br>
              Something more  haphazard &ndash; the kind of city<br>
              Spread below my  perch among the books,<br>
              That mess of  old red sandstone and slate, <br>
              Poured concrete  and crabby winter trees<br>
              Shirricked into  passable streets <br>
              And plausible  stabs at avenues.</p>
            <p>Old fears and  anxiousness, the crowd,<br>
              The crowing  insanities of the office jargon jockeys,<br>
              Settle into a  rough semi-order<br>
              Of staggered  tenemented lines and unkiltered crescents.<br>
              Not quite  resolved, but tractable ground for a mind map <br>
              Or the  afternoon rambles of a psycho-geographer.</p>
            <p>Such a city  state of mind is wordscape too, let me warn,<br>
              A mediocre poem  in which the week attempts<br>
              To walk its  ways through the settlements of the tongue,<br>
              To map shanty  towns and rank prosaic conurbations,<br>
              Shape passages  to lead us back to our civic selves.</p>
            <p>So now, and  once again now,<br>
              My feet feel a  way<br>
              Down the  clanking mental fire escape <br>
              To the street,  start to stumble,<br>
          Try and fail to  beat some sense into my week, this place.</p>            <p>&nbsp;</p>
          <p><a name="paisley"></a><strong>Paisley Weavers&nbsp; (At  Sma&rsquo; Shot Cottage, Paisley)</strong></p>
          <p>How did it feel to animate<br>
            These awkward wooden frames,<br>
            To sit on a winter&rsquo;s afternoon<br>
            With a dreich sky skeined low<br>
            Over Shuttle Street,  Paisley,<br>
            And shuffle frosted clouds of breath<br>
            Between the warp and weft of the<br>
            Coarse cloth spread across this loom?<br>
  &nbsp;<br>
            To work a passage from this room<br>
            To the New St. Kirk-yard over the way,<br>
            A criss-crossed million threads<br>
            The patterned shrouding of your days; <br>
            Time the ruthless click clack <br>
            Of the patient treadle going forward and back.</p>
          <p>Surely to God they must have fucked about<br>
            And let things rip, you think,<br>
            The tenants of this emptied space;<br>
            Shaken loose from time to time <br>
            Their measured, manufactured lives <br>
            In misrule and unravelling,<br>
            Have drunk themselves maroculous<br>
            And flashed a swatch of hairy arse<br>
            At Baillie, laird, and councillor;<br>
            Just once or twice have lived a little,<br>
            Like a bit snapped off from Breughel <br>
            Or like Roberts Fergusson and Burns.</p>
          <p>For weren&rsquo;t the weavers radicals<br>
            And firebrands? A folk,<br>
            When time and chance gave leave,<br>
            Who waxed in millenarian hope<br>
            Of that paradisal life on earth,<br>
            A home-spun Utopia on the River Cart?</p>
          <p>So why, then, does this room,<br>
            With its skeletal loom<br>
            And its hard wood recessed beds<br>
            And heavy artless furniture<br>
            Feel so blankly sabbatarian?<br>
            So uselessly utilitarian?</p>
          <p>What hint here of Paisley Pattern's<br>
            Oriental brilliance and lustrousness,<br>
            The eyes fired with the luxuries of other worlds?</p>
          <p>These cold plaster walls won't help:<br>
            For all they have attended and stood witness to<br>
            They give back nothing,<br>
            Offer for investment<br>
            Of time or interest <br>
            No return.</p>
          <p>Unrelieved by ornament, resonantly empty,<br>
            They are spotless and joyless and tight-lipped;<br>

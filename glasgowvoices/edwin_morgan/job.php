            <p><a name="black"></a><strong>Black Sea</strong><strong> Children</strong></p>
            <p><strong><em>Fazil  Iskander&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </em></strong><strong> </strong><br>
              <strong>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </strong></p>
            <p>Come  all you swashbuckling bath-swabbers, tuba-cheeky chimney-sweeps,<br>
              Story-tellers,  snake-oil merchants, conjurors, <em>artistes</em>,<br>
              Dogged  trackers, treasure-seekers, shaggy earth-lopers,<br>
              Lemonade-lovers,  leaders of your own leaders!<br>
              Thrust  your carpe-diem snouts in watermelon pulp!<br>
              Your  blessed bellies will sing and gurgle and blup.</p>
            <p>Tell  me now, tell me, is he your pal, your comrade,<br>
              Runt  of a lounge-lizard, well-licked ugly duckling?<br>
              Better  the air of the seas! Better the air of the forests!<br>
              Leaves  are the best medicine, trees are your doctors.<br>
              Scramble  up into the hills, children! Seize the jetting waterfall<br>
              That  quivers at your feet<br>
              Like  a bright blade plunged in peat!</p>
            <p>(The  wind balloons your shirt,<br>
              The  sun stands at its height.<br>
              Dive  into the sea-swirl<br>
              Like  frogs, swim with all your might!) </p>
            <p>Out  there are bays and backwaters, Athens and Istanbul,<br>
              A  sea of snorting dolphins right to the Pole!<br>
              Joking  apart, it&rsquo;s true! This noted fisher<br>
              Tunes  with his aerial to the submarine sea-bass singer.<br>
              Crab-traders,  divers, sea-bed shellfish-lords,<br>
              Bold  Red guardsmen, silent Red Indian hordes,<br>
              Future  cosmonauts with skin tanned by the sun,<br>
              Flourish,  children, mischief-makers. I salute you one by one.</p>
            <p><strong>&nbsp;[Translated </strong><strong>17 March 1988</strong><strong>]</strong></p>

            <br><a name="aretik"></a><p><strong>Aretik Isahakian</strong></p>
            <p>Where  at this moment<br>
              is  the uncut stone<br>
              waiting  for when<br>
              the  grave&rsquo;s my home?</p>
            <p>What  if I&rsquo;ve tramped<br>
              the  roads and sat<br>
              with  a girl in my arms<br>
              on  that very slab? &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <strong>(1909)</strong></p>
            <p>&nbsp;</p>
            <p>Birds  take joy<br>
              in  the coming spring<br>
              putting  all their heart<br>
              into  their love-chirping.</p>
            <p>Well,  I was once<br>
              an  adept at<br>
              birds&rsquo;  talk,<br>
              I  remember that.</p>
            <p>And  then I lost <br>
              my  soul, from day<br>
              to  day of my life<br>
              with  its bitter games</p>
            <p>and  now I don&rsquo;t know<br>
              what  their tongue means<br>
              to  say, that wise<br>
              tongue  with its dreams.&nbsp; <strong>(1919)</strong></p>
            <p>&nbsp;</p>
            <p>My  neighour&rsquo;s son has died<br>
              only  twenty years old &ndash;<br>
              I  had just lent him a book &ndash;<br>
              a  lively, healthy lad.</p>
            <p>The  blue night is all<br>
              spring  scent, spring moon,<br>
              I  lean on the window-sill<br>
              transfixed  with pain.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <strong>(1921)</strong></p>
            <p>&nbsp;</p>
            <p>In  some far off forest<br>
              the  strong oak grows,<br>
              my  coffin-wood.<br>
              And  my death is so near<br>
              that  the deep thick oak boughs<br>
              rustle  through my blood.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <strong>(1928)</strong></p>
            <p>&nbsp;[<strong>Translated  May 1977]</strong></p>

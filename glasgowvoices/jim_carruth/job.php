            <br><p><strong>A Shout in the Street</strong><br>
(for Kenny  Hunter)</p>
            <p>Where the city  is the gallery<br>
              celebrate the  furred and feathered,<br>
              survivors of  the man made.</p>
            <p>Raise up on  broken pallet,<br>
              empty barrel,  plinth of worn tyre,<br>
              statues to  small lives.</p>
            <p>Where  landscapes of man<br>
              are the ancestors  of white goods,<br>
            old fridges  and tvs cast aside.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </p>
            <p>Adjust your  picture to lifted wings. <br>
              Above those  silent screens <br>
              honour the  unknown pigeon.</p>
            <p>Where excessive  waste splits bin bags<br>
              exposing the  colour of discard, <br>
              banana skin and  fag packet</p>
            <p>and everyday  is thrown away,<br>
              analyse with fox and hawk<br>
              end products  as self portraits.</p>
              <p>&nbsp;</p>
<P><a name="brush"></a><strong>Brush Strokes</strong></P>
<p><em>i.&nbsp; Two  crabs (1889)</em></p>
<p>On the canvas<br>
  crabs too  close </p>
<p>for kindness<br>
  pincer and  claw</p>
<p>bright red<br>
  carapaces</p>
<p>created by <br>
  bold gestures</p>
<p>that will slash <br>
  the artist&rsquo;s  ear.</p>
<p><em>ii.&nbsp; Field  with the ploughman(1889)</em></p>
<p>From his view  of nearby fields<br>
  and the hills  of les Alpilles</p>
<p>painted from  his room<br>
  he has taken  away</p>
<p>the bars on  his window<br>
  the high  asylum wall</p>
<p>but left the  ploughman<br>
  his two heavy  horses</p>
<p>hauling dark  scars across<br>
  his wheat  field stubble.</p>
<p><em>iii.&nbsp; Rain  Auvers (1890)</em></p>
<p>Auvers<br>
  hunched<br>
  in the hollow</p>
<p>rain slicing<br>
  the canvas<br>
  slanted  strokes</p>
<p>crows<br>
  the track<br>
  to the  cemetery.</p>
  <p>&nbsp;</p>
  <p><a name="dovima"></a><strong>Dovima with Elephants</strong><br>
    <em>(1955 Gelatin silver print &ndash;  Richard Avedon)</em></p>
  <p>Beauty poses  in black<br>
    beside a brace  of reluctant old age,<br>
    for to capture  this moment is everything.<br>
    Its statement  tall and elegant&#8212;<br>
    unblemished  skin bedecked in Dior.</p>
  <p>Be ready to  bear witness<br>
    as its graceful  messenger walks free between <br>
    the grey tethered  in their heavy chains.<br>
    They are  unable to escape<br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; comparison.</p>
  <p>Standing like some  shapely Samson, <br>
    shorn of  unsightly locks she reaches out <br>
    to touch both  deep grooved bodies<br>
    as though this  could bring these pillars of time <br>
    tumbling down around  Youth&rsquo;s crucifix.</p>
  <p>This divine  human whose long smooth neck <br>
    stretches back  to catch all the light<br>
    echoes in turn  the curve of an ancient trunk<br>
    already rising  above her<br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp;to trumpet the first warning. &nbsp;</p>
  <p>&nbsp;</p>
  <p><a name="stag"></a><strong>Stag</strong></p>
  <p>In the third  Summer<br>
    of the war<br>
    the orphans<br>
    wandered  further<br>
    into the  country<br>
    eager for  sport<br>
    and found him<br>
    on a dry river  bed<br>
    legs buckled<br>
    under a  failing body<br>
    eyes black  with flies.</p>
  <p>The children  swarmed<br>
    over the dusty  bank.<br>
    With their  fists<br>
    branches and  rocks<br>
    they clubbed<br>
    the frightened  beast<br>
    tore at his  hide<br>
    silenced his  bellow.<br>
    Small crimson  hands<br>
    held entrails<br>
    high in the  sun.</p>
  <p>Somewhere<br>
    beyond <br>
    the final  breath<br>
    the victory  songs<br>
    laughter  stopped.<br>
    Left behind<br>
    by the others<br>
    one boy<br>
    dropped his  weapon<br>
    lay down  against<br>
    the still-warm  belly.</p>

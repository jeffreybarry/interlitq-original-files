            <p><a name="bottle"></a><b>Bottle of Rain, &pound;10 for 30ml</b></p>
              <p>This is not the dead rain<br>
              lying foul in an urn and spilled<br>
              to slake the summer&rsquo;s thirst<br>
              or the dark rain, howling<br>
              with ice, that batters<br>
              at the fire-bright window.</p>
              <p>This is the rain that gives itself<br>
              to gravity, free-falling. Landing<br>
              fat cat paws on burning streets<br>
              that shiver at its touch.<br>
              This is the rain that raps a challenge<br>
              on the earth, and hangs to hear<br>
              the echo of reply. That drops<br>
              each rhythm like a gauntlet,<br>
              speeds up, laughing, through the dance:<br>
              pavane to polka to jig to reel until<br>
              there's no air, just the rain and ground<br>
              both ringing in a single rising fall.</p>
              <p>This is the rain that draws back, spent,<br>
            and everything beneath it stunned awake.</p>
            

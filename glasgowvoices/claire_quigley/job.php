            <br><p><b>The Fish Man</b><p>
              <p>drives a van that drags the haar with it.<br>
              He bellows in the hollow tongue of foghorns.<br>
              And he trawls the streets for buyers<br>
              for the shining haul he brings.</p>
              <p>But every fish is round-eyed with betrayal,<br>
              and people hint at things he wants to hide.<br>
              Secrets out of sight below the counter&mdash;it's said<br>
              that there are some who've seen his scales.</p>
              <p>At night, tail-first, he slides into the water, dives<br>
              until he finds the hanging shoals. Cool skin slips<br>
              through the silver-beaded curtains, cold hands<br>
              reach out to drown them in the air.</p>
            <p>Even when he&rsquo;s here&mdash; dry in daylight&mdash;<br>
              he can&rsquo;t quite shake the sea out of his eyes.<br>
              Those who dare to meet their deep, uneasy blue<br>
              feel the pavement pitch and roll beneath their feet.</p>
              <p>&nbsp;</p>
          <P><a name="uplift"></a><b>Uplift</b></p>
          
            <p>He tells me the equations only hold<br>
            and keep the plane from falling<br>
            if somebody believes</p>
            <p>and since you can&rsquo;t be sure<br>
            on any flight who does believe<br>
            it&rsquo;s down to him.</p>
            <p>I&rsquo;ve never heard this Peter Pan<br>
            approach to physics and I laugh,<br>
            but later see him</p>
            <p>pulling up on armrests, eyelids lowered<br>
            in meditation on equations read<br>
            in books. And once or twice I think</p>
            <p>I catch him looking through the window<br>
            watching Bernoulli dance along the wing.</p>
            <p><em>Note: Bernoulli's Equations can be used to calculate the lift produced by aircraft wings.</em></p>

            <p><a name="foxes"></a><strong>Foxes</strong></p>
            <p>A passion fuelled by foxes cannot last.<br>
              Our joiners cobbled up this mirrored bliss.<br>
              A skater&rsquo;s weight will force a pond&rsquo;s collapse.<br>
              Fox-love is a game of hit or miss.</p>
            <p>I&rsquo;ll throw them chicken wings, a slice of beef.<br>
              Porch lights will ignite when foxes run.<br>
              Our garden is a pool of disbelief.<br>
              Vermin have their holes in kingdom come.</p>
            <p>The synaesthete sees colour in a word;<br>
              a tune is bitter almond, orange peel.<br>
              A fox&rsquo;s nose is cleaner than a sword.<br>
              Our kiss was burning bibles on a wheel.</p>
            <p>Hail, as foxes gnaw their daily bread,<br>
              the winter pavement serving as a dish.<br>
              We snuffled out our boundaries in bed.<br>
              Christ send me, quick, another night like this.</p>

            <p><a name="apocrypha"></a><em>from</em> <strong>Apocrypha</strong></p>
            <p>VIII</p>
            <p>Abraham wielded a watering can.<br>
              With star-mangled fervour<br>
              he sprinkled the Arctic, the Sahara.</p>
            <p>Five years later, a riot of wild<br>
              orchids and tropical liana<br>
              convulsed the Arndale shopping centre.</p>
            <p>Moths fled their equator.<br>
              With twelve-inch tongues uncoiled<br>
              they drilled for glacial nectar.</p>
            <p>Some species perished: inverted<br>
              atmospheres, increased cloud cover<br>
              snuffed the jewelled frog, the grail spider.</p>
            <p>When moonlight wobbled<br>
              Abraham knocked a nail through it.</p>
            <p>&nbsp;</p>

            <br><p><strong>Arthur&rsquo;s Seat, Edinburgh</strong></p>
            <p>The night air carried  nothing of the city;<br>
              the sky, a slate grey-blue  beyond routine<br>
              bankruptcy, the government  of loss.<br>
              Blackbird, rattling its  thicket, had no<br>
              ear for trumpets. Spring  intuited itself.</p>
            <p>What sank from high dark to  horizon, stone-in-<br>
              water, escaped all  newspapers and the day&rsquo;s<br>
              thin infatuations: a  canopied ring of lights<br>
              hovered there:  ghost-carousel, UFO:<br>
              the Blackford Hill  observatory. Come dawn,</p>
            <p>an ordinary world returned:  dew on bracken,<br>
              a team of painted,  fire-forged horses<br>
              dropping like stars from the  sun&rsquo;s womb<br>
              to carry off a crowd of  shoeless children.</p>

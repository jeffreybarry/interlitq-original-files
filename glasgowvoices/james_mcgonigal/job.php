            <p><strong>Anniversary</strong></p>
            <p>I saw many  things done by heart &ndash;<br>
              days were  counted on an abacus of mesh,<br>
              the wire made  up a crossword minus clues,<br>
              men wore  walkie-talkies like campaign medals</p>
            <p>while we longed  to be where fish drift<br>
              between weeds,  to speak in water vowels<br>
              and master the  diphthongs of silt.<br>
              The river  spilled night from its haversack</p>
            <p>and packed  instead reflections of three deer<br>
              at first light,  hooves in mud, drinking.<br>
              A boy with  shaven head saw this<br>
              with his own eyes,  he told us.</p>
            <p>&nbsp;</p>
            <p><a name="understudy"></a><strong>Understudy</strong></p>
            <p>When sunrise  warmed the hills&rsquo; shoulders<br>
              they rippled  with pleasure &ndash; such<br>
              a treat of heat  after darkness.</p>
            <p>I was studying  for the role of my father<br>
              and found  hesitations &ndash; a glance of unease &ndash;<br>
              worked like a  breeze </p>
            <p>stippling shade  under a lime tree.<br>
              Soon I wore the  part like horse skin <br>
              visible only  when bleeding or pestered by flies.</p>
            <p><strong>&nbsp;</strong></p>
            <p><a name="not"></a><strong>Not a day</strong></p>
            <p>I wanted to  write not a day<br>
              will go past  when you won&rsquo;t<br>
              think of your  father &ndash; your body<br>
              older by years  now than ever his was.<br>
              Crossing the  park under grey skies<br>
              maybe for  morning rolls it seems<br>
              you are walking  to meet him.</p>
            <p>And even a day <br>
              when you think  he did not cross<br>
              your mind you  think now possibly<br>
              yes &ndash; only his  thought escaped you.<br>
              He is around  here often like the trees<br>
              up on that slope  standing on tiptoe<br>
              to follow the  fate of a rain cloud<br>
              over each  other&rsquo;s shoulders.</p>
            <p>Water in the pond  this morning<br>
              has camouflaged  itself as clay.<br>
              Moorhens scratch  rules in cuneiform<br>
              from a survival  book of reed-running. <br>
              Their vees of  ripples<br>
              soon slip back  into smooth opaque.<br>
              Fathers or  mothers pushing go-chairs<br>
              bring toddlers  to the edge to look &ndash;<br>
              ducklings and  chicks at swim.</p>
            <p>Who kick both  legs together <br>
              rattling their  buggies in delight.<br>
              I pass them by &ndash;  just some old guy<br>
              of indeterminate  age with a grey <br>
              beard but still  looking not too bad <br>
              considering in  these Original chinos <br>
              with the  expanding waistband <br>
  &lsquo;built for  durability and comfort&rsquo;.</p>
<p>&nbsp; </p>            
<p><a name="preparations"></a><strong>Preparations for Easter</strong></p>
<p>She was  sheltering in the church porch<br>
  from a sharp  wind and the gossip<br>
  and he was there  in its half-dark too,<br>
  taking a break  from his mowing.<br>
  Holy Week  notices, posters for pilgrimages,<br>
  a diocesan  letter twitched in the draught<br>
  as we opened the  outer door and stepped inside.</p>
<p>She had a  beautiful head, and was taller than he was<br>
  and heavier too,  I&rsquo;d say, her jeans were tight<br>
  across backside  and thighs. And she was hanging<br>
  on his every  word, and hanging on his every silence.<br>
  Magdalene  mistook her risen Christ for a gardener,<br>
  but there was no  mistaking the reek of grass and petrol<br>
  off this one,  overpowering the perfume from her blouse.</p>
<p>In church, all  of the statues were hooded in purple<br>
  for Good Friday.  Out in the porch, her eyes were starry<br>
  for the wee  gardener with the crush<br>
  of the first  mown grass of April on his boots.</p>
<p>Which man here  among us would not turn<br>
  from his own  devotions at the thought of hers?</p>
<p>&nbsp;</p>
<p><a name="half"></a><strong>The Half-Awake Soul</strong></p>
<p>The years of my  soul have passed<br>
  like the warmth  of a bed<br>
  under sheets of  moonlight or rain.<br>
  The length of my  soul has turned over<br>
  from one side to  the other.</p>
<p>The best months  were spent in silence,<br>
  they were  passing over into silence.<br>
  I&rsquo;ll say no more  about that<br>
  breath silvering  life&rsquo;s mirror<br>
  not tell who was  glimpsed there.</p>
<p>I remember whole  weeks passed at ease<br>
  stretched out on  the new grass in May,<br>
  or awake as the  sea is now<br>
  turning waves to  a lather of light<br>
  in the channels  of night.</p>
<p>The best days  all had their moments<br>
  caught in  birdsong or bird flight.<br>
  The last look of  a cloud leaving the hill behind.<br>
  Ourselves making  little of it,<br>
  turning back to  the work of tomorrow.</p>
<p>The years of my  soul have come to this<br>
  chill morning in  a long bed.<br>
  Under a duvet of  sunlight or snow,<br>
  is there  strength enough in ankle and thigh<br>
  down narrow  paths to go?</p>

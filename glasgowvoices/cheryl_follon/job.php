            <br><p><strong>A Love Potion Concocted  Together with Leaves</strong></p>
            <p>Careful, careful &ndash; as mother&rsquo;s recipe said &ndash;<br>
              add in the something leaves,<br>
              one at a time is best;</p>
            <p>snip-snip the Seven Sisters&rsquo; rose<br>
              like it was the littlest toe<br>
          of your lover in bed.</p>
          <p>&nbsp;</p>
          <p><a name="honey"></a><strong>Honey</strong></p>
            <p>According  to Nemesis&rsquo; erotic handbook<br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<em>The  Night and the Perfumed Rose</em> &ndash; <br>
            written  in a dripping cave<br>
            sometime  in the fifteenth century<br>
            somewhere  in the foothills of old Greece &ndash;<br>
            a  buttered bowl of honey&rsquo;s what you need&nbsp;<br>
            mixed  with chicken stock to make a soup.<br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;The  thing for the lover of all lovers &ndash;<br>
            to  pluck the thick gold honey<br>
            from  the thick of the locust tree<br>
            with  a long thin barb<br>
          and  your five fingers spread out for balance.&nbsp;</p>
          <p>&nbsp;</p>
          <p><a name="travelling"></a><strong>Travelling Eye (Looking Down Summer)</strong></p>
          <p>Past St  Peter&rsquo;s and Desire Street, past<br>
            the red  love heart shaped hibiscus <br>
            and Uncle  Earl in his cast</p>
          <p>sipping a  cup of coffee, past<br>
            the hanging  gardens,<br>
            then  turning left, past the <em>Gasthouse</em>.</p>
          <p>Past old  Nellie knitting a sock, past<br>
            the red  phone booth<br>
            burning in  the sun</p>
          <p>(that&rsquo;s  been kaput now<br>
            for almost  three weeks) past<br>
            the melons;  you&rsquo;re nearly there, nearly there.</p>
          <p>Past the  little restaurant on the corner,<br>
            owned by  the French man,<br>
            and where  they&rsquo;ll be cooking up</p>
          <p>pumpkins  and raisins,<br>
            coriander  and cinnamon<br>
            and plums and  ginger to stuff in pastries.</p>
          <p>Over the  high walls into the secret garden<br>
            off Ursuline Street,<br>
            where my  friend and me</p>
          <p>shattered  our soft knees<br>
            trying to  net fruit,<br>
            or catch  lovely ladies bathing in the nude!</p>
          <p>Past all  those wooden shutters &ndash; green<br>
            and yellow  and blue<br>
            shut  close-tight</p>
          <p>in the  middle of the day<br>
            and all  whilst the sun is blazing!<br>
            What the  devil is going on behind there? &ndash;</p>
          
          <p>Homoerotic  love affairs, horrible marital<br>
            breakdowns,  arguments<br>
            and lots of  red hot sex!</p>
          <p>Well that&rsquo;s  our guess,<br>
            and this  little place<br>
            stinks of  donkey&rsquo;s dung all the year round.</p>
          <p>Past the  house where they brew damson gin<br>
            in long  classy thin<br>
            lovely  bottles, past</p>
          <p>the Man  with the Glass Eyes<br>
            and his big  polished trombone;<br>
            ah you&rsquo;re  nearly there; it&rsquo;s almost in your reach.</p>
          <p>&nbsp;</p>
          <p><a name="hair"></a><strong>Hair and My Grandmother&rsquo;s Ring</strong></p>
          <p>O Mother used  to cut it with a knife<br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;taken from the butcher&rsquo;s bag<br>
            or the hatchet  from the stairs.<br>
            I&rsquo;d sit in the  chair<br>
            and let her  cut it back;<br>
            I suck from a  dish of green-tops and beef.</p>
          <p>My old  grandmother&rsquo;s plaited up my hair,<br>
             &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;thin as a sapling.<br>
            She&rsquo;d smear  some Glory on<br>
            with the flat  of her old palm<br>
            and turn her  wedding ring<br>
            three times  for luck and sometimes four.</p>
          <p>&nbsp;</p>
          <p><a name="dog"></a><strong>The  Dog:&nbsp; A Tale of Love, Bad Love and  Longing</strong></p>
          <p>1</p>
          <p>Then  devilish, rooting for pearls &ndash;<br>
            her  trousers, gusset, blouse &ndash;<br>
            her  cuffs &ndash; a scent &ndash; cunty! &ndash;<br>
            rises  up through the lot.<br>
            She&rsquo;s  been seeing Major Cott<br>
            on  Fridays and Sundays <br>
            for  seven months straight now.<br>
            A  dirty, blousy, wayward girl.</p>
          <p>2</p>
          <p>I  plough down, upend, career &ndash;<br>
            a  cold and scentless thing,<br>
            like  glass &ndash;<br>
            geegaws,  a diamond ring &ndash;<br>
            things  she&rsquo;d snaffle and stash<br>
            from  her ugly old man.<br>
            She&rsquo;d  gladly live in a caravan<br>
            a  million miles from here.</p>
          <p>3</p>
          <p>An  endless line of snout &ndash;<br>
            I&rsquo;ll  sing to you and push<br>
            my  body through the seven<br>
            zones  of palpable pleasure:<br>
            toadstools,  minerals,<br>
            mossy  turds,<br>
            sweet-peas,  semen and blood.<br>
            I&rsquo;ll  root and rout them out.</p>
          <p>4</p>
          <p>Of  course there&rsquo;re tales to tell &ndash; <br>
            these  back up pell-mell.<br>
            A  cleaver crashes skulls &ndash;<br>
            well,  one time, yes.<br>
            The  woods a hush-hush place<br>
            as  that grave was dug<br>
            and  the body flopped in.<br>
            The  wallet went straight down the well.</p>
          
          <p>5</p>
          <p>By  nine the window&rsquo;s up<br>
            and  a big bun&rsquo;s cooling.<br>
            Sign:&nbsp; Ready when you are.<br>
            The  Kansas man cleans up,<br>
            shifts  a rag and cup.<br>
            He  sorts his hair in the mirror.<br>
            Sign:  Ready and waiting.<br>
            He  clutches his bag and comes.</p>
          <p>6</p>
          <p>Her  hair, well what&rsquo;s to say?<br>
            That&rsquo;s  white gold spun<br>
            through  the fingers of Homunculi.<br>
            It&rsquo;s  sweeties, herbs &ndash;<br>
            saffron,  maybe &ndash; it&rsquo;s weeds.<br>
            Sometimes  peppery,<br>
            throwaway  candies, a plum<br>
            on  a dish in the sun.</p>
          <p>7</p>
          <p>The  skin, well that&rsquo;s a tale:<br>
            a  fairy tale, all that &ndash;<br>
            corn  and spice and sugar.<br>
            (A  fairy makes nice things<br>
            with  pretty things inside.)<br>
            They  bet to find its peer<br>
            in  the woods-floor hut<br>
            but  they&rsquo;re drunk, and fail.</p>

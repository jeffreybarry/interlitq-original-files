            <br><p><strong>Glasgow</strong></p>
            <p>'Glasgow is the best of plays: you can act  in it and watch it at the same time.'<br>
              Edwin Morgan</p>
            <p>1<br>
              The narrow path leads down on the diagonal,  close to the wall.<br>
              Intimate enough to see the fossil strata,  the wisps of grass, what life<br>
              Insinuates its roots into the fissures,  holds on, grows its green out upwards from the shadow,<br>
              And how the shadows fall across the canyon  walls, and slowly lean and move across<br>
              Their textures, as the sun does its old arc  thing once again. You move in closer when<br>
              The mule brigade ride up and want to pass,  flatten yourself against the vertical<br>
              Plane behind you, watch them place their  sure hoofs near the edge.</p>
            <p>2<br>
              Thus I was in Arizona, years ago. Today,  the pleasure on the path and edges of<br>
              The city is the same: a history of stories  getting deeper in a walking world<br>
              Or maze of traffic made for circling,  zig-zags, ziggaruts, explorable depths<br>
              And heights that show you out across the  world in blue. Let's list a few.<br>
              One Sunday afternoon down by the Clyde,  walking past the statue with her arms<br>
              Raised to remember the reason why the  Spanish Civil War was fought, we came<br>
              To the Finnieston Crane, no-one around and  the small gate in the fence unlocked.</p>
            <p>3<br>
              We went up. Steel steps took us to the top,  but standing on the horizontal there,<br>
              I saw a further ladder on the side wall of  the cabin, got to the roof. And smoked a cigarette,<br>
              Looking around. The rooftops all before you  and blue all the way to Arran, Ayrshire, South.<br>
              The conurbation, East. The Islands, West  and North, and mountains in the North-West, through<br>
              The gap at the end of the Great Western  Road, that leads you to the Gaeltacht,<br>
              By the pass of the Camel's Hump. Came down  again and walked back into town.<br>
              Co-ordinate points, a history, a sense of  what you still believe is: inextinguishable, live.</p>
            <p>4<br>
              Scenic? Hardly. But nevertheless. What  archeology supplies sets layers and connections in<br>
              The mind. What's now? Bridges and the river  still, the skyline: Scott's Gothic tower points up like<br>
              Aspiration, spaceship-sharp; so much else  points down, is earthed and mobile, Dennistoun bars and<br>
              West End brasseries. Take off the top of  your skull and open your brain to the air: let's go<br>
              For the open-topped tour. Victorian  Glasgow: George Square and grandiloquence, architecture<br>
              Rising and ornate, that flourishes its  culmination in the Art School on its own diagonal, steep.<br>
              Below that, Merchant City, slave trade and  tobacco lords, and money building East End</p>
            <p>5<br>
              Market manners, commerce, a deep exchange  of language, a currency we trade in, even now. <br>
              Below that, the Cathedral and the crypt to  mark old Mungo and his long ago approach<br>
              To the Molendinar Burn, the water now to be  seen only in a well up in Blackhill. But it's<br>
              The mix of the thing, the shoulders rub  together, the acting as you walk and watch, the lifting<br>
              Of concern, the taking part: looming on  that green-roofed, narrow-spired Cathedral is<br>
              Victoria's Infirmary, a massive block of  stone and glass opaque with grime, and looming over both,<br>
              From further East, the shadowy Necropolis,  the village of the dead, with John Knox high</p>
            <p>6<br>
              On pillar at its summit, arm stretched out,  in supplication? benison? Deer and foxes roam here now.<br>
              That's good. But not enough. Under that  there's more. Just fourteen feet below St Vincent Street:<br>
              The carboniferous. Such knowledge as the  strata bring began for me in childhood, walking with my<br>
              Papa from railway to bookshop to shop,  arcade to arcade, where colours were bright on covers and<br>
              Spines and diamonds, and language spoke  with beguilment, on pages and in air. Conversations<br>
              Made and make the space work bright: what  seams streak through pollutions of the bad beliefs,<br>
              Bodies made to end in haste and  unfulfilment, the pathos now itself cliche, industrial and after that.</p>
            <p>7<br>
              So, not nostalgia, please. What is so sad  and cannot now be claimed, is unreturnable.<br>
              Recognise that. Dividedness, and not  diversity. It's there all right. But when you feel this city work,<br>
              As a friendly force that drives the green  life up and out of the earth, the air enamelled blue<br>
              With all the conviction of speech in its  best gambols, it's something worth a listen to and look<br>
              Around, and colourful, as Dore's Dante's  deep Inferno, keeps you guessing, horrified, for people,<br>
              And knowing that the love and laughter,  light, are in there too, right to the end.&nbsp;&nbsp;&nbsp; <br>
          From far beginnings we might never know,  but what the present is, is what we do.</p>            <P></td>

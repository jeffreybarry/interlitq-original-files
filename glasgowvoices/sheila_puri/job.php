            <p>When he  first did away with the mud walls<br>
              And put up  cement and clay bricks<br>
              He built a  driveway <br>
              This was in  the days where the word driveway was un-coined in the English language<br>
              There being  only possibly a hundred cars in the English-speaking world<br>
              And in the Punjabi  speaking, maybe three and these too<br>
              Driven by a  Maharaja, English <em>and Scottish</em> Sahibs <br>
              Yes  Scottish,<br>
              Lest we  forget <em>the colonised can also be the colonisers</em><br>
              No guilt,  no finger pointing to the past <br>
              After all India too is a burgeoning economy.<br>
              Pundits on the  BBC, in barely concealed voices say, <br>
              Fifty years  from now India will be a Super-Power,<br>
              Adding,<br>
              And we will  be like the Old Roman and Greek empires.</p>
            <p>Tourists of  the ex-Indian villager, ex-untouchable variety will come to snap at a crumbling  Big Ben.<br>
              The tables  of guilt will turn<br>
              Or is it  just the chairs?</p>
            <p>11</p>
            <p>Grandfather  battered at the mud walls<br>
              Until they  fell in small thuds <br>
              Filling the  earth<br>
              With untold  stories.</p>
            <p>Cement was  mixed and bricks bought,<br>
              And the  mistris told<br>
              To make an  opening in the dwelling&rsquo;s enclosing wall <br>
              Wide enough  for two cars to travel abreast<br>
              (After all  he had nine sons, surely nine times more likely than those with five, three, or  none to have cars in days to come)</p>
            <p>&nbsp;</p>
            <p>111</p>
            <p>Passersby  glanced<br>
              Pondering that  the wall looked like a mouth with several teeth missing.</p>
            <p>To his face  in high-pitched tones they said <em>paramamatma</em> will bring great prosperity  to one with such vision.<br>
              Behind his  back they sniggered into their hands<br>
              Convinced the  man had finally lost his mind<br>
              Nine sons  could do that<br>
              Too much of  a good thing can bring disaster to one so proud.<br>
              Pulling sugar-beet  from the field they muttered for God to keep them humble but sane.<br>
              And at  night eating the unsold <br>
              With Coarse  Rye Rotis.<br>
              The wives lamented  that sugar-cane won&rsquo;t fill the bellies of the <br>
              Children<br>
              They told  the wives to keep their tongues shut<br>
              Or else  they would see the back of His hand</p>

<p>His parents having fled a brutal conflict, the young prince was left to fend for himself, in a small boat.</p>
<p>Sailing towards the isle of a myriad shipwrecks, the haunt of birds, he rode the crests of sonorous waves. He set shore at the very place where the devotee of a spurned creed, holding the elements in his sway, awaited his predestined charge. Divining the prince’s advent, and crowned with an oak garland, he had abandoned his refuge in the ruined fastnesses of age-old fortresses.</p>
<p>He had come to transmit to his acolyte the riddle of a lore guarded too jealously to be transcribed.</p>
<p>Breathing the island’s quickening air, the young prince flourished, commander of a legion of birds, emissaries of tidings from eternity, desirous of serving their master’s will.</p>
<p>In the evening, his halcyon days eclipsed by a lone regret fed by his mentor’s evasion, the island’s shadow fell like a shroud over the violet sea.</p>
<p>As the moon drowned in the tempest, the prince was flooded with the reminiscence of a woman remote to him only in distance.</p>
				<p>Argentine-bound to find her, he embarked on a vessel proof to the most turbulent of storms.</p>
				<p>Atop the clouds, in the loftiest chamber of a tower, she nursed an inextinguishable hope.</p>
				<p>The outcast prince had found his only sister, immured in her interminable vigil.</p>
<p>He learned the reason for their separation and, through her counsel and the wisdom acquired on the wind-swept island, reclaimed the valiant subjects of his kingdom.<br><bR></p>
 
				<p><strong>Cuento desvariado</strong></p>
 
<p>El infante de los reyes proscritos fue abandonado en un esquife, después de vencidos en la contienda desesperada.</p>
<p>Bogaba en medio del cántico de las olas salvajes, hacia la isla de los naufragios, visitada por las aves. Aportó derechamente donde lo esperaba el adepto de una ciencia aborrecida, árbitro de los elementos, adornado con una guirnalda de roble. Había dejado su retiro, entre las ruinas de fortalezas inmemoriales, al sospechar el arribo del predestinado.</p>
<p>Debía transmitirle las enseñanzas fiadas a la memoria de una secta formal, temerosa de escribirlas.</p>
<p>El niño creció con sólo respirar un aire vital. Mandaba sobre la milicia de las aves, celosas de contentar su voluntad inocente y de contarles mensajes de un origen superior.</p>
<p>Su vida apacible conserva el dejo de un solo pesar, desde la evasión inopinada del maestro. La isla alargaba en ese momento de la tarde su sombra triangular sobre el mar violáceo.</p>
<p>La luna, anegada en la borrasca, inspira al solitario la imagen de una mujer distante, de alma simpática.</p>
<p>La busca en un bajel insumergible, de estela argentina.</p>
<p>Ella vive, abrazada a una esperanza, en el aposento más alto de una torre.</p>
<p>El proscrito descubre su única hermana en la mujer vigilante.</p>
<p>Conoce el principio de su separación y recupera, por sus avisos y con los medios aprendidos en la isla tormentosa, los bravos súbditos de sus progenitores.
</p>




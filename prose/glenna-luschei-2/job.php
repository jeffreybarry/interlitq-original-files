           <p><br><br></p>



				<p>New Year's Day 2017, and too many things we wanted to do before we left Cuba on the midnight flight.  First the bumpy jeep ride to the river, then the boat trip, followed by snorkeling in the lake.  At last, lunch at the alligator farm and then back to our hotel in Matanzas for one last swim before heading to the airport in Havana.</p>

				<p>It started off as the trip of a lifetime.  I signed up for the Santa Barbara Museum of Art Cuban art tour for children.  I travelled with my son, Tom, his wife, Yasmin and my two grandchildren, Linda, 12 and Andrew, 6. We visited homes of artists and enjoyed meals with them.  My favorite artist was Sandra Ramos who portrayed Cuba as a caiman, or alligator.</p>

				<p>I dressed in my swimming suit, but when I started to put on my sandals they broke apart.  I said, to my daughter- in- law, "Yasmin, look what happened to my sandals." </p> 

				<p>"Glenna, your feet are swollen,” Yasmin gasped.  “There is a medical clinic next door.  Let’s call them.” </p>

				<p>"No, they might keep me here."</p>

				<p>"It's too dangerous for you to fly in a pressurized cabin."</p>

				<p>We made the call. The doctor and her team arrived in five minutes but the two stretcher-bearers did not bode well.  The doctor looked like a Red Cross nurse in red, white, and blue.  She smiled pleasantly, her voice was firm.  When I told her we had a midnight flight, she said, “Before you get on any plane, we need to take you to the clinic and do everything we can to reduce the swelling in your feet.” She said I had pulmonary edema brought on by heart failure.</p>

				<p>All this in Spanish and the beginning of a saga which kept me in a Cuban hospital for a week.</p>

				<p>I felt my heart contract, then beat wildly.  I agreed to go to the clinic.  It was a decision that changed my life, maybe saved my life.</p>

				<p>The two stretcher-bearers carried me to an ambulance.  My two grandchildren, Linda and Andrew, came to kiss me goodbye before the medical team carried me away. Yasmin gave me a hug.</p>

<p>“I know you’re doing the right thing,” she said.  “Tom will be right by your side.” I sighed with relief. My heart bumped.</p>


				<p>The clinic was a ramshackle building with apparently only one exam room.   There was another table with a woman on it.  The doctors helped her out and then turned to me.  They put me on an IV and introduced a bundle of electrolytes to take down the swelling.  “Usted está muy bonita,” the doctor said.  She was trying to cure me by flattery.  She told me I looked like a famous Cuban actress, Rosita Fornes.  “She had an affair with Cantinflas. You look just like her.” </p>

				<p>Heaven help me.  All I wanted to do was go home.  </p>

				<p>As the hours dragged on, I said to my son Tom, "The deal is, when the plane leaves, you go.”</p>

				<p>He said in return, "The deal is, I stay here until we can go together."  He was the child who kept vigil over my daughter Linda until her death of AIDS at the age of 36.</p>

				<p>“Go get your mother some ice cream,” the doctor said, “the parlor is down the street.”</p>

				<p>"It's ten o'clock, Tom replied, "Is it open?"  She said, “It’s always open.”</p>

				<p>It was, and we ate ice cream cones while we waited for the swelling to go down, still planning to board the plane. </p>

				<p>At the last minute the doctor sent me to the hospital instead.  The swelling wasn’t subsiding and my heart rate was wildly high.  Ice cream was not going to cure me.</p>

				<p>Yasmin and the children boarded the flight without us and arrived safely home in California at her parents’ house.  She later told me that her family formed a circle to pray for me.  Her own grandmother had died only two days before and the family was still in the midst of mourning her.  I will always be grateful for Yasmin’s Mexican family and their exuberant and generous nature, their caring of the elders, especially me. </p>

				<p>Another ambulance ride and Tom and I arrived at the hospital ICU later that night.  It was freezing.  There was a bed for me in the ICU but there were no sheets or blankets.  I found out that in Cuba, patients bring their own supplies, including soap, towels and toilet paper.  Worse luck, I also had to have a catheter. </p>

				<p>When I was a tourist in Cuba on an earlier trip, a guide told us that people would really appreciate the soap and shampoo in our hotel bathrooms.  But it didn’t get through to me how scarce those small luxuries really are.  The Cubans did everything they could to share with me, and when I left I vowed to share with them.  For one thing, the x-ray machine wobbled when I stood on it.  I shared the x-ray with my own doctor when I got back home, and he considered it a curiosity, almost a relic.  I promised in my heart to contribute a new x-ray machine to the clinic.  </p>

				<p>For now I was trying to get into bed and somehow get warm.  A nurse brought me a yellow blanket.  It was all I had for comfort.  “A woman died today and her family never picked up her things,” she said.  Tom gave me a hug, pressed my hands, wished me goodnight and disappeared.  I was grateful for the warmth of his touch but was frightened I would never see him again. </p>

				<p>After Tom left, I lay in bed in the dark, wrapped in the yellow blanket.  I felt profoundly alone, shivering and wondering if I should send out a report for my lost son.  I was relieved to learn the next morning that Tom had spent the night just down the hall.</p>

				<p>In the morning, a nurse dabbed my face awake with cold water and brought me a cup of sour yogurt which I found delicious.</p>  

				<p>To my immense relief I caught sight of Tom outside the hall talking to the doctors.  He seemed jovial.  Could he possibly have good news?  I kept motioning for him to come in, but he didn’t acknowledge me.  What if he was safe outside but never able to come into the room?  I might be isolated for the rest of my life.  I was crazy with anxiety and exhaustion. </p>

				<p>When Tom came in, he told me that the medicine, Solotol, which my cardiologist prescribed, was damaging my heart.  The Cuban doctors changed my prescription, but the pharmacy did not have my new medicine in stock.  </p>

				<p>“I’ve got to go out and search for it,” Tom said.  I remembered the bizarre healing practices I had seen, and hoped that he did not have to throw blood on chickens as in the santería ceremonies.  </p>

				<p>Tom left and Dr. José came in. </p> 

				<p>I put him on notice. “Voy a salir mañana,” I said.  He told me I was not going tomorrow because I was 83 years old and had heart failure.</p>

				<p>“Y que tengo que hacer para salir?” I asked.</p>

				<p>“Tranquila,” he said. “Relax”.</p>

				<p>I had no choice.  I wrapped the yellow blanket tighter around me and lay back on my sweater, which I was using as a pillow.  I was shattered and my heart was broken too. My heart was beating out a gliding rhumba.  Matanza was the place where Cuban-African rhythms emerged, including the rhumba, so maybe it was just getting well.</p>

				<p>I thought of Tom and his kindness towards me and how he took care of my daughter Linda when she was so sick.  I drifted off and remembered an earlier time when we spent our lives in other hospitals, caring for Linda and hoping and praying she would be healed.</p>

				<p>Hours later, I woke to the sound of an old woman’s voice, “un poquito agua por favor,” she said over and over again.  Nobody answered.  I got up with my own liter of Ciego Montero water.  A nurse shook her head at me, “She is not thirsty and she is not sick.  We just give her a hospital bed to protect her. She has nowhere else to go.”</p>


				<p>Abuela<br><Br></p>




<p>I bit the dust in Matanzas<Br>
like the Spanish fleet<Br>
the indigenous Cubans destroyed.<Br><Br>

I bit the dust in Matanzas<Br>
where all mornings unfold alike.<Br>
The nurse dabs me awake with cold<Br>
water from the mountain stream.<Br>
She leaves the liter bottle stamped<Br>
with the troll to last all day.<Br>
All Cubans drink the same fairy-tale water.<Br><Br>

All afternoons sleep.<Br>
Abuela tunes up, “Ay Dios mio ayúdame”.<Br>
Second day I have entered her litany.<Br>
She chants, “Ella es norteamericana y habla español.”<Br>
By the time she winds down<Br>
the nurse brings us rice and a drumstick.<Br>
I begin to crave the sour yogurt.<Br><Br>

Late afternoon the TV clicks on. <Br>
Children march in the streets.<Br>
Abuela naps.<Br><Br>

Evening at last. Another drumstick.<Br>
The nurses retire<Br>
and I creep out to the heavenly unlocked balcony.<Br><Br>

Life goes on.<Br>
The fiesta convertibles like the one I rode in<Br>
	and stray dogs pass each other in the street.<Br><Br></p>






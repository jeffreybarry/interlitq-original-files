			<p>The days dragged by as we waited interminably. As for me, I indulged in no end of wishful thinking, picturing the boat that would transport Mareika from Ameland and, hard on the heels of such deliverance, the advent of the lighthouse keeper who would relieve me of my duties. The prospect of bidding the island farewell kept both of us buoyant, and especially Mareika. </p>

				<p>But as day followed endless day, her expectancy appeared to me to border on the manic. She would subject me to a daily catechism about the mainland, and was nothing short of bemused when I told her that a myriad of cities existed there, that houses were made of cement, that there were premises known as shops that accepted money for goods. Reflecting on the prospect of another life, she spent each day in a state of ever-increasing absorption. “So it is goodbye to Ameland,” we would exclaim with one voice, and without the slightest trace of nostalgia. </p>

				<p>There was, after all, nothing to keep me in Ameland and Mareika, in essence as much a foreigner as I was within the confines of these shores, would have set sail before me. 
And as for the horizon beyond this wretched outcrop, I would be assured of a future that, if far from glorious, would free me from my encroaching sense of dread.</p>

				<p>But the natives of Ameland had different plans for us, insisting that the birth of our child, a pretext for the mummery of potions, spells and washcloths, would have to take place on the island. Any woman who fell pregnant was to be kept apart from her husband, and so I would be forbidden access to Mareika, during which time incense would be burnt, libations drunk, sacrifices made, and exorcisms conducted against the demons of the earth and sea. While the islanders avowed that they were Christian, not one bible was anywhere to be seen, not a single priest had set foot there for centuries, and the only religion practiced was their own concoction. I had not learnt to read for nothing, and I had far greater faith in the ministrations of my hometown doctor, or even my own mother, than the mumbo-jumbo of these so-called midwives.</p>
				<p>The women of the island started to pester Mareika, contending that it would be futile for her to visit the mainland. Mareika did not know how to counter their assertions.</p>

				<p>“I wish they would leave you alone, it has nothing to do with these fishwives,” I repeated, like a mantra.</p>

<p>But, as they harried her with visits, Mareika could not summon the courage to face down the intruders. With euphoria giving way to despondency, she grew agitated, shutting herself away, fearful of a nebulous threat, and that she would never come to embark. Indeed, she was besieged by a panic that grew in intensity, until she appeared to be at her wits’ end. Prey to powerlessness, I grasped that Mareika’s dejection, poignant in itself, compounded the many obstacles that faced us. </p>

				<p><br><br></p>
				
				<p>Nos quedamos esperando. Cada vez con más frecuencia comencé a imaginar el arribo del bou en que partiría Mareika y, poco después, la llegada de mi sustituto para el faro. La idea de partir nos mantenía alegres, sobre todo a Mareika, quien se ponía más ansiosa y eufórica cada día. Me preguntaba por el continente y se quedó absorta cuando le expliqué la existencia de miles de ciudades y gentes, de casas de ladrillo y cemento, de tiendas y del dinero, que tanto le costó comprender. Pasaba el día distraía y absorta pensando en otra vida. ¡Adiós, adiós Ameland!  nos decíamos ella y yo sin nostalgia alguna mientras hacíamos planes para nuestra vida futura. De Ameland no me llevaba nada, incluso Mareika era extraña a la isla. En el continente, en cambio, me esperaban meses de salario y un futuro que no pensaba promisorio, pero al menos sin los agobios de una tierra aislada.</p>
 
				<p>Ameland tenía planes muy distintos para el niño y no tardaron en hacérnoslo saber. Querían que naciera allí y que el parto siguiera las costumbres del lugar. En la isla todo se solucionaba con paños, hierbas y embrujos. La mujer encinta era tomada a cargo por la comunidad y privaban al marido del derecho a verla. Después, durante días, encendían inciensos, hacían libaciones, sacrificios y exorcismos contra demonios terrestres y marinos. Se decían cristianos, pero nadie tenía una Biblia, aquellas regiones no habían visto un sacerdote durante siglos y la única religión que conocían era la inventada por ellos. Yo sabía leer, no creía en supersticiones y, en todo caso, confiaba más en los buenos oficios de mi madre o del médico del continente que en las improvisadas matronas de la isla.</p>

<p>Las mujeres comenzaron a agobiar a Mareika insistiendo en lo inútil de viajar a tierra. Mareika no sabía cómo defenderse:<br>
	- Que te dejen en paz, no es asunto de ellas  -repetía yo.</p>

				<p>Pero no se atrevía o no podía enfrentarlas y siguieron viniendo a casa. Mareika se ponía cada vez más nerviosa, pasando de la euforia al desgano. Comenzó a encerrarse y a no salir más que conmigo, se sentía insegura, amenazada, temiendo no poder embarcarse y que todo fracasara, siendo presa de episodios de angustia cada vez más intensos y frecuentes, como si por momentos estuviera fuera de sí, ante lo que yo nada podía hacer excepto sumar a las dificultades su propio desánimo.</p>

				
	






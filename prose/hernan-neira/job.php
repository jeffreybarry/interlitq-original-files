           <p><br><br></p>

				<p align="center"><strong>Argumento</strong><br><br>Inspirado en <em>A Modest Proposal For Preventing The Children of Poor People in Ireland From Being a Burden to Their Parents or Country, and For Making Them Beneficial to The Public</em>, de Jonathan Swift</p>

<p>Una pareja adulta, constituida por un economista recién jubilado y su esposa, dialogan
sobre la ventaja que ofrecería el comer niños pobres, menores de un año. Él ve los
provechos que tendría para el crecimiento económico, la estabilidad política y la seguridad
ciudadana, mientras ella ve las ventajas que tendría para una vida cada vez más confortable.
	Aparentemente antagónicos por temperamento, son una pareja perfectamente <em>afiatada</em>, que por distintos caminos llega a la misma conclusión. El diálogo sólo es interrumpido por pequeñas incursiones a la cocina, de la que salen agradables olores. ¿Qué se prepara en ella? ¿Pasarán los personajes de la propuesta teórica al acto de cocinar y comer un niño? El conjunto se desarrolla en un tojo jocoso e irónico, donde el público reirá al ver qué tan cómica puede ser una proposición que al mismo lo seduce y espanta.</p>



<p align="center">Nota al director:<bR>
	No se usará cuero o piel animal para el montaje.</p>


<p><strong>Personajes:</strong><br>
MARIDO: aproximadamente 60 años<br>
	MUJER: unos diez años menor que él</p>

<p><em>Salón de un departamento medianamente elegante de los años 1980, con un diseño
conceptual. Un computador Apple II, en un escritorio y una impresora de la época. Al
fondo, una biblioteca (algo desordenada, que se note usada y no como adorno) y un retrato
de Friedrich von Hayek, dedicado. En un lado, una pequeña mesa de comedor. En un
	costado, una puerta que da a una cocina, de la que sólo se entrevé un pedazo.</em></p>

<p><em>El MARIDO y la MUJER nunca se dirigen personalmente al otro, sino que hablan como
para sí mismos, incluso cuando hay respuesta. Son un matrimonio conservador de la
época. Él es ex profesor universitario y consultor financiero; ella no tiene educación
	superior.</em></p>

<hr>


				<P>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong>MARIDO</strong></P>
<p><em>El MARIDO termina de escribir en el computador, imprime una hoja en una impresora de
agujas, que tarda. Levanta la hoja y lee lo que estaba escribiendo, dándole tono de una
	cancioncilla de cabaret o al estilo de Beggar’s Opera .</em></p>

<p>Produce tristeza<br>
a quienes caminan por esta gran ciudad<br>
o viajan por el país<br>
ver las calles tan llenas de mendigas, seguidas<br>
	por tres, cuatro o cinco niños, harapientos, que importunan a los transeúntes.</p>


<p><em>Entra la MUJER, con cuatro paquetes de compra. Bien vestida, pero no elegante. Lleva
una cartera colorida, grande, pero sin exagerar. El MARIDO la ve pasar muy cerca, pero
apenas la sigue con los ojos. Ella también le mira, como si no le viera. No se saludan.
Todos los gestos son de mucha confianza entre ellos y no hay agresividad ni indiferencia, a
pesar de que no tienen aparente comunicación. La MUJER tira los paquetes en una
pequeña mesa y se sienta al lado de ellos. La MUJER permanece quieta. Él no la mira. La
MUJER comienza a abrir el primer paquete. Lo hace con destreza, pero meticulosa y
lentamente. Poco a poco, la cancioncilla del MARIDO adquiere un tono de diálogo
	normal.</em></p>

<p>Esas mujeres<bR>
se ven forzadas<br>
a vagabundear para obtener<br>
la subsistencia de sus indefensos niñitos.<br>
Creo que conservadores, liberales,<br>
Socialistas y ecologistas, estarán de acuerdo:<br>
en el estado actual del país, esa multitud de niños,<br>
colgados del cuello, de los brazos o de las espaldas de las mujeres<br>
son un dolor extraordin....<br>
	¡Ay, querida!</bR>


<P>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong>MUJER</strong><p>
<p>Mirando los paquetes<bR>
	¡Ay, qué lindo!</p>

	<P>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong>MARIDO</strong></P>
<p>Ay, ay, ay.<br>
que ni nosotros<br>
ni la nación lo<br>
					podemos soportar.</p>

	<P>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong>MUJER</strong></P>
	<p><em>Sigue abriendo el primer paquete.</em></p>
	<p>¡Ahhhh, ay, ahhhh!</p>

	<P>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong>MARIDO</strong></P>
<p>Pobreza, hambre, aborto, abandono, desnutrición, enfermedades, ladrones, mendigos,
	pordioseros, desolación, muerte...</p>

	<P>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong>MUJER</strong></P>
	<p>¡Ay, ay, ay! Mis Amigas se mo-ri-rán de envidia.</p>
<p><em>Termina de abrir el primer paquete. No se dirige al MARIDO. Comienza a sacar un
vestido, de fiesta, hecho en un cuero muy delgado, de color llamativo pero elegante, del
	que sólo se asoma una parte.</em></p>
<p>¿No es bonito? Y tan útil... Hace tanto que lo estaba buscando. Ya había perdido las
	esperanzas.</p>

	<P>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong>MARIDO</strong></P>
<p>Por eso, cualquiera que encuentre una forma elegante, fácil y barata de hacer de esos niños
	miembros razonables y útiles para nuestra sociedad, merecerá ...merecerá … </p>

	<P>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong>MUJER</strong></P>
	<p>Comer, a esta hora siempre tienes ganas de comer.</p>

	<P>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong>MARIDO</strong></P>
<p>...una estatua. Alguna gente, más bien pesimista, está legítimamente preocupada por la
gran cantidad de pobres que tenemos en nuestra república, muchos mutilados o enfermos.
Pero mi proposición ni es pesimista ni se limita a los mendigos, sino a todos los que nacen
de padres con dificultades económicas. Hace años que me vengo ocupando del tema.
Incluso pensé alguna vez en ello, cuando estudiaba en la Universidad Católica, porque esto
es un tema moral, además de económico, pero entonces no era el momento. Después,
cuando terminé mi doctorado, el Ministerio de Hacienda me absorbía todo el tiempo. Sólo
ahora puedo hacerlo, gracias al año sabático que me dieron en la universidad. He dedicado
años al estudio de cómo liberar a la nación de tan dolorosa molestia, pero por fin veo frutos.
¿Te das cuenta lo que significa resolver el problema de los niños pobres y además
	transformarlos en algo útil al público?</p>

	<P>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong>MUJER</strong></P>
	<p><em>Deja el primer paquete y comienza a abrir el segundo.</em></p>
	<p>Hermoso, hermoso, hermoso.</p>

	<P>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong>MARIDO</strong></P>

<p>San padre Hurtado fue mi inspiración, pero él era tan, tan...tan ingenuo. En el fondo, era un
hombre del pasado, entre conservador y socialista, no un hombre moderno, no se daba
cuenta de que la solución a la pobreza es un asunto puramente económico y no de buenas
intenciones, ni de cumplir con mandamientos ni de abolir la propiedad privada. Entendió el
problema, pero no la solución, porque él recogía a los niños pobres, ¿y después? Fundó el
Hogar de Cristo, allí los criaba, los mantenía por años, lo que le obligaba a estar mendigando dinero, pidiendo donaciones a las grandes familias, que él conocía bien por venir de una de ellas. La iglesia está equivocada. Incluso algunos economistas, por
brillantes que sean. Friedman, por ejemplo, se equivoca cuando calcula cuánto cuesta
mantener un niño, por mucho que tenga razón cuando en el capítulo cuarto de <em>Free to
choose</em>, dice que nuestra nación se encuentra dividida en dos: por un lado, los que reciben ayuda y, por otro, los que la pagan.</p>

<p><em>Lentamente, la MUJER saca del segundo paquete un par de guantes, largos, hechos de un
cuero muy delgado, que hacen juego con el vestido. Los extiende en la palma de la mano y
	sobre el brazo. Los mira con deleite.</em></p>

<p>El costo de mantener un niño se incrementa con la edad, según una curva exponencial, que
fue estudiada por Richard A. Cannonball, cuyos resultados expuso en <em>Contribuciones a la
teoría de los incentivos perversos bajo información asimétrica en un mercado con
demanda insuficiente</em>, publicado por la <em>Journal of Contemporary Social Economy</em>,
Cambridge, Massachussets, 2009. Para un bebé recién nacido basta la leche de su mamá y
un poco de sol, ambos gratis, más unos cuantos andrajos, que ella puede obtener con su
profesión de mendiga. Después, los padres tienen que producir, comprar, recibir caridad o
robar los alimentos de sus hijos. El período más caro en la mantención de un niño es la
adolescencia, pues, es sabido que los adolescentes tragan todo el día. Por eso hay que
proceder a temprana edad. Es exactamente cuando los niños cumplen un año que propongo
	llevar a cabo mi proyecto.</p>

<p><em>La MUJER coloca los guantes en el paquete de donde los sacó, pero deja la mitad afuera.</em></p>

	<P>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong>MUJER</strong></P>
	<p>Fue en una tienda exclusiva. Los conseguí por la mitad de su valor.</p>

	<P>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong>MARIDO</strong></P>
<p>¿Tienes una idea de cuántos niños pobres hay en nuestro país? ¿Sabes cuántos de esos
niños pobres son pordioseros o viven de la mendicidad de sus padres? Uno de cada tres. De
éstos, la mitad pasa hambre. Son los datos oficiales. Tenemos una población de 22 millones
	de habitantes.</p>

<p><em>La MUJER comienza a sacar del primer paquete el vestido del que había dejado asomado
un pedazo. Se va con él delante de un espejo. Allí se saca el vestido que lleva puesto y se
coloca el nuevo. Se mira ante el espejo. Todo lentamente, con un poco, sólo un poco de
	erotismo. El MARIDO la mira desvestirse unos instantes, mientras habla.</em></p>

	<P>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong>MUJER</strong></P>
<p>¿Tienes una idea de cuántas tiendas están de rebajas? ¿Sabes cuántas de ellas hacen
verdaderas liquidaciones? Una de cada tres. De éstas, la mitad ha bajado los precios un
cincuenta por ciento. Son datos oficiales. Tenemos una población de veintidós mil tiendas.
	Ayúdame con el cierre.</p>

	<P>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong>MARIDO</strong></P>
<p><em>Se levanta y avanza sin soltar la hoja que lee. Al acercarse donde la MUJER, coloca la
hoja en la misma mesita de los paquetes. Le sube el cierre a la MUJER.
Parsimoniosamente, toma la hoja y vuelve. Se sienta. Mira a la MUJER tan sólo un
	instante. Por mientras, ella se ajusta el vestido y él continúa leyendo.</em></p>

<p>Dos millones y medio de niños en nuestro país son mendigos o viven de la mendicidad de
sus padres. Mantenerlos es un problema objetivo, en cualquier circunstancia, pero más
tomando en cuenta el estado actual de la nación, que es complicado: racionalización
económica, ajuste fiscal, disminución de nuestras exportaciones, desfinaciamiento de las
pensiones, baja del dólar, alza de las tasas de interés, emigración de la mano de obra
calificada, proteccionismo de la Comunidad Europea... Todo eso genera inestabilidad política, lo que disminuye las inversiones, que se van a otros países. No podemos seguir
pensando en que a los pobres los mantenga el Estado, ni la caridad pública, ni tampoco la
privada. No hay con qué mantener a esa multitud y tampoco es bueno. Y eso, sin tomar en
cuenta que los seguros de desempleo desincentivan el esfuerzo. Es necesario que los pobres
	se vuelvan útiles, productivos.</p>

	<P>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong>MUJER</strong></P>
	<p><em>Se mira en el espejo. Todo lentamente, con un poco, sólo un poco de erotismo.</em></pm>

				<p>¿Crees que me queda bien? ¡La textura es tan suave! ¡Ay!</p>
    
				<P>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong>MARIDO</strong></P>
<p>Esos niños pobres no tienen la habilidad ni las fuerzas para ser empleados en alguna
fábrica. Y la existencia de un salario mínimo hace aun más difíciles las cosas. Eso impone a
los empleadores una discriminación contra las personas con baja calificación. Tomemos un
adolescente poco formado, cuyos servicios valen, digamos un euro por día. Estará ansioso
de trabajar por ese dinero para adquirir mayores competencias. Sin embargo, la ley dice que
el empleador debe pagarle dos euros, lo que significa imponerle al empleador una caridad
de otro euro. Al final, lo que sucede es que no se le contrata. Y los niños más chicos ni
siquiera tienen la habilidad para ser carteristas, por mucho que ya a los seis años comiencen
	a alargar los dedos.</p>

<p><em>La MUJER abre un tercer paquete. De él saca unas botas, también de cuero, que hacen
juego con el vestido y los guantes. Se las coloca. Queda completamente vestida como parauna fiesta. Se mira en el espejo con un deleite ligeramente erótico.</em></p>

				<P>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong>MARIDO</strong></P>
<p>Tengo un amigo, de Rotterweiheim, que conoce bien el negocio. Me asegura que un
muchacho de unos doce años vale unos 15000 euros y una chica, si es bonita, bastante más,
aunque, si es fea, bastante menos. Antes de esa edad no se venden bien o simplemente
nadie los compra, porque gastan mucho y producen poco. Pero eso es sólo una parte del
asunto. El verdadero problema es que esos quince mil euros en que se vende un chico
saludable no cubren los costos de la alimentación y vestuario necesarios para llegar a esa
	edad.</p>

<p><em>La MUJER comienza a sacarse los guantes y las botas. El MARIDO se levanta y la baja el
cierre; vuelve al escritorio. Ella se saca el vestido lentamente. Se queda en calzones y
sostén. Mira su cuerpo en el espejo, primero a un par de metros; después, se acerca hasta
el espejo y lo aplasta con su cuerpo, en una situación semi erótica. Más tarde, algo
	bruscamente -pero sin rabia- se aleja del espejo y se viste con la ropa del comienzo.</em></p>

<p>La ley de salario mínimo, la pobreza y la inseguridad son un mal nacional, fruto de políticas
populistas, pero erradas, con consecuencias nefastas. Hay descontento, robos, huelgas,
manifestaciones, comunistas, ecologistas, antisistemas, disturbios... <em>Pausa</em>. ¿Y la comida? Malthus tenía razón: la población crece más rápidamente que los alimentos. El equilibrio natural sólo se logra con hambrunas, pero nosotros, que somos civilizados, podemos
	encontrar otros métodos de alcanzarlo.</p>

				<p><em>La MUJER se dirige hacia la cocina. Ruidos del trabajo de la MUJER en la cocina.</em></p>

<p>El costo de producción de esa mano de obra no calificada es más alto que lo que esta mano
de obra produce. Por eso, necesariamente son un gasto para sus padres, para el Hogar de
Cristo o para el Estado, además de los peligros que involucra su paso de mendigos a
	ladrones a medida que crecen. Eso tiene una solución.</p>

<p><em>La MUJER vuelve al salón sacándose el delantal, recoge lo recién comprado y lo coloca en los paquetes iniciales. Mientras lo hace:</em></p>

				<P>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong>MARIDO y MUJER</strong></P>
				<p><em>Con entonación de canción de cabaret o de Beggar’s Opera.</em></p>
<p>I shall now therefore humbly propose my own Thoughts, which I hope will not be liable to
	the least Objection.</p>

				<P>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong>MUJER (sic)</strong></P>
<p><em>En tono indiferente, sin soltar las manos del paquete, exactamente con la misma
	entonación que antes se contó en inglés.</em></p>

<p>Y ahora, en consecuencia, voy a proponer, humildemente, mis propios pensamientos, que
	espero no sean susceptibles de la menor objeción.</p>


<p><em>De la cocina sale un vaporcillo con un perfume de comida, que se distribuye en la sala
	(debe olerse en la sala).</em></p>

				<P>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong>MARIDO</strong></P>
<p>Not be liable to the least Objection. I have been assured by a very knowing French chef of
my acquaintance in __________ [<em>colocar el nombre, en inglés, de la ciudad donde se da la
obra</em>], that a young healthy Child well Nursed, is, at a year Old, a most delicious nourishing
and wholesome Food, whether Stewed, Roasted, Baked, or Boiled; and I make no doubt
	that it will equally serve in a Fricasie, or a Ragoust.</p>

				<P>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong>MUJER</strong></P>
				<p><em>Mantener el ritmo y entonación de la versión anterior en inglés.</em></p>
<p>Libre de la más mínima objeción. Un chef francés, que conozco aquí en __________
[<em>colocar el nombre de la ciudad donde se da la obra</em>], gran entendido, me ha asegurado que un niño jovencito, saludable, bien alimentado, es, a la edad de un año, el alimento más delicioso, la comida más saludable, ya sea estofado, asado, al horno o cocido; y no tengo dudas de que es igual servido como fricasé o ragú.</p>

				<P>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong>MARIDO</strong></P>
<p>La mía, es una solución técnica, racional, moderna, no ideológica, apolítica que, a
diferencia de tantas otras, prevé estímulos para obtener un resultado incluso mejor que
aquél que otros obtienen con castigos, sermones o programas de esterilización masivos,
según sea el gobierno de turno. Pero, a pesar de ser técnica, la mía no es sólo la propuesta
	de un científico, sino la de un ciudadano comprometido con su país.</p>

<p><em>Se pone el delantal que previamente usaba su MUJER y va hacia la puerta de la cocina. Se le escucha sacar los ingredientes, verterlos, etc.. Sigue su parlamento dentro de ella, fuera
	del escenario.</em></p>

<p>STROGONOFF<br>
6 filetes de ...<br>
2 cebollas<br>
300 gr. de champiñones Portobello<br>
2 cucharaditas de coñac<br>
20 cm3 de crema fresca<br>
Aceite de oliva o mantequilla<br>
Una pizca de pimentón<br>
sal y pimienta<br>
	1 cucharadita de harina de trigo o de maíz para mezclar con la salsa</p>

<p>Acompañamiento<bR>
Una o dos tazas de arroz<br>
un poco de mantequilla<br>
	sal y pimienta</p>


			<p align="center">(Chapter Twenty-One)<br>
				Orchids</p>

<p>Orchids remind me of the day in 1984 when Linda married her true love, Michael. They’d met two years earlier, at the Hard Rock Café in Hollywood.  He was a teacher in New York City.  She’d been struggling to break into magazine editing, a challenge on the West Coast.  After an intense bicoastal relationship, she moved back east to live with him and advance her career.  On the way, she fell ill in Phoenix and had to be hospitalized, but no diagnosis emerged.  She recovered and brushed it off as exhaustion.</p>
 	<p>A small worry dropped like a seed into my mind, though I too ignored the warning.  Michael always seemed sick. Once when I was in New York on business, I invited them to dinner. Linda said they could not come because Michael had not been feeling well.</p>
<p>When I first visited Linda and Michael, she took me to see her <em>Ladies’ Home Journal</em> office in Manhattan.  We watched a special Punjabi curry being prepared in a state-of-the-art kitchen. When I asked for the recipe, the chef pinned me with an indignant stare.</p>
				<p>“You must read the magazine, madam,” he said with a thick East Indian accent.</p>
 	<p>Our old friend Madge Huntington, who had known Linda since she was a little girl in Mexico City, had helped advance her editing career, first at <em>Good Housekeeping</em>, then at the <em>Journal</em>.  We met for lunch on Madison Avenue.  We talked about Linda’s rapid rise in the editorial world.  She was just thirty years old.</p>
				<p>“I can’t believe I already have my dream job.”  Linda flashed a gorgeous, happy smile.</p>
<p>I thank God now that she had her heart’s desire, even if only for a short season.  She made a home with Michael, who taught at Long Island University, and she worked hard at a creative job that she loved.  Vital and finally content, she experienced the wonderful pulsating heart of New York City every day with the man she had chosen and adored. They soon became engaged.  My heart grew light, knowing she looked forward to a blissful life and a family with Michael in the midst of the excitement and creativity that is New York.  The pain of the past seemed to have lifted from her for good.</p>
<p>She and I were reconciled as well, which meant the world to me.  I threw an engagement luncheon for her and her co-workers at The Russian Tea Room.  That same day, she and I and our friend Terry Hoyt picked out her Lenox china at Bloomingdale’s.  Terry gave her the first place setting.  As we laughed and hurried home in the windy Manhattan afternoon, life appeared to be all joy and orchids ahead.  </p>
<p>After her years of struggle and unhappiness, I wanted to make her wedding gorgeous. The sunny summer day that Linda and Michael recited their vows, a crowd of white flowers bloomed around her feet and her beautiful face glowed with love.</p>
   	<p>A few months after the wedding, Linda called me and my husband Bill at our 
Carpinteria ranch.  She and Michael were at the National Institutes of Health in Atlanta.  In a low frightened voice, she told us that Michael had developed full-blown AIDS.  She added that he’d been HIV positive for years, the victim of a contaminated blood transfusion.  A shunt to his liver had failed and his condition was worsening.  She must have known about his disease even before the wedding, but she had hidden her nightmare from us.  It tore up my heart that she had borne the grief and terror alone.</p>

<p>At the time, I was working on my Ph.D. at the University of California at Santa Barbara.  My dissertation, full of the company of Portuguese troubadours and mystics, became my comfort.  Tom had left for college.  Bill and I were restoring the old Horton ranch house and coping with the savageries of country life.  The neighbor’s dogs murdered our new chickens.</p>
				<p>And then this unfathomable tragedy erupted on the scene.</p>
<p>I craved the challenge and orderly contemplation of my doctoral research topic, “Fatal Attractions in Luso-Brazilian Literature.”  Strangely, pondering the romantic and suicidal songs of ancient Hispanic and Portuguese troubadours helped me to be calm and centered when I talked with Linda as Michael’s illness worsened.  My study was the one place I could go to think of something other than the looming tragedy in my daughter’s life.  I couldn’t even begin to contemplate the possibility that Linda might have contracted the disease from her husband.</p>

<p>Michael died in the fall.  I flew to New York to help Linda sort through his things and make some kind of plan for her life, so brutally transformed.  Her pain was palpable.  I was determined that I’d be optimistic, caring, and as supportive as she would allow.</p>
<p>One evening as I lay in the bathtub of her one-bedroom apartment on Long Island, I overheard her speaking to her father on the telephone.  </p>
<p>“Dad,” she said, “It’s a comfort to have her with me.”  Then, there was a reference to T-cell counts, doctor’s appointments, and to some future but “quite final” plans.</p>
<p>My heart pounded and my skin grew cold in the warm water.  In a flash, a terrifying rift opened in my heart.  My daughter was HIV positive.  Almost as fast, my brain switched from what I couldn’t begin to apprehend to more familiar territory.  Once again, I felt that Linda had turned to her father with her secrets, deliberately excluding me.</p>
<p>I was her mother, the healer, the consoler.  What was she keeping from me?  My mind reeled away from the possibility that she had AIDS.  Instead, I retreated to my familiar feelings of being a distant second-best to her father.  In spite of my anger, I wanted to hold her close, to soothe her brow and tell her everything would be fine, to comfort her as I had when she was a baby.  But now she was shutting me out.  </p>
<p>Instead, I clambered out of the tub and threw on my robe.  As she finished her conversation with her father, I slipped into the living room and removed from her open wedding album a picture of her, standing between Michael and Martin.  I hid it in my suitcase among my clothes.  Like a child passed over for a prize, I couldn’t bear this reminder that I was always “second-best” in her life.</p>
<p>My mothering instincts vanished in my brain’s frantic attempt to deny what I knew in my gut—that my daughter might have AIDS.  Fear made me revert to old family wounds; to the other, more familiar, fear—that I didn’t count in her life.  Revisiting the sad archaeology of my marriage kept me from committing my consciousness to the ultimate meaning of the words I’d heard Linda tell her father.</p>
<p>We were distant over breakfast.  I knew she had noticed the absent photograph.  On the way to her office, on the Long Island Railway, she stiffly told me that the photographs displayed in her apartment were the only way she could remember her husband’s face.</p>
<p>“I’m tired of struggle and anguish,” she said, tears shining in her eyes. “I just want to feel like an ordinary person living her life, like everyone else.”</p>
<p> “I want to see extraordinary people living their lives—people who can survive their pain and emerge radiant,” I replied.  My voice sounded harsh.  She surely felt I was discounting her profound grief.</p>
<p>Fear had its grip on my brain.  All my guiding and healing instincts were gone awry.  I hated myself for behaving like a child, but I pressed the argument.</p>
<p>“You only care about your father.  Why else would you have confided in him and not me?  What were you two talking about?  Why won’t you tell me?”  I demanded as the train swayed under us.</p>
				<p>She burst into tears.</p>
<p>I’ll never forget the suffering on the face of the man sitting next to us.  I think he would have leaped up and rushed away, but there was nowhere else to go in the crowded car.</p>
<p>That evening when we met in my hotel room, I hugged Linda, apologizing for my crazed emotional outburst.</p>
				<p>“I hope you haven’t called your father,” I said.</p>
				<p>“Too late.”  Linda laughed and shrugged.  </p>
<p>Madge had invited us to her Fifth Avenue apartment for dinner.  The three of us reminisced about our days in Mexico, and the time we dug clams on the beach at Madge’s Long Island home.  The evening brought respite from the winter cold and the day’s pain.  We succeeded in dressing the wounds for the moment.</p>
<p>Linda was kind and hospitable for the rest of my stay.  I, of course, gave her back the photo, but a disquieting silence lingered between us. Like a jealous girl, I clung to the feeling that she didn’t love or need me as much as she did Martin.  Once again, she was Athena, sprung fully armed for her battles from the forehead of Zeus.  I still grieve that I wasted those precious days with her.  Given another chance, I’d cherish every second in her presence and give her all the love and comfort I could muster.</p>
<p>In the spring, Linda came out to stay with us at the ranch.  We attended the Santa Barbara orchid festival together, visiting the nurseries to feast our eyes on myriads of exotic flowers. We bought orchids with great paper sleeves to protect the flowers. We all marched home, carrying those treasures that towered above us. That night at dinner, Linda seemed exhausted and sad.  I noticed shadows under her blue eyes.</p>
<p>Taking a deep breath, she looked at me, then Bill, then at her brothers and sister and said, “I need to tell you that I’m HIV positive.”  She’d lived with her secret for two years.  Her T4 helper cell count had fallen to 200 and she felt we needed to know.</p>
<p>The normal T4 count is somewhere between 500 and 1500 cells per cubic millimeter of blood, but we learned that many “healthy” people live their whole lives with T4 cell counts of around 300.  So, even as Linda told us that she was HIV positive, I grasped at the hope that she, at least, would live until a cure could be found.  It was the only possibility my mind would allow.</p>
<p>In my prayers that night, I pleaded with God to heal her, “Please, Lord, please let her live.” </p>
<p>Over the next months, I ignored the poignancy of her simple desire for peace and 
ordinary happiness.  Instead, I focused on plans for medical treatments, finding the best doctors, the newest clinical trials.  I didn’t yet understand that Linda had a clearer vision than I of what was truly important in her life.  Perhaps she’d waited so long to tell me because she knew I’d try to heal what couldn’t be healed.</p>
<p>I couldn’t accept that she might want to let go into her fate with grace and dignity.  Instead, I pushed her to fight for her life.  As a result, I grew disoriented and distraught, even as she seemed to become more centered, stronger.  I raged inwardly at AIDS; I was angry that I hadn’t known sooner.  I believed that I might have helped her find effective early medical interventions if I’d known sooner.  The reality in 1985 was that there were none to be had.</p>







            <p><strong>Sea Widow</strong><bR>
            <em>for G.C.</em></p>
            
            <p>Burying a compass for her dead captain,<br>
she begins a lop-sided life at the edge<br>
of a fishing village: stops smoking out<br>
the truant boys, stays upstairs,<br>
swings in slow circles all day,<br>
bed to window, window to bed.<br><br>

Her doleful gardener, henna in his hair,<br>
has feathers for fingers, famed across<br>
his many milespans. Fathers and mothers,<br>
lovers, rogues — they all bring him<br>
girls to cure on her kitchen floor.<br><br>

She dreads the slivered screams at night —<br>
sentinels scurrying into her sleep, spearheads<br>
glinting in the sweep of the lighthouse beam.<br><br>

She dreads him growing richer by gold rings,<br>
but keeps him for his fog-riding roses<br>
that come flooding in over the salt wind.<br><br>


</p>

<p><strong>Ductility</strong></p>
     
<p>he always writes longhand<br>
word by word a steady pull<br>
the slow and soft<br>
advance of seepage <br>
dusk to dark to dawn<br>
or a bulldozer one-eye blind<br>
pulsing home at night<br>
and he tries to remember<br>
what he had started to say<br>
changing as he writes it<br>
changing as words each one<br>
fashions itself fastens itself<br>
words with subaqueous thrills<br>
and silent terrors and every<br>
time he is startled by<br>
what he has drawn out<br>
longer thicker more vicious<br>
than he has grasped before —<br>
then the sting of whiplash as<br>
hissing it shoots to a new star<br>
<br>

</p>

<p><strong>Mombasa Diamond</strong><br>
<em>for Ammamai</em></p>

<p>Acid can easily eat a lock away<br>
and let any thief in, my aunt told me<br>
as we sat in the dusk at her doorway,<br>
striped Brahmin white-and-ochre.<br><br>

But the real way up, she said,<br>
the way out of this earthly cycle,<br>
is the unforced way: thread-end<br>
through the needle’s eye,<br>
a Buddha trick of attention<br>
and clear sight or the clean ecstasy<br>
of a bhakti glide.<br><br>

Half-lit in the flickering<br> 
of the oil lamps in their hands,<br>
the Narayana chanters slid by,<br>
slipped into the temple gate.<br><br>

We knew them all in the dark<br>
by their gait and singsong,<br>
just as we knew each faceless beggar<br>
by his bowl and call.<br><br>

I waited for her to reach the part<br>
about lotus leaves unwetted by water,<br>
and asked her again,<br>
and again she told me<br>
how she knew about acid:<br><br>

her husband returning home one day<br>
from his lab at the British factory,<br>
hands in bandages,<br>
cursing aqua regia and the gloves of flame<br>
that would never come off all his life.<br><br>

And in a twist of paper in his pocket<br>
all that remained of his wedding ring — <br>
just the diamond her father had brought from Mombasa,<br>
intact, and now sparkling upon her nose-pin.<br>
</p>


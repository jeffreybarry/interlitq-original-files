            <p><br>
            Peter France writes: <br><br></p>
            <p>Batyushkov, a precursor of Pushkin, was known for the classical harmony of his verse. Mandelstam wrote of him: ‘No-one commands such curves of sound, / never was there such speech of waves.’ His early poems are epicurean in tone, but his mature work, written during and after his military engagement in the Napoleonic wars, shows an increasing pessimism. Soon after 1820 he succumbed to mental illness which put an end to his career as a poet.</p>
	<p>The poem translated here, addressed to a friend, expresses Batyushkov’s reaction to the destruction of Moscow in 1812.

          	</p>
            <br><Br>
            <p><strong>To Dashkov</strong></p>
            
            <p>My friend, I have seen a sea<br>
Of evil, heaven’s vengeance,<br>
Our pitiless enemies’ deeds, <br>
War and its deadly fires.<br>
I have seen the rich in hordes<br>
Fleeing in rags and tatters;<br>
I have seen the pallor of mothers<br>
Driven from their dear land.<br>
I have seen them at the crossroads<br>
Clutching babies to their breast,<br>
Sobbing in desperation,<br>
And scanning with a new terror<br>
The red glow in the sky.<br>
Three times with horror since then<br>
I have wandered desolate Moscow<br>
Among the ruins and tombs.<br>
Three times I have washed with tears<br>
Of grief her sacred ashes.<br>
Where once the mighty halls,<br>
Towers of ancient tsars,<br>
Witnessed the glories of old<br>
And the new glory of today;<br>
Where they rested in peace, the relics<br>
Of holy monks, the shrines<br>
That passing time ignored;<br>
And where the hand of luxury<br>
Raised up in golden Moscow<br>
Temples and gardens, the fruit<br>
Of days of peace and work –<br>
Everywhere I could see<br>
Only ashes, dust and rubble,<br>
Heaps of bodies by the river,<br>
Pale regiments of beggars!<br>
And you, my friend, my comrade,<br>
Tell me to sing of love,<br>
Of carefree joy and leisure<br>
And youthful revelry!<br>
Among the storms of war<br>
In the city’s fearful glow,<br>
To summon the shepherdesses<br>
To dance to the flutes of peace!<br>
To tell of the crafty wiles<br>
Of Armidas and fickle Circes<br>
Among the graves of my friends<br>
Lost on the field of glory!<br>
No, no! let my talent wither<br>
And the lyre so dear to friendship<br>
Perish, if I forget you,<br>
Moscow, my golden home!<br>
No, no! until I bring<br>
My life, my love of country<br>
As a sacrifice to vengeance<br>
For my fathers’ ancient city;<br>
Until with the wounded heroes<br>
Who know the road to glory<br>
I three times bare my breast<br>
To the enemies’ close ranks –<br>
My friend, until that time<br>
Be the muses and graces strangers,<br>
And the wreaths woven by love<br>
And the hectic joys of wine!<br>
</p>

<p><strong>К ДАШКОВУ </strong></p>
Мой друг! я видел море зла <br>
И неба мстительного кары; <br>
Врагов неистовых дела, <br>
Войну и гибельны пожары. <br>
Я видел сонмы богачей, <br>
Бегущих в рубищах надранных;<br> 
Я видел бледных матерей, <br>
Из милой родины изгнанных! <br>
Я на распутье видел их, <br>
Как, к персям чад прижав грудных, <br>
Они в отчаяньи рыдали <br>
И с новым трепетом взирали<br> 
На небо рдяное кругом. <br>
Трикраты с ужасом потом <br>
Бродил в Москве опустошенной,<br> 
Среди развалин и могил; <br>
Трикраты прах ее священной <br>
Слезами скорби омочил. <br>
И там - где зданья величавы<br> 
И башни древние царей, <br>
Свидетели протекшей славы <br>
И новой славы наших дней; <br>
И там - где с миром почивали <br>
Останки иноков святых <br>
И мимо веки протекали, <br>
Святыни не касаясь их; <br>
И там, - где роскоши рукою,<br> 
Дней мира и трудов плоды, <br>
Пред златоглавою Москвою <br>
Воздвиглись храмы и сады, - <br>
Лишь угли, прах и камней горы, <br>
Лишь груды тел кругом реки, <br>
Лишь нищих бледные полки <br>
Везде мои встречали взоры!.. <br>
А ты, мой друг, товарищ мой, <br>
Велишь мне петь любовь и радость,<br> 
Беспечность, счастье и покой <br>
И шумную за чашей младость! <br>
Среди военных непогод, <br>
При страшном зареве столицы, <br>
На голос мирныя цевницы <br>
Сзывать пастушек в хоровод! <br>
Мне петь коварные забавы <br>
Армид и ветреных Цирцей <br>
Среди могил моих друзей, <br>
Утраченных на поле славы!.. <br>
Нет, нет! талант погибни мой <br>
И лира, дружбе драгоценна, <br>
Когда ты будешь мной забвенна, <br>
Москва, отчизны край златой! <br>
Нет, нет! пока на поле чести <br>
За древний град моих отцов <br>
Не понесу я в жертву мести <br>
И жизнь, и к родине любовь; <br>
Пока с израненным героем, <br>
Кому известен к славе путь, <br>
Три раза не поставлю грудь <br>
Перед врагов сомкнутым строем, -<br> 
Мой друг, дотоле будут мне <br>
Все чужды Музы и Хариты, <br>
Венки, рукой любови свиты, <br>
И радость шумная в вине! <br>

<p>

</p>


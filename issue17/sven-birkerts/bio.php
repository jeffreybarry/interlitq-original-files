                  <p>Sven Birkerts has been editor of <em>AGNI</em> since July 2002. He is the author of nine books: <em>An Artificial Wilderness: Essays on 20th Century Literature</em> (William Morrow), <em>The Electric Life: Essays on Modern Poetry</em> (William Morrow), <em>American Energies: Essays on Fiction</em> (William Morrow), <em>The Gutenberg Elegies: The Fate of Reading in an Electronic Age</em> (Faber &amp; Faber), <em>Readings</em> (Graywolf), <em>My Sky Blue Trades: Growing Up Counter in a Contrary Time</em> (Viking, 2002), <em>Reading Life</em> (Graywolf, 2007), <em>Then, Again: The Art of Time in the Memoir</em> (Graywolf, 2008), and <em>The Other Walk: Essays</em> (Graywolf, 2011). He has edited <em>Tolstoy’s Dictaphone: Writers and the Muse</em> (Graywolf) as well as <em>Writing Well </em> (with Donald Hall) and <em>The Evolving Canon</em> (Allyn &amp; Bacon). His new book, <em>The Other Walk</em>, will be published in 2011.  
He has received grants from the Lila Wallace-Reader’s Digest Foundation and the Guggenheim Foundation. He was winner of the Citation for Excellence in Reviewing from the National Book Critics Circle in 1985 and the Spielvogel-Diamonstein Award from PEN for the best book of essays in 1990. Birkerts has reviewed regularly for <em>The New York Times Book Review, The New Republic, Esquire, The Washington Post, The Atlantic, Mirabella, Parnassus, The Yale Review, </em> and other publications. He has taught writing at Harvard University, Emerson College, Amherst College, and Mt. Holyoke College, and is director of the graduate Bennington Writing Seminars. He lives in Arlington, Massachusetts, with his wife and two children.</p>

                  <p>Innokenty Fedorovich Annensky was born in Omsk in 1855, the son of a civil servant. The family returned in 1860 to St Petersburg, where Annensky attended school and university, graduating in classical languages. His childhood and youth were marked by frequent illness and by his father’s financial troubles and forced retirement. He taught Latin and Greek in schools in the capital and in Kiev, becoming headmaster of the prestigious ‘lycée’ at Tsarskoe Selo. He did not belong to literary groupings, and was only just beginning to be appreciated as a poet at the time of his death, from heart failure, in 1909. Only one volume of his poetry was published in his lifetime (<em>Quiet Songs</em>, 1904); his principal collection, <em>The Cypress Casket</em>, appeared posthumously in 1910, He also published two highly original collections of critical essays, translated French symbolist poetry and the complete works of Euripides, and himself wrote four tragedies on classical subjects. </p>

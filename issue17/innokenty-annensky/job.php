            <p><br>
            Peter France writes: <br><br></p>
            <p>The Russian poet Innokenty Annensky was neglected as a poet during his life-time and belonged to no literary groupings. He is sometimes associated with his contemporaries, the Symbolists, but does not really belong with them, even though he admired and translated Mallarmé. Since his death, however, he has had a body of devoted followers in Russia, beginning with Nikolay Gumilyov and Anna Akhmatova, and continuing with Gennady Aygi. Dimitri Obolensky remarked in his 1962 <em>Penguin Book of Russian Verse</em> that the ‘flawless beauty [of his poetry] has not been sufficiently appreciated outside Russia’. He has been very little translated into English; I hope that the few poems translated here will give some idea of his special tone of voice.</p>
            <p>Annensky graduated in classical languages from St Petersburg, and had a career as a teacher, finishing as headmaster of the famous ‘lycée’ of Tsarskoe Selo (once attended by Pushkin). He made a remarkable translation of Euripides’ tragedies in which, as Mandelstam put it, ‘he absorbed the serpentine venom of wise Hellenic speech’. He also wrote his own classical tragedies and some remarkable critical essays, but his central work is a fairly slender collection of short lyric poems, dense and sharp in their diction, attentive both to the sensations of life and to the shifts of an acute and tormented sensibility. Some of the best of these are gathered in his posthumous collection <em>The Cypress Casket</em>, where many are arranged in groups of three (‘trefoils’), though all can be read in isolation. One of the trefoils is given in its entirety here. ‘To my Sister’, on the other hand, is an independent poem, addressed not to the poet’s sister, but his sister-in-law, the wife of his elder brother, who with her husband gave him a place of refuge in his troubled adolescence. </p>
			<p>The poem ‘Balloons for the Kids’ is an original notation of street patter, reminding one perhaps of <em>Petrushka</em> and prefiguring similar ventures such as Blok’s ‘The Twelve’. My attempt to translate it is a bit of a free-wheeling experiment, more concerned with rhyme and rhythm than with literal meaning.
          	</p>
            <br><Br>
            <p><strong>Silver Noon</strong></p>
            
            <p>The gleam of silver at noon<br>
Has not yet scattered the mist,<br>
Shot through with wounds of the sun<br>
The mist is still yellower at noon,<br>
Still yellower, still more deathly.<br>
But noon is burning so sternly<br>
That now I can barely endure<br>
The snatches of lilac and scarlet<br>
Of balloons that the eye just makes out<br>
Among scraps of funereal fire.<br>
And why should they all come running,<br>
These joyful, these crazy crowds<br>
Seeking to capture the sun?<br>
And why should the sun caress them,<br>
Airy creatures in a dead space!<br>
But in incense all will grow dim,<br>
The silver of flames and brocade,<br>
The pomp of the undertaker:<br>
For Pierrot and Harlequin<br>
Come with candles to stand at the grave!<br>
Oh white funereal pomp!<br></p>

<p><strong>Серебряный полдень</strong></p>

<p>Серебряным блеском туман<br>
К полудню еще не развеян,<br>
К полудню от солнечных ран<br>
Стал даже желтее туман,<br>
Стал даже желтей и мертвей он...<br>
А полдень горит так суров,<br>
Что мне в этот час неприятны<br>
Лиловых и алых шаров<br>
Меж клочьями мертвых паров<br>
В глазах замелькавшие пятна...<br>
И что ей тут надо скакать,<br>
Безумной и радостной своре,<br>
Все солнце ловить и искать?<br>
И солнцу с чего ж их ласкать,<br>
Воздушных на мертвом просторе!<br>
Подумать,- что помпа бюро,<br>
Огней и парчи серебром<br>
Должна потускнеть в фимиаме:<br>
Пришли Арлекин и Пьеро,<br>
О белая помпа бюро,<br>
И стали у гроба с свечами!<br></p>

<br><Br>
			<p><strong>Balloons for the Kids</strong></p>

<p>Balloons, come and buy my balloons!<br>
Balloons from the kids!<br>
Money from the dads!<br>
Young gents, come and buy my balloons!<br>
Foxy coat, let’s see your spare cash,<br>
Don’t cling on to the trash:<br>
I’ll let them fly up to the sky,<br>
In two hours, look out, look up high!<br>
It’s good to be free, so they say.<br>
Tweet-tweet, your worship, let’s play.<br>
Just buy, they’ll be on cloud nine.<br>
No bargaining – three seventy-five!<br>
&nbsp;&nbsp;&nbsp;&nbsp;Could I take any less<br>
&nbsp;&nbsp;&nbsp;&nbsp;For emancipation – bliss?<br>
&nbsp;&nbsp;&nbsp;&nbsp;No, you won’t....<br>
Hey Granny, what d’you want?<br>
&nbsp;&nbsp;&nbsp;&nbsp;Just a tot?<br>
Sorry, but see what I’ve got....<br>
&nbsp;&nbsp;&nbsp;&nbsp;So it goes –<br>
Another one grows,<br>
But our Punch<br>
with his head screwed on tight<br>
doesn’t grow a fat paunch,<br>
but looks high in the sky<br>
with his lofty thoughts.<br>
Which one will you have?<br>
Don’t squeeze me until I am yours,<br>
Mess it up and it’s flat...<br>
&nbsp;&nbsp;&nbsp;&nbsp;Balloons for the kids,<br>
&nbsp;&nbsp;&nbsp;&nbsp;Red ones, purple ones,<br>
&nbsp;&nbsp;&nbsp;&nbsp;Cheap as they come!<br>
&nbsp;&nbsp;&nbsp;&nbsp;Balloons for the kids!<br>
Hey, fur collar, speak Fritz?<br>
Take ten – they’re in couples<br>
And the rest for free...<br>
Shame your German’s so weak,<br>
Talk’s better than roubles!<br>
Let’s have you, old man!<br>
It’s like you – spick and span –<br>
This great big fatty,<br>
&nbsp;&nbsp;&nbsp;&nbsp;Yellow as putty,<br>
With a heart saying Katy...<br>
&nbsp;&nbsp;&nbsp;&nbsp;Going for a song<br>
Just five,<br>
And another twenty-five,<br>
And then ten more for the best.<br>
Here’s one with the government crest!<br>
&nbsp;&nbsp;&nbsp;&nbsp;Balloons, buy my kiddies’ balloons!<br>
&nbsp;&nbsp;&nbsp;&nbsp;Good people, just buy my balloons,<br>
&nbsp;&nbsp;&nbsp;&nbsp;And then you watch out, you saloons!<br></p>

			<p><strong>Шарики детские</strong></p>

<p>Шарики, шарики!<br>
Шарики детские!<br>
Деньги отецкие!<br>
Покупайте, сударики, шарики!<br>
Эй, лисья шуба, коли есть лишни,<br>
Не пожалей пятишни:<br>
Запущу под самое небо -<br>
Два часа потом глазей, да в оба!<br>
Хорошо ведь, говорят, на воле...<br>
Чирикнуть, ваше степенство, что ли?<br>
Прикажите для общего восторгу,<br>
Три семьдесят пять - без торгу!<br>
&nbsp;&nbsp;&nbsp;&nbsp;Ужели же менее<br>
&nbsp;&nbsp;&nbsp;&nbsp;За освободительное движение?<br>
&nbsp;&nbsp;&nbsp;&nbsp;Что? Пасуешь?..<br>
Эй, тетка! Который торгуешь?<br>
&nbsp;&nbsp;&nbsp;&nbsp;Мал?<br>
Извините, какого поймал...<br>
&nbsp;&nbsp;&nbsp;&nbsp;Бывает -<br>
Другой и вырастает,<br>
А наш Тит<br>
Так себя понимает,<br>
Что брюха не растит,<br>
А все по верхам глядит<br>
От больших от дум!..<br>
Ты который торгуешь?<br>
Да не мни, не кум,<br>
Наблудишь - не надуешь...<br>
&nbsp;&nbsp;&nbsp;&nbsp;Шарики детски,<br>
&nbsp;&nbsp;&nbsp;&nbsp;Красны, лиловы,<br>
&nbsp;&nbsp;&nbsp;&nbsp;Очень дешевы!<br>
&nbsp;&nbsp;&nbsp;&nbsp;Шарики детски!<br>
Эй, воротник, говоришь по-немецки?<br>
Так бери десять штук по парам,<br>
Остальные даром...<br>
Жалко, ты по-немецки слабенек,<br>
А не то - уговор лучше денег!<br>
Пожалте, старичок!<br>
Как вы - чок в чок -<br>
Вот этот - пузатенький,<br>
&nbsp;&nbsp;&nbsp;&nbsp;Желтоватенький<br>
И на сердце с Катенькой...<br>
&nbsp;&nbsp;&nbsp;&nbsp;Цена не цена -<br>
&nbsp;&nbsp;&nbsp;&nbsp;Всего пятак,<br>
Да разве еще четвертак,<br>
А прибавишь гривенник для барства -<br>
Бери с гербом государства!<br>
&nbsp;&nbsp;&nbsp;&nbsp;Шарики детски, шарики!<br>
&nbsp;&nbsp;&nbsp;&nbsp;Вам, сударики, шарики,<br>
&nbsp;&nbsp;&nbsp;&nbsp;А нам бы, сударики, на шкалики!..</p>

<br><br>

			<p><strong>Dying</strong></p>

<p>Thank God, here is the shade again!<br>
Why it is I do not know,<br>
But since the morning I have felt<br>
This dying hanging over me<br>
All the livelong twilit day!<br>
Serving out its bitter time<br>
Between decrepit yellow walls,<br>
Shrivelled, shuddering on its string,<br>
A gloomy red balloon hangs there<br>
Between decrepit yellow walls!<br>
And impotent, just like a shade,<br>
All this livelong twilit day<br>
Keeps tugging, tugging at the string,<br>
Unable to cut short its pain<br>
All this livelong twilit day…<br>
If only night came quickly, night!<br>
To feel yourself slipping away <br>
Into swooning, reconciled<br>
And stupefied, going out again<br>
Into the stupefying night!<br>
And if up there above my head<br>
That dark red thing, barely alive<br>
Would wait its time over the bed<br>
Before becoming so like me…<br>
That dark thing, barely alive,<br>
Up there, right above my head…</p>

			<p><strong>Умирание</strong></p>

<p>Слава Богу, снова тень!<br>
Для чего-то спозаранья<br>
Надо мною целый день<br>
Длится это умиранье,<br>
Целый сумеречный день!<br>
Между старых желтых стен,<br>
Содрогается опалый<br>
Шар на нитке, темно-алый,<br>
Между старых желтых стен...<br>
И бессильный, словно тень,<br>
В этот сумеречный день<br>
Все еще он тянет нитку<br>
И никак не кончит пытку<br>
В этот сумеречный день...<br>
Хоть бы ночь скорее, ночь!<br>
Самому бы изнемочь,<br>
Да забыться примиренным,<br>
И уйти бы одуренным,<br>
В одуряющую ночь!<br>
Только б тот, над головой,<br>
Темно-алый, чуть живой,<br>
Подождал пока над ложем<br>
Быть таким со мною схожим...<br>
Этот темный, чуть живой,<br>
Там, над самой головой...</p>

<br><br>

			<p><strong>To a  Sister</strong></p>

<p><em>To A. N. Annenskaya</em></p>

<p>Evening – and the green nursery<br>
With its low ceiling.<br>
A boring book in German.<br>
Nanny in glasses knitting.</p>

<p>I seem to see a novel,<br>
A yellow paper-back…<br>
I could even read the title<br>
If this mist was less thick.</p>

<p>You were still Alina then,<br>
A rosy thought in your eyes,<br>
The big cape on your dress,<br>
A grey shawl on your shoulders</p>

<p>My knees deep in the chair<br>
As I fixed my eyes on you,<br>
I loved your tender hands<br>
With their delicate veins.</p>

<p>The obscure flow of your words<br>
Was my music of the spheres…<br>
Where I listened for the clash<br>
Of your own special r’s…</p>

<p>In the bronze candlestick<br>
A tallow candle gutters…<br>
Sweet, quietly-sorrowful,<br>
This all lives in the heart…</p>

<p><strong>Сестре</strong></p>

<p><em>А. Н. Анненской</em></p>

<p>Вечер. Зеленая детская<br>
С низким ее потолком.<br>
Скучная книга немецкая.<br>
Няня в очках и с чулком.</p>

<p>Желтый, в дешевом издании<br>
Будто я вижу роман...<br>
Даже прочел бы название,<br>
Если б не этот туман.</p>

<p>Вы еще были Алиною,<br>
С розовой думой в очах<br>
В платье с большой пелериною,<br>
С серым платком на плечах...</p>

<p>В стул утопая коленами,<br>
Взора я с Вас не сводил,<br>
Нежные, с тонкими венами<br>
Руки я Ваши любил.</p>

<p>Слов непонятных течение<br>
Было мне музыкой сфер...<br>
Где ожидал столкновения<br>
Ваших особенных р...</p>

<p>В медном подсвечнике сальная<br>
Свечка у няни плывет...<br>
Милое, тихо-печальное,<br>
Все это в сердце живет...</p>








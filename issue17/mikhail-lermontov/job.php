            Peter France writes: <br><br></p>
            <p>Lermontov rocketed to fame in 1837 with his fierce poem on the death (or murder) of Pushkin, ‘Death of a Poet’. He has remained a key figure in the Russian poetic pantheon. Although he had been writing poems from boyhood, his reputation rests on the extraordinary body of writing (short and long poems, stories and the novel <em>A Hero of our Times</em>) produced between 1837 and his death in the Caucasus (like Pushkin, in a duel) four years later. Like all his writing, ‘New Year’s Day’, composed in 1840, is a romantic (not to say Byronic) work, expressing with characteristic eloquent music his rejection of fashionable St Petersburg society.

          	</p>
            <br><Br>
            <p><strong>New Year's Day</strong></p>
            
            <p>How often, as I stand in the bright crowd,<br>
it all seems like a dream against the loud<br>
   &nbsp;&nbsp;cacophony of dancing music<br>
and hissing, whispered speeches learnt by heart;<br>
before my eyes the soulless figures float<br>
   &nbsp;&nbsp;cased in the masks of decent fashion.<br>
And as I feel on my cold hands the touch<br>
of brazen hands of city beauties who<br>
   &nbsp;&nbsp;have learnt the art of never blushing,<br>
while seeming to admire their busy gleam,<br>
I dwell in secret on an ancient dream,<br>
   &nbsp;&nbsp;its sacred sounds now half-forgotten.<br>
And if somehow I find a way to flee<br>
the world around me, like a bird set free<br>
   &nbsp;&nbsp;I fly in memory against the current;<br>
I am again a child and all around <br>
see places that I knew, the lofty house,<br>
   &nbsp;&nbsp;the ruined greenhouse in the garden,<br>
the sleeping pond caught in a net of weeds,<br>
and then the village chimneys, and far off<br>
   &nbsp;&nbsp;the mist that drifts along the meadows.<br>
I enter the dark alley, where the light<br>
of evening threads the bushes, and my feet<br>
   &nbsp;&nbsp;tread shyly on the dun leaves’ crackle.<br>
And then a mysterious longing fills my heart,<br>
I think of her and I shed tears, I love,<br>
  &nbsp;&nbsp;I love the one my dreams created,<br>
her eyes that sparkle with an azure fire,<br>
her rosy smile that seems the dawning light<br>
   &nbsp;&nbsp;of day that breaks beyond the spinney.<br>
There I would sit alone for hours on end,<br>
all-powerful king of an enchanted land,<br>
   &nbsp;&nbsp;and memory preserves that vision<br>
Amidst the storms of passion and unease,<br>
an island of freshness in the brackish seas,<br>
   &nbsp;&nbsp; still blooming on the desolate water.<br>
But when, awoken from that sleep, I see<br>
my error and the crowds that shun my dream<br>
   &nbsp;&nbsp;like an unwanted stranger at the party,<br>
Oh, how I want to spoil their merry games<br>
by flinging in their face metallic lines<br>
   &nbsp;&nbsp;burnished by bitterness and anger!...<br>

</p>

<p><strong>1-е января</strong></p>
   <p>  Как часто, пестрою толпою окружен,<br>
     Когда передо мной, как будто бы сквозь сон,<br>
     При шуме музыки и пляски,<br>
     При диком шепоте затверженных речей,<br>
     Мелькают образы бездушные людей,<br>
     Приличьем стянутые маски,<br>
     Когда касаются холодных рук моих<br>
     С небрежной смелостью красавиц городских<br>
     Давно бестрепетные руки, --<br>
     Наружно погружась в их блеск и суету,<br>
     Ласкаю я в душе старинную мечту,<br>
     Погибших лет святые звуки.<br>
     И если как-нибудь на миг удастся мне<br>
     Забыться, -- памятью к недавней старине<br>
     Лечу я вольной, вольной птицей;<br>
     И вижу я себя ребенком, и кругом<br>
     Родные все места: высокий барский дом<br>
     И сад с разрушенной теплицей;<br>
     Зеленой сетью трав подернут спящий пруд,<br>
     А за прудом село дымится -- и встают<br>
     Вдали туманы над полями.<br>
     В аллею темную вхожу я; сквозь кусты<br>
     Глядит вечерний луч, и желтые листы<br>
     Шумят под робкими шагами.<br>
     И странная тоска теснит уж грудь мою:<br>
     Я думаю об ней, я плачу и люблю,<br>
     Люблю мечты моей созданье<br>
     С глазами полными лазурного огня,<br>
     С улыбкой розовой, как молодого дня<br>
     За рощей первое сиянье.<br>
     Так царства дивного всесильный господин --<br>
     Я долгие часы просиживал один,<br>
     И память их жива поныне<br>
     Под бурей тягостных сомнений и страстей,<br>
     Как свежий островок безвредно средь морей<br>
     Цветет на влажной их пустыне.<br>
     Когда ж, опомнившись, обман я узнаю<br>
     И шум толпы людской спугнет мечту мою,<br>
     На праздник незванную гостью,<br>
     О, как мне хочется смутить веселость их<br>
     И дерзко бросить им в глаза железный стих,<br>
     Облитый горечью и злостью!..<br>




</p>


                  <p>Mikhail Yurievich Lermontov was born in Moscow in 1814, the son of a retired army captain (who traced his ancestry to Scotland). His aristocratic mother died when he was three, and he was brought up at his grandmother’s estate in central Russia. Educated initially at home, he moved to Moscow in 1827, attended school there, and began to write poetry. In 1834 he became a guards officer, known for his disdainful ‘Byronic’ manner. His eloquently indignant poem on Pushkin’s death in a duel brought him instant fame in 1837, but also exile to the Caucasus. In 1840 he published two volumes of verse and his ground-breaking novel <em>A Hero of our Times</em>. On a return visit to the Caucasus in 1841 he in his turn was killed in a duel. Alongside Pushkin, he became one of Russia’s most famous poets.</p>

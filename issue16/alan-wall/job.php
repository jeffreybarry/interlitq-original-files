            <p><strong>The Fourth King</strong></p>
            <p>Convinced they said January<br>
not December.<br>
There I stood in Damascus Central<br>
fur-coated, cigar in mouth,<br>
one silent servant either side.<br>
My vast sandalwood leather-strapped trunks<br>
crammed with enough curios<br>
to start a car-boot sale<br>
in the Bethlehem suburbs.<br>
Ivory-handled razors<br>
tortoise-shell combs.<br>
 <br>
No sign of them.<br>
Thought they must be on the train already.<br>
Trans-Levantine Express:<br>
a train in the days<br>
when our boys knew how to make them:<br>
mahogany, silver, brass, silk, cotton, porters.<br>
Grilled fish for breakfast<br>
peacock breast pat&eacute; served in the evening.<br>
 <br>
Strolled from one end to the other<br>
in clouds of Havanna<br>
peering in every carriage<br>
except where the blinds drawn down<br>
made enquiry verboten<br>
to anyone but a uniformed Venus.<br>
Ribbons and stays<br>
cognac and lipstick<br>
endearments breathed in steam hieroglyphics<br>
on the dark glass pane.<br>
 <br>
Sat in the dining compartment<br>
searching in vain for my three friends' faces.<br>
Stare through a speeding window.<br>
See the nova<br>
up there in the dark<br>
bright as a nuclear conflagration.<br>
 <br>
Found a poker game<br>
where I lost enough<br>
for five poor men of my kingdom<br>
to live out five lifetimes<br>
without one smudge of dirt on their fingers.<br>
 <br>
On the second night<br>
a certain countess from the Steppes<br>
smiled a smile<br>
my royal heart's accustomed to.<br>
Her couchette smelt of eau-de-cologne<br>
Turkish cigarettes<br>
a thousand nights exactly like this one.<br>
 <br>
Expert she was<br>
treating each caress as the first.<br>
Her sighs primeval.<br>
Needed gold as all Russian countesses do.<br>
 <br>
Then in the hours left over<br>
before the village revelations<br>
I read frantically<br>
through my sacred books:<br>
astrology<br>
geometry<br>
futurology<br>
the predicted death of empires<br>
prefigured in the sky at midnight once<br>
over Babylonia.<br>
 <br>
Arrived at the promised platform in Bethlehem<br>
the day my companions were leaving<br>
noting in their faces<br>
an absence of the usual kingly fraughtness<br>
that palace calendar ticking protocols behind our eyes.<br>
Radiance or vacancy? I couldn't say.<br>
Thought I'd travel to Jerusalem<br>
by taxi<br>
stay for a while<br>
at the King David Hotel.<br>
My Russian countess<br>
joined me for the weekend.<br>
A raven-headed fiddler<br>
played 'Dark Eyes' at our table in the evening. <br>
 <br>
The pilgrims<br>
didn't convince me<br>
torn as they were<br>
between Torah and tequila.<br>
In the distance somewhere<br>
occasional explosions followed by sirens.<br>
 <br>
Back finally to this palace<br>
high on its hill<br>
above my mighty rivers.<br>
Creatures scurry over marble floors<br>
lives bent solely to please and entice me.<br>
Bowing so deep their spines<br>
describe crescents in livery.<br>
Papers signed with a yawn:<br>
one life to be granted before noon<br>
another forfeited come evening.<br>
My vizier's metal nose<br>
shines candlelight flares<br>
across parchment.<br>
Trees vomit greenness<br>
over of my lawns.<br>
Flowers whisper in darkness<br>
plotting one more aphrodisiac spring.<br>
 <br>
Rumours from time to time<br>
of the scattered trinity: Melchior, Balthazar<br>
...the other whose name I've now forgotten...<br>
revising credos, refurbishing basilicas<br>
smashing old stone gods<br>
from sunlight to dust and rubble.<br>
 <br>
(For myself I have outlawed<br>
zealotry and iconoclasm<br>
in my termperate territories)<br>
 <br>
I could have added one more red letter<br>
to my liturgy<br>
one more rubric<br>
this time in Greek and named<br>
so they say<br>
Epiphany.<br>
 <br>
I take comfort.<br>
 <br>
Despite the portent<br>
(a galaxy swallowed in flame<br>
to make one single announcement &mdash;<br>
a serious splash in the red-tops these days)<br>
they got the wrong man.<br>
What kind of god<br>
dies crying nailed to a piece of rough wood<br>
Roman soldiers laughing all round him<br>
as the Easter weather finally breaks?</p>

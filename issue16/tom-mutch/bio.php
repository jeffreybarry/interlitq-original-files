                  <p>Tom Mutch was born in 1951 in Sydney, Australia and moved to NZ 1971. He has been painting for over 40 years and is represented in public collections as well as many private collections. He has worked in various disciplines - painting, sculpture, print-making, performance installation, film-making and writing.</p>
			<p>Mutch's discipline of working in series with strong allegorical resonance and symbolic imagery has created an identifiable 'thumbprint' in his art. This talented, multi-faceted artist's artworks are ecological and social comments on contemporary and/or historical issues.</p>
			<p>While maintaining painting as his primary expression, Mutch has, over the past 10 years, developed his passion as a novelist. He has written and published <em>The Superbird Trilogy</em>; and completed three other manuscripts.</p>
			<p>His animated short film of the same title as his first novel <em>The Birth of Superbird</em>, has been credited with the New York Independent International Film Festival Grand Jury Award for Best Animation in November 2005.</p>
			<p>He describes himself as a symbolist neo-realist and is a colourist by nature.</p>

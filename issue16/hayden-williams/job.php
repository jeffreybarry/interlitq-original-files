            <p><strong>Taranaki</strong></p>
            <p>In New Plymouth,<br>
			the sea is most often lead,<br>
			the horizon a lie of escape,<br>
			the windwand a measure of warning.<br>
			A stone church sways seasick in grass,<br>
			in waves of gravestone,<br>
			and a cursed stream hides through the town<br>
			like an eel in watercress.<br>
			At the backpackers there are chalets<br>
			beside that concealed black ribbon,<br>
			where pale flayed gums<br>
			groan and rattle,<br>
			crowd you into their cramped wet valley,<br>
			where the weather keeps you prisoner<br>
			under banks of saturnine cloud,<br>
			lodged with seemingly unpleasant companions,<br>
			with an old breast-less woman,<br>
			whose dead mouth flat-lined long ago,<br>
			who is also waiting in transit,<br>
			but unable to free herself from<br>
			the incestuous orbit of the mountain.<br>
			Is it the purity of that snowcap<br>
			that attracts such an unclean spirit?<br>
			Karma drawing our misery together<br>
			as the strands of eels in that cold creek<br>
			plait and rise to feed from scraps?</p>
&nbsp;<br>
&nbsp;<br>
            <p><strong>Shawarma Obscura</strong></p>
            <p>Glistening meat<br>
			turns like a dervish &mdash;<br>
			the Turkish owner<br>
			cuts down its side<br>
			and catches slivers<br>
			in a crescent moon.
<br><br>
			His Japanese wife<br>
			trots like a marionette<br>
			red cheeks<br>
			blood in a milk pail<br>
			plum dark eyes<br>
			dots by a shodo brush.
<br><br>
			She aches from grinning<br>
			bows and bears away trays<br>
			passes benches painted<br>
			white as cherry blossoms<br>
			for health inspectors<br>
			and dreams of a fishing village.
<br><br>
			The Welsh kitchen-hand chops chicken.<br>
			Happy to be useful for something<br>
			he hums a long-gone folksong<br>
			boils floor-mopping water<br>
			in a sosban fawr.
<br><br>
			They all work together now:<br>
			bringing, combining ingredients.<br>
			The Turk tests his English<br>
			chatting as he spices amber marinade<br>
			his hand in a jar marked 'Hints' &mdash;<br>
			crossed out, rewritten 'Herbs'.<br>
			Watchful as a minaret<br>
			he glowers down and spies<br>
			stray plastic not tidied away.
<br><br>
			"I want no overseas object here," he says.<br>
			The Welshman corrects him:<br>
			"You mean 'foreign'."
<br><br>
			When they laugh<br>
			they are a strange nation<br>
			stranded by fog on the window.</p>


            	<p>What a long silence there has been since last I wrote of love.</p>
				<p>Now a person has come into my life to whom I am strangely drawn. It is painful when you think you are done with all that to come suddenly across 'The One'. To know that you no longer have the weapons of desire, or the confidence to make him openly your own.</p>
				<p>With Andreas, I do not even know his sexuality - whether woman-orientated, gay, or asexual. I know I love him, and one day I realised he was like Gerard - the man I loved and lost at 29. Andreas has that curiously male beauty that chooses to abdicate its power, and to be expressive and dominant in other subtle and pervasive ways. </p>
				<p>Andreas teaches t'ai chi and the Alexander techniques. He carries himself like a prince. His mouth is subtle, curved and tender, yet intensely masculine. On 14th February I sent him a Valentine's Day card from 'Madame X'.</p>
				<p>On Leap Year's Day I send another card that said, "In England when a woman proposes to a man, he is supposed to give her a silk gown when he says no. Such would be my grief if you refused me that no silk gown could console me. Therefore, I will merely tell you, I want your body."</p>
				<p>Later I sent another card (again, anonymous) that said, "I want your body ... and your mind."</p>

				<p>Recently I sent a final card, white daisies touched with gold and blue. I wrote inside, "The experience of loving you is the experience of loving you. It doesn't have to be sexual to be physical and it doesn't have to be physical to be real."</p>
				<p>We went to his concert, Clare and I. He sang a song in his native Greek about the women throwing down their handkerchiefs to the man of their desire. I sent him next a blue silk handkerchief. He is himself an azure soul. His body is entrancing, but his mind, too, is rich and deep and nourishing.</p>
				<p>I love Andreas, and I accept he may never love me, want me, touch me, kiss me. I accept he may be gay; and that his spirituality and the healing arts may satisfy him so wholly he has no time for love. I wish I were young enough to bear his child, and to entice him with my beauty. If he has a preference, it is for Clare, who is slender, blonde and boyish.</p>
				<p>Clare has money and style. I am without these things. All I have is words, intrigue, and a deep abiding respect for his presence in the world. He is my fantasy. He is most real.</p>

				<p>
				Lying against you<br>
				speaking of love<br>
				my tremulous warm breath<br>
				melts the sweet fragile words<br>
				it lightly bears<br>
				liquidly slips them into<br>
				your unresistant heart.
				</p>
				
				<p>I know deeply where I will not look that his heart is most resistant. I know what I am doing is wrong. I do it anyway.</p>
				<p>His garden is going to be featured in Essentials in August. It is a bower of loveliness. He designs easy-to-wear, faintly ethnic garments; dispenses Bach flower remedies with delicate precision, and is an experienced holistic masseur. He is a wondrous light to me. He sings like an angel, and has a keen perfection. Night and morning I think of him.</p>
				<p>The last time he gave me a massage he asked me to feel his stomach so I could see how he does deep abdominal breathing. Beneath my hand I felt the pectoral muscles and the coarse hair that curls over them.</p>

				<p>
				Breathing is power.<br>
				In these dry grasses, tigers<br>
				beckon, growling low.
				</p>
				<p>
				Your hands are silent;<br>
				they carry messages<br>
				I need to understand.
				</p>
				
				<p>Before Andreas began the first massage I booked with him, he taught me three yoga exercises to do each morning. How pleasing it will be to start the day thinking of him, under his instruction. His care.</p>
				<p>He begins massage by oiling my back, and doing a few upward soothing strokes. He then works up with firm pressure along the spine. I am aware of his kneeling beside me. Sometimes I let my hand droop carelessly upon his foot. He uses his breathing to assist his work. Now and then he says something that reveals to me how much he knows, and what he thinks, how he is whole and sane and has integrity, as I do not. Though we are not good, we can love the good.</p>
				<p>Yet around Andreas is a clearly-defined 'personal space' that can only be breached - if ever - by his invitation. On Thursday night, talking to him after t'ai chi, I noticed one ear had been pierced. As a signal?</p>
				<p>He invited us in for herbal tea, but Clare was hungry and wouldn't stay. Afterwards he said, "We could have sent out for a pizza." But I was happy to be alone with Andreas, even though it seemed like a forced entry. If I am wrong in what I do, nevertheless I am alert, aware.</p>
				<p>That night he decided my toe-nails needed cutting and took my feet into his lap to do it, serious and concerned. Then he gave each foot a massage. Afterwards he held them cupped between his warm, dry hands. This is intimacy, though he would deny it.</p>
				<p>I do not dream of sex with Andreas, though sometimes I can hardly breathe from wanting to touch him freely. I dream of lying beside him on his bed, our bodies entwined, our eyes meeting. We say nothing. We lie there. We are humans, being.</p>
				<p>Andreas has a curving mouth. His eyes are blue and kind. Sometimes his soft, thinning hair gives him the look of a baby-bird. I want to touch him. I want to hold him. I throw a party I cannot afford simply so that I can sit beside him on the sofa while he sings. His voice vibrates in my breast, stirring me almost to orgasm. I sit quiet and still. I say nothing. I do nothing.</p>
				<p>I would like Andreas to place his hands upon my stomach and send good thoughts deep into the core of my being. I would like his hands on my damaged back, on my wounded heart. He shows me photographs of himself when he was young and a dancer. He is more beautiful now. He stands so straight and elegant; at ease in his body and his self. He is private and mysterious. I want never to damage him. I send him three boxes as Madame X &mdash; blue, yellow, red.</p>
				
				<p>Blue is the colour of tranquillity.</p>
				<p>I send you blue.</p>
				<p>You are the primary reason for my tranquillity.</p>

				<p>Inside the blue box I place a blue bird of happiness by Boehm.</p>

				<p>Yellow is the colour of intellectual wisdom.</p>
				<p>I send you yellow.</p>
				<p>You are the primary reason for me to learn good things.</p>

				<p>Inside the yellow box I place a yellow brocade notebook, and a gold pen.</p>

				<p>Red is the colour of passion.</p>
				<p>I send you red.</p>
				<p>You are the primary reason why I take such risks.</p>

				<p>Inside the red box I place a red velvet heart pierced with a paper knife.</p>

				<p>He tells his brother he is being stalked, and Georgiou tells me. He says Andreas has a partner, and there has been jealousy and hurt because of Madame X. He says, "Andreas has done nothing to deserve this persecution."</p>
				<p>Once in the walled garden of Battersea Park he picks loganberries from the tree, puts them between his lips and feeds them to me. The red juice runs down our chins and stains our mouths.</p>
				<p>Once when a group of us plan a picnic in Greenwich, he asks me to make spanakopita, Greek spinach and feta in filo pastry. I scour the shops for the right ingredients. I get up at 6 a.m. to make it fresh. I carry two heavy pies baked on a plate, and a bottle of ouzo across London.</p>
				<p>Once when we leave a concert together he presses me up against the wall and kisses me with open mouth and wet tongue.</p>
				<p>I listen to Georgiou. I stop going to t'ai chi. I book no more massage sessions. Six weeks later I am living far away from Railton Road in another part of London.</p>
				<p>Love is not healthy for a woman of 49. Love can not be a persecution.</p>
				<p>I make up my mind to live without love.</p>
			

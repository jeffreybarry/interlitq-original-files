            <p>I think this is a sad story. Made all the more so because it is true. And because it concerns my father.</p>
			<p>My father was, on the whole, a happy man and a playful father. The sort of father who makes your stewed-fruit bowl look like a clown, with liquorice mouth and lolly red nose. The sort of father who looks down into the ice-cream cabinet and says, "As I see it, you'll have to go for the extra double mega chocolate ultra expensive one." The sort of father who friends, with their grumpy or distant or working- too- hard fathers looked upon with a mixture of jealousy, wonder, and occasional  disbelief.</p>
			<p>But there were times when a great crushing sadness would fall upon my father. It fell, like a net from the trees, to trap him and pin him to his bed, pushing the air from his great booming chest. </p>
			<p>My mother tended to him in his dark times. She kept us children quiet and she kept him going. She sat by his bedside or lay beside him and, like a mother wiping the brow of a sick child told him that "it will all be alright. You will get through this."</p>
			<p>A traditional healer once told my father that he had moya &mdash; divine wind &mdash; the ability to channel the spirits and speak in tongues, to see beyond. I don't know whether my father believed this healer. My mother, an intensely rational, scientific woman certainly didn't. But what my mother admitted, what we all knew, was that in his silent room, from the darkness of his net, my father would sometimes revert to an earlier time, when he was a little boy. And, as that little boy he would speak the Yiddish of his beloved grandfather who had died (my mother checked this) when he was only four or he would chatter, in childish prattle, to his boyhood friends. Or he would sob.</p>
			<p>"Don't cry," my mother whispers to the little boy curled up in her husband's body. "Don't cry."</p>
			<p>"But the buttons," sobs my father. "She made me give it to him without them."</p>
			<p>"It's just a few buttons.  All will be fine."</p>
			<p>Then my father's great body shudders, as if the little boy is trying to flee both the adult body which holds him and the net which traps it. "But how could she?" he bawls. "How could she make me give it to him?"</p>
			<p>And my mother, softly, softly eases the story out of him. The story of the buttons which she will, many years later, tell me and which I am telling you now.</p>
			<p>When my father was born there lived in a small shed in their garden, a house servant or, as my father's family would have called him, a house boy. This man worked in the house and in the garden and he sent money home, to the distant hills where his own family lived. You might think that such a man, with his own children far away, would resent the little prince born to the family that he worked for, but he did no such thing. He loved the new little baby and my father, as baby, toddler, child, and adult adored him in return.</p>
			<p>Many years later, when my father had died, my mother and I talked about what made him. How a family such as his had created a man such as him. "There are many influences that make a person," my mother said. "In your father's case there was his curiosity, his friends, his studying and there was a servant. I think that man had more to do with his up bringing than&hellip; well, your father adored him. I met him only once. We drove to his village, before our honeymoon, to visit him. He died not long after that."</p>
			<p>As I imagine it, my mother is still wearing her white wedding dress when she steps from the car and onto the dusty village streets. Her bridal train spreads out behind her and she points her satin toes, like a ballerina, over the potholes in the road. This, of course, is nonsense. She would have been wearing something quiet and comfortable and good for travel. My father would have opened the car door for her and she would have stepped, in sensible shoes, into the throng of barefoot children come to greet them and to lead them up to the maduna's hut. </p>
			<p>The old man stands in the doorway. He is bent over his walking stick. His ancient chest is thin and exposed. His tattered, paint stained shirt flaps in the breeze.</p>
			<p>"How could she make me give it to him?" my father wept. "How could she do that?"</p>
			<p>"It's ok my darling. It's over now."</p>
			<p>My father has just started school. His mother is cleaning out her husband's cupboard. She finds an old paint spattered shirt. She calls her son and hands him the shirt. "You can give this to the houseboy. Your father won't wear it any more."</p>
			<p>My father folds the shirt over his arm and walks slowly, careful with his prize. He is swollen with pride. He has a gift for his friend. </p>
			<p> "Hold on a minute," his mother calls him back. Give me the shirt."</p>
			<p>She is holding a tiny pair of scissors.</p>

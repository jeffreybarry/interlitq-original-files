                  <p>Andrea Jeftanovic (Santiago, 1970). Es socióloga y obtuvo un doctorado en literatura hispanoamericana por la Universidad de California, Berkeley. Es autora de las novelas <em>Escenario de guerra</em> (Alfaguara, 2000; Baladí, España 2010;) y <em>Geografía de la Lengua </em>(Uqbar, 2007), así como el conjunto de testimonios y&nbsp;entrevistas&nbsp; <em>Conversaciones con Isidora Aguirre</em> (Frontera Sur, 2009) y co-autora del libro <em>Crónicas de oreja de vaca</em> (Bartleby, 2011). Sus relatos han integrado diversas compilaciones nacionales y extranjeras. </p>
                 
                  <p>Además, tiene una producción ensayística sobre literatura y dramaturgia contemporánea, parte de ésta se reúne en el libro <em>Hablan los hijos. Discursos y estéticas en la perspectiva infantil</em> (Cuarto Propio, 2011). Ha impartido talleres de narrativa, campo en el que publicó el volumen colectivo <em>Cuartos Contiguos </em>(2007).&nbsp;&nbsp;</p>
                 
                  <p>Ha obtenido varias distinciones, entre ellas, Premio Consejo Nacional de la Cultura y las Artes a la mejor obra editada, Juegos literarios Gabriela Mistral, mención especial en el Premio Municipal. Asimismo ha sido invitada a numerosas ferias, encuentros y residencias literarias en España, Cuba, Portugal, Estados Unidos y otros países. </p>
                
                  <p>Actualmente se desempeña como académica en la Universidad de Santiago de Chile y trabaja en nuevos proyectos literarios.</p>
                 
                  <p>El cuento presentado forma parte de su nuevo libro de cuentos, <em>No aceptes caramelos de extraños</em>,  a publicarse por Uqbar editores en septiembre del presente año.</p>                  </td>

          <td height="219" colspan="2" valign="top" class="texto2"><p align="center"><br>430<o:p></O:P></p>
            <p>As far as I can 
            recall, the initial shiver of inspiration was somehow prompted by a 
            newspaper story about an ape that, after months of coaxing by a 
            scientist, produced the first drawing ever charcoaled by an animal. 
            This sketch showed the bars of the poor creature’s 
            cage.<o:p></O:P></p>

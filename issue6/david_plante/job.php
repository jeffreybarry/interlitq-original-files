          <td colspan="4" rowspan="2" valign="top" class="texto2"><p class="MsoNormal" style="MARGIN: 0in 0in 0pt"><br><br><strong>The History of 
            Love</strong></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; LINE-HEIGHT: 200%"><span style="mso-tab-count: 1">   <br>   
                     </span>Start with: 
            amo, amas, amat, amamus, amatis, amant.</p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; LINE-HEIGHT: 200%"><span style="mso-tab-count: 1">            
            </span>To try for timelessness in writing, though the most 
            “timeless” writing is the writing in which words have accrued most 
            history, as do ancient ruins.</p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; LINE-HEIGHT: 200%"><span style="mso-tab-count: 1">            
            </span>To write with the acute awareness of the history of each 
            word.</p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; LINE-HEIGHT: 200%"><span style="mso-tab-count: 1">            
            </span>To write out of love for the words, which is to love 
            history.</p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; LINE-HEIGHT: 200%"><span style="mso-tab-count: 1">            
            </span>And where was the love of words most sustained? The absolute 
            non-believer in the Ultimate Divine must admit: in prayer.</p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; TEXT-INDENT: 0.5in">And to write</p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt"><span style="mso-spacerun: yes">                                          
            </span><i style="mso-bidi-font-style: normal"><span style="FONT-FAMILY: 'Times New Roman Italic'; mso-bidi-font-family: 'Times New Roman'; mso-ascii-font-family: 'Times New Roman Italic'; mso-hansi-font-family: 'Times New Roman Italic'">he came to me smiling<o:p></o:p></span></i></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt"><o:p></o:p></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt">as a prayer.</p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt"><o:p></o:p></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt"><span style="mso-tab-count: 1">            
            </span>Or </p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt"><span style="mso-spacerun: yes">                                      
            </span><span style="mso-spacerun: yes">  </span><i style="mso-bidi-font-style: normal"><span style="FONT-FAMILY: 'Times New Roman Italic'; mso-bidi-font-family: 'Times New Roman'; mso-ascii-font-family: 'Times New Roman Italic'; mso-hansi-font-family: 'Times New Roman Italic'">he 
            held out his arms to me<o:p></o:p></span></i></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt">as a prayer.</p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt"><o:p></o:p></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; TEXT-INDENT: 0.5in">Or</p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt"><span style="mso-spacerun: yes">                                        
            </span><i style="mso-bidi-font-style: normal"><span style="FONT-FAMILY: 'Times New Roman Italic'; mso-bidi-font-family: 'Times New Roman'; mso-ascii-font-family: 'Times New Roman Italic'; mso-hansi-font-family: 'Times New Roman Italic'">he 
            loved<o:p></o:p></span></i></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt">as a prayer.</p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt"><o:p></o:p></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; LINE-HEIGHT: 200%"><span style="mso-tab-count: 1">            
            </span>As he spoke, holding up what he called a jar as if in 
            salutation, I noted a scar on his jaw. The jar was half-filled. We 
            were in the midst of standing men, mostly aged, some old, and some 
            young men, all holding what he called jars, all of us in dim light. 
            I was holding an empty jar. He asked me if I would like a refill, 
            and smiled, a spirited smile, when I thanked him. He hurriedly drank 
            his jar down and took mine from my hand, touching my fingers with 
            the tips of his fingers. 
            <br>             
                         
                         
                          
                           
                        
                         
                          
                          
                        
                        
                            
                           
                        
                         I invited him home.</p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; TEXT-INDENT: 0.5in; LINE-HEIGHT: 200%">He 
            was thirty five and I sixty five.</p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; TEXT-INDENT: 0.5in; LINE-HEIGHT: 200%">In my bed, to hold him, naked, was for me to be 
            thirty five, to be thirty five and to have a tight scalp of short 
            soft hair, to have a brow, cheek bones, nose, lips, jaw, neck, 
            clavicle clearly and beautifully defined (oh, and all the more 
            clearly and beautifully defined in the almost dark of the room, his 
            scar gone) was to have a long, lean body, smoothly curving at the 
            shoulders, the thighs, the buttocks. <br><span style="mso-tab-count: 1">            
            </span> 
                           
             He pulled back, perhaps suddenly seeing me as I was, which made me see 
            myself as I was, and I thought: I should never 
            have invited him here, he can only be with me 
            because he wants something from me, will demand something from me and will become violent 
            if I can’t give him what he demands. His eyes shone 
            in the darkness of their hollows.</p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; TEXT-INDENT: 0.5in; LINE-HEIGHT: 200%">He 
            said, ‘I must tell you—,’<span style="mso-spacerun: yes">  
            </span>and he lay still.</p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; TEXT-INDENT: 0.5in; LINE-HEIGHT: 200%">Empedocles 
            the pre-Socratic philosopher wrote:</p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; TEXT-INDENT: 0.5in; LINE-HEIGHT: 200%"><span lang="EL" style="mso-ansi-language: EL">That all be reunited into one 
            by an act of Love.<o:p></o:p></span></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; TEXT-INDENT: 0.5in; LINE-HEIGHT: 200%"><span lang="EL" style="mso-ansi-language: EL">Now that he is dead, I try to 
            understand, or at least magnify my sense of, the altogether sudden 
            love that came to me when he told me, before we made love, that he 
            was dying, that love making with him could be dangerous for me, 
            could end in my dying too. This love came to me as a<span style="mso-spacerun: yes">  </span>‘sense’ with all the 
            multiple levels that that word raises:  from ‘sense’ as sensation, a 
            feeling that I was in my body pulled towards him more than ever by 
            the pull of his body’s dark gravity, to ‘sense’ as an implied 
            meanging, to ‘sense’ as some strange apprehension beyond meaning, 
            the very strange apprehension of an all uniting one in Love. And 
            there was in the Love a flash of joy.<o:p></o:p></span></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; TEXT-INDENT: 0.5in; LINE-HEIGHT: 200%"><span lang="EL" style="mso-ansi-language: EL">Our love-making was 
            simple.<o:p></o:p></span></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; TEXT-INDENT: 0.5in; LINE-HEIGHT: 200%"><span lang="EL" style="mso-ansi-language: EL">He spent the night, cuddled to 
            me.<o:p></o:p></span></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; TEXT-INDENT: 0.5in; LINE-HEIGHT: 200%"><span lang="EL" style="mso-ansi-language: EL">We slept together until late 
            in the morning.<o:p></o:p></span></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; TEXT-INDENT: 0.5in; LINE-HEIGHT: 200%"><span lang="EL" style="mso-ansi-language: EL">In the wan light, he looked 
            wan, and said little.<o:p></o:p></span></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; TEXT-INDENT: 0.5in; LINE-HEIGHT: 200%"><span lang="EL" style="mso-ansi-language: EL">At our late breakfast, both 
            bathed and he in clean underclothes I gave him, he told me he didn’t 
            have enough money to return to where he lived.<o:p></o:p></span></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; TEXT-INDENT: 0.5in; LINE-HEIGHT: 200%"><span lang="EL" style="mso-ansi-language: EL">Trying to make a joke, I asked 
            him what he would have done if I had not taken him home?<span style="mso-spacerun: yes">  </span>His smile was 
            crooked.<o:p></o:p></span></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; TEXT-INDENT: 0.5in; LINE-HEIGHT: 200%"><span lang="EL" style="mso-ansi-language: EL">He did not look well. He did 
            not look well.<o:p></o:p></span></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; TEXT-INDENT: 0.5in; LINE-HEIGHT: 200%"><span lang="EL" style="mso-ansi-language: EL">In his pale face, the scar 
            appeared recent, red.<o:p></o:p></span></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; TEXT-INDENT: 0.5in; LINE-HEIGHT: 200%"><span lang="EL" style="mso-ansi-language: EL">But he must keep up, for 
            appreciation, some conversation. He spoke well, and I was more 
            attentive to the individual words than his general topic, wondering 
            how, through him, this still young man, should be articulated such 
            ancient words as: </span><i style="mso-bidi-font-style: normal">I, 
            you, us, essentially. <o:p></o:p></i></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; TEXT-INDENT: 0.5in; LINE-HEIGHT: 200%"><span lang="EL" style="mso-ansi-language: EL">He stopped 
            talking.<o:p></o:p></span></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; TEXT-INDENT: 0.5in; LINE-HEIGHT: 200%"><span lang="EL" style="mso-ansi-language: EL">I said, ‘You’re not 
            well.’<o:p></o:p></span></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; TEXT-INDENT: 0.5in; LINE-HEIGHT: 200%"><span lang="EL" style="mso-ansi-language: EL">‘I’m 
            not.’<o:p></o:p></span></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; TEXT-INDENT: 0.5in; LINE-HEIGHT: 200%"><span lang="EL" style="mso-ansi-language: EL">‘What do you want that I can 
            give you?’<o:p></o:p></span></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; TEXT-INDENT: 0.5in; LINE-HEIGHT: 200%"><span lang="EL" style="mso-ansi-language: EL">He lowered his head and said, 
            ‘I want to believe everyone’s death is 
unique.’<o:p></o:p></span></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; TEXT-INDENT: 0.5in; LINE-HEIGHT: 200%"><span lang="EL" style="mso-ansi-language: EL">‘It is.’<o:p></o:p></span></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; TEXT-INDENT: 0.5in; LINE-HEIGHT: 200%"><span lang="EL" style="mso-ansi-language: EL">‘And I want to believe in life 
            after death.’ <o:p></o:p></span></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; TEXT-INDENT: 0.5in; LINE-HEIGHT: 200%"><span lang="EL" style="mso-ansi-language: EL">And I, the ab</span><span lang="ES-AR" style="mso-ansi-language: ES-AR">solute</span><span lang="EL" style="mso-ansi-language: EL"> non-believer in the Divine 
            One, said, ‘There is.’<o:p></o:p></span></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; TEXT-INDENT: 0.5in; LINE-HEIGHT: 200%"><span lang="EL" style="mso-ansi-language: EL">I gave him money and made him 
            swear he would contact me when he was back to wherever he lived. 
            <o:p></o:p></span></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; TEXT-INDENT: 0.5in; LINE-HEIGHT: 200%"><span lang="EL" style="mso-ansi-language: EL">‘Goodbye,’ he said,<span style="mso-spacerun: yes">  </span>and, 
            ‘Love.’<o:p></o:p></span></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; TEXT-INDENT: 0.5in; LINE-HEIGHT: 200%"><span lang="EL" style="mso-ansi-language: EL">I was away for months, in 
            another city, in another country. Returned, I heard that he had 
            died.<o:p></o:p></span></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; TEXT-INDENT: 0.5in; LINE-HEIGHT: 200%"><span lang="EL" style="mso-ansi-language: EL">Make this the center of all 
            writing: a prayer to the dead, a prayer to the dead, a prayer in 
            thanksgiving for all the words the dead have left 
            us:<o:p></o:p></span></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; TEXT-INDENT: 0.5in; LINE-HEIGHT: 200%"><span lang="EL" style="mso-ansi-language: EL">I love, you love, he/she 
            loves, we love, you love, they love. <o:p></o:p></span></p>
            <p> </p>
			
			
			
			<br><strong>Metaphysics</strong><br><br>
<p class="MsoNormal" style="MARGIN: 0in 0in 0pt; LINE-HEIGHT: 200%"><span style="mso-tab-count: 1">            
</span>He and his college roommate became lovers. After graduation,<span style="mso-spacerun: yes"> </span>they separated and he did not see his 
former lover in forty years, or hear from him in thirty. When he learned that 
his former lover, whom he had loved and who had loved him, was dead, the 
immediate image that came to him was:</p>
<p class="MsoNormal" style="MARGIN: 0in 0in 0pt; LINE-HEIGHT: 200%"><span style="mso-tab-count: 1">            
</span><i style="mso-bidi-font-style: normal"><span style="FONT-FAMILY: 'Times New Roman Italic'; mso-bidi-font-family: 'Times New Roman'; mso-ascii-font-family: 'Times New Roman Italic'; mso-hansi-font-family: 'Times New Roman Italic'">The 
sheets of both their beds, stripped from their separate beds and piled in a 
tangled heap between the beds, the sunlight through the room’s window shining on 
the sheets</span></i>.</p>
<p class="MsoNormal" style="MARGIN: 0in 0in 0pt; LINE-HEIGHT: 200%"><span style="mso-tab-count: 1">            
</span>He remembered the courses in philosophy they had taken together. </p>
<p class="MsoNormal" style="MARGIN: 0in 0in 0pt; LINE-HEIGHT: 200%"><span style="mso-tab-count: 1">            
</span>A friend said to him, “When you try to be metaphysical, you become 
pretentious.” And he sensed in that accusation all the learning he had had in 
logic, in epistemology, in ontology become suddenly an affectation, the learning 
of his years in a Jesuit college, as if his desire to answer the questions, 
“What is reasoning?” or “What is apprehension and how does one see the universal 
in the particular?” or “what is the essence of being?” had no relevance, were 
questions that had no meaningful answers, so, being meaningless, must not be 
asked.</p>
<p class="MsoNormal" style="MARGIN: 0in 0in 0pt; LINE-HEIGHT: 200%"><span style="mso-tab-count: 1">            
</span>But as a student he believed in philosophy, which belief he had shared 
with his roommate even before they became lovers; believed that philosophy’s 
ultimate meaning was proof of the existence of God.<span style="mso-spacerun: yes">  </span>He had followed the reasoning from 
causality, followed, as if up a long, long ladder, the reasoning from causality 
up the to the top of the ladder to the Uncaused Causer, the One God in whom all 
originated and all was, up to where—faith required a leap higher than the 
ladder, a leap higher than reason, a leap into the unknown.</p>
<p class="MsoNormal" style="MARGIN: 0in 0in 0pt; TEXT-INDENT: 0.5in; LINE-HEIGHT: 200%">He leapt and 
he fell.</p>
<p class="MsoNormal" style="MARGIN: 0in 0in 0pt; TEXT-INDENT: 0.5in; LINE-HEIGHT: 200%">His former 
roommate, his former lover caught him in his arms.</p>
<p class="MsoNormal" style="MARGIN: 0in 0in 0pt; TEXT-INDENT: 0.5in; LINE-HEIGHT: 200%">In The Summa 
Theologica, Saint Thomas Aquinas wrote: “Incorporeal things, of which there are 
no images, are known to us by means of their relation to sensible bodies of 
which there are images. And so when we understand something about incorporeal 
things, we have recourse to the images of bodies, although there are no images 
of incorporeal things themselves.”</p>
<p class="MsoNormal" style="MARGIN: 0in 0in 0pt; TEXT-INDENT: 0.5in; LINE-HEIGHT: 200%">His former 
roommate, his former lover gave him faith<span style="mso-spacerun: yes">  
</span>beyond reason: in the miraculous occurrence, beyond reason, of Everything 
All Together, which apprehension could not come from experience, for no one had 
the experience of everything, but had a “sense” of everything.</p>
<p class="MsoNormal" style="MARGIN: 0in 0in 0pt; TEXT-INDENT: 0.5in; LINE-HEIGHT: 200%">To believe 
this “sense” of everything<span style="mso-spacerun: yes">  </span>as proof 
of Divinity, a Loving Divinity who inspires in us the “sense” of Everything 
Together-- </p>
<p class="MsoNormal" style="MARGIN: 0in 0in 0pt; TEXT-INDENT: 0.5in; LINE-HEIGHT: 200%">That the 
Loving Divinity, Loving the World, gives us the vital “sense” of the World, and 
in that “sense” inspires in us Love for the World.</p>
<p class="MsoNormal" style="MARGIN: 0in 0in 0pt; TEXT-INDENT: 0.5in; LINE-HEIGHT: 200%">To sustain 
the Love, by keeping the Love centered in an image:</p>
<p class="MsoNormal" style="MARGIN: 0in 0in 0pt; TEXT-INDENT: 0.5in; LINE-HEIGHT: 200%"><i style="mso-bidi-font-style: normal"><span style="FONT-FAMILY: 'Times New Roman Italic'; mso-bidi-font-family: 'Times New Roman'; mso-ascii-font-family: 'Times New Roman Italic'; mso-hansi-font-family: 'Times New Roman Italic'">The 
body of his lover, the naked body of his young and beautiful and haloed lover, 
within a sphere, a celestial sphere</span></i>, <i style="mso-bidi-font-style: normal"><span style="FONT-FAMILY: 'Times New Roman Italic'; mso-bidi-font-family: 'Times New Roman'; mso-ascii-font-family: 'Times New Roman Italic'; mso-hansi-font-family: 'Times New Roman Italic'">a 
Divine Sphere, ordered by the gentle immortal impulse of blameless 
Love.<o:p></o:p></span></i></p>
<br><br><strong>A 
Prayer</strong> <br><br>
<p class="MsoNormal" style="MARGIN: 0in 0in 0pt; TEXT-INDENT: 0.5in; LINE-HEIGHT: 200%">We walked 
together about the old part of the city, once a walled-in city, the walls mostly 
ruins.</p>
<p class="MsoNormal" style="MARGIN: 0in 0in 0pt; LINE-HEIGHT: 200%"><span style="mso-tab-count: 1">            
</span>He said to me, “No, I can’t believe in God, I can’t. Over the centuries, 
an untold, a truly untold number of prayers have been said to God for God’s 
help, and I can’t understand why God hasn’t answered every single one if God is 
a loving God. If God has answered some, they’re very few compared to those 
untold number of prayers that were not answered, prayers cried out by the 
desperate for God’s help, whole walled-in cities crying out for God to deliver 
them from massacre. And I won’t accept that God is testing our faith. The test 
is unreasonable, and if there is a God, God is supposed to be a reasonable as 
well as a loving God. No, no.”<span style="mso-tab-count: 1">     </span></p>
<p class="MsoNormal" style="MARGIN: 0in 0in 0pt; LINE-HEIGHT: 200%"><span style="mso-tab-count: 1">            
</span>I asked him, “Did you once believe?”</p>
<p class="MsoNormal" style="MARGIN: 0in 0in 0pt; LINE-HEIGHT: 200%"><span style="mso-tab-count: 1">            
</span>And he answered, “I once believed.”</p>
<p class="MsoNormal" style="MARGIN: 0in 0in 0pt; LINE-HEIGHT: 200%"><span style="mso-tab-count: 1">            
</span>“And you stopped believing.”</p>
<p class="MsoNormal" style="MARGIN: 0in 0in 0pt; LINE-HEIGHT: 200%"><span style="mso-tab-count: 1">            
</span>“I more than said a prayer, I implored in prayer for God’s help, and God 
didn’t help.” He laughed. “I needed God’s help as a matter of life and 
death.”</p>
<p class="MsoNormal" style="MARGIN: 0in 0in 0pt; LINE-HEIGHT: 200%"><span style="mso-tab-count: 1">            
</span>“Yours?”</p>
<p class="MsoNormal" style="MARGIN: 0in 0in 0pt; LINE-HEIGHT: 200%"><span style="mso-tab-count: 1">            
</span>“Someone else’s life or death.”</p>
<p class="MsoNormal" style="MARGIN: 0in 0in 0pt; LINE-HEIGHT: 200%"><span style="mso-tab-count: 1">            
</span>“And this someone else died.”</p>
<p class="MsoNormal" style="MARGIN: 0in 0in 0pt; LINE-HEIGHT: 200%"><span style="mso-tab-count: 1">            
</span>“Died.”</p>

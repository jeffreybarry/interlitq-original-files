          <td colspan="4" rowspan="2" valign="top" class="texto2">  
            <br>
            <p><span lang="EN-GB">When the 
            telegram came to tell me I had won an Exhibition to Newnham, I was 
            hiding in the school washrooms to avoid a freezing hockey 
            pitch.<span style="mso-spacerun: yes">  </span>I still remember 
            the first elation, and then, almost at once, a puzzling 
            half-thought: <I 
            style="mso-bidi-font-style: normal"><SPAN 
            lang=EN-GB>Cambridge</SPAN></I><I 
            style="mso-bidi-font-style: normal"><SPAN lang=EN-GB> will be my 
            escape.</SPAN></I></p>
            <P><SPAN lang=EN-GB>But escape from what exactly?<SPAN 
            style="mso-spacerun: yes">&nbsp; </SPAN>Mine had not been an unhappy 
            childhood.</SPAN><BR><BR>My earliest memories are of a 
            semi-detached suburban house on </SPAN><st1:street><st1:address><span lang="EN-GB">Groby 
            Road</span></ST1:ADDRESS></ST1:STREET><span lang="EN-GB">, 
            </span><st1:place><span lang="EN-GB">Leicester</span></ST1:PLACE><span lang="EN-GB">; a road which led to the outskirts of town past the 
            cemetery, towards a quarry where children were not allowed to 
            play.<span style="mso-spacerun: yes">&nbsp; </span>My parents came 
            there from </span><st1:place><span lang="EN-GB">Liverpool</span></ST1:PLACE><span lang="EN-GB"> just after 
            my first birthday, leaving behind a larger house in 
            </span><st1:street><st1:address><span lang="EN-GB">Arundel 
            Avenue</span></ST1:ADDRESS></ST1:STREET><span lang="EN-GB">, 
            </span><st1:place><span lang="EN-GB">Bootle</span></ST1:PLACE><span lang="EN-GB">. </span><st1:place><span lang="EN-GB">Liverpool</span></ST1:PLACE><span lang="EN-GB"> was in the 
            grip of the Depression, which had not yet paralysed 
            </span><st1:place><span lang="EN-GB">Leicester</span></ST1:PLACE><span lang="EN-GB">.</span></P>
            <p><span lang="EN-GB">My mother and 
            father lived together peaceably, though they were very different in 
            background and temperament. She was a gentle, shy creature with a 
            soft voice and a Grammar School education, while he had left school 
            at twelve to be apprenticed<span style="mso-spacerun: yes">&nbsp; 
            </span>as a cabinet maker.<span style="mso-spacerun: yes">&nbsp; 
            </span>She was a small, delicate woman and he was powerfully built 
            and, if not exactly handsome - in my adolescence I saw a resemblance 
            to Humphrey Bogart - always ebullient.<span style="mso-spacerun: yes">&nbsp; </span></span></p>
            <p><span lang="EN-GB"><o:p></O:P></span></p>
            <p><span lang="EN-GB">He had set up his 
            own workshop, and I thought of him as rich because he drove a big 
            car and liked to spend money.<span style="mso-spacerun: yes">&nbsp; 
            </span>He took us to grand hotels and expensive restaurants and 
            enjoyed giving presents. '<i style="mso-bidi-font-style: normal">Izzy</i>,' my mother would 
            reproach him, because she did the firm's books and knew their 
            financial situation to a penny.<span style="mso-spacerun: yes">&nbsp; </span>He laughed and shrugged 
            while I gazed up at him with complete trust.</span></p>
            <p><span lang="EN-GB"><o:p></O:P></span></p>
            <p><span lang="EN-GB">Naturally I adored 
            him.</span></p>
            <p><span lang="EN-GB"><o:p></O:P></span></p>
            <p><span lang="EN-GB">Once, though, 
            in the Maypole grocery shop close by our house, I was shaken by a 
            brief glimpse into what it meant to run out of money.<span style="mso-spacerun: yes">&nbsp; </span>I can still smell the sweet 
            tea and smoky odours of bacon sides, and hear my mother say 
            nervously to the shopkeeper: 'We're going off on holiday this 
            evening.<span style="mso-spacerun: yes">&nbsp; </span>Will next 
            Tuesday be all right?'<span style="mso-spacerun: yes">&nbsp; 
            </span>And I remember tugging at her sleeve, because I knew it was 
            next week we were leaving for </span><st1:place><span lang="EN-GB">Devon</span></ST1:PLACE><span lang="EN-GB">.<span style="mso-spacerun: yes">&nbsp; </span>When I said as much, my 
            mother's face turned a peculiar red.<span style="mso-spacerun: yes">&nbsp; </span>Then I realised with horror 
            that she'd been lying because she couldn't pay.<span style="mso-spacerun: yes">&nbsp; </span>She didn't take my hand as 
            we walked silently home together along 
            </span><st1:street><st1:address><span lang="EN-GB">Groby 
            Road</span></ST1:ADDRESS></ST1:STREET><span lang="EN-GB">.<span style="mso-spacerun: yes">&nbsp; </span></span></p>
            <p><span lang="EN-GB"><o:p></O:P></span></p>
            <p ><span lang="EN-GB">I was an only 
            child, round-eyed and more shocked by my own mistake than my 
            mother's embarrassment; she was a good, kind woman, but it was my 
            father who stole my affections.<span style="mso-spacerun: yes">&nbsp; </span>Wickedly, I never felt sorry 
            for her, even though I knew she was often ill.<span style="mso-spacerun: yes">&nbsp; </span>As soon as I started school, 
            Dad made my breakfast - poached eggs on toast - while she 
            rested.<span style="mso-spacerun: yes">&nbsp; </span>He always took 
            me there in his car.<span style="mso-spacerun: yes">&nbsp; 
            </span>And on holiday he enjoyed fairgrounds and seaside piers, 
            roller-coaster rides and booths where you could win fluffy 
            toys.<span style="mso-spacerun: yes">&nbsp; </span>He watched 
            patiently as I tried to lower a penny mechanical crane over trinkets 
            lying on piles of coloured pebbles, never scooping up so much as a 
            boiled sweet.<span style="mso-spacerun: yes">&nbsp; </span>In a 
            treasure hunt one afternoon on Skegness Sands we were luckier; we 
            won a miniature boat with white sails.<span style="mso-spacerun: yes">&nbsp; </span>He was 
            triumphant.</span></p>
            <p><span lang="EN-GB"><o:p></O:P></span></p>
            <p><span lang="EN-GB">Both my 
            parents came from Russian Jewish families who left 
            </span><st1:city><st1:place><span lang="EN-GB">Odessa</span></ST1:PLACE></ST1:CITY><span lang="EN-GB"> at 
            the end of the nineteenth century to live in 
            </span><st1:country-region><st1:place><span lang="EN-GB">Britain</span></ST1:PLACE></ST1:COUNTRY-REGION><span lang="EN-GB">.<span style="mso-spacerun: yes">&nbsp; </span>The 
            resemblance ended there.<span style="mso-spacerun: yes">&nbsp; 
            </span>Dad's father - <i style="mso-bidi-font-style: normal">Zaida</i> as I was told to call 
            him - was a scholar and a dreamer; his children kept the family 
            timber firm going and my father was very proud of him because he had 
            studied at a <i style="mso-bidi-font-style: normal">Yeshivah</i> and 
            could understand the sacred books.</span></p>
            <p><span lang="EN-GB"><o:p></O:P></span></p>
            <p><span lang="EN-GB">My mother's 
            family was dominated by Solomon, a small, clean-shaven patriarch, 
            with starched triangles to his white collars and a single rose-cut 
            stone in his tiepin.<span style="mso-spacerun: yes">&nbsp; </span>He 
            was a cold, scary man, but a shrewd one. He began life in 
            </span><st1:country-region><st1:place><span lang="EN-GB">Great 
            Britain</span></ST1:PLACE></ST1:COUNTRY-REGION><span lang="EN-GB"> as 
            an apprentice glazier, riding around the streets on a bicycle with 
            plate glass under his arm.<span style="mso-spacerun: yes">&nbsp; 
            </span>By the time I knew him, he was a successful glass merchant 
            whose firm put windows into the Cumberland Hotel in 
            </span><st1:city><st1:place><span lang="EN-GB">London</span></ST1:PLACE></ST1:CITY><span lang="EN-GB">.<span style="mso-spacerun: yes">&nbsp; </span>On his 
            rare visits, I could see he disapproved of my father 
            profoundly.</span></p>
            <p><span lang="EN-GB"><o:p></O:P></span></p>
            <p><span lang="EN-GB">I saw a good 
            deal more of Zaida, who was large and affectionate, with a ginger 
            beard and deep laughter lines round his blue eyes.<span style="mso-spacerun: yes">&nbsp; </span>I liked to sit on his knee 
            while he told me stories about the sunny </span><st1:place><span lang="EN-GB">Black Sea</span></ST1:PLACE><span lang="EN-GB"> 
            </span><st1:place><st1:placetype><span lang="EN-GB">port</span></ST1:PLACETYPE><span lang="EN-GB"> of 
            </span><st1:placename><span lang="EN-GB">Odessa</span></ST1:PLACENAME></ST1:PLACE><span lang="EN-GB">, where Jews were allowed to enter Russian schools and 
            live wherever they wanted.<span style="mso-spacerun: yes">&nbsp; 
            </span>Zaida loved to describe the street music, the peddlers and 
            the market stalls.<span style="mso-spacerun: yes">&nbsp; 
            </span></span></p>
            <p><span lang="EN-GB"><o:p></O:P></span></p>
            <p><span lang="EN-GB">As a boy, he 
            had lost the top joint of one of his fingers when left in charge of 
            a circular saw.<span style="mso-spacerun: yes">&nbsp; </span>He 
            showed me the stump of his smooth, unmarked knuckle.<span style="mso-spacerun: yes">&nbsp; </span>Soon after that accident, he 
            was sent off to study the arguments of the Talmudic Rabbis in 
            </span><st1:city><st1:place><span lang="EN-GB">Odessa</span></ST1:PLACE></ST1:CITY><span lang="EN-GB">.<span style="mso-spacerun: yes">&nbsp; </span>He married 
            there and had to earn the money to feed his wife and child by 
            working on the docks unloading fish; though it may be, as one of my 
            aunts told me, that his wife supported him by working in a 
            factory.</span></p>
            <p><span lang="EN-GB"><o:p></O:P></span></p>
            <p><span lang="EN-GB">He was not a 
            fastidious man.<span style="mso-spacerun: yes">&nbsp; </span>His 
            cardigans sagged at the back and he smelled of peppermint and 
            snuff.<span style="mso-spacerun: yes">&nbsp; </span>He left his 
            cigars half-smoked in ashtrays all round the house when he came to 
            stay with us.<span style="mso-spacerun: yes">&nbsp; </span>My mother 
            folded her lips tightly as she gathered them up.</span></p>
            <p><span lang="EN-GB"><o:p></O:P></span></p>
            <p ><span lang="EN-GB">Zaida had complete faith 
            in God's protection.<span style="mso-spacerun: yes">&nbsp; 
            </span>Once I asked him whether he was afraid of dying.<span style="mso-spacerun: yes">&nbsp; </span>He shrugged and pointed at 
            the ceiling. 'He will look after me,' he said, 'He always has.'<span style="mso-spacerun: yes">&nbsp; </span>That faith led him to 
            abandon his wood shop in </span><st1:city><st1:place><span lang="EN-GB">London</span></ST1:PLACE></ST1:CITY><span lang="EN-GB"> in 
            the early years of the twentieth century and take his wife and 
            children to </span><st1:country-region><st1:place><span lang="EN-GB">Canada</span></ST1:PLACE></ST1:COUNTRY-REGION><span lang="EN-GB">, where he hoped to become a farmer.<span style="mso-spacerun: yes">&nbsp; </span>Perhaps he was remembering 
            </span><st1:place><span lang="EN-GB">Southern 
            Russia</span></ST1:PLACE><span lang="EN-GB">, where fruit and 
            vegetables grow easily.<span style="mso-spacerun: yes">&nbsp; 
            </span>The land he was allocated by the Canadian government, 
            however, was close to </span><st1:city><st1:place><span lang="EN-GB">Montreal</span></ST1:PLACE></ST1:CITY><span lang="EN-GB">, 
            where heavy snow and ice cover the ground for more than six months 
            of the year.<span style="mso-spacerun: yes">&nbsp; </span>He also 
            found himself surrounded by distrustful Ukrainian Catholics, by no 
            means enthusiastic to find a Jewish family in their midst.<span style="mso-spacerun: yes">&nbsp; </span>They were amused at his 
            efforts rather than helpful.</span></p>
            <p><span lang="EN-GB"><o:p></O:P></span></p>
            <p><span lang="EN-GB">My father, 
            who was four when they arrived in 
            </span><st1:country-region><st1:place><span lang="EN-GB">Canada</span></ST1:PLACE></ST1:COUNTRY-REGION><span lang="EN-GB">, recalled some moments of sheer magic nevertheless: he 
            liked to tell me how his mother threw boiling sugar out on to the 
            snow which made a delicious brittle toffee.<span style="mso-spacerun: yes">&nbsp; </span>He was a natural Romantic, 
            and always had a wondering response to the world around him.<span style="mso-spacerun: yes">&nbsp; </span></span></p>
            <p><span lang="EN-GB"><o:p></O:P></span></p>
            <p><span lang="EN-GB">My mother's 
            family was an altogether different breed.<span style="mso-spacerun: yes">&nbsp; </span>They didn't believe in God 
            or his protection, and they were impatient with the Romantic 
            temperament.<span style="mso-spacerun: yes">&nbsp; </span>My mother 
            was very proud of her elder brothers, Joseph and Maurice, who took 
            First Class degrees and learned to speak a perfectly inflected 
            standard English.<span style="mso-spacerun: yes">&nbsp; </span>Both 
            were thin men, with narrow faces, though Jo became redder and more 
            corpulent as he grew older.<span style="mso-spacerun: yes">&nbsp; 
            </span>Soon after Jo came down from University, he persuaded Solomon 
            that the family name should be changed from Goldstein to 
            </span><st1:city><st1:place><span lang="EN-GB">Compton</span></ST1:PLACE></ST1:CITY><span lang="EN-GB">.<span style="mso-spacerun: yes">&nbsp; </span>The change 
            was a move towards assimilation into gentile 
            society.</span></p>
            <p><span lang="EN-GB"><o:p></O:P></span></p>
            <p><span lang="EN-GB">I thought 
            very little about being Jewish before the war, though I always took 
            days off for Jewish holidays and never went into prayers at the 
            school's morning Assembly.<span style="mso-spacerun: yes">&nbsp; 
            </span>Until the war broke out, there were not many Jewish families 
            in </span><st1:place><span lang="EN-GB">Leicester</span></ST1:PLACE><span lang="EN-GB"> and I knew 
            them all.<span style="mso-spacerun: yes">&nbsp; </span>Some of them 
            were related to me.<span style="mso-spacerun: yes">&nbsp; </span>My 
            parents played cards with them, usually poker.<span style="mso-spacerun: yes">&nbsp; </span>I thought being Jewish was 
            like being part of an extended family.<span style="mso-spacerun: yes">&nbsp; </span>Or, I suppose, a 
            tribe.</span></p>
            <p><span lang="EN-GB"><o:p></O:P></span></p>
            <p><span lang="EN-GB">How 
            anglicised were we?<span style="mso-spacerun: yes">&nbsp; </span>My 
            mother in her hat and tweed coat at a bus-stop looked like any other 
            </span><st1:place><span lang="EN-GB">Midlands</span></ST1:PLACE><span lang="EN-GB"> housewife.<span style="mso-spacerun: yes">&nbsp; 
            </span>My father, built like a sportsman, was accepted without demur 
            at Glen Gorse Golf Club.<span style="mso-spacerun: yes">&nbsp; 
            </span>Still, D.H. Lawrence, who wrote in his letters about his 
            dislike of rich Jews at the seaside, would probably have detected 
            something foreign in him.<span style="mso-spacerun: yes">&nbsp; 
            </span>There were not so many immigrants in 
            </span><st1:country-region><st1:place><span lang="EN-GB">Britain</span></ST1:PLACE></ST1:COUNTRY-REGION><span lang="EN-GB"> then.</span></p>
            <p><span lang="EN-GB"><o:p></O:P></span></p>
            <p><span lang="EN-GB">My father had 
            nothing but contempt for those who tried to deny their origins.<span style="mso-spacerun: yes">&nbsp; </span>He liked to go to the 
            Synagogue and sing the familiar tunes on a Saturday morning.<span style="mso-spacerun: yes">&nbsp; </span>My mother lit candles on a 
            Friday night.<span style="mso-spacerun: yes">&nbsp; </span>On 
            Passover, I loved the crackly <i style="mso-bidi-font-style: normal">matzos</i> which I ate with 
            butter and apricot jam. I remember, as a very small child, my father 
            told me the story of the Hebrews escape from slavery in 
            </span><st1:country-region><st1:place><span lang="EN-GB">Egypt</span></ST1:PLACE></ST1:COUNTRY-REGION><span lang="EN-GB">.<span style="mso-spacerun: yes">&nbsp; </span>His 
            version of the tale<i style="mso-bidi-font-style: normal"> 
            </i>absorbed me completely; the account in the <i style="mso-bidi-font-style: normal">Haggadah</i> read later was 
            something of a disappointment.<span style="mso-spacerun: yes">&nbsp;&nbsp;&nbsp;&nbsp; </span>Still, I 
            enjoyed the game of searching for the <i style="mso-bidi-font-style: normal">afikomen </i>he had hidden, and 
            the reward when I found it.<span style="mso-spacerun: yes">&nbsp;&nbsp; 
            </span></span></p>
            <p><span lang="EN-GB"><o:p></O:P></span></p>
            <p><span lang="EN-GB">Ours was a 
            very mild, almost English allegiance to the old rituals.<span style="mso-spacerun: yes">&nbsp; </span>No one in the whole 
            community wore the long side-locks or Polish gear of present-day 
            orthodox Jews.<span style="mso-spacerun: yes">&nbsp; </span>Even my 
            rabbinical Zaida did not expect his wife to wear a <i style="mso-bidi-font-style: normal">sheitel</i>.<span style="mso-spacerun: yes">&nbsp; </span>My father was a genuine 
            believer, though, and when I confronted him later in life with 
            Evolution and Astronomy, he was not so much assertive as puzzled. 
            'What can we little human beings know of all that?' he would 
            say.<span style="mso-spacerun: yes">&nbsp; 
            </span></span></p>
            <p><span lang="EN-GB"><o:p></O:P></span></p>
            <p><span lang="EN-GB">The 
            </span><st1:city><st1:place><span lang="EN-GB">Comptons</span></ST1:PLACE></ST1:CITY><span lang="EN-GB"> 
            were socialist and atheist, and found my father's belief in God 
            tiresome, though they were far too polite to argue with him.<span style="mso-spacerun: yes">&nbsp; </span>I liked them well enough, 
            but loyally accepted my father's opinion of them for many 
            years.<span style="mso-spacerun: yes">&nbsp; </span>He was 
            particularly dismissive of Annie, a handsome, sophisticated woman, 
            Matron of a hospital near </span><st1:city><st1:place><span lang="EN-GB">Durham</span></ST1:PLACE></ST1:CITY><span lang="EN-GB">, 
            because she was a spinster.<span style="mso-spacerun: yes">&nbsp; 
            </span>Sometimes she shared a bedroom with me and I was amazed by 
            her underwear of lilac lace, very unlike the functional white 
            underclothes I had seen on my mother.<span style="mso-spacerun: yes">&nbsp; </span>In turn, she remembered my 
            mother's feminine prettiness as a girl, and was disappointed in my 
            stronger, more foreign features, once sadly remarking that my looks 
            resembled those of my father's family.</span></p>
            <p><span lang="EN-GB"><o:p></O:P></span></p>
            <p><span lang="EN-GB">I was not 
            anxious about that resemblance or my Jewishness, but in my 
            </span><st1:place><st1:placename><span lang="EN-GB">Junior</span></ST1:PLACENAME><span lang="EN-GB"> 
            </span><st1:placetype><span lang="EN-GB">School</span></ST1:PLACETYPE></ST1:PLACE><span lang="EN-GB"> some time in 1937 or 1938 I was given a brusque 
            awakening.<span style="mso-spacerun: yes">&nbsp; </span>I remember 
            the smell of wet coats hanging up on the low hooks of the Junior 
            Cloakroom, so it was probably just after lunch.<span style="mso-spacerun: yes">&nbsp; </span>I had won one of the silver 
            medals the Form Teacher awarded every week for good work.<span style="mso-spacerun: yes">&nbsp; </span>As I was pulling off my 
            coat, with the medal proudly pinned to my tunic, an unpleasantly 
            red-faced girl, whose name I can still remember but won't use, came 
            up to me and sneered: 'My father says you are nothing but a dirty 
            Jew.'<span style="mso-spacerun: yes">&nbsp; </span>I was in floods 
            of tears as I told my father after school.<span style="mso-spacerun: yes">&nbsp; </span>He laughed away her words, 
            but I heard my parents talking about what action to take long after 
            I had gone to bed.<span style="mso-spacerun: yes">&nbsp; </span>My 
            mother would not consider a change of school.<span style="mso-spacerun: yes">&nbsp; </span></span></p>
            <p><span lang="EN-GB"><o:p></O:P></span></p>
            <p><span lang="EN-GB">She had 
            taught me to read before I was four and I took to it so passionately 
            that she wrote to her brother Jo to ask advice about which were the 
            best schools.<span style="mso-spacerun: yes">&nbsp; </span>Jo wrote 
            back that she should save her money since, if I had any 
            intelligence, it would come out whatever school I was sent to.<span style="mso-spacerun: yes">&nbsp; </span>In spite of her admiration 
            for Jo, my mother ignored this advice and chose an excellent 
            </span><st1:place><st1:placename><span lang="EN-GB">Junior</span></ST1:PLACENAME><span lang="EN-GB"> 
            </span><st1:placetype><span lang="EN-GB">School</span></ST1:PLACETYPE></ST1:PLACE><span lang="EN-GB"> at the other side of </span><st1:place><span lang="EN-GB">Leicester</span></ST1:PLACE><span lang="EN-GB">, for which 
            I needed to wear a dark velour hat with a smart crest on it, and a 
            navy blue coat.</span></p>

            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt">TO AUTUMN</p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt"><i style="mso-bidi-font-style: normal">for Donald 
            Revell</i></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt"><o:p></o:p></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt"><br>Trash instead of 
            geese</p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt">Has landed all over 
            our pond!</p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt">Single sheets of 
            newsprint lie apart</p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt">Or overlap, as if 
            collecting dew:</p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt">Our son, who has come 
            here</p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt">In a stroller to applaud birds,<br><br>    
             </p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt"><o:p></o:p></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt">Applauds their 
            absence, stands, and picks up rocks,</p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt">Enumerating: <i style="mso-bidi-font-style: normal">One, two, 
            three,<o:p></o:p></i></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt"><i style="mso-bidi-font-style: normal">Four, seven, eight</i>.</p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt 1in">This year our 
            summer lasts</p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt">Until October, an 
            unhealthy state</p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt">Of which we take 
            advantage, taking walks</p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt">That last all 
            morning, making pebbled tracks,</p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt">Staying away from the road. Meanwhile big 
            trucks<br><br>    
               </p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt"><o:p></o:p></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt">Patrol our Concord 
            Avenue, their red</p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt">Sides' single 
            question flourishing</p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt">In circus script, 
            like handbills' old good news:</p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt">"Who But W. B. 
            Mason?"</p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt">Who indeed. </p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt 0.5in; TEXT-INDENT: 0.5in">Yellow clover 
            abides</p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt">Beside all our 
            footpaths. Hundreds of miles away</p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt">Last night, a tumultuous infestation of 

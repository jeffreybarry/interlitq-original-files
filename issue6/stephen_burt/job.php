<p class="MsoNormal" style="MARGIN: 0in 0in 0pt">THE 
SOUL</p>
<p class="MsoNormal" style="MARGIN: 0in 0in 0pt"></p>
<p class="MsoNormal" style="MARGIN: 0in 0in 0pt"><br>Easy to 
recognize in its costume</p>
<p class="MsoNormal" style="MARGIN: 0in 0in 0pt">made up of 
Sunday puzzles and Scrabble tiles,<br><br>   
    </p>
<p class="MsoNormal" style="MARGIN: 0in 0in 0pt"></span></p>
<p class="MsoNormal" style="MARGIN: 0in 0in 0pt">you can take 
it, but not very far.</p>
<p class="MsoNormal" style="MARGIN: 0in 0in 0pt">Nor can you 
baste, drip-dry or evaluate<br><br>   
   </p>

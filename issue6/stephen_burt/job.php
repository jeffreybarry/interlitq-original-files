            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt">Shut down, for over 
            an hour, a baseball game.</p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt">The Indians won. An 
            opaque, sticky cloud</p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt">Befuddled the 
            opposing pitcher no end.</p>

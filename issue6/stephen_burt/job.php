            ATLAS MOTH<o:p></o:p></span></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; TEXT-ALIGN: center" align="left"><o:p></o:p></span></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; TEXT-ALIGN: center" align="left"><o:p></o:p></span></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; TEXT-ALIGN: center" align="left"><br>Now I am an 
            adult<span style="mso-spacerun: yes">  </span>&amp; I will 
            never eat again.<o:p></o:p></span></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; TEXT-ALIGN: center" align="left">All the weight 

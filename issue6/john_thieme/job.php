            <br>
            <p>We 
            never met Himmelstein.<span style="mso-spacerun: yes">  
            </span>Yet in some ways he was more important to us during those 
            Septembers in the late 1980s than any of our closest friends.<span style="mso-spacerun: yes">  </span>Perhaps he never existed at 
            all.<span style="mso-spacerun: yes">  </span>But he was very 
            real for us.<span style="mso-spacerun: yes">  </span>And 
            that, after all, is what matters most.<o:p></o:p></span></p>
            <p>It 
            began in 1985.<span style="mso-spacerun: yes">  </span>We were 
            touring – or to be more precise motoring around in a fairly aimless 
            way – in </span><st1:state><st1:place><span lang="EN-GB">Provence</span></st1:place></st1:state><span lang="EN-GB"> 
            and late one afternoon began looking for somewhere to stay in 
            </span><st1:country-region><st1:place><span lang="EN-GB">Arles</span></st1:place></st1:country-region><span lang="EN-GB">.<span style="mso-spacerun: yes">  </span>The city 
            was disappointing.<span style="mso-spacerun: yes">  
            </span>Perhaps we were too tired to appreciate it, but it failed to 
            minister to the expectations aroused by our <i style="mso-bidi-font-style: normal">Michelin</i> guide.<span style="mso-spacerun: yes">  </span>Driving around the narrow 
            streets in the afternoon rush hour, only to find a <i style="mso-bidi-font-style: normal">complet</i> sign outside a hotel 
            the <i style="mso-bidi-font-style: normal">Michelin</i> seemed to 
            think particularly well of, wasn’t what we’d had in mind. <span style="mso-spacerun: yes"> </span>We left the city’s traffic, 
            its Roman remains and the prospect of a starry, starry night on the 
            café terrace at the Place du Forum behind.</span><o:p></o:p></span></p>
            <p>A few miles outside 
            </span><st1:country-region><st1:place><span lang="EN-GB">Arles</span></st1:place></st1:country-region><span lang="EN-GB"> we came across a “perfect” setting.<span style="mso-spacerun: yes">  </span>A hillside with cypresses 
            and olive trees thrown into relief by the ochre-tiled roofs of the 
            region.<span style="mso-spacerun: yes">  </span>In the distance 
            the craggy rocks of Les Baux.<span style="mso-spacerun: yes">  
            </span>And in the foreground, as if to order, an idyllic retreat: a 
            small hotel (which had escaped listing in the <i style="mso-bidi-font-style: normal">Michelin</i>) with a discreet 
            swimming-pool, surrounded by neo-classical columns and a shady 
            garden.<span style="mso-spacerun: yes">  </span>A little 
            village was nearby.<span style="mso-spacerun: yes">  
            </span>Despite everything, we may still go back and so its anonymity 
            has to be preserved: I’ll simply mention the words “mill”, “Daudet” 
            and “letters”.</span><o:p></o:p></span></p>
            <p>We 
            asked for a room at the hotel desk.<span style="mso-spacerun: yes">  </span>There were south-facing 
            rooms and north-facing rooms, south-facing suites and north-facing 
            suites, all at different rates.<span style="mso-spacerun: yes">  </span>And although there were 
            several cars parked at the front – BMWs, Audis, Mercedes and an Alfa 
            Romeo – there were vacancies in each category.<span style="mso-spacerun: yes">  </span>The receptionist, seemingly 
            won over by the self-parody we had schooled ourselves to inject into 
            our bad French (laugh at yourself first and the world just may laugh 
            with you!), pressed four keys into our hands and insisted that we 
            inspect all the “<i style="mso-bidi-font-style: normal">possibilités</i>”.<span style="mso-spacerun: yes">  </span>The rates were reasonable 
            and each of the “<i style="mso-bidi-font-style: normal">possibilités</i>” was considerably better than 
            anywhere we had stayed during our previous nights in 
            </span><st1:country-region><st1:place><span lang="EN-GB">France</span></st1:place></st1:country-region><span lang="EN-GB">.<span style="mso-spacerun: yes">  </span>But from 
            the moment we saw it, there was no doubt in either of our minds that 
            Number 23, the south-facing suite at the end of the second floor 
            with a balcony overlooking the hillside, the cypresses and the pool, 
            was to be our home for the coming week.<span style="mso-spacerun: yes">  </span>To call it a “suite” was 
            perhaps hyperbole, though there <i style="mso-bidi-font-style: normal">were</i> two distinct levels: 
            Number 23 had a small living area that gave access to the balcony 
            and, up some open-backed stairs, there was a minute bedroom, which 
            Anne immediately dubbed “the ashtray”.<span style="mso-spacerun: yes">  </span>The bed itself had failed to 
            make up its mind whether it was single or double, but seemed 
            suitably cosy for the space available.<span style="mso-spacerun: yes">  </span>All in all, everything was 
            clearly “perfect”.<span style="mso-spacerun: yes">  </span>We 
            went back to the receptionist, old friends by now, and declared our 
            preference.<span style="mso-spacerun: yes">  </span>She smiled, 
            a look of complicity, as if to say that she had known all along that 
            we would make the right choice.</span><o:p></o:p></span></p>
            <p>It 
            was at this point that complications set in.<span style="mso-spacerun: yes">  </span>We asked for Number 23 for a 
            week and, as we did so, a moustachioed man of a certain age appeared 
            from the office behind her.<span style="mso-spacerun: yes">  
            </span>Unaware of his intrusion, the receptionist had begun to 
            insert our name beside the figure “23” for each day of the coming 
            week.<span style="mso-spacerun: yes">  </span>He looked over 
            her shoulder, shook his head wearily at her naivety, scribbled 
            something on a piece of paper and pushed it in front of her.<span style="mso-spacerun: yes">  </span>A single word, scrawled in 
            large capitals: HIMMELSTEIN”.<span style="mso-spacerun: yes">  
            </span>She hesitated and, flustered, began to apologise … and 
            apologise … and apologise.<span style="mso-spacerun: yes">   </span>She shouldn’t have 
            given us the key to Number 23.<span style="mso-spacerun: yes">  
            </span>It was a “<i style="mso-bidi-font-style: normal">possibilité</i>” for one night, 
            but after that we would have to change and perhaps we would not want 
            to move.<span style="mso-spacerun: yes">  </span>The 
            moustachioed man, whom Anne would christen “Terry Thomas”, looked on 
            in silence.<o:p></o:p></span></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; LINE-HEIGHT: 150%"><i style="mso-bidi-font-style: normal"><span lang="EN-GB">We</span></i><span lang="EN-GB"> hesitated.<span style="mso-spacerun: yes">  </span>We were tired and the 
            thought of changing rooms the next day was far from appealing, but 
            the attractions of Number 23 – its tastefully furnished “lounge”, as 
            Anne would come to call it, the bed nestling between imitation-oak 
            beams and the balcony overlooking the pool and the cypresses – won 
            out.<span style="mso-spacerun: yes">  </span>We took it for the 
            night.<span style="mso-spacerun: yes">  </span>Carrying our 
            cases up to Number 23, we hissed “Himmelsteins” at one another and, 
            on arrival, climbed up to the bed and collapsed in a fit of 
            giggles.<span style="mso-spacerun: yes">  </span>And in that 
            innocent era, just before we all became p.c. , we descended into 
            stereotyping.</span><o:p></o:p></span></p>
            <p>“Beaten by the Germans again,” said Anne, “even though <i style="mso-bidi-font-style: normal">we</i> won the war.”<span style="mso-spacerun: yes">  </span><o:p></o:p></span></p>
            <p>“We could always barricade ourselves in and 
            refuse to surrender when they arrive,” I contributed.<o:p></o:p></span></p>
            <p>“Always assuming Himmelstein is a ‘they’ and 
            not a ‘he’, ‘she’ or ‘it’.”<o:p></o:p></span></p>
            <p>“Some kind of force?”<o:p></o:p></span></p>
            <p>“Yes, a sinister subversive 
            organization.”<o:p></o:p></span></p>
            <p>“Or even a code word?”<o:p></o:p></span></p>
            <p>“Yes, a secret weapon to be used <i style="mso-bidi-font-style: normal">contre les anglais</i> without 
            their even knowing it exists,” she embellished.<span style="mso-spacerun: yes">  </span><o:p></o:p></span></p>
            <p>We 
            hissed a few more “Himmelsteins” at one another, took showers and, 
            tacitly agreeing to suspend the subject until the next day, went 
            into the village for dinner.<span style="mso-spacerun: yes">  
            </span><o:p></o:p></span></p>
            <p>The next morning we switched rooms early and 
            went off to </span><st1:city><st1:place><span lang="EN-GB">Avignon</span></st1:place></st1:city><span lang="EN-GB"> 
            for a day that left us concluding that the popes did more for 
            civilization when they invented Chateauneuf than when they built 
            that palace in which they lived for seventy – or was it three 
            hundred and fifty? – years and which we managed to get round in nine 
            minutes.<span style="mso-spacerun: yes">  </span>To be honest, 
            Anne claims it was eight and three-quarters, but my biological 
            stopwatch was sure it was more like nine and a half and after years 
            of retrospective analysis we’ve agreed to differ.<span style="mso-spacerun: yes">  </span></span><o:p></o:p></span></p>
            <p>We 
            didn’t see Himmelstein at the hotel that evening, nor on any of the 
            succeeding days.<span style="mso-spacerun: yes">  </span>From 
            beside the pool we shot glances at the balcony of Number 23, but it 
            was always deserted.<span style="mso-spacerun: yes">  </span>So 
            we remained ignorant of Himmelstein’s identity, even at moments 
            heretically doubting “his” existence and wondering if, God and all 
            his mercenary angels forbid, we might be the victims of a ruse 
            perpetrated by<span style="mso-spacerun: yes">  </span>Terry 
            Thomas.<span style="mso-spacerun: yes">  </span>It became necessary to invent our own Himmelstein.<o:p></o:p></span></p>
            <p>That 
            year and subsequently several possible Himmelsteins stayed in the 
            hotel.<span style="mso-spacerun: yes">  </span>There were two 
            portly middle-aged Germans who dominated the restaurant every 
            evening,<span style="mso-spacerun: yes">  </span>less because 
            of anything they did than because of their sheer bulk and the vast 
            quantities of food and drink that vanished from their table.<span style="mso-spacerun: yes">  </span>There were two spry, 
            amphibious Himmelsteins, who spent most of their mornings jumping in 
            and out of the swimming-pool, towelling one another fondly and then 
            disappearing for the rest of the day.<span style="mso-spacerun: yes">  </span>There was a bespectacled solitary Himmelstein, who read Mann, 
            </span><st1:place><span lang="EN-GB">Hesse</span></st1:place><span lang="EN-GB"> and American 
            science fiction fanzines in the shade of the hotel cypresses, never 
            as far as we could see conversing with anyone.<span style="mso-spacerun: yes">  </span>The main possibility of 
            Himmelstein being a crowd came in the form of coachloads of 
            boisterous, elderly German package tourists, who would stay a single 
            night in the hotel.<span style="mso-spacerun: yes">  
            </span>Weary from their daytime travelling, they habitually insisted 
            on sharing secrets with us over drinks in the bar after dinner.<span style="mso-spacerun: yes">  </span>Most of their confidences 
            consisted of expressing their wish to be “good comrades” with the 
            English.<span style="mso-spacerun: yes">  </span>They seldom 
            mentioned the war, but to us these conversations were like an 
            inverted rerun of that agonising episode of 
            </span><st1:place><st1:placename><i style="mso-bidi-font-style: normal"><span lang="EN-GB">Fawlty</span></i></st1:placename><i style="mso-bidi-font-style: normal"><span lang="EN-GB"> 
            </span></i><st1:placetype><i style="mso-bidi-font-style: normal"><span lang="EN-GB">Towers</span></i></st1:placetype></st1:place><span lang="EN-GB">.<span style="mso-spacerun: yes">  </span>The 
            problems of their generation, not ours.<span style="mso-spacerun: yes">  </span>We would splutter our way to 
            bed, careering up the stairs and along the corridors, imagining 
            ourselves as employees of the Ministry of Silly Walks.<span style="mso-spacerun: yes">  </span>There was only one possible 
            woman Himmelstein.<span style="mso-spacerun: yes">  </span>She 
            travelled alone, cowered the hotel staff into total submission to 
            all her needs, which included “business services”, though we never 
            discovered exactly what these amounted to.<span style="mso-spacerun: yes">  </span>There were also various 
            possible German-Swiss Himmelsteins, identifiable by the CH 
            number-plates on their cars.<span style="mso-spacerun: yes">  
            </span>But each of them was too clean and polite and, to our 
            untutored eyes, insufficiently obsessive to be a Himmelstein.<span style="mso-spacerun: yes">  </span>We could only imagine 
            Himmelstein as a male individualist and gradually surrendered those 
            of our fantasies that had tried to make “him” plural, female or 
            Swiss.</span><o:p></o:p></span></p>
            <p>Before long we came to reject all the putative 
            Himmelsteins we saw in the flesh in favour of a fiction of our own 
            invention.<span style="mso-spacerun: yes">  </span>“Our” 
            Himmelstein was a cultured man, given to not-infrequent bouts of 
            wild eccentricity and occasional ferocity.<span style="mso-spacerun: yes">  </span>He travelled with his wife and, 
            if they had children, they were now adult and left behind in 
            </span><st1:state><st1:place><span lang="EN-GB">Bavaria</span></st1:place></st1:state><span lang="EN-GB">, 
            Baden Baden or </span><st1:state><st1:place><span lang="EN-GB">Bremen</span></st1:place></st1:state><span lang="EN-GB">.<span style="mso-spacerun: yes">  </span>Frau 
            Himmelstein was a redoubtable woman, capable of reducing the 
            haughtiest French waiter to jelly and answerable to no one in the 
            world except her husband.<span style="mso-spacerun: yes">  
            </span>Himmelstein himself, though totally unexceptional in 
            appearance – slightly built, sagging and rosaceous in the face – 
            was, quite simply, a nonpareil: an unfulfilled genius, but a genius 
            all the same.<span style="mso-spacerun: yes">  </span>A man 
            with unerring taste and a capacity for extracting the best from all 
            possible situations.<span style="mso-spacerun: yes">  </span>We 
            decided that he had first come to the hotel about a decade ago, 
            since when he had managed to secure the occupancy of Number 23 for 
            every second week in September by the sheer force of his will.<span style="mso-spacerun: yes">  </span>We speculated that he had 
            paid to keep the hotel out of the <i style="mso-bidi-font-style: normal">Michelin</i> and in a show of 
            loyalty discarded our own copy, throwing it out of the car window on 
            a 1986 drive to Uzès.<span style="mso-spacerun: yes">  
            </span>Our admiration for “him” was unbounded.</span><o:p></o:p></span></p>
            <p>Having been bested by Himmelstein for possession of 
            Number 23 for what we quickly came to agree was the most desirable 
            week of the year in 1985, we resolved to compete with him in future 
            years.<span style="mso-spacerun: yes">  </span>In 1986, we 
            faxed the hotel in April, trying to book Number 23 for the second 
            week in September.<span style="mso-spacerun: yes">  </span>It 
            was already taken.<span style="mso-spacerun: yes">  </span>We 
            went the following week.<span style="mso-spacerun: yes">  
            </span>In 1987 we wrote in March.<span style="mso-spacerun: yes">  </span>Number 23 was again 
            unavailable.<span style="mso-spacerun: yes">  </span>We booked 
            another room for the same week.<span style="mso-spacerun: yes">  </span>As we left that year, we 
            tried to make our booking for the second week of September 1988 
            there and then.<span style="mso-spacerun: yes">  </span>Anne 
            thought she heard the (new) receptionist whisper “Himmelstein”, but 
            we could never be sure of this.<span style="mso-spacerun: yes">  </span>In any event Number 23 was 
            once again unavailable and we booked for the following week.<span style="mso-spacerun: yes">  </span>The pattern seemed set in 
            stone.<o:p></o:p></span></p>
            <p>Following Himmelstein in 1986 revealed a new aspect of 
            his character: his violent temper.<span style="mso-spacerun: yes">  </span>We took a freshly made 
            cigarette-hole in a curtain and a cracked tile on the balcony of 
            Number 23 as evidence that his creative drive had, for once, been 
            frustrated by Frau Himmelstein.<span style="mso-spacerun: yes">  </span>Perhaps their spat had begun 
            when she had refused to go to the select gourmet restaurant in Les 
            Baux (the “tucked-away” one that had escaped the prying eyes of the 
            <i style="mso-bidi-font-style: normal">Michelin</i> inspectors) or 
            when, on the evening before their departure, she had suggested that 
            next year they should try a farmhouse outside Lucca for their 
            September escape.<span style="mso-spacerun: yes">  
            </span>Whatever the cause, we both agreed that Himmelstein must have 
            been sorely provoked to have acted as he did. </span></p>
            <p>In 
            1987, we were, we assumed, at the hotel at the same time.<span style="mso-spacerun: yes">  </span>By this point Himmelstein 
            had become everything we were not.<span style="mso-spacerun: yes">  </span>The flamboyant genius who 
            imbibed vast quantities of the local Mas de Gourgonnier, while 
            writing brilliant, unfinished chapters of his never-to-be-published 
            novels; the demented architect who returned every year to this 
            Provençal landscape as “one of the last habitable spots on earth”; 
            the travel-book writer who had devoted his life to ensuring that 
            reports of places like the hotel and the “tucked-away” restaurant 
            never found their way into print; the dedicated wind-watcher who 
            pooh-poohed the power of Le Mistral and came to Provence each 
            September in the hope of experiencing one of the rarer vintages of 
            the Marin.<span style="mso-spacerun: yes">  </span><o:p></o:p></span></p>
            <p>Gradually he educated us.<span style="mso-spacerun: yes">  </span>He was our necessary 
            fiction, because we were suburban, ordinary. <span style="mso-spacerun: yes"> </span>During 
            those Septembers in </span><st1:state><st1:place><span lang="EN-GB">Provence</span></st1:place></st1:state><span lang="EN-GB"> 
            we also had our “adventures”, but they were of a quite different 
            kind from Himmelstein’s.<span style="mso-spacerun: yes">  
            </span>There was the afternoon when I grew a moustache in three 
            hours from sitting in the hot sun; Anne’s sprained ankle by the 
            swimming pool; my exciting twenty-four hours running between bedroom 
            and bathroom after eating <i style="mso-bidi-font-style: normal">cassoulet d’agneau</i> with 
            “enough baked beans to blow me to Perpignan” and the night we were 
            marooned in Nîmes after the car’s fan belt broke.<span style="mso-spacerun: yes">  </span>Less dramatic, but an 
            equally important part of our personal folklore, was the occasion, 
            when opening a perrier by the cashier in the cafeteria attached to 
            the Arles supermarket, Anne managed to flick its cap into the 
            pristine crème caramel of a woman at a nearby table.<span style="mso-spacerun: yes">  </span>With considerable aplomb and 
            a multiplicity of swiftly muttered “<i style="mso-bidi-font-style: normal">pardons</i>” and “<i style="mso-bidi-font-style: normal">je suis désolés</i>”, Anne 
            ceremoniously removed the bottle-top, but not without leaving a 
            fingerprint that stunned the luckless stony-faced woman whose 
            dessert had been penetrated into silence.<span style="mso-spacerun: yes">  </span>All these incidents 
            furnished us with stories to tell and retell, but, embroider them as 
            we would, they could never hold a candle to Himmelstein’s 
            escapades.<span style="mso-spacerun: yes">  </span></span><o:p></o:p></span></p>
            <p>We 
            imagined Himmelstein terrorizing three Texan tourists, who had 
            stumbled upon the hotel by chance, into leaving the same day; 
            Himmelstein up at the crack of dawn to declaim poetry at Daudet’s 
            mill; and a Pan-like naked Himmelstein chasing a fearful maid, who 
            had dared to put her head around the door of Number 23, while he was 
            taking a shower, along the second-floor corridor.<span style="mso-spacerun: yes">  </span>Each of the hotel’s rooms 
            boasted a “<i style="mso-bidi-font-style: normal">Ne pas 
            déranger</i>” sign which, reversed, read “<i style="mso-bidi-font-style: normal">Veuillez faire la chambre</i>”, 
            “Please make up my room” and “<i style="mso-bidi-font-style: normal">Bitte das Zimmer in Ordung 
            bringen</i>”.<span style="mso-spacerun: yes">  
            </span>Himmelstein was, we were sure, personally responsible for the 
            German translation.<span style="mso-spacerun: yes">  
            </span>Most 
            memorable of all was the occasion when he stormed out of the 
            “tucked-away” restaurant at lunch, because his filet was overcooked, 
            and with a fully supportive Frau Himmelstein in tow drove to 
            </span><st1:city><st1:place><span lang="EN-GB">Genoa</span></st1:place></st1:city><span lang="EN-GB"> for 
            dinner.<span style="mso-spacerun: yes">  </span>They didn’t get 
            back until after the small hours and caused a furore, trying to get 
            Terry Thomas to open up the hotel gates, which were always locked at 
            </span><st1:time minute="0" hour="0"><span lang="EN-GB">midnight</span></st1:time><span lang="EN-GB">.</span><o:p></o:p></span></p>
            <p>In 

            to believe or cannot believe what Dr Rao has told him.<span style="mso-spacerun: yes">&nbsp; </span>Even though the children are 
            much younger than Venkatesh, they have no trouble believing 
            everything that Appa tells them about bacteria and viruses.<span style="mso-spacerun: yes">&nbsp; </span>When Appa speaks, somehow 
            you know that it’s the absolute truth and nothing but the 
            truth.<span style="mso-spacerun: yes">&nbsp; </span>The only 
            sugarcane the children taste is in their grandmother’s house, in her 
            fragrant, clean kitchen.<span style="mso-spacerun: yes">&nbsp; 
            </span>No virus would dare to enter the kitchen belonging to Appa’s 
            mother.<o:p></O:P></p>
            <p>When 
            Appa gets sick, Ma follows him to the hospital and stays with 
            him.<span style="mso-spacerun: yes">&nbsp; </span>Soon Appa’s mother arrives in 
            </SPAN>Poona</SPAN>, 
            summoned by their own mother.<span style="mso-spacerun: yes">&nbsp; 
            </span>She too stays in the hospital with Appa, all day, every 
            day.<span style="mso-spacerun: yes">&nbsp; </span>The children have 
            no memory of that.<span style="mso-spacerun: yes">&nbsp; 
            </span>Maybe because their mother and grandmother leave the house 

          <td colspan="4" rowspan="2" valign="top" class="texto2">  
            
			<p>ii<o:p></O:P></p>
            <p>The 
            children enjoy asking Appa to say ‘mosquito’; he pronounces it 
            ‘moss-quito’, which makes them laugh.<span style="mso-spacerun: yes">&nbsp; </span>Already the children have a 
            different accent when they speak English.<span style="mso-spacerun: yes">&nbsp; </span>But no matter how much 
            English they learn, they will always call their father 
            ‘Appa’.<o:p></O:P></p>
            <p><br>iii<o:p></O:P></p>
            <p>No 
            one can beat Appa at killing flies.<span style="mso-spacerun: yes">&nbsp; </span>Four, five, six flies dead 
            within minutes.<span style="mso-spacerun: yes">&nbsp; </span>Appa 
            washes his hands more often than anyone else in the 
            world.<o:p></O:P></p>
            <p><br>iv<o:p></O:P></p>
            <p>The 
            eldest remembers a time when she thought flies were harmless, 
            friendly creatures.<span style="mso-spacerun: yes">&nbsp; 
            </span>That was when she was four and the youngest was still a 
            baby.<span style="mso-spacerun: yes">&nbsp; </span>She remembers 
            watching a fly sitting beside a sliced tomato in the kitchen, how a 
            few green seeds were floating in the watery juice.<span style="mso-spacerun: yes">&nbsp; </span>She remembers watching the 
            fly rub its fine, filament hands, and then, how it rubbed its face. 
            She watched and watched, and even started to speak with the fly 
            until Appa rushed up to her and scolded her for playing with a 
            fly.<span style="mso-spacerun: yes">&nbsp; </span>‘Flies are 
            dangerous,’ he informed her. ‘Flies are full of diseases, and they 
            will make you very sick,’ he warned.<span style="mso-spacerun: yes">&nbsp; </span>Never again did the eldest 
            look kindly upon a fly.<o:p></O:P></p>
            <p><br>v<o:p></O:P></p>
            <p>When 
            the children fall and run home with bleeding elbows and knees, Appa 
            says, ‘don’t cry.<span style="mso-spacerun: yes">&nbsp; </span>Now, 
            now, stop crying.<span style="mso-spacerun: yes">&nbsp; </span>Your 
            white blood cells will help you.<span style="mso-spacerun: yes">&nbsp; </span>Your white blood cells will 
            start working now.<span style="mso-spacerun: yes">&nbsp; </span>Stop 
            crying.<span style="mso-spacerun: yes">&nbsp; </span>Now go and wash 
            it out with soap.’<span style="mso-spacerun: yes">&nbsp; </span>And 
            that really makes the children quiet.<span style="mso-spacerun: yes">&nbsp; </span>Stunned.<span style="mso-spacerun: yes">&nbsp; </span>They try to imagine their 
            white blood cells as they peer at their bright red 
            knees.<o:p></O:P></p>
            <p><br>vi 
            <o:p></O:P></p>
            <p>The 
            eldest remembers Appa’s anatomy book.<span style="mso-spacerun: yes">&nbsp; </span>How the blood flows one way 
            and then another.<span style="mso-spacerun: yes">&nbsp; </span>Red, 
            blue, red: so the veins are coloured. Appa has explained the 
            function of the heart and the lungs to the children and has told 
            them how the blood must flow unhampered.<span style="mso-spacerun: yes">&nbsp; 
</span>Always.<o:p></O:P></p>
            <p><br>vii<o:p></O:P></p>
            <p>The 
            children learn the names of viruses.<span style="mso-spacerun: yes">&nbsp; </span>There are days when Appa 
            comes home with Dr Banerjee.<span style="mso-spacerun: yes">&nbsp; 
            </span>They have their afternoon tea with biscuits while Appa talks 
            about viruses and bacteria.<span style="mso-spacerun: yes">&nbsp; 
            </span>Dr Banerjee listens; the children also listen.<span style="mso-spacerun: yes">&nbsp; </span>The word, ‘chikungunya’, 
            makes the children laugh; ‘chikungunya’ makes them laugh so much 
            that years later, elderly Appa remembers their laughter when he is 
            told that his sister has caught chikungunya.<span style="mso-spacerun: yes">&nbsp; </span><o:p></O:P></p>
            <p><br>viii<o:p></O:P></p>
            <p>Appa 
            doesn’t know that the children spend their days in the garden 
            foraging for sour things to eat:<span style="mso-spacerun: yes">&nbsp; </span>raw tamarinds, tamarind 
            leaves, gulmohar leaves and gulmohar buds and flowers, and even a 
            strange new clover invading the grass.<span style="mso-spacerun: yes">&nbsp; </span>If Appa knew, he would 
            remind them of bacteria and viruses.<span style="mso-spacerun: yes">&nbsp; </span>If Appa knew, he would get 
            angry and tell them that if they get sick it will be their own 
            fault.<span style="mso-spacerun: yes">&nbsp; </span>It is their 
            decision.<span style="mso-spacerun: yes">&nbsp; </span>This is what 
            makes the children feel grown-up and important and afraid.<span style="mso-spacerun: yes">&nbsp;&nbsp; </span><o:p></O:P></p>
            <p><br>ix<o:p></O:P></p>
            <p>The 
            children have discovered huge mulberry bushes at the edge of an 
            empty, dry and barren field.<span style="mso-spacerun: yes">&nbsp; 
            </span>A dirt-dry field.<span style="mso-spacerun: yes">&nbsp; 
            </span>The mulberry bushes inspire the eldest to sing English 
            nursery rhymes.<span style="mso-spacerun: yes">&nbsp; </span>Every 
            now and then the children inspect the berries to see if they are 
            ripe enough; occasionally they find the berries in a perfect state 
            of dark blackish purple. And then they have a feast; they don’t miss 
            a single berry.<span style="mso-spacerun: yes">&nbsp; </span>The 
            children, however, are basically impatient, and are just as happy to 
            eat the sour, green berries, especially when they are hungry for 
            sour things.<o:p></O:P></p>
            <p><br>x<o:p></O:P></p>
            <p>Appa 
            says the children must have eggs and vitamin C.<span style="mso-spacerun: yes">&nbsp; </span>The first time Appa shows 
            them the small plastic bottle containing vitamin C tablets, the 
            children are awed.<span style="mso-spacerun: yes">&nbsp; </span>They 
            are amazed. Appa opens the bottle carefully; and then, solemnly and 
            ceremoniously, he gives one tablet to each of the children. They love 
            the sour taste of the tablets.<span style="mso-spacerun: yes">&nbsp; 
            </span>It is a sour taste which is different from all the other sour 
            tastes they know.<span style="mso-spacerun: yes">&nbsp; </span>The 
            tablets, of course, have no trace of leaves or grass or flowers or 
            fruits.<span style="mso-spacerun: yes">&nbsp; </span>The tablets 
            don’t remind them of the wind ruffling their hair.<span style="mso-spacerun: yes">&nbsp; </span>The tablets have a pure 
            sourness all their own, like a sound that exists somewhere but 
            cannot be heard by anyone.<span style="mso-spacerun: yes">&nbsp; 
            </span>Not even by dogs.<span style="mso-spacerun: yes">&nbsp; 
            </span>The first day, the children finish half the bottle.<span style="mso-spacerun: yes">&nbsp; </span>The next day, when Appa 
            finds out, he is worried and anxious and he examines the children’s 
            faces.<span style="mso-spacerun: yes">&nbsp; </span>But nothing is 
            amiss.<span style="mso-spacerun: yes">&nbsp; </span>The children 
            look completely like themselves.<span style="mso-spacerun: yes">&nbsp; </span>And then, Appa says they can 
            only have one tablet a day.<span style="mso-spacerun: yes">&nbsp; 
            </span>Maximum.<span style="mso-spacerun: yes">&nbsp; </span>This 
            time, the children listen.<span style="mso-spacerun: yes">&nbsp; 
            </span>Even they feel that the vitamin C tablets are more powerful 
            than tamarinds and gulmohar leaves; and even stronger than 
            lemons.<span style="mso-spacerun: yes">&nbsp; </span>The tablets 
            remind them of medicine, and medicine is always dangerous, they 
            agree.<span style="mso-spacerun: yes">&nbsp; </span>Nonetheless, it’s not as bad as 
            the time when the youngest, when still a baby, had swallowed 
            mothballs, and when the eldest had accidentally swallowed a metal 
            star.<o:p></O:P></p>
            <p><br>xi<o:p></O:P></p>
            <p>The 
            children have a long history with eggs.<span style="mso-spacerun: yes">&nbsp;&nbsp; </span>The children know that 
            when they were babies, their mother fed them soft boiled eggs; 
            sometimes she mixed the egg yolks with fresh mango juice.<span style="mso-spacerun: yes">&nbsp; </span>The children can’t imagine 
            eating such food, but they loved it, their mother reassures them. 
            Then they progressed to hard boiled eggs and then suddenly it was 
            scrambled eggs.<span style="mso-spacerun: yes">&nbsp; 
            </span>Scrambled eggs with ketchup.<span style="mso-spacerun: yes">&nbsp; </span>None of their relatives, 
            none of their cousins or uncles or aunts or grandparents have ever 
            eaten eggs, let alone eggs with ketchup.<span style="mso-spacerun: yes">&nbsp; </span>One summer, Appa’s mother, 
            who is a pure vegetarian, and very strict when it comes to obeying 
            rules, allows them to cook eggs in her kitchen.<span style="mso-spacerun: yes">&nbsp; </span>Somehow Appa must have 
            persuaded her that eggs are important.<span style="mso-spacerun: yes">&nbsp; </span>The children, however, feel 
            neutral towards eggs.<span style="mso-spacerun: yes">&nbsp; 
            </span><o:p></O:P></p>
            <p><br>xii<o:p></O:P></p>
            <p>Appa 
            has told the children about rabies.<span style="mso-spacerun: yes">&nbsp; </span>He has told them to stay 
            away from monkeys.<span style="mso-spacerun: yes">&nbsp; </span>He 
            has warned them many times.<span style="mso-spacerun: yes">&nbsp; 
            </span>But no one has told the monkeys to stay away from the 
            children.<span style="mso-spacerun: yes">&nbsp; </span>If Appa knew 
            that sometimes the children eat gulmohar leaves thrown down to them 
            by monkeys in the trees, then he might even faint, the children 
            believe, and afterwards he would give them more 
            injections.<o:p></O:P></p>
            <p><br>xiii<o:p></O:P></p>
            <p>The 
            children would like to be friends with the monkeys, but the monkeys 
            have different ideas.<span style="mso-spacerun: yes">&nbsp; 
            </span>The problem is that the monkeys do not really care for the 
            children.<span style="mso-spacerun: yes">&nbsp; </span>The monkeys 
            like to tease the children, and the children like to tease the 
            monkeys.<span style="mso-spacerun: yes">&nbsp; </span>It’s an 
            endless game.<span style="mso-spacerun: yes">&nbsp; </span>The 
            children think the monkeys throw gulmohar leaves and flowers to them 
            out of kindness, but the monkeys don’t see it that way.<o:p></O:P></p>
            <p><br>xiv<o:p></O:P></p>
            <p>Dr 
            Banerjee is the best at giving injections.<span style="mso-spacerun: yes">&nbsp; </span>Even Appa knows that, and so 
            he says to the children, ‘how lucky for you that Dr Banerjee has 
            come with me to give you vaccinations!’<span style="mso-spacerun: yes">&nbsp; </span>The children know the city 
            is oozing with cholera.<span style="mso-spacerun: yes">&nbsp; 
            </span>It feels like a huge storm; it feels as if the clouds are 
            full of cholera, as if a tidal wave will crash over their garden any 
            moment now, and bring even more cholera.<o:p></O:P></p>
            <p><br>xv<o:p></O:P></p>
            <p>Dr 
            Banerjee is very tall.<span style="mso-spacerun: yes">&nbsp; 
            </span>His hands are large and soft and very gentle.<span style="mso-spacerun: yes">&nbsp; </span>When he gives you an 
            injection you don’t even feel it.<span style="mso-spacerun: yes">&nbsp; </span>Not even a butterfly can be 
            so gentle.<span style="mso-spacerun: yes">&nbsp; </span>Appa is firm 
            and competent, but when he gives you an injection you can feel your 
            veins and your bones trembling.<span style="mso-spacerun: yes">&nbsp; </span>Trembling and 
            crying.<o:p></O:P></p>
            <p><br>xvi<o:p></O:P></p>
            <p>The 
            worst vaccinations the children receive are the smallpox 
            vaccinations in school, brutally and literally screwed into the 
            insides of their forearms.<span style="mso-spacerun: yes">&nbsp; 
            </span>The eldest, being left-handed, makes sure she receives the 
            vaccination on her right arm.<span style="mso-spacerun: yes">&nbsp; 
            </span>Afterwards, for days she cradles her right arm as if it were 
            a sick child in need of constant attention.<span style="mso-spacerun: yes">&nbsp; </span>She peers at the blood 
            encrusted vaccination marks which look like two little wheels; 
            small, dark Ashoka Chakras.<span style="mso-spacerun: yes">&nbsp; 
            </span>Sometimes creamy yellowish white froth seeps out, dead white 
            blood cells.<span style="mso-spacerun: yes">&nbsp; </span>Sometimes 
            the skin within the Chakra puckers up as if against some mysterious 
            cold air that she herself cannot feel.<span style="mso-spacerun: yes">&nbsp; </span>Her arm feels like it is no 
            longer hers.<span style="mso-spacerun: yes">&nbsp; </span>Her arm feels like a piece of wood lying beside a stream in a 
            huge forest; no longer hers, but reclaimed by the earth.</p>
            <p>For 
            days the vaccinated arm is kept dry; unwashed until it is clear that 
            the cut circles of skin are beginning to heal.<span style="mso-spacerun: yes">&nbsp; </span>It’s a miracle that the 
            children are all able to resist the urge to scratch their 
            vaccination marks.<span style="mso-spacerun: yes">&nbsp; </span>And 
            it’s a miracle that their inflamed, irritated and scabbed skin 
            always heals smoothly, despite repeatedly being branded with the 
            same smallpox vaccination in the same spot.<span style="mso-spacerun: yes">&nbsp; </span>Years later, there’s not 
            even a hint of a scar.<span style="mso-spacerun: yes">&nbsp; 
            </span><o:p></O:P></p>
            <p>Appa 
            makes a note to himself that the children have received their 
            smallpox vaccinations in school.<span style="mso-spacerun: yes">&nbsp; </span>He seems to nod to himself, 
            satisfied that the school is responsible, the government is 
            working.<span style="mso-spacerun: yes">&nbsp; </span>He praises the 
            children for not scratching their vaccination marks and for looking 
            after their arms so nicely.<o:p></O:P></p>
            <p><br>xvii<o:p></O:P></p>
            <p>Sometimes 
            the children complain about their food.<span style="mso-spacerun: yes">&nbsp; </span>This dal tastes like ashes, 
            they say.<span style="mso-spacerun: yes">&nbsp; </span>And the 
            milk!<span style="mso-spacerun: yes">&nbsp; </span>The boiled 
            buffalo milk they have to drink three times a day smells of old 
            balloons.<span style="mso-spacerun: yes">&nbsp; </span>This milk 
            tastes like rotten, old balloons, they say.<o:p></O:P></p>
            <p>How 
            do they know, their mother asks, have they ever eaten ashes and 
            balloons?<o:p></O:P></p>
            <p>But 
            the children persist; we just know, they say, and gulp down their 
            hot milk as fast as they can, trying to smell and taste as little as 
            possible.<o:p></O:P></p>
            <p><br>xviii<o:p></O:P></p>
            <p>One day Appa goes to 
            </SPAN>Kutch</SPAN>.<span style="mso-spacerun: yes">&nbsp; </span>It will be a field trip, 
            Appa tells the children beforehand.<span style="mso-spacerun: yes">&nbsp; </span>The children don’t really 
            know what a field trip is.<span style="mso-spacerun: yes">&nbsp; 
            </span>They can’t imagine Appa wandering through fields and gardens 
            in the same manner that they do.<span style="mso-spacerun: yes">&nbsp; </span>They think he might be 
            hunting down mosquitoes and viruses, and bacteria, of course.<span style="mso-spacerun: yes">&nbsp; </span>As Appa prepares for his trip he 
            asks the children whether they would like anything special, anything 
            in particular from </SPAN>Kutch</SPAN>.<span style="mso-spacerun: yes">&nbsp; </span>The eldest is uncertain; she 
            has just finished reading <i style="mso-bidi-font-style: normal">Beauty and the Beast</i>, and is 
            under a curious spell of magic herself.<span style="mso-spacerun: yes">&nbsp; </span>The eldest tends to live in 
            another world.<span style="mso-spacerun: yes">&nbsp; </span>Still, 
            she decides to be pragmatic and asks for a doll; which would be 
            better than an enchanted flower, she’s certain.<span style="mso-spacerun: yes">&nbsp; </span>Besides, she loves dolls and 
            everyone expects her to wish for a doll.<span style="mso-spacerun: yes">&nbsp; </span>What she secretly wishes for is a gigantic, thick book of 
            stories, preferably fairy tales from all over the world, but she 
            fears that such a book would be very expensive and surely not to be 
            found in </SPAN>Kutch</SPAN>; such a book could only exist in 
            </SPAN>Poona</SPAN> or </SPAN>Bombay</SPAN>.<o:p></O:P></p>
            <p>The 
            youngest, for once, is not uncertain.<span style="mso-spacerun: yes">&nbsp; </span>He knows exactly what he 
            wants.<span style="mso-spacerun: yes">&nbsp; </span>‘I want a 
            sword,’ he says without any hesitation.<span style="mso-spacerun: yes">&nbsp; </span>‘A real sword!<span style="mso-spacerun: yes">&nbsp; </span>Bring me a sword.’<span style="mso-spacerun: yes">&nbsp; </span>And Appa, to everyone’s 
            surprise, nods and agrees.<o:p></O:P></p>
            <p>Before 
            he leaves, Appa summons the children, and in his solemn, ceremonious 
            way, he embraces them and says good-bye.<span style="mso-spacerun: yes">&nbsp; </span>The children are always 
            saddened by such occasions.<span style="mso-spacerun: yes">&nbsp; 
            </span>They do not like to see Appa leave.<span style="mso-spacerun: yes">&nbsp; </span>This time they fear that he 
            will be gone for a long time, and they’re right, Appa is gone for 
            almost a month.<span style="mso-spacerun: yes">&nbsp; 
            </span><o:p></O:P></p>
            <p>Then 
            there is a week when their mother has no word from Appa.<span style="mso-spacerun: yes">&nbsp; </span>Everyone is concerned; and 
            the youngest, in order to provide a logical explanation for this 
            lack of communication on Appa’s part, imagines that Appa has fallen 
            into quicksand.<span style="mso-spacerun: yes">&nbsp; </span>With 
            great excitement and drama, the youngest enacts Appa’s struggle with 
            quicksand.<span style="mso-spacerun: yes">&nbsp; </span>This only 
            serves to make their mother feel more frightened and worried.<span style="mso-spacerun: yes">&nbsp; </span>But the eldest finds the 
            quicksand story funny.<span style="mso-spacerun: yes">&nbsp; 
            </span>The youngest is truly a clown.<span style="mso-spacerun: yes">&nbsp; </span>The eldest has great faith 
            in Appa; she believes that he is invincible and that nothing will 
            ever happen to him.<span style="mso-spacerun: yes">&nbsp; </span>She 
            knows that Appa is simply too busy.<o:p></O:P></p>
            <p><br>xix<o:p></O:P></p>
            <p>When Appa returns home with a sword from 
            </SPAN>Kutch,</SPAN> 
            the youngest is overwhelmed.<span style="mso-spacerun: yes">&nbsp; 
            </span>But now he doesn’t know what he can do with a sword without 
            getting into trouble.<o:p></O:P></p>
            <p><br>xx<o:p></O:P></p>
            <p>Dr Work from </SPAN>Alabama</SPAN> has come 
            to </SPAN>Poona</SPAN>; 
            he keeps a leopard in his house.<span style="mso-spacerun: yes">&nbsp; </span>These 
            are the days when ‘Pune’ is still ‘</SPAN>Poona</SPAN>’.<span style="mso-spacerun: yes">&nbsp; </span>The leopard is tame but 
            nonetheless wild. <span style="mso-spacerun: yes">&nbsp;</span>Mrs 
            Work rescued it after hunters killed its mother; the leopard was a 
            baby then, but now it’s grown-up.<span style="mso-spacerun: yes">&nbsp; </span>The children don’t believe 
            the leopard is tame.<span style="mso-spacerun: yes">&nbsp; </span>‘I 
            tell you it has a leopard’s heart and a leopard’s mind; it will 
            never be tame’; the eldest tells the second eldest.<span style="mso-spacerun: yes">&nbsp; </span>The younger ones are quiet. 
            They’re supposed to finish their dinner, but they can’t.<span style="mso-spacerun: yes">&nbsp; </span>They simply stare at the 
            leopard pacing in front of them.<span style="mso-spacerun: yes">&nbsp; </span>Everyone’s parents are 
            eating in another room, a room without a leopard.<span style="mso-spacerun: yes">&nbsp; </span>Appa is talking about 
            viruses with Dr Work again.<span style="mso-spacerun: yes">&nbsp; 
            </span>The mothers don’t want to hear about diseases while they’re 
            eating.<span style="mso-spacerun: yes">&nbsp; </span>They want to 
            talk about flowers; let’s discuss something pleasant, they say.<span style="mso-spacerun: yes">&nbsp; </span>The leopard never 
            tires.<span style="mso-spacerun: yes">&nbsp; </span>‘It needs lots 
            of space’, the eldest says.<span style="mso-spacerun: yes">&nbsp; 
            </span>The second eldest agrees.<span style="mso-spacerun: yes">&nbsp; </span>‘Leopards don’t like to sit 
            or lie down.<span style="mso-spacerun: yes">&nbsp; </span>They’re 
            not like dogs, you know.’ <o:p></O:P></p>
            <p>Appa 
            is not afraid of the leopard; he’s not worried that the children are 
            eating dinner with a leopard. Dr Work’s leopard is certainly clean, 
            free of viruses and bacteria.<o:p></O:P></p>
            <p><br>xxi<span style="mso-spacerun: yes">&nbsp;&nbsp;&nbsp;&nbsp; 
            </span><o:p></O:P></p>
            <p>One 
            day, even Appa gets sick, and has to be taken to hospital.<span style="mso-spacerun: yes">&nbsp; </span>The children cannot 
            understand how Appa could get sick. They focus instead on the piles 

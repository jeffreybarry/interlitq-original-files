            home late at night when the children are fast 
            asleep.<o:p></O:P></p>
            <p><br>xxii<o:p></O:P></p>
            <p>What 
            the children remember is that their mother’s youngest brother, 
            Bha’mama, comes to stay with them.<span style="mso-spacerun: yes">&nbsp; </span>Bha’mama is the greatest 
            storyteller that the children know.<span style="mso-spacerun: yes">&nbsp; </span>The eldest is always trying 
            to imitate him.<span style="mso-spacerun: yes">&nbsp; 
            </span>Bha’mama is also a singer and a musician and he knows the 
            best jokes.<span style="mso-spacerun: yes">&nbsp; </span>He makes up 
            new jokes all the time, just like that.<span style="mso-spacerun: yes">&nbsp; </span>To the uninitiated, his 
            jokes can be very strange.<span style="mso-spacerun: yes">&nbsp; 
            </span>In the evening, Bha’mama sings and sings, and the children 
            listen.<span style="mso-spacerun: yes">&nbsp; </span>As they listen, 
            the children drift and fall, slide and sink into his song; they 
            begin to live inside his singing.<span style="mso-spacerun: yes">&nbsp; </span>Sometimes they imagine that 
            they are lost birds and Bha’mama has come to rescue 
            them.<o:p></O:P></p>
            <p>xxiii<o:p></O:P></p>
            <p>When 
            the children play house, the eldest always plays Bha’mama and Appa 
            and doctor; the second eldest is Mina mami, Ma and nurse, and the 
            youngest must always be the baby, simply the baby who tends to fall 
            ill every day and must be rushed off to the doctor.<span style="mso-spacerun: yes">&nbsp; </span>‘Medicine is crucial,’ the 
            eldest says, ‘we have to find medicine for the baby, our baby 

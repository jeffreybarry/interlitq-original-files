            that the medicine must be made, prepared with great care and 
            precision.<span style="mso-spacerun: yes">&nbsp;&nbsp; 
            </span>Luckily, there is a neem tree in their garden, a neem tree 
            with bitter, medicinal leaves.<span style="mso-spacerun: yes">&nbsp; 
            </span>The eldest and the second eldest gather the freshest leaves 
            they can find, crush them and steep them in water and then persuade 
            the youngest to drink this tonic.<span style="mso-spacerun: yes">&nbsp; </span>The youngest is a good 
            sport, but one day, the medicine is far too bitter, and he has to 
            gulp down far more than he can handle.<span style="mso-spacerun: yes">&nbsp; </span>But after that day, the 
            eldest and the second eldest agree that baby Kalyan is cured and 
            will never need such medicine again.<o:p></O:P></p>
            <p><br>xxiv<o:p></O:P></p>
            <p>Sometimes heat and fatigue and boredom make 
            the children fight. And then, the eldest would say to the second 
            eldest, ‘don’t follow me around like a tiger’s tail.’ ‘So 
            that makes you a tiger?’<span style="mso-spacerun: yes">&nbsp; 
            </span>The second eldest asks.<span style="mso-spacerun: yes">&nbsp; 
            </span>The 
            younger ones don’t care.<span style="mso-spacerun: yes">&nbsp; 
            </span>They don’t mind being called ‘tiger’s tail’.<span style="mso-spacerun: yes">&nbsp; </span>They 
            know who they really are.<span style="mso-spacerun: yes">&nbsp; 
            </span>Besides, they like cats.<o:p></O:P></p>
            <p><br>xxv<o:p></O:P></p>
            <p>There 
            is something that makes the children feel like birds. And that is 
            bolkeri.<span style="mso-spacerun: yes">&nbsp; </span>First the 
            morning, and then the whole day and then the entire holiday season 
            is changed by the taste of bolkeri.<span style="mso-spacerun: yes">&nbsp; </span>Salt and turmeric mixed with 
            the sourness of tough green mangoes on their way to picklehood. 
            Bolkeri emerging after two days or three days, bolkeri taken out of 
            the darkness of earthenware jars, bolkeri on their way to be dried, 
            purified by heat and sun.<span style="mso-spacerun: yes">&nbsp; 
            </span>The children are each allowed a piece of bolkeri.<span style="mso-spacerun: yes">&nbsp; </span>No more.<span style="mso-spacerun: yes">&nbsp; </span>It makes them feel like 
            birds.<span style="mso-spacerun: yes">&nbsp; </span>It makes them 
            feel fulfilled but oddly wanting something more, something 
            else.<span style="mso-spacerun: yes">&nbsp; </span>Chewing on the 
            bolkeri, they think of the Monkey Princess, whose mother, a real 
            human Queen, craves such raw mangoes.<span style="mso-spacerun: yes">&nbsp; </span>The 
            story of the Monkey Princess reminds </SPAN>them 
            of other stories.<span style="mso-spacerun: yes">&nbsp; </span>And 
            suddenly, ‘we want to listen to a story’, the younger ones say to 
            the older ones, ‘a new story.’<o:p></O:P></p>
            <p><br>xxvi<o:p></O:P></p>
            <p>There 
            is only one type of ice cream that Appa approves of, and that is 
            rose ice cream.<span style="mso-spacerun: yes">&nbsp; </span>Not 
            made from roses, of course, but milk.<span style="mso-spacerun: yes">&nbsp; </span>Not just any milk, but cows’ 
            milk prepared by Appa’s mother.<span style="mso-spacerun: yes">&nbsp; </span>But why not from roses 
            also?<span style="mso-spacerun: yes">&nbsp; </span>The eldest says, 
            ‘of course there are roses in rose ice cream.<span style="mso-spacerun: yes">&nbsp; </span>After all, we eat roses all 
            the time; all the time when we want a snack and there’s nothing else 
            in the garden or in the house.’<o:p></O:P></p>
            <p>After 
            bolkeri, rose ice cream changes the holidays.<span style="mso-spacerun: yes">&nbsp; </span>Every year there is one day 
            when their grandmother boils and then cools litres of milk, the best 
            cows’ milk. It takes hours to prepare.<span style="mso-spacerun: yes">&nbsp; </span>Then, this milk is taken to 
            the dairy, and the next day barrels of ice cream are delivered.<span style="mso-spacerun: yes">&nbsp; </span>Rose ice cream.<span style="mso-spacerun: yes">&nbsp; </span>And in the evening everyone 
            eats bowls and bowls of ice cream.<span style="mso-spacerun: yes">&nbsp; </span>Nothing else.<span style="mso-spacerun: yes">&nbsp; </span>No dal or rice or 
            chapattis.<span style="mso-spacerun: yes">&nbsp; </span>No 
            vegetables.<span style="mso-spacerun: yes">&nbsp; </span>Only rose 
            ice cream. Rose ice cream rules the night.<span style="mso-spacerun: yes">&nbsp; </span><o:p></O:P></p>
            <p>The 
            children can stay up very late and eat as much ice cream as they 
            want.<o:p></O:P></p>
            <p>It’s the 
            only ice cream Appa approves of.<span style="mso-spacerun: yes">&nbsp; </span>It is pure, he tells them, 
            free of viruses and bacteria.<o:p></O:P></p>
            <p><br>xxvii<o:p></O:P></p>
            <p>Kabar 
            is the blackbird with a yellow beak.<span style="mso-spacerun: yes">&nbsp; </span>Deep dark brown, those kabar 
            feathers; a rich luxurious brown, coffee and chocolate and velvet, 
            and a shimmer of silk. A crow is blacker, a crow is truly 
            blue-black.<span style="mso-spacerun: yes">&nbsp; </span>Mornings 
            the garden is scoured by hundreds of yellow beaks.<span style="mso-spacerun: yes">&nbsp; </span>The sun merges into those 
            dark brown beings.<span style="mso-spacerun: yes">&nbsp; 
            </span>Suddenly the birds are there, and suddenly 
            gone.<o:p></O:P></p>
            <p><br>xxviii<o:p></O:P></p>
            <p>Rita 
            has the longest, thinnest face that the eldest has ever seen.<span style="mso-spacerun: yes">&nbsp; </span>Rita wears sturdy spectacles 
            and looks like a thin, old grandmother even though she’s only 
            ten.<span style="mso-spacerun: yes">&nbsp; </span>Rita always spots 
            the lone blackbird as if it were her duty, her destiny.<span style="mso-spacerun: yes">&nbsp; </span>‘One for sorrow,’ she 

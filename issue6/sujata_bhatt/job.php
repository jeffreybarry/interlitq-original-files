            sad face as he comes out to fetch a coconut.<span style="mso-spacerun: yes">&nbsp; </span>‘Venkatesh can only drink 
            coconut milk’, Dr Rao tells the children.<span style="mso-spacerun: yes">&nbsp; </span>Appa is gone for ten days 
            but to the children it feels like ten years.<span style="mso-spacerun: yes">&nbsp; </span>The children think Appa must 
            be getting horrible, bitter medicine, and they feel sorry for 
            him.<span style="mso-spacerun: yes">&nbsp; </span>They wonder 

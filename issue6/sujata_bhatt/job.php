            them, as if the lariwallah has added a shot of viruses to the juice, 
            and as if Appa has witnessed him doing that.<span style="mso-spacerun: yes">&nbsp; </span>Clearly, Venkatesh is not 
            afraid of viruses and bacteria.<span style="mso-spacerun: yes">&nbsp; </span>Dr Rao certainly must have 

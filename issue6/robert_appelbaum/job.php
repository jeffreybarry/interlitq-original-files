          <td colspan="4" rowspan="2" valign="top" class="texto2">  
            <br>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; TEXT-ALIGN: center; mso-pagination: widow-orphan" align="center"><span lang="EN-GB"><o:p></O:P></span></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; mso-pagination: widow-orphan"><span lang="EN-GB"><span style="mso-tab-count: 1">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
            </span>What’s all the fuss about?<span style="mso-spacerun: yes">&nbsp; </span>That was my question. To 
            listen to Londoners, in the past twenty years the city had become a 
            world capital of cuisine, its best restaurants on par with the best 
            anywhere, its ethnic restaurants the envy of Europe. Encouraged by 
            the flourishing of farmer’s markets, a new ethic of organic and 
            locally sourced produce and animal products, and an infrastructure 
            of food obsession, featured daily on television, the print media, 
            and the Internet, its food culture was said to be vibrant, 
            innovative, even world-shaking.<span style="mso-spacerun: yes">&nbsp; </span>The <i>Guide Michelin</i> 
            for restaurants has for the first time devoted a whole volume to it, 
            making it only the fourth city to be so documented, after Paris, New 
            York, and San Francisco, and causing not a few outbursts of superbia 
            among the locals.<A title="" style="mso-footnote-id: ftn1" href="#_ftn1" name=_ftnref1><sup>1</sup></A> Indeed, I have heard 
            well travelled Londoners say they prefer their home town to Paris, 
            which is too much of a museum to their taste.<span style="mso-spacerun: yes">&nbsp; </span>There are no new ideas in 
            </span><st1:city><st1:place><span lang="EN-GB">Paris</span></ST1:PLACE></ST1:CITY><span lang="EN-GB">, the 
            feeling goes, nothing but the tried and true – steak frites and 
            moules frites and, if you’re lucky, a very old cassoulet.<span style="mso-spacerun: yes">&nbsp; </span>In 
            </span><st1:city><st1:place><span lang="EN-GB">London</span></ST1:PLACE></ST1:CITY><span lang="EN-GB">, by 
            contrast, just about anywhere you go you are going to be served the 
            next new thing – and very likely by a Frenchman, who has fled his 
            homeland for more fertile kitchens.</span></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; mso-pagination: widow-orphan"><span lang="EN-GB"><span style="mso-tab-count: 1">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
            </span>I was sceptical.<span style="mso-spacerun: yes">&nbsp; 
            </span>I had had some excellent meals in 
            </span><st1:city><st1:place><span lang="EN-GB">London</span></ST1:PLACE></ST1:CITY><span lang="EN-GB">, 
            but I had had some mediocre and downright awful meals there as 
            well.<span style="mso-spacerun: yes">&nbsp; </span>I always had a 
            good time visiting the metropolis, and being food obsessed myself, I 
            could think of no better way to <i>ouvrir l’appétit</i> on a cool 
            dark evening than to wander the back streets of the West End, the 
            restaurant capital of the capital, in search of the perfect menu and 
            the empty table.<span style="mso-spacerun: yes">&nbsp; </span>But I 
            didn’t believe the hype.<span style="mso-spacerun: yes">&nbsp; 
            </span>I was suspicious of the chauvinism.<span style="mso-spacerun: yes">&nbsp; </span>And in any case, being 
            something of a social scientist, I needed to see for 
            myself.</span></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; mso-pagination: widow-orphan"><span lang="EN-GB"><span style="mso-tab-count: 1">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
            </span>        Being something 
            of a social scientist, too, I needed to engage in seeing for 
            myself from an uncommon perspective.<span style="mso-spacerun: yes">&nbsp; </span>I needed to check out the 
            scene not as a naive consumer, or a food critic giving advice to 
            consumers, but as an informed participant observer.<span style="mso-spacerun: yes">&nbsp; </span>I needed to do a field study 
            in other words, and observe, with a plausible degree of 
            systematicity and critical self-consciousness, as both an insider 
            and an outsider, the customs of the natives.<span style="mso-spacerun: yes">&nbsp; </span>The fuss about 
            </span><st1:city><st1:place><span lang="EN-GB">London</span></ST1:PLACE></ST1:CITY><span lang="EN-GB"> 
            food, for or against, accurate or phony, is itself an aspect of a 
            larger phenomenon: the buying and selling, the providing, the 
            eating, the digesting, the reporting, the assessing, and the 
            circulation of information about food in 
            </span><st1:city><st1:place><span lang="EN-GB">London</span></ST1:PLACE></ST1:CITY><span lang="EN-GB">.<span style="mso-spacerun: yes">&nbsp; </span>These 
            things are all parts of a single cultural field, and it is to the 
            cultural field that the social scientist will likely turn his or her 
            attention. <span style="mso-spacerun: yes">&nbsp;</span>How, really, 
            is food culturally organized in and about 
            </span><st1:city><st1:place><span lang="EN-GB">London</span></ST1:PLACE></ST1:CITY><span lang="EN-GB">?<span style="mso-spacerun: yes">&nbsp; </span>How was it 
            organized, that is – for this, in my view, is what ‘the cultural’ 
            adds up to – phenomenologically, which is also to say, given the 
            constitution of the phenomenal, symbolically, structurally, 
            materially, and economically?<span style="mso-spacerun: yes">&nbsp; 
            </span>That is the main question.<span style="mso-spacerun: yes">&nbsp; </span>And at bottom it involves 
            coming to terms with the <i>situation</i> of food in that postmodern 
            world of which </span><st1:city><st1:place><span lang="EN-GB">London</span></ST1:PLACE></ST1:CITY><span lang="EN-GB"> is 
            one of the main social and economic centres.<span style="mso-spacerun: yes">&nbsp; </span>Where and how is food 
            <i>located</i> in a city like </span><st1:city><st1:place><span lang="EN-GB">London</span></ST1:PLACE></ST1:CITY><span lang="EN-GB">?<span style="mso-spacerun: yes">&nbsp;&nbsp; </span>What 
            material and symbolic values are assigned to it?<span style="mso-spacerun: yes">&nbsp; </span>How are these values 
            asserted, negotiated, and experienced?<span style="mso-spacerun: yes">&nbsp; </span>  And what then is all 
            the fuss about?</span></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; mso-pagination: widow-orphan"><span lang="EN-GB"><span style="mso-tab-count: 1">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
            </span>So there are the research questions, large and small.<span style="mso-spacerun: yes">&nbsp; </span>Unfortunately, I only had 
            five days in which to perform an initial study of the subject, and, 
            being a social scientist, I didn’t have a lot of money to do it with 
            either.<span style="mso-spacerun: yes">&nbsp;&nbsp; </span>But that 
            could prove to be a boon.<span style="mso-spacerun: yes">&nbsp; 
            </span>Under the conditions of scarcity imposed on me by my budget, 
            I could observe myself observing the cuisine of the city in 
            circumstances as most food critics, travel writers, and especially 
            consumers really do experience it – hit or miss, with limited time 
            and resources at their disposal.<span style="mso-spacerun: yes">&nbsp; </span>I could study it from a 
            vantage point of what Bernard Lahire (2003) calls the ‘sociology of 
            the individual’, or what followers of Michel de Certeau (1998) call 
            the ‘sociology of doing’.<span style="mso-spacerun: yes">&nbsp; 
            </span>At least playfully, in the spirit of the Menippean Satire as 
            well as in the framework of modern sociology and cultural studies, I 
            could look at the world of food consumption from an emphatically 
            limited, contingent, under-resourced vantage point, and therefore 
            see it as only an individual can see it – for that is what 
            individuals are, limited, contingent and under-resourced, required 
            to pursue individual ends within the terms of a system that is not 
            of their own making.</span></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; mso-pagination: widow-orphan"><span lang="EN-GB"><span style="mso-tab-count: 1">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
            </span>Whether I could observe without evaluating was another 
            question.<span style="mso-spacerun: yes">&nbsp; </span>   
                It is one thing to observe the 
            customs of the natives, however self-aware one is about the 
            limitations of observation: it was another to eat their food; and it was 
            still another to eat their food with or without registering value 
            judgements about it.<span style="mso-spacerun: yes">&nbsp; </span>In 
            the end, I found that value judgements were inescapable, in large 
            part because no one like me can sample a cuisine, whatever the 
            guidelines of a ‘science’, without also assessing it.<span style="mso-spacerun: yes">&nbsp; </span>To taste is also to exercise 
            taste, to make discriminations among sensations and processes with a 
            view toward pleasure.<span style="mso-spacerun: yes">&nbsp; 
            </span>There can be no fully neutral science of food.</span></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; mso-pagination: widow-orphan"><span lang="EN-GB"><span style="mso-tab-count: 1">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
            </span>I have seen plenty of studies from social scientists that 
            seem to presume otherwise, to be sure, imagining to extricate 
            themselves and their readers from the subjectivity they are 
            studying, and to assert a disinterested sociology of the group, but 
            the results are unconvincing.<span style="mso-spacerun: yes">&nbsp; 
            </span>That may be said even about Pierre Bourdieu and his 
            <i>Distinction </i>(1984), a work that is commonly cited as if it 
            were the final word on the matter, held up as definitive proof that 
            the subjective experience of taste is really an objectively 
            determined performance of class – and this in spite of several 
            inconvenient facts that undermine that assessment.<span style="mso-spacerun: yes">&nbsp; </span>For one thing, at the time 
            of its publication in English, Bourdieu’s study was already dated, 
            describing a </span><st1:country-region><st1:place><span lang="EN-GB">France</span></ST1:PLACE></ST1:COUNTRY-REGION><span lang="EN-GB"> of the past, and especially a working class culture, 
            like similar cultures in </span><st1:country-region><st1:place><span lang="EN-GB">Britain</span></ST1:PLACE></ST1:COUNTRY-REGION><span lang="EN-GB"> and the </span><st1:country-region><st1:place><span lang="EN-GB">United 
            States</span></ST1:PLACE></ST1:COUNTRY-REGION><span lang="EN-GB">, 
            that was in its death throes.<span style="mso-spacerun: yes">&nbsp; 
            </span>For another, for all its surveys and tables of results, it 
            actually took the form of a polemic against Kantian aesthetics and 
            the snobbery of post-war Parisian intellectuals.<span style="mso-spacerun: yes">&nbsp; </span>And for a third, Bourdieu 
            was not actually a cultural relativist and was not arguing that 
            taste was merely determined by class, but arguing instead that taste 
            was a domain of the social struggle for dominance (see Bennett 
            2005).<span style="mso-spacerun: yes">&nbsp; </span>Bourdieu was 
            writing with an agenda.<span style="mso-spacerun: yes">&nbsp; 
            </span>And if the disinterestedness of the sociology of the group is 
            a bit of a phantom in the work of Bourdieu, it is even more so in a 
            recent, British sociology of food that claims that people like me, 
            with cosmopolitan eating habits and interests and lusts, are 
            ‘cultural omnivores’ (Warde 2000: note the use of this loaded term 
            ‘omnivore’ as if it were merely a dispassionately descriptive term) 
            or even worse, that such cultural omnivores are ‘predatory,’ their 
            practices involving a ‘pillage of resources’, a ‘scouring of 
            habitats’, an ‘uprooting and repackaging of the foreign, the novel, 
            the dangerous’ (Bell 2002).<A title="" style="mso-footnote-id: ftn2" href="#_ftn2" name=_ftnref2><sup>2</sup></A><span style="mso-spacerun: yes">&nbsp;&nbsp; </span>Mainstream food 
            sociology in Britain is no less located in its assessments of the 
            food practices of modern Brits than the Brits themselves.<span style="mso-spacerun: yes">&nbsp; </span>Indeed, it is surprisingly 
            biased.<span style="mso-spacerun: yes">&nbsp; </span></span></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; mso-pagination: widow-orphan"><span lang="EN-GB"><span style="mso-tab-count: 1">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
            </span>So: both the practice of eating and the study of it are 
            located in all kinds of ways; they always have an agenda, and always 
            must.<span style="mso-spacerun: yes">&nbsp; </span>In my study, in 
            any case, the legitimacy of which can only be assessed by its 
            results, the subject and the sociologist would be one.<span style="mso-spacerun: yes">&nbsp; </span>The individual and the 
            sociologisation of the individual through systematic observation 
            would be one. Or rather two, since the research would be a team 
            effort, involving an invaluable research associate (Marion Scott 
            Appelbaum), as well as the team leader (me).</span></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; mso-pagination: widow-orphan"><span lang="EN-GB"><span style="mso-tab-count: 1">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
            </span>Together we laid our plans carefully.<span style="mso-spacerun: yes">&nbsp; </span>We would stay in a budget 
            hotel in a residential area, a little off the beaten track but 
            convenient enough to the restaurants and shops we wanted to 
            visit.<span style="mso-spacerun: yes">&nbsp; </span>To save money, 
            we would do most of our major dining at lunch time, and take 
            advantage of lunch-time specials.<span style="mso-spacerun: yes">&nbsp; </span>We would visit as many 
            different kinds of places as time allowed, although we were 
            especially interested in the new things and the most popular things 
            among knowing Londoners – for example, that hybrid form of social 
            life known as ‘modern British cuisine’.<span style="mso-spacerun: yes">&nbsp; </span>We would also visit shops 
            and markets, and study street life.<span style="mso-spacerun: yes">&nbsp; </span>What, again, was the 
            phenomenological, material and symbolic place of food, the situation 
            of food in modern </span><st1:city><st1:place><span lang="EN-GB">London</span></ST1:PLACE></ST1:CITY><span lang="EN-GB">?<span style="mso-spacerun: yes">&nbsp; </span>We took 
            along notebooks, a laptop, and a Dictaphone, along with a <i>Time 
            Out Guide to Dining in London</i>, 2007 edition (the <i>Michelin 
            </i>is much less thorough), and observations culled from various 
            Internet sites, including the on-line Zagat survey and the annual 
            World’s Fifty Best Restaurants featured on the site of the 
            London-based <i>Restaurant</i> magazine.<span style="mso-spacerun: yes">&nbsp; </span>Knowing how limited our 
            resources were, we knew not only that our findings would be partial, 
            tentative, and somewhat arbitrary, but that our project was 
            following less in the footsteps of Boas, Malinowski, and 
            Lévi-Strauss than in those of the countless journalists for the <i style="mso-bidi-font-style: normal">New York Times</i> going on city 
            breaks and reporting back to well-heeled readers about where to 
            stay, what to see, and where to dine in Prague, or Singapore, or 
            Kansas City.<span style="mso-spacerun: yes">&nbsp; </span>Only we 
            would not pretend to observe the object of a 36-hour city with 
            timely objectivity, dispensing advice to consumers.</span></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; mso-pagination: widow-orphan"><span lang="EN-GB"><span style="mso-tab-count: 1">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
            </span>Even if I am only something of a social scientist, I am 
            officially very much a professional literary critic, and so the fact 
            that I was following in the footsteps of journalists provided an 
            additional incentive.<span style="mso-spacerun: yes">&nbsp; 
            </span>My trip to </span><st1:city><st1:place><span lang="EN-GB">London</span></ST1:PLACE></ST1:CITY><span lang="EN-GB"> 
            would not only provide a window on the sociology of the individual, 
            but give me a chance to look behind the scenes of what amounts to a 
            literary genre.<span style="mso-spacerun: yes">&nbsp; 
            </span>Thirty-six Hours in Akron, What’s Doing in Berlin: fun to 
            read, providing you with tips for a future visit if ever you’re 
            lucky enough to go on one, and in any case furnishing you with the 
            vicarious pleasure of pretending to go on a whirlwind trip to some 
            far-off burg, these exercises in travel journalism have a form of 
            their own, a rhetoric of their own, an illusory objectivity all 
            their own.<span style="mso-spacerun: yes">&nbsp; </span>In fact, 
            after a while, a modern literary critic may well come to find that 
            there is something fictional about them, or even factitious.<span style="mso-spacerun: yes">&nbsp; </span>They are not only formulaic; 
            they almost always describe an experience that is <i>too good to be 
            true</i>.<span style="mso-spacerun: yes">&nbsp; </span>I wanted to 
            see what lay behind the exercises, and think about how language and 
            convention mediates between the experience of the writer and the 
            material that is actually written.<span style="mso-spacerun: yes">&nbsp; </span>So, again, we had two kinds 
            of questions to answer.<span style="mso-spacerun: yes">&nbsp; 
            </span>What would our five days in </span><st1:city><st1:place><span lang="EN-GB">London</span></ST1:PLACE></ST1:CITY><span lang="EN-GB"> 
            tell us about the culture of eating in 
            </span><st1:city><st1:place><span lang="EN-GB">London</span></ST1:PLACE></ST1:CITY><span lang="EN-GB">, 
            and what would it tell us about the culture of writing about 
            food?</span></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; mso-pagination: widow-orphan"><span lang="EN-GB"><o:p></O:P></span></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; mso-pagination: widow-orphan"><span lang="EN-GB"><span style="mso-tab-count: 1">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
            </span>We were out on the streets within moments of checking into 
            our hotel.<span style="mso-spacerun: yes">&nbsp; </span>We were in 
            Kensington, not a ‘typical’ </span><st1:city><st1:place><span lang="EN-GB">London</span></ST1:PLACE></ST1:CITY><span lang="EN-GB"> 
            neighbourhood one might say, but what is ‘typical’, and why?<span style="mso-spacerun: yes">&nbsp; </span>Kensington is very wealthy 
            and residential; it has its own ‘high street’ with small and large 
            shops.<span style="mso-spacerun: yes">&nbsp; </span>It is not a 
            major destination for either locals or tourists, although there are 
            a number of hotels (we got an excellent deal, in a comfortably 
            shabby establishment, it being off-season) and there were plenty of 
            tourists on the streets.<span style="mso-spacerun: yes">&nbsp; 
            </span>It is not a place where one goes out of one’s way to dine, 
            but it is not an area where one cannot dine either.<span style="mso-spacerun: yes">&nbsp; </span>At the time of my research 
            there, an old department store was being converted into a very large 
            new Whole Foods market, the first outpost in 
            </span><st1:country-region><st1:place><span lang="EN-GB">Britain</span></ST1:PLACE></ST1:COUNTRY-REGION><span lang="EN-GB"> of the American organic-gourmet food chain, and, it has 
            been said in the press, sure to change food retailing in 
            </span><st1:country-region><st1:place><span lang="EN-GB">Britain</span></ST1:PLACE></ST1:COUNTRY-REGION><span lang="EN-GB"> utterly.<span style="mso-spacerun: yes">&nbsp; 
            </span>Perhaps we were researching a ‘before’, with an ‘after’ to 
            come.<span style="mso-spacerun: yes">&nbsp; </span>In any case, the 
            sociological model of that which is statistically ‘typical’ in the 
            context of a vast, diverse, cosmopolitan, and always rapidly 
            changing metropolis is almost certainly irrelevant. What is typical 
            about </span><st1:city><st1:place><span lang="EN-GB">London</span></ST1:PLACE></ST1:CITY><span lang="EN-GB"> is 
            its variety. Or not quite: for even if 
            </span><st1:city><st1:place><span lang="EN-GB">London</span></ST1:PLACE></ST1:CITY><span lang="EN-GB"> has 
            lots of variety in its social and cultural life, it has variety in 
            its own way.<span style="mso-spacerun: yes">&nbsp; </span>It has its 
            own way, one might say, of <i>distributing variety</i>, and what one 
            encounters when one visits the city, is not only what is unique 
            about unique places, institutions, and people, but perhaps even more 
            importantly, and certainly more noticeably, are its systems of what 
            may be called <i>continuity-in-variety</i>.<span style="mso-spacerun: yes">&nbsp; </span>These are systems inscribed 
            in its architecture, its structures of transportation and 
            communication, its patterns of work and leisure, of production, 
            distribution, and consumption.<span style="mso-spacerun: yes">&nbsp; 
            </span>The guidebooks direct the tourist to ‘landmarks’ like the 
            Nelson column in Trafalgar Square; the restaurant guides direct the 
            gourmand-tourist to places like Sketch, ‘the most expensive 
            restaurant in London,’ as almost all references to the establishment 
            are quick to mention (Cooke, 2003).<span style="mso-spacerun: yes">&nbsp; </span>I will have more to say 
            about the idea of the unique in what follows.<span style="mso-spacerun: yes">&nbsp; </span>Suffice it to say for now, 
            that though the unique is everywhere promoted, it is the 
            ‘everywhere’ itself that really presses upon experience, and 
            continuity-in-variety is what the social scientist is obliged to 
            observe and explain. </span></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; mso-pagination: widow-orphan"><span lang="EN-GB"><span style="mso-tab-count: 1">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
            </span>Out on the streets, in Kensington, as social scientists more 
            or less following the route of a tourist-<i>flaneu</i>r-gourmand, 
            passing along a route from Kensington Road to Kensington High 
            Street, sensing the continuities of London life – the architecture, 

            lang=EN-GB>, or anywhere else in 
            </SPAN><st1:country-region><st1:place 
            ><span lang=EN-GB 
            >Britain</SPAN></ST1:PLACE></ST1:COUNTRY-REGION><span 
            lang=EN-GB>, therefore presents a unique 
            challenge.<span style="mso-spacerun: yes">&nbsp; 
            </SPAN>In the midst of the </SPAN><st1:place 
            ><st1:placetype><span 
            lang=EN-GB>Tower</SPAN></ST1:PLACETYPE><span 
            lang=EN-GB> of </SPAN><st1:placename 
            ><span lang=EN-GB 
            >Babel</SPAN></ST1:PLACENAME></ST1:PLACE><span 
            lang=EN-GB> of food here, it is very difficult 
            for a restaurateur </SPAN><span lang=EN-GB>to 
            find his or her own and proper language, not to mention his or her 
            own proper voice or style.<span style="mso-spacerun: yes" 
            >&nbsp; </SPAN>And in the midst of the Darwinian 
            struggle for custom, it is very difficult to find an infrastructure 
            to rely upon.<span style="mso-spacerun: yes" 
            >&nbsp; </SPAN>Go to a restaurant in a seaside 
            town in any of the Latin countries, and you have a very good chance 
            of getting a fine seafood dinner, and that is because the 
            infrastructure is there.<span style="mso-spacerun: yes" 
            >&nbsp; </SPAN>The restaurateur doesn’t have to 
            look far for decent fish, or decently trained servers and cooks; 
            they are already there.<span style="mso-spacerun: yes" 
            >&nbsp; </SPAN>In </SPAN><st1:city 
            ><st1:place><span lang=EN-GB 
            >London</SPAN></ST1:PLACE></ST1:CITY><span 
            lang=EN-GB>, however, the produce, the service, 
            and the cookery all have to be searched out, nurtured, and 
            sustained.<span style="mso-spacerun: yes">&nbsp; 
            </SPAN>They cannot be taken for granted. The money and effort 
            required to develop and sustain the system is one of the reasons why 
            food, good as it sometimes is, is so expensive in </SPAN><st1:city 
            ><st1:place><span lang=EN-GB 
            >London</SPAN></ST1:PLACE></ST1:CITY><span 
            lang=EN-GB>, and it is one of the reasons why 
            the logic of the multiple has come to dominate the restaurant 
            scene.<span style="mso-spacerun: yes">&nbsp; 
            </SPAN></SPAN></P>
            <p class=MsoNormal 
            style="MARGIN: 0in 0in 0pt; mso-pagination: widow-orphan" 
            ><span lang=EN-GB><span 
            style="mso-tab-count: 1" 
            >&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
            </SPAN>We discovered a pointed example of this in one of the first 
            places we visited, the celebrated Acorn House.<span 
            style="mso-spacerun: yes">&nbsp; </SPAN>An 
            outgrowth of Jamie Oliver’s Fifteen project, Acorn House is at once 
            a commercial institution and a charitable trust, which trains chefs 
            from disadvantaged backgrounds and serves reduced price meals to 
            agency employees.<span style="mso-spacerun: yes" 
            >&nbsp; </SPAN>Its main mission is both 
            ecological and aesthetic.<span style="mso-spacerun: yes" 
            >&nbsp; </SPAN>It wants to provide food with 
            minimal waste and maximum sustainability, producing the lowest 
            possible carbon footprint, using only locally sourced and seasonal 
            ingredients; and it wants to do all that on the ground floor of an 
            office block at the side of a drab and busy thoroughfare, near 
            King’s Cross, while both providing cheap meals for employees and 
            competing for the custom of fussy Londoners capable of spending 
            thirty quid a head or more for a meal.<span 
            style="mso-spacerun: yes">&nbsp; </SPAN>It has 
            its own compost heaps and vegetable plot on the roof of the office 
            building, and only the most eco-friendly of equipment, furnishings, 
            and fittings.<span style="mso-spacerun: yes" 
            >&nbsp; </SPAN>It gets its cooking oil from rape 
            seed grown within a hundred miles or so of the centre of London, its 
            pork and artichokes from even closer, its table water (with low 
            energy filtration) from the tap.<span style="mso-spacerun: yes" 
            >&nbsp; </SPAN>Though rather late in the day by 
            international standards, given the examples from way back of<span 
            style="mso-spacerun: yes">&nbsp; </SPAN>Chez 
            Panisse in </SPAN><st1:city><st1:place 
            ><span lang=EN-GB 
            >Berkeley</SPAN></ST1:PLACE></ST1:CITY><span 
            lang=EN-GB> and Slow Food in Bra, Acorn House is 
            aiming to create a new infrastructure of restaurant service.<span 
            style="mso-spacerun: yes">&nbsp; </SPAN>The 
            sourcing of ingredients, the developing of the work force, the 
            management of the home economy of the establishment, and even the 
            financial structure are all new and unique.<span 
            style="mso-spacerun: yes">&nbsp; </SPAN>The 
            cuisine too, though based on traditional Italian principles, is 
            somewhat new.</SPAN></P>
            <p class=MsoNormal 
            style="MARGIN: 0in 0in 0pt; mso-pagination: widow-orphan" 
            ><span lang=EN-GB><span 
            style="mso-tab-count: 1" 
            >&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
            </SPAN>Our meal there, taken in a sleek, open and inviting dining 
            room, with light green furnishings along with lots of wood, was all 
            about vegetables.<span style="mso-spacerun: yes" 
            >&nbsp; </SPAN>We ordered from the ‘Menu for 
            March’, the only menu available.<span style="mso-spacerun: yes" 
            >&nbsp; </SPAN>I started with a rich beetroot and 
            cardamon soup, </SPAN><st1:city><st1:place 
            ><span lang=EN-GB 
            >Marion</SPAN></ST1:PLACE></ST1:CITY><span 
            lang=EN-GB> with a watercress and parsley 
            concoction.<span style="mso-spacerun: yes" 
            >&nbsp; </SPAN>I then had grilled sea bream 
            served on a bed of two items from a so-called salad menu, borlotti 
            beans and artichokes, and Marion had roasted guinea fowl au jus 
            served on a bed of three other salad items, celeriac, salsify, and 
            spring cabbage with pancetta. </SPAN></P>
            <p class=MsoNormal 
            style="MARGIN: 0in 0in 0pt; mso-pagination: widow-orphan" 
            ><span lang=EN-GB><span 
            style="mso-tab-count: 1" 
            >&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
            </SPAN>The flavours of every item were fresh, assertive but 
            balanced, and every item too was cooked exactly <i 
            >à point</I> – which is to say, at just the right 
            amount of doneness.<span style="mso-spacerun: yes" 
            >&nbsp; </SPAN>The beetroot soup tasted of beets; 
            the guinea fowl roast, which was hot, unctuous and moist all the way 
            through, tasted of fowl, and was slightly gamy.<span 
            style="mso-spacerun: yes">&nbsp; </SPAN>This was 
            no supermarket chicken.<span style="mso-spacerun: yes" 
            >&nbsp; </SPAN>The salads were stars in their own 
            right.<span style="mso-spacerun: yes">&nbsp; 
            </SPAN>We especially enjoyed the salsify, a root vegetable we had 
            not had much experience with before, its flavour and texture 
            something between celery, parsnip, and asparagus.<span 
            style="mso-spacerun: yes">&nbsp; </SPAN>These 
            ‘salad’ items, slightly vinegary and garlicky in taste, added 
            lightness, contrast, and variety to the heavy meat of the guinea 
            fowl and the dense fish flavours of the sea bream.</SPAN></P>
            <p class=MsoNormal 
            style="MARGIN: 0in 0in 0pt; mso-pagination: widow-orphan" 
            ><span lang=EN-GB><span 
            style="mso-tab-count: 1" 
            >&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
            </SPAN>But of course they are not really ‘salad’ items, in the 
            Italian sense of the word.<span style="mso-spacerun: yes" 
            >&nbsp; </SPAN>They were not <i 
            >insalate; </I>they were <i 
            >verdure</I> and <i 
            >fagioli</I>.<span style="mso-spacerun: yes" 
            >&nbsp; </SPAN>And though classic Italian cuisine 
            might serve any of them as antipasti, it would never serve them, at 
            room temperature, with an oil and vinegar dressing, as an 
            accompaniment to a large portion of hot animal flesh or fish.<span 
            style="mso-spacerun: yes">&nbsp; </SPAN>Though 
            everything on the menu (apart from some classically Northern 
            European soups and deserts) was Italian, the dinner itself was 
            something else; it was an innovation, and yet another hybrid 
            affair.</SPAN></P>
            <p class=MsoNormal 
            style="MARGIN: 0in 0in 0pt; mso-pagination: widow-orphan" 
            ><span lang=EN-GB><span 
            style="mso-tab-count: 1" 
            >&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
            </SPAN>Not that there is anything wrong with that, as a character on 
            <i>Seinfeld</I> might say.<span 
            style="mso-spacerun: yes">&nbsp; </SPAN>But the 
            very success of what Acorn House is doing betrays the contradictions 
            inherent in modern </SPAN><st1:city><st1:place 
            ><span lang=EN-GB 
            >London</SPAN></ST1:PLACE></ST1:CITY><span 
            lang=EN-GB> cookery. Here, in a restaurant 
            devoted to local sourcing (and that in the midst of the largest 
            conurbation in Europe), the items themselves were often of foreign 
            provenance – Italian beans, African guinea fowl, not to mention 
            olive oil and charcuterie from Italy and what is openly acknowledged 
            to be dairy products from Norway and France.<span 
            style="mso-spacerun: yes">&nbsp; </SPAN>Most of 
            the cookery was foreign too.<span style="mso-spacerun: yes" 
            >&nbsp; </SPAN>And yet again, here in a 
            restaurant devoted to Italian principles, the meals themselves were 
            put together in a non-Italian way, an Acorn House way.<span 
            style="mso-spacerun: yes">&nbsp; </SPAN>(Nor for 
            that matter, were there any Italians on staff, as far as we could 
            tell.)<span style="mso-spacerun: yes">&nbsp; 
            </SPAN>Acorn House was not only trying to promote not only a new 
            ecology of restaurant management, but a new way of putting meals 
            together, a new way of eating, which mimicked Italian traditions but 
            also violated them in favour of something less conventional and 
            ceremonial.</SPAN></P>
            <p class=MsoNormal 
            style="MARGIN: 0in 0in 0pt; mso-pagination: widow-orphan" 
            ><span lang=EN-GB><span 
            style="mso-tab-count: 1" 
            >&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
            </SPAN>And it was doing it, again, by establishing what amounted to 
            a new infrastructure of food.<span style="mso-spacerun: yes" 
            >&nbsp; </SPAN>Acorn House was meant to be not 
            just a restaurant, but an institution with an identity, and it was 
            meant to have an identity that not only branded it, giving it an 
            image attractive to customers, but that also&nbsp;laid down an 
            economy of restauration, from organic rape seed farmers in Essex to 
            compost pits on a rooftop in King’s Cross.<span 
            style="mso-spacerun: yes">&nbsp; </SPAN>It is 
            all very heroic, and I wish them the best.<span 
            style="mso-spacerun: yes">&nbsp; </SPAN>But it 
            also amounts to a phenomenon Andrew Ross (1991) once called 
            attention to on the subject of New Age science.<span 
            style="mso-spacerun: yes">&nbsp; </SPAN>An 
            alternative to establishment science, New Age science is 
            counter-cultural, oppositional, even revolutionary.<span 
            style="mso-spacerun: yes">&nbsp; </SPAN>But in 
            its attempts to oppose the establishment, it ends up mimicking 
            it.<span style="mso-spacerun: yes">&nbsp; 
            </SPAN>No practice is more capitalistic, entrepreneurial, and 
            consumerist than New Age science, with its alternative publishing 
            houses, medical facilities, health products, trade shows, and 
            boutiques.<span style="mso-spacerun: yes">&nbsp; 
            </SPAN>And the same thing could be said about the New Age innovation 
            called Acorn House.<span style="mso-spacerun: yes" 
            >&nbsp; </SPAN>If it is trying to revolutionise 
            the way people eat,&nbsp;though in a counter-cultural spirit, it is 
            in fact doing no more no less than what most successful new ventures 
            in </SPAN><st1:city><st1:place 
            ><span lang=EN-GB 
            >London</SPAN></ST1:PLACE></ST1:CITY><span 
            lang=EN-GB> do.<span style="mso-spacerun: yes" 
            >&nbsp; </SPAN>The fuss about London, over and 
            above the unfortunate facts that most food in London is still not 
            very good, and most Londoners still eat poorly (on which see 
            Blythman, 2006) stems from the un-centred, un-hierarchical energy of 
            the London food scene: every new successful restaurateur in London 
            is trying to reinvent London cookery, trying to bring forth, out of 
            the Darwinian jungle of the London food scene, a new and dominant 
            species.<span style="mso-spacerun: yes">&nbsp; 
            </SPAN>But the first commandment of </SPAN><st1:city 
            ><st1:place><span lang=EN-GB 
            >London</SPAN></ST1:PLACE></ST1:CITY><span 
            lang=EN-GB> cookery isn’t then to ‘make it 
            good’, but something much more ambitious.<span 
            style="mso-spacerun: yes">&nbsp; </SPAN>It is 
            biblical, even: ‘Go forth and multiply!’</SPAN></P>

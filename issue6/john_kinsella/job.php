            <br><p><strong><br>The Sleep of 
            Blowflies</strong><br><br>The shady side of the house<br>
is subsumed by blowflies.<br>
It is stinking hot and stormy<br>
and they've stopped moving. <br>
<br><br>
They <em>might</em>
  be asleep.<br>
Or is blowfly sleep a <em>torpor</em>
     ?<br>
The house is their corpse <br>
in which eggs won't set,<br>
<br><br>
maggots can't take. No<br> 
forensic kit gives clues. The stimuli<br> 
of heat and light disrupt<br> 
circadian manifests, the sense<br>
<br><br>
of day and night. Memory?<br>
Mushroom body writes <br>
short-sleep lines, struggles <br>
to calibrate sleep regulation<br>
<br><br>
machinery. Opening<br>
a door or window, blowflies <br>
erupt to settle near<br> 
places of departure:<br> 
<br><br>
too quick for sleep? Resting,<br>
waiting, malingering,<br>
or an attempt to rescue <br>
sleep lost in forensic <br>
<br><br>
etymology, battling<br> 
to stay wide awake<br>
in the dark, the swarm<br>
      of isolation.<br>
<p><br><br><br>
<b>Letter to Frieda</b>
<br>
<br>
Had a flashback tonight,<br>
straight into the devilry<br>
of an acid trip, a gold flake<br>
rehabilitation of childhood,<br>
<br>
<br>
where nature stripped colour<br>
to components<br>
and flowers insisted<br>
on sparklers - nasturtiums -<br>
<br><br>
too bright, pluperfect<br>
eyes open, blackness<br>
machinate as the second person:<br>
what am I trying to say here?<br>
<br><br>
It's like paraphrasing song lyrics,<br>
their goodness buried<br>
under copyright. I've forgotten<br>
the names of the birds<br>
<br><br>
seen today, and even outlines<br>
are fading away<br>
from cities - this rain,<br>
this little and less rain,<br>
<br><br>
teasing seed still dry<br>
in the ground,<br>
picking at tracks<br>
and wheel ruts,<br>
<br><br>
would-be Godsend<br>
whispering in that charged<br>
kind-of-way: synapse<br>
artery vein.<br>

            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; TEXT-ALIGN: center" align="center"><b style="mso-bidi-font-weight: normal"><span lang="EN-GB" style="FONT-SIZE: 9pt">OUR 
            MEETINGS<o:p></o:p></span></b></p>
            <p><span lang="EN-GB">In 
            the autumn of 1983 my wife and I bought our home at Polperro on the 
            south coast of </span><st1:city><st1:place><span lang="EN-GB">Cornwall</span></st1:place></st1:city><span lang="EN-GB">.<span style="mso-spacerun: yes">  </span>Topple 
            Cottage was, and is, an ancient fisherman’s cottage in the very 
            heart of the village, three storeys high, the ground floor being 

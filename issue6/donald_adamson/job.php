          <td height="219" colspan="2" valign="top" class="texto2"><p><br><br>The book I am 
            writing is an account of the life of A.L. Rowse, historian, poet and 
            diarist, who was a Cornish friend and neighbour.<span style="mso-spacerun: yes">  </span>But it is no fulsome tribute 
            to this great man.<span style="mso-spacerun: yes">  
            </span>Rather, 
            it is a record of our friendship and an appraisal of his life and 
            work, “warts and all”. </span></p></td>

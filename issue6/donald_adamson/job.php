          <td colspan="4" rowspan="2" valign="top" class="texto2">
            <p><o:p><span lang="EN-GB"> So I shall deal first in 
            my book with the circumstances and events of Rowse’s life; then, in 
            the second part, with his historical and literary achievements; and 
            finally, in the third part, with my friendship with him over 
            thirteen years. </span></p>
            <p><o:p><span lang="EN-GB"> There will be a final 
            chapter on the Art of Biography, relating my biography to the 
            general characteristics of the genre. </span></p>
            <p><o:p><span lang="EN-GB"> Rowse was a controversial 
            and contradictory figure, best known in some quarters for his 
            self-proclaimed “solution”, in 1963, of the first problem of 
            Shakespeare’s <i style="mso-bidi-font-style: normal">Sonnets</i>.<span style="mso-spacerun: yes">  </span>Who was “Mr W.H.”, to whom 
            they were dedicated by their publisher “T.T.”?<span style="mso-spacerun: yes">  </span>Was “Mr W.H.” the “Fair 
            Youth” of the first 126 <i style="mso-bidi-font-style: normal">Sonnets</i>?<span style="mso-spacerun: yes">  </span>And was that “Fair Youth” 
            Shakespeare’s patron, the young Earl of Southampton?<span style="mso-spacerun: yes">   </span> </span></p>
            <p><o:p><span lang="EN-GB"> Secondly, ten years later, 
            Rowse became well known to the point of notoriety for his “solution” 
            of the second problem.<span style="mso-spacerun: yes">  
            </span>Who was the so-called “Dark Lady” of Shakespeare’s <i style="mso-bidi-font-style: normal">Sonnets</i>, to whom he 
            addressed the last twenty-eight of these poems? </span></p>
            <p><o:p><span lang="EN-GB"> In my book I weigh the 
            arguments on either side of each problem question – and come to a 
            conclusion. </span></p>
            <p><o:p><span lang="EN-GB">    
              Rowse was<span style="mso-spacerun: yes"> 
            </span>a brilliant young history don at All Souls who, 
            cold-shouldered by the Oxford History Faculty, was never invited to 
            deliver a single history lecture to the University’s 
            undergraduates;<span style="mso-spacerun: yes">  </span>a 
            writer of major historical works on Tudor England who nevertheless 
            felt powerfully drawn towards literature and the writing of poetry; 
            a man of Conservative tastes and inclinations, even in his early 
            manhood, who nevertheless began his adult life as a radical 
            political thinker – and early exponent of Keynesianism – and who in 
            two General Elections stood as a Labour Parliamentary 
            candidate. </span></p>
            <p><o:p>It was my good fortune to know him 
            well and even on two memorable occasions to experience that full 
            outpouring of his wrath which seemed to be bestowed only upon his 
            closest friends.<span lang="EN-GB"><span style="mso-spacerun: yes"> </span>Much 
            of my book is based on the (unpublished) diary I have been keeping 
            since 1966.</span></p>
            <p><o:p><span lang="EN-GB"> I have conducted a great 
            deal of background research into my subject:<span style="mso-spacerun: yes">  </span>tracing his family – 
            nephews, nieces, cousins – in France, Australia, Canada and the 
            United States; following his footsteps in America, albeit (for the 
            most part) from a distance; and studying in depth the Cornish and 
            Oxford backgrounds of his life. </span></p>
            <p><o:p><span lang="EN-GB"> My work is still only a 
            biography in progress but here anyway are two extracts in the 
            making.<span style="mso-spacerun: yes">  </span>The first deals 
            with the sad topic of the official recognition of A.L. Rowse, so 
            long withheld but finally granted to him in the twilight months of 
            his life.<span style="mso-spacerun: yes">  </span>The second is 
            taken from my account of our friendship. </span></p>
            <p><span lang="EN-GB"> Readers will understand that they are not the 
            extracts containing my most important findings.<br><br><br><br>    
                      
            <br><br></p>
            <p class="MsoNormal" style="MARGIN: 0in 0in 0pt; TEXT-ALIGN: center" align="center"><b style="mso-bidi-font-weight: normal"><span lang="EN-GB" style="FONT-SIZE: 9pt">A.L. ROWSE, HONOURED 
            MAN</span></b><span lang="EN-GB" style="FONT-SIZE: 9pt"><o:p></o:p></span></p>
            <p><span lang="EN-GB">To 
            a far greater extent than most people, Rowse was used to experiencing 
            the extremes of honour and disregard.<span style="mso-spacerun: yes">  </span>He won high plaudits at 
            school, and never more so than when, in 1922, he was awarded the 
            only annual Cornwall County Scholarship, £60 p.a. intended to help 
            finance a student's expenses at University.<span style="mso-spacerun: yes">  </span>To win an Open Scholarship, 
            worth £80 a year, from St Austell County School to that noblest of 
            </span><st1:city><st1:place><span lang="EN-GB">Oxford</span></st1:place></st1:city><span lang="EN-GB"> 
            colleges, </span><st1:place><st1:placename><span lang="EN-GB">Christ</span></st1:placename><span lang="EN-GB"> 
            </span><st1:placetype><span lang="EN-GB">Church</span></st1:placetype></st1:place><span lang="EN-GB">, was an extraordinary achievement.<span style="mso-spacerun: yes">  </span>Add to this the award, by 
            the Drapers' Company in June 1922, of a Soley Scholarship worth £60 
            p.a. for three – extended to four – years, and his tally of early 
            academic honours was complete.<span style="mso-spacerun: yes">  
            </span>At Christ Church came a <i style="mso-bidi-font-style: normal">proxime accessit</i>   
                in the University competition for the Newdigate Prize 
            for the best English poem of the year, a First in Modern 
            History (though not, it seems, the best Modern History First of the 
            year) and, soon afterwards, a Prize Fellowship of All Souls.<span style="mso-spacerun: yes">  </span>ALR was the first man of 
            working-class origin to win such a Fellowship, though by no means 
            the youngest man overall as Hensley Henson had been eleven months 
            younger on his election to a Prize Fellowship in 1884 and, even in 
            the year of ALR'S own election, Roger Makins was two months his 
            junior.<span style="mso-spacerun: yes">  </span>St Austell 
            County School and many Cornish people (ALR's family, Sir Arthur 
            Quiller Couch, the Pethericks, Miss Coode, Noreen Sweet) took pride 
            in this Fellowship; the school was given a half-day holiday on 
            receipt of the news; and George Pilcher, MP for Penryn and Falmouth, 
            wired his congratulations.</span></p>
            <p><span lang="EN-GB">But of honour, or recognition, meted out to him by his 
            own University very little was afterwards forthcoming, though from 
            1951 onwards the situation was rather different at 
            </span><st1:city><st1:place><span lang="EN-GB">Pasadena</span></st1:place></st1:city><span lang="EN-GB">, 
            </span><st1:city><st1:place><span lang="EN-GB">Lynchburg</span></st1:place></st1:city><span lang="EN-GB"> 
            and elsewhere – indeed, almost everywhere – in the 
            </span><st1:country-region><st1:place><span lang="EN-GB">United 
            States</span></st1:place></st1:country-region><span lang="EN-GB">.<span style="mso-spacerun: yes">  </span>  
                At Oxford he never lectured in the History 
            Faculty, was never elected into a Fellowship at any other college, was 
            beaten by Nowell Myres to a history lectureship at Christ 
            Church in 1926, deputized for Sir Arthur Salter at All 
            Souls</span><span lang="EN-GB"> in</span></st1:metricconverter><span lang="EN-GB"> 
            his lectures not on history but on political theory, achieved no 
            Ford Lectureship within the University and no Honorary Studentship 
            of his old college.<span style="mso-spacerun: yes">  </span>Nor 
            was he appointed to supervise any theses though several times he 
            acted as an examiner for a B.Litt or D.Phil.<span style="mso-spacerun: yes">  </span>Through the All Souls system 
            he did, however, move up to become Sub-Warden in 1950-1952 and 
            (because of inopportune deaths) Acting Warden in 1951-1952.<span style="mso-spacerun: yes">  </span>A D.Litt. came his way in 
            1953, for which he had supplicated in the normal manner.<span style="mso-spacerun: yes">   </span>Yet, having progressed 
            – almost on the principle of Buggins' turn – as far as being Acting 
            Warden of All Souls, he narrowly lost out to John Sparrow in the 
            1952 Wardenship election:<span style="mso-spacerun: yes">  
            </span>no dishonour there!<span style="mso-spacerun: yes">  
            </span>He was not, on the other hand, adequately honoured by All 
            Souls in later years, for although in the course of time he was 
            elected an Emeritus Fellow of the College (something which was by no 
            means a foregone conclusion), he did not achieve the Honorary or 
            Distinguished Fellowship that was probably his due.<span style="mso-spacerun: yes">  </span><i style="mso-bidi-font-style: normal">Honorary</i> doctorates were 
            also comparatively few and far between: <span style="mso-spacerun: yes"> </span>one from 
            </span><st1:state><st1:place><span lang="EN-GB">New 
            Brunswick</span></st1:place></st1:state><span lang="EN-GB"> in 1960, 
            another from </span><st1:place><st1:placename><span lang="EN-GB">Lynchburg</span></st1:placename><span lang="EN-GB"> 
            </span><st1:placetype><span lang="EN-GB">College</span></st1:placetype></st1:place><span lang="EN-GB"> in 1984, and – probably most gratifying of all – an 
            honorary D.Litt. from </span><st1:place><st1:placename><span lang="EN-GB">Exeter</span></st1:placename><span lang="EN-GB"> 
            </span><st1:placetype><span lang="EN-GB">University</span></st1:placetype></st1:place><span lang="EN-GB"> as early as May 1960, predating the 
            </span><st1:state><st1:place><span lang="EN-GB">New 
            Brunswick</span></st1:place></st1:state><span lang="EN-GB"> award by 
            five months.<span style="mso-spacerun: yes">   </span>But 
            none, for example, from Harvard, which my own supervisor had, who 
            was also a Fellow of All Souls.</span></p>
            <p><span lang="EN-GB" style="mso-bidi-font-size: 11.0pt">It also took a long time for a 
            memorial tablet to be erected to him at All Souls, an honour which 
            came much more easily to Isaiah </span><st1:state><st1:place><span lang="EN-GB" style="mso-bidi-font-size: 11.0pt">Berlin</span></st1:place></st1:state><span lang="EN-GB" style="mso-bidi-font-size: 11.0pt">, Leo Amery, G.M. 
            Young and others.<span style="mso-spacerun: yes">  </span>And 
            even then there has been an unexpected twist to the honour as ALR 
            had left money in his will for the erection of the plaque and at the 
            foot of that plaque are inscribed the words “Ipse Testamento Suo 
            Legavit” (“he himself bequeathed the money for this memorial’”), 
            words which the Fellows of All Souls have cunningly disguised in 
            Latin.<o:p></o:p></span></p>
            <p><span lang="EN-GB">From within his own county, which had no university, 
            recognition came, after a period of somewhat strained relations with 
            his home town, when he was invited to write the excellent local 
            history <i style="mso-bidi-font-style: normal">St Austell: Church, 
            Town, Parish</i> (1960).<span style="mso-spacerun: yes">  
            </span>In September 1968 the Cornish Gorsedd admitted him as a Bard, 
            by the name of <i style="mso-bidi-font-style: normal">Lef A 
            Gernow</i> <i style="mso-bidi-font-style: normal">Tramor</i> (<i style="mso-bidi-font-style: normal">Voice of Cornwall 
            Overseas</i>).<span style="mso-spacerun: yes">   
            </span>Almost within view of Trenarren House and the vista of the 
            sea he loved so much stands the memorial stone unveiled to him on 
            Black Head in July 1999.<span style="mso-spacerun: yes">  
            </span>Seven months later, in February </span><st1:metricconverter productid="2000, a"><span lang="EN-GB">2000, 
            a</span></st1:metricconverter><span lang="EN-GB"> further memorial was 
            unveiled to him – as “Historian &amp; Poet” – in the Jesus Chapel of 
            Truro Cathedral.<span style="mso-spacerun: yes">  </span>This 
            plaque stands alongside those previously erected in honour of 
            Richard<b style="mso-bidi-font-weight: normal"> </b>Trevithick, Sir 
            Arthur Quiller Couch and Charles Henderson.</span></p>
            <p><span lang="EN-GB">Meanwhile, on the wider national stage, ALR first 
            appeared in <i style="mso-bidi-font-style: normal">Who's Who</i> in 
            1938.<span style="mso-spacerun: yes">  </span>In February 1947, 
            as “author of <i style="mso-bidi-font-style: normal">A Cornish</i> 
            <i style="mso-bidi-font-style: normal">Childhood</i>, etc”, he was 
            accorded the rare honour of election to Fellowship of the Royal 
            Society of Literature “by invitation of Council” (the entrance fee 
            being waived!), whereas most candidates for this distinction are put 
            forward not by Council but, “from personal knowledge”, by at least 
            four Fellows.<span style="mso-spacerun: yes">  </span>In 
            1952-1953 he was President of the English Association:<span style="mso-spacerun: yes">  </span>in Coronation Year this 
            befitted an expert on the first </span><st1:city><st1:place><span lang="EN-GB">Elizabeth</span></st1:place></st1:city><span lang="EN-GB"> 
            who was already taking a keen interest in Shakespeare.<span style="mso-spacerun: yes">  </span>Towards the end of the same 
            year he was featured in <i style="mso-bidi-font-style: normal">The 
            Sunday Times</i> Portrait Gallery: <span style="mso-spacerun: yes"> </span>the photograph was one of the 
            celebrated portraits by Douglas Glass; the article itself was headed 
            “An Elizabethan”.<span style="mso-spacerun: yes">   
            </span>It was a sure mark of public recognition.<span style="mso-spacerun: yes">  </span> 
            </span></p>
            <p><span lang="EN-GB">Established now as one of the two leading authorities on 
            Tudor England, ALR delivered the Raleigh Lectureship of the British 
            Academy on “Sir Richard Grenville's Place in English History” in 
            1957, and the Trevelyan Lectures, on “The Elizabethans and America”, 
            at Cambridge in the following year:<span style="mso-spacerun: yes">  </span>the invitation to deliver 
            these Cambridge lectures was, he said, “the greatest honour that has 
            befallen me [hitherto]”.<span style="mso-spacerun: yes">  
            </span>Also in 1958 he joined the prestigious ranks of the 
            </span><st1:place><st1:placename><span lang="EN-GB">British</span></st1:placename><span lang="EN-GB"> 
            </span><st1:placetype><span lang="EN-GB">Academy</span></st1:placetype></st1:place><span lang="EN-GB">, Sir John Neale, Richard Pares and A.J.P. Taylor being 
            among his nominators.<span style="mso-spacerun: yes">  
            </span>Much later in his life came a further indication of a 
            prestige that was perhaps still growing, perhaps already slightly on 
            the wane, when in 1972 he was elected to membership of the Athenæum 
            Club under its prestigious Rule 2 (as W.B. Yeats had been 
            thirty-five years previously).<span style="mso-spacerun: yes">  
            </span>I have always been fascinated by the great store he set by 
            the Benson Medal of the Royal Society of Literature, conferred upon 
            him in 1982 for meritorious works in “poetry, fiction, history, 
            biography and belles-lettres”.<span style="mso-spacerun: yes">  
            </span>He was only the twenty-eighth medallist since 1916; among its 
            previous recipients had been Gabriele d'Annunzio, Lytton Strachey, 
            E.M. Forster and Philip Larkin: <span style="mso-spacerun: yes"> </span>recognition by and of “the 
            happy few”!</span></p>
            <p><span lang="EN-GB">The enigma is at the level of public honours, though here 
            too there is a considerable measure of clarity if one looks deeply 
            enough and in the right places.<span style="mso-spacerun: yes">  </span>Was ALR offered honours he 
            refused, or was he offered nothing?<span style="mso-spacerun: yes">  </span>I occasionally asked him 
            whether he would accept a national honour if it were offered 
            him.<span style="mso-spacerun: yes">  </span>“If it would help 
            to sell my books”, he would reply.<span style="mso-spacerun: yes">  </span>There is no doubt that what 
            he most desired was the OM, that supreme accolade of personal 
            distinction which was conferred upon his All Souls colleague Sir 
            William Holdsworth in 1943, and which was also enjoyed by G.M. 
            Trevelyan, Gilbert Murray, Lord Halifax, T.S. Eliot, and that 
            “Quaker milksop” G.P. Gooch: ALR's words to me.<span style="mso-spacerun: yes">  </span>In later years it was also 
            conferred upon his former pupil C.V. Wedgwood and upon Isaiah 
            </span><st1:state><st1:place><span lang="EN-GB">Berlin</span></st1:place></st1:state><span lang="EN-GB">.<span style="mso-spacerun: yes">  </span>Veronica 
            Wedgwood, not ALR, was Gooch's replacement.<span style="mso-spacerun: yes">  </span>This is how the system tends 
            to work:<span style="mso-spacerun: yes">  </span>substitute one 
            historian for another; “she has my </span><st1:place><span lang="EN-GB">OM</span></st1:place><span lang="EN-GB">” was ALR's 
            reaction.</span></p>
            <p><span lang="EN-GB">An 
            immense prestige is attached to the Order of Merit, which, partly 
            because so many second-ranking politicians (or statesmen) have been 
            appointed to the Order of the Companions of Honour, the latter has 
            been unable to rival.<span style="mso-spacerun: yes">   
            </span>ALR's publisher Harold Macmillan, himself an OM for the last 
            ten years of his life, was fond of recalling that three OMs (Morley, 
            Bryce, Hardy) attended his wedding in 1920.<span style="mso-spacerun: yes">  </span>At the party held at 
            Trenarren to celebrate the award of his CH the by then very 
            debilitated stroke-victim whispered to me that he “would have 
            preferred something more academic” (by which he meant the Order of 
            Merit).<span style="mso-spacerun: yes">  </span>For one reason 
            or another there was no chance that he would be offered it.<span style="mso-spacerun: yes">  </span>Was he ever offered a 
            knighthood, as William Waldegrave has suggested?<span style="mso-spacerun: yes">  </span>Certainly, much was done to 
            block “[his] </span><st1:place><span lang="EN-GB">OM</span></st1:place><span lang="EN-GB">” by the so-called 
            Maecenas Committee on which both Isaiah Berlin and C.V. Wedgwood 
            sat.<span style="mso-spacerun: yes">  </span>It was a big job 
            even to get ALR the CH, something to which, in 1993 and again in 
            1996, I myself devoted a good deal of time and effort.<span style="mso-spacerun: yes">   </span>From March 1989 to 
            June 1990 Woodrow Wyatt had tried in vain with Margaret Thatcher to 
            obtain an honour for him, and <i style="mso-bidi-font-style: normal">he</i> was merely thinking of a 
            knighthood.</span></p>
            <p><span lang="EN-GB">Honour is not always the polar opposite of dishonour, 
            often being the antithesis of obscurity or oblivion or disrepute; it 
            is not even synonymous with reputation.<span style="mso-spacerun: yes">  </span>The “most unkindest cut of 
            all”, two years after ALR's death, has been the memoir of him by his 
            All Souls colleague John McManners, who, clergyman as he was and no 
            doubt plain-speaking friend, conducted with becoming decorum his 
            memorial service in All Souls College Chapel.<span style="mso-spacerun: yes">  </span>However, according to this 
            memoir, ALR was “an impossible figure” “tarnished” by “lunatic 
            self-importance”, “overweening self-esteem”,<span style="mso-spacerun: yes">  </span>“selfishness”, “spleen”, 
            “ranting”, “rumbustious vulgarity”, and a man who in one of his 
            books “blatantly plagiarizes”.<span style="mso-spacerun: yes">  
            </span>It is this insinuation of plagiarism, based on a joke about 
            All Souls, that most rankles.<span style="mso-spacerun: yes">  
            </span>Incidentally, <i style="mso-bidi-font-style: normal">rumbustiousness</i> is no 
            dishonour, nor is it a personal failing.</span></p>
            <p><span lang="EN-GB">Extreme epithets such as those of John McManners do 
            perhaps detract from ALR's reputation; they do nothing to impugn his 
            honour.<span style="mso-spacerun: yes">  </span>He fortunately 
            cared little for his personal reputation despite having a fatherly 
            but also businesslike concern for the reputation of books that were 
            personally and distinctively his.</span></p>
            <p><span lang="EN-GB">What remains to be established is his reputation as one 
            of the best of British diarists.</span><o:p></o:p></p>

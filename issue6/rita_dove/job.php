              <p><br>A footnote in musical history becomes 
            the keystone to Rita Dove's latest book-length lyric narrative: 
            In 1803 violinist George Polgreen Bridgetower (1780-1860), 
            wunderkind progeny of a white European woman and a black "African 
            Prince", travels from London to Vienna to meet the continent's "bad 
            boy" musical genius, Ludwig van Beethoven. By all rights, the sonata 
            subsequently inspired by and composed in honor of the mulatto's 
            talents should have borne his name -- had not young George, still 
            exuberant from having premiered the difficult piece to great 
            acclaim, become fresh with a girl Ludwig also fancied. Around this 
            crucial moment, Dove builds a passionate, eccentric pageant of 
            eighteenth and nineteenth century life -- from Haydn's discovery of 
            the dark-skinned child genius in the servants' quarters of a 
            Hungarian castle to Paris right before the French Revolution; from 
            the Prince of Wales' doomed pleasure palace in Brighton to the 
            raucous open air entertainments offered by Vienna's Prater; from 
            Napoleon's ravaged battlegrounds to the self-satisfied refinements 
            of Cambridge. A panoply of luminaries and extras – grave robbers and 
            courtiers, street musicians and aristocrats -- populate this 
            grandiose yet melancholy tale.  </p>

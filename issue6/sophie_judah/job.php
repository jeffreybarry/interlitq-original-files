            cemetery in Simla.<span style="mso-spacerun: yes">  </span>We 
            always took the body to </span><st1:city><st1:place><span style="mso-ansi-language: EN-US; mso-bidi-font-style: italic">Delhi</span></st1:place></st1:city><span style="mso-ansi-language: EN-US; mso-bidi-font-style: italic"> for 
            burial."<o:p></o:p></span></p>
            <p>"He is right, Betty," Ronnen's mother said in an attempt to 
            make peace.<span style="mso-spacerun: yes">  </span>The cemetery in </span><st1:city><st1:place><span style="mso-ansi-language: EN-US; mso-bidi-font-style: italic">Delhi</span></st1:place></st1:city><span style="mso-ansi-language: EN-US; mso-bidi-font-style: italic"> is 

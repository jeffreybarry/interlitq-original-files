            <p>Charlie became angry.<span style="mso-spacerun: yes">  
            </span>"Don't teach us our duty.<span style="mso-spacerun: yes">  </span>We'll do what we want," he 
            said.<o:p></o:p></span></p>
            <p>"Yes, but I, as a self-respecting Jew, won't have a part in 
            it.<span style="mso-spacerun: yes">  </span>When I face my 

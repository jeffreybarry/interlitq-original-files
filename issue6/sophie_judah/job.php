            <p>"Betty," his mother shouted and she waved 
            her handkerchief high above her head. A small woman in a blue dress 
            waved back. Ronnen wondered why his parents felt threatened by such 
            a frail-looking creature. The </span><i style="mso-bidi-font-style: normal"><span style="mso-ansi-language: EN-US">ayah</span></i><span style="mso-ansi-language: EN-US; mso-bidi-font-style: italic"> had 
            been made to clean and polish for days to make the house 
            sparkle.<span style="mso-spacerun: yes">  </span>His parents 
            hoped to impress Aunt Betty and if possible make her regret having 
            left the beautiful 'hill-station' of Simla for a desert town called 
            Dimona in </span><st1:country-region><st1:place><span style="mso-ansi-language: EN-US; mso-bidi-font-style: italic">Israel</span></st1:place></st1:country-region><span style="mso-ansi-language: EN-US; mso-bidi-font-style: italic">.<span style="mso-spacerun: yes">  </span>"They have just the bare 
            necessities," his father had said, "and they come here and try to 
            preach to us."<o:p></o:p></span></p>
            <p>For the next few days Ronnen listened 
            carefully to every word his aunt uttered but found no propaganda for 
            </span><st1:country-region><st1:place><span style="mso-ansi-language: EN-US; mso-bidi-font-style: italic">Israel</span></st1:place></st1:country-region><span style="mso-ansi-language: EN-US; mso-bidi-font-style: italic"> in 
            anything she said.<span style="mso-spacerun: yes">  </span>She 
            seemed to have taken an oath not to upset her relatives.<span style="mso-spacerun: yes">  </span>The only strange thing was 
            that she did not allow the maid to wash her underclothes although 
            she did let her do the rest of the laundry.<span style="mso-spacerun: yes">  </span>She polished her shoes 
            herself too.<span style="mso-spacerun: yes">  </span>She did 
            not put on airs, as he had expected her to.<span style="mso-spacerun: yes">  </span>She ate rice and curry with 
            her fingers, her Hindi was unaccented and grammatically 
            correct.<span style="mso-spacerun: yes">  </span>It was as 
            though she had never been away.<span style="mso-spacerun: yes">  </span>She shopped for articles 
            like glass bangles and brass-ware that were either unavailable or 
            expensive in </span><st1:country-region><st1:place><span style="mso-ansi-language: EN-US; mso-bidi-font-style: italic">Israel</span></st1:place></st1:country-region><span style="mso-ansi-language: EN-US; mso-bidi-font-style: italic"><span style="mso-spacerun: yes">   </span>Ronnen thought that 
            she was no different from any other Indian 
            lady.<o:p></o:p></span></p>
            <p>On Friday</span><i style="mso-bidi-font-style: normal"><span style="mso-ansi-language: EN-US"> </span></i><span style="mso-ansi-language: EN-US; mso-bidi-font-style: italic">his 
            mother took care to have everything perfect for the </span><i style="mso-bidi-font-style: normal"><span style="mso-ansi-language: EN-US">Shabbat.</span></i><span style="mso-ansi-language: EN-US; mso-bidi-font-style: italic"><span style="mso-spacerun: yes">  </span>His father had taken the 
            unusual step of giving the servants the day off on Saturday so that 
            'that woman' would not find anything wrong with their 
            lifestyle.<span style="mso-spacerun: yes">  </span>Betty said 
            nothing about the prayers which seemed unusually long to 
            Ronnen.<span style="mso-spacerun: yes">   </span>She just 
            sang all the Friday songs without noticing that the children did not 
            know the words.<span style="mso-spacerun: yes">  </span>She 
            taught them new tunes and enjoyed the meal.<span style="mso-spacerun: yes">  </span>There was no meat in the 
            house as long as she was there since she observed strict </span><i style="mso-bidi-font-style: normal"><span style="mso-ansi-language: EN-US">kashrut</span></i><span style="mso-ansi-language: EN-US; mso-bidi-font-style: italic">.<span style="mso-spacerun: yes">  </span>Delicious river-fish, cooked 
            in coconut juice was served along with white rice, vegetables and 
            salads.<span style="mso-spacerun: yes">  </span>Betty seemed 
            happy.<span style="mso-spacerun: yes">  </span>She said that 
            the meal reminded her of her mother's cooking.<o:p></o:p></span></p>
            <p>Everything would have ended well if Mr. Koletkar had not 
            died.<span style="mso-spacerun: yes">  </span>The man was over 
            eighty years old and had been ailing for some time.<span style="mso-spacerun: yes">  </span>One of his sons lived in 
            </span><st1:city><st1:place><span style="mso-ansi-language: EN-US; mso-bidi-font-style: italic">Delhi</span></st1:place></st1:city><span style="mso-ansi-language: EN-US; mso-bidi-font-style: italic"> and 
            the other was an Army Officer stationed in 
            </span><st1:city><st1:place><span style="mso-ansi-language: EN-US; mso-bidi-font-style: italic">Jodhpur</span></st1:place></st1:city><span style="mso-ansi-language: EN-US; mso-bidi-font-style: italic"> at 
            the time.<span style="mso-spacerun: yes">  </span>Mr. Koletkar 
            lived with his daughter who had married a Hindu businessman.<span style="mso-spacerun: yes">  </span>Aunt Betty visited him at 
            least once a week claiming that she knew him from childhood and that 
            her visits brought him some measure of joy.<o:p></o:p></span></p>
            <p>Mr. Koletkar's sons were informed about the death.<span style="mso-spacerun: yes">  </span>Ronnen's family, being the 
            only other Jewish family in town made all the arrangements.<span style="mso-spacerun: yes">  </span>His mother and Betty sewed 
            the burial clothes while his father bathed and dressed the 
            body.<span style="mso-spacerun: yes">  </span>Everything was 
            ready before the sons arrived next day.<o:p></o:p></span></p>
            <p>"We have decided on a cremation," Charlie, the elder son 
            said.<o:p></o:p></span></p>
            <p>"What!" Betty exploded. <o:p></o:p></span></p>
            <p>"We want to cremate the body," Ellis, the younger son 
            repeated.<o:p></o:p></span></p>
            <p>"Why?" Betty demanded.<o:p></o:p></span></p>
            <p>"We have no time for all the ceremonies that a burial 
            entails.<span style="mso-spacerun: yes">  </span>Also, we do 
            not have enough leave.<span style="mso-spacerun: yes">  
            </span>We have our jobs to consider and have to return to 
            work.”<o:p></o:p></span></p>
            <p>Betty lost control of her temper and her language.<span style="mso-spacerun: yes">  </span>"What utter bullshit!" she 
            said.<span style="mso-spacerun: yes">  </span>"I grew up in 
            this country and know that the Indian Army gives its officers two 

            <p>"Don't judge us without knowing our situation," Charlie said 
            before Ellis could react.<span style="mso-spacerun: yes">  
            </span>"There's no Jewish cemetery here. <span style="mso-spacerun: yes">  </span>There's no </span><i style="mso-bidi-font-style: normal"><span style="mso-ansi-language: EN-US">minyan</span></i><span style="mso-ansi-language: EN-US; mso-bidi-font-style: italic"> 
            either."<span style="mso-spacerun: yes">    
            </span><o:p></o:p></span></p>
            <p>"You are doing things for your own convenience.<span style="mso-spacerun: yes">  </span>You have two grown sons 
            each.<span style="mso-spacerun: yes">  </span>That makes four men and you could always have got somebody 

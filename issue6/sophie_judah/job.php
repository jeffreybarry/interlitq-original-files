            </span>They won't accept outsiders."<o:p></o:p></span></p>
            <p>"If you really want a Jewish funeral you can bury him in his 
            own garden."<o:p></o:p></span></p>
            <p>"We'd never be able to sell the property if there's a grave 
            in it," Charlie said.<o:p></o:p></span></p>
            <p>"Ah!<span style="mso-spacerun: yes">  </span>So it is 
            yourselves, not the dead person you are thinking 
            about."<o:p></o:p></span></p>
            <p>"Don't be ridiculous," Ellis said.<span style="mso-spacerun: yes">  </span>"It was daddy's wish.<span style="mso-spacerun: yes">  </span>He didn't want to trouble us 
            with all the ceremonies."<o:p></o:p></span></p>
            <p>"That's unimportant.<span style="mso-spacerun: yes">  
            </span>After a death it's the duty of the community to do the proper 
            Jewish thing.<span style="mso-spacerun: yes">  </span>He has no 

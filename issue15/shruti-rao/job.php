            <p>
We rushed past my city at 2am<br>
in winter's fog<br>
attacking the Skoda screen.<br>
Sitting next to me, my dad&#8212;<br>
journalist by day, superhero by night&#8212;<br>
hunched every so slightly <br>
over the wheel<br>
and I swear, I felt something leave him.</p>


<p>I wish I knew this city <br>
this city that seemed like my city<br>
this city of lonely stretches of vast roads<br>
jewelled by infinitely powered street lights<br>
(or maybe it was the Diwali)<br>
this city of alien people and hostel students&#8212;<br>
this city of splattered dogs on every blind curve<br>
of dead cars adorning dividers<br>
this city of friends who live alone.</p>

<p>The huge metro stations<br>
lining my vision every few minutes<br>
show me finally<br>
what Bombay people call culture shock&#8212;<br>
expansiveness;<br>
and I wonder how long she must have waited there<br>
&#8212;in the chill of a JNU winter<br>
with dogs in heat and sleepy security guards<br>
for company&#8212;<br>
to be held.</p>

<p>And in that moment,<br>
upon returning at 3am<br>
(one hour to save the world)<br>
in that moment,<br>
when I saw the drunkard,<br>
lay the epiphany.</p>

<p>Of reading and imagining<br>
thousands of drunkards on lonely nights<br>
and of really experiencing one.<br>
Of her father's mortality<br>
that threatened to strike upon her arrival at Guwahati<br>
and my father's mortality<br>
that had just entered conception,<br>
noiselessly.</p>

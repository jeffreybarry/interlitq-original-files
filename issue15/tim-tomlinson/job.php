<p><strong>French Quarter, 1974</strong></p>

<p>On Bourbon Street, a man who doesn't <br>
drink hawking his poems to a crowd </p>

<p>that doesn't read on a Friday night when <br>
the legs of a stripper poke through </p>

<p>a second storey window every seven <br>
seconds and the Runaway Kid nibbles </p>

<p>a 99¢ sukiyaki from a stick <br>
near a cart selling Lucky Dogs. &nbsp;</p>

<p>&ldquo;Get away from me, kid, you bother me,&rdquo;<br>
says the Lucky Dog salesman, as he krauts </p>

<p>up another dog for the overweight <br>
tourist the Kid panhandles for change. </p>

<p>The Runaway Kids&rsquo;s been missing <br>
for six weeks, his employers the local blood </p>

<p>banks, his arms a pin-cushion of missed hits <br>
and half-purple bruises.  &ldquo;If you don&rsquo;t buy,&rdquo; </p>

<p>the poet drones, &ldquo;I can&rsquo;t sell.&rdquo;  The Kid <br>
wipes his fingers on napkins he nicks </p>

<p>from Lucky Dogs, he pockets the change <br>
he cadged off the tourist, and he buys </p>

<p>a poem.  &ldquo;A love poem,&rdquo; he tells the poet, <br>

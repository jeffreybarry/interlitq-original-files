            <p><strong>Out West Where His Best Friend Suffers <br>
           Depression at Her Grandmother&rsquo;s Spread</strong></p>
           
            <p>In the morning the grandmother shows him <br>
            the rattlesnake twisted around the chicken </p>
             
            <p>wire, knotted up worse than a curtain cord, <br>
            back and over and through a dozen holes. </p>
            
            <p>&ldquo;It gets in,&rdquo; she says, &ldquo;but it can&rsquo;t wind out – <br>
            the head forgets where the tail&rsquo;s been.  Sound </p>
           
            <p>familiar?&rdquo;  He&rsquo;s just come from her <br>
            granddaughter&rsquo;s bedroom, cotton-mouthed and guilty </p>
           
            <p>as a wet dog slinking off a sofa. <br>
            They&rsquo;d made awkward, hungover love, the bed </p>
            
            <p>frame banging the walls. &ldquo;She&rsquo;s sick,&rdquo; <br>
            the grandmother continues, &ldquo;don&rsquo;t you know that?&rdquo; </p>
           
            <p>And before he says ma&rsquo;am she says, &ldquo;She thinks <br>
            her daddy raped her.  My son.&rdquo;  She stamps </p>
            
            <p>a foot on the dry ground, her white socks limp<br>
            at her slippers.  &ldquo;Is that what these doctors </p>
            
            <p>tell her?&rdquo;  He avoids the blaze of her eyes, <br>
            and she gives up.  With a weeder she pokes </p>
           
            <p>the snake.  It&rsquo;s too exhausted to rattle. <br>
            He says, &ldquo;I guess maybe I believe her.&rdquo;</p>
           
            <p>She says, &ldquo;You think so, do you?  Isn&rsquo;t that <br>
            loyal.&rdquo;  She turns for the house, muttering. </p>
            
            <p>&ldquo;Poking around in all them holes … getting <br>
            its damn self killed…&rdquo;  He finds a stick, tries </p>
           
            <p>to push the snake free, but his help digs wounds <br>
            into its flesh and flies land on the fresh blood. </p>
            
            <p>Red ants proceed up the wire.  Blackbirds <br>
            on the fence posts throw patient shadows.</p>

            <p LANG="en-US"> <strong>Simply Darwish, II: Black Rain Storm</strong></p>
            
            <p LANG="en-US"> The sky suddenly wreaks havoc upon us<br>
            And pours down clear from blackened clouds<br>
             Flooding the heart’s lingering drought</p>
           
            <p LANG="en-US"> The outside torrents rap bus windows<br>
            Matching my wild heart beating your name<br>
            And I take a volume of Darwish out to read</p>
            
            <p LANG="en-US"> The golden street lights after dusk <br>
            flashlight the window one by one<br>
            And crystal glow worms drip down my page</p>
            
            <p LANG="en-US"> Perhaps the couple seeking shelter has<br>
            A brittle love made small by passing rain<br>
            Or one so strong and rich it expands the sky</p>
            
            <p LANG="en-US"> Now the heavens are grey and blue and orange<br>
             Like cedar forests burning the distance,<br>
            Devised by a brilliant craftsman </p>
            
            <p LANG="en-US"> Black scraps of cloud criss-cross the sky<br>
            Like skinny wolves charging at the bloody moon <br>
            Suddenly stabbed by a jagged sword</p>
            
            <p LANG="en-US"> I wade home through water and mud<br>
           past brand new cascades dashing down<br>
            Water always binds me to your name</p>
           
            <p LANG="en-US"> I walk behind a singing soul diffusing <br>
            into darkened mystery, but Darwish whispers: <br>
            A simple black rain storm, no more, nor less</p>
            

<p><strong>Some Observations in a Coffee Shop in Suzhou</strong></p>
<p LANG="it-IT"> In a cozy café in Suzhou<br>
One can browse <em>The New Yorker</em> and Doris Day.<br>
 (Both old, incidentally)<br>
 Greece on the wall,<br>
 America on the table cloth,<br>
 Russia on the liquor list,<br>
 Hungary on the phonogram,<br>
 And a whole territory on your plate.</p>

<p> You put Mukherjee aside<br>
 to watch the trash man drop it all outside.<br>
 99% of those on motorbikes are female, <br>
 Plus one male with a boy.</p>

<p> They light an oil lamp for you,<br>
 to illuminate your continental feast<br>
 and your Darjeeling tea.</p>

<p> How many worries you have swallowed so far!<br>
 The shop owner lectures you on brown sugar versus Splenda.<br>
 You choose headache over hypoglycemia.</p>

<p> The printed table cloths tell humble stories<br>
 of Midwestern proprieties.<br>
 None of them is your story,<br>
 not even the one entitled &ldquo;All things grow with love.&rdquo;</p>

<p> Some grow without love.<br>
 And some in spite of love.<br>
 Like you.</p>

<p> Suspended between clocks and visions,<br>
 you know what is out there.<br>
 Hanshan Temple.<br>
 Red incense burning over grey skies.<br>
 Imaginary tranquility<br>
 of the kind life does not grant anymore.</p>

<p> The sugar twirls in your hot tea.<br>
 What has not settled now,<br>
 will not settle.<br>
 What has been done, cannot be undone.</p>


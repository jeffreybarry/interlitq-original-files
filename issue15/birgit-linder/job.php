<p LANG="en-US"> <strong>Rumi, or, My Heart is a Vagabond</strong></p>

Rumi is the master of love. <br>
He sent me to you.<br>
He said, &ldquo;Here is your journey.&rdquo;<br>
And I lost myself on the way. </p>

<p LANG="en-US"> You never came to look for me.<br>
 Because I am not the purest of the pure.<br>
 I am not Absence.<br>
 I am nowhere near Tebriz.</p>

<p LANG="en-US"> Because you are too busy searching<br>
 For your enlightenment,<br>
 Even if it only is in my love.</p>

<p LANG="en-US"> You look for the palace of the Sultan,<br>
But my love is an overgrown maze.</p>

<p LANG="en-US"> You search for agates and coral,<br>
But my heart is only flesh.</p>

<p LANG="en-US"> You wake the troublemaker</p>
 <p>And then let him play next to me.</p>

<p LANG="en-US"> You search for your soul in a sea full of pearls,<br>
And I only offered you crumbs of bread.</p>

<p LANG="en-US"> You look for eternal pleasure, joy, and life, </p>
<p LANG="en-US"> Even if it is only in my love.</p>

<p LANG="en-US"> You grow wings in your heart,<br>
But I am a white sparrow stuck in mud.</p>

<p LANG="en-US"> Now my heart is a vagabond,<br>
And if you look for it in me today,<br>
You won&rsquo;t see a trace of it. </p>

<p LANG="en-US"> Go, put your head on your pillow.<br>
Leave me burning.</p>

<p LANG="en-US"> I am already turning to smoke,<br>
Rising toward the sky.</p>

<p LANG="en-US"> Toward the sky that was supposed <br>
To expand your heart.</p>

<p LANG="en-US"> You are beautiful,<br>
Even if only it is in my love.</p>

<p LANG="en-US"> Go, rest your head on your pillow.<br>
Leave me to my own night.</p>

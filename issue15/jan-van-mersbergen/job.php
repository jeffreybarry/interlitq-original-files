            <td colspan="4" rowspan="2" class="texto2" valign="top">
            <br>
            
            <p>A boxer is running through the city. He heads down a street with tall buildings on either side, darts between parked cars, runs diagonally across a junction, down a bike path, crosses a bridge and follows the curve of the tram tracks. Anyone passing would think he was in training. But he’s running faster than usual. His breathing is out of control. His eyes are wide.</p>
            <p>His boxing boots fly silently over the pavement. Fragments of sentences echo around his head, accompanied by the ringing of a bell. Disconnected words thud against his eardrums, buzzing sounds, distorted, far away. Then suddenly they become clear.</p>
            <p>Stop. </p>
            <p>He lands a punch.</p>
            <p>Stop that!</p>
            <p>He lands another punch. Again he hears a bell, sharper and louder than before. Stop, someone screams. He feels a hand on his shoulder, fends it off with a jab of his elbow. He throws a left hook, hits the man square in the face and turns back to his opponent. </p>
            <p>Stop that! he hears again. He lands another punch, and another, and another. </p>
            <p>He crosses a busy main road and runs into a park. He comes to a patch of grass with a bronze statue in the centre, a woman holding a child in the air as though she wants to entrust it to the clouds.</p>
            <p>The boxer slows, panting, and looks at the statue. He sits down on a bench. The bushes and trees stand motionless between him and the street with the tramlines. Dark grey clouds slide past behind the trees. There are no birds, not even pigeons.</p>
            <p>He feels fine drops of rain on his face. The leaves on the trees move gently in the breeze. A man in a denim jacket is standing on the other side of the park, beneath the awning of the cigar shop on the corner. He’s looking in the boxer’s direction. Another man comes out of the shop, lights a cigarette, and says something to the man in the denim jacket, who replies without taking his eyes off the boxer. The smoke dissolves in the air. The boxer looks down at his legs and at the wood of the bench, as it slowly darkens in the rain.</p>
            <p>He hears footsteps. For a moment, he seems resigned to his fate. He waits for a deep voice to say something, to speak his name, to pin him to the bench. When it comes, the tone isn’t what he expected: Hey, you’re Danny Clare, aren’t you? </p>
            <p>The man walks over and stands in front of him, turns up the collar of his denim jacket. The other man stops behind his friend, off to one side. With no expression on his face, the boxer looks at the two men.</p>
            <p>You are him though, aren’t you? The boxer?</p>
            <p>Danny gets up.</p>
            <p>We saw you, says the man in the denim jacket. He tugs at his collar again, trying to shield his neck from the rain.</p>
            <p>Against that big blond guy, it was. The Hungarian.</p>
            <p>The other man corrects him: Bulgarian.</p>
            <p>Danny doesn’t react. He just clasps his hands. </p>
            <p>Good fight, that was.</p>
            <p>The cigarette falls to the wet gravel and the man crushes it with his foot. The two men smile at the boxer. The man in the denim jacket says something else, but his voice fades away and Danny looks down at the cigarette butt, which is still smouldering, and then at his feet. Now he can hear words from his conversation with Pavel, at the boxing school. And there’s that click in his head again, when it all fell into place, and the click that came afterwards when everything around him imploded and went black. </p>
            <p>I don’t know what you’re talking about, he says. He runs to the park exit, leaving the men and the statue behind. He goes through the gate, crosses the tramlines and races along the brick wall and around the corner. Finally, he reaches a busy dual carriageway, with an endless stream of cars flowing out of the city. That’s the road he wants. The rain sweeps against his face. He runs past a supermarket and sees a black kid pushing a line of shopping trolleys inside. He passes beneath a viaduct with drops of rainwater clinging to its solid metal girders. Reflections of the posters on the walls ripple dimly in the puddles. He stops in the shelter of a tree by a big roundabout. On his right, a railway line hangs high above the street. He sees the station just beyond the roundabout. A long train is pulling in, its wheels screeching. The boxer puts his hands in his pockets. His keys, his loose change, his mobile – it’s all still in the changing room at the boxing school.</p>
            <p>The traffic spins around the roundabout and fans out along the roads leading to and from the city. He takes the road to the motorway. He crosses over, walks through the long grass in the centre of the roundabout, waits for a gap in the traffic, crosses again, stands by the roadside and raises his thumb. A car soon stops for him. There’s an old man at the wheel. I can take you a few kilometres down the motorway, he says.</p>
            <p>The boxer nods and gets in.</p>
            <p>I’ll drop you off at the petrol station. You’ll be able to get another ride from there, no problem.</p>
            <p>The man accelerates gently, navigates a few bends and heads onto the motorway. Opera plays on the radio. The voice pierces through the noise of the engine. When Danny looks at the radio, the man turns the knob and the music becomes louder. The voice grates on his nerves. They sit in silence for a few minutes. Then the man takes the exit for the petrol station. When they reach the pumps, Danny thanks him and steps out of the car into the smell of petrol.</p>
            <p>You’re welcome, says the man. </p>
            <p>Danny slams the car door.</p>

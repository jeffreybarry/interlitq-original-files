            <p ALIGN="RIGHT"> <em>Un nombre y la fecha de un origen en el viejo documento.</em><br><em>Identidad andante que junto a los otros papeles acomodé sin<br> dificultad dentro de mi bolso, luego en mi valija y ya en casa, junto<br> a mis más queridos recuerdos. Me gusta pensar que ella me eligió<br> cuando los olvidó o los extravió; quién puede saberlo.</em></p>
            
            <p ALIGN="JUSTIFY">Bratislava, 1997 </p>
           
            <p ALIGN="JUSTIFY"><em>A principios del siglo, el turismo vienés consideraba Bratislava casi un agradable suburbio, al que se podía llegar en menos de una hora para paladear sus vinos blancos…, </em>mis ojos se detuvieron en las últimas palabras de esa nota que me entregaron al subir al tren; la sed y la necesidad de una copa de vino me quemaron de pronto la garganta. Por la ventanilla ya se dibujaba la primera curva que llevaba a la estación. Miré mi reloj: la hora perfecta para cenar; y me colgué del hombro el bolso preparada para descender. </p>
            <p ALIGN="JUSTIFY">Antes de salir de la estación, busqué un puesto de revistas para conseguir una guía de la ciudad. No tenían ninguna, pero me señalaron un pequeño negocio donde podía comprarla. Allí, entre muchos otros libros, casi perdida, encontré una pequeña, desgastada, con ilustraciones antiguas y en blanco y negro. Era la única escrita en español y eso me terminó de convencer para comprarla. </p>
            <p ALIGN="JUSTIFY">Busqué un taxi que me condujera al centro y pronto pude divisar los contornos de la ciudad del otro lado del Danubio. El río me recibía majestuoso, iluminado por el reflejo del antiguo castillo. Crucé el puente Nový Most, atraída por los olores de la maravillosa noche de primavera, y observé las primeras calles de la Ciudad Vieja.</p>
            <p ALIGN="JUSTIFY">Pensé otra vez qué es lo que hacía allí, en ese lugar. </p>
            <p ALIGN="JUSTIFY">Al organizar mi itinerario, no había, ni siquiera por un instante, reparado en ese punto de la Europa Central, presente sin embargo en mi ruta hacia Budapest. Pero algo me había llevado a tomar la decisión sobre la marcha. Y lo cierto es que había sido así. Sin meditarlo demasiado, en un impulso que no supe detener y que surgió de improviso frente a la pequeña ventanilla de la estación de trenes en Praga: ¿por qué no quedarme dos noches en Bratislava? </p>
            <p ALIGN="JUSTIFY">Y ahí estaba, feliz, dejándome conducir por las calles de la antigua ciudad con agradecida libertad, dejándome llevar por un destino imprevisible sin importarme lo que resultara de él. </p>
            <p ALIGN="JUSTIFY">A veces ocurría así, solo se trataba de seguir avanzando.</p>
            <p ALIGN="JUSTIFY">Mis pensamientos se detuvieron junto con el taxi frente la puerta de mi hotel. Fue en ese momento, al pagar el viaje, cuando me di cuenta de que aún tenía la guía entre mis manos. Nunca la había soltado desde la compra, como si mis manos se hubieran adherido, casi hundidas, a ella. La quise apoyar a un costado pero, al liberarla de mis dedos, se desarmó sobre mis piernas, al modo de las hojas de un árbol seco, y entonces aparecieron esos papeles, doblados y grisáceos y un viejo documento.</p>
           
            <p ALIGN="JUSTIFY">… <em>No hubo despedida, no hubo adiós, </em>decía el comienzo de uno de ellos<em>.</em></p>
          
            <p ALIGN="JUSTIFY">Me quedé mirando todo aquello sin entender, mientras el conductor me señalaba que debía bajar del taxi. Sin embargo, en vez de entrar al hotel, caminé arrastrando mi valija, con la guía desordenada y apretada entre mis manos, hasta el final de la calle donde había visto minutos antes algunos bares. Me senté en una de las mesas y pedí una copa de vino; y por sugerencia del mozo, una porción de <em>piroggi. </em>Murmullos de la nueva lengua, desconocida, me acompañaban como una cadencia, a tono con el desconcierto que me provocaba la voz que había comenzado a emerger de esos papeles.</p>
            <p ALIGN="JUSTIFY">Los acomodé sobre la mesa mientras a un costado apoyaba con cuidado la guía, y continué leyendo.</p>
            
            <p ALIGN="JUSTIFY">…<em>No hubo despedida, no hubo adiós; no pudo ser posible. Tampoco hay fecha de regreso o de reencuentro. No sé si podré vivir con ese sentimiento, aunque lo intentaré. Se lo he prometido y cumpliré con mi palabra. No debo imaginar nuestras ausencias recorriendo como fantasmas los rincones de nuestra casa. Comprendo que los años que vienen serán muy difíciles. ¿Cómo encontraré las fuerzas? </em></p>
            
            <p ALIGN="JUSTIFY">Revisé el resto,</p>
           
            <p ALIGN="JUSTIFY"><em>Él me lo pidió en su última carta y aquí estoy, ya en camino. Desgarrada por dentro y por fuera. Pero, ¿qué sentido tenía quedarme? Pronto será muy difícil viajar, las cosas empeoran cada día. Esta guerra ya nos ha separado tanto…</em></p>
           
            <p ALIGN="JUSTIFY">sólo pedazos de papeles </p>
            
            <p ALIGN="JUSTIFY">… <em>decirle que ya he iniciado este viaje, tantas veces conversado, para regresar a España. Me gustaría poder contarle que me he visto obligada a detenerme en Bratislava. Dormí algunas horas y recién desperté cuando el silbato del tren anunció la llegada a Hlavna Stanica. Hace frío, lo siento en cada uno de mis dedos abrigados con los guantes que él me regalara. Pero no es solo el frío intenso de esta ciudad, sino el que también corre, sin abandonarme, por mi cuerpo. Me veo en la urgencia de  moverme para no morir congelada. </em></p>
            
            <p ALIGN="JUSTIFY">inconclusos.</p>
          
            <p ALIGN="JUSTIFY"><em>Después de más de un día entero sin comer, siento el dolor que me provoca el hambre. Debo aguardar una hora aquí antes de que el tren abandone la estación. No tengo demasiado dinero ni sé si mis pies extraviados en estos zapatos viejos me lo permitirán, pero intentaré caminar hasta encontrar algún puesto abierto. Tampoco debo alejarme. No conozco el idioma. Según esta guía, el centro de la ciudad no se encuentra muy lejos. Por lo pronto, intentaré no perderme en la oscuridad de las calles sinuosas de Bratislava. Me aferro a esta libreta; a su foto, mi único recuerdo; a estos papeles…</em></p>
           
            <p ALIGN="JUSTIFY">Han pasado varios años ya desde aquella primavera. </p>
            <p ALIGN="JUSTIFY">Aun así he conservado todo: los pequeños pedazos de papel, cada trozo de hoja, el viejo documento y por supuesto, la guía de Bratislava. </p>
            <p ALIGN="JUSTIFY">No he revelado –como si se tratara de una confesión− lo que supe por ellos, ni a nadie le he dicho lo que ella además me contó esa noche.</p>
            <p ALIGN="JUSTIFY">Desde aquella primavera he sido fiel a ese secreto.</p>

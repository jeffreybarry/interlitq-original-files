              <p><span class="titulo2"><a name="spanish">Estimado Invitado</a>,</span></p>
              
            <p>Como Presidente de <em>The International Literary Quarterly</em>, me es un placer darle la bienvenida al número 15 de la revista.</p>
            
 <p>Respecto a las adiciones al equipo de redacción de <em>Interlitq</em>, el consejo editorial aprovecha esta oportunidad de introducirle a <a class="link1" href="http://interlitq.org/staff/neil_langdon_inglis/bio.php">Neil Langdon Inglis</a> quien ejercerá a partir de ahora como el Editor Adjunto de la revista, y a <a class="link1" href="http://interlitq.org/staff/sara-besserman/bio.php">Sara Besserman</a>, <a class="link1" href="http://interlitq.org/staff/conor-bracken/bio.php">Conor Bracken</a>, <a class="link1" href="http://interlitq.org/staff/krista-oehlke/bio.php">Krista Oehlke</a> y <a class="link1" href="http://interlitq.org/staff/stephanie-smith/bio.php">Stephanie Smith</a> como Editores Asistentes.</p>
 
 
<p>En cuanto a las contribuciones a la edición 15, espero que disfrute de la poesía evocativa de <a class="link1" href="http://interlitq.org/issue15/birgit-linder/bio.php">Birgit Linder</a>, <a class="link1" href="http://interlitq.org/issue13-2/geraldine-maxwell/bio.php">Geraldine Maxwell</a>, <a class="link1" href="http://interlitq.org/issue15/zosimo-quibilan/bio.php">Zosimo Quibilan, Jr.</a>, <a class="link1" href="http://interlitq.org/issue15/shruti-rao/bio.php">Shruti Rao</a>, <a class="link1" href="http://interlitq.org/issue15/ian-smith/bio.php">Ian C. Smith</a>, <a class="link1" href="http://interlitq.org/issue15/jose-flore-tappy/bio.php">José-Flore Tappy</a>, <a class="link1" href="http://interlitq.org/issue11/john-taylor/bio.php">John Taylor</a>, <a class="link1" href="http://interlitq.org/issue15/tim-tomlinson/bio.php">Tim Tomlinson</a> y <a class="link1" href="http://interlitq.org/issue15/joe-wiinikka-lydon/bio.php">Joe Wiinikka-Lydon;</a> y la prosa inteligente y con fuerza de <a class="link1" href="http://interlitq.org/issue15/claudia-apablaza/bio.php">Claudia Apablaza</a>, <a class="link1" href="http://interlitq.org/issue15/maria-claudia-otsubo/bio.php">María Claudia Otsubo</a>, <a class="link1" href="http://interlitq.org/issue15/joost-vandecasteele/bio.php">Joost Vandecasteele</a>, <a class="link1" href="http://interlitq.org/issue15/jan-van-mersbergen/bio.php">Jan Van Mersbergen</a> y <a class="link1" href="http://interlitq.org/issue15/laura-watkinson/bio.php">Laura Watkinson</a>,  todos estos textos perfectamente complementados por las ilustraciones de <a class="link1" href="http://interlitq.org/issue15/alexis-hunter/bio.php">Alexis Hunter</a>.</p> 

<p>Quisiera también aprovechar esta oportunidad para invitarlos a contribuir con una donación para <em>The International Literary Quarterly</em>, una organización sin fines de lucro en el estado de Nueva York. Desde su inicio en noviembre del 2007, esta revista ha publicado el trabajo tanto de Nobeles Laureates como de autores emergentes en más de noventa idiomas, en su búsqueda por hacer que la mejor literatura este al alcance de todos. No importa el lugar del planeta donde nos encontremos, con un simple teclear de dedos podemos acceder a ella. </p>

<p>Debido a que el acceso a <em>Interlitq</em> es absolutamente gratuito y a que el sitio esta exclusivamente dedicado a la literatura sin ningún tipo de publicidad o sponsor, y hasta la fecha sin ninguna institución que la patrocine; resulta vital comprender que las donaciones son esenciales para el sustento de la revista. Muchas gracias por ayudarnos a garantizar la continuidad de Interlitq en la incansable labor de promover y cultivar la literatura internacional y de esta manera ser partícipes, a través del poder de la literatura, de la hazaña de romper aquellas barreras todavía existentes entre nosotros.</p>
 
<p>Espero que disfrute del número 15 de <em>Interlitq</em>, y que vuelve a visitarnos en Agosto 2011 cuando la edición 16 será publicada, incluyendo textos de escritores de Nueva Zelanda y otros partes del mundo.</p>
<p>Con mis saludos cordiales,</p>             
       
       <p><a class="link1" href="http://www.interlitq.org/staff/peter_robertson/bio.php">Peter Robertson</a></p>
        

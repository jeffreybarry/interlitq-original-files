           <p>  <span lang="EN-US">As the President of <em>Interlitq</em>, I am delighted to welcome you to Issue 15.</span> </p> 
          
           <p>With regard to new additions to <em>Interlitq's</em> editorial team, the Board of Editors would like to take this opportunity to welcome <a class="link1" href="http://interlitq.org/staff/neil_langdon_inglis/bio.php">Neil Langdon Inglis</a>, previously an Associate Editor of the review, as <em>Interlitq's</em> Deputy Editor with effect from this issue, and <a class="link1" href="http://interlitq.org/staff/sara-besserman/bio.php">Sara Besserman</a>, <a class="link1" href="http://interlitq.org/staff/conor-bracken/bio.php">Conor Bracken</a>, <a class="link1" href="http://interlitq.org/staff/krista-oehlke/bio.php">Krista Oehlke</a> and <a class="link1" href="http://interlitq.org/staff/stephanie-smith/bio.php">Stephanie Smith</a> as newly-appointed Assistant Editors.</p>
              
              <p>In terms of contributors to Issue 15, I hope that you will enjoy the evocative poetry of <a class="link1" href="http://interlitq.org/issue15/birgit-linder/bio.php">Birgit Linder</a>, <a class="link1" href="http://interlitq.org/issue13-2/geraldine-maxwell/bio.php">Geraldine Maxwell</a>, <a class="link1" href="http://interlitq.org/issue15/zosimo-quibilan/bio.php">Zosimo Quibilan, Jr.</a>, <a class="link1" href="http://interlitq.org/issue15/shruti-rao/bio.php">Shruti Rao</a>, <a class="link1" href="http://interlitq.org/issue15/ian-smith/bio.php">Ian C. Smith</a>, <a class="link1" href="http://interlitq.org/issue15/jose-flore-tappy/bio.php">José-Flore Tappy</a>, <a class="link1" href="http://interlitq.org/issue11/john-taylor/bio.php">John Taylor</a>, <a class="link1" href="http://interlitq.org/issue15/tim-tomlinson/bio.php">Tim Tomlinson</a> and <a class="link1" href="http://interlitq.org/issue15/joe-wiinikka-lydon/bio.php">Joe Wiinikka-Lydon</a>; and the intelligent and compelling prose of <a class="link1" href="http://interlitq.org/issue15/claudia-apablaza/bio.php">Claudia Apablaza</a>, <a class="link1" href="http://interlitq.org/issue15/maria-claudia-otsubo/bio.php">María Claudia Otsubo</a>, <a class="link1" href="http://interlitq.org/issue15/joost-vandecasteele/bio.php">Joost Vandecasteele</a>, <a class="link1" href="http://interlitq.org/issue15/jan-van-mersbergen/bio.php">Jan Van Mersbergen</a> and <a class="link1" href="http://interlitq.org/issue15/laura-watkinson/bio.php">Laura Watkinson</a>, and all of these texts embellished by the arresting artwork of <a class="link1" href="http://interlitq.org/issue15/alexis-hunter/bio.php">Alexis Hunter</a>.</p>
               

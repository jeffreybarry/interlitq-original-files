            <p>
              <strong>Vindstille</strong></p>
            <p>I have my memories, some award winners<br>
            even if they lack the charm of a Doisneau<br>
            also, mementoes of trepid exploration.<br>
            One, an example of good composition<br>
            always a comfort, I keep near my bed.<br>
            A woman stands on a jetty of rocks<br>
            holding an infant, her back to camera<br>
            motionless, facing a lake, or sea.<br>
            Clouds bank behind a distant boat.<br>
            The silence is a perfect example of art<br>
            in this early Scandinavian monochrome.<br>
            Mother and child.  Journey’s end or beginning.</p>
           
            <p>I bought it in Denmark, driving from Norway.<br>
            Later, just off a ferry, near Lubeck<br>
            I captured heavy holidaying Germans<br>
            in a vast caravan park where it rained.<br>
            My tent humid, gear now locked in the car<br>
            I plodded around puddles to the laundry<br>
            where I tried to translate instructions.<br>
            I ended up screaming at a machine.<br>
            Watching hausfraus,  obese twins, sniggered.<br>
            Ja, ja, I muttered, thinking of Arbus<br>
            as I bundled up my pathetic smalls.</p>
          
            <p>My decrepit car tracked history’s map crabwise<br>
            amusing Europe’s posing border guards<br>
            their sneers echoing those lumpen twins’.<br>
            I cut myself opening a can of beans<br>
            bad news buzzing me again in the fast lane.<br>
            When I was no longer the slowest driver<br>
            East German police tailed me over potholes<br>
            past Leipzig’s cindering orange haze<br>
            after I shot their helicopter in a car park<br>
            my thoughts by then U-turning for home.<br>
            Was it Auden who said photography<br>
            brought a new sadness to the world?</p>
            
            <p>With my precious lenses in leather pouches<br>
            and the knowledge that when the eye blinks<br>
            it sucks part of someone else inside it<br>
            as well as part of himself at that moment<br>
            I carried that woman and child in my pack<br>
            wrapped like my equipment and damaged hand<br>
            a talisman during those <em>Sturm und Drang </em>days.<br>
            It encourages me when opportunity lurches<br>
            as I round a corner of the human map.<br>
            I concentrate on the miracle of light.<br>
            Yes, that, and timing and silver emulsion. </p>

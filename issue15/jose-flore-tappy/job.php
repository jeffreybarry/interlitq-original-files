<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<em>à la femme de Loth</em></p>

<p>Parfois sans raison<br>
traînée de cigare sur la terre froide<br>
cette même ombre me désigne du doigt<br>
inerte<br>
à moi-même étrangère</p>

<p>*</p>

<p>Comment guérir<br>
mettre en plâtre<br>
cette fracture<br>
comment tenir<br>
l&rsquo;incoercible</p>

<p>Bander étroitement<br>
les deux parts de moi-même<br>
serrer dur<br>
le présent le passé<br>
unir<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; de force<br>
ces deux moitiés<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; brisées</p>

<p>Méprise</p>

<p>*</p>

<p>La face<br>
tranchée<br>
un profil pour hier<br>
l&rsquo;autre pour demain</p>

<p>Borgne<br>
chaque fois<br>
derrière le bandeau noir<br>
d&rsquo;une vie disjointe</p>

<p>*</p>

<p>Trop osciller<br>
 &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;à démis<br>
l&rsquo;os<br>
 &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;de ma raison</p>

<p>folie&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;   folie</p>

<p>Les pensées vrillent<br>
dans leur chant<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; dissonant</p>

<p>*</p>

<p>Faut-il tenir l&rsquo;espoir par l&rsquo;anse pour ne pas le briser&nbsp;?</p>

<p>*</p>

<p>Si vétuste la lumière<br>
à peine quelques grains<br>
jetés<br>
dans la main des vivants</p>

<p>Bienheureuses<br>
les poules<br>
sans esprit</p>

<p>moi je picore<br>
les restes avariés<br>
d&rsquo;un vieux rêve<br>
passé<br>
que rien ne peut me faire<br>
lâcher</p>

<p>*</p>

<p>Dans ma chair la mémoire<br>
ses dents de lait<br>
qui mordent encore<br>
voraces<br>
laissant la marque à vif<br>
des premiers jours<br>
d&rsquo;alarme</p>

<p>*</p>

<p>Un seul regard une seule<br>
pensée tournés vers<br>
l&rsquo;arrière<br>
à jamais glacent<br>
les plus brûlants désirs</p>

<p>Ses yeux remplis<br>
de froids cristaux</p>

<p>On lui crache au visage<br>
on lui crache aux genoux<br>
pétrifiés</p>

<p>on passe outre</p>

<p>*</p>

<p>Ma tête<br>
qu&rsquo;une seule<br>
goutte de ta voix<br>
creuse<br>
jusqu&rsquo;au supplice<br>
ma tête frappe<br>
les murs trop rapprochés<br>
des jours</p>

<p>vainement</p>

<p>Coups sur coups<br>
jamais ne brisent<br>
le fer de la hantise</p>

<p>*</p>

<p>Est-ce le vent<br>
à la poursuite du vent<br>
ou le cri de l&rsquo;absence<br>
qui court qui siffle<br>
dans mes veines&nbsp;?</p>

<p>*</p>

<p>Nos pas frottent frottent<br>
s&rsquo;obstinent à frotter<br>
e leur cuir usé<br>
une tache<br>
que nulle pluie n&rsquo;efface</p>

<p>*</p>

<p>Impatiente<br>
je frapperai des talons<br>
sur la terre sonore<br>
battrai le rythme<br>
sur ce tambour de guerre<br>
ferai trembler la peur</p>

<p>Oh que mes mains mes pieds<br>
pétrissent tout ce silence<br>
en boule en projectile</p>

<p>*</p>

<p>Le soleil a sorti ses couteaux<br>
les tourne les retourne<br>
s&rsquo;amuse<br>
à les lancer<br>
tout autour de nos têtes<br>
éblouissant métal<br>
au tranchant qui miroite</p>

<p>*</p>

<p>Puisse le sol<br>
casqué d&rsquo;or d&rsquo;écarlate<br>
cloche ardente<br>
retentir sous mes pas<br>
porter loin ma fureur<br>
et réveiller les ombres</p>

<p>Qu&rsquo;il vibre<br>
dans l&rsquo;espace désert<br>
ouvrant toutes grandes<br>
les portes de la mer</p>

<p>*</p>

<p>Mais ta douce tyrannie<br>
à ma gorge se noue<br>
tire son lacet<br>
sur le cœur qui plie<br>
brise d&rsquo;un coup net<br>
le souffle</p>

<p>*</p>

<p>Roussie la terre<br>
rabougries les pousses<br>
que la poussière étouffe</p>

<p>C&rsquo;est la pierre crue<br>
au goût de fer et de mazout<br>
c&rsquo;est l&rsquo;ortie<br>
qui nous remplit la bouche<br>
c&rsquo;est la racine d&rsquo;amertume et<br>
l&rsquo;épine d&rsquo;acacia</p>

<p>*</p>

<p>Mon ombre<br>
à l&rsquo;aventure<br>
divague<br>
silhouette somnambule</p>

<p>D&rsquo;aller venir<br>
à force d&rsquo;errer<br>
finira-t-elle<br>
un jour<br>
par user — l&rsquo;égarée —<br>
sa pointe sa mine obscure<br>
à l&rsquo;arête vive des pierres</p>

<p>crayon fébrile qu&rsquo;aucune main<br>
ne dirige</p>

<p>*</p>

<p>Les jardins ont mis<br>
leurs gants de givre<br>
aux clochettes rouillées</p>

<p>absence absence<br>
j&rsquo;épelle<br>
le rien<br>
j&rsquo;ânonne</p>

<p>ici&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;là-bas<br>
de moi&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;    à moi<br>
chancelantes syllabes</p>

<p>et glisse l&rsquo;échevelée<br>
folie entre mes doigts<br>
livides</p>

<p>*</p>

<p>Venimeux les désirs<br>
qui pourrissent</p>

<p>À reculons<br>
je fuis</p>

<p>Sous mes pieds<br>
la trappe</p>

<p>dépossession</p>

<p>*</p>

<p>Chassée <br>
des maisons familières<br>
lépreuse<br>
je hante<br>
l&rsquo;avers et le revers<br>
des terres</p>

<p>dépossédée<br>
du peu du tout<br>
chair vive<br>
sous le fléau du vent</p>

<p>*</p>

<p>Et pourtant et pourtant<br>
la lumière sans discontinuer<br>
passe et repasse ses plumes<br>
sur nos nuques raidies</p>

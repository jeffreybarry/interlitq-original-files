            <p LANG="en-US">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<em>to Lot&rsquo;s wife</em></p>
          
            <p LANG="en-US">Sometimes for no reason<br>
            a trail of cigar ashes on the cold dirt<br>
            this same shadow points its finger at me<br>
            lifeless<br>
            a stranger to myself</p>
           
            <p LANG="en-US">*</p>
           
            <p LANG="en-US">How to heal<br>
            put a cast on<br>
            this fracture<br>
            how to hold <br>
            the uncontrollable</p>
           
            <p LANG="en-US">Bandage closely<br>
            my two parts<br>
            tighten hard<br>
            the past the present<br>
            join<br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;by force<br>
            these two shattered<br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;halves</p>
            
            <p LANG="en-US">Mistake</p>
            
            <p LANG="en-US">*</p>
            
            <p LANG="en-US">The severed<br>
            face<br>
            one profile for yesterday<br>
            the other for tomorrow</p>
           
            <p LANG="en-US">Each time<br>
            one eye sightless<br>
            behind the black blindfold<br>
            of a divided life</p>
            
            <p LANG="en-US">*</p>
            
            <p LANG="en-US">Wavering too much<br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;has put the bone<br>
            of my mind<br>
             &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;out of joint</p>
           
            <p LANG="en-US">madness&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; madness</p>
           
            <p LANG="en-US">My thoughts spin<br>
            in their dissonant<br>
            song</p>
            
            <p LANG="en-US">*</p>
           
            <p LANG="en-US">Must hope be held by the handle so it won&rsquo;t be shattered?</p>
            
            <p LANG="en-US">*</p>
            
            <p LANG="en-US">The light so run-down<br>
            barely a few seeds<br>
            tossed<br>
            into the hands of the living</p>
            
            <p LANG="en-US">Lucky are<br>
            mindless<br>
            hens</p>
           
            <p LANG="en-US">I peck about<br>
            the rotting remains<br>
            of a long gone<br>
            old dream<br>
            nothing can make me<br>
            release</p>
            
            <p LANG="en-US">*</p>
            
            <p LANG="en-US">In my flesh memory&rsquo;s<br>
            milk teeth<br>
            still biting<br>
            voraciously<br>
            leaving an open wound<br>
            the first days<br>
            of alarm</p>
           
            <p LANG="en-US">*</p>
            
            <p LANG="en-US">Looking or<br>
            thinking back<br>
            just once<br>
            chill forever<br>
            the most burning desires</p>
            
            <p LANG="en-US">Her eyes full<br>
           of cold crystals</p>
           
            <p LANG="en-US">You spit in her face<br>
            you spit on her petrified <br>
            knees</p>
          
            <p LANG="en-US">you walk past her</p>
           
            <p LANG="en-US">*</p>
            
            <p LANG="en-US">My head<br>
            that a single<br>
            drop of your voice<br>
            digs into<br>
            like a torture<br>
            my head hitting<br>
            walls of days<br>
            too close to each other</p>
           
            <p LANG="en-US">in vain</p>
            
            <p LANG="en-US">Thud after thud<br>
            never shatter<br>
            the iron of dread</p>
           
            <p LANG="en-US">*</p>
           
            <p LANG="en-US">Is it the wind<br>
            chasing the wind<br>
            or the shriek of absence<br>
            running whistling<br>
            in my veins?</p>
           
            <p LANG="en-US">*</p>
            
            <p LANG="en-US">Our footsteps rub rub<br>
            keep on rubbing<br>
            with their worn leather<br>
            a stain<br>
            no rainfall can erase</p>
           
            <p LANG="en-US">*</p>
            
            <p LANG="en-US">I will impatiently tap<br>
            the resonant ground<br>
            with my heels<br>
            beat out the rhythm<br>
            on this war drum<br>
            make fear quiver</p>
           
            <p LANG="en-US">Oh may my hands my feet<br>
            knead all this silence<br>
            into a ball into a projectile</p>
           
            <p LANG="en-US">*</p>
           
            <p LANG="en-US">The sun has drawn its knives<br>
            turns and twists them <br>
            takes fun<br>
            throwing them <br>
            all around our heads<br>
            dazzling metal<br>
            shimmering blades</p>
            
            <p LANG="en-US">*</p>
            
            <p LANG="en-US">May the ground<br>
            helmeted in gold in scarlet<br>
            ardent bell<br>
            ring out beneath my footsteps<br>
            carry my fury afar<br>
            and waken the shadows</p>
           
            <p LANG="en-US">May it quake<br>
            in the empty vastness <br>
            opening wide<br>
            the gates of the sea</p>
            
            <p LANG="en-US">*</p>
           
            <p LANG="en-US">But your sweet tyranny<br>
            knots a lump at my throat<br>
            tightens its shoelace <br>
            over my heart that gives in<br>
            crushes with a clear blow<br>
            my breath to bits</p>
           
            <p LANG="en-US">*</p>
            
            <p LANG="en-US">The scorched earth<br>
            the stunted sprouts<br>
            choked by dust</p>
            
            <p LANG="en-US">It&rsquo;s raw stones<br>
            tasting of iron and heating oil<br>
            it&rsquo;s nettles<br>
            filling our mouths<br>
            it&rsquo;s roots of bitterness and<br>
            thorns of acacias</p>
            
            <p LANG="en-US">*</p>
            
            <p LANG="en-US">My shadow<br>
            roams<br>
            rambles<br>
            a sleepwalking silhouette</p>
           
            <p LANG="en-US">By dint of coming<br>
            going wandering astray<br>
            will my shadow<br>
            one day <br>
            end up wearing down<br>
            its point its obscure lead<br>
            on the sharp spines of stones</p>
           
            <p LANG="en-US">feverish pencil that no hand<br>
            guides</p>
           
            <p LANG="en-US">*</p>
           
            <p LANG="en-US">The gardens have donned their<br>
            rusty-belled <br>
            frosty gloves</p>
          
            <p LANG="en-US">absence absence<br>
            I spell out<br>
            nothingness<br>
            I mumble</p>
            
            <p LANG="en-US">here&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;over there<br>
            from me&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;   to me<br>
            wobbly syllables</p>
           
            <p LANG="en-US">and slip the frenzied<br>
            madness between my livid<br>
            fingers</p>
            
            <p LANG="en-US">*</p>
            
            <p LANG="en-US">Venomous any desire<br>
            that rots</p>
            
            <p LANG="en-US">I flee<br>
            backwards</p>
           
            <p LANG="en-US">Beneath my feet<br>
            the trapdoor</p>
          
            <p LANG="en-US">dispossession</p>
          
            <p LANG="en-US">*</p>
            
            <p LANG="en-US">Chased<br>
            from familiar homes<br>
            leprous<br>
            I haunt<br>
            the obverse and the reverse<br>
            of the land</p>
             
            <p LANG="en-US">am stripped <br>
            of little of everything </SPAN><br>
            bared flesh<br>
            beneath the wind&rsquo;s scourge</p>
            
            <p LANG="en-US">*</p>
             
            <p LANG="en-US">And yet and yet the light<br>
            never ceases brushing<br>
            its feathers back and forth<br>
            on our stiffened necks</p>

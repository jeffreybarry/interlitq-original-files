<p><strong>I Think of You and Think of Skin</strong></p>


<p>I think of you and think of skin –<br>
its soft stretch, ways it may fold, or be pressed,<br>
occasionally tear. How it absorbs the fix it’s in,<br>
ends up not innocent, unkissed, yet simple in its rest -<br><br>

your father’s work, to expose such by the sun, <br>
harried and tawed and dressed for the intent<br>
of its disguise with musks and roses. For you, years gone,<br>
I’m pale now, peeled, unwritten-on, a reproach unmeant -<br><br>

white widow in my quilled intransigence, a swan<br>
alone. Days such as these, late summer of my life,<br>
I wonder at this want to turn a yearling’s tongue<br>
to subtleties so hollow, telling tender lies –<br><br>

those nights you travelled me with words, blood-nibbed<br>
and burning, tell me I was your first, flawed, outlawed script.<br><br>


<em>John Shakespeare, William’s father, was a ‘whyttawer’ ( worker in white leather)
who made gloves  from calf, kid, lamb and rabbit skins so fine they were almost translucent. He might spend up to a year preparing the hides before beginning his work. The sonnet is in Anne Hathaway’s voice.</em><br><br><br></p>






<p><strong>At Chios, Abandoned Leprosarium</strong></p>


In another life I lived<br>
somewhere like this:<br>
the white stones on the path<br>
eyeless as birds’ eggs,<br>
sweet airs of sea and pinewoods,<br>
courtyards of window shutters and shade.<br>
Quiet as if only a moment since<br>
children, playing, had been called in.<br><br>

There was love here, companionship,<br>
gold charred slowly into flesh. No need<br>
for hiding from us hidden. But now careless light<br>
petrifies everything. <br><br>

From room to room unrolled mattresses,<br>
snowstorms of ceiling plaster, children’s notebooks<br>
bleached bare by sun by rain<br>
shun me and I am shamed. <br><br>

Light me a candle, St Lazarus, as if<br>
your lipless kiss<br>
might warm these past lives<br>
that linger, frost or smoke beyond the clearing. <br><br><br>





</p>





            
           




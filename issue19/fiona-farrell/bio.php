                  <p>Fiona Farrell is one of New Zealand’s leading writers, publishing work across a variety of genres. </p>

<p>Her first novel, <em>The Skinny Louie Boo</em>k won the 1992 New Zealand Book Award for fiction. <em>The Hopeful Traveller</em> (2001) and the widely popular <em>Book Book</em> (2004) and her most recent novel, <em>Limestone</em> (2008) were shortlisted for Montana New Zealand Book Awards. These novels along with <em>Mr Allbones’ Ferrets</em> (2007) were also nominated for the International Dublin IMPAC Award. Her short fiction has appeared in the company of Alice Munro and Hanif Kureishi in Heinemann UK’s annual <em>Best Short Stories</em> (1990 and 1994, ed. Gordon and Hughes). She has published three collections of poetry, and her work appears in major anthologies including <em>The Oxford Book of New Zealand Poetry</em> and Bloodaxe’s best-selling <em>Being Alive</em>. Her play <em>Chook Chook</em> is one of Playmarket New Zealand’s most frequently requested scripts. </p>

<p>She is a frequent guest at festivals in New Zealand, and has also appeared at the Edinburgh International Book Festival and the Vancouver International Writers’ Festival. </p> 

<p>She has held residencies in France (the 1995 Katherine Mansfield Fellowship to Menton)  and Ireland (the 2006 Rathcoola Residency).  </p>

<p>Fiona Farrell has received numerous awards, most notably the 2007 New Zealand Prime Minister’s Award for Fiction.

 



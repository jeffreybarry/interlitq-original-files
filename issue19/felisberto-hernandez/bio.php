                  <p>Felisberto Hernández nació a principios del siglo XX en la ciudad uruguaya de Montevideo. Era hijo de Prudencio Hernández, natural de Tenerife, en las Islas Canarias y de Juana Silva, de la ciudad de Rocha. Era el mayor de cuatro hermanos.</p>

<p>A los 9 años comienza sus estudios de piano que profundizará más tarde con el profesor de piano Clemente Colling, que le enseña composición y armonía. A los 16 años, da clases particulares de piano, y comienza a dedicar horas a su práctica musical y a trabajar ilustrando musicalmente películas, pues sus dificultades económicas harán que acepte el empleo de pianista en varias salas de cine mudo. A los 20 años comienza a dar recitales e incluso interpreta algunas obras de su creación. Tres años más tarde, toma clases de piano con Guillermo Kolischer y consigue ser un buen instrumentista.</p>

<p>Hasta 1942 será un pianista itinerante entre Uruguay y Argentina: la orquesta del café La Giralda, en Montevideo, pianista y director de una orquesta en el café-concierto de Mercedes, Teatro Albéniz de Montevideo, Teatro del Pueblo de Buenos Aires.</p>

<p>En París, en su momento de mayor esplendor, conoció a África de las Heras, española, veterana de la Guerra Civil y agente de la KGB a quien se le encomendó seducirlo, por ser Felisberto individualista a ultranza. Tras casarse se instalaron en Montevideo donde ella trabajó como espía y finalmente se divorciaron sin que él supiera el papel que había desempeñado (véase A. Dujovne Ortiz, <em>La muñeca Rusa</em>, Alfaguara, 2009). Sobre las relaciones complicadas de Felisberto con las mujeres (se casó seis veces), existen dos testimonios de mayor interés. El de Paulina Medeiros (Tacuarembó, 1905- Montevideo, 1992) con la que tuvo relaciones entre 1943-47 (<em>Felisberto Hernández y yo</em>, 1974), y con la que tuvo además una notable correspondencia. Y el de Reina Reyes (Montevideo, 1904-1993), vinculada del 1954 a 1958, en su libro de cartas y notas, <em>¿Otro Felisberto? </em>(Montevideo, Banda Oriental, 1994).</p>

<p>Como escritor, a los 23 años, Felisberto empezó a publicar aunque nunca fue muy conocido. Tras la última etapa musical itinerante, abandonó la carrera de pianista dedicándose exclusivamente a la literatura.</p>

<p>Felisberto citaba dos nombres recurrentes en sus lecturas: Henri Bergson y Marcel Proust (también a Kafka). Y es que los cuentos y novelas cortas de Felisberto recrean el mundo de su infancia y juventud, evocan personas que conoció, barrios de Montevideo. Su narrativa se basa en el recuerdo como motor de la escritura, continuando la búsqueda proustiana. Una magdalena, una calle, un tren, un piano, pueden encerrar recuerdos y hacernos revivir sensaciones. La construcción de gran parte de sus cuentos se apoya en la reivindicación de lo lateral, como en <em>La cara de Ana. </em>Y una temática recurrente e interesante es el lugar primordial que le da a los objetos inertes (entre otros, así sucede en '<em>El vestido blanco, Las hortensias, El caballo perdido</em>).</p>

<p>Aunque su trabajo de escritor eclipsó su carrera de pianista, su obra entera está impregnada de música, tanto en los temas evocados (un profesor de piano, un recital, un bandoneón), como en la forma de contar, al sugerir emociones con palabras de cierta sonoridad, al transformar el sentido de las palabras en función de los sonidos, al construir partes de su relato como variaciones de un mismo tema musical. Para Juan Carlos Onetti, que lo admiraba, su libro más importante era su autobiográfico <em>Por los tiempos de Clemente Colling </em>(1942), más que otros posteriores y famosos, en los que aparecía como más "ingenuo".</p>

<p>Especialista en el ámbito de la narrativa breve, sus obras han sido traducidas, tardíamente, a varios idiomas: alemán, francés, inglés, italiano, griego y portugués Sin embargo, en un viaje a París intentó publicar, sin éxito, pese al apoyo generoso del escritor Jules Supervielle, de origen montivedeano. En España se difundió en 1974-1975 gracias al esfuerzo de Cristina Peri Rossi.</p>

<p>Para Julio Cortázar , es rechazable la mera etiqueta de 'fantástica' para su obra: "nadie como él para disolverla en un increíble enriquecimiento de la realidad total que no sólo contiene lo verificable sino que lo apuntala en el lomo del misterio". Ha sido considerado luego un maestro tanto por éste como por Gabriel García Márquez. A pesar de haber influido a muchos autores americanos, la extraña ficción de sus cuentos hace brotar un universo totalmente personal y que no puede ser comparado totalmente con los cuentos más urbanos, más intelectualizados de Cortázar. Italo Calvino, que prologó una versión italiana de Felisberto, lo definió como "un escritor que no se parece a nadie: a ninguno de los europeos y a ninguno de los latinoamericanos, es un <em>francotirador </em>que desafía toda clasificación y todo marco, pero se presenta como inconfundible al abrir sus páginas.

 


</p>

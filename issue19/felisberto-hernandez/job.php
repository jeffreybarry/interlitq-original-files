<p>(First published in <em>Turnrow</em>, Fall 2007)</p>

<p>Peter Robertson writes: </p>
<p><em>This June I crossed the immense River Plate on my way to Montevideo, to meet Walter Diconca, the grandson of Uruguayan writer, Felisberto Hernández (1902-1964). I had come to another country to enlist Walter’s support in obtaining the literary rights for my translation of Hernández’s “The White Dress”. Months before, I had stumbled across this story, published in the 1925 collection, “Fulano de Tal”, and dedicated to María Isabel Guerra, Felisberto’s first wife. As I read “The White Dress,” I was compelled by its images, giving primacy not to the protagonist’s inner musings but his obsessive observation—shot through with sexual tension and encroaching menace—of the external world. </em></p>

<p><em>While Felisberto has proved to be a seminal influence on major Latin American writers, such as Gabriel García Márquez, Julio Cortázar and Carlos Fuentes, this quixotic writer defies categorization. Italo Calvino has said that Hernández was like no other writer, either European or Latin American, and Felisberto himself was at pains to distance himself from any literary affiliation. With regard to the genesis of his work, he would only say: “I don’t know how my stories come to be written—each has its own internal life.” </em></p>

<p><em>It is to be hoped that, in the English-speaking world, Felisberto Hernández will come to receive the recognition that he deserves—this would be a far cry from the critical neglect that dogged his career. Indeed, in terms of adversity, Felisberto's life appears to have been inspired by one of the many tangos he, in his days as a concert pianist, would have played in the music halls of Uruguay. A fitting thought as I once again crossed the River Plate on my way back to Buenos Aires. </em></p>


<p><Br><bR><br></p>
<p align="right">Dedicated to María Isabel G. de Hernández</p>

            
<p><strong>The White Dress<br></strong></p> 


<p><strong>I</strong></p>

<p>I was standing outside, looking up at the balcony. From where I was, I could see that the two glass doors had been flung open and were facing each other diametrically, inside the room. Marisa was standing there too, her back almost grazing one of the glass doors. But, all of a sudden, someone called her from within and she left the scene. No sooner had she gone than I sensed that her departure had failed to evoke any intimation of absence. Indeed, I grew conscious of the fact that, all the while, the two glass doors had been looking at each other intently, that she had been a trespasser. She had encroached on the sanctity of that mute, immutable thing: the two doors staring at each other.</p>

<p><strong>II</strong></p> 

<p>It did not take me long to discover the only thing that engaged me about the two glass doors: the pleasure I derived from their inviolate positions; and the anguish that invaded me when these were transgressed. The positions that gave me pleasure were only two: when the glass doors faced each other, in sullen collusion; and when they were shut together and therefore at one. If Marisa pulled the doors back and they passed, even by a fraction, the precise point where they faced each other, I could not stop my jaw from clenching, my body from seizing up. At moments like this I would make a preternatural physical effort, willing the doors to revert to their perfect symmetry. Were this to be prevented, I had no doubt that the two glass doors would incubate a rancorous hatred whose outcome we could not predict. </p>

<p><strong>III</strong></p> 

<p>The most sacrilegious assaults on one of the two positions that gave me pleasure would occur in the evening, as Marisa and I wished each other goodnight. </p>

<p>On these occasions she would hesitate as she closed the two doors, leaving an invidious gap between them. I could tell that she was blind to the need of the two glass doors to be fused together forthwith, in implacable union.</p> 

<p>In the dark space that remained between the two doors, there was scarcely enough room for Marisa’s head. She looked nonchalant as she smiled at me, clearly reluctant to say goodbye. I could tell that she was oblivious to that intangible, yet menacing, force born of her delay in closing the two glass doors. </p>

<p><strong>IV</strong></p> 

<p>One evening, Marisa invited me inside and I felt elated. Later, she asked me to stand with her on the balcony. To get there, we had to negotiate the space between the two glass doors. Surveying them, I was bemused by their inscrutability: it seemed that, before we passed, they had been thinking one thing; and, after we passed, quite another. In any case, we walked through the gap that separated them. After Marisa and I had been talking for a while, and I had started to forget about the glass doors, I felt them touching my back in hypnotic movements. And, turning round, I saw that the doors were right up against my face. In fact, they had succeeded in pushing Marisa and me to the very edge of the balcony. My instinct was to jump off there and then, taking Marisa with me. 
</p>

<p><strong>V</strong></p> 

<p>One morning I was ecstatic because we had just got married. But when Marisa opened a wardrobe, I felt as perturbed as I had been by the glass doors, by this excessive aperture. One evening, while she was away, I went to take something out of the wardrobe. Although I felt like a desecrator, I opened it nonetheless. Spellbound, I stood there inert. My head was motionless, as were the contents of the wardrobe, and one of Marisa’s white dresses, which looked just like her, without arms, without legs, with no head.</p>


<p><br><br><bR><br></p>
<p align="right">A María Isabel G. de Hernández"</p>





<p><strong>El Vestido Blanco<br></strong></p>


<p><strong>I</strong></p>

<p>Yo estaba del lado de afuera del balcón. Del lado de adentro, estaban abiertas las dos hojas de la ventana y coincidían muy enfrente una de la otra. Marisa estaba parada con la espalda casi tocando una de las hojas.</p>

<p>Pero quedó poco en esta posición porque la llamaron de adentro. Al Marisa salirse, no sentí el vacío de ella en la ventana. Al contrario. Sentí como que las hojas se habían estado mirando frente a frente y que ella había estado de más. Ella había interrumpido ese espacio simétrico lleno de una cosa fija que resultaba de mirarse las dos hojas.</p>


<p><strong>II</strong></p>

<p>Al poco tiempo yo ya había descubierto lo más importante, lo más primordial y casi lo único en el sentido de las dos hojas: las posiciones, el placer de posiciones determinadas y el dolor de violarlas. Las posiciones de placer eran solamente dos: cuando las hojas estaban enfrentadas simétricamente y se miraban fijo, y cuando estaban totalmente cerradas y estaban juntas. Si algunas veces Marisa echaba las hojas para atrás y pasaban el límite de enfrentarse, yo no podía dejar de tener los músculos en tensión. En ese momento creía contribuir con mi fuerza a que se cerraran lo suficiente hasta quedar en una de las posiciones de placer: una frente a la otra. De lo contrario me parecía que con el tiempo se les sumaría un odio silencioso y fijo del cual nuestra conciencia no sospechaba el resultado.</p>


<p><strong>III</strong></p>

<p>Los momentos más terribles y violadores de una de las posiciones de placer, ocurrían algunas noches al despedirnos.</p>

<p>Ella amagaba a cerrar las ventanas y nunca terminaba de cerrarlas. Ignoraba esa violenta necesidad física que tenían las ventanas de estar juntas ya, pronto, cuanto antes.</p>

<p>En el espacio oscuro que aún quedaba entre las hojas, calzaba justo la cabeza de Marisa. En la cara había una cosa inconsciente e ingenua que sonreía en la demora de despedirse. Y eso no sabía nada de esa otra cosa dura y amenazantemente imprecisa que había en la demora de cerrarse.</p>


<p><strong>IV</strong></p>

<p>Una noche estaba contentísimo porque entré a visitar a Marisa. Ella me invitó a ir al balcón. Pero tuvimos que pasar por el espacio de esos lacayos de ventanas. Y no se sabía qué pensar de esa insistente etiqueta escuálida. Parecía que pensarían algo antes de nosotros pasar y algo después de pasar. Pasamos. Al rato de estar conversando y que se me había distraído el asunto de las ventanas, sentí que me tocaban en la espalda muy despacito y como si me quisieran hipnotizar. Y al darme vuelta me encontré con las ventanas en la cara. Sentí que nos habían sepultado entre el balcón y ellas. Pensé en saltar el balcón y sacar a Marisa de allí.</p>



<p><strong>V</strong></p>

<p>Una mañana estaba contentísimo porque nos habíamos casado. Pero cuando Marisa fue a abrir un roperito de dos hojas sentí el mismo problema de las ventanas, de la abertura que sobraba.</p>

<p>Una noche Marisa estaba fuera de casa. Fui a sacar algo del roperito y en el momento de abrirlo me sentí horriblemente actor en el asunto de las hojas. Pero lo abrí. Sin querer me quedé quieto un rato. La cabeza también se me quedó quieta igual que las cosas que había en el ropero, y que un vestido blanco de Marisa que parecía Marisa sin cabeza, ni brazos, ni piernas.</p>








            
           




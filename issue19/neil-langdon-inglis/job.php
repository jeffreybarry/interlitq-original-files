<p>"Hollow Earth"<br><br>

David Standish<br><br>

First Da Capo Press edition 2006<br>
Published by Da Capo Press<br>
A Member of the Perseus Books Group<br><br>

ISBN-13: 978-0-306-81373-3<br>
ISBN-10: 0-306-81373-4<br><br></p>

<p>Remember those lazy Sunday afternoons in the days before cable and video when there was nothing to watch on TV? I bitterly recall having to choose between the test card, “Match of the Day,” a Western called "The Virginian" (a.k.a.  “The Men from Shiloh”) starring James Drury (or was it Dreary?), and Doug McClure as his sidekick Trampas. McClure also appeared in the 1976 fantasy horror movie "At the Earth's Core", based on a novel by Edgar Rice Burroughs. The nadir arrived for me one day when "The Virginian" was on one channel, "Earth's Core" on another. I couldn't abide Westerns—but subterranean adventure movies were the pits of the world in every sense.</p>

<p>Their tense and unreflective action sequences drove me to distraction. I was vexed by the predictable cycles of capture and escape in which the "heroes" were prodded, tortured, or subjected to initiation rituals, all the while falling in love with sympathetic native girls. Near-death experiences, in quicksand or in the jaws of dinosaurs, were <em>de rigueur</em>. </p>

<p>David Standish (professor at the Medill School of Journalism) finds enchantment in such material, as his blurb suggests: "The long and curious history of imagining strange lands, fantastical creatures, advanced civilizations, and marvelous machines below the Earth's surface." Standish is spelunking for literary gold and diamonds, but what he actually finds are landfills and waste dumps of penny-dreadful literature buried beneath the Earth’s crust.</p>
 
<p>The subterranean genre exists at the borderlands of science and the imagination, with a strong technological component. Sci-fi writers in previous generations imagined what lay beneath their footsteps; but solving that mystery meant drilling deep holes in the ground. Much mechanical burrowing into the earth occurs in these sagas, including in tales by Tarzan creator Edgar Rice Burroughs (pun intended).  The Tarzan you all know was a jungle dweller. What you may not know is that Burroughs sent Lord Greystoke on one journey to the Earth’s core, in an effort to breathe life into Burroughs' long-running "Pellucidar" series. L. Frank Baum (“The Wizard of Oz”) packed off his beloved Dorothy on a similar jaunt late in his (and her) career, entitled "Dorothy and the Wizard in Oz." These examples of cross-branding looked ahead to "King Kong versus Godzilla" and other hybrid action-adventure sagas.</p>

<p>Pulp fiction is a business. As befits the empire-building impetus to which all these stories trace their roots, we find three interlocking impulses in play. Commerce, colonization... and national pride.</p>

<p>Our tourguide Standish fears such mixed motives. He disapprovingly quotes from "The Goddess of Atvatabar" by William R. Bradshaw (1851-1927), in which the author announces the discovery of a subterranean world, "All civilized nations immediately fitted out vessels of discovery…for the benefit of their respective governments."</p>

<p>In real life, buccaneers dreamt of buried treasure but returned home with ferns, goldfinches, and other specimens that built the foundations of modern-day botany and zoology. The theme of thwarted capitalist expectation will return later in this review, when we consider the actual lives of the chroniclers of fictional exploration. Money was only part of the equation, as Standish admits:</p>

<p>"The idea of completely overcoming nature, subduing and totally dominating it, was one that had been growing during the nineteenth century. And how could it not, given the ceaseless succession of scientific and technical marvels that just kept unfolding?" </p>

<p>This was the doctrine of Manifest Destiny for generations of American scribes. The passage of time was a constant rebuke:</p>

<p>"The American republic was a green new enterprise, with many scoffers just waiting for it to fail; a national expedition would be a way to show the world what America was made of. There was no time to waste."</p>

<p>Time was at a premium for novelists because hollow earth narratives had a built-in shelf life. Standish is good at etching the life cycle of the genre. "The number of hollow earth novels dropped off precipitously after 1910. One likely factor was increased scientific knowledge. Information has a way of dousing the fires of dreams (…) the required suspension of disbelief became more difficult." The advent of the UFO craze—predating Roswell, as the author points out—dealt a further blow.</p>

<p>Yet hollow earth sagas refused to die. Standish celebrates the 1991 animated cartoon "Teenage Mutant Ninja Turtles at the Earth's Core"; and might also have mentioned that “Boys Don't Cry” actress Hilary Swank starred in the straight-to-video 2003 clinker, imaginatively entitled “The Core.”</p>

<p>Today we know that our planet is not honeycombed like a Giant Malteser; yet in the absence of 20th century technology, Edmond Halley and other scientists from the early Newtonian period had only pure theory to rely upon. In their imagination, Newton’s laws ought to have propelled terrestrial matter up against the earth's crust. This meant that the earth must in turn be hollow, or at least contain catacombs. Perhaps fluorescence would bathe the rocks in an eery glow. The problem of underground illumination was resolved by the Symmes hole, brainchild of an eccentric American of the same name. Seizing on anomalous reports of warm conditions in polar areas, John Cleves Symmes (starting in 1818) argued that the zone of fierce cold and ice near the poles was a frozen ring to be traversed. Beyond it, things warmed up, and there was an open polar sea leading to the interior. Thus the earth was not a set of nesting concentric circles plunged into pitch-blackness, but a kind of Mobius strip, open on itself.</p>

<p>"Thanks largely to Symmes […], the idea of the hollow earth had become linked with the fascinating mystery of the poles. Nobody had ever been there, at least to live and tell about it, so they offered complete artistic freedom, a tabula rasa on which anything could be written."</p>

<p>Never purely an American creation, this adventure genre began in fits and starts. The tales of Edgar Allen Poe (“A Descent into the Maelstrom,” etc.) enjoyed a European renaissance through French-language versions prepared by Charles Baudelaire. In Jules Verne's stories, even though a character lights a fire with ‘a lentil’ (rather than a lens) in the 1872 English edition of “20,000 Leagues,” inferior translations posed no barrier to Verne's global success. Verne himself was dissatisfied with the "state of the art" and craned his neck around every corner. As Chris Durban relates, "Born in 1828, the French author was fascinated by technology and his books described airplanes, gas-powered cars, an elevated mass transit system, guided missiles, submarines, the electric chair, air conditioning, and the fax machine—all well before their time." <small> 1</small></p>
 
<p>As the age of technology unfolded, the hunger for such stories became overpowering, and a fresh intake of US visionary writers shared Verne’s delight in technological derring-do. Other byproducts of social change—such as female emancipation—were less well represented. In William Alexander Taylor's "Intermere" (1901), women are equal in theory, but their "chosen" role is generally that of housewife, and those who work under-earn their male counterparts. They receive five fewer years of education than men do, and they're not allowed to own real estate. This is not called boys-own literature for nothing.</p>

<p>Standish has read everything there is to read but his dutiful recitations of the texts fall flat. He is much better on the chequered lives of the storytellers—most obviously with Poe, but even with Burroughs, who had one of the highest rejection rates of any bestseller.  For all their brushes with poverty, Burroughs and Baum kept faith with the US system, which rewarded their perseverance. These literary ragamuffins with their somewhat fertile brains gave birth to merchandise, fan clubs, and all manner of commercial tie-ins and product placement. Burroughs in particular strikes us as all-American, presaging the Environmental movement.</p>

<p>"Burroughs […] was an early Greenie, ahead of his time in realizing the earth's fragility. He gave a speech on ecology to a group on Arbor Day, 1922, and discussed conservation issues in a 1930 radio interview. His drive to produce—writing, movie ideas, moneymaking schemes, endless Tarzan spin-offs (among them comic strips, kids' clubs, Tarzan bread, Tarzan ice cream cups, Tarzan belts, Tarzan bathing suits, Tarzan jungle helmets, Tarzan yoyos, Tarzan candy, etc.,) has in it a nervous mania, a constant thirsty seeking for <em>something</em> that none of this frantic activity ever managed to quench." </p>

<p>Standish tends to resist aesthetic judgments except by inference. He has left out Tolkien and Dante (perhaps he found the Dantean world beneath him—one hopes he did not think it above him). In an egalitarian society, meritocratic judgments invite accusations of snobbery. Yet a book reviewer can ask the question Standish delicately avoids, namely:</p>

<p>What separates Burroughs from Poe? What makes Poe so timeless, and the underworld penny-dreadful literature so ephemeral? </p>

<p>Authors like Burroughs have certain advantages. Their ubiquity is self-reinforcing. They deliver what is a surprise at first, what is expected later. Sequels drain their wells dry, leaving no drops at the bottom. Standish: "Especially in "Tarzan at the Earth's Core" there's a sameness to the attacks, captures, escapes, followed by more attacks, captures, and escapes"—as if Burroughs were operating more on autopilot than not."</p>
 
<p>There are sci-fi stories that have "mined" the topic of subterranean worlds while avoiding the "pitfalls." Standish does not mention "Star Trek" (the original series, known to fans as ST-TOS), yet an empty globe appears in "For the World is Hollow and I have Touched the Sky" (the episode in which the dying Dr. McCoy falls in love); nor should we forget the underworld abode of the Talosians in the ST-TOS pilot ("The Cage,” a story later reprised in "The Menagerie").  <small>2</small></p>

<p>ST-TOS lives on today because its creator Gene Roddenberry cared about obsolescence and circumvented its hazards more deftly than Verne and other sci-fi innovators. Scripts had the crew of the Enterprise rely on generic terminology ("sensors") unlikely to be superseded in the future. As an example of the show’s prescience, circular objects resembling CD-ROMs appear in Mr. Atoz's library in "All Our Yesterdays." Actual cloaking devices are in development as I write this review; transporter room dematerialization remains in the future.</p>

<p>Any other omissions in Standish's "Hollow Earth"? Fittingly, he moves beyond books to discuss cults (that other American commonplace), devoting an entire chapter to the Koreshan cult of Cyrus Teed (a.k.a. "Koresh"), which he treats with affectionate mockery. (We are not told whether the much later David Koresh (1959-93, leader of the ill-fated Branch Davidians of Waco, Texas, took a leaf out of Teed’s book).</p>

<p>"Teed's belief that we are living inside the earth, beyond which there is <em>nothing</em>, can be seen as a sort of ultimate metaphysical retreat to the womb—the entire universe as a small enclosed protective egg, finite, comprehensible, safe."</p>

<p>Standish describes how the Koreshans in Florida were offered a weekly news column in exchange for a pledge to vote a certain way in the 1904 Democratic primary and general election. "But the election of 1906 proved more troublesome. It became known that in 1904 the Koreshans had defected from the Democratic party and voted as a group for [Republican] Teddy Roosevelt (…).” Local Democrats prevailed upon the Koreshans to sign an oath concerning their party loyalty. The Koreshans, not easily scared off, simply amended the pledge before signing it and voted anyway. So Teed and friends were not eccentrics, but independent voters who broke with the Democratic Machine of the Old South, decades before Dixie embraced the Republican Party during the upheavals of the 1960s.</p>

<p>Is Standish's book a good read? Standish's palette of adjectives and nuance is not as substantial as he supposes, as we observe in such phrases as "Determining latitude was a breeze" (…) But longitude was a bitch." The reference to longitude does scant justice to one of the greatest challenges ever faced by seafarers and seafaring nations. Books of popular scholarship need not stoop in this way. This title is for devotees of science fiction only.<br><bR></p>


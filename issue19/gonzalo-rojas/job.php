<p><strong>Don't Copy Pound</strong></p>
<p>
Don’t copy Pound, don’t copy the incredible copier<br>
that is Ezra, let him write his masses in Persian, in Cairo-Aramaic, in Sanskrit,<br>
with his half-learned Chinese, his limpid dictionary Greek,<br>
his Latin hidden under fallen leaves, his unfettered<br>
blurry Mediterranean, the nonagenarian artifice<br>
of making and remaking till arriving by groping <br>
at the great palimpsest of the only One:<br>
don’t judge him for dispersion; the atoms needed to be collected,<br>
to be woven this way, the visible and invisible, in the warp of the momentary<br>
and the immobile threads; just let him loose,<br>
with his blindness, to see, to see once more, because that is the verb: to see,<br>
and that’s the Spirit, the as-yet unfinished<br>
and ardent, what it is we truly love<br>
and what loves us, if truly we’re the Son<br>
of Man and Woman, the uncountable in the depths of the unnamable;<br>
no, you new semi-gods<br>
of language without Logos, of hysteria, apprentices<br>
of the original wonder, don’t rob<br>
the shadow from the sun, think of the canticle<br>
that opens when it closes like a germination, make air,<br>
air-man like the old Ez, who always lived in danger, leap intrepidly<br>
to the stars from the vowels, the tense arc<br>
of contradiction in every velocity of the possible, air and more air<br>
for today and forever, before <br>
and after the purple flare,<br>
simultaneous, instantaneous,<br>
of the rotation, for this flickering world will bleed,<br>
will break free from its mortal axis, and goodbye fertile<br>
traditions of light and marble, and arrogance; laugh at Ezra<br>
and at his wrinkles, laugh from now till then, but don’t take him out; laugh, <br>
frivolous generations that come and go like dust, swarms<br>
of scribblers, laugh, laugh at Pound<br>
with his Tower of Babel on his back like a sign of the other<br>
that came with his tongue:<br>
canticle,<br>
o ye of little faith, think of the canticle.</p>
<p><br><br></p>




<p><strong>No le copien a Pound</strong></p>

<p>No le copien a Pound, no le copien al copión maravilloso<br>
de Ezra, déjenlo que escriba su misa en persa, en cairo-arameo, en sánscrito,<br>
con su chino a medio aprender, su griego translúcido<br>
de diccionario, su latín de hojarasca, su libérrimo<br>
Mediterráneo borroso, nonagenario el artificio<br>
de hacer y rehacer hasta llegar a tientas al gran palimpsesto de lo Uno;<br>
no lo juzguen por la dispersión: había que juntar los átomos,<br>
tejerlos así, de lo visible a lo invisible, en la urdimbre de lo fugaz<br>
y las cuerdas inmóviles; déjenlo suelto<br>
con su ceguera para ver, para ver otra vez, porque el verbo es ése: ver,<br>
y ése el Espíritu, lo inacabado<br>
y lo ardiente, lo que de veras amamos<br>
y nos ama, si es que somos Hijo de Hombre<br>
y de Mujer, lo innumerable al fondo de lo innombrable;<br>
no, nuevos semidioses<br>
del lenguaje sin Logos, de la histeria, aprendices<br>
del portento original, no le roben la sombra<br>
al sol, piensen en el cántico<br>
que se abre cuando se cierra como la germinación, háganse aire,<br>
aire-hombre como el viejo Ez, que anduvo siempre en el peligro, salten intrépidos<br>
de las vocales a las estrellas, tenso el arco<br>
de la contradicción en todas la velocidades de lo posible, aire y más aire<br>
para hoy y para siempre, antes<br>
y después de lo purpúreo<br>
del estallido<br>
simultáneo, instantáneo<br>
de la rotación, porque este mundo parpadeante sangrará, <br>
saltará de su eje mortal, y adiós ubérrimas <br>
tradiciones de luz y mármol, y arrogancia; ríanse de Ezra<br>
y sus arrugas, ríanse desde ahora hasta entonces, pero no lo saqueen; ríanse, livianas<br>
generaciones que van y vienen como el polvo, pululación<br>
de letrados, ríanse, ríanse de Pound<br>
con su Torre de Babel a cuestas como un aviso de lo otro <br>
que vino en su lengua;<br>
cántico,<br>
hombres de poca fe, piensen en el cántico.
</p>





            
           




                  <p>Gonzalo Rojas (1917-2011) was a Chilean poet born in the port town of Lebu. He participated in the surrealist group Mandrágora between 1938-1941, publishing his first book of poems in 1948, and taught literature in the Universities of Valparaíso and Concepción; he also served as a diplomat in Cuba under Salvador Allende. Exiled during the Chilean military dictatorship in 1973, Rojas lived in Caracas and also taught at universities in Germany, the US, Mexico, and Spain. He returned to Chile in 1979. Widely translated, his many books include <em>La miseria del hombre</em> (1948), <em>Contra la muerte</em>  (1964), <em>Oscuro</em> (1977), <em>Transtierro</em> (1979), <em>50 poemas</em> (1982), <em>Del relámpago</em> (1981 and 1984), <em>El alumbrado</em> (1986), <em> Materia de testament</em> (1988), <em>Desocupado lector</em> (1990), <em>Antologeia de aire </em>(1991), <em>Las hermosas </em>(1991) and <em>El ocio sagrado </em>(2002). In 1992, Rojas was awarded both the Chilean National Prize for Literature and the Queen Sofia Prize of Iberian American Poetry, as well as the Octavio Paz Prize of Mexico in 1998 and the Cervantes Prize in 2003. He died in 2011.
 



<p>Con ese título algo grandilocuente quisiera hacer una provocación para pensar algunos temas. Por ejemplo, pensar si estamos ante las puertas de la historia nos lleva a preguntarnos si hay una o varias historias, porque quizás haya varias historias que serían, por decirlo metafóricamente, como varias casas, cada una con su estilo, su arquitectura y su estructura, y dotada de una o varias puertas que nos invitan a entrar. Por eso, decir que estamos ante las puertas de la historia requiere precisar ante las puertas de cuál o de cuáles historias estamos y cuál es el estilo de la casa a la que esas puertas nos conducen.</p>


<p><em><strong>Primer ejemplo. Contabilizar la historia</strong></em><br>
<em>La primera impresión de que tal vez había más de una historia la tuve, siendo niño, con once o doce años, al caer en mis manos una moneda de un país árabe, musulmán, y darme cuenta de que la fecha de la moneda no era la cristiana, es decir, que los años en algunas partes del mundo no se contaban desde el mismo punto de partida que en nuestras tierras, donde se cuenta a partir del nacimiento de Cristo. Es decir, comprendí que el punto de partida de la contabilidad histórica no era el mismo en todo el planeta, que en algunos países el nacimiento de Cristo no tenía la importancia que tenía entre nosotros y que, en consecuencia, en aquellas regiones para mí lejanas muchas cosas debían ser valoradas de otro modo. </em></p>

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;	Años más tarde, ya adulto, comprendería que el momento a partir del que se cuenta una historia o se define como punto de partida era una decisión y que, esa decisión, era portadora de un contenido. ¿Cuál contenido? Primero que todo, la fecha. La fecha desde la cual se comienza a contar la historia es el primer contenido de la historia y lo que además permite contarla. Si no sabemos desde dónde comenzar, no podemos contar la historia, ni nada. <br>
	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Entre las varias o quizás muchas historias que se cuentan en la Tierra, en América, a veces no se sabe bien en cuál de ellas estamos. Quizás, además, sea esa una condición cada vez más frecuente en el planeta o, al menos, en la parte occidental u occidentalizada de él. Ha sido un tema tradicional de la meditación sobre América indagar por la definición de cuál es la más propia de nuestras historias o bien, si no hay historia propia e indagar la relación con una historia que es de un otro o de unos otros. Digo “un otro” o “unos otros” porque durante algunos siglos y hasta hace no muchas décadas varios filósofos e historiadores pensaban que nuestra historia era como un apéndice de la de algunos países europeos. Estos hacían la historia, la escribían y comentaban algunos acontecimientos sucedidos en América como parte de una historia, de una gran historia, que ellos llamaban universal y nosotros, ingenuamente, la llamábamos con el mismo adjetivo de “universal” y que nunca sucedía en nuestras tierras. </p>


<p><em><strong>Segundo ejemplo: la escaramuza</strong></em><br>
<em>Voy a dar otro ejemplo, que me relató un amigo que había estudiado historia en una universidad alemana. Investigando en un archivo, dio con el relato de un cónsul europeo en Iquique, poco después del 21 de mayo de 1879, en el medio de lo que llamamos, en Chile, la Guerra del Pacífico. Esa guerra es eje de uno de los episodios fundadores de la nación, una guerra por medio de la cual el país se da, inventa, conquista o gana, según se quiera mirar, sus fronteras contemporáneas. ¿Y el 21 de mayo de 1879? En ese día, en el contexto de la guerra, ocurrió el Combate Naval de Iquique, efemérides que se celebra todos los años, con gran ceremonial en nuestro país. Sin embargo, para ese cónsul europeo, el principal combate naval del Chile ya independiente era una “escaramuza” entre algunos barcos de Chile y Perú, a unos kilómetros de la costa. Nada más que una escaramuza.</em></p>


<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;¿Ante qué historia estamos, entonces? ¿Somos en la “historia universal” tal sólo una escaramuza? Lo que en la perspectiva de algunos países que sí hacen y escriben la “historia universal” era un detalle más o menos insignificante y aislado, para el nuestro es motivo de pompas que se celebran todos los años. Estamos, entonces, ante una paradoja: hay una historia universal que narra los grandes acontecimientos –que siempre suceden lejos- y donde nuestros placeres y dolores siempre son vistos como algo pequeño, por mucho que a nosotros se nos vaya la vida en ello. ¿Vamos a aceptarlo, vamos a reducir a nada o casi nada los eventos que nos marcan?<br>
	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;No. Pero tenemos que explicarlo. Me sumo a los que piensan que no hay acontecimientos que tengan un valor absoluto en la historia. Se requiere cierta dosis de humildad para aceptar ese principio, pero esa dosis de humildad, hoy, permite situarse mejor en el mundo y comprenderlo también mejor. Esa dosis de humildad es indispensable, no sólo para entendernos, sino también para vivir. </p>


<p><em><strong>Tercer ejemplo: José de Acosta </strong></em><bR>
<em>En 1580, José de Acosta, Provincial de los jesuitas en Perú, es decir, en palabras de hoy, jefe de los jesuitas en ese país, criticaba a sus compatriotas españoles, que se desesperaban por no obtener lo que querían de los indios y terminaban usando la fuerza contra ellos, ya sea para explotarlos laboralmente, ya sea porque en casi un siglo no habían podido “convertirlos” al cristianismo. ¿Qué les dice Acosta? ¡Que miren a la historia! ¿Y qué encuentran en ella? Que el mismo Cristo nunca pudo convertir excepto a unos pocos discípulos y que, sin embargo, nunca propuso una guerra religiosa mientras que, en cambio, un puñado de españoles pretende convertir a miles de indios en un territorio mucho más grande que varias veces Europa y en mucho menos tiempo. Mirado desde el punto de vista de los acontecimientos del momento, el intento de Cristo fue un fracaso: apenas consiguió convertir a un grupúsculo de seguidores, varios de los cuales fueron condenados a muerte; mirado desde un punto de vista histórico y más amplio, se trató de un acontecimiento que, entretejido con otras fuerzas, cambió la faz del mundo.</em></p>


<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;	¿En qué fecha vamos a decir que comienza nuestro país? ¿Con la declaración de independencia (1810) o con el fin de las guerras de independencia (1820)?; ¿en la época de la llegada de los europeos (1492), en la época de los primeros asentamientos indígenas?; ¿o bien mucho antes, cuando llegaron los primeros humanos a Chile, hace unos 14 mil años, en Monteverde, cerca de la actual ciudad de Puerto Montt? Cualquiera de esas opciones nos lleva a contar una historia distinta y a entra en ella de distinta manera. Me atrevo a decir que nuestra historia no tiene un comienzo, porque antes del comienzo, siempre es posible situar otro comienzo. Antes de los criollos, los españoles; antes de los españoles, los pueblos indígenas; antes de los pueblos indígenas, los asentamientos humanos de Monteverde; antes de ellos, algunos grupos que llegaron no sabemos ya por dónde, pero que venían probablemente de regiones más nortinas de América. <br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;	La historia no es una propiedad, ni una cosa, sino una forma de contar un relato que somos nosotros mismos. Necesitamos instalar el comienzo de la historia para poder contar la historia, pero esa instalación es precaria y fruto de una elección. Por eso, en esta casa que es la historia, no hay un propietario exclusivo, ni tampoco un derecho por haber sido el primero en llegar a ella, porque muchos fueron los primeros en ocuparla y siempre encontraremos a alguien que vino antes. Una visión filosófica de la historia nos provee de una saludable humildad al colocarlos en un lugar ético y político en muchas circunstancias en que el simple relato histórico carece de esa perspectiva y se vuelve tiránico al pretender que tiene un comienzo único. No vale, para la historia, la metáfora bíblica de la piedra sobre la que se construye una iglesia; no tenemos un Pedro, ni hace falta tenerle, porque tenemos varios Pedros.<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;	La pregunta por las puertas de la historia debiera ser sustituida por otra sobre las puertas de <em>las</em> historias, de las historias de nuestras casas, de nuestras moradas históricas. Quizás vivamos en varias casas y ninguna de ellas sea exactamente propia ni exactamente arrendada. La historia no es una propiedad, ni una cosa, sino un forma de contar un relato que somos nosotros mismos, nómades en cuanto a comienzos y destinos. Por eso, también, en esta casa, no hay un propietario exclusivo, ni tampoco un derecho a ocuparla por algún título absoluto, sino múltiples títulos, obligados a co-existir, a convivir, a tener un destino común, incluso quizás a forjarnos un destino feliz sobre la base de esa diversidad.<br> 
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;	Mejor así, mejor no contaminar la historia con la noción de propiedad, porque la historia es de todos. Pero es necesario entrar a esa historia, en esa morada, habitarla. Eso supone conocerla y abandonar la idea de que hay una historia universal, lejana, allá en Europa o en otro sitio, de la cual somos una periferia. América está entrando en la historia, pero no en esa historia, en la historia supuestamente universal. América entra en la historia al comprender que podemos darnos varios comienzos, que todos esos comienzos son válidos: la Independencia, la llegada de los europeos, los asentamientos indígenas, los primeros humanos en el sur del continente -hace catorce mil años-, los americanos que llegaron a nuestras tierras probablemente desde el norte del continente. Esa historia nuestra no es la historia-universal-de-la-expansión-europea, pero tampoco podemos decir que se oponga a ella. Nuestra historia, lo “nuestro”, no es algo cerrado, sino algo abierto. Nuestra historia son múltiples moradas en las que vivimos y, a la vez, que llevamos con nosotros mismos, en nosotros mismos, al menos desde hace catorce mil años y mucho más también. Un poco de historia, entonces, para ser humildes y ser pacientes, sin dejar de ser lo que somos y alcanzar lo que queramos ser. 


<p>Here in Morocco, Humanity is turning<br>
as down a mountain pass, past the cafe<br>
in spring-time winter, the hour of the blown scarf,<br>
the torturous good-byes beneath a pale sky.<br>
Christos amore dei Christos magnus<br>
I'll be waiting beneath the regular cup,<br>
white as my heart, as this letter,<br>
the snow above, reflected only in your eyes.<br>
A perfect time for springtime. And for skiing.<br>
After the hotel, down we went.<br>
I picked you up in the snows then,<br>
and replaced your mittens, one on each hand,<br>
and you looked at me with a dazed look,<br>
trying to understand,<br>
as I picked you up and brushed you off,<br>
and then you brushed me, and it,<br>
turning.<br>
I lie awake alert at night,<br>
sometimes in the same hotel, listening to the moose crossing,<br>
naming the colleges and towns I've seen,<br>
the lodges, and the roadstands,<br>
the inns, the establishments,<br>
the factories, the state monuments . . .<br>
idly in the dark.<br>
A lone radio<br>
cries out the combination<br>
that would turn my heart in yours,<br>
I would turn to return to you,<br>
garbled though, like everything in this cylindrical bubble, --<br>
who have left me in no gladness,<br>
lady of the technicolor and peak madness,<br>
Russian who jumped out of a cake<br>
to find me charming,<br>
as I had only dreamed myself<br>
(though "he led a charmed life", they said),<br>
someone who I could talk to.<br>
Why is this disrupt<br>
at the edge of a dark night<br>
in other circumstance?<br>
At any rate they come,<br>
the silver on the table reflecting their uproar,<br>
this exodus of military civil servants,<br>
as civil as can be, here in Morocco.<br>
They thread the horizon in the eye of god.<br>
When I was a child, I was shown an old book,<br>
over-sized, of colored maps<br>
you could pull out.<br>
They traced the fortunes of the several states.<br>
Bespoke of sieges. Masteries. Culture.<br>
An evocation of the uniquely delectable<br>
hidden within their knowing cloud of code,<br>
the blatant portent of their colophon.<br>
I buried myself here, and in imperial avuncular voices<br>
the planners of the state bespoke on siege of war,<br>
huddling the horses of a carousel<br>
into color, orange, red, green . . .<br>
I trifoliated the mecums of chance,<br>
the polyfugals of index.<br>
I restored the primeval indicator<br>
its absence absolute.<br>
Inhumanly as possible<br>
I culled the grass.<br>
My mother rode me by the English canal.<br>
I was shown an old book,<br>
kept under a pillow,<br>
to memorize against my brother,<br>
at an early, impressionable age.<br>
The prize of the moon<br>
I wanted for someone only,<br>
as the sun sank. Brilliant city!<br>
I wait for you, tired by the canal,<br>
to deliver me of these letters,<br>
these vocables of pebbles in a stream,<br>
the word I am for you.



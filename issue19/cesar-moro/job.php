<p><em><strong>La vida escandalosa de César Moro</strong></em></p>

<p>Dispérsame en la lluvia o en la humareda de los torrentes que pasan<br>
Al margen de la noche en que nos vemos tras el correr de nubes<br>
Que se muestran a los ojos de los amantes que salen<br>

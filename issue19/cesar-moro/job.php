<p>I was a teenager and it was from Ludwig Zeller that I first learned about César Moro. It must have been around the time he acquired the newly published <em>La tortuga ecuestre y otros textos</em> (Ed. Julio Ortega, Monte Avila, 1976), a selection of Moro’s most emblematic poems. I could never have predicted the importance Moro’s work would have on my own trajectory as a poet and as a translator. There is so much that Moro, his example, his brilliant poetry exemplify for so many of us. Foremost is his spirit of revolt, one that would force him away from his native Peru, first to Paris, then to Mexico. Moro lived truly. Moro lived <em>freely</em>. He assumed liberty in all its forms, turning the liabilities of his self-imposed exiles into tools for poetic expression. While in Paris he participated in collective projects of the first surrealist group. Significantly, he assumed the French language as a tool for poetic transformation. In Mexico he organized publications and exhibitions and wrote some of the most nakedly beautiful love poetry of surrealism. Always, however, there was an extra bit of “otherness” in Moro, a separateness that was made all the more complete by the fact of his homosexuality, which he assumed intelligently and entirely, and which enriched his poetics in ways that can only be termed as revolutionary. </p>

<p>The poems presented here form part of a translation project, which I undertook a few years ago with André Coyné’s generous permission. Except for “Love Letter,” which was written in French in 1942 (it was translated into Spanish by Emilio Adolfo Westphalen), the poems form part of a collection titled <em>La tortuga ecuestre</em>, which César Moro wrote in Spanish between 1938 and 1939.</p>



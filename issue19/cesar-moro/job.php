The solemn carnivorous and bituminous forests<br>
Where strange wanderers get drunk their eyes wide open<br>
Under grand catapults and elephantine heads of cattle<br>
Suspended in Babylonian Trasteverean fashion<br>
The river that crowns your earthly and excessive appearance <br>
Drops furiously like lightning on the vestiges of the day<br>
Deceitful heaping of medals of hackbuts of sponges<br>
Winged bull with significant happiness biting at the breast or dome<br>
Of a temple emerging from the ignominious light of day in the middle of rotten <br>

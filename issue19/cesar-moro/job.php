<p><em><strong>You Come in the Night with Fabulous Smoke in Your Hair</strong></em></p>


<p>You appear<br>
Life is certain<br>
The smell of the rain is certain<br> 
You are born of the rain<br>
It brings you knocking at my door<br>
Oh tree<br>
The city and the seas you sailed<br>
And the night they open at your step<br>
The heart comes back from a distant place and leans out <br>
Until it touches your brow<br>
And beholds your dazzling magic<br>
Like a mountain of gold or of snow<br>
With fabulous smoke in your hair<br>
And the noctural creatures in your eyes<br>
Your body made of burning embers<br>
You water portions of the night<br>
And the blocks of night that fall from your hands<br>
And the silence grows roots at your arrival<br>
And the turmoil and the waves<br>
And the houses that sway<br>
And the wavering light and a harder shadow<br>
Your words like river traffic<br>
No sooner do you come than you are gone<br>
And you want to set my life afloat<br>
Yet merely prepare my death<br>
And the death of waiting<br>
And my dying knowing that you are far away<br>
And the silence and the waiting for time <br>
To live when you come<br>
And you surround me in shadows<br>
And make me luminous<br>
And you plunge me into the phosphorescent sea where your existence unfolds<br>
Where we engage in dialogue you and my dark frightful notion of your being<br>
Star cast off by the apocalypse<br>
Among the bellowing of tigers and tears<br>
Of pleasure and eternal moaning and eternal<br>
Finding of solace in the rarified air<br>
Where I wish to confine you<br>
That I may slip down the slope that is your body<br>
Reach your sparkling feet<br>
Reach your feet made of twin constellations<br>
In the terrestrial night<br>
Which follows you chained and mute<br>
Climbing ivy made of your blood <br>
Holding up your head that is flower of dark crystal<br>
An aquarium that holds the planets the trailing comets<br>
And the power that holds the world that keeps the oceans in steady balance<br>
Your brain made of luminous matter<br>
And my endless adherence and my love being born incessantly<br>
Which wraps itself around you<br>
Which your feet travel<br>
Leaving indelible traces<br>
Where we read the history of the world<br>
And the future of the universe<br>
And the luminous merging of my life<br>
To your existence<br><br><br></p>

<p><em><strong>Vienes en la noche con el humo fabuloso de tu cabellera</strong></em></p>

<p>Apareces<br>
La vida es cierta<br>
El olor de la lluvia es cierto<br>
La lluvia te hace nacer<br>
Y golpear a mi puerta<br>
Oh árbol<br>
Y la ciudad el mar que navegaste<br>
Y la noche se abren a tu paso<br>
Y el corazón vuelve de lejos a asomarse<br>
Hasta llegar a tu frente<br>
Y verte como la magia resplandeciente<br>
Montaña de oro o de nieve<br>
Con el humo fabuloso de tu cabellera<br>
Con las bestias nocturnas en los ojos<br>
Y tu cuerpo de rescoldo<br>
Con la noche que riegas a pedazos<br>
Con los bloques de noche que caen de tus manos<br>
Con el silencio que prende a tu llegada<br>
Con el trastorno y el oleaje<br>
Con el vaivén de las casas<br>
Y el oscilar de luces y la sombra más dura<br>
Y tus palabras de avenida fluvial<br>
Tan pronto llegas y te fuiste<br>
Y quieres ponder a flote mi vida<br>
Y sólo preparas mi muerte<br>
Y la muerte de esperar<br>
Y el morir de verte lejos<br>
Y los silencios  y el esperar el tiempo<br>
Para vivir cuando llegas<br>
Y me rodeas de sombra<br>
Y me haces luminoso<br>
Y me sumerges en el mar fosforescente donde acaece tu estar<br>
Y donde sólo dialogamos tú y mi noción oscura y pavorosa de tu ser<br>
Estrella desprendiéndose en el apocalipsis<br>
Entre bramidos de tigres y lágrimas<br>
De gozo y gemir eterno y eterno<br>
Solazarse en el aire rarificado<br>
En que quiero aprisionarte<br>
Y rodar por la pendiente de tu cuerpo<br>
Hasta tus pies centelleantes<br>
Hasta tus pies de constelaciones gemelas<br>
En la noche terrestre<br>
Que te sigue encadenada y muda<br>
Enredadera de tu sangre<br>
Sosteniendo la flor de tu cabeza de cristal moreno<br>
Acuario encerrando planetas y caudas<br>
Y la potencia que hace que el mundo siga en pie y guarde el equilibrio de los <br>
	mares<br>
Y tu cerebro de materia luminosa<br>
Y mi adhesión sin fin y el amor que nace sin cesar<br>
Y te envuelve<br>
Y que tus pies transitan <br>
Abriendo huellas indelebles<br>
Donde puede leerse la historia del mundo<br>
Y el porvenir del universo<br>
Y ese ligarse luminoso de mi vida<br>
A tu existencia<br><br><br></p>

<p><em><strong>Fire and Poetry </strong><br>
On the golden water the sun reflects the zenith’s hand</em></p>

<p>I<br><br>

I love love<br>
On Tuesdays but not on Wednesdays<br>
I love the love of disunited states<br>
Love that is two hundred and fifty years old<br>
Under the negative influence of Judaism on the lives of monks<br>
Love of sugarbirds and of hay, of ice and aluminum love that is pocket-sized<br>
The love I love has a bloody face and two immense doors into the void<br>
Love appearing in two hundred and fifty installments over five years<br>
Love of fragile economy<br>
Like a country endlessly expanding<br>
Over thousands of naked people who are treated like animals<br>
In order to obtain the simplest weapon of love<br>
So crime may stay out late and drink the clear water<br>
From the day’s warm blood<br><br><br>

II<br><br>

I love a love of thick branches<br>
Wild like a jellyfish<br>
Sacrificial love<br>
Daytime sphere where the entirety of spring <br>
Swings spilling blood<br>
Love made of rings of rain<br>
Of transparent stone<br>
Of mountains that fly and dissipate<br>
That turn into tiny rocks<br>
Love that is like a stabbing<br>
Like a shipwreck<br>
The complete loss of breath and of voice<br>
The kingdom of thick shadows<br>
With its protruding killer eyes<br>
The longest saliva<br>
The anger of losing oneself<br>
The frenzied waking in the middle of the night<br>
Under the storm that leaves undresses us<br>
A distant bolt of lightning turns trees<br>
Into firewood of hair which utters your name:<br>
Days and hours of unending nakedness<br><br><br>


III<br><br>

I love the fury of losing you<br>
Your absence riding the horse of time<br>
Your shadow and the idea of your shadow<br>
Cast over a field of water<br>
Your kestrel eyes in the hands of time<br>
Undoing me and recreating you<br>
Time dawning leaving me more alone as I come<br> 
Out of dream than an anti-diluvian animal lost in the shadows of time<br>
Like a toothless beast running after its prey<br>
Like a kite in the sky evolving with clockwork precision<br>
I see you in a thunderous jungle as I linger over you<br>
With the deadly force of dynamite exploding<br>
I distribute your veins among my selves I drink your blood<br>
And struggle against daylight tearing apart the dawn<br>
I undo the body of death<br>
And time finally belongs to me<br>
And the night catches up to me<br>
And the dream which annuls me devours you<br>
And I can assimilate you like ripe fruit<br>
Like a stone on a island that is sinking<br><br><br>

IV<br><br>

Slow water slow road slow accidents<br>
A descent suspended in mid air a slow wind<br>
The slow passing of slow time <br>
The night does not end and love slows down<br>
The legs cross over slowly knotting they grow roots<br>
The head falls and the arms rise<br>
The bed’s sky its shadow slowly fall<br>
Your dark body like a waterfall slowly falling<br>
Into the abyss<br>
We twist slowly in the hot air of this sizzling room<br>
While the nocturnal butterflies look like large sheep<br>
It would be simple now to slowly destroy each other <br>
To tear out our limbs to drink our blood slowly<br>
Your head spins your legs envelop me<br>
Your armpits and all their hair glow in the night<br>
Your naked legs<br>
Their exact angle<br>
The smell of your legs<br>
The slowness of perception<br>
Alcohol slowly lifts me <br>
The alcohol born of your eyes will later<br>
Grow your shadow<br>
Will tear at my hair as I slowly rise<br>
To your animal lips<br><br><br>

V<br><br>

The days are pouring the slow waters<br>
Head of hair the golden sand<br>
A volcano returns to its origin<br>
I watch you and I count the hours<br>
Time’s back divinely wounded<br>
A naked amphora cleaves the water<br>
Dew confines your body<br>
To the secret regions of a magic mountain<br>
Covered in doll shoes and business cards of the gods<br>
Asmodeus Nero Caligula Agrippina Louis II of Bavaria<br>
Anthony Cretin Caesar<br>
Your name appears intermittently<br>
Over an immense dough roll<br>
At times it fills the entire horizon<br>
Sometimes it peoples the sky with tiny bees<br>
I can always read it from every direction<br>
As it grows and becomes confused with all the words in its wake<br>
Or  becomes just an immense piece of light<br>
Or the furtive stepping of animals in the forest<br>
Or a spider slowly dropping on my head<br>
Or the furious alphabet<br><br><br>

VI<br><br>

The slow water the slow minimal variation<br>
The slow weightless face<br>
A sigh cut slightly<br>
The minuscule pebbles<br>
The imperceptible mountains<br>
The water slowly falling<br>
On the world<br>
Next to your burning kingdom<br>
Behind the walls the space<br>
And nothing but navigable space<br>
The room that rises and falls<br>
The waves do nothing<br>
The dog watches the house<br>
The wolves retreat<br>
The dawn stalks us to deliver its heavy blow<br>
Blind sleeping<br>
A tree has grown taller<br>
In vain I close the windows<br>
I look at the moon<br>
The wind has not stopped calling at my door<br>
The dark life is about to begin<br><br><br></p>



<p><em><strong>El fuego y la poesía</strong>

En el agua dorada el sol quemante refleja la mano del cenit</em></p>

<p>I<br><br>

Amo el amor <br>
El martes y no el miércoles<br>
Amo el amor de los estados desunidos<br>
El amor de unos doscientos cincuenta años<br>
Bajo la influencia nociva del judaísmo sobre la vida monástica<br>
De las aves de azúcar de heno de hielo de alumbre o de bolsillo<br>
Amo el amor de faz sangrienta con dos inmensas puertas al vacío<br>
El amor como apareció en doscientas cincuenta entregas durante cinco años<br>
El amor de economía quebrantada<br>
Como el país más expansionista<br>
Sobre millares de seres desnudos tratados como bestias<br>
Para adoptar esas sencillas armas del amor<br>
Donde el crimen pernocta y bebe el agua clara<br>
De la sangre más caliente del día<br><br><br>

II<br><br>

Amo el amor de ramaje denso<br>
Salvaje al igual de una medusa<br>
El amor-hecatombe<br>
Esfera diurna en que la primavera total<br>
Se columpia derramando sangre<br>
El amor de anillos de lluvia<br>
De rocas transparentes<br>
De montañas que vuelan y se esfuman<br>
Y se convierten en minúsculos guijarros<br>
El amor como una puñalada<br>
Como un naufragio<br>
La pérdida total del habla del aliento<br>
El reino de la sombra espesa<br>
Con los ojos salientes y asesinos<br>
La saliva larguísima<br>
La rabia de perderse<br>
El frenético despertar en medio de la noche<br>
Bajo la tempestad que nos desnuda<br>
Y el rayo lejano transformando los árboles<br>
En leños de cabellos que pronuncian tu nombre<br>
Los días y las horas de desnudez eterna<br><br><br>


III<br><br>

Amo la rabia de perderte<br>
Tu ausencia en el caballo de los días<br>
Tu sombra y la idea de tu sombra<br>
Que se recorta sobre un campo de agua<br>
Tus ojos de cernícalo en las manos del tiempo<br>
Que me deshace y te recrea<br>
El tiempo que amanece dejándome más solo<br>
Al salir de mi sueño que un animal antediluviano perdido en la sombra de los días<br>
Como una bestia desdentada que persigue su presa<br>
Como el milano sobre el cielo evolucionando con una precisión de relojería<br>
Te veo en una selva fragorosa y yo cerniéndome sobre ti<br>
Con una fatalidad de bomba de dinamita<br>
Repartiéndome tus venas y bebiendo tu sangre<br>
Luchando con el día lacerando el alba<br>
Zafando el cuerpo de la muerte<br>
Y al fin es mío el tiempo<br>
Y la noche me alcanza<br>
Y el sueño que me anula te devora<br>
Y puedo asimilarte como un fruto maduro<br>
Como una piedra sobre una isla que se hunde<br><br><br>

IV<br><br>

El agua lenta el camino lento los accidentes lentos<br>
Una caída suspendida en el aire el viento lento<br>
El paso lento del tiempo lento<br>
La noche no termina y el amor se hace lento<br>
Las piernas se cruzan y se anudan lentas para echar raíces<br>
La cabeza cae los brazos se levantan<br>
El cielo de la cama la sombra cae lenta<br>
Tu cuerpo moreno como una catarata cae lento<br>
En el abismo<br>
Giramos lentamente por el aire caliente del cuarto caldeado<br>
Las mariposas nocturnas parecen grandes carneros<br>
Ahora sería fácil destrozarnos lentamente<br>
Arrancarnos los miembros beber la sangre lentamente<br>
Tu cabeza gira tus piernas me envuelven<br>
Tus axilas brillan en la noche con todos sus pelos<br>
Tus piernas desnudas<br>
En el ángulo preciso<br>
El olor de tus piernas<br>
La lentitud de percepción <br>
El alcohol lentamente me levanta<br>
El alcohol que brota de tus ojos y que más tarde<br>
Hará crecer tu sombra<br>
Mesándome el cabello lentamente subo<br>
Hasta tus labios de bestia<br><br><br>

V<br><br>

Verte los días el agua lenta<br>
Una cabellera la arena de oro<br>
Un volcán regresa a su origen<br>
Verte si cuento las horas<br>
La espalda del tiempo divinamente llagada<br>
Un ánfora desnuda hiende el agua<br>
El rocío guarda tu cuerpo<br>
En lo recóndito de una montaña mágica<br>
Cubierta de zapatos de muñeca y de tarjetas de visita de los dioses<br>
Armodio Nerón Calígula Agripina Luis II de Baviera<br>
Antonio Cretina César<br>
Tu nombre aparece intermitente<br>
Sobre un inmenso ombligo de panadería<br>
A veces ocupa el horizonte<br>
A veces puebla el cielo en forma de minúsculas abejas<br>
Siempre puedo leerlo en todas direcciones<br>
Cuando se agranda y se complica de todas las palabras que lo siguen<br>
O cuando no es sino un enorme pedazo de lumbre<br>
O el paso furtivo de las bestias del bosque<br>
O una araña que se descuelga lentamente sobre mi cabeza<br>
O el alfabeto enfurecido<br><br><br>

VI<br><br>

El agua lenta las variaciones mínimas lentas<br>
El rostro leve lento<br>
El suspiro cortado leve<br>
Los guijarros minúsculos<br>
Los montes imperceptibles<br>
El cayendo lenta<br>
Sobre el mundo<br>
Junto a tu reino calcinante<br>
Tras los muros el espacio<br>
Y nada más el gran espacio navegable<br>
El cuarto sube y baja<br>
Las olas no hacen nada<br>
El perro ve la casa<br>
Los lobos se retiran<br>
El alba acecha para asestarnos su gran golpe<br>
Ciegos dormidos<br>
Un árbol ha crecido<br>
En vano cierro las ventanas<br>
Miro la luna<br>
El viento no ha cesado de llamar a mi puerta<br>
La vida oscura empieza<br><br><br></p>

<p><em><strong>The Scandalous Life of César Moro</strong></em></p>

<p>Disperse me in the rain in the smoke of torrents that pass<br>
Beyond the night where we meet each time they draw aside the clouds<br>
That reveal themselves to the eyes of lovers when they leave <br>
Their sturdy castles whose towers of blood and ice <br>
Tinge the ice and rip through leaping of belated homecomings<br>
My friend the king brings me to the side of his royal and real tomb<br>
Where Wagner guards the door with the faithfulness<br>
Of a dog gnawing at the bone of glory<br>
While intermittent and divinely mournful rains<br>
Wear down the hairdo of the flying streetcar of relapsed homicidal<br>

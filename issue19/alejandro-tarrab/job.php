<p><strong>Our Art</strong></p>
<p>
We hate each other. We give each other<br>
sad folk songs and harsh blows with stones of sound. Low strokes, on our faces. We like what we do. We’re here to hate each other, to spit at each other and yank each other by the face. With needles in the chest, my heart. Center, scepter. We dance. We draw our heads together in such a way that we can hear each other: our art. We build a shelter, an altar full of portraits: us, talking nonsense, dancing to shove, tough. Later we see the photos, daguerrotyped, burned and yellow, and we nearly weep. Together. With needles in the eyes, my heart, we build what’s ours: sad songs, harsh blows thrown and legible, a fine dust just alighting on the skin. In those photos there are children in formaldehyde. Fetuses diluting in the image. Aberrations of two heads saying <em>art</em> into the ears. We see them and we implore for us, for the sons of the sons and so on. Soon we end up laughing. It’s moving. How is it that one thing leads to another?, how is it that one extreme stays behind and drags us toward the other? To laugh at what we are, with the needles now giving way. Sedation. Contractions of happiness. Shoves of happiness together. So, we feel a new compulsion to create. Something we call <em>ours</em>, neither yours nor mine. Something inherited, founded, bound to a history, but that isn’t history. Something other, unoriginal, that burns us such that we must say it. Something flagrant and anonymous. A performance that hits the un-center of what’s created. Your head close to mine (percussion, I can barely hear you). In secret you captivate me <em>we have to dance. Something ephemeral, tragic, and fractured. Dangerous.</em> Strobes, harsh blows against the stone. We’re here to hate each other. There’s a song whose lyrics say (I’m no good at translation): we undress in the darkness and my fingers 
will pass through the holes in your spine.</p>
<p><br></p>


<p><strong>Arte nuestro</strong></p>


<p>Nos odiamos. Con canciones folk<br>
tristes y golpes duros con piedras de sonido, nos damos. Toques bajos, en la cara. Nos gusta lo que hacemos. Estamos aquí para odiarnos, para escupirnos y sacarnos por la cara. Con agujas en el pecho, corazón. Centro, cetro. Bailamos. Juntamos de tal manera las cabezas, que podemos oírnos: arte nuestro. Construimos un refugio, un altar con retratos: nosotros hablando sinsentido, bailando a empellones, recio. Después vemos las fotos daguerrotípicas, quemadas y amarillas, y lloramos casi. Juntos. Con agujas en los ojos, corazón, construimos lo nuestro: canciones tristes, golpes duros que proyectan y pueden leerse, un polvo fino posándose apenas en la piel. En esas fotos hay niños en formol. Engendros diluyéndose en la imagen. Aberraciones de dos cabezas diciéndose al oído <em>arte.</em> Las vemos e imploramos por nosotros, por los hijos de los hijos y así. Acabamos pronto riendo. Es conmovedor. ¿De qué manera una cosa lleva a otra?, ¿de qué manera un extremo queda atrás y nos arrastra hacia otro extremo? Reírse de lo que somos, con las agujas ya cediendo. Sedación. Contracciones de alegría. Empellones de alegría juntos. Entonces, sentimos un nuevo impulso por crear. Algo que llamamos <em>nuestro</em>, que no es tuyo ni mío. Algo heredado, fundamentado, ligado a una historia, pero que no es historia. Algo ajeno no-original, que nos quema y debemos decirlo. Algo flagrante sin su autor. Una performance que acierte en el no-centro de lo creado. Tu cabeza junto a la mía (percusiones, casi no puedo oírte). En secreto me cautivas <em>hay que bailar. Algo efímero, aciago y fraccionado. Azaroso</em>. Led, golpes duros contra la piedra. Estamos aquí para odiarnos. Hay una canción cuya letra dice (no soy bueno traduciendo): nos quitaremos la ropa en la oscuridad y con los dedos
ellos repasarán los huecos de tu columna.</p>





            
           




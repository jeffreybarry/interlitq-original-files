    <td colspan="2" valign="top" class="texto2"> I am delighted to welcome you 
      to Issue 1 of <i>The International Literary Quarterly</i>.<br> <br>
      We strive to publish the best in contemporary literature while shunning 
      all ideological affiliations. Indeed, the driving force behind <i>The International 
      Literary Quarterly</i> is that it be a broad church in the world of letters, 
      a forum for outstanding poetry and prose, whether in its original version 
      or in translation, and for criticism that is trenchant and thought-provoking.<br> 
      <br> </td>

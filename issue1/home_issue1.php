        Novack</a>; new poetry by <a href="http://www.interlitq.org/issue1/george_szirtes/bio.php" class="link1">George 
        Szirtes</a>; and an essay on the Italian author, Curzio Malaparte, by 
        <a href="http://www.interlitq.org/issue1/daniel_gunn/bio.php" class="link1">Daniel 
        Gunn</a>. All of these contributions are published alongside artwork by 
        the review's Art Editor, <a href="http://www.interlitq.org/staff/calum_colvin/bio.php" class="link1">Calum 
        Colvin</a>, the distinguished Scottish artist.<br>
        <br>
        We hope that you will return to visit us three months from now. Writers 
        do not exist in a void and literature is a collaborative process, a cross-fertilization 
        of perceptions and ideas between the author and the reading public.<br>

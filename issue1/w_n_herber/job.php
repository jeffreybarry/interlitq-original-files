          <td colspan="2" rowspan="2" valign="top" class="texto"><p align="left">vertebra
              by intercostal cog, your shoes<br>
              loll outwards as the blade &#8211; an eyebrow of steel, <br>
              the moon&#8217;s regard &#8211; begins, as wielded by<br>
              this nun-battered Dublin-Geordie lass who lifts<br>
              your jowls gently in the snow-lit morning<br>
              and strums upon the fretboard of your throat.<br>
              For this is where all opera takes root,<br>
              the pulse of your nostalgia for unlived-in<br>
              eras, that sin of breathing elsewhere than<br>
              this greedy moment&#8217;s need to blame, <i>verismo</i><br>
              is only conjured by proximity<br>
              to blood. All chatter falls like an old key falls <br>
              and cuts the slush, the orchestra of combs<br>
              and scissors seems to pause, to concentrate <br>
              on this small nearby risping shifting note<br>
              as though to cracklings in an infant&#8217;s lung.<br>
              She is the diva of scrape, the <i>spinta </i>of slice,<br>
              her <i>tessitura</i> runs from jugular<br>
              to nostril till she smacks you back<br>
              into the day you&#8217;ll haunt with alcohol<br>
              and soap, anachronistic neck,<br>
              shaven and shriven and white as a baton.</p>
            <p align="left"><br>
              <span class="titulo1">Northumberland Deer</span></p>
            <p align="left">She&#8217;s startled over stubble by our train,<br>
              this doe that veers between the ghosts of trees<br>
              but, finding only openness and sea,<br>
              goes bounding from her instincts&#8217; Pleistocene<br>
              towards a gathering of farm machines.<br>
              This tamed herd, too inert to sense her, lies<br>
              within a yard&#8217;s false lee, though one head rises:<br>
              door slamming, thinking keys, he hasn&#8217;t seen<br>
              her yet as we keep passing, must maintain<br>
              our epidemic progress &#8211; driven by<br>
              some mimic fear she stirs in us, we fly<br>
              towards the border set down in our brain.<br>
              Her flight will meet a stranger in the breeze,<br>
              while ours is from ourselves as a disease.</p>
            <p align="left"><br>
              <span class="titulo1">The Sickle</span></p>
            <p align="left">The second day my hand still trembled from<br>
              the sickle. We see it now as attribute,<br>
              those ageing symbols' symbol, death and work,<br>
              and like to overlook the thing itself,<br>
              bulb-handled in warm wood, the cursive blade<br>
              a darkened, runnelled metal, cheaply made<br>
              and left inside the old tin bath with saws,<br>
              fence staples, in the dust-black, padlocked shed<br>
              among the furniture and frames thrown out<br>
              of the old peoples' version of a house,<br>
              the cobwebbed halter for their long-dead mule.<br>
              We want to make it moon and question mark,<br>
              cedilla of skeletal script, a lip,<br>
              but it is quite at ease with all this mess,<br>
              the afterlife of things and half-life of<br>
              their meanings: it's accustomed to the edge<br>
              between the real and the irrelevant.<br>
              A little oil would help it sing out as<br>
              it's lifted from its bed; serrations, rust,<br>
              acknowledge its return to use, to light.<br>
              And all I did was cut the long dry grass <br>
              behind the outhouse where the washing line<br>
              plays out its yellow plastic smile. I took<br>
              their three foot nodding lengths in hand,<br>
              half baby fishing rods and half the shades<br>
              of ostrich feathers, and I hacked them once<br>
              or twice, and cut their shins and thistles' throats<br>
              until our towels could hang in peace.<br>
              And all the time the sickle silently<br>
              displayed its neatness, crooking in the strays<br>
              and never needing more than three light chops<br>
              at any head, and though I cut away<br>
              from my leg every time it whispered past<br>
              'flesh of my edge, bone of my blade,' and cut<br>
              until it was too easy to cut close,<br>
              and then I paused, and put the thing away.</p>
            <p align="left"><br>
              <span class="titulo1">Visitation Hour</span></p>
            <p align="left">There is another level of the dead<br>
              from those who fill dreams with their frightened stench<br>
              or those whose gaping presence needs our dread<br>
              to drink from like Ulysses' bloody trench.<br>
              You know it by its ease, that stirring from<br>
              some porous sleep to these, as crossbow-spined,<br>
              boletus-palated, you watch them form<br>
              and savour your upended time like wine --<br>
              although these are the neckless bottles, ripped<br>
              old skins, the failures to contain, poor loves;<br>
              the failures to begin, who, half-unzipped,<br>
              consult with me and cannot find their graves<br>
              until the cockerel calls and stamps his mound,<br>
              and I write all their damned prescriptions down.</p>
            <p align="left"><br>
              <span class="titulo1">A Myth of Scotland</span></p>
            <p align="left">Even twenty years ago, given the large proportion
              of the population descended from successive waves of settlers, the
              teaching of Scottish history and literature was a commonplace in
              South America and in the southern United States. However, Scottish
              Studies have fallen into a sudden and appalling state of decay since
              the final collapse of the Second Republic in 1998, exactly three
              hundred years after the establishment of New Caledonia in Panama.
              I therefore intersperse a few comments to contextualise this brief
              selection of poems.</p>
            <p align="left"><br>
              The astonishing rapid success of the Darien Scheme, which meant
              settlers had driven the Caledonian Canal through that narrow isthmus
              of land between the two oceans by 1707, at least gave Scotland mercantile
              independence, and that it could begin to plot secession from the
              more material dominion of the English.</p>
            <p align="left">&nbsp;<br>
              <span class="texto">from</span><span class="titulo1"> Darien</span><br>
              &nbsp;</p>
            <p align="left">Sir William laid his wife and bairn<br>
              fu meekly in the mould<br>
              and twined a lock frae ilkane&#8217;s hair<br>
              and said, &#8216;Noo I am auld.&#8217;<br>
              <br>
              &#8216;But simmer&#8217;s heat at last is past<br>
              and we hae breid eneuch,<br>
              nor England&#8217;s ban nor Spanish hand<br>
              sall cast us from this cleuch!&#8217;<br>
              <br>
              And aa that Company o Scots<br>
              gaithered roond him then<br>
              the livin and the deid aneath<br>
              the sile o Darien.<br>
              <br>
              Fair fa, fair fa, tae Panama,<br>
              the font o oor remeid,<br>
              whaur ilka coin o Scottish gowd<br>
              wiz paid fur by oor deid.<br>
              &nbsp;</p>
            <p align="left"> By 1796, then, and the establishing of the first
              Scots Republic, the wealth of South American trade had transformed
              the nation&#8217;s capital, and herds of <i>llamas</i> roamed the
              Highlands. The literary scene, too, was thriving, as a wealthy Edinburgh
              supported the renewed interest in the Scots &#8216;language&#8217;
              as it was reclassified, and the craze for &#8216;Ploughman Poets&#8217;
              meant that more successful figures like Robert Burns could retire
              from physical labour and hold literary court, drawing the nascent
              English &#8216;Romantics&#8217; north. </p>
            <p align="left"><br>
              from <span class="titulo1">To a <i>Llama</i></span><br>
              &nbsp;</p>
            <p align="left"> O humpless camel, eel-neckit sheep,<br>
              brocht tae Scotia owre the Deep<br>
              fae Darien tae nibble neeps,<br>
              maist haughty Llama,<br>
              fae Panama tae Perth, ye leap<br>
              oor panorama.<br>
              <br>
              Yet when I glance intae yir een<br>
              nae insicht in there dae I glean<br>
              at aa thae views ye maun hae seen<br>
              Peebles tae Peru!<br>
              Yir goave is dozent, lunkit, mean,<br>
              ye parvenu.<br>
              <br>
              Ye mind me o thae English bardies<br>
              wha hunker roond me in thir cardies<br>
              nithert, noddin, Ned-come-tardies<br>
              that luke sae glum --<br>
              De Quincy, Keats -- aa dull Worthwardies<br>
              oan laudanum.</p>
            <p align="left">Throughout the 19th century, Scottish writing thrived,
              with Scott&#8217;s invention of detective fiction in <i>The Murder
              of Midlothian</i>, a recreation of the assassination of Bonny Prince
              Charlie, or &#8216;BPC&#8217; as he became known. Hogg&#8217;s <i>Confessions
              of a Justified Winner</i> satirised the dubious morality of the
              Scottish entrepreneur, whose wealth was gained not just in Central
              America and the West Indies, but on the slave plantations of the
              southern states of the US. When the Dundee poet William MacGonagall
              visited New York in 1887, Scottish writers were still held in sufficient
              esteem that he was immediately introduced to Walt Whitman, with
              whom he formed a long and affectionate friendship.</p>
            <p align="left"><br>
              from <span class="titulo1">The Queen of New Jersey</span><br>
              <br>
              When I first came to the Town of New York<br>
              I bobbed up Broadway most like a cork<br>
              and happened upon a lecture by the Good Grey Poet,<br>
              Walt Whitman of Camden, and before you could know it<br>
              I became his best friend because of my rhymes<br>
              which he thought as rough as my hands or the times<br>
              and said that my hair was as dark as Poe's raven<br>
              and that he liked a Scotchman to be clean-shaven<br>
              whereas his beard and hair was as white as Glencoe<br>
              and he was going to eat shad in Gloucester, and would I like to
              go?<br>
              And soon we shared his cottage in Camden<br>
              and I was as happy as a Rangers fan in Hampden.</p>
            <p align="left">By the early twentieth century, Scotland&#8217;s &#8216;empire&#8217;
              was taking on an increasingly virtual existence, while the advocacy
              of &#8216;Scots&#8217; as a separate language was beginning to seem
              increasingly anachronistic. Small wonder that a new and iconoclastic
              school of poets arose in response to Scotland&#8217;s neutrality
              during the First World War, advancing <i>realpolitik</i> theories
              of Union with England, and satirising the traditionalists. The most
              prominent of these was Christopher Murray Grieve, who invented the
              comic persona of &#8216;Hugh MacDiarmid&#8217;, most baroque of
              Scotsmen.</p>
            <p align="left"><br>
              <span class="titulo1">The Gutterjaw</span><br>
              <br>
              Ae dreich forenoon whaur the spew&#8217;s whummelt<br>
              Eh heard thon gantin soon<br>
              a gutterjaw wi&#8217;iz harns kicked oot<br>
              address thi mune &#8211;<br>
              and Eh thoch o the way yir fiss luked torn<br>
              when you were born.<br>
              Eh didnae keek in thi chanty&#8217;s bowl that nicht,<br>
              Eh hudnae time,<br>
              but Eh hae thocht o thon pissheid&#8217;s speak<br>
              ever sin syne &#8211;<br>
              and Eh think ilka nicht as Eh pit ye tae bed<br>
              ye&#8217;ll grow up a ned.</p>
            <p align="left">Grieve&#8217;s cynical vision of intra-British politics
              appeared justified by Churchill&#8217;s decision to occupy Scotland
              during World War Two in order to prevent England being surrounded
              by questionably &#8216;neutral&#8217; states. The post-war Restoration
              left Scotland impoverished and insecure, and the discovery of oil
              in the 1970s came too late for the country to yet again reinvent
              itself. The Oil Wars with Norway and Iceland saw the tenacious Darien
              Spirit cited for the last time by the nation&#8217;s first female
              premier, Margo MacDonald, as the limited naval resources of the
              nation contested for a share of the new world market.<br>
              <br>
              From <span class="titulo1">War-Songs of the Nation's Favourite,
              Lena Martell</span><br>
              <br>
              One well at a time, Sweet Jesus,<br>
              That's all our navy defends,<br>
              Just give us the strength to pump all the oil on which we depend,<br>
              Shetland is gone, Sweet Jesus,<br>
              and Orkney may not hold the line,<br>
              Help Margo today<br>
              to blow up Norway<br>
              one fjord at a time.</p>
            <p align="left"><br>
              <span class="titulo1">The Ancient Association of Amphibians</span><br>
              <br>
              When I was young enough for New Year&#8217;s Day<br>
              to hold no threat yet of heid-nipped dehydration<br>
              a dozen souls would dip into the Tay<br>
              and, baptised as the dawn&#8217;s most blue-faced babes,<br>
              become ambassadors for all translation.<br>
              (As black Leb substituted for Black bun,<br>
              I recast them as a cult with sunken graves<br>
              they visited to harvest their frog-faced young.)<br>
              A change of motive tells us how we&#8217;ve changed:<br>
              this year the pier was hoatching with a host<br>
              of learner divers, diverse pirates, chained<br>
              to a lemming need to crack the river&#8217;s crust.<br>
              I watched a Santa swim for Lapland, and thought,<br>
              &#8216;One well-positioned whale could swallow the lot.&#8217;</p>
            <p align="left"><br>
              <span class="titulo1">The Heel</span></p>
            <p align="left">I'd just come out of Exhibition Park,<br>
              remembering that weed I'd tasted once <br>
              in the <i>aloni</i>, the old threshing circle in Apokoronas,<br>
              considering its straggly sweetness <br>
              and staring at a roadsign as</p>
            <p align="left">the car caught her by the heel. <br>
              The sign was for the street the hospital is on, <br>
              and said 'Queen Victor' because someone <br>
              had broken the rest off, working the metal <br>
              back and forth until it tore.</p>
            <p align="left">She'd stepped in her fur-lined floppy boots<br>
              into three lanes of morning traffic,<br>
              and neither she nor the car had yielded,<br>
              so there it was, somehow Amazonian, her heel,<br>
              pinched between barely-yielding rubber</p>
            <p align="left">and the tarmac. She flopped on the bonnet<br>
              and he half got out, not realizing,<br>
              and the weight rocked on her<br>
              for a further moment as we yelled, <br>
              as though it was assessing something.</p>
            <p align="left">And when, released, her ankle swelled,<br>
              I was telling her to take her boot off<br>
              because I'd been a passenger in Dave Kean's dad's car<br>
              one time he dropped us at the Grove<br>
              and I'd put my foot out before he'd stopped.</p>
            <p align="left">He was shouting sorry, offering <br>
              to phone or drive, but I'd already gone <br>
              back to the circle filled with asphodel,<br>
              where once the farmer on his wheel-less chariot,<br>
              the <i>volosiros</i>, would be dragged across the wheat.</p>
            <p align="left">And I was really thinking of that grip<br>
              when the mother dipped her infant in the Styx, <br>
              nipping at his heel so as not to <br>
              lose him, whoever he might kill, not<br>
              to lose him ever to those shadowy fields.</p>
            <p align="left"><br>
              <span class="titulo1">Eschatology of the Fly</span></p>
            <p align="left">Why did you do me downward as I dithered by,<br>
              dozy as a dachshund in the warm mid-air?<br>
              On the fifth lap of the kitchen, then, you caught me out,<br>
              smacked me with the whammy of your flat moon-mace.<br>
              You broke my gallant topsail of the sheeniest glass,<br>
              shattered every pane upon my mizzen mast,<br>
              as well as stoving in my stubbled fur-box ribs,<br>
              so that I lay along the painted concrete floor<br>
              and panted as I pedalled at the lower nocht<br>
              as though it were a bike, and saw the ceiling be<br>
              the bottom of the clearest deepest sea all set <br>
              for my abrupt departure. Was it exercise<br>
              or envy or the chance to try your giant eye <br>
              and flex your long slow arm against my circuitry?</p>
            <p align="left">Come with me now into the after-tunnels where<br>
              gravestones stick like eggs against the worm-built walls<br>
              and larva struggle in their sticky shrouds for air.</p>
            <p align="left">In winglessness we must crawl in these gutters up<br>
              through the seepage of the silage and your honey tears, <br>
              sometimes across the tin gut&#8217;s floor and sometimes on<br>
              the grey intestine&#8217;s roof tiles where we&#8217;ll tattoo out<br>
              dead <i>mariachis</i> in our tap shoes till at last we meet<br>
              a porter of those stoppered parts, who sizes you<br>
              for seizing or for ease of the innoculant &#8211;<br>
              and so appears behind you with the needle&#8217;s push<br>
              into your lolly headpiece like a long sharp hair,<br>
              then the nulling of your dream of only being you<br>
              begins just like a bleaching of your nasal stems:<br>
              wasabi-eucalyptus then the lung-green pin<br>
              porcupines the passages behind your eyes<br>
              till what you are can&#8217;t get away from all you&#8217;re not.</p>
            <p align="left">And then you&#8217;re just some runnel-scurf and well-chewed
              wax<br>
              they roll in grit and grease until you globulate<br>
              as jubejubes for their ratty daughters that depend <br>
              among the ribcage webs &#8211; one eyelash all you&#8217;ve left<br>
              and all it knows of ways to go is up the shaft,<br>
              the bones all scratched with charms in every lingo known,<br>
              with canting old confessions, incorrect accounts,<br>
              commandments in such characters as brook no doubts<br>
              though nobody can read them now their lights are out.</p>
            <p align="left">And everywhere the twining worm and rusty ant<br>
              and everywhere the bat&#8217;s crapped and the white owl flits<br>
              until the collarbones patrolled by dragonflies<br>
              where mantises catch hold of you like prayerwheels<br>
              and pop you in that hive-head of the rolling eyes &#8211;<br>
              Beelzebub the brilliantined colossal lord<br>
              surveys this middle realm through every aperture<br>
              that&#8217;s opened to him through your artful wantonry<br>
              and watches you as closely as the One above.</p>
            <p align="left">Did you suppose we didn&#8217;t have necropoli &#8211;<br>
              a species focussed so upon the nethers&#8217; flux?<br>
              That nobody could judge you as you broke the bread<br>
              I&#8217;d crawled across and emptied out my juices on?<br>
              A million small communions each compose our world,<br>
              and mean we all share constantly the joyous feast<br>
              and once we are consumed we share a common dream.</p>
            <p align="left"><br>
              <span class="titulo1">Dead Villages</span></p>
            <p align="left">Because the needle sharpness of the dark,<br>
              its million brilliant pores, means that instead<br>
              of puffy orange you can see the trail,<br>
              that sky-long cloud of the galactic spill;</p>
            <p align="left">because that renovated sky is pitched<br>
              above your memories of childish night;<br>
              you start to think you're travelling in time <br>
              back through the laden rooms, the mannered past</p>
            <p align="left">towards a coolness somewhere in these hills,<br>
              the height of which is only measured by<br>
              the constellated legions they eclipse, <br>
              where they inhabit though they're not alive</p>
            <p align="left">those lightless villages you could drive through<br>
              though your lights too would then refuse to find them<br>
              outside the empty-glassed <i>cafeneion</i><br>
              or gathered underneath the walnut trees.</p>
            <p align="left">Or so you think as you fold up the sheets <br>
              you hung out in the midday heat and then <br>
              forgot: they could be gathered there beneath <br>
              these still undancing points and looking up.</p></td>

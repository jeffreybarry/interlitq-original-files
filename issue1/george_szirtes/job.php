        <tr> 
          <td colspan="2" rowspan="3" valign="top" class="texto2"> <strong><br>
            </strong>
            <div align="center"> 
              <p align="left"><span class="texto2"><strong>1. Set the clock going</strong></span><br>
                <span class="texto2"><br>
                Time shrinks back into itself, curls up small,<br>
                until it&#8217;s no more than a newly-born<br>
                child or a fieldmouse scurrying past a wall</span></p>
              <p align="left" class="texto2">of the house where the child lies. 
                Time shorn<br>
                of its fur, of its history, is not time<br>
                but absence, something you can shoe-horn</p>
              <p align="left" class="texto2">into any space. It is the romantic 
                sublime<br>
                waiting for us in bed with days on its hands,<br>
                with history, change, the regular chime<br>
                <br>
                of the body-clock, the rustling sands<br>
                of the human hourglass with its nipped waist, <br>
                the breathless waiting for a moment that stands</p>
            </div>
            <p class="texto2">as still as the furniture so carefully placed<br>
              for human convenience, into which room<br>
              human concerns file with their curious chaste</p>
            <p class="texto2">demands such as: love me. Do not assume<br>
              authority over me. Try to be a comfort.<br>
              Feed me. Empower me. Bring me to bloom,</p>
            <p class="texto2">now! Life is virginal at core and short,<br>
              the ego unbroken, forever folded in<br>
              upon itself, the refuge of last resort.<br>
              <br>
              Difficult then for anyone to begin<br>
              a conversation with love or to start<br>
              anywhere but with the delicate thin</p>
            <p class="texto2">light of the room, the tender colour chart<br>
              of the child&#8217;s face, the frail shell of the voice<br>
              within which it moves, the thrum of the heart</p>
            <p class="texto2">in its barely opaque chest. It&#8217;s best to rejoice<br>
              in what arises, since delight as simple as this <br>
              is singular and never spoilt for choice.</p>
            <p class="texto2">&nbsp;<br>
            </p>
            <p class="texto2"><strong>2. Mind the Door</strong></p>
            <p class="texto2">No shortage of the romantic sublime. It drops<br>
              about your head in bomb-showers, flowering<br>
              into fire. It flashes across your scalp and crops<br>
              <br>
              mountains to leave the ashen dead cowering<br>
              in burned-out ditches. It careers across the road<br>
              in large goods vehicles, rises in towering</p>
            <p class="texto2">buildings, up lift-shafts, reads the genetic code<br>
              of suffering and cracks it like matchwood, the me<br>
              and you of it gone. It is happy to explode</p>
            <p class="texto2"> itself then come back for more. It whips the sea<br>
              up like stiff cream and shatters it like glass<br>
              then spills it into the shallow sanctuary</p>
            <p class="texto2">where the poor have gathered waiting for it to pass.<br>
              The child&#8217;s bed is a speck of dust it notes<br>
              in its passage across a million blades of grass,</p>
            <p class="texto2">one of several million other such motes<br>
              of light it sweeps by, lonely, magnificent,<br>
              meaningless as it calms down and floats</p>
            <p class="texto2">away serenely with its energy unspent.<br>
              The same with history whose headlong rush<br>
              excuses the odd unfortunate event</p>
            <p class="texto2">preferring power, the work of the broad brush,<br>
              to responsibility with its insistence<br>
              on clean laundry and a regular wash.<br>
              <br>
              History&#8217;s inches are icons of long distance,<br>
              its ideas best realized at heights<br>
              requiring neither comment nor assistance</p>
            <p class="texto2">from those citing individual rights<br>
              as their excuse. The child lies in its bed<br>
              watching light shift while days and nights</p>
            <p class="texto2"> succeed each other, wholly unassisted.<br>
              desire begins and loss. Music swells<br>
              behind the closed door where the elders tread. </p>
            <p><br>
              <strong><span class="texto2">3. The Girl in the Dressing Gown</span></strong><br>
              <br>
              <span class="texto2">A girl in a dressing gown, about nine years 
              old,<br>
              turns towards the camera and smiles back<br>
              at the photographer. It is the controlled</span></p>
            <p><span class="texto2">moment. It is Vermeer and Goya, the crack<br>
              between great landmasses of becoming,<br>
              almost an elsewhere, the point where the track<br>
              <br>
              becomes visible as it vanishes, numbing<br>
              and quickening. You can see the earlier face<br>
              and read the later one. Time keeps drumming</span></p>
            <p><span class="texto2">its fingers impatient with being in one place<br>
              so long, but here it is now. Look. Just there<br>
              in the flash of the eye, in the faint trace</span></p>
            <p><span class="texto2">of foreknowledge we detect and seem to share<br>
              with her like hindsight or a guess<br>
              we must hazard because chance is everywhere.</span></p>
            <p><span class="texto2">Slowly the word &#8216;you&#8217; begins to 
              address<br>
              her condition. You: the camera&#8217;s trick<br>
              of intimacy and numb tenderness.</span></p>
            <p><span class="texto2">So let us say you to her. You, says the click<br>
              of recognition. Yes that was the girl<br>
              with the pearl ear-ring, the aristocratic<br>
              <br>
              </span><span class="texto2">Maya, the startled bride in Chagall&#8217;s 
              whirl-<br>
              wind orbiting the moon. But this is merely<br>
              conversation between images that unfurl</span></p>
            <p><span class="texto2">like flags to mark the spot where you nearly<br>
              stand at age nine, the future drawn towards you<br>
              determined never to see you as clearly</span></p>
            <p><span class="texto2">as the present can. Something is travelling 
              through<br>
              the grey veins of the grey print. I daren&#8217;t name it<br>
              because naming is too easy. Naming is what we do</span></p>
            <p><span class="texto2">when taking possession. We call it and so 
              claim it,<br>
              but the moment is unclaimed. The image is elsewhere.<br>
              Let somebody else develop it and frame it.</span></p>
            <p><br>
              <strong><span class="texto2"> 4. Naming</span></strong><br>
              <br>
              <span class="texto2">That you swims out of incomprehensible<br>
              water, a local stream bearing shopping bags,<br>
              old shoes, dead leaves, petals, past a scribble</span></p>
            <p><span class="texto2">Of froth caught on the bank where the tide 
              drags<br>
              past low branches according to its own<br>
              genetic code, a hieroglyph all swags</span></p>
            <p><span class="texto2">and flourishes, the flowing and the flown<br>
              merging into each other. Who can read<br>
              a language so mumbled, in such undertone?</span></p>
            <p><span class="texto2">Threadbare capillaries tangle and bleed<br>
              under the skin of it. I&#8217;ll tell you who you are<br>
              says the bully. You are the narrow weed</span></p>
            <p><span class="texto2">I can twist round my finger. You are the avatar<br>
              of terminal weakness. I name you mine<br>
              and dispose of you. I can throw you as far</span></p>
            <p><span class="texto2">as a body can fly. Beyond the thin grey line<br>
              of the stream, on the far bank, behind a wall<br>
              that shelters a garden, in its own crystalline</span></p>
            <p><span class="texto2">moment, the self hears the distant call<br>
              of the voice that must find it, that knows its shape<br>
              from within, but is not quite locatable</span></p>
            <p><span class="texto2">either in the head or in the singular landscape<br>
              they both inhabit. The landscape is all echo<br>
              and distance. It is impossible to escape</span></p>
            <p><span class="texto2">from it and see it whole. So you, my love, 
              know<br>
              no more than I do where we stand and hear<br>
              each other call by name whatever names shadow</span></p>
            <p><span class="texto2">in voices that waken, comfort and endear<br>
              themselves to us through their brokenness<br>
              and make a piercing sweet cry in the ear.</span></p>
            <p>&nbsp;</p>
            <p><strong><span class="texto2">5. Dressing</span></strong></p>
            <p><span class="texto2">Out of the dark a torso, more garment than 
              flesh,<br>
              the weight of invisible breasts behind the high<br>
              empire line and twist of cord; empty and fresh</span></p>
            <p><span class="texto2">as air. It is just waiting for you to try<br>
              the outfit on for size, to become woman<br>
              and fill it out without pondering the why</span></p>
            <p><span class="texto2">and how of it, to step into the common<br>
              form-hugging sheath and gracefully undertake<br>
              the obligations implied by such things. No-one<br>
              <br>
              goes naked in the world after all. For whose sake<br>
              do you become who you are? Are you alone<br>
              in the dark? Is it for yourself you ache</span></p>
            <p><span class="texto2">in the morning? Even if you were stone,<br>
              like a goddess, you would desire beyond<br>
              your fixity something already half-known</span></p>
            <p><span class="texto2">yet negotiable. As a child you respond<br>
              to the adult&#8217;s gravity with a blank stare<br>
              of instinctive hunger. You touch your blonde</span></p>
            <p><span class="texto2">hair and bunch it in your fist. You prepare<br>
              your flirtatious look. You play at control,<br>
              then lost, start crying at the small despair</span></p>
            <p><span class="texto2">you&#8217;re stuck with. But this is the soul<br>
              prepared for you, these garments that glow<br>
              in the dark and burn as fierce as coal.<br>
              <br>
              and out of the same dark step the slow<br>
              suitors in their allotted garments, unsure<br>
              of their own identities, hoping to follow</span></p>
            <p><span class="texto2">the patterns they&#8217;ve guessed at, a mature<br>
              untroubled roundness weighing at their hearts,<br>
              and the breasts press against cloth as if nature</span></p>
            <p><span class="texto2">insisted they do so, as if there were darts<br>
              piercing them, as if becoming were all<br>
              in the hollow waiting garment that closes and parts.</span></p>
            <p><br>
            </p>
            <p><strong><span class="texto2">6. Kindertransport</span></strong><br>
              <br>
              <span class="texto2">As a child you move slowly over the vast<br>
              floor of the kitchen, the almost endless yard,<br>
              crawling over both while minutes crawl past<br>
              <br>
              in the opposite direction. Once you discard<br>
              the walking frame you hurtle between handholds<br>
              of chairs and cupboards, taking the odd hard</span></p>
            <p><span class="texto2">knock, while your anxious mother comforts 
              and scolds<br>
              in equal measure. But your world is still small<br>
              as you discover in the car as one hill folds</span></p>
            <p><span class="texto2">into another or an older child climbs the 
              high wall<br>
              that hides the desirable, barely imagined domain<br>
              of elsewhere that is located beyond parental</span></p>
            <p><span class="texto2">granting. The world expands but will never 
              deign<br>
              to offer itself whole, you quickly realise,<br>
              nor does it feel any obligation to explain</span></p>
            <p><span class="texto2">its motives. But what if, before your very 
              eyes,<br>
              things changed, flip over, vanish or explode?<br>
              It happens sometimes. Alien empires rise</span></p>
            <p><span class="texto2">like towers, thousands scatter down the road.<br>
              As a child you move with them unmoved, quaint,<br>
              adaptable, curious, carrying your tiny load<br>
              <br>
              of responsibility. Girl and boy. The faint<br>
              lines of gender wash briefly away in the haste<br>
              of departure without a word of complaint</span></p>
            <p><span class="texto2">in the great gust wafting you onward. Why 
              waste<br>
              time on difference when the powerful cohesive<br>
              of fear holds the clock together and the displaced</span></p>
            <p><span class="texto2">are in one small corner or container? We live<br>
              from moment to moment, say the sharpened senses<br>
              working through ears and eyes, say the plaintive</span></p>
            <p><span class="texto2">cries of those left behind. We are without 
              defences,<br>
              without options. It&#8217;s where we go. It&#8217;s where<br>
              we are. And here we are, clambering over fences,<br>
              <br>
              entering the given space others prepare<br>
              for us, we children, we romantics, we fallers<br>
              in love in gardens, with our fistfuls of hair.</span></p>
            <p><br>
            </p>
            <p> <span class="texto2"><strong>7. Once upon&#8230;</strong></span><br>
              <br>
              <span class="texto2">Once upon a time the story was told<br>
              of a time before time, an unfixed point in space<br>
              we could inhabit while the adult world rolled</span></p>
            <p><span class="texto2">past us, a story in which the desperate race<br>
              was internalised in the drumming sound<br>
              of pulses, settling forever behind the face<br>
              </span><span class="texto2"><br>
              we made at the precise moment we found<br>
              the hidden treasure, the lost child, the frog<br>
              turned prince, the giant or genie unbound</span></p>
            <p><span class="texto2">and risen, the terrifying saucer-eyed dog,<br>
              the witch with the metal nose, the very image<br>
              of Tycho Brahe found years later. Our dialogue</span></p>
            <p><span class="texto2">was with creatures requiring kindness and 
              courage.<br>
              We were not ourselves as seen in the looking glass,<br>
              not firmly definable, but a kind of mirage</span></p>
            <p><span class="texto2">that attended and haunted the back of the 
              class<br>
              when the teacher&#8217;s voice blurred into afternoon<br>
              and the sun had long slipped by under the grass</span></p>
            <p><span class="texto2">and the distant houses were gone or would 
              be soon<br>
              with their ghost inhabitants slipping in through open<br>
              windows, and the light above was a balloon</span></p>
            <p><span class="texto2">drifting to sleep. It was that time, when 
              time was broken<br>
              and piecemeal, that identity gained weight,<br>
              fleshed out. It is that time that is woken</span></p>
            <p><span class="texto2">in us when we seem to fall or gravitate<br>
              towards each other without knowing it,<br>
              a time that is both too early yet late</span></p>
            <p><span class="texto2">enough to be ourselves, the classroom lit<br>
              with eyes, the dream working its way through<br>
              troubled nerves, the nightbirds beginning to flit</span></p>
            <p><span class="texto2">from haze of branch to haze of branch and 
              the blue-<br>
              indigo of our lives solidifying into selves<br>
              we might meet anywhere and address as you.</span></p>
            <p>&nbsp;</p>
            <p><strong><br>
              <span class="texto2"> 8. A Time</span></strong><span class="texto2"><br>
              <br>
              Once upon a time when myths were still falling<br>
              about us like tall shadows, when you emerged<br>
              from infancy and heard the body calling</span></p>
            <p class="texto2">out of the fog of the street, and were urged<br>
              to follow, at first into the garden, and then<br>
              beyond the gate where the hormones surged<br>
              <br>
              like crowds of unruly children, half-grown men,<br>
              and gusts of sheer energy shook you into fear<br>
              and loneliness as is the way with children,</p>
            <p class="texto2">tumbling forward, so deep within your ear<br>
              you could feel the blood gathering then gone<br>
              south, into the world, in the spurt of desire.</p>
            <p class="texto2">Mechanics of growth: the yearning to be done<br>
              and out before desire is itself, no more<br>
              than pain, embarrassment, anticipation</p>
            <p class="texto2">of more pain, at the point where the door<br>
              snaps open, beyond the garden, out<br>
              in the street and beyond, beyond metaphor</p>
            <p class="texto2">into body, into flight, into squalor and doubt<br>
              and romance. The children run forwards<br>
              like troops into fire hearing a distant shout,</p>
            <p class="texto2">the gunfire of laughter and death. Whose words<br>
              are they speaking? Can you hear your own cry<br>
              in the strange cacophony or sound the chords</p>
            <p class="texto2">of the music where they are shouting? You lie<br>
              on your bed. The child is trickling painfully away,<br>
              passing into a future you yourself supply.</p>
            <p class="texto2">I watch from a distance as skinny girls play<br>
              among themselves, I am too young for this.<br>
              They move past me laughing. I must stay</p>
            <p class="texto2">in my childhood a while. Already I miss<br>
              you, the billowing forth that I anticipate<br>
              is not for me, not yet. I am waiting for your kiss.</p>
            <p class="texto2">&nbsp;</p>
            <p class="texto2"><span class="texto2"><strong>9. The Birds</strong></span></p>
            <p class="texto2">Sometimes you see a shape that hovers in the eye<br>
              and lodges itself in the furniture of the mind,<br>
              part light, part sound, between silence, music and cry</p>
            <p class="texto2">that sings out of half-open drawers. You find<br>
              yourself staring at it through the bedroom door,<br>
              caught in the mirror, perfectly defined</p>
            <p class="texto2">like your own incomprehensible face but more<br>
              light, more sound. Sometimes I wake and stare<br>
              at you beside me as if on a foreign shore,</p>
            <p class="texto2">an object cast there, a figure of the air<br>
              locked into itself, strange, magical<br>
              and potent, a burden I can just about bear</p>
            <p class="texto2">to support without the heart breaking, as small<br>
              as this one moment in a universe without scale,<br>
              and, inside that bright moment, the sum of all</p>
            <p class="texto2">such moments, years, decades. Outside, frail<br>
              birds are beginning their morning ablution,<br>
              chirruping and squabbling, a musical Braille</p>
            <p class="texto2">that ears may trace and decipher in the commotion<br>
              as multiple layers of meaning, your face<br>
              more comprehensible than my own, the impression</p>
            <p class="texto2">of meaning everywhere as superfluous grace,<br>
              the windowsill, the sheets, the books by the bed,<br>
              the reflection lightly imprinted on the space</p>
            <p class="texto2">behind a chair, the crumpled clothes, the bedspread<br>
              with its foothills, its geologies. The universe<br>
              arrives at the station. The shape of your head</p>
            <p class="texto2">hangs in the vast forecourt where crowds disperse<br>
              and gather like dense flowers, where everything moves<br>
              forward beyond the street, where there is no reverse,</p>
            <p class="texto2">only, rarely, this blessed stasis: the light that 
              loves<br>
              you and holds you still. And slow as dreams,<br>
              birds rise from the roof, swifts, sparrows, thrushes, doves,</p>
            <p class="texto2">as they have always done, their shapes the phonemes<br>
              of a pure language. And you are there, beautiful<br>
              as their whistles and slurs, their trilling, their screams.</p>

          <td colspan="2" valign="top" class="titulo2">The Birds <span class="texto2">by 
            </span> <span class="link2"><a href="/issue1/george_szirtes/bio.php" class="link3">George 
            Szirtes </a></span></td>
          <td><img src="../../images/issue_img/spacer.gif" width="1" height="23" border="0" alt=""></td>

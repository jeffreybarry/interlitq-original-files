              <p align="left"><br>
                <strong><span class="texto2"> INTRODUCTION</span></strong> </p>
              <p align="left"><span class="texto2">For each group of refugees, 
                even for every individual refugee, the experience of persecution, 
                violence, humiliation, loss of home and loved ones and identity 
                is unique. Yet there is a sameness despite the differences, a 
                commonality. Today we look at Congo, the former Yugoslavia, Iraq, 
                Israel/Palestine. But genocidal violence, the brutality of people 
                who lived in harmony as friends and neighbors before becoming 
                enemies, and the self-interested intervention of outside forces--major 
                powers and their commercial, industrial and military interests-- 
                are nothing new. </span></p>
            </div></td>
          <td rowspan="3"><img name="home_r3_c6" src="/images/spacer.gif" width="16" height="368" border="0" alt=""></td>

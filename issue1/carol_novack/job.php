          <td colspan="2" valign="top" class="texto2"> <p align="center"><strong><br>
              I</strong></p>
            <p align="left">The otiose man is in one of his moods. This morning 
              he awakened with another red mole on his nose. He says the moles 
              are taking over his nose and blames it on poisonous lunar fumes. 
              The mole on the moon on the moon, the jester sings out of tune. 
              That is not funny! says the man.</p>
            <p align="left">He wants a spire on his tower, an acute scimitar of 
              a spire to spear passing swans. He wants swans for his little Dovey. 
              See how she preens herself in the hot-tub, grows bored with the 
              man's ears and mouth, what he cannot hear and what he speaks yet 
              cannot utter.</p>
            <br> </td>
          <td><img src="../../images/issue_img/spacer.gif" width="1" height="219" border="0" alt=""></td>
        </tr>
        <tr> 
          <td colspan="4" rowspan="2" valign="top" class="texto2"> <p align="left">She 
              tries to pry open the shutters, mutters, flutters, sprays French 
              violets onto their featherbed. See how she feigns sleep on the sofa 
              when he pulls at her slippers. See how she keeps her legs crossed 
              so he cannot enter her. The man blames the poor in the projects 
              next door. Her ankles are blue with bruises.</p>
            <p align="left">The naturally dark and winsome architect with cleft 
              chin longs for little blond Dovey with her dewy amber eyes, dancing 
              pheromones, and filigreed fingers crowded with precious stones. 
              She often reads Rilke aloud. Stones don't talk, she confides to 
              the architect, on a spree of impulse.</p>
            <p align="left">The spire shall be taller than the Bishop's, the husband 
              demands. Spare no expense, he commands. Cook will roast swans till 
              tender with honey and mint and all will be well. His wife's tiny 
              belly will swell with little otiose men and the tower will chime 
              their favorite song.</p>
            <p align="left">The architect will spare no sense. He will create 
              a ladder to curve like a swan's neck bending into the sea. There 
              will be a yellow gondola at the bottom of the ladder, a gondola 
              to bear them to Tuvalu.<br>
            </p>
            <p align="center"><strong> II</strong></p>
            <p align="left"> What is their favorite song? The architect wonders 
              as he adds nutmeg to his lentil soup. His daughter's gosling is 
              honking in the outhouse. The child is playing Brahms on her viola. 
              What is their favorite song? Strains of <i>The</i> <i>Flying Dutchman</i> 
              throb through his veins, in vain. How can he possibly find a voice 
              of his own? He will propose love to Dovey on his knees, just as 
              his mother proposed his conception to the architect's father. He 
              will find his own song and sing it, by Jove.</p>
            <p align="left"> The architect plays with a cow bone moist with soup. 
              He thrusts his tongue in the bone's hole to draw the white marrow 
              out of its home. He sucks, wondering what will be their favorite 
              song, the duet of the architect and his bird when she is his. Something 
              that sounds like daffodils, or a song to bring out the color of 
              her eyes? <i>See the<br>
              pyramids along the Nile?</i></p>
            <p align="left"> <i>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
              </i>&nbsp;<strong>III</strong></p>
            <p align="left"> The jester is hiding in the clothes closet of the 
              architect's daughter. Listening to the yearnings of the sensuous 
              viola, he almost forgets his mission. Espionage is not his favorite 
              pastime by a long shot &#8211; more tasteful by far to twirl about 
              on one's fingers and amuse, then lie in a flowerbed gazing at galaxies. 
              His goose will be cooked if he doesn't return with evidence that 
              the otiose man is right. The man <i>must</i> be right.</p>
            <p align="left"> Viola stops moaning and the child leaves the room. 
              Our jester wishes he were anywhere and anyone else and had the courage 
              to do and be other than.</p>
            <p align="left"> As the architect and his daughter relish soup with 
              horseradish, the jester sneaks into the drafting room. If you were 
              there, you might wonder why the bells in his hat don't alarm. One 
              could explain that when he's dejected, the bells are dull. On the 
              other hand, did anyone say he wears a hat?</p>
            <p align="left"> There are two models on the table: one of a scimitar 
              atop a tower; the other, the scimitar's mirror image as a ladder 
              curving down to the sea. One is a war, the other a poem. The jester 
              enters the poem and escapes. He thinks at last longing here I go 
              to Tuvalu, into her arms I shall row.</p>
            <p align="left"> When the architect returns to his table, he will 
              note the disappearance of the yellow boat. He will scratch his head 
              and wonder if he'd moved the gondola, with thought or without, maybe 
              into a cove to protect it from winds. That would've been sensible, 
              but never mind. He will create another gondola. Easy come, easy 
              go.</p>
            <p align="center"> <br>
              <strong>IV</strong></p>
            <p align="left">The otiose man Herbert is in a rage. He has awakened 
              with another red mole on his nose and no jester to proffer evidence 
              of treachery. He throws a tantrum at the cat.</p>
            <p align="left">Little Dovey is preparing Tuvaluan toast with pureed 
              dates and aged chevre from Corfu. The scents vibrate attractively 
              in Herbert's invaded nose. He does not want to think of treachery 
              when the early sun through the shutters alights on her hair; vaguely 
              wonders why his instinct is sharpening the blood-drenched razor 
              he holds in his hand.</p>
            <p align="left">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<strong>&nbsp;&nbsp; 
              V</strong></p>
            <p align="left">You have read Shakespeare and Sophocles (et al.) and 
              imagine that Herbert will kill the architect or at least Dovey; 
              or Dovey will kill Herbert in self-defense. In truth, you would 
              prefer the architect to rescue Dovey in time, in good and right 
              time, your local time zone time, not a thraneen of second too soon 
              or too late. Cinderella and Sleeping Beauty walk in your sleep. 
              So you envision the architect and his Dovey entering the poem and 
              sailing away. </p>
            <p align="left">You have always conjured yourself as either the architect 
              or Dovey, rescued by the architect.</p>
            <p align="left">To be expected the knots are fast and the delicate 
              gondola whirls madly in the robust winds of the Pacific. Dovey clings 
              to the side of the boat, the heroic little boat, without the architect. 
              In a moment of unaware self-insight, he chose war and money, pleading 
              fatherhood.</p>
            <p align="left">The next moments are enigmatic to the populace. No 
              one claims to have noticed the passing of these moments during birthday 
              dinners, wine tastings, stock market undulations, abrupt and slow 
              deaths, and always romantic catastrophes. Rumor claims that Rigoletto 
              appeared to take the lady from the ocean just as her being was expiring.</p>
            <p align="left">On Funafuti, the capitol isle of the isles of Tuvalu, 
              the jester and his lady have made their home a happy ending without 
              aspirations.</p>

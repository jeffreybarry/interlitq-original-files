          <td colspan="2" valign="top" class="titulo2">&#8220;It is the Fate of 
            Europe to Become Naples&quot;: Curzio Malaparte and the Plague of 
            Benevolent Interventionism. 
            <p> <span class="texto2"> by <a href="/issue1/daniel_gunn/bio.php" class="link3">Daniel 
              Gunn</a> </span></td>
          <td><img src="../../images/issue_img/spacer.gif" width="1" height="23" border="0" alt=""></td>

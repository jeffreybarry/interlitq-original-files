          <td colspan="7" rowspan="2" valign="top"><p><br>
              <span class="texto2"> Even still, when they collided, she wasn&#8217;t 
              ready. She was jolted backwards and sideways, shunted so violently 
              that even the air around her wasn&#8217;t prepared and couldn&#8217;t 
              move fast enough. She heard a bang, a painful metallic shriek, and 
              then nothing.</span></p>
            <p class="texto2">Not nothing. She could hear in the distance the 
              car crash soundtrack: squealing tyres, breaking glass, metal compacting 
              on stone, wood, metal, the engine wheezing and hissing.</p>
            <p class="texto2">Gloria scrambled out of the car. Apart from a broken 
              heel on her new boots she was okay. She could see, she could stand, 
              she seemed fully conscious. She rolled her head from side to side 
              and shook her arms and legs. She checked to make sure, squeezing 
              each limb all the way up and all the way down again. She was okay, 
              that was the important thing. The other car had only clipped her 
              nearside, it hadn&#8217;t hit her head on, but whoever was inside 
              the Lexus couldn&#8217;t have been so lucky.</p>
            <p class="texto2">It took her a while to locate her phone but all 
              her rabbity scrabbling under the seats proved pointless anyway; 
              this empty countryside was so forgotten it didn&#8217;t even have 
              network coverage. She was going to have to deal with this alone.</p>
            <p class="texto2">Gloria ran to the field where the Lexus had come 
              to a halt, her height varying with alternate steps: five foot three, 
              five foot six, as she ran. She hoped the driver would be okay, she 
              was looking forward to an explanation as to how, on a quiet country 
              road, he managed to run into her, the only other car for miles around. 
              She might have been killed for God&#8217;s sake. How the hell was 
              she going to get home? Gloria hoped the driver had good insurance. 
              These boots, for instance, had not been cheap.</p>
            <p class="texto2">First sight of it shocked her. Close to a river 
              in a field of young trees, the Lexus lay upside down and smashed 
              to pieces. Those trees that had not bent in the path of the runaway 
              car had broken, leaving them white-boned and jagged. Torn branches 
              lay around and across the battered bodywork, dressing it in camouflage.</p>
            <p class="texto2">The Lexus was rocking slightly on its roof, like 
              an insect unable to right itself. Two of the wheels were still turning 
              impotently in the air. Gloria pulled off the foliage and hunkered 
              down to see inside. Then she wished she hadn&#8217;t. </p>
            <p class="texto2">The driver was dead, no doubt about it: he dangled 
              from his seatbelt harness, his neck bent at an impossible angle. 
              His eyes and his mouth were open. </p>
            <p class="texto2">Gloria looked back at the road. It was still deserted. 
              There was nothing around here, only fields and trees, no people 
              when you needed them but, in the distance on top of a hill, there 
              was a cottage. She would go there and get help. </p>
            <p class="texto2">She tried to block out the broken-necked man, to 
              peer past him to the passenger seat but she couldn&#8217;t see much. 
              She moved round to the passenger door. There was something here 
              that Gloria couldn&#8217;t quite understand. A mouth and a nose 
              pressed against the glass. Only a mouth and a nose: not a face; 
              not eyes and brows and forehead, only a few disparate components 
              of a face. Where was the rest of it? Whatever it had been, whoever 
              it had belonged to, it seemed no longer human, like a specimen in 
              a laboratory jar. As she tried to make sense of it, the glass steamed 
              slightly and cleared, steamed and cleared again, with the rhythm 
              of a faint breath. </p>
            <p class="texto2">The door opened much easier than she had imagined 
              it would and as the contents spilled out they began to make more 
              sense. The mouth and nose were after all, thank God, connected to 
              something else. She had been confused by the fact that every other 
              part of the body was completely covered in a black hooded robe. 
              The heavy body flopped forward on to the wet grass. It was breathing 
              hard and moaning. Gloria moved towards the head end and spoke to 
              it.</p>
            <p class="texto2">'Are you okay?&#8217;<br>
              The shape was no longer suspended by its seat belt, no longer upside 
              down so the nose and mouth were now covered with a cloth flap. Two 
              scared eyes were all that could be seen from a slit in the mound 
              of material. <br>
              &#8216;Are you okay? What&#8217;s your name?&#8217;<br>
              &#8216;Haya.&#8217;<br>
              &#8216;Haya. That&#8217;s good. Are you alright Haya?&#8217;<br>
              &#8216;I don&#8217;t know. I think I am.&#8217;<br>
              Muffled beneath the veil Gloria heard a Glasgow accent as strong 
              as her own. <br>
              &#8216;Where&#8217;s Dilip? Where&#8217;s my husband?&#8217;<br>
              &#8216;I&#8217;m not sure, but we can&#8217;t worry about that now,&#8217; 
              Gloria surprised herself with the instinctive lie. &#8216;We have 
              to concentrate on one thing at a time. Let&#8217;s get you sorted 
              first,&#8217; she said, sounding confident and in charge, &#8216;D&#8217;you 
              have a phone? Mine doesn&#8217;t have coverage here.&#8217;<br>
              &#8216;No.&#8217; <br>
              &#8216;That&#8217;s okay. We&#8217;ll manage. We&#8217;ll have to 
              get help. There&#8217;s a cottage up on the hill. D&#8217;you think 
              you&#8217;re able to walk?&#8217;</p>
            <p class="texto2">Moaning and stretching, Haya pulled up on to all 
              fours, a square shape in the long grass like a draped table, and 
              stayed that way. <br>
              &#8216;D&#8217;you want me to help you walk up there?&#8217;<br>
              &#8216;No, I won&#8217;t make it, there isn&#8217;t enough time. 
              Don&#8217;t leave me here, don&#8217;t leave me, please!&#8217;<br>
              &#8216;No, said Gloria a lot calmer than she felt, &#8216;it&#8217;s 
              ok, I won&#8217;t leave you.&#8217; <br>
              Gloria wanted to hold out her hand to reassure Haya, to take reassurance 
              herself from the contact, but something stopped her.</p>
            <p class="texto2">She had seen veiled women before of course. There 
              were two or three living in her street, she had passed them but 
              she had never actually met them. Beside their rigorous modesty Gloria 
              always felt vaguely guilty though she didn&#8217;t know why. She 
              was Area Sales Manager in a tough business with a team of mostly 
              male staff. She didn&#8217;t consider herself to be immodest but 
              she certainly had nothing to be scared or ashamed of. She was one 
              of the lucky ones. Veiled women reminded her of how lucky she was.</p>
            <p class="texto2">&#8216;Don&#8217;t worry,&#8217; she said, kicking 
              bits of broken glass and plastic away from the surrounding area. 
              &#8216;We can wait here. It&#8217;s not a problem. With a bit of 
              luck somebody up in the cottage will have seen us. There&#8217;s 
              probably an ambulance on the way right now.&#8217; <br>
              As Gloria lifted a dangerous looking piece of wing mirror she saw 
              that the grass was smeared with blood. <br>
              &#8216;We should really take a look at you, see if there&#8217;s 
              anything that needs immediate attention. Are you in pain Haya?&#8217;<br>
              The moaning was now interspersed with loud panting and Gloria dreaded 
              the thought of whatever gory mess lay beneath the big black gown. 
            </p>
            <p class="texto2">She sat beside her in the grass and gently laid 
              her hand on the woman&#8217;s back. Haya was worryingly hot.<br>
              &#8216;Okay, let&#8217;s have a look shall we?&#8217;<br>
              The shrouded head nodded. <br>
              &#8216;Right oh. First things first. D&#8217;you want to take off 
              your&#8230;&#8217; Gloria gestured, circling her face with her finger. 
              &#8216;D&#8217;you want to take it off Haya? You seem awfully hot.&#8217;<br>
              &#8216;I can&#8217;t. I can&#8217;t show my face,&#8217; she said 
              between laboured breaths. <br>
              Her tone was not aggressive but Gloria felt chastised, embarrassed.<br>
              &#8216;Sorry. I&#8217;m so sorry.&#8217;<br>
              &#8216;Not to men. Not if an ambulance is coming.&#8217;<br>
              &#8216;Okay, okay.&#8217; It came out blunt and Gloria hadn&#8217;t 
              meant it that way, &#8216;whatever you want.&#8217; <br>
              &#8216;We were trying to get to my parents,&#8217; mumbled Haya. 
              &#8216;It was my fault. I was scared, the pains were coming faster 
              and faster. I was screaming. Dilip got nervous. He was driving too 
              fast. It was my fault.&#8217;</p>
            <p class="texto2">Gloria was processing this when there was another 
              moan, a deeper, almost satisfied moan and then a gushing visceral 
              sound from beneath the robe. <br>
              &#8216;I think the baby&#8217;s coming now. The waters&#8230;&#8217;<br>
              It was a whisper, as though Haya was ashamed to give Gloria this 
              trouble. <br>
              &#8216;Okay,&#8217; said Gloria with the same clipped tone, &#8216;baby&#8217;s 
              coming, that&#8217;s okay, we can do that.&#8217;<br>
              She stood up and circled Haya, moving, not to escape but as a means 
              to thinking. When she turned back Haya had removed her veil. It 
              lay on the grass beside her, a scrunched-up square of material. 
              Haya was much younger than Gloria had imagined, a plain girl, square-jawed 
              with eyes that were slightly too small for her oblong face. Her 
              hair and face were dripping with sweat.</p>
            <p class="texto2">Gloria smiled. &#8216;Don&#8217;t worry, everything&#8217;s 
              going to be okay,&#8217; she said, squatting down beside her, using 
              her fingers to brush the damp hair from Haya&#8217;s face. <br>
              &#8216;Is it?&#8217;<br>
              &#8216;Of course. Women do this every day, don&#8217;t they?&#8217; 
              <br>
              &#8216;Have you done this before?&#8217; Haya asked.<br>
              Gloria had perhaps given the wrong impression with her confident 
              managerial manner. <br>
              &#8216;Well, no, I don&#8217;t have kids, I&#8217;ve never&#8230;&#8217;<br>
              &#8216;That&#8217;s okay, said Haya kindly, and Gloria heard an 
              unasked-for sympathy.<br>
              &#8216;But nature will take its course, it&#8217;ll be okay.&#8217; 
              <br>
              &#8216;What&#8217;s your name?&#8217;<br>
              &#8216;Gloria. Sorry, can&#8217;t believe I forgot to tell you my 
              name.&#8217;<br>
              &#8216;Thanks Gloria.&#8217;</p>
            <p class="texto2">Gloria patted her lap indicating that Haya should 
              lay her head there, but this was a short-lived hiatus between contractions. 
              <br>
              When the next one came Haya rolled over and arched her back. She 
              flipped up her robe to expose pale but solid legs. She put her thumbs 
              in the waistband of her soaking pants and with one smooth movement, 
              pulled them off. With her legs open Haya gave off a strong vinegary 
              smell.</p>
            <p class="texto2">Preparing for action Gloria took off her pink lambswool 
              cardigan and bunched it into a pillow for Haya&#8217;s head. Then 
              she took the wet pants from her hand and wrung them out. They were 
              top quality Marks and Spencer&#8217;s pants, the same ones Gloria 
              wore, too good to throw away and they might be useful for cleaning 
              the baby. If, please God, the baby came out alive. Haya screwed 
              up her face in agony. <br>
              &#8216;How often are the contractions?&#8217;<br>
              &#8216;I don&#8217;t know.&#8217; Haya squeezed the words out. &#8216;It&#8217;s 
              sore all the time.&#8217;<br>
              &#8216;What are you going to call the baby?&#8217;<br>
              Haya didn&#8217;t answer. Taken over by another wave of contractions, 
              she was grunting through her teeth. Gloria wished she could do something 
              to help her with the pain. She was a brave girl; if it had been 
              her she&#8217;d have screamed blue murder.</p>
            <p class="texto2">The contraction seemed to pass and Haya curled into 
              a ball. Gloria stood over her using first her hands, and then the 
              discarded veil, as a fan. <br>
              &#8216;I knew this bloody thing would come in useful for something.&#8217; 
              said Gloria. <br>
              Haya caught her eye and held it for a second before Gloria looked 
              away sheepishly. <br>
              &#8216;Sorry.&#8217;<br>
              They giggled, and kept giggling, getting more hysterical until the 
              next pain hit and they stopped abruptly. <br>
              &#8216;Haya?&#8217; <br>
              Haya had moved back on to all fours, this time with her legs splayed 
              wide. The pains seemed to be coming more or less continuously. It 
              wouldn&#8217;t be long now. Surely someone in the cottage would 
              have called an ambulance? Hopefully it would get here before the 
              baby. </p>
            <p class="texto2">Haya moved constantly and Gloria followed, kicking 
              sharp debris out of harm&#8217;s way, flapping the veil and blowing 
              on Haya&#8217;s face, the only comfort she was able to offer. <br>
              &#8216;Can you look?&#8217; said Haya.<br>
              It was a second before Gloria understood what she meant.<br>
              &#8216;Yes, of course.&#8217;<br>
              Up to this point Gloria had ignored the fact that Haya was partially 
              naked and studiously avoided looking at her private parts but now 
              she knelt between Haya&#8217;s open legs and peered into her cervix. 
              <br>
              &#8216;Oh my God! I can see it!&#8217;<br>
              Haya was grunting again, a sustained productive grunt this time. 
              She had started pushing.</p>
            <p class="texto2">Gloria put her hands on Haya&#8217;s knees and squeezed 
              hard with her at every contraction. She lifted Haya&#8217;s feet 
              and placed them on her shoulders, giving her something to push against. 
              Gloria could feel her own womb clench and strain with the effort. 
              She felt euphoric, privileged that she was here, squatting in the 
              grass covered in mud and mucus, as feral as uncovered meat, as natural 
              as a flower opening. Haya was strong. She pushed hard and Gloria 
              dug her toes into the grass and leaned forward, never taking her 
              eyes off the dark circle of hair that was slowly emerging from Haya. 
              Amidst the blood and slime Gloria put out her hands to receive the 
              baby&#8217;s hot little head. <br>
              &#8216;I&#8217;ve got it! Keep pushing Haya, come on, nearly there.&#8217;</p>
            <p class="texto2">And then the whole baby slid out. It was so slippery 
              Gloria was afraid she&#8217;d drop it. Its head looked too big and 
              its chest was large in comparison to the rest of the long thin body 
              but it seemed okay. With the shock of meeting the air the baby splayed 
              its fingers and flexed its little body, nearly slipping from her 
              grip. Its tiny chest expanded and it cried out a thin pathetic wail. 
              Using Haya&#8217;s pants Gloria tenderly wiped the small frightened 
              face. She checked for fingers and toes the way she had seen it done 
              on telly and everything seemed to be where it should be.<br>
              &#8216;It&#8217;s okay, the baby looks fine Haya.&#8217;<br>
              Then she remembered to look. <br>
              &#8216;It&#8217;s a wee girl. You&#8217;ve got a lovely wee girl.&#8217;</p>
            <p class="texto2">As Gloria held her close she felt the child warm 
              her breast and, in the time it took to breathe, fell in love. She 
              watched her draw her first breath. She could see how she would grow: 
              the infant becoming a toddler, then a kid, then a woman. Gloria 
              rocked her in her arms, and felt her duty heavy as a warm blanket.<br>
              Haya was still bearing down as Gloria watched with fascination the 
              placenta, a steaming lump of blood and membranes, deposited on the 
              grass. <br>
              &#8216;We have to cut the cord,&#8217; said Haya.</p>
            <p class="texto2">Gloria, still with the child in her arms, reached 
              over and lifted a shard of wing mirror from the grass behind her. 
              She chopped at the umbilical cord which was still attached to the 
              infant but was too squeamish to put any real effort behind it. <br>
              &#8216;Is this hurting you?&#8217;<br>
              &#8216;Don&#8217;t be daft,&#8217; said Haya impatiently, pulling 
              herself into a sitting position, &#8216;here, give me it.&#8217; 
              <br>
              Gloria handed over the piece of mirror and let Haya get on with 
              it. With one hand she gathered up her grass-stained cardigan, shook 
              off the bits of leaves and tinder and wrapped the baby in it. She 
              was a lovely child. As her body became oxygenated her skin flushed 
              and her little glowing face was framed in the pink cardigan. <br>
              Haya cut the cord and fell back in an exhausted heap.</p>
            <p class="texto2">&#8216;Please, can I hold her?&#8217;<br>
              &#8216;Of course you can, she&#8217;s your baby. Well done mum.&#8217; 
              <br>
              Gloria scooted round and sat behind the new mother offering support 
              for her to sit up and feed the baby. Haya held her hand out and 
              Gloria grasped it firmly and squeezed. <br>
              &#8216;Thank you so much Gloria, I can&#8217;t thank you enough, 
              you were brilliant.&#8217;<br>
              &#8216;Me? I didn&#8217;t do anything; you&#8217;re the one who&#8217;s 
              brilliant.'<br>
              Gloria watched over Haya&#8217;s shoulder as she put her daughter 
              to her breast. <br>
              &#8216;We&#8217;re going to call her Noor. We chose that for a girl, 
              Dilip and I.&#8217;<br>
              &#8216;It&#8217;s a lovely name,&#8217; said Gloria, &#8216;hello 
              Noor.&#8217;<br>
              At the mention of Dilip Haya started to cry. Gloria cried too, both 
              of them holding hands, and watching Noor, gently rocking together. 
              After a while the crying stopped but they kept rocking. <br>
              &#8216;She&#8217;s third generation,&#8217; said Haya.<br>
              &#8216;Third generation Glaswegian? Poor wee soul.&#8217;<br>
              Haya glanced at Gloria and they giggled again. <br>
              &#8216;We&#8217;re very proud of you Noor,&#8217; said Gloria, beaming 
              smiles down upon her, &#8216;She suits that pink though, doesn&#8217;t 
              she?&#8217;</p>
            <p class="texto2">They heard the tyres crunch on the broken glass 
              before they saw it, the white ambulance just visible through the 
              broken trees. <br>
              &#8216;The veil!&#8217; whispered Haya urgently, &#8216;where is 
              it?&#8217;<br>
              Gloria searched the grass for it while Haya covered her chest and 
              pulled up her hood. Driven by Haya&#8217;s panicked tone Gloria 
              started out frantically, but as she thought about what finding the 
              veil would mean, the curtain that would drop again between them, 
              her energy deserted her. <br>
              The ambulance stopped and they heard the door open. <br>
              &#8216;I&#8217;ll find it myself,&#8217; said Haya sharply, &#8216;here, 
              hold her for me,&#8217; and she thrust Noor into Gloria&#8217;s 
              arms. <br>
              &#8216;They&#8217;ll have to examine you anyway,&#8217; said Gloria, 
              &#8216;every bit of you,&#8217; but Haya wasn&#8217;t listening. 
              She had found the veil. It was wet and she was trying to dry it, 
              squeezing and rubbing it against her robe.</p>
            <p class="texto2">The ambulance crew, a man and a woman, approached 
              through the trees. Soon they would be here and take charge. They 
              would all be taken to hospital: Dilip, in a second ambulance, would 
              go to the morgue. Gloria, despite insisting she was fine, would 
              be asked to wait in Accident and Emergency, just to be sure. Noor 
              and Haya would be taken directly to the neonatal suite.</p>
            <p class="texto2">Dilip&#8217;s body would be removed to the undertakers 
              in no great hurry. Haya&#8217;s family would come quickly, sharing 
              their grief and joy, to take her and the baby home. Hours later 
              Gloria would be finally released and would take a taxi back to her 
              flat. The wrecks would be towed, insurance companies would eventually 
              settle, everyone would get on with their lives.</p>
            <p class="texto2">&#8216;Haya, please,&#8217; Gloria said sadly, &#8216;you 
              don&#8217;t have to put it on.&#8217;<br>
              But Haya did not look at Gloria. <br>
              &#8216;I&#8217;m sorry,&#8217; Haya whispered, her voice hoarse 
              as she covered her face with the damp veil. Then with a resolute 
              nod she opened her arms to receive her daughter.</p>
            <p class="texto2">Gloria looked down at little Noor, wriggling naked 
              in the swaddling clothes; she looked so nice in pink. </p></td>

              <span class="texto2"> Gloria was driving with all due care and attention. 
              She wasn&#8217;t examining the glint of silver at her roots, she 
              wasn&#8217;t distracted by worrying about this month&#8217;s sales 
              figures, she wasn&#8217;t even changing gear. Her conscience was 
              clear. She saw the other car careening down the country lane towards 
              her, its back end fishtailing out of control; she saw it, there 
              just wasn&#8217;t anything she could do about it. Except brace. 
              And be vividly aware of all her unprotected areas: elbows, shins, 
              knuckles; the knobbly vertebrae on her back. </span><br>

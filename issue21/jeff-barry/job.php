<p>I needed to find another on the same plane of existence, one who had read that book. That’s why I came to this country, learned the language, and roamed this city. I thought I could recognize him by merely a passing glance, the fabric of his jacket, the part of his hair.</p>

<p>For hours every day I walked the streets, searching the faces of old men, boys, frail women steadily making their way home from the store, couples cuddling in doorways, college students studying in cafés, the woman standing on the curb peering down the street for the twenty-two bus. No, it wasn’t her or any of those behind her, ordering themselves so carefully, waiting for the bus. </p>

<p>But there in the center of the road, why had I not thought of it before? The Obelisk leading to the avenue of booksellers. I walked swiftly, passing storefronts, glancing at the people browsing inside, their faces unknown, spending the evening waiting for the restaurants to open. Yet I saw nothing that alerted me. Maybe across the street where more stores lined their tables with books. </p>

<p>The heavy traffic prevented me from dashing across mid-block. Heading to the corner for the next light I dodged the girls walking arm in arm, then sidestepped along the carpet of rings and necklaces stretched on the sidewalk, checking briefly the long face of the street vendor just to be sure he was not the ghost persisting in my head. </p>

<p>The signal to cross had not yet changed. A light breeze brushed against the back of my neck. I heard music, orchestral. A figure floated by the corner of my eye and hung gleaming beneath the streetlight. Drifting skywards a stream of letters flowed from behind me.</p>

<p>The light had gone green. People surged ahead, crossing Avenida Corrientes but I was transfixed, captured by the letters swirling around me. I feared turning, facing the store, afraid that I had gone mad and simply would confront a bleak, everyday reality. The letters passed along my skin like drops of rain sliding down my face, across my lips. </p>

<p>So little energy left within me after the caress of these typefaces, but this was not all. There was still the muse who pushed these letters towards me. The anticipation had been so strong I believed disappointment was pre-determined. I placed my hands deep within my pockets, clinching my fists to keep myself from trembling, just as I had done at thirteen for my first kiss. I shifted my feet, turned, straightened my shoulders, and lifted my eyes.</p>

<p>The letters streamed in parallel ribbons from the back of the store. The crowd browsing through the books hid the origin from my sight.</p>

<p>I no longer felt the urgency, the need to push and scrape myself through those shoppers, some solitary, others huddled together. It never occurred to me that the letters would stop flowing and disappear. They formed words in a language unknown to me.</p>

<p>The letters came wrapping around the back of a man. An “Ñ” climbed up his sleeve, danced on his shoulder, and jumped into the flow of its fellow letters trailing out the store.</p>

<p>All this time, here stood the person I sought. Could there be no other? This man, his hips so wide my arms could never reach around but would simply sink, losing themselves in his soft flesh.</p>

<p>Those corpulent folds were the fate for which I had longed. The letters had pulled me here. Time for a smile, a tug on the heart. We can love purely for the words emanating from the soul. No more is needed, everything else is merely surface.</p>

<p>One step, then a half more. I raised my arms, ready to rest my face against his shoulder but then he walked away.</p>

<p>The letters were not his to share, for then I saw a young woman smile as she leafed through a book, the letters leaping from the pages, prancing along her fingers. I wanted to reach out, grab her hands, place the tips of my fingers along her wrist but I stood there motionless. The letters filled the space between us, a chasm I knew not how to cross. Some element lacked within me.</p>

<p>I fell in love with her words not her beauty. All her life I will be the only man to love her purely for her words. </p>

<p>But when she looked up, she did not even notice me. At first, I thought she was watching the letters twirl between us, then skipping beyond me. She did look at my eyes, but only for a moment before passing elsewhere. The letters held nothing for her.</p>

<p>She closed the book, set it back on the table amid the others, and the letters slowed, gradually disappearing as she walked away. My instinct was to follow her out the store but I hesitated and she was gone. I turned back towards the books on the table. </p>

<p>It wasn’t another but a place that I sought, here among these books where the letters flowed. I come every day, all day. I’m here when the store opens and when it closes. I no longer bother to bath, shave, or change clothes. I sleep on the street at night. Eventually the clerks asked me to leave the store saying my body’s stench offended everyone. Prevented from forever crossing the threshold, I camp here on the sidewalk just outside. </p>

<p>One day watching the letters spiral from the store and cascade into sky, I saw the woman from that very first night. I remembered the letters dancing along her slender hand and how I thought I had loved her. As she walked by me, I lifted my hand towards her, saying, “I thought it was you.”  Bearded and filthy, sitting in rags on the sidewalk day and night, I mumble, “I thought it was you.”


</p>






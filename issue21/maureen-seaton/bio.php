                  <p>Poet Maureen Seaton earned an MFA from Vermont College in 1996. She is the author of the poetry collections <em>Fear of Subways</em> (1991), winner of the Eighth Mountain Poetry Prize; <em>The Sea Among the Cupboards</em> (1992); <em>Furious Cooking</em> (1996), winner of both the Iowa Poetry Prize and a Lambda Literary Award; <em>Little Ice Age</em> (2001); <em>Venus Examines Her Breast</em> (2004), winner of the Publishing Triangle’s Audre Lorde Award; and <em>Cave of the Yellow Volkswagen</em> (2009). </p>
 
<p>Using collage techniques to create delight and dissonance, Seaton’s poetry has been described as unusual, compressed, and surrealistic. Seaton has explored the possibilities of collaboration throughout her career, writing poetry with Denise Duhamel in such collections as <em>Exquisite Politics</em> (1997), <em>Oyl </em>(2000), and <em>Little Novels</em> (2002). She also collaborated with Samuel Ace on <em>Stealth</em> (2011) and with Neil de la Flor on <em>Sinead O’Connor and Her Coat of a Thousand Bluebirds</em> (2011). Seaton, Duhamel, and David Trinidad edited an anthology titled <em>Saints of Hysteria: A Half-Century of Collaborative American Poetry</em> (2007).</p>
 
<p>Seaton is author of the Lambda Literary Award–winning memoir <em>Sex Talks to Girls</em> (2008), in which she addresses motherhood, sobriety, and sexuality. She teaches at the University of Miami in Coral Gables, Florida.




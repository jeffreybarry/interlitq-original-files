<p>It is said that everyone in the United States old enough to remember anything can remember where they were and what they were doing when they heard the news that President John F. Kennedy had been assassinated. Well, if one Jewish boy from Dublin, Ireland can also clearly remember the event, then maybe it is not an exaggeration that everyone in the Western world also has clear memories of that day in November, a day that began slow shift of the star-spangled 60's from Camelot to Apocalypse. </p>

<p>As someone just entering his teens in the early 60's, I believed in Kennedy the only way you could believe in someone then--completely. We hadn’t yet learned to look cynically and distrustfully or even apathetically at our leaders. We were innocent and the boyish Kennedy echoed that clean-cut optimism and innocence. It was our time: JFK, Pope John XX111 and a new group, the Beatles, sang our music. A cord of unity bound us all together. Our hearts and minds resounded to new sounds, new words, and new phrases: Ecumenism, Ask not what your country. Yeah. Yeah. Yeah. </p>

<p>In the past few months, all these figures had touched my life in one way or another. Like nearly every Dubliner, I had stood in the crowd along Dublin's main thoroughfare, O'Connell Street, to welcome Kennedy to Ireland. He was, after all, the first "Irish" President. We had been given the afternoon off school in honor of the event. In June of that year, I had been saddened by the death of John XX111. Despite my insular Jewish world, in a country like Ireland, it was impossible to escape the aura of that gentle, almost mystical man, so I felt it was no blemish on my Jewishness to be touched by his death. </p>

<p>But best of all, and here I felt that I stood on the threshold of history, I had seen the Beatles "live" on their first and only visit to Dublin, along with 3,000 other screaming kids. They played for only twenty minutes but the emotions they stirred up made us feel that the present was rich in our hands and we could all embrace the future unreservedly, full in the confidence that it was ours for the taking. Who had any reason to doubt that the future would not continue on its bright ascension? </p>

<p>It was already night-time in Dublin--eight hours ahead of Dallas, TX, --on that November night of 1963. It was<em> erev Shabbos</em>, and the scene at my house was the same as every Friday night. The family gathered around the large dining-room table, unlike the rest of the week when we ate in the kitchen. The atmosphere was bright: flaring candles, white tablecloth, cleanliness, freshness, satisfaction. We sang the traditional songs welcoming the Sabbath: <em>Shalom Aleychem, Eshet Chayil</em>. My father said the Kiddush over the wine. We ate the special Sabbath meal and sang special Sabbath songs, called <em>z'mirot</em>. My younger brother and I picked some trivial nonsense to fight over. My father's yarmulke balanced precariously on his head as once again the candles hypnotized him and he fell asleep under their spell. </p>

<p>Being an observant family, we did not have the television or radio on, believing that it was not in keeping with the Sabbath spirit. "Put aside the material, devote yourself for one day to the spiritual." So, on Friday nights I went for a lesson in Talmud, with a Rabbi who lived close by. To be honest, I wasn't that crazy about learning Talmud, as I never could figure out the way the ancient rabbis argued and digressed from the issues at hand and never seemed to arrive at any firm conclusions.</p>

<p>On that particular night, we were to start a new volume <em>Betzah</em>, all about the problems of an egg laid on a holiday, which didn’t promise to be any more interesting that the last tractate we had unsuccessfully tackled, <em>Baba Metziah</em>, which began with two men fighting over a prayer shawl.</p>

<p>The November air was crisp and the walk invigorating. I arrived refreshed and ready to learn; however, before I could take off my coat, the Rabbi asked me if I had heard the news. "What news?" I asked. "President Kennedy has been killed" "What! What!" I repeated like an autistic parrot. You walk on solid ground and suddenly you are flying through emptiness. A shot rings out and everything goes haywire. Even the rabbi, usually insulated in his Talmudic tomes, was stunned.</p>

<p>"Who told you?" I asked, hoping for an unreliable source. <br>
"A neighbor came by. It was on the television." <br>
I recited the blessing on hearing tragic news: "<em>Baruch dayan haemeth</em>." (Blessed is the True Judge)  It was a testament to our acceptance of the irrational in our lives.</p>

<p>I was hungry for details but still it was the Sabbath and I was there for another purpose. We began to read the now even more alien Aramaic script. For an hour we tried to forget the world outside but its vibrations could be felt in every line we read. </p>

<p>After the lesson, I rushed home and told my parents the news. I begged them to put on the T.V. They refused. The spiritual prevailed for at least one more day. I missed the famous broadcast when Charles Mitchell, Ireland's leading broadcaster, read the news of the President's death with tears flowing down his cheeks. Thus we remained insulated in our little world of the Sabbath. </p>

<p>In the morning the paper arrived but already we sensed it was out of date as events tumbled hectically one after the other and speculation ran rife. And so the day passed, slowly and with dread, but full of the usual Sabbath rituals: morning services when many of the congregants were, like my family, unwilling to break the strict rules that governed our lives; a large Sabbath meal followed by a <em>shlof</em>, (a sleep) then back  for more services and the traditional third meal. Being Orthodox, we walked everywhere and so I had the opportunity to gawk fleetingly in shop windows and hear the broadcasts briefly from the T.V's and radios--but nothing was clear. </p>

<p>And so we waited impatiently for the <em>Havdalah</em> ceremony that marked the end of Sabbath--just in time for the 6 p.m. news. <em>Havdalah</em> is a beautiful ceremony, rich in rituals and symbols. As usual, we gathered around my father who held an overflowing kiddush cup in his hands. In the darkened room, we could only see by the light of the braided candle which my brother held as high as "he wanted his bride to be." We sniffed a box full of spices to revive our spirits which, according to the mystics, were weakened by the departure of an extra soul which had joined us for the Day of Rest.</p>

<p>But on that night in 1963, the words of the service had a peculiar ring, one that I was absorbing for the first time. The words of the prayer struck me with a new and darker meaning: <em>Hamavdil beyn koddesh lechol, beyn or le choshech</em>,” He who divides between the sacred and the profane, between light and darkness.”</p>

<p>Somewhere deep down, further down than I could possibly comprehend at that moment, as the light of the candle was extinguished in the wine, I sensed that those words no longer contained the whole story. There was no easy way to block out the physical, to draw a distinction between the forces of good and evil. All that followed during that decade, all the other cruel blows to our hopes and faith, seemed inevitable. An emptiness had begun to creep in, a feeling that only those who had hoped for so much, only to have it wrenched away from them, could possibly understand.



</p>





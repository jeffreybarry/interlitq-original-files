<p><em>Genesis</em><bR><bR>

Before there was a world<bR>
there was space.<bR>
The universe was possible.<bR>
Nothing was certain.<bR><bR>

Now the soul may speak <bR>
of the change that takes<bR>
one life to another.<bR>
The gods, who know all<bR>
that can be done,<bR>
inspire my song<bR>
from the Creation<bR>
onward to this <bR>
that is now.<bR><bR><bR><bR>




<em>Pygmalion</em><bR><bR>

See the statue smile.<bR>
Touching her, <bR>
He feels a tremor in the stone.<bR>
And her eyes, <bR>
They watch the artist at work.<bR>
He speaks to her,<bR>
Murmuring thoughts <bR>
He dare not say aloud,<bR>
Not to the world he knows.<bR>
She is art, and understands<bR>
What cannot be spoken.<bR>
It is felt too deeply,<bR>
Like the love he feels for his creation.<bR><bR><bR><bR>




<em>Beggars’ Bounty</em><bR><bR>

(The gods, disguised in rags, experience life on earth)<bR><bR>

That shall include the horn-shaped skull<bR>
Washed on the strand by ocean tides.<bR>
And the leaf of the forest,<bR>
Dried out on the stone ground.<bR>
The gawping eyes of toads. <bR>
The leap of a hart evading the hunter.<bR>
Reflections on clear water,<bR>
Dazzling Daedalus and son.<bR>
Then hearing celestial sounds<bR>
In cedar and sycamore.<bR><bR>

There are things to lose <bR>
In the dream of freedom.<bR><bR><bR><bR>




<em>The Bacchae</em><bR><bR>

The wind that shook the vines<bR>
Raised the skirts of women,<bR>
Scrambling to the summit<bR>
Of their wilder dreams.<bR>
The god of abandon<bR>
Makes madness of everything.<bR>
In this state nobody cares.<bR>
The silken shreds cling to the gorse.<bR>
Satin shoes discarded sink into mud.<bR><bR>

The rains that fall on the city<bR>
Slow the pursuit of men<bR>
In search of reason.<bR>
Sleep overtakes them.<bR>
Without iron and fire <bR>
The King is powerless.<bR>
All he has he summons,<bR>
Prepared alone to face the goat-god.<bR>
What he sees destroys him.<bR><bR><bR><bR>




<em>Orpheus Appears</em><bR><bR>

On a desolate plain <bR>
Comes the heavenly song<bR>
From the God-gifted one,<bR>
Lyre in hand,<bR>
Wrestling with the wind.<bR>
He wanders in search of shade<bR>
Where none is found<bR>
On half-barren ground.<bR><bR>

The sound of his song <bR>
Is sure to move heaven <bR>
To raise the cypress<bR>
Among the wild grass<bR>
For the sake of harmony<bR>
That stills the air<bR>
And lulls the wild heart.<bR><bR>

All manner of trees<bR>
Position themselves hurriedly,<bR>
Arms outstretched in gestures fixed.<bR>
Their speech is silenced.<bR>
Sadness steals through the forest<bR>
Till the music wakens the world,<bR>
Opening the eyes of songbirds.<bR><bR><bR><bR>




<em>Actaeon</em><bR><bR>

‘How dare he trespass?’<bR>
We asked of Actaeon,<bR>
Knowing all the while <bR>
The laws of chance favoured him<bR>
To be the one who saw her.<bR>
Revealed, ashamed and vengeful,<bR>
She was acting like one forsaken.<bR>
It was all leading to the end.<bR>
The hunting instinct stirs <bR>
The will unleashed from reason,<bR>
Hurrying to the chase.<bR>
Antlered Actaeon has been found wanting.<bR><bR><bR><bR>




<em>Orpheus Descending </em><bR><bR>

Centaurs are still in Arcadia,<bR>
As still as frost.<bR>
Into the fissure of earth<bR>
Goes this life ephemeral,<bR>
Deeper than the certainties<bR>
Of the hermit’s dream.<bR>
The darkness alone<bR>
Sees Orpheus go down<bR>
To the rumoured margin<bR>
Of an exceptional scene,<bR>
Returning to find nothing<bR>
Has changed, except hope.<bR>






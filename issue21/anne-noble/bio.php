                  <p>Anne Noble is one of New Zealand’s most widely recognised and respected contemporary photographers with images renowned for their beauty, complexity and the conceptual rigour. Anne is Professor of Fine Arts (Photography) at Massey University Wellington, she was awarded the New Zealand Order of Merit for her services to photography in 2003. </p>
 
<p>Her substantial body of work spans landscape, documentary and installations incorporating both still and moving images. She often works in series, enabling her to explore the medium and its possibilities in great depth. Since 2001, she has been researching and photographing Antarctica. In a project that has taken her to Antarctic centres all over the world as well as Antarctica itself, she explores the cultural construction of place and how our knowledge of such places is shaped through representation and imagination. Her images challenge the traditional depiction of the Antarctic landscape as heroic, picturesque or sublime and seek to suggest some of the current complexities that that underlie the region’s beauty. In 2008 she won a prestigious US National Science Foundation Artists and Writers Award to travel and work in Antarctica to complete this project. There were 97 applicants for six awards and she was the only recipient from outside the United States.</p>
 
<p>Anne was a recipient of an Arts Foundation Laureate Award in 2009.  The Laureate Award is an investment in excellence across a range of art forms for an artist with prominence and outstanding potential for future growth. Their work is rich but their richest work still lies ahead of them. The Award recognises a moment in the artists' career that will allow them to have their next great success.</p>  
 
<p>Another substantial series, <em>Ruby’s Room</em> developed between 1998 and 2006, has attracted considerable international attention for its highly original depiction of childhood. Conceived as an alternative archaeology of childhood, Anne, in association with her daughter, documented some of the many things children do with their mouths while eating and playing. This series was selected by the Musée du Quai Branly in Paris as the keynote contemporary photography exhibition for the inaugural Paris PhotoQuai Biennale of Photography in 2007. </p>
 
<p>Anne Noble has been at the forefront of photographic practice in New Zealand since first attracting attention in the early 1980s with her acclaimed photographs of the Wanganui River. In 2001 she was honoured with a retrospective exhibition and a major book about her work spanning 20 years. Initiated by the Dunedin Public Art Gallery, the exhibition toured from 2001 – 2003. </p>
<p>Other exhibiton highlights include: <em>Ice Blink: Antarctic Photographs</em>, Centre for Contemporary Photography, Melbourne, 2008; <em>Antarctica: Joyce Campbell, Anne Noble, Connie Samaras</em>, Pitzer Galleries Claremont College, Los Angeles 2007, <em>Reveries: Photography and Mortality</em>, National Portrait Gallery, Canberra, 2007; <em>ICONICA: The connection between contemporary art and reality</em>, Patio Herreriano, Museo de Arte Contemporaneo, Valladolid, Spain, 2006; <em>In Cold Light</em>, Centre for Contemporary Photography, Melbourne, 2005; <em>Anne Noble: Ruby’s Room</em>, Speilhaus Morrison Gallery, Berlin, 2005; <em>Critic’s Choice</em>, Neuer Berliner Kunstverein, Berlin, 2005; <em>High Chair: New Zealand Artists on Childhood</em>, St Paul St, Auckland, 2005; <em>The Line Between Us</em>, Monash University Museum of Art, Melbourne, 2004, <em>Slow Release: Recent photography from New Zealand</em> at the Heide Museum of Modern Art, Melbourne, 2002; <em>Observed and Contrived: Recent International Photography</em> at the Queensland Art Gallery 1998 and the Asia Pacific Triennial in Brisbane, 1993.





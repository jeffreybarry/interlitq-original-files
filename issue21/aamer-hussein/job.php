            <p>1.<br><br>

That day Shams and I sat on the train and talked endlessly. The fields outside were covered in water. It was July but there had been floods. <br><br>

2.<br><br>

We travelled for seven hours and reached the town on the edge of the river at five. The sun was still high and the roofs of the tall fortlike buildings glowed in its rays. At once we set off to wander the lanes of the unknown town. The river had broken its banks and several streets were under water.<br><br>

3.<br><br>

After the literary festival that evening and the next morning we made the same long journey back. Shams said, I brought along a book but I'm not in the mood to read, let's talk. We talked and talked and outside the rainwater still drowned the fields. Once it seemed as if the branches of a submerged tree were making gestures to the sky – but we talked on and on.<br><br>


4.<br><br>

I'd known Shams for thirty-five years. When we first met I was seventeen and she about thirty-five. We both liked singing and poetry. Then there was a revolution in her country, and traitors took over mine.  At thirty I gave up singing and some years later she did too. She'd had some successes, but I was too shy and too busy to look for fame. We also began to write at about the same time. I wrote stories and she accounts of her life. People remarked to us both, You colour your writings with exile.<br><br>

5.<br><br>

Some time ago we'd had a misunderstanding. It was on her side.  But during that long journey she didn't refer to it, and neither did I.  Perhaps silent anger can be washed away by rainwater. I remember Shams saying, What a worry it is, this having to make a living wage - it kills you - I'm far away from my country, far away from my faith - I don't want to die alone in this desolate city.<br><br>

6.<br><br>

When we said goodbye I saw regret in her eyes.<br><br>

7.<br><br>

I met Shams once again after that journey. She took me to dinner. She thanked me for many things but I couldn't understand what her gratitude was for.<br><br>

8.<br><br>

One day in winter a weeping friend told me, Shams is ill - a flood of worries has swallowed her up. It snowed that day.  <br><br>

9.<br><br>

Shams and I spoke only once more. On the phone. Again, her grateful words – then she said, When I'm a little better come and see me.<br><br>

10.<br><br>

But just before spring arrived she died.   <br><br>

11.<br><br>

She left no will, But a friend said she had given verbal instructions that she wanted fire, not earth, as her final element.    


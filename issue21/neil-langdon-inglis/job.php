            <p>Did you know that Galileo Galilei was a loyal son of the Catholic Church, whose views on heliocentricity were privately accepted by many in the Vatican? Were you aware that Galileo could have saved himself a great deal of trouble had he couched his discoveries in hypothetical language, honoring the conventions and pieties of his time? That the charges against him were largely his own fault?</p>

<p>I gathered these nuggets from a modern audiobook on the history of science. The lecturer dismissed the Ionian Enlightenment (which prefigured the 17th century scientific revolution by two millennia) in a single paragraph. Scientific discoveries in this course were credited always to groups, never to individuals. This same professor devoted one third—<em>fully one third</em>—of his course to the patristic authors (of early Christianity) and medieval scholasticism. He eschewed the term “Dark Ages” (preferring “Late Antiquity”). In closing, he cautioned his listeners that post-Enlightenment scientists owed a debt of gratitude to the Medieval church, which had laid down rules for the posing of questions and logical analysis that had underpinned Western science ever since. What Robert Hooke, Michael Faraday, and Charles Darwin (all practical men) would have made of this assertion is fascinating to contemplate.</p>

<p>Carl Sagan would have rejected this bizarre suggestion out of hand. For Sagan, the Dark Ages were dark indeed; an eternity of wasted time and squandered opportunity. This much-loved author and educator (1934-1996) held the now unfashionable view that scientific progress rested on the achievements of great men and women. A professor of astronomy at Cornell University, Sagan put forward his manifesto in the documentary series <em>Cosmos</em> (1980), which ranks alongside Kenneth Clarke’s <em>Civilisation</em> (1969) and Jacob Bronowski’s <em>The Ascent of Man</em> (1973) as a landmark in television history. Never universally admired, Sagan elicited the envy, jealousy, and fear that await all popularizers. Given that the Fox TV network began broadcasting an updated version of Cosmos (hosted by Neil deGrasse Tyson) in March 2014, the time is ripe to look back and ask: What kind of show was the original Cosmos, what manner of man created it, and what is the program’s legacy?</p>

<p>The first clue comes in the subtitle “A Personal Voyage.” Cosmos is also a rather solitary journey for the narrator—in the spaceship in which he cruises the galaxy, there is no crew. Is it then a history of science documentary? The historical re-enactments (especially of the strained relationship between prim Johannes Kepler and gold-nosed Tycho Brahe) are highlights of the show. But Cosmos does not follow an academic syllabus, such as a high school chemistry course might with the periodic table of elements in episode 1, carbon chemistry in episode 12, etc. It is cross-cutting, episodic, and superficially unstructured. Yet it only seems casual; in that respect <em>Cosmos</em> closely resembles its creator. When I met Carl Sagan in person and shook his hand at a conference in Seattle WA in 1994, my prevailing impression was one of… sternness. </p>

<p>Sternness about what, and why? The conference in question was organized by Committee for the Scientific Investigation of Claims of the Paranormal, then CSICOP (now renamed CSI, the Committee for Skeptical Inquiry), which Sagan and other skeptics set up in 1976 to counteract the escalating tide of interest in the paranormal. Believers dared to hope that Sagan was but a lukewarm supporter of CSICOP; his avuncular manner undoubtedly contrasted with the harsh tactics favored by Uri Geller’s nemesis, James Randi, and other debunkers.  Yet Sagan’s intentions were similar, even if his style was different. He was reaching out to audiences of newcomers and laymen, not to hector them, but to awaken an interest in their minds, light a fire in their hearts, and capture them for science and reason.</p>

<p>Sagan comes across as a hard-core skeptic only in his treatment of the 1961 “Zeta Reticuli Incident,” the alleged UFO abduction of Betty and Barney Hill, who would later tell the world about a “star map” which they had seen on board their captors’ spaceship. Why Sagan should hold up this basically harmless story to ridicule is not clear – there are worthier targets – but perhaps Sagan’s displeasure reflects his personal attachment to the idea of space travel itself. “We are made of starstuff” (yes, he utters those legendary words) and to the stars it is our destiny to return.  But this return journey will not be easy, and there are unlikely to be any obliging little green men around to help us on our way. We will have to do the hard work of getting there by the sweat of our own brow—assuming we don’t destroy ourselves first.</p>

<p>The threat of nuclear self-destruction is one of the touches that date the documentary. The passage of time has left its mark in other areas as well. We know so much more today about astronomical subjects Sagan cared about; the Mars Rovers (only prototypes in <em>Cosmos</em>) have roamed the Red Planet’s service, the doughty Voyager 1 spacecraft is entering interstellar space. Extrasolar planetary systems are giving up their secrets—how Sagan would have relished the news! These issues will have to be addressed in the 2014 <em>Cosmos</em>. There is no question that the 1980 <em>Cosmos</em> needs revisions—as Sagan and his wife Ann Druyan would have been the first to admit. Several episodes in the version of <em>Cosmos</em> I watched contained updates by an older and grayer Sagan ten years after the fact.  The documentary footage itself incorporated what appeared to be Hubble photographs, a reference to Comet Shoemaker-Levy 9, and a scene of a laptop user chatting into a cellphone – all post-1980 developments.</p>

<p>Two segments in particular have stood the test of time. The first focuses on the Ionian Enlightenment (the explosion of scientific knowledge in Ancient Greece 600 years BCE). The concepts of the evolution of species, the atomic theory of matter, evidence-based medicine, and heliocentricity emerged in a blaze of discovery, only to vanish. As for the second segment, the honor goes to Christiaan Huygens and 17th century Holland. The connection across time and space is clear. Both civilizations promoted the technology on which their lives and livelihoods depended. Both were seafaring regions that set great store by practical science, careful measurement, shipbuilding techniques, and navigational aids. Absent such technologies, no exploration of Planet Earth (let alone Outer Space) is possible; and exploration is mankind’s destiny. As Sagan reminds us, “the fusion of facts with dreams opened the way to the stars.” These parallel sagas hold the key to understanding the educational message of <em>Cosmos</em>. We owe these legacies an immense debt of gratitude. That is why these scientific blossomings stir our imaginations so powerfully. Science and Civilization are one.</p>

<p>What are the practical tools that Sagan, as an educator, used to make his points? He cares intensely about books—the old-fashioned kind (I do not think he would have been a Kindle user).Two other focal points in the <em>Cosmos</em> story are the Library of Alexandria, handsomely re-enacted using special effects and the New York Public Library, mercifully still standing, unlike its predecessor (however far afield he may go, Sagan always hastens back to Manhattan). There is a darker undercurrent in the Alexandria sequences. As Sagan notes, scholarship in ancient times was the preserve of the few. When stormclouds formed over the Library, there was nobody on hand to advocate for knowledge and certainly no average citizens who grasped the stakes involved. The remarkable Hypatia, the multitalented Alexandrine scholar (not all Sagan’s heroes are male), was ripped to pieces by Bishop Cyril’s gang of thugs. Destruction came to the Library soon after, and the Dark Ages (not “Late Antiquity”) began with a vengeance. Countless treasures—some leaving behind only a catalogue title, and most not even that—were lost for ever. </p>

<p>Look closely, and you can see why Sagan deplores superstition as much as he cherishes knowledge. Could he have had in mind the title of the 19th century classic “Extraordinary Popular Delusions and the Madness of Crowds”? Everything good and worthwhile can be snatched away by the rabble. The educator has an affirmative obligation to counteract this tendency. In <em>Cosmos</em>, we see him inspiring young minds in a classroom—Sagan’s very own sixth-grade classroom in Bensonhurst, Brooklyn. Coming full circle, he is at his most effective in a simple setting, handing out Voyager 2’s pictures of our solar system, and answering questions with humanity, honesty, and decency. The children are captivated; the awakening joy on their faces, one feels certain, was experienced by few of Sagan’s detractors standing in front of their own blackboards.</p>

<p>Sagan can strike up this rapport with his audience because he never lost touch with his own youthful sense of wonder and passion for knowledge. For the young lad from Brooklyn, books, a good education system, and a soaring imagination were a passport out of relatively humble origins (which he does not disown) and a ticket to the stars. Such things are not to be taken lightly or for granted. Why? He mentions in passing the influx of German intellectuals into the United States in the 1930s and it is here that one realizes that Sagan is paying tribute to immigrant forbears, and celebrating among other things the Jewish-American experience, informed by folk memories of pogroms, and of course, the Holocaust.</p>

<p>I like to think of Sagan in Egypt, demonstrating how Eratosthenes, a chief librarian at Alexandria in the 3rd century BCE, was able to measure the circumference of the Earth with a high degree of accuracy using nothing but a humble stick and plenty of shoe leather. Like Eratosthenes, Sagan could work marvels with the simplest of props. 
</p>


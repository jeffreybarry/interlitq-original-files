                  <p>Carole Smith had poems and articles published before applying to study creative writing with Aamer Hussein at Southampton University. She completed her PhD in March 2013 and, as part of her thesis, wrote a novel examining how greatly the expectations of and pressures on young women in England have changed since the 1920s. She has since worked with Aamer Hussein on another short story he originally wrote in Urdu, and is now concentrating on a sequence of poems and short stories of her own.</p>

<p>She was born in Southampton but as a teenager lived in Cyprus and Germany, where her father served with the army. She continued to travel after her marriage, living and working in Kuala Lumpur and Washington DC. In 1978 she returned to England to join <em>Hansard</em> as a reporter; and retired as Editor of Debates, House of Lords, in 2003, when she moved to the New Forest.





<p><strong>When I Was Straight</strong></p>

<p>1.<br><br>

There was a man in the moon<br>
&amp; a man at the head of the table.<br>
There was a woman with a dishrag<br><br>

draped across her narrow shoulder.<br>
Sometimes they were on television,<br>
&amp; the laugh track softened their features,<br><br>

made me long for them like a summer’s cool rain.<br><br>

In that first world, the man always drove, <br>
&amp; the woman always read a magazine—<br>
<em>Good Housekeeping or Better Homes &amp; Gardens</em>.<br><br>

The woman did not know how to work<br>
the lawnmower, &amp; the man did not know<br> 
how to work the microwave.  They were hesitant<br><br> 

&amp; grateful in the presence of each other’s bodies.<br><br>

When I see them now, I am too old to be mistaken<br>
for their daughter.  The women no longer smile, <br>
pat my hand, promise the right man is soon to<br><br>

come along.  The men do not whistle or nod.<br>
Even the sky grows distant, the new moon turns charcoal<br>
against gray.  “It’s getting dark out,” the couples say,<br><br>

&amp; draw the doors closed, leading to the master bedroom.<br><br><br><br></p>
 

<p>2.<br><br>

It was a shame.  It was a phase.<br>
It was a secret.<br><br>

I wanted every man I met.<br>
I courted danger on the dance floor.<br><br>

<em>I was insatiable.<br>
I was indiscriminate.</em><br><br>

My lifestyle was visible in my hair cut,<br> 
my choice of shoes, the tattoos I didn’t have.<br><br>

<em>I was insatiable.<br>
I was flamboyant.</em><br><br>

I couldn’t get enough—lipstick,<br>
laundry detergent, sexy lingerie.<br><br>

I bought in bulk.  I stocked my shelves. <br> 
Some thought me unnatural.<br><br>

<em>I was insatiable.<br>
I was derelict.</em><br><br>

At times, I found myself unable to stop<br> 
talking about it.  Flaunting.<br><br>

<em>I was insatiable.<br>
I was reckless.</em><br><br>

The doctor felt certain I would outgrow it.<br>
The pastor worried for my soul.<br><br>

<em>What shall I do in the meantime?</em> I wondered.<br><br>

For some, it was the most interesting thing<br>
about me.<br><br><br><br></p>







 

<p>3.<br><br>

It was like a game of Red Rover, &amp;<br>
someone was always being sent over,<br>
flung out into the field of un-belonging<br>
&amp; struggling to break back in.<br><br>

The team that called you didn’t want<br>
you, &amp; the team that had you<br>
couldn’t keep you.  No one was <br>
content to run or stay put.<br><br>

So was it any surprise that when <br>
they shouted, “Red Rover! Red Rover!<br>
Send Julie right over,” my feelings were<br> 
mixed?  Relief split hairs with regret.<br><br>

Pumping my legs as hard as I could, <br>
I broke clean through their tightly knit line.<br>
The trouble was, I never stopped running.<br>
I never turned back &amp; joined hands. <br><br><br><br></p>
 

<p>4.<br><br>

I could tell my mother how<br>
I wanted her to brush my hair<br>
&amp; braid it through with ribbons.<br><br>

I could tell my father how <br>
I loved baking cookies &amp;<br>
pinning damp clothes on the line.<br>

That I liked folding napkins &amp; saying grace <br>
&amp; dressing my dolls for dinner.<br><br>

But I could not tell them I was also <br>
the kind of girl who walked up slides<br>
in muddy sneakers, who threw stones<br><br>

at the slats of neighbors’ fences,<br>
&amp; spit through the lattice when she thought<br>
no one was looking.<br><br>

That when I saw a NO TRESPASSING sign,<br>
I thought of it more as an invitation.<br><br>

In other words, I learned early on<br>
about the double life.  How you could<br>
smile brightly into someone’s face, <br><br>

saying “Yes” when you meant “No,”<br>
“True” when you meant “False,”<br>
your sticky fingers crossed behind your back.<br><br><br><br></p>

<p>5.<br><br>

A ruler was called a <em>straightedge</em> <br><br>

<em>Straight talk</em> was smart talk<br><br>

A <em>straight man</em> was funny by proxy<br><br>

Sober people walked <em>straight lines</em><br><br>

A <em>straight face</em> was useful for poker<br><br>

<em>Straight-laced</em> was superior to rash<br><br>

<em>Straight As</em> were the standard for achievement<br><br>

The righteous path was called <em>The Straight &amp; Narrow</em><br><br>

Good girls were always <em>straight as an arrow</em><br><br>

<em>With a straight bat</em> was the way to play sport<br><br>

<em>A straight-shooter</em> never minced words<br><br>

Peter told Wendy <em>straight on till morning</em><br><br>
Do you follow me?  Did you get it all <em>straight?</em>





</p>





<p><strong>The Glass Swan</strong><br><br>
 
January midnight, a numbness of winter,<br>
not for the first time, I am last awake.<br>
The house is silent except for the hum<br>
of the coal fire, the blue song of the fridge.<br><br>
 
All the winters I have been alive, the weather<br>
has been teaching its hard lessons:<br>
those who lived so intensely are gone.<br>
I shall not see them again, though I speak with them<br><br>
 
in all the aching chambers of the mind.<br>
Ice has hold of the earth, as those things<br>
which are true but unwelcome, grip memory.<br>
Look at this fire in the hearth, feel it.<br><br>
 
Bank it up against the night. It is all we have, these<br>
corporeal things: these candlesticks, this glass swan.<br><br><br></p>
 
<p><strong>Coffin Texts</strong><br><br>
inscribed on the inside<br>
of my cardboard house<br>
while I wait to decompose<br>
in the deep woodland places<br>
where my body can become<br>
leaf mould, food<br>
for oaks and rowans.<br><br>
 
No need to paint my image<br>
with a gold face, as if bathed<br>
in sunlight. But let children<br>
colour my coffin with crayons,<br>
doodle me happy.<br><br>
 
No guide to the netherworld<br>
but poems to sing me asleep<br>
poems I have knit into my bones.<br>
This magic is true. A spell<br>
to perfect the body and return it<br>
to the cradle of earth.<br><br>






</p>





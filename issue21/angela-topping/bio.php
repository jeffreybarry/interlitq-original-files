                  <p>Angela Topping is a Cheshire-based poet. Her first two collections were published by Stride (1988 and 1999). Further collections were published by bluechrome (2007), Salt (<em>The New Generation</em> 2010, a best-selling collection for children; <em>I Sing of Bricks</em> 2011) and Lapwing (Paper Patterns 2012). She had a Rack Press pamphlet in 2011 and two chapbooks with Erbacce, one of which was written to accompany The Lightfoot Letters art and poetry exhibition with Maria Walker. In 2013, a selected poems, <em>Letting Go</em>, was published by Mother’s Milk Books. In the same year, Angela completed a residency at Gladstone’s Library, and won first prize in the Buzzwords poetry competition. Angela’s poems have appeared in over 50 anthologies and been included in magazines such as Poetry Review, London Magazine and many others. She has also edited and co-edited several anthologies. She is a seasoned reader and workshop leader, and has a background in education. She is the author of several critical books for Greenwich Exchange and has co-authored textbooks on poetry for OUP. 



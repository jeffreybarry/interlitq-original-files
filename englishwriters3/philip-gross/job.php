           <p><br><br></p>
<p><em>These new poems complement and complete the sequence Time in the Dingle published as a chapbook by the International Poetry Studies Institute, Canberra, in 2015.</em><br><br></p>
<p><strong>Their soundings</strong><bR><bR>

see through walls – thicker than walls, <bR>
the chain-mail darkness of inside the yew, <bR>
the holly, the dust-sheet drapes of ivy,<bR><bR>

inwardness which only intricately <bR>
niggled gimlet-pricks of light get through. <bR><bR>

And yet the plump and sudden.. what?<bR>

hedge sparrows? seem to pass<bR>
at a tilt, a hurtle, without let or hindrance. <bR><bR>

Or they hold still, fill <bR>
the whole bush with their stillness –<bR>
just the ping-trace of their calls <bR><bR>

to stitch the world together, space <bR>
to matter, and their own, <bR><bR>

each other's, place in it. <bR><bR><bR>

 
*<bR><bR><bR>


<strong>All the contraries, </strong><bR><bR>

at play, in the dingle... Things it wants to be: <bR>
a busy lull, an airy womb filled with a shudder-<bR>
pulse of green, <bR>
&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;                         both too quick and too slow <bR>
to grasp by looking – listen, there's a depth <bR>
&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;           above us – a snug vertigo <bR><bR>

with, now, that single bird-blip: the affectless <bR>
curiosity <bR>
&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;                of sonar. It will find us. <bR>
        &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;                                               Space <bR>
too close, sometimes, too pressing in, <bR>
almost, to bear... Something <bR><bR>

that's beginning, always, <bR>
  &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;                                      even as it ends.
<bR><bR><bR>


*<bR><bR><bR>


<strong>This is the life</strong><bR><bR>

implicit, something being carried, <bR>
brought to term – so it figures<bR>
to such as us past breeding. (Oh, <bR>
&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;         but the tweaks of the genes, <bR>
&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;         still, on the servants’ rusty bell-pull<bR>
&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;         in the old house, and the tinkling <bR>
 &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;        in the cellar far below.)<bR><bR>

This charged space. Waiting.<bR>
But what would birth mean,<bR>
out of this, what spasm as what<bR>
 &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;        was implied becomes world <bR>
 &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;        and what was outside and beyond us <bR>
 &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;        sits in the palm of the hand, <bR>
 &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;        tiny, tight as a nut?<bR><bR><bR>

 
*<bR><bR><bR>


<strong>In his last year</strong><bR><bR>

he walked this way, daily. Nothing scared, <bR>
nothing startled, shrilled or jangled its alarm. <bR><bR>

He made no dent in the air.<bR><bR>

Though no one knew yet, he’d already gone that far.<bR><bR><bR>

 
*<bR><bR><bR>


<strong>Down time</strong><bR><bR>

in the dingle: the wild garlic has let itself go – well past its Best-By<bR>
anyway, who cares?  As June ebbs, so the birdsong too <bR>
is a slack tide. The bright and brittle, maybe cutting, edge to it <bR>
goes blunt like the leaves' dulled green sunk in its own acquaintance. <bR>
One thrush holds the high ground longest but it's not a conversation, <bR>
only with himself. Then quiet.  Moist heat. If the air had pages <bR>
they'd be curling at the corners. And the question is: <bR>
now that the whole place starts to turn away from you <bR>
and inwards, is the presence less or – <bR>
like the offish tang of garlic <bR>
wilting to digest itself –<bR>
just concentrating, <bR>
more?<bR><bR><bR>

 
*<bR><bR><bR>


<strong>It takes me out</strong><bR><bR>

of myself, he would say. Stopped <bR>
mid walk, mid step almost, following <bR>
each chink of sound, all at once  <bR>
to its several source. <bR>
The unseen. <bR><bR>

Spreading out, the spaces in him, <bR>
between thought and thought and <bR>
himself, into a great <bR>
dispersion.<bR><bR>

When the time comes, scatter me.<bR><bR><bR>

 
*<bR><bR><bR>


<strong>Who,</strong><bR><bR> 

<em>If I cried out, would hear me</em>…? Rilke, if that’s a plea,<bR>
then no, not the birds any more than your angels, <bR><bR> 

not this unseen host, this constellation <bR>
of bright points of presence. What brings me <bR><bR>

or you or anybody to a cry <bR>
means nothing in this bird-space, tree-space, prey- <bR><bR>

or hunter-space except… attend <bR>
a moment: danger? or advantage? Then it could go on<bR><bR>

(<em>Fling this emptiness / out of your arms, <bR>
Back into the spaces/ into which we breathe...</em>)<bR><bR>

unhindered: human weeping, wind, a distant <bR>
playground clangour soft as leaf fall, passing siren, rain. <bR><bR><bR>

 
*<bR><bR><bR>


<strong>It writes itself,</strong><bR><bR>

the dingle: scribble-pad <bR>
for the hithering-thithering <bR>
gnat flecks, the hoverflies’<bR>
quiver and twitch, <bR>
 &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;  &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;  &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;                               some stray<bR>
imago flipped like a silvery<bR>
5p, heads-tails, into day-<bR>
long life... <bR>
  &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;  &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;  &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;                  Praise be <bR>
to God’s rough workings, <bR>
sometimes bird-snack, some-<bR>
times intersecting, in a brief<bR>
spark, with a squint of light.<bR><bR><bR>

 
*<bR><bR><bR>


<strong>Dawn</strong><bR><bR>

chorus...? No, rather the clatter of toolkits<bR>
&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;   	on echoing floorboards, <bR>
the measuring up of spaces in the day: <bR>
&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;    	the long ticking out <bR>
and snick back of a metal inch-tape, <bR>
then the tiny power tools out again. <bR><bR>

Or earlier, the day still on the drawing board: <bR>
&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;    	the architects, extrapolating vectors. <bR>
Calculus of sound. An audible force diagram<bR>
&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;   	inked on the air. Close your eyes, <bR>
see it as dim blue-glowing graphics you can turn<bR>
and flex through three dimensions. Only <bR><bR>

you are absent, you, dark matter, moving through <bR>
&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;    	that matrix, to be known<bR>
by the way the space distorts around you <bR>
&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;    	as you pass. Your only power: <bR>
to be the dumbness into which their sound <bR>
collapses. (All around you now, its splintering.)<bR><bR>

To be the blind spot, larger today. And one day <bR>
&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;    	there's the whole wood, gone.<bR><bR><bR>

 
*<bR><bR><bR>


<strong>Gone</strong><bR><bR>

&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;   	Imagine that.<bR><bR>

The sound of it.<bR>
Air's harp unstrung.<bR>
&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;  &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;  &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;   			A great <bR>
&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;  &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;  &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; unlistening.<bR><bR><bR><bR></p>




<p><strong>Liverpool Alexandria</strong><br><br>

If they had stayed in Liverpool,<br>
if the business had prospered… They say<br>
he spoke Greek, all his days, with that edge<br>
of strangeness.<br><br>

It was a seaport, after all,<br>
like the other: he could have followed<br>
his fancies down different streets, loitered<br>
in Victorian pubs heavy with gilt and brass<br>
instead of the pavement cafes.<br><br>

It is not as if he were one for road names<br>
or landmarks. His city was not made of weather,<br>
knew no seasons. Its map was people: Cleopatra,<br>
shop assistants, Coptic priests, Lefkios<br>
and Iasis, flaneurs, emperors, debauchees,<br>
all crowding in, on every corner an Antony.<br><br>

It could have worked, in that city of Welsh chapels<br>
and Chinatown, that importer of languages.<br>
He could have written the epitaphs of so many:<br>
slaves, sailors, emigrants, chancers, young men<br>
going to seed… it would have been just like home,<br>
would have <em>become</em> home. <br><br>

A life mapped out in one city, he said,<br>
would follow the same contours anywhere:<br>
we'd have had him, the quiet stroller,<br>
bringing folk in the Walker back to life, <br>
haunting the Philharmonic, locking eyes<br>
with lounging shadows in Gibraltar Place. Already<br>
he'd made the odd poem in English; soon enough<br>
it would have become his music, shaped his thought,<br>
even if he spoke it, always, with a Greek lilt.<br><br><br><br>

<strong>Ronald</strong><br>
<em>(Lord Cardigan's horse)</em><br><br>

Ronald galloped <br>
to where the noise came from, <br>
then back again. <br><br>

They always want you to go <br>
toward the noise. <br>
Mostly, more come back.<br><br>

Some horses break,<br>
lie screaming, thrashing<br>
in the smell of blood. <br><br>

Or their man breaks <br>
and they stay by him, <br>
nuzzling his face.<br><br>

Ronald came back, <br>
like one in six,<br>
he lived to be old.<br><br>

When his man broke, <br>
Ronald was to walk <br>
behind the coffin <br><br>

but stepping so slow <br>
and with no man <br>
set him on edge<br><br>

and the laudanum <br>
they used to calm him <br>
made him doze off <br><br>

(against a wall, <br>
let's hope, like Marvin's horse <br>
in Cat Ballou).<br><br>
 
Someone sounded<br>
the cavalry charge<br>
in his old ear: it pricked, <br><br>

and he paced with the guns,<br>
the music, left his man lying<br>
and came back. <br><br><br><br>

<strong>In the Land of the Dead</strong><br>
<em><small>CREON: The good and the bad are not equally deserving.<br>
ANTIGONE: Who knows? In the land of the dead they may be.<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Sophocles: Antigone</small></em><br><br>

Now and then, a mind turns a corner<br>
or tops a hill, and the view is what<br>
it never dreamed of, the world so <em>other</em>,<br><br>

it is thrown off course, can't see straight,<br>
or rather, it sees for the first time<br>
that straight is not what it had thought.<br><br>

Walls and horizons, nothing is plumb<br>
but all aslant. It is how the girl sees,<br>
for a moment, past the narrow room<br><br>

where we step round each other, set laws<br>
to ease our time together. It is sound:<br>
it renders our life civil. But her eyes<br><br>

have opened on the endless land beyond,<br>
with its soaring valleys, its plunging mountains,<br>
its lack of signs and frontiers: the land<br>
where all the maps are to draw again.<br><br><br><br>



<strong>In the Town of N</strong><br><br>

a gentleman, for the moment nameless,<br>
alights, with dusk falling, at the inn,<br>
where the host quite fails to recognise<br>
a three-volume picaresque in train,<br><br>

though his daughter, sensing destiny<br>
at the door, rehearses the blushing speech<br>
she won’t need when the Enigmatic Lady<br>
arrives, in a drift of scent, by next week's coach.<br><br>

The –shire Regiment is giving a ball <br>
 meanwhile, at the town's assembly rooms.<br>
Tradesmen will go unpaid; some foolish girl<br>
will elope from a dull and decent home<br><br>

when they march. The gentleman will prove<br>
to be Lord L– under a false name.<br>
Someone will die; someone will fall in love.<br>
After "Finis", nothing will be the same<br><br>

except the town of N, settling once more<br>
to normal, nowhere, nondescript: this town<br>
that sleeps between marbled end-papers, <br>
waiting for the next story to happen.<br><br>














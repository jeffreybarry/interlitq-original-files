                  <p>Adam Horovitz was born in London in 1971 but was brought up in Slad, in the West of England, from the age of three months. He has been writing since childhood, following in the footsteps of two poet parents in a slightly circuitous zigzag, releasing a number of pamphlets, appearing in magazines and anthologies and reading at venues and festivals across Britain since the 1990s. </p>

<p>In 1996, he represented Britain at the inaugural Days of Poetry and Wine festival in Slovenia. He was poet in residence for Glastonbury Festival’s official website in 2009 and voted onto the Hospital Club 100 as an ‘emerging talent’ in 2010. In 2012 he was awarded a Hawthornden Fellowship. </p>

<p>His first full collection, Turning, was published by Headland in 2011. Since then, he has released A Thousand Laurie Lees, a poetry-fuelled memoir of growing up in the Slad valley, made famous by Laurie Lee in Cider with Rosie (released as Edge of Day in the USA), written to coincide with Lee’s centenary year in 2014, Only the Flame Remains, a pamphlet that complements the memoir, and, in 2015, Little Metropolis, a CD of poetry and music exploring and celebrating small town life in England with musician Josef Reeve.  </p>

<p>In 2015 he was chosen as one of Ledbury Poetry Festival’s Versopolis poets, part of a pan-European project in which 11 poetry festivals exchange emerging poets. He is currently the Herefordshire poet in residence and is finalising a second collection of poetry. </p>

<p>"Adam Horovitz writes poems of great beauty and truth; poems which are earned through experience, suffering and love and deployed in a physical language of scrupulous integrity. He is the real deal." Carol Ann Duffy </p>



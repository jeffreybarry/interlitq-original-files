in the blue light of longing and distraction<br>
that flickers through our many selves <br><br>

as we pour through screens and fracture <br>
into further blazing multitudes,<br>
as the collision impact debris of desire and loss<br>
litters our atmospheres with endless signs<br><br>

that do not lead, however we may wish it, <br>

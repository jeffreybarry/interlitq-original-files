<p><strong><em>The Afterlife of Dietrich Bonhoeffer</em></strong><br><br></p>


<p>i <em>Bonhoeffer imprisoned</em><br><br>

Nothing to do but smoke and study<br>
the emptiness of time, wait with my books<br>
for the guards to arrive<br>
swearing like knives, stamping out the cold. <br><br>

Cruelty settles only on my skin, like ink<br>
until the pen's bled dry. Who <br>
is Christ for us today?<br>
A sharpening of tools in the antechamber.<br><br>

The mansion of their father has many <br>
rooms lined with metal teeth.<br>
We learned too late; it is not the thought<br>
but readiness to take responsibility.<br><br>

What use the edifice, the structure, if<br>
in building it we throttle and dismember?<br><br><br>


ii <em>A survivor remembers the road to extinction</em><br><br>

A long road in April<br>
under American mortar <br>
but he was calm, so calm,<br>
as if prison <br>
had eaten nothing of him.<br><br>

The guards wept sweat and blood,<br>
jittered like kittens <br>
on the approach to Flossenbürg. <br>
If they were praying, no one heard but Bonhoeffer <br>
or, if there's such a creature, God.<br><br>

Asked for a Sunday service<br>
before the rope, he read Isiah in the mud.<br>
<em>With his wounds we are healed.</em><br>
So many wounded gods out there.<br>
Which one to choose?<br><br><br></p>

<p><strong><em>42nd Dream of You</em></strong><br><br></p>


<p>This time, you lead me <br>
along a rain-bruised lane <br>
ripe with the aniseed scent <br>
of crushed cow parsley.<br><br>

An ever-unreachable yard ahead, <br>
your smile is bound tight as ivy<br>
as you dance past blood spots<br>
of campion, beckoning,<br><br>

calling, laughing; your voice seductive <br>
as the slow chant of tree branches<br>
that bend to an imagined breeze.<br>
<em>Come</em>, you whisper. I keep on coming, <br><br>

incautious, stumble on loose stones,<br>
on the invisible track the badgers make <br>
nightly across tarmac, as they snarl <br>
territorial love songs to the cloud-locked moon.<br><br>

Through a shifting map of hedgerows<br>
I watch you become birdsong, a smile<br>
of light at the open window,<br>
an invitation to enter into day.<br><br><br></p>


<p><strong><em>To be Read Looking Upward Over Water</em></strong><br><br></p>


<p>Travelled home with the taste of you<br>
soft in my mouth. Spent the journey laughing <br>
easy as stream water over last scraps of ice, <br><br>

breath crazed on the carriage window,<br>
its patterns condensing <br>
into a ghosted close-up of your face<br><br>

whilst a vulgar winter sun crooned <br>
hangover songs from the sea’s stomach. <br>
I pressed deeper in. The train’s glass membrane <br><br>
		
	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;planted a cool kiss on my illuminated eyelids<br><br>

as sky blurred, slid sideways, became <br>
fathomable (again, at last), the clouds <br>
pulsing from grey to silver, outwards <br><br>

into a white semaphore, a new lexicon<br>
born from the breaking of long winter silences<br>
to be read looking upward over water<br><br>

punctuated by black dots <br>
of wading birds, wide expanses of sand <br>
and mudflats that are as skin <br><br>
		
	&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;ecstatic under the tongue of the tide.<br><br><br></p>
<p><strong><em>Collision Impact Debris</em></strong><br><br></p>
<p><small>Including two lines borrowed from ‘The Imperfect Knight’ by John Fairfax</small><br><br></p>


<p><em>Because I am everyone bewitched by the mirror</em><br>
I see the sky fall open like tired leather, <br>
like a jeweller’s drawstring bag. The doors croak <br>
wide enough for autumn’s last unrotted leaves.<br><br>

I smell the invisible stain of hope <br>
hung in mildew clusters at the edge of sight, <br>
some shy spore of Death<br>
caught letting corruption swing into reverse.<br><br>

No, it will never be enough to know <br>
that we are loved. In the shallow silver <br>
of the ecstatic glass that binds me, <Em>my eyes <br>

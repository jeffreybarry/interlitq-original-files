                  <p><strong>Joanne Limburg</strong> has published poetry for adults and children, memoir and fiction. She won an Eric Gregory Award for her poetry in 1998 and published her first collection, <em>Femenismo</em> (Bloodaxe) in 2000. A second collection, <em>Paraphernalia</em>, followed in 2007 and was a Poetry Book Society Recommendation. In 2010, Atlantic Books brought out her first prose book, a memoir about OCD, anxiety and poetry, called <em>The Woman Who Thought Too Much</em>, which was shortlisted for the Mind Book of the Year Award. After finishing the memoir, and prompted by the death of her brother, she turned to poetry again, and wrote the poems in <em>The Oxygen Man </em>(Five Leaves Press, 2012), which deal with what it means to love and lose a sibling. <em>Bookside Down</em> (Salt), which came out the following year, was, by contrast, a response to her son’s demand that she write some ‘funny poems, for children’; it was runner-up for the CLPE Poetry Award in 2014. Her most recent book is the novel, <em>A Want of Kindness</em> (Atlantic Books, 2015), which she based on the life of Queen Anne, pre-throne.  <em>A Want of Kindness</em> took some years to write and research, and Joanne is grateful for assistance received from the Arts Council, the Society of Authors and the Royal Literary Fund, who gave her a placement as Writing Fellow at Magdalene College, Cambridge, from 2008 to 2010. She lives in Cambridge with her husband and son, and is currently working towards a PhD in Creative Writing at Kingston University. She hopes to bring out a new poetry collection in the next couple of years.</p>

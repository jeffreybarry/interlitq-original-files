<p><Strong>Hari Kyo, 8th February</strong></p>




<p>Into the panna cotta I plunge paperclips –<br>
&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;               they’ve held me together in a paper storm, <br>
 &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;                                                      helped me to make sense of sequences.<br><br>

I push in my empty Mitsubishi uni-ball – <br>
&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;              it’s written itself out, telling of a long lost warrior <br>
                                                              &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;  smiling towards me in a storm.<br><br>

I plant a line of post-its with out-dated notes –<br>
                  &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;     ‘noble, elegant, gracious’, <br>
                         &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;             ‘turn right at the B2214 Avery Hill Road’.<br><br>

I pitch a cracked coaster, <br>
        &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;   where lemon and ginger tea, and coffee, <br>
                       &nbsp; &nbsp;  &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;           have balanced fact from fiction day to day.<br><br>

And last – a hair slide in two halves. <br>
          &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;   It used to clear my brow<br>
                            &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;  to dwell on purple hills, and valleys caked in snow.<br><br></p>


















<p><em>Hari Kyo is the Japanese Festival to old and broken needles for Kimono makers; also a time to value the small, everyday objects of daily life.</em><br><br><br></p>

<p><strong>Noss</strong></p>




<p>It’s not easy to photograph an island –<br>
&nbsp; &nbsp; &nbsp; &nbsp;         the spongy sphagnum moss beneath your feet,<br>
&nbsp; &nbsp;    mauve marsh violets beside a stream,<br>
&nbsp; &nbsp; &nbsp; &nbsp; &nbsp;        a gannet shutting wings against fuselage<br>
just before he hits the sea.<br><br>

The wide ewe wades along an ancient path<br>
 &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;   &nbsp;             about to birth her twins,<br>
   &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;&nbsp;      she’s safe on the west side of the dry stone dyke.<br>
 &nbsp; &nbsp; &nbsp; &nbsp;       To the east great skuas pose,<br>
&nbsp; &nbsp;    flagging their high striped wings above the buffeted moor.<br>
They’re keen to feed on newborn lambs.<br><br>

Lichens ring stones, three Op Art eider ducks <br>
&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;                settle on worn ledges shelving to the sea.<br>
&nbsp; &nbsp;      Noss Sound laps and slaps the rocks<br>
keeping Bressay at bay.<br><br><br></p>












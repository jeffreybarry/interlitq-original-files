                  <p> Amanda Hopkinson is a visiting professor at the Centre for Translation and Interpretation Studies at Manchester University, and at the Centre of Creative Writing, Translation and Publishing at City University, London. From 2004 to 2012 she was the Director of the British Centre for Literary Translation at the University of East Anglia. She has been a literary translator from the Spanish, Portuguese and French for thirty years: authors include Carmen Boullosa and Ricardo Piglia; Paulo Coelho and Jose Saramago; and Dominique Manotti. She also translates non-fiction, principally on human rights; and writes books on Latin American art and photography.</p>

<p>    Together with Geraldine D’Amico, Amanda Hopkinson co-directs the international festival of the languages of literature and music, Notes & Letters, held each October at Kings Place, London. She also lectures widely on both literature in translation and Latin American culture, most recently at the Jagiellonian University (Krakow); Helsinki University; Ryerson University (Toronto); and Glasgow and Essex Universities. Amanda Hopkinson is a trustee of both the journal Modern Poetry in Translation and of English PEN, where she co-founded PEN’s Writers in Translation committee in 2004. She is currently translating Elena Poniatowska’s novelised biography of the British Surrealist Leonora Carrington and completing A History of Photography in Mexico.</p>



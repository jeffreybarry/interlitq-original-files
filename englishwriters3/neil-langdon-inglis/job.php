<p>The Emperor of All Maladies—A Biography of Cancer<bR>
(by Siddhartha Mukherjee, 2010, pubs. Scribner)</p>

<p>Unnatural History—Breast Cancer and American Society<br>
(by Robert A. Aronowitz MD, 2007, pubs. CUP)</p>

<p>In 1953, the beloved English contralto Kathleen Ferrier passed away at the age of 41.  Throngs showed up for her funeral, but few would have known the cause of her death. Diagnosis and initial treatment had occurred two years previously; in between periods of hospitalization and convalescence Ferrier continued to perform and record, until a recurrence in the Spring of her final year brought her career to an end. Her diaries reveal that she instructed her nurse/secretary to share as little medical information as possible, even among close friends. </p>

<p>Of course, Ferrier’s troubles had not simply begun in 1951. Illness had been at the back of her mind for years, at least since her ex-husband had fetched her a blow (what kind of blow?) during an ill-fated marriage in the 1930s. Inexplicable pains had come and gone during the war years. With a premonition of death she hired a nurse. Shortly afterwards, the signs of disease became too incontrovertible to ignore. Therapies were administered by boisterous male surgeons using the limited technology available, which bought her extra time (or perhaps not).</p>

<p>Her fatal illness, of course, was breast cancer, and although Ferrier's case is not mentioned in "Unnatural History", her story exemplifies an earlier generation's cultural attitudes surrounding this disease and its hallmarks. The “smiling oncologist” who brimmed with self-confidence but could do little of practical value; the double-edged assault on the female body; myths and mysteries surrounding causation; questions over the efficacy of treatment; and above all, the fear that dared not speak its name.</p>

<p>These themes are also explored in "The Emperor of All Maladies," which undertakes an ambitious history of cancer itself.  Both books have a North American focus, although cancer research and public awareness were hardly unknown elsewhere; a prominent anti-tobacco campaign in wartime Germany featured a man being chewed alive by a giant cigarette (“You don’t smoke it—it smokes you!”).</p>

<p>"Unnatural" and "Emperor" are both worth your time ("Emperor" gets more attention in this review); yet neither is by any stretch of the imagination a fun read.</p>

<p>Mukherjee begins at the beginning, with the first known written reference to cancer ("no cure" -- by doctor-to-the-Pharaohs Imhotep). The great challenges and long odds in cancer treatment have always attracted the best minds among the elites, some hungry for glory, others moved by the desire to help mankind (with both impulses present in one and the same individual). Such rising stars have gone on to lay down the law (and hence, encouraged popular misconceptions) about this most feared of human diseases. </p>

<p>A perfect example is Dr. William Stewart Halsted (1852-1922), the Baltimore pioneer of radical mastectomies to whom "Unnatural History" devotes considerable attention. A complex character, Halsted was not insensitive (he corresponded at length with his patients). Still, his rigid orthodoxy overshadowed a generation of young surgeons, even as he cautiously hedged his bets about the outcomes patients and their anxious families could expect from his methods.</p>

<p>The cut/burn/poison era continued with the advent of X-ray therapy, followed by the rise of chemo. The war on leukemia elicited a particularly ferocious response (from “Emperor”, p. 168):</p>

<p>“The treatment protocol (…) could only be described as (…) “an all-out combat.” To start with, the standard antileukemic drugs were given in rapid-fire succession. Then, at defined intervals, methotrexate was injected into the spinal canal using a spinal tap. The brain was irradiated with high doses of X-rays. Then, chemotherapy was bolstered even further with higher doses of drugs and alternating intervals, “in maximum tolerated doses.” The treatment lasted up to two and a half years; it involved multiple exposures to radiation, scores of blood tests, dozens of spinal taps (…) the regime was considered so overwhelmingly toxic that the trial was assigned to relatively junior physicians…”</p>

<p>Dissenting views were occasionally heard. The "determinist" school of medicine made itself unpopular by pointing out that longer post-surgical cancer times were an illusion attributable to early detection (the ultimate time of death was unaffected). This was never the dominant assessment; and given human nature, optimistic talk has long coexisted with grim reality. Nor is Mukherjee immune. He makes conventionally supportive remarks about mammography (not always backed up by the latest international studies), and proudly asserts that today's researchers stand on the shoulders of giants. His data point the other way. Toiling in the absence of today’s scientific knowledge of cancer's genetic mechanisms (oncogenes and tumor suppressor genes), exactly how helpful were Halsted and all those early pioneers?</p>

<p>The late 1960s heralded a change of tack as the USA began throwing serious money at the problem, as evidenced by the "War on Cancer" launched by Richard Nixon, a president who blended policy and politics more astutely than he is given credit for. Meanwhile, researchers kept busy. The 1990s saw the rise of the Human Genome Project and its evil twin, the Cancer Genome Atlas (TGCA). Advances in knowledge of genetics ushered in targeted therapies (Herceptin for particular types of breast cancer, Gleevec for chronic myeloid leukemia or CML), which have yielded tangible results, even as other areas of cancer treatment have shown more equivocal progress.</p>

<p>In the space of a few weeks in January 2016, a spate of celebrities both major and minor succumbed to an ancient malady, all passing away at approximately the same age. These obituaries reveal what we already know—that cancer is a pugnacious adversary, rising to its feet after every knockout. It will never go away, for oncogenes are part of our physiological makeup, the dark underbelly to healthy, normal cellular mechanisms. Longer survival times and better quality of life for cancer sufferers do seem within our grasp, however. It helps to be an optimist, within reason--and with all due respect to Imhotep, at least some options are available now.</p>

<p>NEIL L. INGLIS </p>




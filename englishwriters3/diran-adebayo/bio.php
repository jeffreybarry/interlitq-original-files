                  <p>Diran Adebayo is a novelist, academic and cultural critic best known for his stylish, inventive tales of London and the lives of African diasporans. His work has been characterised by its interest in multiple cultural identities, subcultures, and its distinctive use of language. His debut novel, Some Kind of Black, won him numerous awards, including the Writers Guild of Great Britain's New Writer of the Year Award, the 1996 Saga Prize, a Betty Trask Award, and The Authors' Club's 'Best First Novel' award. It was also long-listed for the Booker Prize, and is now a Virago Modern Classic. His second novel, the ‘neo-noir fairytale’ My Once Upon a Time was also widely praised, and, with its ‘musical’ style, solidified his reputation as a groundbreaker. In 2003 The Times Literary Supplement named him  one of its Best Young British Novelists. His short fiction has appeared in anthologies such as ‘OxTales’.  As a critic, he's written extensively in the national press, discussing everything from politics to popular culture, including sports - the centrepiece of his next book, the memoir, ‘“Random, and Cricket.”</p>


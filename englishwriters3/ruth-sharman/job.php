<p><strong>Kettle’s Yard, Cambridge</strong><br><br>

<em>a continuing way of life … in which stray objects, stones, glass, pictures, sculpture are arranged in light and in space</em><br><br><br><br>



It’s like coming home<br>
after all these years,<br><br>

to a lightness<br>
almost forgotten,<br><br>

this reverence for things<br>
crafted or found – a huddle <br><br>

of stones, for instance,<br>
with shoulders hunched<br><br>

against the wind, <br>
or a seedhead horned <br><br>

like a tribal mask –<br>
things gracefully<br><br>

co-existing, this Miró <br>
by the door no more solid,<br><br>

it seems to say,<br>
than the browned umbel <br><br>

of an allium, a painted<br>
wildflower no lovelier<br><br>

than light reflected<br>
off a piano stool,<br><br>

things separate, simple,<br>
and connected, as the lines<br><br>

of a seashell echo<br>
the grain of scrubbed deal,<br><br>

its curve the perimeter<br>
of the table top.<br><br><br><br><br><br>


 




<strong>Silver Washed Fritillaries</strong><br><br><br><br>




You were right: it was never<br>
just about butterflies – your fritillaries,<br>
for instance, that were less washed<br>
with silver than this landscape. Yes,<br>
it was about you, my shy, awkward,<br>
complex father, a way of connecting.<br>
And it’s about being your eyes now<br>
and reporting back (as if such a thing<br>
were possible), making a mental note<br>
of the first white violets piercing <br>
winter debris, or this sea of grasses <br>
flowing towards a hill that’s been singled out<br>
for special attention by the light, <br>
and how the silver wash takes us  <br>
circling back to those afternoons <br>
watching for your butterfly in Neroche.<br>
You told me just before you died <br>
you thought <em>something</em> carried on,<br>
though you wouldn’t commit yourself<br>
to words like God or Heaven,<br>
and after all these were your church<br>
and your religion, the woods and fields,<br>
all the intricacies of butterflies and plants<br>
you trusted as evidence of purpose <br>
and design. But isn’t this the only way <br>
you live on, in these flimsy connections <br>
between butterflies and light, between sky <br>
and grasses and a name? Then why<br>
does it feel wrong to have poured you<br>
into the hard ground rather than let you fly<br>
free of earth, weight and limitations, <br>
scattering your atoms over the wide hills <br>
or in some opening in the woods<br>
where fritillaries settle on bramble flowers,<br>
lifting wings not feebly washed<br>
but jagged with bolts of silver lightning? <br><br><br><br><br><br></p>
                                       













                  <p>Michael Arditti is a novelist, short story writer and critic.   His novels are The Celibate (1993), Pagan and her Parents - Pagan’s Father in the USA (1996), Easter (2000), Unity (2005), A Sea Change (2006), The Enemy of the Good (2009), Jubilate (2011), The Breath of Night (2013) and Widows and Orphans (2015).  His short story collection, Good Clean Fun was published in 2004.  He was awarded a Harold Hyam Wingate scholarship in 2000, a Royal Literary Fund fellowship in 2001, an Oppenheim-John Downes memorial award in 2003, and Arts Council awards in 2004 and 2007.  He was the Leverhulme artist in residence at the Freud museum in 2008.  His novels have been short- and long-listed for several literary awards and Easter won the inaugural Waterstone’s Mardi Gras award.  In 2012 he was awarded an Honorary DLitt by the University of Chester.  He has written literary and dramatic criticism for various papers and is currently theatre critic of the Sunday Express.</p>

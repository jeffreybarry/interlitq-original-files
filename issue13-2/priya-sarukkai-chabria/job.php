            <p><strong>We Must Talk</strong></p>
           
            <p>We must talk &amp; do so on this soil that turns<br>
            swamp turns shivering sea: turn it into neutral<br>
            waste (<em>o limned in clemency!</em>) that<em> </em>buoys us <br>
            both on seaweeds bereft of weight, of wakes. </p>
           
            <p>We must talk … remembering love, summoning its<br>
            hands on us as we sing from siren perches <em>come die</em><br>
            <em>against me please</em> or break off, each a wailing wall<br>
            in murk submerging while pores pour incense spores. </p>
            
            <p>We must talk over my swell of fear and your tide <br>
            of neglect, casual in its sweep. But do you want <br>
            to swim <em>past its rich rewards </em>to see me reappear? Alarm<br> 
            whimpers in me saying darken, dive, disappear. </p>
             
            <p>We must talk -- beyond hope to look at what may<br>
            be.  Around us glitter gifts – our life of years together --<br>
            as litter. Sparse grace. Could we build on flotsam yet?<br>
            Can we net phosphorescence? <em>What’s your take? </em></p>
            
            <p>We must talk; talk of anguish caused that so weeps to hide<br>
            its face while shame seeps from entrails to lustrate me in <br>
            inkiness; a black beacon to which your regret is a lifejacket <br>
            hurriedly hurled   just out of reach. <em>Is it deliberate, my sweet?</em></p>
            
            <p>We must talk: am I imagining you shipwrecked while<br>
            you are safe elsewhere, your shadow too? My tongue,<br>
            marooned, still stutters blessings after you. Tell me at our<br>
            accident site over the sirens’ blare <em>there’s nothing to rescue</em>.</p>
            
            <p>We must talk. As I drown a slip of moonshine intones<br>
            your words familiar, bewildering, as your reflection <br>
            melts into me as craved chyme, as the horizon<br>
            <em>unrelenting</em> lobs stars overhead      beyond time. </p>
            
            <p>We must talk, must answer prayers part <br>
            the waves herald vision but my dissolved<br>
            retinas can’t see you anymore <em>my love my love</em></p>
           

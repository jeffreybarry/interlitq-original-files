            <p><SPAN LANG="en-US">(for </SPAN><SPAN LANG="en-US">María Cleofé Boglio)</SPAN></p>
            <p><SPAN LANG="en-US">In the Argentine, her pater recited Pascoli,<BR>
              brooding on autumnal Tuscan afternoons when,<BR>
              as a boy, dogs straining at their leashes,<BR>
              he walked with his father, searching for truffles. <BR>
              Like the beads of a rosary, she counted the stations of his cross:<BR>
              Mussolini, his years escaping, Paolo's embrace, Ethiopia.<BR>
              Under a plate in the kitchen she hid each coveted letter<BR>
              from a cherished, faceless, stranger, <BR>
              savoring the name of every distant cousin.<BR>
              On her terrace, alone, on autumn afternoons,<BR>
              she sensed, in the long shadows, that town she had never known:<BR>
              the child in his father's arms, the plangent dogs,<BR>
              the desperate partisan chasing along the rooftops. <BR>
              It was autumn then and war was raging.</SPAN></p>
            <p><BR>
            </p>
            <p><em><a name="spanish">Del Latin recordis</a></em><strong> </strong></p>
            
              <p>(Para María Cleofé Boglio)</p>
              El nos leía a Pascoli en la luz<BR>
              de la mañana y hablaba de las tardes<BR>
              aquellas del otoño, los perros oliendo <BR>
              entre las setas, cuando iba con su padre<BR>
              a buscar trufas. Ella sabía de memoria<BR>
              la vida de él. El nombraba la guerra,<BR>
              los años escapando, el abrazo<BR>
              de Paolo y Etiopía. Ella escondía<BR>
              bajo el plato las cartas que llegaban,<BR>
              y les sabía los nombres a los primos<BR>
              lejanos. A veces en las tardes<BR>
              recientes del otoño, ella recuerda<BR>
              a Pascoli y a un pueblo que no ha visto:<BR>
              hay un niño con su padre y unos perros,<BR>
              y hay un hombre que se larga por los techos,<BR>
              y un amigo, y es otoño,<BR>
              y es la guerra.</p>

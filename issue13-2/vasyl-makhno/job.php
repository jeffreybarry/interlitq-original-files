            <p><em><strong>Winter Letters</strong></em></p>
            
            <p LANG="en-US"><strong>My Father</strong></p>
            <p LANG="en-US">1</p>
            <p>you know my father was the same age as John Lennon<br>
            when John was singing it was batteries he clamped on<br>
            and on weekends he would also tie one on as a car mechanic<br>
            something would always break - plugs flooded by gas would fail<br>
            Beatles would fly to India - winters were passing by<br>
            and the flower children were breaking stalks and stanzas</p>

            <p>you know the little town: a few cars - city hall - market square<br>
            cobblestones set over centuries like verse in rhyme<br>
            window panes in buildings vibrated like a violin deca<br>
            end of the 50's: the children of war - a damaged generation<br>
            wearing a cheviot coat in rubber boots knee-high<br>
            standing at the bus stop was my mother - a student</p>

            <p>you know in music classes: accordion guitar or dombra*<br>
            music demands a sacrifice as if from a donor<br>
            the school of ed orchestra in a former synagogue<br>
            now converted to a district club - is kept warm by a kerosene stove<br>
            in his brand-new &quot;hazon&quot;** dragging repeatedly on his cigarette -<br>
            my father is waiting for my mother - on the corner</p>

            <p>you know after a few years their failed marriage fell apart<br>
            the music had suddenly changed - I grew and fattened up<br>
            John who married Yoko headed throngs of hippies<br>
            started singing about flowers - altered his wardrobe and style<br>
            grew a beard/moustache like a prophet in the wilds<br>
            allured by freedom and the leftists</p>
            
            <p>you know that little town will also change as military<br>
            units occupy the barracks airmen and corpsmen<br>
            the spring and fall draftees as cyclical as nature's seasons<br>
            will sing John's songs: about yesterday and the day before<br>
            about unrequited love - singly and in chorus -<br>
            about all that passes by</p>

            <p>you know on the street corner where my father awaited my mother<br>
            the music has not changed it sounds the same to me<br>
            and I hear that orchestra off tune and off beat<br>
            and John lying on the pavement - shot in New York -<br>
            in her dark glasses by now the aged Yoko<br>
            and the music like divorce I simply don't get</p>

            <p>you know because my father was the same age as John Lennon<br>
            I see him as a young man speeding in the cab of his &quot;hazon&quot;<br>
            while my mother stands on the corner waiting humming &quot;Let It Be&quot;<br>
            she is his girl and he is hurrying flying<br>
            they have but a brief moment left - indeed only an eyewink<br>
            but their music and the music of the Beatles will remain with me</p>
            <p><em>*Dombra - a Kazakh </em><em>string instrument</em></p>
            <p><em>**Hazon - a Soviet truck</em></p>
            <p LANG="en-US">&nbsp;
            </p>
            <p LANG="en-US"><strong>Eugene</strong></p>
            <p LANG="en-US">2</p>
            <p>The magi have also arrived in New York and so did Christmas<br>
            as Mary and Joseph are fleeing together on a donkey<br>
            and the Christmas moon shines like a brand-new penny<br>
            and also the kutya* - and the January snow falls into the ocean all night long<br>
            and Broadway is crunching like a fresh croissant<br>
            and so does Rockefeller Center</p>
            
            <p>and through the Christmas night one who doesn't think himself a magus<br>
            nor a lone wolf watching the path of those two<br>
            is my friend Eugene - he enters the wintry orchard<br>
            of New York streets - and searches for his keys and lock<br>
            perhaps that's why he looks like a pursued wolf<br>
            and a lonely Sartre</p>
             
            <p>only there in the subconscious mind of his previous life<br>
            he is still sated with snows - there he had his fill of kutya<br>
            there he knows no New York - doesn't pay monthly rent<br>
            the landlord doesn't drag him to court for his music and books<br>
            there the loneliness does not wrench his guts<br>
            and the &quot;Strand&quot; does not pull him by the hand</p>
           
            <p>Eugene usually stands across from the Carpatho-Ruthenian church<br>
            fumbles in his pockets for bills and change<br>
            he knows every alley - he knows about Trotzky and Warhol<br>
            complains about old age - and about the death he could do without<br>
            his small fortune he carries in a bag<br>
            with a Coca-Cola ad</p>
            
            <p>to Eugene old age means the destiny of music<br>
            scarf - beret - overcoat with missing buttons<br>
            windows in his apartment blocked by books from the floor up<br>
            the constant loneliness that stalks him like a killer<br>
            on his table there is coffee the smell of which has spilled out<br>
            like milk from a carton</p>
            
            <p>and because the magi have by-passed Sixth and Seventh streets<br>
            Eugene believes that the snow is really bees that leave their hives<br>
            right on Christmas - and this thought is as sweet as halvah<br>
            stretching as a trail of marijuana smoke<br>
            Eugene encounters a Mexican named Juan<br>
            and takes the man for a magus</p>

            <p>but the holidays are passing though they always abide with you throughout winter<br>
            Eugene and I are together but always alone in New York<br>
            in his overcoat like a disheveled angel he turns the corner<br>
            dragging bags full of books on the wet asphalt<br>
            two female soloists run outside into the cold from the Amato<SPAN LANG="en-US"> Opera</SPAN> building<br>
            to announce: the year had ended</p>
          
            <p><em>*Kutya is a traditional Ukrainian Christmas dish</em></p>
            <p><em>**Strand is a known bookstore in </em><em>New York's Greenwich Village</em></p>
            <p>&nbsp;
            </p>
            
            <p><strong>STATEN ISLAND</strong></p>
            <p><SPAN LANG="en-US">3</SPAN></p>
            <p>Staten Island - is an island meaning - a cripple on crutches<br>
            a ferry will get you there - the GPS won't miss it<br>
            and along the ocean baseball fields and Capodano<br>
            boulevard - desolate and windy - in December and January<br>
            This island which has borrowed the space it needs<br>
            with shops and restaurants</p>

            <p>will immerse up to its ankles into the frigid Atlantic - Meaning<br>
            it is the key that starts a &quot;Toyota&quot; - It views<br>
            both the Verrazano bridge and the outlines of Brooklyn's jaw bones<br>
            and sees an earring - this ear piercing as a challenge<br>
            and then the ships - which sail to New Jersey<br>
            iced up from the snows - that came down heavily</p>
            <p>burying hillocks and streets - and the Buddha at the museum<br>
            a parked &quot;Alfa Romeo&quot; - whose number I'll forget<br>
            a golf course - and the old Italians enjoying soccer<br>
            pizza and films about an indestructible mafia<br>
            drinking coffee in trattorias - eating chocolate muffins<br>
            their Sicilian defense penetrated by a goal</p>
            <p>In reality on Staten Island stillness is displaced by the sliding snow<br>
            it scurries like a mouse into darkness - darting like a gray squirrel<br>
            there's the smell of Chinese soup and Italian dough<br>
            the gathering of black elbows and basketball shirts<br>
            the jazzy chase of a car which at midnight eludes<br>
            police &quot;Chevys&quot; - and will make tomorrow's paper</p>
            <p>to me this winter with its snowfalls dictates letters and verses<br>
            walks along the ocean that will resolve nothing<br>
            because writing a book is like observing motion<br>
            like peeping through a lock in the door - and the canine<br>
            passion to sniff all around your space - and by the pier to spy<br>
            on a couple kissing while firmly holding hands</p>

            <p>because life after forty tells you to look at things more simply:<br>
            and not to ask why snow and stillness come in equal parts<br>
            and also not to ask about the meaning of signs<br>
            and why in these snows your doubts - feeling their way like blind people -<br>
            are pushing towards you - there's the stench of bird droppings<br>
            for all of this - bro - you must have known before</p>

            <p>therefore this island having warmed up the parked &quot;Alfa Romeo&quot;<br>
            with birds sitting on it mainly puffed up seagulls<br>
            is sharing with you the snow and the word - like cigarettes - a friend<br>
            and tells you not to question life's principles and proportions<br>
            and to the winter birds warming themselves on the strait<br>
            you toss some bread on the run - followed by an occasional cent</p>

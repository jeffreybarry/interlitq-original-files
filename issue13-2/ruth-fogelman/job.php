            <p><strong>There Used To Be In Jerusalem</strong></p>
           
            <p>There used to be in Jerusalem an old man<br>
            with leathery face and white-stubble chin.<br>
            He led his mule down our narrow lane,<br>
            huge cans of kerosene strapped to its sides,<br>
            copper bells clanging about its neck.<br>
            The kerosene heated our rooms, cooked our stews.</p>
            
            <p>There used to be in Jerusalem an old man<br>
            with deep-furrowed brow and white-stubble chin.<br>
            He came clanking down our cobbled lane,<br>
            slung over his shoulder – a large brass box. <br>
            He would knock on our doors – <br>
            &ldquo;Any pots to repair or kettles to mend?&rdquo;</p>
            
            <p>There used to be in Jerusalem an old man<br>
            with sunken cheeks and white-stubble chin.<br>
            He hobbled down our tunneled lane,<br>
            hanging from his shoulder – a threadbare sack.<br>
            He would call out, &ldquo;<em>alte zacheen</em>… <em>alte zacheen</em>…&rdquo; *<br>
            We ran down to the courtyard, gave him a shirt or two.</p>
             
            <p>There used to be in Jerusalem an old man<br>
            with pendulous lip and white-stubble chin.<br>
            He sat on a wicker stool at his copper shoe-stand<br>
            opposite the Citadel at Jaffa Gate.<br>
            We too, were his customers, one foot raised on the stand<br>
            while, with two brushes, he polished our shoes.</p>
            

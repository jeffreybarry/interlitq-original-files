<p><strong>Esther’s Choice</strong></p>
<p>…<em>on the third day Esther put on her royal apparel…</em></p>
<p ALIGN="RIGHT">Esther 5:1</p>

<p>Of course I’d heard of Vashti’s victory – <br>
how she chose to rebel and die<br>
rather than submit to his whims.</p>

<p>Why didn’t I, too, resist, prefer death <br>
to lying each night like a wooden plank?<br>
What made my uncle, with one slight nod,<br>
encourage me to this degrading life?<br>
But what good would martyrdom have been?</p>

<p>Yet now I choose to face death,<br>
by taking action, lose eternal life:<br>
I don my robes, perfume wrists,<br>
and line my eyes and lips<br>
before I dare approach him.</p>

<p>For how can I sit, protected, <br>
and ignore my people’s plight? <br>
How can I refuse to act<br>
when their existence hangs <br>
like an autumn leaf on an oak?</p>

<p>Will my three-day fast move heaven?<br>
Will my people’s prayers be heard?<br>
Will he extend his scepter of gold<br>
Or will he, in anger, remove me<br>
Before I say a word?</p>

<p>If he grants my request,<br>
tomorrow, instead of sitting with the wise<br>
at a sacred feast of savories and meat,<br>
I shall sit with pompous pigs<br>
at a pagan spread of pastries and wines.</p>

<p>And if not…</p>


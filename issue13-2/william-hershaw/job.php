            <td colspan="4" rowspan="2" class="texto2" valign="top">
            <br>
            
           
            <p> Christ succour  and remeid that stad<br>
            
             for he livis at ease that freely livis,<br>
           
             so  sing  with us -  away, winter, away !<br>
           
             Obey and thank thy God for all,<br>
           
             for martyr thou art for Scotlandis richt. </p>
            
            
            <p> Thoch Luve be sweit, oftimes it is full sour<br>
            
             and the mair vertew thou sud hae tae keep thee.</p>
            
            
            <p> A harlot wonnit neir by and scho<br>
            
             shot her meikle mou and aye scho winkit<br>
           
             - Allace ! With a rair -  reven is ma reid ! <br>
           
             Ane spark of Luve then til his hairt cud spring<br>
           
             ilk day.</p>
            
           
            <p> - My Guddame wes a gay wyfe, but she wes<br>
            
             the Fiendis fell. Mahoun gart cry - A dance! <br>
           
             Syne -  gousty shaddois of eild and grisly deid.<br>
            
             Nou sensual pleesour hes banisht Chastitie.<br>
            
             Nae cause tae sing, for in this New Year I see<br>
            
             Willie, allane, at Christ’s Kirk on the Green.</p>
            
            
            <p> - O Mistress mine, til you I me commend !<br>
            
             Hir persone so perfyte, Nature in hir<br>
           
             but breakis my hairt, and nocht the better -<br>
           
             O Venus clere, of Goddis stelliyit,<br>
           
             fane wad I luve, bot quhairabout ?<br>
           
             Luf hes me wardit in ane park of pane,<br>
            
             without gledness, quhair ivir I ryd or go.<br>
            
             Thir Lenterne dayis ar luvely lang,<br>
            
             your twa fair ene is wyte of all my wo,<br>
           
             For hir maik mair dule, <br>
            
             than I may  nocht indure!<br>
           
             All winter throcht this ros wis reid and now<br>
            
             irkit I am with langsum Luvis lair,<br>
            
             mara bhfaigh me’ furtacht traa’th,<br>
            
             go hairt, unto the lamp of licht, go hairt,<br>
            
             to seek ma spreit I sent my hairt tae thee,<br>
            
             to follow thee, it frae my body fled.<br>
            
             Now skaillis the sky, the nicht is neir gaun,<br>
           
             frae bank to bank, frae wuid tae wuid I rin,<br>
            
             led bi a blin, and teachit bi a bairn,<br>
           
             the ane’s but smoke, the ither is but the wind.</p>
            
            
            <p> Lang  may the ladies  staund <br>
            
             wi their gowden kems tae haund -<br>
            
             and wi ae lock o his gowden hair <br>
            
             we’ll theek our nest when it grouwes bare.</p>
           
            
            <p> O, he’s mounted her <br>
            
             on a milk white steed<br>
            
             and they rade on and on, <br>
            
             they rade and aa<br>
           
             till it’s  whaur hae ye been, <br>
            
             my handsome young man ?</p>
           
           
            <p> Yestreen I dreamed a dolefou dream, <br>
            
             Clerk Saunders said - A bed for you and me,<br>
           
             Elfland, where thou and I this nicht maun gae,<br>
            
             tae the bonnie milldams  o Binnorie,<br>
           
             till the channerin worm doth chide </p>
           
            
            <p> - But  whit wull ye leave tae your bairns and your wife ?<br>
          
             O the curse o Hell fae me sall ye beir.<br>
           
             
            <p> The like o Habbie was nae than but now  -<br>
            
             wha wadnae be in love wi bonnie Maggie?<br>
            
             She’s aff wi the Gaberlunzie Man !</p>
           
            
            <p> Lord (since thou know’st where all these atoms are)<br>
            
             abhore and hold a synod in thy heart.</p>
            
           
            <p> In days when our King Robert rang his trews<br>
           
             (nou he gings droupin about the dykes),<br>
           
             refleck, I was a dug in much respect.</p>
           
           
            <p> - Set down! Drive at the jango till he spew,<br>
           
             wooed and merried and aa merried and wooed.<br>
           
             The sky saddens with the gathered storm.</p>
            
            
            <p> A hare, ae morning, chanced tae see a partan<br>
            
             dance the reel o Tullochgorum.<br>
            
             Cope sent a challenge tae Dunbar -<br>
            
             The Flooers o the Forest are aa wede awa<br>
             and all true Scots wi God importunat be<br>
           
             from Pride, Poverty and Greed united.<br>
           
            
            <p> North, our kintra leid is faur fae barren<br>
           
             so tell me how tae woo thee, Love ; o tell<br>
            
             wi routh o gabby saws and says and jokes<br>
            
             hou cauld blaws the nippan North wi angry souchs !<br>
          
             Waes o ma hert fa in shooers fae ma ee,<br>
            
             I backward cast my ee on prospects drear.</p>
           
            
            <p> The mair she bangs the less she squeals an hey,<br>
          
             frae the Forth, wha will buy my caller herrin ?<br>
           
             I shall rowe thee in my plaid, my bonnie<br>
            
             and we in dreams behold the Hebrides.<br>
            
             I hae grutten monie a day <br>
            
             as the laverock fae the blae lift drops.<br>
           
             And aye she loot the tears doonfa for Jock.</p>
            
            
            <p> Mirk and rainy is the nicht, there’s no a starn,<br>
            
             in aa the cairry,<br>
            
             twas on a keen December night, John Frost<br>
            
             thro aa  the kintra-side he ran <br>
           
             an far<br>
            
             my young footsteps in infancy wandered <br>
           
             up in the midnight towers, the belfried spire,<br>
            
             and the night has come, and planets glinted, O ! </p>
            
            
            <p> Ower the graves of the martyrs the peewits cry<br>
            
             for his father’s house looked out upon a firth<br>
            
             - nae sign o a thow yet, <br>
            
             ay that’s me, John Watt,<br>
            
             hameward, <br>
            
             ower the hill in the sma, sma rain.<br>
            
             O wad this braw hie-heapit toun sail aff,<br>
            
             these windy spaces are surely my own,<br>
            
             though the summer make a chill wet weather.<br>
           
             There was a star, but nou it disnae shine,<br>
            
             we were a tribe, a family, a people.<br>
            
             Nae man or movement’s worth a damn unless<br>
            
             whummelt  I tak a bobquaw for the lift,<br>
            
             crawlin aboot like a snail in the mud<br>
           
             the great stones of the tomb enfolded Jesus.</p>
            
           
            <p> She gane : and wi her aa ma simmer days.<br>
           
             As weill, imphm, in Embro tae the ploy.</p>
            
             
            <p> Today a fine old face has gone under,<br>
           
             the salt-crusted sea boot, the red-eyed mackerel,<br>
           
             frost in my lungs is harsh as leaves scraped up<br>
           
             for it wad dee, the Cypress buss I plantit.</p>
            
           
            <p> For I will sooth you in a softer bed,<br>
           
             the nether rocks of a headland never,<br>
           
             faain continuallie, wi nae devall.</p>
           
            
            <p> She socht me, <br>
            
             dwell her thochts whaur dwalt her een ?<br>
           
             Hou monie things throu the years I hae been !</p>
           
             
            
            <p> So farewell, Rhynie, and adieu to you!<br>
          
             A recruiting sergeant on me cast an eye,<br>
          
             we wandered up Jamaica Street and roond.<br>
            
             It’s there I had my first braw nicht wi Belle,<br>
            
             aa the roses and geans will turn tae bloom.</p>
            
            
            <p> The shuttle rins, <br>
            
             the shuttle rins wi speed<br>
           
             and them that wark the hardest are aye <br>
            
             wi least provided nou<br>
            
             your Daddy’s doon the mine ma darlin,<br>
            
             your natural food it is tatties and herrin.<br>
            
             Too well they knew the danger and the risk.</p>
           
            
            <p> When came ye over, bold Erin Go Bragh ?<br>
            
             He disnae like yer face, it’s Paddy are ye?<br>
            
             I ken whaur I’m gaun, you’re no comin wi me.</p>
            
            
            <p> A wis ludgin wi Big Aggie, <br>
            
             she’s juist a Kelty clippie<br>
            
             hame drunk on Monday  nicht cam I<br>
            
             singing -<br>
            
             My Blackbird Is Forever Flown !<br>
            
             -  Oh lease me on your curly pow.<br>
           
             My joys would be complete, lassie,  lie near me.</p>
            
             
            <p> And wi you, and wi you, <br>
            
             and wi you, ma Johnnie lad,<br>
           
             and ho the cuck and hey the cuckoo’s nest.</p>
            
            
            <p> and noo ma caunle it is neir brunt oot,<br>
            
             she lies ablow my body’s lust and luve.<br>
            
             The waiting good I send this drop to melt,<br>
            
             go sad or sweet or riotious with beer,<br>
           
             I lie here still. Yes truly still. </p>
            
            
            <p> Oh had her apron bidden doun the Kirk<br>
            
             wad ne’er hae kenned</p>
           
           
            <p> She’s howket a grave bi the licht o the muin<br>
           
             and when I return I will lay ye doon.<br>
           
             To her all along by the Magdalen Green<br>
            
             an the rose is aye the redder aye.</p>
           
            
            <p> The green grass owre his grave  was growing,<br>
           
             he was  a bonnie callant <br>
            
             and he played at the ba,<br>
            
             young Jamie Foyers, the flooer o them aa,<br>
           
             aye,  the young lad named John Thomson ,<br>
           
             fae the Wellesley Pit he came.</p>
            
            
            <p> Cam aa ye tramps and hawkers, <br>
          
             ye gaitherers ablaw,<br>
            
             nnvent, bright friends, Theology or die.<br>
            
             There’s the chink  atween thinkin and daein,<br>
           
             owre monie ghaists, fir me tae gang ma lane<br>
            
             tae staund atween the day - sky and the nicht.<br>
            
             Their daughters and their sons are a wood going,<br>
           
             aince mair tae the plantin and the pearlin -<br>
           
             Eros rules, floret silva undique.</p>
           
            
            <p> They ca’d him Jock the Lum, my faither,<br>
            
             he was a makar, weel ye may believe it.<br>
            
             There is ae silence for Baudelaire,<br>
            
             what galaxies he endit whan you deed !</p>
            
            
            <p> Ring of waves, row of nets, string of lights,<br>
            
             doon tae the shore at the fit o the toun<br>
            
             we socht for bait on the braid bay sands,<br>
            
             skirlin and lauchin, ilk  wi spindthrift weet</p>
            
            
            <p> Gif I had ae short simmer o sang<br>
            
             An caa my  nain da heft I cannae hae,<br>
            
             the lane sodger lad, his leefou lane,<br>
            
             An aa for whit ? Was Hamlet gyte ? Discuss<br>
           
             a sairie actor that strunts and rants his hoor . . .<br>
           
             Ocht wheesht ma hert, an wheesht yet mair ma mou.</p>
           
            
            <p> Yer  poems are deean in the Ha’s o Academe,<br>
            
             there are ither sichts for willin een tae see,<br>
           
             for there like a barfuit bairn staunds nicht.<br>
           
             Whit I wis, I’m no nou, <br>
            
             - Ye’ve cheenged, you say,<br>
            
             an wi whit ferlie een, syne, dae I see ?<br>
           
             We dinnae speak lik this : we do not talk thus.<br>
           
             Do they not know they wring my heart as well ?<br>
            
             I wrote on the page from the West to the East,<br>
            
             God wove her web of being all divine,<br>
            
             howker o braw seams, Christ the Miner.<br>
            
             As long as there shall but one hundred of us remain alive,<br>
            
             land of my heart forever, Scotland the . . .<br>
           
             As long as there shall but one hundred of us remain alive,<br>
           
             A   long as there shall but one hundred of us remain alive,<br>
           
             A   long as there shall but one hundred of us remain alive,<br>
           
             A   lon   as there shall but one hundred of us remain alive,<br>
         
             A   lon  as   here    &nbsp;&nbsp; all but one hundred of us remain alive,<br>
           
             As long as there shall but one   &nbsp;und&nbsp;ed of us remain alive,<br>
           
             As long as  &nbsp;here  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;	      one &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;	          s   &nbsp;em&nbsp;in   &nbsp;live,</p>

                  <p><span lang="EN-US">Born  in January, 1891, in Warsaw, Poland, Osip Emilievich Mandelstam was  raised in the imperial capital of St. Petersburg, Russia. His father was  a prominent leather merchant and his mother a teacher of music.  Mandelstam attended the renowned Tenishev School and later studied at  the Sorbonne, the University of Heidelberg, and the University of St.  Petersburg, though he left off his studies to pursue writing. He  published his first collection, <em>Kamen,</em> or <em>Stone</em> (1913),  when Russian Symbolism was the dominant persuasion. Like Mayakovsky and  Khlebnikov, who cleared the ground for Russian Futurism, Mandelstam  departed from this old mode of expression in favor of a more direct  treatment of thoughts, feelings, and observations under the aegis of  Acmeism, a programme that included Nikolay Gumilev and Anna Akhmatova.  As translator Clarence Brown observes, Mandelstam's variant of Acmeism  was a mixture of poetics and moral doctrine, the former based on an  "intuitive and purely verbal logic of inner association" and the latter  on a kind of "democratic humanism." His second book, <em>Tristia</em> (1922), secured his reputation, and both it and <em>Stone</em> were released a year later in new editions.</span><br>

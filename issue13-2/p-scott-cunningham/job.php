            <p><strong>In the Future</strong></p>
             
            <p>all exams will contain four Morton Feldman questions:</p>
             
            <p>1) List from most accessible to least accessible all 181 of Feldman&rsquo;s compositions<br>
            2) Name three people Feldman made love to<br>
            3) Hum <em>Rothko Chapel</em><br>
            4) What does sound mean?</p>
            
            <p>In the future,</p>
            <UL>
              <LI>
                All corporate jingles will derive 	from Feldman&rsquo;s music
              <LI>
                The beat of the human heart has 	been proven to be atonal              <LI>
                Before dinner, it is customary to 	read a Morton Feldman essay or two
              <LI>
                The word &ldquo;boredom&rdquo; now means 	&ldquo;a state of liminal ecstasy&rdquo;, as in &ldquo;This sex that we&rsquo;re 	having on the edge of an Aegean cliff, Turkish heroin electrocuting 	our veins, is boring&rdquo;
              <LI>
                To express excitement or approval 	for the home team at sporting events, the crowd goes silent
              <LI>
                Jazz has been declared &ldquo;silly&rdquo;
              <LI>
                The French are derided as 	hopelessly narrow-minded
              <LI>
                Density of conversation, as in 	talking too much when one is talking, and then making no sounds at 	all when one is not talking, is considered a hallmark of genius
              <LI>
                Anecdotes are traded on the stock 	exchange, where Feldman anecdotes remain the most valuable. Like 	this one,
              </UL>
            
            <p>Feldman was dying in the hospital in Buffalo. The cancer had spread too quickly. Barbara was with him, and he was ready, telling her he had lived a full life and had no regrets. Only one thing was bothering him, the beep of the EKG machine. &ldquo;It sounds like something I would have written,&rdquo; he said, &ldquo;like hearing myself thinking.&rdquo; He asked the nurse if she could turn it off. &ldquo;That&rsquo;s against the law,&rdquo; she said. Could he could play a record over it? What if he called in a few friends and they hooked themselves up to one, too? By now snow was falling into the dusk, and the situation was hopeless. Feldman took Barbara&rsquo;s hand and said, &ldquo;I know how to stop the beeping.&rdquo; And then he did.</p>

            <p><strong>A Famous Anecdote in Reverse</strong></p>
            
            <p>Jackson Pollack!<br>
            A man walking with his hands in his pockets<br>
            The intersection of 6th Ave &amp; Fourteenth St. <br>
            Feldman turns and looks<br>
            Wolpe points toward the street<br>
            What about the common man?<br>
            I care about the sounds<br>
            It&rsquo;s too esoteric<br>
            Feldman plays<br>
            Play it<br>
            I brought something new<br>
            Have a cup of tea<br>
            Wolpe!<br>
            Feldman!<br>
            The tea is boiling<br>
            Steps creak<br>
            The long subway ride<br>
            The sun rises<br>
            Strange, indecipherable dreams</p>
            <p><BR>
            </p>
             
            <p><strong>Buffalo &lsquo;86</strong></p>
             
            <p>The snow in Buffalo blows<br>
            to and fro<br>
            burying cars along the avenue home.</p>
             
            <p>No<br>
            one&rsquo;s inside to turn their radios on<br>
            so<br>
            silence plays silently to no one—<br>
            
            <p>rows of sunken clouds<br>
            domes of metal, leather and snow.<br>
            
            <p>I can&rsquo;t open the window<br>
            so the television&rsquo;s on—<br>
            news like a fog horn<br>
            an always-ringing phone</p>
             
            <p>the fathers and husbands of Buffalo<br>
            emerging, at dawn, from their homes—</p>
            
            <p>heavy-clothed<br>
            armed with brushes and shovels.</p>
           
            <p>One by one<br>
            they scrape off the snow<br>
            the engines turn on</p>
            
            <p>and I open the lid of my piano.</p>
            

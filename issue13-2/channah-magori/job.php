<p><strong> Lamentations: From a People-in-Mourning</strong></p>

<p>In the Holy Land<br>
After the bus bombs,<br>
After the restaurant bombs,<br>
After the market bombs, <br>
We plant people <br>
Instead of trees<br>
And the forests grow <br>
In grey stone.</p>
 
And &#8212; the gardeners of granite<br>
The gatherers of memories &#8212;<br>
Water stones<br>
That will not grow.</p>

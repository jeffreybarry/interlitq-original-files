            <p><strong>Outrunning Trains</strong></p>
           
             
            <p>Like a child outrunning trains <br>
            Her long hair flying in figure eights<br>
            Ribbons and bows kissing <br>
            The butterfly wings<br>
            Leading the skylarks<br>
            As formation queen<br>
            She runs</p>
            
            <p>With the place in her heart<br>
            Whose lifetime is a hollow<br>
            Waiting only for this moment<br>
            To be filled </p>
           
            <p>Like a young bride<br>
            Whose heart is a clock<br>
            Tick tock, tick tock<br>
            Until time shouts out<br>
            <em>This time – my time –</em><br>
            Collects the seconds<br>
            Her trousseau<br>
            She listens</p>
             
            <p>But the train<br>
            Outruns her<br>
            And her braided eights<br>
            No longer infinities in the air<br>
            Kiss the butterflies goodbye <br>
            And the birds move south<br>
            And the clock winds down<br>
            Until every hour is the same hour<br>
            And the seconds grow dusty <br>
            In their yellow tissue paper </p>
            
            <p>And she dips her heart in bronze<br>
            Like baby shoes<br>
            Its chambers hard<br>
            Like hollow bells<br>
            She hangs the clappers<br>
            That strike the sounds<br>
            That echo dreams</p>
             
            <p>And she listens to the <br>
            Eights of infinity<br>
            As they ride the winds<br>
            Round and round<br>
            In the place of her heart</p>
            
            <p>She lays her head down<br>
            She listens.</p>

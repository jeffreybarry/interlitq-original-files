            <p><strong>De las familias</strong></p>
           
            <p ALIGN="RIGHT"><em>a Patricia Malone</em></p>
            <p>I</p>
            
            <p>Un fuerte olor de vinos desplazándose en la casa,<br>
            una fuerte carpa de sombras ocupa la noche.<br>
            Es cuestión de hacernos con el tiempo, de sodomizarlo.<br>
            Le pisarás su cabeza de perfumes<br>
            lo dejarás en el suelo abierto<br>
            para vivirnos entre manzanas y palabras</p>
            
            <p>palabras palabras solares</p>
            
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;para las manzanas y las uvas</p>
            
            <p>elegir entre el miedo y las palabras</p>
            
            <p>&ldquo;Dejaré de escribirte,<br>
            dejaré de escribirte palabras insolentes en el cuerpo.<br>
            Tendremos una familia.&rdquo;</p>
           
            <p>Ella quería ser su puta<br>
             &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;y no lo supo o no lo supo decir<br>
            y se quedó sin tiempo,<br>
             &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;y otro tiempo le hizo un hijo.</p>
           
            <p><BR>
            </p>
            <p>II</p>
           
            <p>Las mariposas arruinaron una tarde de lluvia.</p>
            
            <p>&ldquo;¿Vos me lo escribiste?&rdquo;<br>
            Ella ocupaba todo el sol. <br>
            Lloraba.<br>
            Gastaba el papel entre las manos <br>
            con inocencia.<br>
            &ldquo;¿Hiciste esto para mí?&rdquo;<br>
            Reía y lloraba de palabras.</p>
            
            <p>Todavía eran jóvenes y simples como los comienzos.<br>
            Ahora no entienden la música de la lluvia.</p>
            
            <p><BR>
            </p>
            <p>III</p>
            
            <p>&ldquo;<em>Siempre haces fiestas para ocultar el silencio Mrs. Dalloway.&rdquo;</em></p>
           
            <p>Domingo, como los domingos un color pastel. Una fiesta.<br>
            La familia; la conversación insignificante (sostenida);<br>
            el cuadro del plato minucioso; el sonreír ordinario.<br>
            Siempre los ojos del niño. Algo vacío.</p>
            
            <p>(El silencio en la cama; el otro a medio decir, tras los ojos.)</p>
           
            <p>El prolongado esfuerzo de construir algo que no amamos.<br>
            Los años.</p>
          
            <p><BR>
            </p>
            <p>IV</p>
            
            <p>&ldquo;¿Por qué no me paseas?&rdquo;</p>
             
            <p>(…) y encendió algo para ocupar el silencio; no sabía callar—<br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;las llamas heladas del tedio<br>
            Tal vez hacía diez años que estaban <em>muertos</em><br>
            que simulaban <br>
            que se repetían con terca desgana<br>
             &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;años ya- para qué<br>
            años<br>
            diciendo palabras de otros</p>
            
            <p>una cama molestada por dos cuerpos<br>
            dos cuerpos, una sábana, todos los anhelos todas<br>
            las noches<br>
            una misma dirección<br>
            - sus alientos, sus besos se repelen - </p>
           
            <p>Se obligan a vivir por molestias.<br>
            Escuchan<br>
            es decir repiten<br>
            para postergar el vacío<br>
             &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;cuyo nombre olvidaron<br>
            no recuerdan esa planta—<br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;  los espejos deshabitados del sótano.</p>
            
            <p>Olvidaron el idioma de la lluvia.</p>
           
            <p>Olvidaron lo que estaba en la sangre<br>
            como plantas que se mudan al hielo.</p>
            
            <p>En la vida había cosas tan fuertes.<br>
            &ldquo;Hoy estoy demasiado cansada para tener emociones.&rdquo;<br>
            Se cansaron de traslucir el tedio.<br>
            Ambos hubieran jurado<br>
            que guardaban fotos de su amor.<br>
            Ahora hace demasiado que son ellos.</p>
            
            <p>Tienen la certeza de que sólo viven en lo que ocultan.<br>
            Se van secando de intermitencia<br>
            de lo que no son,<br>
            de la escasez de lo oculto.<br>
            (Habitar nuestro engaño merece sus paréntesis.)<br>
            Instantes reídos en el último barro,<br>
            lo poco, lo mío; mis actos de sombra.<br>
            Viven con la certeza insoportable de que no se conocerán.</p>


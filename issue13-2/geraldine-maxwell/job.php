<p>Sitting in a pot of distracted thinking on the train journey to visit sick Rose, Florence thought about Nick's face the night before above her as he moved inside her, only remotely in her. And afterwards his constant mumbling about sex and youth and enlightenment, knowing she wasn't that interested. He wanted them to try Iowaska, a root of magic realism from the Amazon, the only downside being copious vomiting, a small price to pay, he said, for insights of a universal magnitude. But Florence was all too aware that Nick just wanted to have his own insight confirmed. She knew she was noticing too much; how everything about him was yellowing, his skin like parchment, the lines around his eyes deepening like the marks of all his words, etched disappointments.</p>
<p>But something unspeakable might slither into sense, she had said lightly, like that slim black monolith from Kubrick's 2001 spinning in unlimited space. </p>
 <p>Surely even that's limited, the space in your head, he'd said and lit another cigarette and they both laughed but Florence thought again he was just another nail in her bed of nails, that there was something about his momentum of casualness to life that made her uncomfortable, almost envious.</p> 
 <p>"But what  if it were some joy beneath you didn't know you were capable of?  Something physical he said later.  He was stroking his inner thigh as if it were a cat, sipping his white wine, waiting for 'Match of the Day' to come on. "It might help conjure up an orgasm," he said," from some wildly different perspective. Maybe linked to a cellular memory of a relation, like your mother for instance "</p> 
<p>"Like I said, an unspeakable thing. " Florence said and thought of Rose for some reason, and that she would visit after all go and see her the next day. </p>


   <p>As she walked toward the exit in Charing Cross, an aroma of freshly baked bread led her thoughtlessly toward a brown and cream kiosk with 'VintageBake' in curly orange letters above it. She stood there for a moment, scrutinizing each pile of different loaves, farmhouse white, sourdough, a pile of light golden baguettes, before pointing at two dark molten lumps. "Apricots, spices, molasses and walnuts," explained the thin and exhausted Indian woman behind the counter. As she went through the process of buying the bread without knowing quite why, Florence realized she was shamefully afraid of the sight of Rose, her cheekbones surely by now little shark fins, her eyes inky slants of reproach as cancer sucked her in.</p>
<p>And so she over-hugged her oldest friend as soon as she stepped through the apartment door, so that Rose said stop, stop will you, my ludicrous wig will fall off again, but her face shone gratefully enough at Florence who touched the thick dark brown hair curiously and said, "well, it's amazing, and probably meticulously grown on the head of a day-dreaming nun - in fact I think I need one myself, I could pretend to be a new me, I've always wanted it to be that easy haven't you?" and Rose laughed and kissed her saying, "oh you haven't changed and yes, I would love to be a new you too."</p>



<p>Relieved that Rose did not appear quite dead yet, and without thinking, Florence picked up an ornate silver framed wedding photograph on top of a black Steinway piano, that reminded her that Rose had been considered good enough to become a professional pianist, but had got married and muddled instead. And here she was in the photograph, Rose as a young fresh bride, her face split with infectious laughter because a sudden gust of wind had lifted the ancestral lace train startled above her head like a finger from God to his chosen and there next to her was her chosen, Joe, forever caught out in his attempt at a married face and for a few seconds Florence was conscious of how both she and Rose were then; neither enough for him, and not enough for themselves.</p>
<p>Florence said, in a slightly hypnotized tone. as if this had just occurred to her, "Oh, but look at both of you, young and happy, so beautiful and only nineteen with all your lives ahead of you, of us," and she looked at her friend with her eyelids half-mooned across those once-so-loved eyes of even dark blue, and that long still languid face and Florence thought, yes, you are still infinitely beautiful and unachievable, even in sickness.</p>
<p>"Yes, it was such a blustery afternoon," said Rose equally, as if it were nothing that day to her, just something among many, and she took the picture and put it back in its place and took her into the sitting room where they talked about the fairly immediate and the long ago, deftly stepping over the last twenty years during which they had only exchanged Christmas cards. Rose talked about her grown and only child Clover who had dropped out of university though Rose wryly admitted she could hardly judge, having wasted vast swathes of life with a career in two failed marriages. She looked expectantly at Florence who said that at least Rose had a child to complain about at least, her own time taken up by the dogs and her sister who drank too much and might have to go into rehab and of course Nick, although she painted him brighter than he shone. "It's our age Florence," Rose said, "it's the years of diminishing returns; there are bouquets of unused females like us rotting slowly all over the world," and she looked at Florence with her unclear smoker's skin, her lost dark eyes.</p>
<p>"I love your sweater by the way," said Florence though she didn't move to feel the cashmere because of the terrifying thin stick of Rose's arm underneath.</p>
<p>"It's Italian cashmere, a get-well present, feel it."</p>
<p>Florence walked toward the huge sash window, a silhouette against the grey flat light outside, the murmur of the constant rain blotting her voice.</p>
<p>"What?" asked Rose, wondering if it wasn't already time to turn on lights or music or something to tune out Florence's gathering intensity.</p>
<p>Florence turned and in a stringy tone said, "M o n eeee, I said I have hardly any money, nearly all my money has gone."</p>

 <p>Rose was not entirely surprised, she had after all been within close orbit of Florence's rich family since late childhood, hung drawn and fascinated by them; their idle, almost vicious affection for each other that passed for love. Years ago, Rose had managed to convince Florence that her inheritance was compensation and Rose was now all too aware that its absence meant hovering over a fathomless void.</p>
<p>"It's a bit like heroin," she said, "this drip drip drip of easy money, its infantilizing. The trouble is we are  too old to grow up now."</p>
 <p>"What waffle you still talk," laughed Florence, who thought Rose would have hid her pleasure in even the tiniest downfall of the healthy. "So but seriously, have you found God yet? You were always so confidently secular."</p>
<p>Her comment hung in the air like washing. Rose did sometimes in the early hours pray but a half-truth was easier, so she said, "Oh but I'm far more interested in the solar system, physics explained for idiots, Attenborough on insects etc, but I can feel it creeping in the back door when I'm desperate; school chapel, hymns, the Lord's Prayer. But now tell me more about this Nick."</p>
<p>"Oh he could never really be the one," replied Florence looking down, her words falling into some dark water of their own, that Rose knew all too well. "He's probably polyfilla, but I do love him, need him rather. But love was far truer back then, don't you agree?"</p>
<p>"I'm so sorry Florence." Again, she thought, once again.</p>
<p>"What on earth are you sorry about, Rose? Anyway, I need a cigarette, would it be OK to light up if I smoked out of the window?"</p>
<p>Rose wanted to tell her she was smoking herself into a grave. But of course they both knew Florence would be around long after her and probably smoking a cigarette over Rose's grave, before throwing the butt end on top of the coffin along with a handful of earth.</p>
<p>Banished to the freezing street Florence lit up and the heavens opened and poured a sea of rain upon her head. "Forty odd years," she said aloud to the long grey stree in the waning late afternoon, all those years and then a clot of grief, rather like vomit she thought, came up and she stood smoking and crying until calmer, she pressed the intercom to go back up to apartment 12a.</p>
<p>"I'm back, and completely soaked," she called out as cheerily as she could feeling like a caught fish as she stood in the small hallway with its stained cream carpet and old family portraits. She called out again, "so I'm going to dry off, then let's meet in the kitchen for a drink."</p>



<p>The bathroom was bristling white, newly painted with half-burned candles on the side of the bath among the bath oils. She dried her hair on the towel and looked in the mirror and decided she still looked pretty good for fifty-five, before she saw the razor and the shaving foam and the watch beside the bath, the absent-minded and lying watch of Joe. Florence was fifteen again for the full twenty minutes it took to collect herself before she went to the kitchen, where Rose was sitting at the table visibly splintered with distress. Florence opened wine and put down the round heavy loaves from 'Vintagebake' on the table, before sitting close to Rose and putting her arm around her.</p>
<p>"We can have a slice now, and you can have it for toast in the morning. It's not the sort that goes off too quickly, though they're pretty tough to slice."</p>
<p>Rose said, "you know I think it's hard to believe one has been truly loved until the skin of time has dried. Did you know that he and I fell in love as children Florence, at that house your parents rented? He was ten and I was eleven and we walked in the kitchen garden without speaking and I can still remember 'Tears of a Clown' was playing distantly on a radio, and then when we met again we were sixteen and it felt the same, and I forgot myself for years."</p>
<p>"Oh yes," said Florence, "I remember those days vividly, we all played in that barn until it got dark every night that week, but you were only there one afternoon and I spent every day with him for six weeks. Sometimes there were about eight of us kids and all of us under the hay with it in our eyes, everywhere, hiding from the grown-ups Do you think now, Florence, that we are really grown- ups?"</p>
<p>"Not particularly, no," said Florence and they both laughed and held each other quietly and for a long time until finally Rose spoke about the other bookend of her life, about what it was like to know you are dying, to go on appearing cheerful so as not to embarrass anyone with the delicate stench of despair, of trying to get to sleep at night while wrestling for hope. Eventually they both agreed she must rest now, another friend was arriving that Florence didn't care much for, and so began the slow exit, calling a cab, chatting about nothings.</p>
<p>In the medicinal blackness of the taxi, Florence felt the stain of all that had disappeared begin to take hold, and so to take her mind off her loss she thought of other things. Her mind followed her eyes instead, out to the streets of people walking somewhere in the dwindling dusk and she imagined the knots of their lives, and disappearance  of the honey bee and what that would mean one day to the world because of the lack of its brushing gently the tiny sex of a flower and she thought almost warmly then of Nick sitting in her home on the computer, not thinking about her, under the vastness of the night sky.</p>













           

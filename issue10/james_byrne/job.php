          <td colspan="4" rowspan="2" valign="top" class="texto2">  
            <br><p><strong>Pastoral</strong></p>
            <p>Blown dust the  days since I was called<br>
              from the  rabbit-hole of childhood. By ear <br>
              history is  stage-set, it recreates via rains<br>
              playing  Schubert or untaken photographs.&nbsp; </p>
            <p>They said I  came out with a thorn in my foot&mdash; <br>
              hillcloud  child who spoke with a large name,&nbsp; <br>
              blossy among  broken hedges and molten fields.</p>
            <p>When the house  hellbelled I retouched an image <br>
              of hyaline  mists gridlocked to corn. The memory&nbsp; &nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <br>
              of sky-sagas  at Pankridge Farm held like a salve. </p>
            <p>Artifice to my  own laziness, cold at the leash,&nbsp; <br>
              I listened to  the quiet motherese of a voice <br>
              until it was  clamant, exasperated to nerve&mdash;<br>
  &lsquo;Home&rsquo; it  repeated. &lsquo;Home. Come home&rsquo;.</p>
            <p>&nbsp;</p>
            <p><strong>Four Interpretations of Photographs by Claude  Cahun</strong></p>
            <p><em>30o </em>(<em>Hand Trio)</em></p>
            <p>The human hand gleams and imposes itself <br>
              cannily, like the white glove of SS Wolff. </p>
            <p>If such configurations could rejoin the living, <br>
              Goebbels would be at the flashbulb, mad as a yapping seadog.&nbsp;&nbsp; </p>
            <p>The black hand belonged to a spying washmaid in Nantes. <br>
              Any half-trained eye can see the curlicues.</p>
            <p>Unbeknown to her&mdash;a '30s Telphousa&mdash; <br>
              she is grinding the wheelmill of war.&nbsp;&nbsp; </p>
            <p>The baby hand is puppet-like and cut from the wrist. <br>
              Its cold digits are sized to the tip of an icicle.<br>
  &nbsp;<br>
              Through the fingers of the child, darkness merges, entwists. <br>
              A blousy tablecloth rivers into ice, bloats to a hold, solid. </p>
            <p><em>&nbsp;</em></p>
            
            <p><em>26o (Military Tailors)</em></p>
            <p>Crocodilian wars where a sacred bull glints the shadow-stamp <br>
              of an 8-ball on his own forehead. </p>
            <p>On conscripting, I was fitted up here. <br>
              Samuel &amp; Finch. Haymarket. A three-piece. 1912.</p>
            <p>It was the suit I pillowed my face into <br>
              fresh from Loos&nbsp;&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;crying: </p>
            <p>God is not dead, <br>
              God wears the face of my enemy sleeping.&nbsp; </p>
            <p>Entrenched&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;  proceeding from dark to dark, <br>
              the early moon would watch back</p>
            <p>doomy, unflinchingly alert,&nbsp;&nbsp;&nbsp; <br>
              like the skull of a pterodactyl. </p>
            <p>&nbsp;</p>
            
            <p><em>44f (Self-Portrait Among  Masks and a Crystal Skull)</em></p>
            <p>i. Centremask&mdash;<br>
              Mutant Japanese. The disconnected neck round as the face of a  Bodhr&aacute;n. </p>
            <p>ii. Far Left Centremask&mdash;<br>
              Baselump of Marx&rsquo;s tomb. Blank eyeholes, the dunce-stare of an age  wasted. </p>
            <p>iii. Upper Left Crystal Skull&mdash;<br>
              One thirteenth of world exposed. The ghost-luck beauty fidgets in its  glass. </p>
            <p>iv. Self-Portrait&mdash; <br>
              Skeletal hermit, invisible until looked for. Wind-weary with a  Gestapo sneer.</p>
            <p>v. Right Centremask&mdash;<br>
              Lunatic humiliated by ancestry. Hair pierced straight. Fraying for  lack of evidence.
  &nbsp;</p>
            <p><em>&nbsp;</em></p>
           
            <p><em>35z (Flowerhead)</em></p>
            <p>The seed of it  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Hindu/Parisian &ndash; &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </p>
            <p>not the blurry  portico <br>
              but desire in  the bangles of a bronze baroness, <br>
              a three-jet of  pigeons&mdash; </p>
            <p>the  nine-blossoms of a head-flower <br>
              petrified,  erect. </p>
            <p>Scabbed wrists  offer possibilities of self-portrait: </p>
            <p>a young-un  spaced clear, genderless <br>
              but for the  touch of a nursery mother. </p>
            <p>This is your  mother. <br>
              Un-witched.  Dregs of you.</p>
            <p>&nbsp;</p>
            <p><strong>Chess in Kirkuk</strong><br>
            <em>for  Fadhil Al-Azzawi</em></p>
            <p>&lsquo;Two men  tossing a coin, one keeping a castle&rsquo;<br>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Ezra Pound, &lsquo;Provincia Deserta&rsquo;</p>
            <p>&nbsp;</p>
            <p>In the Caf&eacute;  Atlas, you furlonged over countless games of chess <br>
              with a  signature move: checkmate, X&rsquo;ings of bishop and queen.<br>
              Victorious, to  be prized as a prisoner, while those you conquered<br>
              were laurelled  with limousines and castles, the new ministers <br>
              of the  slaughterhouse had you wearing old shoes from the dead <br>
              who had no  time to put on their shoes when the guards burst in.</p>
            <p>A thirty-year  exile, honing your skill as chessmaster and prophet. <br>
              In Leipzig,  you saw Saddam bare his polished teeth like Dracula.<br>
              In Paris, a  mirror returned the blooded forehead of Al-Mutanabbi.&nbsp; <br>
              You saw Iraq&rsquo;s  future torturers flogging their backs for bread<br>
              and wished  them a square meal and a hex against the atom bomb.&nbsp; </p>
            <p>Arriving into  giant shafts of sunlight along the Turkish strait,<br>
              you remembered  those who traded their poetry for propaganda;<br>
              not as  turncoats; but simply chessmates flexing pawns for victory.&nbsp;&nbsp;&nbsp; </p>

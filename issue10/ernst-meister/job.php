           &nbsp;<br>
              <p>Ernst Meister,  according to poet and translator Michael Hamburger, wrote poetry that was  &ldquo;among the best German poetry&rdquo; written in the 20 years after the Second World War.  His was a poetry that, as he often said, arose out of nothing, the nothing that  exists between words, the space between a word and a concept, the silence where  language ceases to speak.</p>
             <p> &nbsp;&nbsp;&nbsp;&nbsp; Meister was born in 1911 in Haspe, near Hagen in Germany.  He studied theology, philosophy and German, and published his first volume of  poetry in 1932. During the war, he served in France  and Italy, returning to his studies and to work in the family factory when  it was over. He published no poems between 1932 and 1954, but, as he began to  publish again, he also began to win literary prizes. Besides being a poet, Meister  was also a painter whose work was shown in many exhibitions around this time.  He died in 1979.</p>

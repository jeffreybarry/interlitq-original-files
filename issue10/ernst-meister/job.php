            <p>&nbsp;&nbsp;&nbsp;&nbsp; His poetry is often regarded as  philosophical, and critics have sometimes said they could not understand it.  But it is not in fact difficult poetry or, if there is difficulty in it, it  lies in the fact that it demands a great deal of work on the part of the  reader. Full of religious imagery, his poetry plays with words and explores the  way ambiguities in language give rise to alternative trains of thought.</p>
  <p>&nbsp;&nbsp;&nbsp;&nbsp; The three translations here are from my 2003  book <em>Between Nothing and Nothing </em>(Arc  Publications), and I have chosen them because they illustrate the  characteristic challenges of translating Meister fairly well. The first is  typical of the succinct reflection on a natural phenomenon, which leads to a  comparison with his own state and is a poem in which, as in many of Meister&rsquo;s  poems, layout is crucial. The second poem contains the line from which the  title of the English collection is taken, and is, perhaps, an unusually  optimistic poem, but typical of the incantatory repetition Meister often  favoured. In the third poem a typical Meister word &ldquo;aufgehoben&rdquo; (which means  both &ldquo;undone, crossed-out&rdquo; and &ldquo;kept, preserved&rdquo;) has no equivalent in English,  and so in this version the line-layout is called into service in a way the poet  uses elsewhere to assist in creating the ambiguity: here it is the ambiguity between  &ldquo;is and is not&rdquo; and &ldquo;is and is not held&rdquo;.</p>
  <p>&nbsp;&nbsp;&nbsp;&nbsp; Meister&rsquo;s poetry is both serious and  playful. He deals with the big issues of life, love, and death, but he delights  in catching the reader out. A particular trick was to hide his name, in  anagrams, in his poems. His poetry is characterised, more than anything, by  compression: he is sparing with language, leaving the greatest possible space  for poetic effects on the mind of his reader.</p>

<P><b>Snow</b></P>
<p>Snow falls<br>
  the world turns white</p>
<p>In the sun<br>
  that white glitters<br>
  in every colour</p>
<p>White stars<br>
  blossom in the air</p>
<p>On the horizon<br>
  beyond the mountains<br>
  look: Snow White<br>
  and the Seven Dwarves</p>
<p>At night<br>
  the white is black<br>
  black as the dark queen<br>
  beyond the mountains.</p>
    <p>&nbsp;</p>

  <strong>Schnee</strong>
  </p>
  <p>Schnee f&auml;llt<br>
  die Welt wird wei&beta; </p>
<p>In der Sonne<br>
  glitzert das Wei&beta; <br>
  in allen Farben</p>
<p>Wei&beta;e Sterne<br>
  bl&uuml;hn in der Luft</p>
<p>Am Horizont<br>
  hinter den Bergen<br>
  sieh  Schneewittchen<br>
  und die sieben  Zwerge</p>
<p>Nachts<br>
  ist das Wei&beta; schwarz<br>
  wie die finstere  K&ouml;nigin<br>
  hinter den Bergen</p>
<p>&nbsp;</p>
<p><strong>The Carnival Over</strong></p>
<p>The carnival  over, the lean times came<br>
  the days of mouldy bread and bitter roots<br>
  I hungered for the flesh of figs<br>
  I thirsted for oranges</p>
<p>I joined a  caravan and crossed<br>
  the desert on a date hunt<br>
  Sand stuck in my throat<br>
  A camel&rsquo;s back<br>
  became my home<br>
  The hours like ovens round my head<br>
  the constellations Scorpio and Crucifix</p>
<p>At daybreak the  horizon grew red<br>
  with a mirage which would not come closer<br>
  Only one oasis gave us shelter<br>
  its water smelt of poppies moon and fire<br>
  its dates and figs had shrivelled up</p>
   <p>&nbsp;</p>

  <p><strong>Nach Dem Karneval</strong></p>
<p>Nach dem Karneval kamen die Magertage<br>
  mit Schimmelbrot  und Bitterkraut<br>
  Mich hungerte  nach Feigenfleisch<br>
  mich d&uuml;rstete  nach Apfelsinen</p>
<p>Mit einer Karawane ging ich<br>
  durch die W&uuml;ste  auf Datteljagd<br>
  Der Sand stak mir  im Hals<br>
  Der R&uuml;cken des  Kamels<br>
  war meine Heimat<br>
  Die Stunden waren  &Ouml;fen um die Stirn<br>
  die Sterngebilde  Kreuz und Skorpion</p>
<p>Am Morgen bl&uuml;hte rot am Horizont<br>
  die Fata Morgana  die nicht n&auml;herkam<br>
  Nur einmal nahm  uns eine Oase auf<br>
  das Wasser roch  nach Feuer Mohn und Mond<br>
  Feigen und  Datteln waren verdorrt</p>
  <p>&nbsp;</p>
<p><strong>A Day In Exile</strong></p>
<p>A day in exile<br>
  a house without doors or windows</p>
<p>Time drawn on a  tablet<br>
  charcoal<br>
  on white</p>
<p>In a chest<br>
  the mortal masks<br>
  Adam<br>
  Abraham<br>
  Ahasuerus<br>
  Who knows all the names</p>
<p>A day in exile<br>
  when the hours stoop<br>
  to climb out of the cellar<br>
  and into the room</p>
<p>Shadows  gathered<br>
  round the oil-lamp&rsquo;s eternal flame<br>
  tell their stories<br>
  along the walls<br>
  with ten dark  fingers</p>
  <p>&nbsp;</p>

<p><strong>Ein Tag Im Exil</strong></p>
<p>Ein Tag im Exil<br>
  Haus ohne T&uuml;ren  und Fenster</p>
<p>Auf wei&beta;er Tafel<br>
  mit Kohle  verzeichnet<br>
  die Zeit</p>
<p>Im Kasten<br>
  die sterblichen  Masken<br>
  Adam<br>
  Abraham<br>
  Ahasver<br>
  Wer kennt alle  Namen</p>
<p>Ein Tag im Exil<br>
  wo die Stunden  sich b&uuml;cken<br>
  um aus dem Keller<br>
  ins Zimmer zu  kommen</p>
<p>Schatten versammelt<br>
  um&rsquo;s &Ouml;llicht im  ewigen L&auml;mpchen<br>
  erz&auml;hlen ihre  Geschichten<br>
  mit zehn finstern  Fingern<br>
  die W&auml;nde entlang </p>
<p>&nbsp;</p>
<p><strong>When I Have  Gone</strong></p>
<p>When I have  gone<br>
  the sun might burn still</p>
<p>The planets  still move<br>
  to their own laws<br>
  round a centre<br>
  no-one knows</p>
<p>The lilac still  smell<br>
  as sweet<br>
  the snow send out its white rays</p>
<p>When I go away<br>
  from our forgetful earth<br>
  will you speak<br>
  my words<br>
  a while for me?</p>
  <p>&nbsp;</p>
<p><strong>Wenn Ich Vergehe</strong></p>
<p>Wenn ich vergehe<br>
  wird die Sonne  weiter brennen</p>
<p>Die Weltk&ouml;rper werden sich<br>
  bewegen nach  ihren Gesetzen<br>
  um einen  Mittelpunkt<br>
  den keiner kennt</p>
<p>S&uuml;&beta; duften wird immer<br>
  der Flieder<br>
  wei&beta;e Blitze  ausstrahlen der Schnee</p>
<p>Wenn ich fortgehe<br>
  von unsrer verge&beta;lichen Erde<br>
  wirst du mein  Wort<br>
  ein Weilchen<br>
  f&uuml;r mich  sprechen?</p><P></td>

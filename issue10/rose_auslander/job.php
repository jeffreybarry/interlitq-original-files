            <p>Rose Ausl&auml;nder&rsquo;s life  was one of almost constant movement, change and turmoil. Her childhood,  however, showed little indication of the upheavals to come. Born Rosalie Scherzer  in 1901 in Czernowitz, then part of Austria-Hungary, she grew up speaking German, and also Hebrew, in a  closely-knit Jewish family. But during the First World War Czernowitz was  occupied by Romania, and she fled with her family to Vienna, then Budapest. Returning after the war, Rose studied Philosophy and  Literature at Czernowitz University, but emigrated to America with a friend, Igaz Ausl&auml;nder, in 1921, after her  father&rsquo;s death. They married two years later, and she began to publish poems in  anthologies. But the marriage did not last and in 1931, after the couple had  divorced, Rose returned to Czernowitz to look after her mother. A first  collection, <em>Der Regenbogen</em> (The  Rainbow), appeared in 1939 but, though it received good reviews, it was impossible  at that time for a Jewish author to be accepted. She tried living in New York but returned very soon to Czernowitz and her family.  In 1941 Czernowitz was occupied by Nazi troops and she and her mother, unable  to escape from the Jewish ghetto, spent most of the next year in hiding. It was  at this time that she first met Paul Celan. Czernowitz was liberated by the  Russians in 1944, and for the next 3 years Rose Ausl&auml;nder had a comparatively  stable life until, in 1946, when Czernowitz became part of the Ukraine, she again left for New York, leaving behind her mother, whose health by this time  was very fragile. In New York she tried to obtain a visa so her mother could join  her, but suffered a breakdown when her mother died in 1947. For the next few  years, Rose Ausl&auml;nder wrote only in English but by the late 1950&rsquo;s, having  returned to Europe several times and renewed her friendship with Celan,  she was writing again in German. <em>Blinder  Sommer</em> (Blind Summer) was published in Vienna in 1965 and was followed by <em>36 Gerechte</em> (36 of the Just) in 1967, <em>Ohne Visum</em> (Without a Visa) in 1974 and <em>Andere Zeichen</em> (Other Signs) in 1975. In 1965 she returned  permanently to Europe, settling in D&uuml;sseldorf, in Germany. She received several literary prizes, and a great  deal of critical acclaim. After 1981, having become very frail, she wrote no  more poetry. She died in the Nelly Sachs Home for the Jewish Elderly in  D&uuml;sseldorf in 1988.</p>
            <p>Her poetry is full  of images from the ghetto years &ndash; of poverty, hunger, coffins, blood, ashes and  smoke. It is, not surprisingly, often about identity, language, family ties, or  Jewish belief. Her later poetry is less strictly formal, more colloquial in  tone, and tends towards slant rhyme, rather than full rhyme, and towards  syntactic pattern rather than regular metre.</p>
            <p>The book from  which these translations are taken was first published in a monolingual version  in 1995 by Arc Publications, Todmorden. A revised bilingual edition is planned  for 2010.</p>

           <p>&nbsp;</p>
              <p>The distances from one star to another<br>
are great, the empty spaces cold and deep.</p>
              <p>The light the primal stars arrange, is old<br>
                belated news, unreadable though bright.</p>
              <p>One thinks of furthest distance, and no  further;<br>
                resigns the ancient secret those lights  keep.</p>
              <p>There is no knowing them. They slip our hold<br>
                to leave us deeper stranded in a night</p>
              <p>whose overwhelming darkness is our cover.<br>
                Numberless about the pole they creep,</p>
              <p>enough to sprinkle the darkness, not to  change<br>
                its intractable dark to light.</p>
              <p>The covering darkness is for us a brother<br>
                to light itself, and is the friend of sleep,</p>
              <p>for pure light would estrange<br>
                itself from the tender limit of our sight.</p>
              <p>Mystics, heard to murmur <em>lux est umbra</em><br>
                <em>Dei</em> &nbsp;(&lsquo;light is the shadow of God&rsquo;),</p>
              <p>loved the simple beauty of their thought,<br>
                were comforted by it, and died in peace.</p>
              <p>A move from deepest shadow to penumbra<br>
                too much to contemplate for awed</p>
              <p>eyes unfit to see the truth they sought,<br>
                the symmetry of figure brought release.</p>
              <p>And some believe the stars may spell<br>
                our destiny because of when we&rsquo;re born,</p>
              <p>that in the houses of the zodiac<br>
                is where our secret story really lives;</p>
              <p>as if that cool indifference could foretell<br>
                the path of wandering souls, adorn</p>
              <p>our domicile, and bring us back<br>
                the kind of peace homecoming gives.</p>
              <p>Hesiod sang of the fourth, heroic age<br>
                between the bronze and this iron age of  ours.</p>
              <p>Its men lived on a bigger scale<br>
                than us in all respects. They fought at Troy</p>
              <p>or Thebes, destroying  armies in their rage,<br>
                no task beyond their godly powers.</p>
              <p>They died as all men do but did not fail<br>
                to storm the dark blue city of the sky</p>
              <p>and there they live and glitter, with their  bow <br>
                or hound at heel, deathlessly bright</p>
              <p>in triumph over all vicissitudes,<br>
                moving in dignity across the vault.</p>
              <p>Looking out now, we know it wasn&rsquo;t so.<br>
                The stories that gave life to them took  flight</p>
              <p>millennia since. Their stellar attitudes<br>
                overawe no more. It&rsquo;s not our fault</p>
              <p>we outgrew stardom of that kind and found<br>
                ourselves uncelebrated and alone,</p>
              <p>unseen by those watchers in the dark.<br>
                No longer made a sacrifice of beasts,</p>
              <p>they lost the fascination to astound<br>
                childish hearts. Pared now to the bone,</p>
              <p>knowing the space we mark<br>
                in parsecs for inconceivable wastes</p>
              <p>vacant and inhuman, we send craft<br>
                to dig about on planets and their moons</p>
              <p>looking for the ice that might suggest<br>
                something beside ourselves had grown</p>
              <p>in sand to which some passing shaft<br>
                of light has reached, latent among the  dunes</p>
              <p>of the frozen night, in the dust.<br>
                We sort the data from whatever drone</p>
              <p>we sent to bring us news; and think<br>
                of going there. It seems the thing to do.</p>
              <p>For most of us there&rsquo;ll be no voyage out<br>
                to populate or even to explore</p>
              <p>whatever eddies in that black-as-ink<br>
                pool of ignorance we thought we knew</p>
              <p>and sounded ages past. This dream about<br>
                some other life, our current lore,</p>
              <p>may go the way of heroes, of horoscopes<br>
                cast as though to comfort in the form</p>
              <p>of dwellings that have known us where we  live.<br>
                All that aside, used now to the loss</p>
              <p>of archaic fantasy and hopes,<br>
                we gaze out at what cannot aid or harm,</p>
              <p>and wonder what it has to give,<br>
                since we no longer fear the stars that cross</p>
              <p>unhappy lovers. <br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; And still those  points dissemble<br>
                effortlessly, as him and her,</p>
              <p>their creatures of a night&rsquo;s tableaux,<br>
                with chairs and tables, bodies and bright  faces.</p>
              <p>Things in no relation still resemble<br>
                objects known, their gift of metaphor</p>
              <p>plentiful, as figures come and go<br>
                in glyphs across the poetic spaces.</p>

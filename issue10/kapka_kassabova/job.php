            <br><p><strong>Lovers  of the Arctic Circle</strong><br>
              <em>After  the eponymous film by Julio Medem</em>            </p>
            <p>I can see it, I can see exactly <br>
              how it will unfold. <br>
              I see us falling through cold <br>
              cloud, water, ice, <br>
              falling like stunned birds <br>
              shot by lightning. <br>
              Our story is already told<br>
              but hasn&rsquo;t yet begun &ndash; <br>
              so I start running, I run <br>
              past my other lives, <br>
              where all the safer choices lie, <br>
              where people that I haven&rsquo;t met<br>
              are signaling with smiles,<br>
              and then they are a blur, <br>
              I run so our story doesn&rsquo;t die.<br>
              I have no time to stop, I&rsquo;m in a wild <br>
              hurry, I&rsquo;m in a dreadful hurry, <br>
              running to the silent white <br>
              where you are high <br>
              up and bent like Atlas <br>
              under darkened skies.</p>
              <p>&nbsp;</p>
<P><strong>The  problem with perfection</strong>
<p>We reach perfection in the garden<br>
  in the slow night between seasons<br>
  by loving silently among carnations.<br>
  I&rsquo;ll never be happier, and I am crying.</p>
<p>This too will change, he says as consolation.<br>
  But it sounds like treason.<br>
  It will get more perfect yet, he whispers.<br>
  He is a gardener and knows such things.</p>
<p>But gardeners know too about dying.<br>
  No, I say, we stay like this or lose all.&nbsp;<br>
  I am a poet, I know nothing of reason,<br>
  I am not wise.&nbsp;I am not consoled.&nbsp;</p>
<p>When tomorrow we rise,<br>
  I will never forgive the seasons.<br>
  I will never enter a garden again.<br>
  This is the problem with perfection.</p>
<p>&nbsp;</p>
<P><strong>Urbina  Station, 3650 m</strong>  
  <p>Welcome to the world&rsquo;s highest station, <br>
    our guide says. This is our last  destination. </p>
  <p>Observe the vapours that arise<br>
    from the snow-peaked elevation <br>
    of Chimborazo, twenty thousand and seven hundred feet. <br>
    Yes, breathing is like eating ice.<br>
    The locals stand like totems in the  sleet. </p>
  <p>This is the terminus for those who dare. <br>
    I&rsquo;m local, I don&rsquo;t really care. </p>
  <p>You feel lost, you sway <br>
    like sickening grass in thin air? <br>
    You paid good money to be lost. <br>
    And now I get to leave, you get to  stay.&nbsp; <br>
    No one will look for you. Adios. </p>
<p>You&rsquo;ll figure out your reasons &ndash; <br>
  the train won&rsquo;t come until next  season.&nbsp; </p>
  <p>&nbsp;</p>
<p><strong>Sometimes we lie </strong></p>
Sometimes we lie in beds big<br>
as houses and safe like mothers. <br>
But not tonight. Tonight, <br>
we can&rsquo;t console each other. </p>
<p>The bed is too small, the night<br>
too long, our happiness too brief,<br>
and under the red eye of the street light <br>
I cry in the snow with old tears, </p>
<p>not because you are gone,<br>
but because you were here,<br>
because we&rsquo;re lost children of long ago &ndash; <br>
what we do, we do out of grief.&nbsp; </p>
<p>&nbsp;</p>
<p><strong>Nobody  will pray for their souls</strong></p>
<p>One Sunday afternoon<br>
  there was a scorched white town<br>
  like a high-pitched scream<br>
  and all shutters were down,<br>
  and the Catholics in the church <br>
  were dreaming bad dreams. </p>
<p>On one side of the town<br>
  was the Lake of Blood where those before <br>
  the Incas had been gored. <br>
  A park for trafficked animals <br>
  was on the far-off shore.&nbsp;&nbsp; <br>
  That, and ashes in the throat. </p>
<p>Two travellers whose names nobody knew<br>
  or where they came from, <br>
  appeared in the town,<br>
  their faces were browned,<br>
  they were a bit lost, just so<br>
  as not to know that love </p>
<p>is shorter than life, that a night<br>
  in this town of long ago <br>
  is their happiest for some time, <br>
  that when tomorrow they are gone,<br>
  their backpacks dark and tall<br>
  against the sun like gravestones,</p>
<p>nobody will pray for their souls. </p>
<p>&nbsp;</p>
<p><strong>Valentine&rsquo;s  Day at the Inca Ruins</strong></p>
<p>No, I am not sight-seeing. <br>
  I&rsquo;m here by mistake, it&rsquo;s February <br>
  and the middle of my life, and I am  looking<br>
  at a broken railway across the skies. <br>
  And all I know is that it wasn&rsquo;t meant  this way, <br>
  the railway was built with sweat, and  people died<br>
  and we don&rsquo;t know their names.&nbsp; <br>
  Please, I say to the stones the Incas  left above the rails - <br>
  no mortar used, the guide explains, <br>
  his voice sounds far away, <br>
  and then he too disappears &ndash; please, <br>
  there has been a mistake. <br>
  I haven&rsquo;t been touched in months, days. <br>
  Each night, I grow old with disuse.<br>
  And now a cold, mean rain begins to fall <br>
  over the five of us &ndash; the shrinking  guide, <br>
  a dog with broken legs,<br>
  a teenaged couple, though I know <br>
  in two years they&rsquo;ll be over, <br>
  and in another hundred nothing <br>
  will be left of us, nothing,<br>
  not even stones or rail tracks, <br>
  and the rain will blow across the plains<br>
  one February night, please, I say to the  Incas <br>
  whose faces were like clay, who lasted  sixty years - <br>
  please, if no one touches me today,<br>
  who will know that we were here. </p></td>

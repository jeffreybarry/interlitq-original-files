            <p><b>Woman at a Window</b></p>
            <p>I see her as a  portrait: <br>
              caught behind  liquid transparency; <br>
              framed;  tear-eyed; her pale face <br>
              distilling  weak light. </p>
            <p>As if  suspended, <br>
              she stands at  her window for hours. <br>
              The  disintegration into sobbing; <br>
              the  lip-trembling; <br>
              the glassiness  of the stare: <br>
              these draw me  to spy <br>
              (no, too harsh  a word!) to study her, <br>
              even though I  wish it weren&rsquo;t so. </p>
            <p>If I could  erase the distance between us, <br>
              we might  converse about death, <br>
              violence, a  star-crossed affair <br>
              or  whateveritis <br>
              that&rsquo;s causing  her to cry. </p>
            <p>And if I could  tell her that we&rsquo;re alike, <br>
              our worlds  equally askew, <br>
              I might see  reflected back at me <br>
              someone other  than myself. </p>
<p>&nbsp;</p>
          <p><b>Borrowing Anne Sexton&rsquo;s Attire</b></p>
          <p>I&rsquo;m thinking  of a black dress.</p>
          <p>I&rsquo;m thinking  of nakedness.</p>
          <p>I&rsquo;m thinking  of becoming an Anne Sexton wannabe who, like a white-trash Marilyn<br>
           clone,  reminds the world of how dead skin is shed as ruthlessly as a poet discarding<br>
             words for the sake of the rhyme.</p>
          <p>I&rsquo;m thinking  of a song, of <em>The Ballad of the Lonely  Masturbator</em>. Serenading myself till<br> twilight, I drink my whisky neat. </p>
          <p>I&rsquo;m thinking  of feet and metre, of dactyls and trochaics, of a villanelle my friend Sylvia<br>  stole, the lament we shared. </p>
          <p>I&rsquo;m thinking  of a woman&rsquo;s voice, of bitterness callused by bad choices, bad marriages and<br>  bad men.</p>
          <p>I&rsquo;m thinking  of a tour, of readings where I wear a red gown and nothing underneath. My<br>  cigarette half-cocked, I listen to an inner voice, tender as a girl&rsquo;s,  persuading me to soften<br> my delivery.</p>
          <p>I&rsquo;m thinking  of the children I never bore.</p>
          <p>I&rsquo;m thinking  of letting go. Not an oven, but a closed garage and a car.</p>
          <p>I&rsquo;m thinking  of the other side, of the paradise of having years trickle from my bones until<br>  old age has drained me, a borehole drawing off the sap. </p>
          <p>I&rsquo;m thinking  of a Massachusetts Pine. Return me to it; bury me amongst its roots.</p>
          <p>I&rsquo;m thinking  of a suit, skin-tight as a coffin or a collected works. <em>The Complete Poems</em>: an<br> epitaph. Arriving home. No escape; the end. </p>
          <p>&nbsp;</p>
          <p><b>Cactus</b></p>
          <p>No snags, I  promise, <br>
            simply a gift  so that we might part smoothly, <br>
            our points of  difference buried. </p>
          <p>The distance  between two spikes <br>
            might once  have measured our connection, <br>
            an infinite  world of tenderness there. <br>
            But now, it&rsquo;s  enough <br>
            that each  sharp star speaks of something long gone. </p>
          <p>Perhaps you&rsquo;ll  take this cactus with you; <br>
            grow into it  from afar, <br>
            rooted by it  wherever you go: <br>
            Ulluru, Rio, Arizona. </p>
          <p>Perhaps you&rsquo;ll  leave it behind, <br>
            allow it to  stand in place of you, <br>
            holding your  memory <br>
            in the same  way a picture might, <br>
            or a closed  book on a shelf in your room: <br>
            a silent,  internal way, <br>
            the way of the  animate, <br>
            the  wrist-watch, the ghost. </p>
          <p>I&rsquo;ll tolerate  either choice, <br>
            for some  winter&rsquo;s morning <br>
            you&rsquo;ll fly  home to darkness <br>
            and you&rsquo;ll  know that you&rsquo;re alive, <br>
            because you&rsquo;ll  stroke this cactus, <br>
            recalling the  moment it was offered, <br>
            and it will  prick you, <br>
            and <em>it</em> will hurt. </p>
            <p>&nbsp;</p>
          <p><b>Black cormorant</b></p>
          <p>Everywhere, a bird  sees suggestions.</p>
          <p>That bruise  upon the headland<br>
            is a stray  magpie prowling<br>
            through  chechian grass.</p>
          <p>That finger of  sand<br>
            is a beach  parted<br>
            by the  movement of water. </p>
          <p>Those flashes  of lightning<br>
            are the sun shining<br>
            upon the  retreating tide. </p>
          <p>That vision  across the harbour<br>
            is a city hidden  beneath<br>
            a blanket of  mist.</p>
          <p>And that ghost  disappearing&nbsp;&nbsp; <br>
            into the sea is  a cormorant<br>
            pretending he  isn&rsquo;t here.</p>

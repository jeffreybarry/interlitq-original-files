&nbsp;<br>
            <p>A note from Charles
Bernstein: I wrote this new translation of Velimir
Khelbnikov&rsquo;s &ldquo;Incantation by Laughter&rdquo; to
celebrate the centennial of the poem. &ldquo;Incantation by
Laughter&rdquo; is the iconic poem of <em>zaum</em>
or trans-sense &ndash; pervasively neologistic &ndash; poetry. <em>Zaum</em>
is not a poetry of opacity, as with much later sound/ideolectical
poetry. Khlebnikov believed <em>zaum</em>
would cross the barrier of national languages, as a sort of
proto-Esperanto. The Russian Futurians remain for me the most inspired
and inspiring group of artists and poets of the modernist period. <a
 href="http://writing.upenn.edu/pennsound/x/Explodity.php" class="link1">PennSound
has a performance of the
Khlebnikov poem</a> </p>
            <p>&nbsp;</p>
            <p><b>Incantation of Laughter</b></p>
            <p>We laugh with our
laughter<br>
loke laffer un loafer<br>
sloaf lafker int leffer<br>
lopp lapter und loofer<br>
loopse lapper ung lasler<br>
pleap loper ech lipler<br>
bloop uffer unk oddurk<br>
floop flaffer ep flubber<br>
fult lickles eng tlickers<br>
ac laushing ag lauffing uk<br>
luffing ip luppling uc<br>
lippling ga sprickling<br>
urp laughter oop laughing<br>
oop laughing urp laughter</p>

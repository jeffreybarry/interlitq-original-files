            <br><p><strong>&lsquo;Can You Describe This?&rsquo;</strong></p>
            <p>Take this  down. Your future is a clutch of wonky  ruins &ndash; <br>
              tigers prowl  through high grasses in Tiananmen Square,<br>
              the Eiffel Tower&rsquo;s scar-coloured now, clotted with  nests, <br>
              and speckled  deer falter down Fifth Avenue.  Every dam&rsquo;s <br>
              fallen to silt  and spilt &ndash; dolphins swish past chandeliers &ndash; <br>
              and farmland,  loosed from man&rsquo;s shoulder, is feral <br>
              with plumed  thistle, mustard, lupine, flowering rape. <br>
              Guano  plasters the Sistine Chapel. Libraries  burn.</p>
            <p>You imagine  this beautiful, I know. Nearly want it. <br>
              But beauty&rsquo;s  gone: wolves sniff Rodin&rsquo;s <em>The Kiss</em>, <br>
              gnaw it and  leave. Love was your own disease,<br>
              and died,  as head-lice died, along with you. <br>
              This is not  justice.&nbsp; Lone and level sands don&rsquo;t  mock.&nbsp;&nbsp; <br>
              There is only  the boundless indifference you failed to see <br>
          when you  were here to see, recording angels.&nbsp; </p>            <p>&nbsp;</p>
          <p><strong>The Language of Flowers, <em>or</em> The Primrose</strong></p>
          <p>You know so  many words for shoe -<br>
            know mules  from brogues, <br>
            your  ballet-slippers, sandals, platforms, <br>
            courts, stilettos, <br>
            wedges,  flip-flops, kitten-heels <br>
            and mary-janes  and slingbacks and the Ugg.</p>
          <p>Once you pressed  flowers, so learnt their names - <br>
            wrote them  by bowed and jaundiced heads -<br>
            but then  the siren-song of cities cooed from your TV<br>
            and you  preferred that Esperanto of want and need:<br>
            Selfridges,  mojitos, latte, weed, <br>
            hoodies and  nightbus, <br>
            Banksy, chorizo, <br>
            Noho, lido and  burlesque show&hellip;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </p>
          <p>and now the  names are lost,<br>
            and now you  need those blossoms back. <br>
            Without the  right words, you can&rsquo;t think clearly.<br>
            Whoever  cared for what&rsquo;s nameless?</p>
          <p>You must  learn what the Stinking Iris is,<br>
            the  Harebell, Angel Bonnet, Mother-die; </p>
          <p>that<em> Populus Tremula</em> means trembling Poplar;<br>
            means the Aspen whispering to us <br>
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&ndash; listen -<br>
          through  leaves like teeth, chattering with fear.</p>
          <p>&nbsp;</p>
          <p><strong>Geraniums</strong></p>
          <p>The thing I  fear most is diminishment,<br>
            the  shutting down of possibility.&nbsp; <br>
            Last night  I was greedy for what could still be:<br>
  <em>I want go to </em><em>Rio</em><em>,</em> I slurred. <em>I  wanna keep chickens! Try mescaline!</em></p>
          <p>But tender  mornings are about small things: <br>
            light tortoise-shelling  the flat, a sip of juice, a DVD. <br>
            I&rsquo;ve a fat  stack of weekend papers<br>
            and my  husband is whistling Spanish Flea.</p>
          <p>On the  windowsill, geraniums pulse. <br>
            They are  sunsets on stalks, grenadine and coral.<br>
            I failed to  water them. Fallen petals<br>
          are saying: <em>you must love this.</em></p><P></td>

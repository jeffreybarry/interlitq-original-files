            <p><b>Oh Mother, My Mother!</b></p>
            <p> This DNA of yours you left as trace within a Cell<br>
              Of this life that it was always there&nbsp;<br>
              After that nine months<br>
              After that toddling<br>
              After that quenching of thirst and hunger<br>
              After that growth!<br>
              And that you wanted me to be always&nbsp;<br>
              Within you and me always away from you!<br>
              <br>
              As I marched passed the lives of life<br>
              To grow old to measure up my own being grown<br>
              Against your height, heart and soul perhaps<br>
              I flew away probably to trick you only<br>
              So that you would often ask<br>
              Where did I have to go?<br>
              Since you knew, in your heart that<br>
              I didn&rsquo;t need to go&nbsp;<br>
              I didn&rsquo;t need to be so cruel<br>
              <br>
              Every moment, did I know that!<br>
              I was there to cry when I didn&rsquo;t understand the<br>
              Gravity of your affection and care for me!<br>
              Instead, I would whine so that you would come to fetch me- your son<br>
              To feed the food of love that you had right from the<br>
              Origin of the Cell that transferred your traces in me<br>
              On these detail, tiny minute of the DNA that I behold you in me<br>
              Always and ever no matter who I am and wherever I go!<br>
              <br>
              Oh mother, my mother!<br>
              You knew what I did to upset you<br>
              You knew that I didn&rsquo;t need to&nbsp;<br>
              And even to let that grow within my very own need of life&nbsp;<br>
              That defined the nature of mankind that you implanted in me<br>
              In the very way I grew so big,&nbsp;<br>
              As if sometimes bigger than yourself and the<br>
              Galaxy like bigger love that you'd fed in me&nbsp;<br>
              With the DNA that you&rsquo;re now and ever<br>
              To become so unruly, for I wouldn&rsquo;t want you to confine me&nbsp;<br>
              Within you for I didn&rsquo;t want to&nbsp;<br>
              Since I felt that I knew you, just knew you<br>
              Through my own being, for you knew I was you and you were me!&nbsp;<br>
              As I grew and raised with the energy to display all those<br>
              Notwithstanding the stubbornness of that kind that<br>
              Had come from you so that I could see how tough and yet loving&nbsp;<br>
              You were, by assuming as if it was nothing, but simply&nbsp;<br>
              The mother, Oh my mother!!&nbsp;<br>
              <br>
              Today, I have broken the connection in the rite<br>
              That you&rsquo;d have not wanted just because you love me<br>
              That my hands are shaking, eyes tearing and heart pounding<br>
              As I didn&rsquo;t do what you would want me to do&nbsp;<br>

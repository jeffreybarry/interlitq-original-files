              Therefore I did exactly the same thing that you would herald time and again<br>
              In your pain that only tears from your eyes could have washed it<br>
              The joy of love and affection and your enormity of kindness and wish of freedom<br>
              That you dreamt I&rsquo;d deserved out in the open, exactly the way you&rsquo;d want&nbsp;<br>
              No matter how many times, I had deliberated to hurt you<br>
              Often just letting the wound to be left to mend on its own!<br>
              Therefore, I flew away, so far away, so conditionally<br>
              I left you behind and still letting me to have this arrogance of life&nbsp;<br>
              That I can care for myself, be my own cell, my own shelf notwithstanding&nbsp;<br>
              The pain I could have caused in your heart&nbsp;<br>
              Even then you felt joyous, just when I felt rejoicing,&nbsp;<br>
              You felt more pain when I was hurt<br>
              You felt sad when I cried<br>
              You felt everything that you meant to feel<br>
              What I felt and that what you too felt!&nbsp;<br>
              Mother, Oh My Mother!<br>
              <br>
              And today, you have left me, almost as if it is forever!&nbsp;<br>

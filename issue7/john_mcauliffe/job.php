            <p><strong>Return</strong><br>
<br>
The towns are not so dark that no one enters;<br>
in nearby docks, the nights<br>
advance on empty lots.<br>
Fanatics gather in community centres.<br>
<br>
A dry spell engenders nostalgia for rain.<br>
The news will consider <br>
the negligent doctor<br>
and just who is immune to the variant strain.<br>
<br>
In cooler queues, low-slung jeans <br>
date the waspie;<br>
the bright bars are smokefree<br>
as the ocean’s photic zones.<br>
<br>
The downturn floats the clearance sale:<br>
staff migrate<br>
and the market<br>
anticipates no return; churches fill,<br>
<br>
the igloo melts, the deserts spread:<br>
north and south,<br>
a dolphin’s found in every port;<br>
new forms of algae feather the tide.<br>
<br>
The boats will travel day and night<br>
and some make land.<br>
For the time being, out of mind<br>
is out of sight:<br>
<br>
no walk-outs, no wild shouts<br>
from the dawning dark.<br>
Someone organises scouts,<br>
someone cleans up the park.<br>

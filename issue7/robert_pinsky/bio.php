                  <td class="texto2" valign="top"><p>Robert Pinsky was born on October 20, 1940 in Long    Branch, New    Jersey. He  received a B.A. from Rutgers University in New    Brunswick, New    Jersey, and  earned both an M.A. and Ph.D. in Philosophy from Stanford University,  where he was a Stegner Fellow in creative writing, and studied under the poet  and critic Yvor Winters. He is the author of several collections of poetry,  most recently <em>Gulf Music: Poems</em>; <em>Jersey Rain</em> (2000); <em>The  Figured Wheel: New and Collected Poems 1966-1996</em> (1996), which received the  1997 Lenore Marshall Poetry Prize and was a Pulitzer Prize nominee; <em>The Want  Bone</em> (1990); <em>History of My Heart</em> (1984); <em>An Explanation of  America</em> (1980); and <em>Sadness and Happiness</em> (1975). He is also the  author of several prose titles, including <em>The Life of David</em>; <em>Democracy,  Culture, and the Voice of Poetry</em> (2002); <em>The Sounds of Poetry</em> (1998), which was a finalist for the National Book Critics Circle Award; <em>Poetry  and the World</em> (1988); and <em>The Situation of Poetry</em> (1977). In 1985 he  also released a computerized novel, <em>Mindwheel</em>. Pinsky has published two  acclaimed works of translation: <em>The Inferno of Dante</em> (1994), which was a  Book-of-the-Month-Club Editor's Choice, and received both the <em>Los Angeles  Times</em> Book Prize and the Harold Morton Landon Translation Award; and <em>The  Separate Notebooks</em> by Czeslaw Milosz (with Renata Gorczynski and Robert  Hass). From 1997 to 2000, he served as the United States Poet Laureate and  Consultant in Poetry to the Library of Congress. During that time, he founded  the Favorite Poem Project, a program dedicated to celebrating, documenting and  encouraging poetry's role in Americans' lives. In 1999, he co-edited <em>Americans'  Favorite Poems: The Favorite Poem Project Anthology</em> with Maggie Dietz.  Other anthologies he has edited include <em>An Invitation to Poetry</em> (W. W.  Norton &amp; Company, 2004); <em>Poems to Read</em> (2002); and <em>Handbook of  Heartbreak</em> (1998). His honors include an American Academy of  Arts and Letters award, both the William Carlos Williams Award and the Shelley  Memorial prize from the Poetry Society of America, the PEN/Voelcker Award for  Poetry, and a Guggenheim Foundation fellowship. He is currently poetry editor  of the weekly Internet magazine <em>Slate</em>. Pinsky has taught at both Wellesley College and  the University of California, Berkeley, and currently teaches in the graduate writing program at Boston University. He  lives in Cambridge, Massachusetts. </p>

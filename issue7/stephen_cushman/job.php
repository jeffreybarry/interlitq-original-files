<strong>Aramaic in Sednaya</strong><br>
            &nbsp;<br>
<p>In  candlelight her tear-tracks outshine<br>
  icons  all over the underground chapel,<br>
  even  the one the faithful have come<br>
  so  far to supplicate, the Virgin herself<br>
  painted  by Luke, now on the wall<br>
  of  a dark little grotto abstaining from sun,<br>
  where  many believe that spending a night<br>
  will  help them conceive, Christian and Muslim,<br>
  their  shoes at the door whether they wear<br>
  headscarves  or not, whether they make<br>
  signs  of the cross again and again<br>
  before  each image or dip some cotton<br>
  in  bowls of oil and take it away<br>
  to  someone sick who&rsquo;s left at home,<br>
  someone  elsewhere who cannot see<br>
  tear-tracks  shine in candlelight<br>
  or  hear the old nun, the desert become<br>
  anything  but desolate, begin to pray<br>
in her savior&rsquo;s native tongue.</p>

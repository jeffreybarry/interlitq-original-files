               <P><strong>Head</strong>&nbsp;<br>&nbsp;<br>
     
                No, I was thinking I would lose my head<br>
                when there was the emperor&rsquo;s man, and thwack&mdash;<br>
                it wasn&rsquo;t my hat that was missing.<br>&nbsp;<br>
                I was a sister or I was a saint,<br>
                maybe a gilded statue of Venus,<br>
                sporting a halo or wearing a hat,<br>&nbsp;<br>
                blood on my bosom or no blood at all,<br>
                gilding a lily or a gala gown;<br>
                I was the garderobe or the avant-garde<br>&nbsp;<br>
                with the guards at my back in the palace.<br>
                What was a bust without a head on it,<br>
                what was a dress without a girl in it,<br>&nbsp;<br>
                a dress or a bag, a drape or a rag.<br>
                Dear Lesbia and poor fat Drusilla,<br>
                an emporium of d&eacute;colletages<br>&nbsp;<br>
                or a model of empiricism.<br>
                No, &ldquo;please save me&rdquo; wasn&rsquo;t a noble thought,<br>
                but save my face, at least that act of grace!<br>&nbsp;<br>
                All this was heady, which didn&rsquo;t mean smart;<br>
                it was the foam or the fizz, or the fat,<br>
              the cut of the gown, the slash of the neck.<br>&nbsp;<br>
              Oh god, how I wanted to dance and dance,<br>
              <em>dress in a lily, shake myself silly</em>.<br>
              The thought wriggled up, but my head was gone.<br>&nbsp;<br>
              It could be me or my image in stone.<br>
              It might be a headstone or a hanger,<br>
              a headache maybe, or a hangover.   </P>       
          &nbsp;<br>

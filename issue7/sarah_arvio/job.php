          <p><strong>Cant</strong>&nbsp;<br>
              &nbsp;<br>
            I said I could never live here, and I<br>
            never could, but I did for two decades&mdash;<br>
            I had fallen into a decadence.<br>&nbsp;<br>
            No, not a cadence, though that too could fall,<br>
            darkly&mdash;ever so darkly&mdash;through a glass,<br>
            or a mirror or a dirty window<br>&nbsp;<br>
            barred like a skeleton, barred like a cage.<br>
            Not as though I didn&rsquo;t care&mdash;I did care.<br>
            I had been carping on the debacle<br>&nbsp;<br>
            for most of ten years in the candlelight,<br>
            the decorum of the core of the deed,<br>
            decked in desire, here in my dark cave&mdash;<br>&nbsp;<br>
            decorated with me, or with my core,<br>
            the dark card of the dark lass and lady,<br>
            dandling on her lap my own life&mdash;my life&mdash;<br>&nbsp;<br>
            while I combed through the old crowns and papers.<br>
            It was tiny but cavernous, it was<br>
            cadaverous, it was my catacomb.<br>&nbsp;<br>
            But could I be decanted from this jar?<br>
            Cant! Cant! The cant of I can&rsquo;t&mdash;no, I can&rsquo;t&mdash;<br>
          comb out my hair, climb out of my lair and<br>&nbsp;<br>
          dance with the wind in the dandelions.<br>
          If the door was ajar, would I go out,<br>
          a cat darting her head around the door<br>&nbsp;<br>
          (carpe diem, carry away the day).<br>
          A canticle of anticipation,<br>
          some dandelion wine and a draft of air.</P></td>

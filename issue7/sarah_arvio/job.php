            <br><p><strong>Bomb</strong></p>
             <p> Was I playing it in the hope of joy,<br>
              play it as it lays, as someone said,<br>
              playing it down or else playing it up,<br>&nbsp;<br>
              or was there some other raison d&rsquo;&ecirc;tre,<br>
              some other raisin drying in the sun,<br>
              or some other reason under the sun.<br>&nbsp;<br>
              Please excuse my French, was there a reason<br>
              for being and doing anything else,<br>
              dreaming or dying for anything else.<br>&nbsp;<br>
              &ldquo;Be content with lack of misery&rdquo;<br>
              was a kind of motto for some of us,<br>
              a kind of bonbon or a platitude,<br>&nbsp;<br>
              or a kind of candy for a sucker<br>
              when what you need is a drink of sense,<br>
              the mot juste and a drink of joy or juice.<br>&nbsp;<br>
              Drink up, drink up, or else drink it down,<br>
              have you ever pondered the difference?<br>
              Not a bon mot or else a play on words<br>&nbsp;<br>
              but a choice between rising and falling.<br>
              Or lay it aside, play it as you can,<br>
              when what you need is some balm for the heart,<br>&nbsp;<br>
              or else a pillow for your pretty head,<br>
              or a pill, or a grape plucked from the vine.<br>
              Maybe a surge, a surfeit or an urge<br>&nbsp;<br>
              or something urgent in the argument,<br>
              as though reasoning could be part of it:<br>
              some say a surge of joy might be killing.</p>
              &nbsp;<br>
              &nbsp;<br>

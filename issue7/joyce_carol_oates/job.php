		  <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
            <EM>It has not happened yet.</EM> This most 
            exquisite moment of equipoise, equilibrium.</p>
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
            The moment between heartbeats. The moment between 
            breaths. The moment of 
            quick sharp terror before the plunge into orgasm, the body’s 
            helpless convulsing as the soul is extinguished like a 
            flame.</p>
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
            Sometime before dawn of March 31. Though 
            Serena has no sense of the date as she has but the vaguest sense of 
            the season: this teasing<st1:place> New England</ST1:PLACE> stasis between late winter and early 
            spring.<span style="mso-spacerun: yes">&nbsp;&nbsp; </span>She is 
            certain she hasn’t been sleeping—not since Andre has left her and 
            the child—yet her eyes spring open alert and dilated.<span style="mso-spacerun: yes">&nbsp; </span>Someone—it must be Andre—has 
            unlocked a rear door, has entered the house quietly and is 
            approaching in the hall.<span style="mso-spacerun: yes">&nbsp; 
            </span>Serena’s heart pounds like a terrified bird beating wings in 
            her chest, she clutches the fifteen-month little Andre in her arms 
            hearing his father’s footsteps in the corridor—<br>&nbsp;<BR>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
            “Andre?<span style="mso-spacerun: yes">&nbsp; </span>We’re in 
            here.”</p>
		  
		  
		  
		  
		  
		  <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
            </SPAN><i style="mso-bidi-font-style: normal">He will know where to 
            find us.<span style="mso-spacerun: yes">&nbsp; </span>I have left 
            messages—so many messages!<span style="mso-spacerun: yes">&nbsp; 
            </span>His assistant knows.<span style="mso-spacerun: yes">&nbsp; 
            </span>His friends know.<span style="mso-spacerun: yes">&nbsp;&nbsp; 
            </span>He 
            knows that I am waiting, and that I am not going to go away.<o:p></O:P></i></p>
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
            This raging insomnia!<span style="mso-spacerun: yes">&nbsp; 
            </span>The sixth night of Serena Dayinka’s vigil in the borrowed 
            house—the Nickelsons’ “big-open house” the child calls it, so many 
            skylights, floor-to-ceiling plate glass windows, sliding glass doors 
            overlooking a hilly late-winter terrain of drained and etiolated and 
            curiously depthless pine woods—on rural-suburban Edgehill Lane three 
            miles north of the village of Tarkington, Massachusetts.<span style="mso-spacerun: yes">&nbsp; </span>Serena has had phases of 
            insomnia in the past—since the age of thirteen, when her father <i style="mso-bidi-font-style: normal">went away</i>—but none has been 
            so virulent as this, raging like wild fire, dry brush, furious 
            crackling flames rushing over the glassy walls, the high white 
            ceilings and the hardwood floors whose high gloss has been dulled by 
            boot prints (she, the grateful occupant of the borrowed house, <i style="mso-bidi-font-style: normal">de facto</i> homeless until her 
            June first residency begins at Breadloaf) has been careless about 
            tracking damp, mud, gravel in and out, indifferent to stains from 
            the child’s spilled food and leaky sodden underpants where in one of 
            his more robust moods he has played at pushing himself across the 
            floor like a little monkey) as other parts of the beautifully 
            furnished house will be discovered to have been defiled by the 
            recent occupants, some more willfully than others.<span style="mso-spacerun: yes">&nbsp; </span><i style="mso-bidi-font-style: normal">How could this horror have 
            happened!<span style="mso-spacerun: yes">&nbsp; </span>We welcomed 
            her into our house to help her out, she was in such a 
            predicament.<span style="mso-spacerun: yes">&nbsp; </span>We did it 
            to help out Andre.<span style="mso-spacerun: yes">&nbsp; </span>We 
            had no idea the poor woman was so--</i><span style="mso-spacerun: yes">&nbsp;&nbsp; </span>Wiping the child’s 
            runny nose on a paper cocktail napkin she then crumples and tosses 
            aside—much of the house is strewn with wadded stiffened 
            tissues—mother and little Andre are lying in a<span style="mso-spacerun: yes">&nbsp; </span>patch of sunshine on the 
            high-gloss living room floor, the several bedrooms in the 
            Nickelsons’ house have become too smelly.<i style="mso-bidi-font-style: normal"><o:p></O:P></i></p>
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
            Serena laughs to think how such superior individuals as 
            Gerald and Danielle Nichelson—he, Andre Gatteau’s<span style="mso-spacerun: yes">&nbsp; </span>poet-critic pal who 
            publishes so frequently in <i style="mso-bidi-font-style: normal">The New York Review of 
            Books,</i> she, a “celebrated”<span style="mso-spacerun: yes">&nbsp; 
            </span>Renaissance art historian--will be obliged to stammer the 
            inevitable clichés, how scripted their reactions will be, like those 
            of TV performers.<span style="mso-spacerun: yes">&nbsp; </span><span style="mso-spacerun: yes">&nbsp;</span>All of Andre Gatteau’s 
            friends, acquaintances, admirers—everyone who has known both Andre 
            and Serena—and those who knew Andre’s son—will be forced to stammer 
            in the days, weeks, months to follow will say <i style="mso-bidi-font-style: normal">But where was Andre, how could 
            Andre have allowed such a horror to 
happen!<o:p></O:P></i></p>
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
            </I>She laughs, to think.<o:p>   </O:P>   </p>
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
            </SPAN>Well, where <i style="mso-bidi-font-style: normal">is 
            </i>Andre Gatteau?<span style="mso-spacerun: yes">&nbsp; </span>In 
            the early-morning of March 31?</p>
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
            Andre is in retreat. <span style="mso-spacerun: yes">&nbsp;</span>Andre has gone away for an 
            indeterminate period of time—it might be a few days, a week, two 
            weeks.<span style="mso-spacerun: yes">&nbsp; </span>Very likely, it will not be longer, nor has he traveled 
            abroad, for, as his assistant knows, he has not taken his passport; 
            she has reason to suspect, since she’d made hurried arrangements for 
            him just the previous week, that Andre Gatteau is at the Lost 
            Lake Mountain Zen Retreat in the Adirondack  Mountains, eleven miles west of Scroon Lake. </p>
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
            </SPAN>Several times in the past twenty-three years, Andre Gatteau 
            has retreated to <st1:place><st1:placename>Lost</ST1:PLACENAME> 
            <st1:placetype>Lake</ST1:PLACETYPE> 
            <st1:placetype>Mountain</ST1:PLACETYPE></ST1:PLACE>.<span style="mso-spacerun: yes">&nbsp; </span>In the exigency of personal 
            crises, <st1:place><st1:placename>Lost</ST1:PLACENAME> 
            <st1:placetype>Lake</ST1:PLACETYPE> 
            <st1:placetype>Mountain</ST1:PLACETYPE></ST1:PLACE> has become his 
            solace, his spiritual home.<span style="mso-spacerun: yes">&nbsp; 
            </span><i style="mso-bidi-font-style: normal">That place where, when 
            you come there, they have to take you in.<span style="mso-spacerun: yes">&nbsp; </span></i><span style="mso-spacerun: yes">&nbsp;</span></p>
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
            </SPAN>Here, in the mist-shrouded Adirondack dawn, so cold in the 
            barely heated sesshin room overlooking leaden-glass Lost Lake that 
            his breath is steaming, Andre Gatteau, fifty-three years old and 
            feeling his age, is sitting <i style="mso-bidi-font-style: normal">zazen </i>with a dozen other 
            seekers of enlightenment under the tutelage of a revered Zen 
            monk.<span style="mso-spacerun: yes">&nbsp; </span>They were wakened 
            in the dark at <st1:time minute="45" hour="5">5:45 A.M.</ST1:TIME> , 
            it is not yet <st1:time minute="30" hour="7">7:30 A.M.</ST1:TIME> 
            and already Andre Gatteau is feeling the strain of the intense Zen 
            meditation.<span style="mso-spacerun: yes">&nbsp; </span>Though 
            Andre has been sitting <i style="mso-bidi-font-style: normal">zazen--</i> in the classic lotus 
            position, buttocks on the bare pine floorboards, ankles tucked 
            beneath (sinewy-muscled) legs, knees raised and hands in loosely 
            gripped fists on his knees—for little more than an hour, already his 
            bladder is pinching with a need to urinate; there have come mocking 
            little jabs of arthritic pain not only in his legs but in his 
            wrists, and in that tenderly vulnerable spot at the base of his 
            spine; he is assailed by distracting thoughts, hornet-thoughts, 
            obsessive thoughts—all that Zen meditation forbids.<span style="mso-spacerun: yes">&nbsp; </span><i style="mso-bidi-font-style: normal">Observe your thoughts.<span style="mso-spacerun: yes">&nbsp; </span>Observe your thoughts as 
            they emerge, as they arise, as they fill your consciousness, as they 
            clamor and howl and fade and vanish, observe your thoughts knowing 
            always that your thoughts are not you: your thoughts are not your 
            Zen-mind.<span style="mso-spacerun: yes">&nbsp; 
            </span><o:p></O:P></i></p>
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
            </SPAN>This is true!<span style="mso-spacerun: yes">&nbsp;&nbsp; 
            </span>He knows this is the one true fact.</p>
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
            </SPAN>And so he is determined, this time at 
            <st1:place><st1:placename>Lost</ST1:PLACENAME> 
            <st1:placetype>Lake</ST1:PLACETYPE> 
            <st1:placetype>Mountain</ST1:PLACETYPE></ST1:PLACE> he will not 
            fail.<span style="mso-spacerun: yes">&nbsp; </span>As he did not 
            fail in the several heroic endeavors of his life, the first of these 
            being a re-invention of his life, a scouring and a cleansing and a 
            re-making of his soul, as an African-American boy of fourteen, in 
            Lakeland, Florida.<span style="mso-spacerun: yes">&nbsp;&nbsp; 
            </span></p>
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
            </SPAN>The eastern sky above 
            <st1:place><st1:placetype>Mt.</ST1:PLACETYPE> 
            <st1:placename>Hood</ST1:PLACENAME></ST1:PLACE> is veined and 
            mottled like a tumorous growth, curious streaks of shadow, crevices 
            of sunshine and rain-swollen cumulous clouds.<span style="mso-spacerun: yes">&nbsp; </span><i style="mso-bidi-font-style: normal">Here is the beauty of the world, 
            without humankind to name it.<span style="mso-tab-count: 1">&nbsp;&nbsp;&nbsp; </span><span style="mso-spacerun: yes">&nbsp; </span><o:p></O:P></i></p>
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
            </SPAN>With the exception of a flush-faced porcine white man in his 
            sixties and an older, emaciated white woman with a starved-hawk of 
            sheer desperation—<i style="mso-bidi-font-style: normal">Help me! 
            help me!</i>—the other Zen-seekers embarked upon this twelve-day 
            sesshin retreat appear to be considerably younger than Andre 
            Gatteau.<span style="mso-spacerun: yes">&nbsp; </span>At the silent 
            breakfast Andre had taken care not to look at anyone very 
            closely—Andre Gatteau isn’t a man who wishes to exchange smiles, 
            greetings, handshakes with strangers—certainly Andre doesn’t want 
            anyone to look closely at <i style="mso-bidi-font-style: normal">him.<span style="mso-spacerun: yes">&nbsp; </span></i>Known for his shyness, 
            or his willful passivity, Andre is one to speak only when directly 
            addressed and then often in a near-audible murmur.<span style="mso-spacerun: yes">&nbsp; </span>(Though he reads his poetry 
            on stage, as a performer, in a voice of astonishing emotional 
            nuance, power, and beauty.)<span style="mso-spacerun: yes">&nbsp; 
            </span>Andre is a stocky dark-skinned man with a wrestler’s build: 
            muscled shoulders, large hands and feet, slightly foreshortened 
            legs.<span style="mso-spacerun: yes">&nbsp; </span>His face looks as 
            if it has been battered, as in a car wreck; he is ugly-handsome, 
            with unusually large alert almond-shaped eyes aslant in his stolid 
            face; on his forehead just beneath his hairline is a sickle-shaped 
            scar that catches the light like a winking third eye.<span style="mso-spacerun: yes">&nbsp; </span>His graying nubby hair is 
            trimmed short.<span style="mso-spacerun: yes">&nbsp; </span>Not a 
            tall man, at five-foot-nine, Andre carries himself with an almost 
            military bearing, he has a dread of stooped shoulders, a drooping 
            head; he has a dread of aging, as he cannot imagine a future in 
            which, by an effort of will, he will not be able to control the 
            circumstances of his life utterly.</p>
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
            </SPAN>At <st1:place><st1:placename>Lost</ST1:PLACENAME> 
            <st1:placetype>Lake</ST1:PLACETYPE></ST1:PLACE>, Andre Gatteau is <i style="mso-bidi-font-style: normal">incognito.</i> <span style="mso-spacerun: yes">&nbsp;</span>In any case no incoming calls 
            are accepted here, and no uninvited visitors.<span style="mso-spacerun: yes">&nbsp; </span>The effort of <i style="mso-bidi-font-style: normal">sesshin</i> is intense 
            meditation: sitting <i style="mso-bidi-font-style: normal">zazen 
            </i>seven to ten hours daily, in the late afternoon walking <i style="mso-bidi-font-style: normal">zazen </i>on the trails circling 
            the retreat.<span style="mso-spacerun: yes">&nbsp; </span>There are 
            interludes of work <i style="mso-bidi-font-style: normal">zazen,</i> 
            Zen instruction, brief breaks for meals, bedtime promptly after 
            sunset.<o:p></O:P></p>
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
            </SPAN>Andre relieves some of the stress of the <i style="mso-bidi-font-style: normal">zazen</i> meditation by going 
            for a run of two to three miles before the evening meal and already 
            by mid-morning his body yearns ahead to that interlude of release, 
            and freedom:<span style="mso-spacerun: yes">&nbsp; </span>always 
            Andre has gone on solitary runs, often in the early morning, or in 
            the early evening after a day of highly concentrated work.<span style="mso-spacerun: yes">&nbsp; </span><i style="mso-bidi-font-style: normal">Take me with you, I’m a runner 
            too, I promise I won’t talk to you Andre please take me with you</i> 
            she’d pleaded before pregnancy distended her small supple body and 
            there was no possibility of Serena joining 
him.<o:p></O:P></p>
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
            </SPAN>It was nothing personal: Andre had never wanted anyone to 
            accompany him, running.<span style="mso-spacerun: yes">&nbsp; 
            </span>Since he’d begun seriously writing poetry in his mid-twenties 
            running—solitary running—has been sacred to him, a time of<span style="mso-spacerun: yes">&nbsp; </span>intense meditation: his 
            model is the blind John Milton who’d purposefully spent much of his 
            time alone, developing his remarkable memory as one might develop 
            muscles through sheer exercise and repetition; Milton could retain 
            as many as fifty hendecasyllables of blank verse in his memory at a 
            single time and then dictate these to whoever was available.<span style="mso-spacerun: yes">&nbsp; </span>In this way the entirety of 
            the magnificent <i style="mso-bidi-font-style: normal">Paradise 
            Lost</i> was composed.<o:p></O:P></p>
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
            </SPAN>No living poet can be said to be “famous” in America—nor even 
            “known”—yet in some quarters, predominantly east-coast, urban, and 
            academic, Andre Gatteau has become a famous name in the past fifteen 
            years: his photograph has appeared in the <i style="mso-bidi-font-style: normal">New York Times</i> (National 
            Book Award, Pulitzer Prize, McArthur award, et al.), he has been the 
            subject of lengthy profiles in the <i style="mso-bidi-font-style: normal">New Yorker </i>and <i style="mso-bidi-font-style: normal">Harper’s. </i>Such attention has 
            made Andre even more self-conscious in public.<span style="mso-spacerun: yes">&nbsp; </span>Serena teased <i style="mso-bidi-font-style: normal">Poor Andre!<span style="mso-spacerun: yes">&nbsp; </span>He resents being recognized 
            when he doesn’t want to be recognized and he resents not being 
            recognized when he isn’t recognized.<o:p></O:P></i></p>
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
            </SPAN>Serena!<span style="mso-spacerun: yes">&nbsp;&nbsp; 
            </span>His heart contracts in pain, he will not think of 
            her.</p>
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
            </SPAN>Nor of the child: his child.</p>
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
            </SPAN>Hers, and his.<span style="mso-spacerun: yes">&nbsp; 
            </span>He cannot think <i style="mso-bidi-font-style: normal">Ours.<o:p></O:P></i></p><p style="mso-bidi-font-style: normal" i="">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
            </SPAN>Andre?<span style="mso-spacerun: yes">&nbsp; </span>Please call me.<span style="mso-spacerun: yes">&nbsp; </span><o:p></O:P></I>
            <p style="mso-bidi-font-style: normal" i="">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </SPAN>I am so sorry, I 
            did not mean to be so emotional.<o:p></O:P></I>
            <p style="mso-bidi-font-style: normal" i="">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
            </SPAN>Did not mean to 
            accuse you, it’s just I am so exhausted.<o:p></O:P></I>
            <p style="mso-bidi-font-style: normal" i="">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
            </SPAN>Andre, forgive me, 
            you know I didn’t mean the ridiculous things I 
            said.<o:p></O:P></I>
            <p style="mso-bidi-font-style: normal" i="">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
            </SPAN>Little Andre is 
            missing you, darling. <span style="mso-spacerun: yes">&nbsp;</span>Here, I will put him on the 
            line, say hello to Daddy, honey, c’mon sweetie Daddy is 
            listening—<o:p></O:P></I>
            <p></p><p style="mso-bidi-font-style: normal" i="">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
            </SPAN>Call my cell phone, 
            Andre.<span style="mso-spacerun: yes">&nbsp; </span>It’s never 
            turned off.<span style="mso-spacerun: yes">&nbsp; </span>I am 
            staying at Gerald’s and Danielle’s as you know, I am expecting to 
            see you here maybe this weekend, please call and confirm will you, I 
            will leave their number another time in case you’ve misplaced 
            it.<o:p></O:P></I>
            
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
            </SPAN>As the Heart Sutra is being chanted by Zen devotees, young 
            Caucasians in coarse-woven monk-robes like a PBS documentary: this 
            continuous lulling chant like slabs of<span style="mso-spacerun: yes">&nbsp; </span>water cascading down a rocky 
            mountain stream.<span style="mso-spacerun: yes">&nbsp; 
            </span>           
                     

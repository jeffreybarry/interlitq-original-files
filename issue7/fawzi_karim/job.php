            <strong><em>The Night Drives Its Nails&nbsp; </em></strong><br>
            &nbsp; <br>
            Winter bares the tree to a solitary nest. <br>
            Rain drips through its mesh of twigs. <br>
            Snow embalms it in a wreath of soft cotton. <br>
            &nbsp; <br>
            That nest is as heavy as <br>
            The silence which clogs my ears, <br>
            Or the smoke which weighs on my lungs. <br>
            It penetrates my dreams, where night <br>
            Is driving and driving its nails. <br>
            &nbsp;<br>

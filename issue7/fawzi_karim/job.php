            <strong><em>Seeing and Calling</em></strong><br>
            &nbsp; <br>
            The god I have always denied enters the house. <br>
            My father, lying in white, is in his death agonies. <br>
            My mother, sisters, brother, half-circled around Him, <br>
            &nbsp;&nbsp; avert their eyes. <br>
            Only I have seen the god coming in. <br>
            Only I am trying not to look at him. <br>
            &nbsp; <br>
            The god is crying, this god I have always denied. <br>
            He is full of compassion, pity. <br>
            The shadow of death hangs over the family. <br>
            Soon there will be nothing but darkness. <br>
            A void will engulf my father&rsquo;s body. <br>
             <br>
            <em>We are listening.</em><br>
            <em>Our sole right is to beg him.</em><br>
            <em>We cannot ask the question which is always on our lips,</em><br>
            <em>Yearning to be spoken.</em><br>
            <em>From such frustration flows our bitterness.</em><br>
            &nbsp; <br>
            Bent-backed is the god, smitten by time, <br>
            A ruined building, sole refuge now <br>
            For refugees from who knows where. <br>
            He slips away, so cautiously, <br>
            And, patiently, unseen, I follow after, <br>
            &nbsp; <br>
            Both lost, in the way a star is lost, <br>
            through desert dust, and mirages <br>
            of water, which ever recedes from us, <br>
            and travellers who went before...<br>
            The bones repeat the voice of the winds.<br>
             <br>
            Across wind-lashed spaces, the wolves howl on. <br>
             <br>
            <em>We are listening.</em><br>
            <em>Our sole right is to beg him.</em><br>
            <em>We cannot ask the question which is always on our lips,</em><br>
            <em>Yearning to be spoken.</em><br>
            <em>From such frustration flows our bitterness.</em><br>

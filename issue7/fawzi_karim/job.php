<strong><em>Winter of God</em></strong><br>
            &nbsp; <br>
            In the summer, Lord, we idle, plotting our futures. <br>
            At your bidding, the seasons of spring and autumn <br>
            Are devoted to planting and harvest <br>
            In preparation for winter, your especial favourite. <br>
            &nbsp; <br>
            Summer we remember <br>
            For the friend who shares his aloneness <br>
            &nbsp;&nbsp; with one other, <br>
            For the neighbour&rsquo;s daughter who runs naked <br>
            &nbsp;&nbsp; beneath the flourish of the palms, <br>
            For the angel who takes the name of my mother, <br>
            And for my father, and the strength of his endurance. <br>
            &nbsp; <br>
            Yet how futile is summer when compared to your winter! <br>
            When you teach us to remember <br>
            That the day of celebration is always the day of our doom. <br>
            When you allow us to reap the fields of experience, <br>
            &nbsp;&nbsp; and then leave us <br>
            Because their fruits do not please you. <br>
            &nbsp;<br>
            Now our dreams grow green beneath the mirrors <br>
            &nbsp;&nbsp; of your sky. <br>
            The stars gleam pink &ndash; the nipples of your virgins <br>
            &nbsp;&nbsp; in their hour of fertility. <br>
            Oh how often, Lord, do we take shelter in prayer, <br>
            Asking for memory to be erased, <br>
            For the autumn to lie as deep as our desires, <br>
            And for spring to offer its dark promise of fertility. <br>
            &nbsp; <br>
            And yet the treachery of winter lies deeper still. <br>
            &nbsp;<br>

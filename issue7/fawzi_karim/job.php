            <strong><em>The Goalkeeper</em></strong><br>
            &nbsp; <br>
            <em>What a Wonderful World</em>, Louis! <br>
            Your voice, rough as the bark of a tree, <br>
            Threads through this lobby of hope. <br>
            &nbsp; <br>
            Ablaze, like phosphor, at your words, <br>
            I glow above this book I am thumbing, <br>
            Beside the stove at which I am warming myself. <br>
            &nbsp; <br>
            How strange is my life, my world &ndash; <br>
            One minute joyous, the next full of care, <br>
            Eyes brimming with tears &ndash; of laughter.<br>
          &nbsp;<br>
          I am a goalkeeper, <br>
Wary of strikers with no ball, <br>
Seeing an infinity of blue emptiness <br>
Between a clatter of legs. <br>

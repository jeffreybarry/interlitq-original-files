            <strong><em>Wall</em></strong><br>
            &nbsp; <br>
            I shut the door &ndash; rain flings into my face &ndash; <br>
            And make for the nearest bar. <br>
            I am free as any stranger in a city. <br>
            I have no kin to worm its way beneath my skin. <br>
            I am a brick in a wall, <br>
            Which will soon be added to other walls, <br>
            And then &ndash; hey presto! &ndash; a fort. <br>
            Inside my head, muffled by its hat, <br>
            I am calming the cries of some woman. <br>
            Rain douses her too. The tongue is silenced. <br>
            &nbsp; <br>
            Two glasses, dear waiter. <br>
            One is for me. <br>
            The other is&hellip;still to come. <br>
            You will know her by her coat, of wool. <br>
            &nbsp; <br>
            The waiter, a customary type, <br>
            Reads the mind of the addict to perfection. <br>
            Another customer waits.<br>
            He nods - and hurries away. <br>

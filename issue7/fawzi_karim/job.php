            <span dir="ltr"> </span><span dir="ltr"> </span>&nbsp;<strong><em>Black Ink</em></strong><strong> </strong><br>
            &nbsp; <br>
            The darkness of this night is greater <br>
            &nbsp;&nbsp; than the power of a sultan. <br>
            Ink from my books, shelf upon shelf of them, <br>
            &nbsp;&nbsp; streams down the curtains. <br>
            Every book is an overturned inkwell. <br>
            &nbsp; <br>
            Patience, I say. Day will dawn, <br>
            And the colours begin to spread. Snatching the brush, <br>
            I strive to paint the curtains the rose of dawn, <br>
            &nbsp;&nbsp; and the walls, green. <br>
            Now waves come washing in, blue flecked with white, <br>
            &nbsp;&nbsp; above the shelves. <br>

            <strong><em>Night&rsquo;s Scavengers</em></strong><br>
            &nbsp; <br>
            These scavengers for wood beside the Thames, <br>
            I take in their glutinous, brown, tar-like stink. <br>
            In the sieve of their thick, matted hair, <br>
            They trap all the dirt and the darkness of London. <br>
            Their exile shelters, nurtures them in silence. <br>
            &nbsp; <br>
            I too am a scavenger,<br>
            I too am an exile , <br>
            Looking to join them,<br>
            Born again, <br>
            A scavenger of words beside the Thames.<br>
            &nbsp; <br>
            But their exile is deeper than mine. <br>
            I see their make-shift shelters from the rain &ndash; <br>
            Boxes, slumped against concrete walls &ndash; <br>
            And how they coil inside, <br>
            Intoxicated spirits, <br>
            Eyelids heavy with numbness. <br>
            &nbsp; <br>
            For warmth, I squat in a phone booth, staring out. <br>
            A sparrow of ice alights. <br>
            A woman&rsquo;s lips leave a promise on the glass. <br>
            My palm throbs between cheek and pillow. <br>
            The smell of weaning plugs my nostrils. <br>
            I hear the gentle swish-swish of autumn foliage. <br>
            A train on some nearby platform sighs. <br>
            A suitcase hesitates to move. <br>
            &nbsp; <br>

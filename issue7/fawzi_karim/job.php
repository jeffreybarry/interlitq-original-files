            </span><strong><em>The Forgotten City</em></strong><br>
            &nbsp; <br>
            Late afternoon. <br>
            The houses are shaded in the lightest of pencil strokes. <br>
            Birds have flown, nests long abandoned. <br>
            Wind flourishes its whips of cold, freezing the tongue; <br>
            Echoes of footfalls; leaves falling; <br>
            Rags and tatters of newsprint. <br>
            &nbsp; <br>
            Birds have flown, their nests long abandoned. <br>
            You too, o errant sand dweller, <br>
            Have left nothing but footprints, <br>
            Having stared at the waves too late, and for too long. <br>
            &nbsp; <br>
            Once the sun burnt the wrist of the city. <br>
            Now it has cooled to a silver bracelet, <br>
            Worn once, now long gone, my forgotten lady. <br>
            &nbsp; <br>

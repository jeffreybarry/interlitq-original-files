            <p><strong>He Admits He is Delirous in Thinking of How He Carried Her</strong></p>
            
            <p>&ldquo;I am delirious in thinking of how I carried you,<br>
            I know it in your mime of my delirium to me.<br>
            And at dawn when our attention turns to </p>
           
            <p>each other, we smother our every<br>
            revenge, we garble thought, mangle<br>
            words, recall in kisses how we</p>
            
            <p>carried us.  From the tango tangle<br>
            of that stone&rsquo;s revenge, our mime of pain<br>
            in release of attention in release from the angle</p>
           
            <p>of that construction, I,  insane<br>
            to the smell of you, the irrelevant<br>
            weight of you, the invisible rain</p>
            
            <p>on the dawn of you, the errant<br>
            world in that klepto-nanosecond, that shrine<br>
            immaterial, that materialised present&mdash;</p>
            
            <p>for our attentions are sometimes too available,<br>
            for restlessness of the forklifts threatens <br>
            the very truth of dawn, renders it metaphorical, <br>
            for in that moment I carried you down <br>
            from that ledge and you felt </p>
           
            <p>impossibly light we were graceful,<br>
            that you in fact must have carried me carrying you <br>
            in order for it to have been possible, the idea <br>
            that for the exact length of a suspended thought </p>
            
            <p>mimesis and truth were one, dawning <br>
            and attention equal, anger set aside, space <br>
            opened its vacumn to clarity so clarity&mdash;like <br>
            the unspoken composite beloved&mdash;could appear.&rdquo;<A NAME="sdfootnote1anc" HREF="#sdfootnote1sym" SDFIXED><sup>1</sup></A></p>

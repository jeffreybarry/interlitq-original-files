           &nbsp;<br>
            <p><strong>The Border</strong></p>
           
            <p>The dead stand on platforms<br>
            in the rain, cases stuffed <br>
            to bulging. They travel beside you.</p>
            
            <p>Out the window fields<br>
            unribbon like film reels,<br>
            clouds gather over the hills. </p>
         
            <p>The train slows to a halt <br>
            between stations. Your watch <br>
            has stopped, your timetable's expired. </p>
            
            <p>The rain has washed away the view.</p>
            
            <p>You are a postcard, <br>
            ephemeral, full of pleasantries. <br>
            What you meant to say trails off. </p>
          
            <p>The dead stare straight ahead. <br>
            They do not talk about the weather.<br>
            They have no special needs. </p>
             
            
            <p>They are a closed book. <br>
            You will never know <br>
            how it ends. </p>
            <p><BR>
            </p>

            <p><strong>Night Fog</strong></p>
            
            <p>after <em>Ochre Figure on Black</em> by George Dannett</p>
            
            <p>We make the journey blind, headlights <br>
            seeking the here and now, a piece <br>
            of tarmac, its pale yellow line <br>
            measuring distance in yards, creatures<br>
            with high-beam eyes who stare, then dart<br>
            into the black. The aperture<br>
            of night opens; we yield to the dark<br>
            closing on our future,<br>
            breathe the cool air, dust and mildew,<br>
            sharp musk of dream.<br>
            As we move forward, we look back;<br>
            a daguerreotype of shadows,<br>
            a map to guide us to the past,<br>
            the veiled land of home.</p>
           

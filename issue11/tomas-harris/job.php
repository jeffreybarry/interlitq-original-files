            <p><strong>Vacant Lot</strong></p>
           
            The dusks over vacant lots<br>
            always are charged with repeating images. Without human form,<br>
            modeled purely out of earth, dissolved in pure rain,<br>
            stretching over pure mud and scraps of greens<br>
            coming loose from the hills on this side of the world<br>
            unbathed by the sun, where the pure agony of sun<br>
            refracts, the pure absence of human form<br>
            in the designated places.<br>
            History ends at the vacant lots. Our pupils<br>
            widened the survey of space and our mouths<br>
            stuttered a broken-off desire, at most our teeth<br>
            bled the tips of our tongues, at most our minds<br>
            dreamed impossible bodies in the red<br>
            separation of earth and sun.<br>
            The dusks over vacant lots<br>
            always are charged with repeating images. Our bodies<br>
            thickened with the onset of shadows, became<br>
            vegetal with the rotten greens, mineralized<br>
            in the empty instant of the night, stuck to<br>
            smoke dispersing in white manes floating toward<br>
            the hole of the night; or waited, as if that final water<br>
            unnamed by images would spill from<br>
            the plazas of the night, that final water of<br>
            myths and dreams that stanched the purple stains<br>
            from our bodies with the cleanliness<br>
            of a new human form.</p>
           
           

            <p>Tom&aacute;s Harris was born in 1956 in La Serena, north of Santiago, Chile. He later moved to Concepci&oacute;n, in the south.<em> </em>During the latter years of the Pinochet dictatorship (1973&ndash;90), he wrote the five books that would make up <em>Cipango </em>(1992), considered his most important work to date.  Since the restoration of democracy, Harris has produced other books of poetry and prose, and has won accolades in Chile and elsewhere in Latin America, including the Neruda award and the Casa de las Am&eacute;ricas prize. Early this year, Bucknell University Press published my translation of <em>Cipango </em>as a bilingual edition. A selection from the book appears below.</p>
            <p><em> </em>The poems in <em>Cipango</em> refer to the voyage of Columbus, who believed he&rsquo;d reached Asia (&ldquo;Cipango&rdquo; or Japan), not the Americas (Hispaniola). Building on that mistaken premise, the book<em> </em>comments on the oppressive legacy of colonialism in Latin America&mdash;manifested in twentieth-century Chile through the military coup and dictatorship. In <em>Cipango</em>, torture and disappearances take place with impunity, as illustrated in poems such as &ldquo;Orompello II.&rdquo; The collective voice expresses fear, displacement, and despair resulting from a clash of worlds. <em>Cipango </em>functions as a metaphor for the violence and degradation of contemporary society. Harris&rsquo;s vision is of a decadent, apocalyptic world that nevertheless contains hope for regeneration, as in the poem &ldquo;Vacant Lot.&rdquo;</p>
            <p> The book is characterized by obsessive imagery; juxtapositions of contemporary and archaic language as well as of incongruous settings; the use of irony; and a dark tone. The speaker addresses personages such as Christopher Columbus, Marco Polo, and the Great Khan, and makes reference to sources including Columbus&rsquo;s diaries, Quevedo, Poe, Genet, Carpentier, <em>Dracula, </em>the film <em>Goldfinger, </em>and Billie Holiday; the protagonist of Nerval&rsquo;s <em>Aur&eacute;lia</em> drifts in and out of the poems. <em>Cipango</em>&rsquo;s formal and structural elements combine to produce the effect of an epic poem.</p>
            <p> My first exposure to Tom&aacute;s Harris&rsquo;s work occurred in 1993, when I was organizing a program series featuring Chilean writers who were to appear at the Americas Society in New York, where I direct the Department of Literature. I became drawn to the texts in <em>Cipango</em>.  As I embarked upon the translation of the book, I was attracted to the poetic voice, which was dark, obsessive, and rhythmic. That voice kept me anchored throughout the process, from the opening poem (&ldquo;Danger Zones&rdquo;) to &ldquo;Poiesis of the Better Life,&rdquo; the stunning piece that concludes the collection.</p>
            <p>Harris&rsquo;s poetry grows out of the Chilean poetic tradition&mdash;a tradition that has produced such greats as Vicente Huidobro, Gabriela Mistral, Pablo Neruda, and Nicanor Parra. But Harris&rsquo;s poems don&rsquo;t celebrate or lament like Neruda&rsquo;s or Mistral&rsquo;s and they aren&rsquo;t playful like Parra&rsquo;s or Huidobro&rsquo;s. Rather, they challenge, bear witness, leave the reader with a question: &ldquo;What will happen after all this?&rdquo; <em>Cipango </em>inspires its readers through its innovative language, its relevance to Chile&rsquo;s history, and its universal themes.</p>
           
            <p>&mdash;<em>Daniel Shapiro</em></p>
        
            <p><strong>Note</strong></p>
            <p>Many of the poems in <em>Cipango</em> are set in Concepci&oacute;n, which has given the book an almost prophetic quality when read today, particularly in light of the recent earthquake and its devastation of that city. </p>

            <p>Premi&egrave;re s&eacute;lection:</p>
        
            <p>In the end, what we need to survive is maimed, burnt and broken, but refuses to be forgotten.</p>
            
            <p><strong>Pour faire une tresse - assemblez trois longues m&egrave;ches de cheveux; une pour l&rsquo;&acirc;me, une pour la raison et une pour l&rsquo;action.</strong></p>
           
            <p>She&rsquo;s sitting in her chair.<br>
            The rocking chair in the kitchen.</p>
           
            <p>She slowly licks the palm of her left hand<br>
            then between the fingers<br>
            curving her tongue over the skin web<br>
            like Marc used to do.</p>
            
            <p>She thinks of her brothers <br>
            of her son Raoul &ndash; gone away, safe to grow up in Montana<br>
            safe from the vicious traps her neighbours are setting.</p>
        
            <p>The clock is ticking. <br>
            It chimes 7:30.</p>
           
            <p>It&rsquo;s not her fault Zo&euml; Treffl&eacute; wasn&rsquo;t able to keep the baby.<br>
            Not her fault the baby died.<br>
            Not her fault Zo&euml; died.<br>
            She had done what she could,<br>
            warning Zo&euml; against the drink,<br>
            warning her that T&eacute;l&egrave;sphore bothering her all the time would hurt the baby,<br>
            weaken her.<br>
            and working around those sick cows was asking for trouble,<br>
            but Zo&euml; wouldn&rsquo;t listen.</p>
           
            <p> And now she knew tonight,<br>
            tonight they would come<br>
            under cover of the new moon<br>
            led by the drunken courage and useless fury of T&eacute;l&egrave;sphore Treffl&eacute;.</p>
          
            <p>They would come and they would kill her.</p>
            
            <p>She hears footsteps on the frozen ground.</p>
           
            <p>She hears Antoine Treffl&eacute; calling her names.  She hears T&eacute;l&egrave;sphore cursing, stomping on the porch, crashing into the door. He comes in a rush, ready with the ropes, but stops clumsily when she simply rises and turns her back to him. The others crowd in, pushing him forward.  They smell of ragotte, burped over pork and cabbage.  She cannot bear to see them look at her as if she is an animal they must hunt and kill. Their fear has a smell that threatens to choke her, make her cry. </p>
           
            <p>She turns when she hears T&eacute;l&egrave;sphore&rsquo;s laboured breathing and knows the lung infection has not cleared.  With no one to help him, he will be dead before the year is out. </p>
            
            <p>Antoine catches the ghost smile creeping. He slaps her across the face, screaming about her arrogance and the others laugh loudly, slapping each other on the back.</p>
           
            <p>T&eacute;l&egrave;sphore grabs the jug of ragotte from Antoine and spins Rubis around to face the men. She stumbles forward.  A young boy near the door makes a little movement, but T&eacute;l&egrave;sphore curses him to keep his place.</p>
          
            <p>-I told you to keep yourself quiet!  he bellows.  -The Almighty Madame Rubis Morin, the cursed healer, does not need your help, little Baptiste.  Here Healer, you want something?  You want some help?  Take some of this, he slurs, wrenching her chin down with his free hand and shoving the jug into her face.  &ndash;This, this is my medicine.  This, is holy water.  Let&rsquo;s see what good it does you. </p>
          
            <p>Rubis jerks her head back and crosses her arms, saying nothing.</p>
          
            <p>-I said drink goddamn you, he snarls, gripping her arm and splashing the alcohol. </p>
         
            <p>Rubis splutters and coughs, the liquid burning her eyes and mouth. </p>
       
            <p>Antoine steps forward.  -Don&rsquo;t waste it for Chrissake.  If she don&rsquo;t want it T&eacute;l&egrave;sphore, I do! </p>
            
            <p>He wrestles the jug from his brother&rsquo;s grip and tips it eagerly to his own mouth.</p>
           
            <p>-Come on, T&eacute;l&egrave;sphore barks, -I didn&rsquo;t come here for no party.  We got things to do einh? he leers.  -We came here to do something, so let&rsquo;s do it. </p>
           
            <p>He yanks the rope expertly around her wrists, hobbles her to her own ankles. </p>
            
            <p>&ndash;Come on bitch, you&rsquo;re coming with us, he snarls, pushing her toward the door.</p>
          
            <p>The men shove themselves up against her as she passes, rubbing their hands on her body and laughing about a waste of good meat.  T&eacute;l&egrave;sphore ignores them, pulls her roughly behind him into the night.</p>
       
            <p>They take her through the yard and up the road,<em> </em>more roughly than is necessary.  They need to hear her cry or plead, whimper or shout, anything to show that they have finally broken her.  Rubis says nothing.  She keeps her eyes on the ground, refusing to give them the satisfaction of knowing, she is already broken. </p>
          
            <p>They drink heavily from the flasks they had filled at Treffl&eacute;&rsquo;s earlier. They shout obscenities to egg each other on and pull her along, jerking on the rope to make her stumble. </p>
           
            <p>They gather stones along the way, large blue field stones.</p>
           
            <p>They bring her to water. </p>
           
            <p>Six-year old Baptiste Treffl&eacute; stays in the deepest shadows.</p>

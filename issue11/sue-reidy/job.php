            <p>Inside the Well of Being<br>
            nightly excesses are curtailed.<br>
            You wouldn&rsquo;t believe the secrets <br>
            revealed before lights out.<br>
            Nothing leaves this room.</p>
           
            <p>Groups, couples and singles<br>
            are all welcome.<br>
            Queens or kings are <br>
            available for couples only<br>
            Singles can share twins.<br>
            Sex before dark is taboo.<br>
            Cooking in your room is forbidden.<br>
            Speak only when spoken to.</p>
            

            <p><strong>Scrabble Cue</strong></p>
             
            <p>A quag is a marshy boggy spot, I quoth.<br>
            A quant is little more than a punting pole.</p>
           
            <p>Go throw a quoit, you&rsquo;ve had your quota. <br>
            Be quiet now while I sew a quilt for the quins. </p>
          
            <p>I wait a quint or musical interval before <br>
            I pick up a quire, four sheets of paper.</p>
            
            <p>Please, don&rsquo;t send me to quod, I quip.<br>
            Don&rsquo;t quiz me, quit while you&rsquo;re ahead. </p>
           
            <p>You call me a quean, an impudent girl.<br>
            We quiver, quaggy, quanting.<br>
            Yawling, yearning, yodeling, yelping.</p>
          
           

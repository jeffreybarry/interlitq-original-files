            <p ALIGN="CENTER"><span class="titulo2"><strong>Eclogue, Elegy, Ode (1927-1928)</strong> <A class="link1" NAME="sdfootnote1sym" HREF="#sdfootnote1anc">*</A></span></p>
            <p ALIGN="CENTER"><U><BR>
              </U></SPAN><BR>
            </p>
             
            <p> <strong>Homage</strong></p>
            
            No myrtle leaves. No laurel. The wall extends<br>
            its vast domain &ndash; insatiable arc<br>
            across the gloom. Out of the darkness<br>
            a ripple. A vibration. Pulsing sound ascends. </p>
             
           
            <p>A poignant voice, extinct, without the plumes<br>
            that vested it in living white,<br>
            arises, agitated from the night,<br>
            in bursts of its ancient tune.</p>
            
            <p>A rumour, softly preening &ndash;<br>
            after the sound has gone, it wants. It longs.<br>
            The solid silence hearkens to those songs,<br>
            resonant with harmony and meaning.</p>
           
            <p>Though time amass oblivion in iron hand,<br>
            strangling the chanting breath, it cannot kill.<br>
            That never-aging voice throbs and pulses still,<br>
            singing to the world of men.</p>
           
            <p>And what has become of the joyous<br>
            mortal flight? Its ecstasy undone,<br>
            it rests beneath the weight of stone,<br>
            sombre in this melancholy light.</p>
            
            <p>What solitude, what sterile peace. It quells<br>
            that muted life, receding in the distance.<br>
            But always, with beautiful insistence<br>
            the ceaseless human undercurrent swells.</p>
           
            <p>Here &ndash; the crystal clarity &ndash; coherence<br>
            moulded into momentary sound.<br>
            But after &ndash; an echo. It isn&rsquo;t bound<br>
            to him who minted its magnificence.</p>
            <p>&nbsp; 
            </p>
            <p> <strong>Eclogue</strong></p>
            
            <p>As high as heaven&rsquo;s vault<br>
            waving with easy grace<br>
            the branch reaches up to the blue &ndash;<br>
            so high, light can&rsquo;t assault<br>
            this sombre space<br>
            or break its holy solitude.<br>
            A single bird intrudes<br>
            to deepen contemplation <br>
            with a tenuous trill.<br>
            Could other song fulfil<br>
            its own culmination,<br>
            subtle as a ripple where it grows,<br>
            like lonely silence, garnered as it flows?</p>
            
            <p>The rose alone imparts<br>
            its perfect presence now,<br>
            yearning from the branch to the sky.<br>
            Or doubtful, it darts<br>
            into the leafy bough &ndash; <br>
            delicate, adolescent, shy.<br>
            These branches don&rsquo;t deny<br>
            the glory of the spring &ndash;<br>
            bent beneath its crown.<br>
            Not a flower flutters down.<br>
            Tenderly they cling<br>
            resisting for a day<br>
            the impulse and the urges of early May.</p>
            
            
            <p>And if the breezes tremble<br>
            through those slender stems<br>
            with only the slightest push,<br>
            then all the blooms resemble<br>
            a revolt of diadems<br>
            excited, scurrying through the bush,<br>
            rose-tinted whirlwinds, a rush<br>
            of living nymphs in flight<br>
            toward the quiet grove.<br>
            But afterward, the treasure-trove<br>
            reveals them in its shifting light,<br>
            rapt in the tremulous leaves,<br>
            captives of the bough&rsquo;s resilient weave.</p>
        
           
            <p>Among the roses &ndash; shimmering<br>
            beads of dew, serene and grave,<br>
            absorbed in their own magnificence.<br>
            No reflections glimmering<br>
            from the passage of that wave,<br>
            adamant and cruel with tenderness.<br>
            These mystic recesses<br>
            won&rsquo;t unseal their locks.<br>
            They only copy from the sky<br>
            some vibrant flurry passing by<br>
            that doesn&rsquo;t pause to look<br>
            at this unparalleled abundance.<br>
            Empty joy, monotony, redundance.</p>
            
            
            <p>Now the present slumbers.<br>
            Languid hours refine<br>
            into a dream, and settle deep.<br>
            Pure delight. Unencumbered<br>
            with disturbance or desire,<br>
            the indecisive moment is asleep.<br>
            What song is this that leaps &ndash;<br>
            unfolding far away<br>
            from an impure flute?<br>
            With rain that bares the root<br>
            a syncopated melody<br>
            advances through the leaves, and sprays<br>
            the air with sudden streaks of grey.</p>
            
             
            <p>Perhaps that falling sound&rsquo;s<br>
            a revenant.  The stealth<br>
            of echoes, issued from a frozen stream<br>
            in caverns underground<br>
            that liquefies itself<br>
            to complement the quiet boughs with green.<br>
            This music, like a dream,<br>
            resides among the reeds.<br>
            Its echo, murmuring<br>
            from lips whose breathing <br>
            nurtured it, recedes,<br>
            stealing it away, deferring<br>
            to the air that follows after, softly stirring.</p>
             
            
            <p>Air of idyllic trees,<br>
            sweet with primal innocence<br>
            and worthy of divine inhabitation.<br>
            But what cold signs are these,<br>
            rising from the somnolence<br>
            in sudden chilling gusts of agitation?<br>
            A secret incantation<br>
            of distant voices<br>
            threatens on the wind.<br>
            Other springs extend,<br>
            with pitiful insistence,<br>
            a fragrance almost dying<br>
            to the spring that now, among the trees, is sighing.</p>
           
             
            <p>Contentment slips away.<br>
            Refuses &ndash; needs to shun<br>
            the waiting promise of fulfilment.<br>
            Fickle as the changing day,<br>
            where has it gone &ndash; <br>
            indistinct, untaken and impatient?<br>
            Look. Asleep. Unhastened <br>
            in a dream<br>
            of future happiness.<br>
            Here &ndash; confused and motionless<br>
            the shifting, tinted scene,<br>
            where fading lights now cast<br>
            a lingering glow of sadness from the past.</p>
            
            <p>Above the peaceful brook,<br>
            a muted mirror made<br>
            of silver foam, without a sound<br>
            the dusk comes down to look<br>
            for its reflection in the shade.<br>
            It settles where its idle plumes are drowned.<br>
            And as the haze draws round<br>
            this barren weariness,<br>
            another presence grows.<br>
            Absence comes to interpose<br>
            its void into awareness,<br>
            while shadows gather high<br>
            to wipe away the contours of the sky.</p>
             
            <p>Silence. Now the lights<br>
            that warmed the day are lost.<br>
            The world is still. No movement breaks<br>
            the deepening night.<br>
            Gone &ndash; the winds that tossed<br>
            the daylight blue with passing wakes.<br>
            And what invisible wall takes<br>
            shape, and gravely closes in<br>
            around the spirit, sadly clinging?<br>
            Heaven stops its singing.<br>
            The surface of eternity ceases to attend<br>
            to the light, the waiting blooms,<br>
            and oversees the horror<br>
            of the deep nocturnal gloom.</p>
            
            <p>&nbsp;
            </p>
            <p> <strong>Elegy</strong></p>
           
            <p>This garden, braced against the fall<br>
            of light as conquering night moves on,<br>
            breathes unnoticed before the dawn,<br>
            silent and content within its walls.</p>
            
            
            <p>Sloth, night and love: the quiet lair<br>
            appeals beneath a dying glow.<br>
            The splendour of the day burns low<br>
            into a secret, languid atmosphere.</p>
            
             
            <p>And in the pallid lantern-light:<br>
            roses, veins of blue, the half-heard moan<br>
            of a naked form, held there alone,<br>
            tenuous in the arms of night.</p>
             
            
            <p>Roses, tender and warm against the hand<br>
            a sweet urge presses to the vine,<br>
            vessels of burning blue; all of a lifetime<br>
            given to that heartless dream in vain.</p>
           
            
            <p>Alive, or a shade? Or marble reclined<br>
            in immortal repose? Or a faultless presence<br>
            offering its sterile air of indolence,<br>
            as cruel as a shiver in the spine?</p>
             
             
            <p>Slowly the figure seems to rise<br>
            with the fickle breeze. It starts &ndash; <br>
            and a hidden wave expands from heart<br>
            to lip, where it comes to life, and dies.</p>
             
            <p>Ambiguous delight. That grace<br>
            will not bestow itself on any man.<br>
            It passes on, disdainful, through a landscape<br>
            of dreams &ndash; unreal river and unreal space.</p>
            
            <p>Fleeing from a stubborn love,<br>
            it breaks those delicate bonds.<br>
            What's this purity that will not respond<br>
            to the mortal caress it was prisoner of?</p>
             
            <p>Amorous solitude. In idle display<br>
            the youthful body lies &ndash; perfect, slow.<br>
            A melancholy pause. In sudden snow<br>
            the sovereign ardour melts away.</p>
             
            <p>And what can you expect, when all is told?<br>
            Boredom and dregs &ndash; the age-old weariness,<br>
            tears falling hopelessly, the sadness<br>
            of the half-made bed, clumsy and cold.</p>
            
            <p>Ephemeral garden, burning deep<br>
            where the fruit of eternity hung &ndash;<br>
            and the docile lip, at a taste, was stung<br>
            from a vast and indifferent sleep.</p>
             
            <p>Out of that dream where universes swirled,<br>
            a distant sleeping form remains &ndash; <br>
            fertile possibility, but hollow and vain<br>
            in the lonely silence of the world.</p>
            
            <p>This ravenous gnaw of bitterness,<br>
            thistle to the lover&rsquo;s blind affection &ndash;<br>
            is this what clouds the perfection<br>
            of the night, angelic and motionless?</p>
             
            <p>No. With the slow revolution of morn<br>
            a tender light affects the sky with doubt.<br>
            A bird is awake on a hidden bough<br>
            and questions the horizon with a song.</p>
           
            <p>The subtle rumour of the world draws near.<br>
            Beauty comes to birth beneath the dawn.<br>
            And as the constant wave moves on,<br>
            this final trace of sadness disappears.</p>
             
            <p>&nbsp;
            </p>
            <p> <strong>Ode</strong></p>
            
            <p>Sadness now succumbs &ndash; an impure ghost<br>
            that dwindles on the air &ndash; with sombre<br>
            splendid indolence. Slowly it swirls<br>
            toward the distance, and is lost.<br>
            The implacable fire of summer<br>
            sends a tremor through the world.<br>
            And deeply life unfurls<br>
            as  peaceful hours run,<br>
            its glorious morning sun<br>
            a whirlwind of delirious design.<br>
            Out of the light, a brilliant pathway shines.<br>
            But on that glowing path, a purer light impends:<br>
            living, lovely and divine,<br>
            a young god, smiling, descends.</p>
             
            <p>From what uncanny realm above<br>
            does that immortal presence deign to come,<br>
            scion of what empyreal breed?<br>
            Made of marble limbs, it feels and loves.<br>
            Motionless, it quivers in the sun,<br>
            and quickens with the breath of shivering need.<br>
            That figure, conceived<br>
            in naked perfection,<br>
            shimmers with reflections<br>
            that glance across the shadowed light.<br>
            And raising its prodigious weight<br>
            from the depths where it lay in dream,<br>
            a powerful fate<br>
            is born into the world &ndash; a force supreme.</p>
             
            <p>Is this a god? A sudden gesture<br>
            breaks across the stillness of its pose<br>
            with a note of muted melody<br>
            that floats, suspended in the air.<br>
            And while the vivid silence flows,<br>
            it lingers there, a revenant of harmony.<br>
            The god of that epiphany,<br>
            forgotten now, sleeps on.<br>
            Its echo is reborn:<br>
            a man the scattering mists reveal.<br>
            No distance&rsquo;s translucence can conceal<br>
            this human beauty from the sense;<br>
            its form unveils<br>
            a universe of timeless presentiments.</p>
            
            <p>Prodigious, palpitating form,<br>
            a perfect body filled with youthful might,<br>
            the ideal blossoming of human grace.<br>
            It raises a triumphant arm,<br>
            solid, yet agile and light,<br>
            and life opens out beneath the arc it has traced.<br>
            All horror that would dare deface<br>
            with shadows and distortion<br>
            such harmonious proportion<br>
            would only meet defiance.<br>
            This is the spirit of self-reliance,<br>
            hardened steel of pride and mastery;<br>
            every move expresses the alliance<br>
            of dominion and restraint with liberty.</p>
             
            <p>When the beautiful power expands<br>
            in the flesh, in the amorous, thankless coiling<br>
            of the flesh; when the shivering need respires;<br>
            when, in the darkness where the blood demands,<br>
            the tension of love unsprings,<br>
            the voice, with a pulsing urge acquires<br>
            a sense of grace that inspires<br>
            an indolent form.<br>
            Against the breast it notices a warmth<br>
            of other being, a body undefended,<br>
            offering its purity, tenderness extended<br>
            like a dying swan in green and brown<br>
            reflections where the underbrush is blended,<br>
            that lives and sings, and in a frenzy, drowns.</p>
            
            <p>But all of the sorrowful cares<br>
            of love that passion stubbornly claims<br>
            from those who give themselves to another &ndash;<br>
            the tender lament, the countless tears<br>
            of complaint and the shame<br>
            of boredom, hidden from the lover,<br>
            while uncertainty hovers<br>
            in a cloud of constant doubt.<br>
            Only freedom can burn out<br>
            this vehement desire,<br>
            not the ironic pyre<br>
            of those terrible, fleeting glories<br>
            that love, with its smouldering fires,<br>
            discovers in the ashes of its victories.</p>
             
            <p>So on those lips a smile is sealed.<br>
            The light of joy begins to spread<br>
            across that manly face.<br>
            And the ancient sadness yields,<br>
            erasing the secret bitterness it fed,<br>
            while innocent candour takes its place.<br>
            The body&rsquo;s limbs retrace<br>
            their natural disparity<br>
            with beautiful dexterity.<br>
            It ripples through the muscles, tensing slow.<br>
            And then, from the bank above the gentle flow,<br>
            the form and its image unify &ndash;<br>
            a lightning flash of snow<br>
            beneath the light from a towering sky.</p>
            
            <p>Content, asleep beneath the azure gleam,<br>
            the water dreamed and slowly passed,<br>
            perpetual, spun from a secret source.<br>
            But swelling in a tumultuous stream<br>
            of broken foam, it shows at last<br>
            the swimmer in his drifting course.<br>
            That form takes sudden force,<br>
            shining in the silver mist.<br>
            The river laps and twists<br>
            in lucid, rippling swirls<br>
            as the swimmer rides its whirl<br>
            of liquid yearning, elusive diadem.<br>
            And sunlight, splendour of purest pearl,<br>
            like a lover caresses his floating limbs. </p>
             
            <p>Along the stream, a whispering pervades<br>
            the wall of leaves with shifting hues<br>
            and dissipates in verdant mysteries.<br>
            Among the restive woodland shades<br>
            an enigmatic shadow now pursues<br>
            the waning light, as it flees.<br>
            The current cleaves its boundaries<br>
            with quiet water sounds.<br>
            And all the sky rebounds<br>
            the singing of a bird within the trees.<br>
            The naked body rises, listens carefully<br>
            beneath a heavy bough, whose flowers<br>
            weave a complicated tapestry<br>
            of trembling, blinding snow.</p>
            
            <p>Oh new-born god. With dazzling ease<br>
            you turn your careless grace<br>
            toward the twilight in the west.<br>
            And now, the calm that whispers in the trees,<br>
            the splendid charm of summer days,<br>
            evaporates in empty idleness.<br>
            The afternoon, content to rest,<br>
            permits itself to yield<br>
            to lazy sleep; it doesn&rsquo;t feel<br>
            its own serene magnificence.<br>
            Across the gleaming distances<br>
            of night, the happy body seems to fly,<br>
            and leaves this world of substances<br>
            to dream, beneath a canopy of purple sky.</p>

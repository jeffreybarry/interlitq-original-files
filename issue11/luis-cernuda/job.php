<p> <strong>Homenaje</strong></p>
<p>
  Ni mirto ni laurel. Fatal extiende<BR>
  Su frontera insaciable el vasto muro<BR>
  Por la tiniebla f&uacute;nebre. En lo oscuro,<BR>
  Todo vibrante, un claro son asciende.</p>
<p>
  
  C&aacute;lida voz extinta, sin la pluma<BR>
  Que opacamente blanca la vest&iacute;a,<BR>
  R&aacute;fagas de su antigua melod&iacute;a<BR>
  Levanta arrebatada entre la bruma.</p>
<p>
  
  Es un rumor cel&aacute;ndose suave;<BR>
  Tras una gloria triste, quiere, anhela.<BR>
  Con su acento armonioso se desvela<BR>
  Ese silencio s&oacute;lido tan grave.</p>
<p>
  
  El tiempo, duramente acumulando<BR>
  Olvido hacia el cantor, no lo aniquila;<BR>
  Siempre joven su voz, late y oscila,<BR>
  Al mundo de los hombres va cantando.</p>
<p>
 
  Mas el vuelo mortal tan dulce &iquest;ad&oacute;nde<BR>
  Perdidamente huy&oacute;? Deshecho br&iacute;o,<BR>
  El m&aacute;rmol absoluto en un sombr&iacute;o<BR>
  Reposo melanc&oacute;lico lo esconde.</p>
<p>
  
  Qu&eacute; paz est&eacute;ril, solitaria, llena<BR>
  Aquel vivir pasado, en lontananza,<BR>
  Aunque, trabajo bello, con pujanza<BR>
  A&uacute;n surta esa perenne, humana vena.</p>
<p>
  
  Toda n&iacute;tida aqu&iacute;, vivaz perdura<BR>
  En un son que es ahora transparente.<BR>
  Pero un eco, tan solo; ya no siente<BR>
  Quien le infundi&oacute; tan l&uacute;cida hermosura.<BR>
</p>
<p><BR>
</p>
<p>
  <strong>&Eacute;gloga</strong></p>
  
  
  Tal alta, s&iacute;, tan alta<BR>
  En revuelo sin br&iacute;o,<BR>
  La rama el cielo prometido anhela,<BR>
  Que ni la luz asalta<BR>
  Este espacio sombr&iacute;o<BR>
  Ni su divina soledad desvela.<BR>
  Hasta el p&aacute;jaro cela<BR>
  Al absorto reposo<BR>
  Su delgada armon&iacute;a.<BR>
  &iquest;Qu&eacute; trino colmar&iacute;a,<BR>
  En irisado rizo prodigioso<BR>
  Aguz&aacute;ndose lento,<BR>

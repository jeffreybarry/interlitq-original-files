            &nbsp;<br>
            <p>This group of poems forms the second section of Cernuda&rsquo;s retrospective edition of his poetic output, <em>La realidad y el deseo</em> [Reality and Desire]. As did Walt Whitman with <em>Leaves of Grass</em>, Cernuda gradually accumulated <em>La realidad y el deseo</em> through successive editions, constantly revising, reordering and editing his own work. The final edition of the book, on which the versions translated here are based, is considered to be one of the masterpieces of modern Spanish poetry.</p>
            <p>These four poems are rather astonishing for their marriage of highly complex structure with profound (if somewhat questionable) philosophical content. This is not the kind of work that avant-garde poets of the late 1920s were normally engaged in. And in fact, shortly afterwards Cernuda turned toward much more open free-verse forms, using a conversational tone and surrealistic imagery. But these poems remain as eloquent witnesses to one poet&rsquo;s extraordinary technical skills.</p>
            <p>This poetic sequence offers a fascinating challenge for translation. Octavio Paz wrote that Cernuda&rsquo;s &ldquo;early poems seem [&hellip;] to be an exercise whose perfection does not exclude affectation, a certain manneredness from which he never entirely freed himself.&rdquo; This balanced assessment might be read as an assignment-sheet for the translator. The formal perfection here is an ever-receding goalpost to strive for. The abstract thought is a difficult prey to capture. And the mannered affectation is a subtle visitor to flirt with.</p>
            

              we arise, adapt and change<br>
              while beyond our earthly planet<br>
              our intensive brainwaves range -<br>
              Do we trespass? &ndash; who&rsquo;ll forgive us?<br>
              Can we forgive our trespassers:<br>
              those who would confine, prevent us<br>
              listening to the messengers?</p>
               
              <p>This our mantra<br>
              our chant our dance<br>
              our gathering, feasting<br>
              our wisdom-field<br>

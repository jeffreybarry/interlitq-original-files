              <p><strong>Prelude</strong>:</p>
             
              <p>This is our mantra<br>
              <em>patter</em> noster<br>
              words our voice said<br>
              phrases repeated<br>
              spoken together<br>
              older younger<br>
              matins compline<br>
              now forgotten<br>
              as meaningless jargon<br>
              <em>thy will be done</em><br>

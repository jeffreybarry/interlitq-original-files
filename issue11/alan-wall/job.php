            <p><strong>The Doctor</strong></p>
            
            <p><strong>1</strong></p>
            
            <p>Doctor Placebo was asked <br>
            If he could vouchsafe<br>
            One true thing at least.</p>
           
            <p>I have abandoned certainties, he said<br>
            Or perhaps they have abandoned me.<br>
            The speed of light remains constant<br>
            In a vacuum, of that I am reasonably certain<br>
            Though disproof is still possible<br>
            At the unreached edges of the universe.</p>
          
            <p>And there&rsquo;s mortality, of course.</p>
           
            <p>Charles P&eacute;guy remarked in <em>Clio</em><br>
            That History constitutes dark reflections<br>
            Upon falling things.<br>
            Shortly afterwards History assassinated him<br>
            For this impertinence.</p>
            <p><BR>
            </p>
            <p><BR>
            </p>
            
            <p><strong>DIY</strong></p>
           
            <p>Placebo operated on himself<br>
            Several times.</p>
          
            <p>These manoeuvres at least<br>
            Alerted him to the heart&rsquo;s attentiveness<br>
            When probed.</p>
            
            <p>Its red alert on encountering<br>
            A foreign body.</p>
           
            <p>He also acknowledged at last<br>
            (As he administered rigorous post-operative care) <br>
            The value of accurate stitching,<br>
            An art he had once thought <br>
            Merely womanish. </p>
            <p><BR>
            </p>
            <p><BR>
            </p>
            <p><strong>Placebo&rsquo;s Relics</strong></p>
           
            <p>Medieval remnants of the true cross<br>
            Marilyn&rsquo;s dress</p>
            
            <p>A fragment of James Dean&rsquo;s last car<br>
            Elvis&rsquo;s guitar</p>
           
            <p>An image, however contrived, of the Cottingley Fairies<br>
            The Hitler Diaries</p>
            
            <p>A first edition of <em>Sigmund Freud: Collected Works</em><br>
            Pip and Joe Gargery&rsquo;s larks</p>
           

            <p><strong>Feeling Cowed By Language In The Night</strong></p>
             
            <p>Everything types its obscure message&mdash;cat claws<br>
            ripping up the welcome ivy on the hemp doormat<br>
            so the good intentions of visitors fray, moth wings<br>
            unfolding neat inverted V&rsquo;s to bat at the lit porch<br>
            lamp; even the dim orb weaver is a cartwheeling<br>
            X in one dark corner, its spinnerets pasting gnats<br>
            into fat white O&rsquo;s. I&rsquo;m dumb as rock under flowered<br>
            sheets. Particulars unspell me. Even the cotton eye<br>
            of the blossoms&rsquo; carmine dye washes out, a faded<br>
            sun of the unsaid. One hundred count threads snip<br>
            loose in my head, each a little tail of the unwritten.</p>
<p>&nbsp;</p>

<p><strong>O&rsquo;Keeffe&rsquo;s Jack-in-the-Pulpit II</strong></p>

<p>This jack shouts through a swollen microphone.<br>
Its hitchhiking spadix nabs us with a bruised thumb&mdash;</p>

<p>(&ldquo;In the pulpit&rdquo; means the minister&rsquo;s collar <br>
is a flare of undone. It shifted gears somewhere </p>

<p>in the before-universe of blank artist&rsquo;s mind, and blared <br>
forth like a purple fantasy of Father Mozart&mdash;the hood </p>

<p>became a windblown noise, a trumpet of hemorrhaged <br>
maroon.) It wears a bandage below the waist, </p>

<p>like any stunned heavyweight fighting past prime. <br>
&mdash;Bloody thumb. Passion trips us.</p>

<p>The jack hot-wires that message home&mdash;<br>
though the jolt is offset with lambent greens&mdash;</p>

<p>with a thousand jittery bells of life-force<br>
blasting from all its bright seams.</p>
<p>&nbsp;</p>
<p>&nbsp;</p>
 
<p><strong>Rembrandt&rsquo;s Late Self-Portraits</strong></p>

<p>Their lit-from-within moons god <br>
only knows what terrors&mdash;</p>

<p>that polished scorch<br>
mark gloom, that burned-up <br>
letters of hope char</p>
 
<p>in the lustrous dim.<br>
They shine like Cro-Magnon <br>
skulls out of the frontal <br>
past. They pulse with </p>
 
<p>the energy of the soul&rsquo;s <br>
primal blast.</p>
 

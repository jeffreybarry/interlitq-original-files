            <p> There is no more shadow. A single teardrop in which a world trembles.</p>
            
            <p ALIGN="CENTER">*</p>
           
            <p> A moment when the mind is draped in stupor. I am back looking for traces, at the top edge of the rock. A moment too acute for words to come out unscathed.</p>
           
            <p ALIGN="CENTER">*</p>
           
            <p> Winter cleans up. Its rudeness better reveals the constant features of the landscape. An eternal vertigo legible in the slumber of the vineyard.</p>
            
            <p ALIGN="CENTER">*</p>
           
            <p> Those who do not forget the incessant mutilation inflected on this earth more willingly pay tribute to the scathing nudity of the sky. Up there the sun turns its ring on his finger and waits to bury the dead, their coins liquefied in their throats. The blue grass whispers another state of being. The path is not closed.</p>
            
            <p ALIGN="CENTER">*</p>
           
            <p> The sky creates its own uneasy feelings, but we are the ones in whom it injects them.</p>
            
            <p ALIGN="CENTER">*</p>
           
            <p> Here it is space that matters. The roads are bare, livelier than nests.</p>
           
            <p ALIGN="CENTER">*</p>
           
            <p> A landscape for effacing wrinkles.</p>
           
            <p ALIGN="CENTER">*</p>
            
            <p> The mystery of foliage in the wind: like a riot that we breathe with our blood.</p>
            
            <p ALIGN="CENTER">*</p>
            
            <p> The words that strike you have already traveled and will continue to travel over a long dusty trail. What could they better leave you with than thirst?</p>
            
            <p ALIGN="CENTER">*</p>
           
            <p> You await an answer. You obtain only an icy reprieve, yet, by hugging it tightly, it nonetheless sometimes turns into a spring-like little puddle. Look: the shadow of the cloud or the bird has come to inscribe itself in it for an instant. This fleeting mark is a fine answer. The suspicion that the scale of values is rotten.</p>
           
            <p ALIGN="CENTER">*</p>
            
            <p> The yes is the pit of the fruit. The pulp is the messenger of the yes, like a word whispered when everything seems at its lowest ebb.</p>
           
            <p ALIGN="CENTER">*</p>
           
            <p> They shouldn&rsquo;t pass beneath this light with such thin lips.</p>
            
            <p ALIGN="CENTER">*</p>
           
            <p> Light has its storms, but the sky no wounds.</p>
            
            <p ALIGN="CENTER">*</p>
            
            <p> The slumber of the vineyard is a mere leap into the space where bunches of  golden grapes already hang from the vines.</p>
            
            <p ALIGN="CENTER">*</p>
            
            <p> This abyss is likeable when you can find lodgings in it.</p>
            
            <p ALIGN="CENTER">*</p>
            
            <p> What voice is always available, even at the end of the rope?</p>
            
            <p ALIGN="CENTER">*</p>
           
            <p> On the ridge, among the blades of wild grass</SPAN>.</p>
            
            <p ALIGN="CENTER">*</p>
            
            <p> In order to find traces of God, you first must deny them.</p>
            
            <p ALIGN="CENTER">*</p>
           
            <p> Only in the finite does the hand become infinite.</p>
           
            <p ALIGN="CENTER">*</p>
           
            <p> Watch the fire. Its barren peak. Forget its fascinating apparitions. Experience this barren fire, which relishes your poverty.</p>
           
            <p ALIGN="CENTER">*</p>
           
            <p>The air so mild, and smelling of trampled wild mint. A temple of such small dimensions that there is no front door. Whoever is inside contemplates in silence, nurturing himself on his own light. Whoever is outside wants to drag him out of there, show him around. And his name? An exotic name that no one remembers. But what&rsquo;s the use of a name? Since he is inside. And without him, without this unfindable door, who would come by? There would be only ashes.</p>
            <p><BR>

            <p><em>Fragments</em></p>
            
            <p>Il n'y a plus d'ombre. Une seule larme o&ugrave; tremble un monde.</p>
            
            <p>Le moment o&ugrave; l'esprit s'habille de stupeur. Moi, rendu aux traces, &agrave; l'ar&ecirc;te de la pierre. Moment trop aiguis&eacute; pour que la parole en sorte indemne.</p>
            
            <p>L'hiver nettoie, sa rudesse laisse mieux appara&icirc;tre les constantes du paysage. Ce vertige &eacute;ternel lisible dans le sommeil de la vigne.</p>
            
            <p>Ceux qui n'oublient pas les incessantes mutilations inflig&eacute;es &agrave; cette terre rendent plus volontiers hommage &agrave; la cinglante nudit&eacute; du ciel. L&agrave;-haut le soleil tourne sa bague et attend d'inhumer les morts, leur monnaie liqu&eacute;fi&eacute;e dans la gorge. L'herbe bleue murmure une autre condition. Le chemin n'est pas ferm&eacute;.</p>
           
            <p>Le ciel cr&eacute;e son propre malaise mais c'est &agrave; nous qu'il l'injecte.</p>
            
            <p>Ici c'est d'espace qu'il s'agit. Les routes sont &agrave; nu. Plus vivantes que des nids.</p>
            
            <p>Un paysage pour effacer les rides.</p>
            
            <p>Le myst&egrave;re du feuillage dans le vent : comme une &eacute;meute que nous respirons avec notre sang.</p>
           
            <p>La parole qui te frappe a d&eacute;j&agrave; parcouru et va parcourir encore un long chemin de poussi&egrave;re. Que pourrait-elle mieux t'abandonner que la soif?</p>
            
            <p>On attend une r&eacute;ponse. On n'obtient qu'un sursis glac&eacute;, mais, de le serrer dans ses bras, il arrive pourtant qu'il se transforme en une petite flaque printani&egrave;re. Vois : l'ombre du nuage ou de l'oiseau vient s'y inscrire un instant. Belle r&eacute;ponse que cette tache br&egrave;ve. Le soup&ccedil;on que l'&eacute;chelle des valeurs est pourrie.</p>
           
            <p>Le oui est le noyau. La chair du fruit est messag&egrave;re du oui &mdash; comme un mot murmur&eacute; au creux de la vague. </p>
           
            <p>Ils ne devraient pas passer sous cette lumi&egrave;re avec des l&egrave;vres si minces.</p>
            
            <p>La lumi&egrave;re a ses orages, le ciel n'a pas de plaies.</p>
           
            <p>Le sommeil de la vigne n'est qu'un bond dans l'espace o&ugrave; pendent d&eacute;j&agrave; des grappes d'or.</p>
           
            <p>Ce gouffre est aimable quand on peut s'y loger.</p>
           
            <p>Qu'elle voix toujours disponible, m&ecirc;me au bout de la corde?</p>
            
            <p>Sur la cr&ecirc;te, parmi les herbes.</p>
            
            <p>Pour retrouver la trace de Dieu, il faut, d&eacute;j&agrave;, nier les traces.</p>
            
            <p>Ce n'est que dans le fini que la main s'infinise.</p>
           
            <p>Voir le feu. Son sommet d&eacute;sertique. Oublier ses fascinantes apparitions. Vivre ce feu d&eacute;sertique, si gourmand de ta pauvret&eacute;.</p>
            
            <p>Tant de douceur et l'odeur de la menthe sauvage foul&eacute;e. Un temple aux dimensions si r&eacute;duites qu'il n'y a pas de porte pour entrer. Qui est dedans contemple en silence, se nourrit de sa propre lumi&egrave;re. Qui est dehors veut l'arracher de l&agrave;, le pr&eacute;senter. Mais le nom? La sauvagerie du nom que personne ne retient. Et &agrave; quoi bon le nom? Puisqu'il est dedans. Et sans lui, sans cette porte introuvable, qui passerait? Il n'y aurait que cendre.</p>
           


            &nbsp;<br>
            <p>The Western Front stretched nearly 500 miles from Switzerland to the Channel coast of Belgium. Millions of soldiers were entrenched along both sides with an area of &lsquo;No Man&rsquo;s Land&rsquo;, sometimes as narrow as thirty yards, separating the armies. Behind the front line, that was protected with the newly employed military technology of &lsquo;barbed wire&rsquo;, were backup trenches where reserve forces could be held in readiness for combat; dugouts were excavated in the trench walls that provided cramped space for shelter and sleep. And behind the trenches were supply depots, field hospitals and military headquarters. Intensive fighting did not occur everywhere at the same time, but the threat of death from poison gas, sniper fire, heavy artillery or unexpected raiding parties, was ever present. Small stretches of ground were lost and retaken repeatedly at terrible cost, while the remaining soldiers continued their daily life against the bizarre &lsquo;normality&rsquo; of dead bodies, mud, rats, lice and boredom. Into this Dantean landscape was projected Private Isaac Rosenberg, A. Coy 11th(S) Batt. K.O.R.L, British Expeditionary Force, who, not surprisingly, had lost all his socks before leaving England.</p>
           
            <p>One thing that enabled soldiers to preserve a sense of connection with the world they had left behind was an efficient postal service. On average letters took no more than two days to get between the &lsquo;Home Front&rsquo; and the Western Front. Rosenberg was able to communicate with family, friends and patrons. He was able to receive parcels and to submit work to Harriet Monroe in Chicago. Albeit the censor forbade him to send poems home because he couldn&rsquo;t &ldquo;be bothered with going through such rubbish&rdquo;, Rosenberg took no notice. There are eighty published letters that date from the twenty-two months he spent in the trenches, and there were certainly others no longer extant. His holographs written on scraps of muddied paper arrived home regularly where his sister Annie lovingly typed them out and returned them for further editing. Shortly after arriving in France he wrote Miss Seaton, &ldquo;I&rsquo;ve been in trouble with bad heels; you can&rsquo;t have the slightest conception of what such an apparently trivial thing means. We&rsquo;ve had shells bursting two yards off, bullets whizzing all over the show, but all you are aware of is the agony of your heels&hellip;&rdquo;</p>
            
            <p>Rosenberg&rsquo;s mother sent socks and underwear, Miss Seaton sent novels, Schiff sent newspapers and books, even Mrs Cohen unexpectedly sent him a volume of poems she had herself published. In a physical environment fraught with danger, it is no exaggeration to suggest that Rosenberg owed his psychological survival to the power of poetry. But he paid for his dreamy preoccupation, being frequently punished for unsoldierly conduct, on one occasion for forgetting to put on his gas mask. His chances of living nearly two years would have been severely diminished, were it not for the intervention of his family, who asked Marsh to use his influence in order to get Isaac posted away from the front line.</p>
           
            <p>Plato objected to poetry on the grounds that it undermined young men&rsquo;s courage by giving expression to emotions, to &ldquo;weakness&rdquo;. Percy Bysshe Shelley, however, believed that poetry had a transformative effect, an enabling function that in a secret alchemy allowed &ldquo;the poisonous waters that flow from death through life&rdquo; to be converted into &ldquo;potable gold&rdquo;. Rosenberg&rsquo;s attitude was nearer to Shelley&rsquo;s. He saw poems as vehicles for the modulation and expression of everyday experience. Poetry was a metamorphic process that forged new forms from the nexus of inner and outer worlds. His commitment to his art surmounted all other concerns. &ldquo;I will not leave a corner of my consciousness covered up&rdquo;, he wrote to Laurence Binyon, &ldquo;but saturate myself with the strange and extraordinary new conditions of this life, and it will all refine itself into poetry later on&rdquo;. Painful experience could be tolerated, even valued, as the material out of which poems were made. &lsquo;Break of Day in the Trenches&rsquo;, was written soon after he arrived in France and &lsquo;Dead Man&rsquo;s Dump&rsquo;, perhaps the most harrowing poem to emerge from the war, derived from his duties as a Royal Engineer almost a year later. </p>
           
            <p>Repair of barbed wire defences was carried out under cover of night. It involved moving mule-carts packed with wire coils and metal stakes up to the front line, and on into no-man&rsquo;s-land. One macabre consequence was the likelihood of running over dead bodies. &ldquo;I wrote a poem about some dead Germans lying in a sunken road where we dumped our wire&rdquo;, he told Bottomley, &ldquo;I have asked my sister to send it on to you, though I think it commonplace&rdquo;. In fact Rosenberg had made a truly awesome poem. Although there are nods to Keats and recurrences of his favourite words &ndash; &ldquo;sceptres&rdquo;, &ldquo;veins&rdquo; etc. &ndash; his distinctive language transcends them. Laden with poetic tropes in a chaos of arresting images, crucifixion allusions, empathy with the enemy, end-rhyme, internal rhyme, visual rhyme, anaphoric repetitions and broken rhythms, the poem graphically conveys the human devastation in which he was mired. The first two stanzas lead us into a work of unprecedented candour &ndash; no other poet had given such an arresting close-up before:</p>
            <blockquote>
            <p> The plunging limbers over the shattered track<br>
             Racketed with their rusty freight,<br>
             Stuck out like many crowns of thorns,<br>
             And the rusty stakes like sceptres old<br>
             To stay the flood of brutish men<br>
             Upon our brothers dear. </p>
            
            <p> The wheels lurched over sprawled dead<br>
             But pained them not, though their bones crunched,<br>
             Their shut mouths made no moan,<br>
             They lie there huddled, friend and foeman,<br>
             Man born of man, and born of woman,<br>
             And shells go crying over them<br>
             From night till night and now.</p>
            </blockquote>
            <p>Rosenberg portrays the earth as waiting impatiently to receive dead souls, their containing bodies &ndash; strewn on the ground like empty sacks. Lines like:</p>
            <blockquote>
            <p>Somewhere they must have gone,<br>
            And flung on your hard back<br>
            Is their souls&rsquo; sack,<br>
            Emptied of God-ancestralled essences.<br>
            Who hurled them out? Who hurled?&rsquo; <br>
             (stanza 4)</p>
            </blockquote>
            <p>and:</p>
            <blockquote>
            <p>His shook shoulders slipped their load,<br>
            But when they bent to look again<br>
            The drowning soul was sunk too deep <br>
            For human tenderness <br>
             (stanza 8) <br>
            </blockquote>
            <p>compare with the best of Gerard Manley Hopkins, although it is unlikely that Rosenberg had seen his work.</p>
            
            <p>In Rosenberg&rsquo;s finely crafted poem &lsquo;Returning, We Hear The Larks&rsquo;, the relief at being still alive at the end of a night&rsquo;s work in no-man&rsquo;s land is tempered by the knowledge that arriving back at the trenches offers only relative safety. But is not, as the title might suggest, a romantic celebration of lark song. Unlike Shelley&rsquo;s skylark whose voice is:</p>
            <blockquote>
            <p>Like a high-born maiden<br>
             In a palace-tower,<br>
            Soothing her love-laden<br>
             Soul in secret hour<br>
             With music sweet as love, which over-<br>
             flows her bower </p>
            </blockquote>
            <p>Rosenberg&rsquo;s bird &ldquo;drops&rdquo; its song:</p>
            <blockquote>
            <p> Like a blind man&rsquo;s dreams on the sand<br>
             By dangerous tides,<br>
             Like a girl&rsquo;s dark hair for she dreams no ruin lies there,<br>
             Or her kisses where a serpent hides.</p>
            </blockquote>
            <p>In a thoroughly modernist and strikingly original treatment of the subject, he has taken the objective danger in his environment and fused it with the deeply felt internal danger that derives from his dread of woman. But there is an ambiguity, the girl herself may be equally at risk, or at least is exonerated from blame, for she is unaware of what her dark hair conceals, &ldquo;dreams no ruin lies there&rdquo;. </p>
           
            <p>The theme is also elaborated in &lsquo;Daughters of War&rsquo; of which there are seven different versions. Rosenberg considered it his best poem. He worked on it from autumn 1916 and completed the final draft the following year. It deals with the two principle preoccupations of most writers and probably all soldiers &ndash; sex and death. Once again, in line with his ambivalent bonding to women, he constructs a mythical scenario in which immortal Amazonian maidens force the souls out of dead male bodies in order to take them for lovers: </p>
            <blockquote>
            <p>Even these must leap to the love heat of these maidens<br>
            From the flame of terrene days<br>
            Leaving grey ashes to the wind&mdash;to the wind.</p>
            </blockquote>
            <p>&ldquo;The end&rdquo;, he wrote Marsh in June 1917, &ldquo;is an attempt to imagine the severance of all human relationship and the fading away of human love&rdquo;. No doubt it was a way of using art to come to terms with his situation. Personifying a sexualised soul engaged with an Amazon in some kind of sensual afterlife, &lsquo;both spiritual and voluptuous at the same time&rsquo;, was a strange yet comforting phantasy. But Rosenberg certainly didn&rsquo;t believe in it. In reality he was more inclined to Lucretius&rsquo;s views, written around 50BC, &ldquo;That in true death there is no second self / Alive and able to sorrow for self destroyed&rdquo;. R.C.Trevelyan had sent him a copy of his translation of the Roman poet, to which Rosenberg responded, &ldquo;Hamlet&rsquo;s enquiring nature so mixed with theology, superstition, penetration, may be more human and general&mdash;But Lucretius as a mood, definite, is fine, proud philosophy. I can say no more than that I got deep pleasure from it&rdquo;.</p>
            
            <p>On 14th September 1917, having had a bath behind the lines and been issued with a clean uniform, Rosenberg set off for a period of ten days leave in London. He was keen to be reunited with family and friends, hoped to meet Bottomley and Trevelyan, with whom he had only ever corresponded, and wanted to complete &lsquo;The Unicorn&rsquo;, an extraordinary verse play about a decaying race who had never seen women. Animals take the place of women, he explained to Bottomley, but the race yearns for continuity. The plot that could only have been devised by a sex-starved soldier would end with &ldquo;a host of blacks on horses, like centaurs and buffaloes&rdquo;, each one clasping a woman, rushing up and sweeping Lilith on to the Chief&rsquo;s Unicorn; after which they would all ride away into the distance. The fantastical storyline, however, conceals a profound struggle on Rosenberg&rsquo;s part to reconcile war experience with belief in the redemptive nature of beauty, sexual violence with sexual love:</p>
            <blockquote>
            <p>LILITH. <br>
            I think there is more sorrow in the world<br>
            Than man can bear.</p>
            
            <p>NUBIAN.<br>
            None can exceed their limit, lady.<br>
            You either bear or break.</p>
            
            <p>LILITH.<br>
            Can one choose to break? To bear,<br>
            To wearily bear in misery.<br>
            Beauty is this corroding malady.</p>
            
            <p>NUBIAN.<br>
            Beauty is a great paradox&mdash;<br>
            Music&rsquo;s secret soul creeping about the senses<br>
            To wrestle with man&rsquo;s coarser nature.<br>
            It is hard when beauty loses.</p>
            </blockquote>
            <p>But the abrupt change of environment was disorientating. Rosenberg found himself restless, unable to read or write and bewildered by the prospect of fitting so many things into such a brief respite.  He had his photograph taken in a studio together with his brother Elkon who was also on leave from the army. Bottomley and Trevelyan were away and Schiff was out when he called. None the less he continued to make a round of his old haunts, the Slade, the Caf&eacute; Royal, and managed to meet up with Marsh. He went to the theatre and may have seen Annetta and Sonia. He was on a high, so much so that his withdrawn character appeared radically changed. Leftwich recorded in his diary:</p>
            
            <blockquote>
            I was walking along the road when I heard someone running behind me [&hellip;] it was Rosenberg in uniform. He had been on a bus going home from the station to see his mother and he had caught sight of me and had jumped off [&hellip;] He was more boisterously happy than I had ever known him before, and he was noisily indignant because he had heard that some people had been saying that he hated the army and wanted to wangle his way out. It was not true, he clamoured. He liked the life and the boys, and he had to fight.
            </blockquote>
            <p>Soon after returning to the front, however, Rosenberg caught the flu. His condition was severe enough to warrant hospitalisation for a period of two months, during which time his regiment was engaged in attacking German defences in the battle of Cambrai, with heavy losses. Any luck he felt at having &ldquo;got out of this late stunt&rdquo;, was to be short lived. By January 1918 he was back in the wintry trenches with a broken spirit. &ldquo;I seem powerless to compel my will to any direction&rdquo;, he wrote Miss Seaton, &ldquo;all I do is without energy and interest&rdquo;. Three weeks later he continued, &ldquo;I do not feel that I have much to say, but I know that unless I write now it will be a long time before you hear from me again&rdquo;.</p>
            
            <p>In the early hours of April 1st after a period of heavy fighting, he was on his way back to the reserve trenches when he answered a call for volunteers to return to the front line, and was killed in close combat. Ten years later, the Imperial War Graves Commission located an unmarked grave near Fampoux, in which the remains of several soldiers on the same burial list as Rosenberg were identified. In October 1928 they were reinterred in the Bailleul Road East Cemetery, St Laurent-Blangy, near Arras<strong>. </strong> A stone bearing Rosenberg&rsquo;s name underneath a Star of David was erected. The words &lsquo;Artist and Poet&rsquo; also appear, for which extra engraving the Commission charged his family three shillings and threepence. </p>


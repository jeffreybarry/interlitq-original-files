            <p LANG="en-US">A tender person who has militarized his tenderness. One quickly becomes a field marshal while playing this game. And one wins for all eternity the impression of being &ldquo;hard to get along with.&rdquo;</p>
           
            <p LANG="en-US">The most obvious thing is forgetting. Every day we need to go back down the same road leading to our limits.</p>
           
            <p LANG="en-US">Sometimes in the evening, I feel like giving our dear Lord a ring on the telephone. I&rsquo;m serious.</p>
          
            <p><SPAN LANG="en-US">Writers whom we get to know after reading their oeuvre. The other writers. No connection between them. We never manage to recover. All the more so in that they are our elders. Between young writers, the </SPAN><SPAN LANG="en-US"><em>oeuvre</em></SPAN><SPAN LANG="en-US"> does not exist.</SPAN></p>
         
            <p LANG="en-US">What is annoying about literary criticism is that it judges something that cannot change.</p>
          
            <p LANG="en-US">Some human beings &ldquo;carry more weight&rdquo; than others. Good, even excellent, writers exist. So much the better for them. Others could not be excellent writers if they tried, yet they are likely to shake up your mental monolith by means of twenty difficult pages. Poetically. See Heidegger.</p>
          
            <p LANG="en-US">Writing letters is dangerous. You write affectionate messages, or even worse, to individuals whose presence you would not be able to bear. No hypocrisy is involved. You have affection for them, at a remove. Yet if they follow up on your epistolary effusions and announce their arrival, you grow pale. This is otherwise only one of a thousand subtleties, as big as roof beams, which turn our relationships with others into a series of fevers and chills. You need a strong pair of lungs.</p>
          
            <p LANG="en-US">Writing means venturing beyond one&rsquo;s capabilities. Whence lying and ethics. No man can write without laying bare his possibilities, his limitations. The man feels so slight that he finds respite only by exaggerating this slightness. He thereby sometimes attains art which, albeit far from ridding him of his misery, emits a fragrance. Beauty exists, but it is not a form: it is a sensation.</p>
          
            <p LANG="en-US">Write while it&rsquo;s hot.</p>
          
            <p LANG="en-US">Mozart. How the note is attacked. Above. Diction. Energy. Stendhal.</p>
            <p LANG="en-US">Man has no prestige, and this is what makes him wish for a God who would remember his great deeds, which other human beings are incapable of retaining. You perform some mighty feat in the neighborhood caf&eacute;. You enter the one next door. There they treat you as they know you. This is what explains solitude.</p>
           
            <p LANG="en-US">I love, love not, can love only God. But human beings will know nothing of this. (Nor God.)</p>
           
            <p LANG="en-US">What worries the cat is motionlessness.</p>
           
            <p LANG="en-US">I get white thoughts into my head.</p>
           
            <p LANG="en-US">It would be good if a journal could be written in the same way that Picasso paints. A distorting journal that would show artistry without losing sight of the obvious martyrdom that results from our being our own ghostwriter, especially in matters of sensibility, which rules us, decrees us, drives us.</p>
            
            <p LANG="en-US">I make no perceptible difference between friendship and love. Both have made me suffer. The physical difference is that friendship makes us suffer like a toothache that only half declares its existence and whose pain remains bearable. As for love, I no longer remember. </p>
            
            <p><SPAN LANG="en-US">Living in the provinces. You cannot imagine the extent to which receiving news </SPAN><SPAN LANG="en-US"><em>too late</em></SPAN><SPAN LANG="en-US"> nullifies the seriousness of events. The extent to which we realize how helpless we are in regard to their evilness. But man is a province incomparably further away than any exile.</SPAN></p>
           
            <p LANG="en-US">A merry diary is unimaginable. When a human being bends over himself, peers into his immediate past, he catches only the disastrous fish.</p>
           

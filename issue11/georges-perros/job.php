            <p>L'art de la photographie offre un beau symbole. On &laquo;&nbsp;prend&nbsp;&raquo; le paysage, ou le ph&eacute;nom&egrave;ne, ou le visage. Et l'on d&eacute;veloppe. Mais ce qui a eu besoin de lumi&egrave;re, d'exposition, ne pourra se &laquo;&nbsp;rendre&nbsp;&raquo; que dans l'obscurit&eacute;.</p>
          
            <p>Rien de plus naturel que de vouloir &ecirc;tre aim&eacute; pour soi-m&ecirc;me. Et rien de plus sot, car soi-m&ecirc;me n'existe pas. L'amour est toujours approximatif.</p>
           
            <p>Po&egrave;me. Un homme est mourant. MOURANT. On le transporte &agrave; la clinique. On le sauve. Le po&egrave;me, c'est l'op&eacute;ration.</p>
            
            <p>Pour vivre, il faut se pencher, s'appuyer sur une partie de ce qui nous traverse perp&eacute;tuellement. Et qui nous caract&eacute;rise. Pourquoi ceci plut&ocirc;t que cela ? Pourquoi ne pas &ecirc;tre, et continuer &agrave; &ecirc;tre b&ecirc;tes, puisque, par moment, nous le sommes ? Nous donnons raison, c'est-&agrave;-dire nous souffrons ce qui nous para&icirc;t ad&eacute;quat &agrave; notre nature, l'inconnue par excellence. Et qui finit par creuser une orni&egrave;re dans ce chaos. Donc, finalement, je m'appellerai G.P. comme devant. Donc vivre est inutile puisqu'il s'agit de penser ce qui est trouv&eacute;.</p>
          
            <p>Il est des individus qui impliquent fatalement Dieu, sans trop le nommer, mais dont l'existence est terriblement ang&eacute;lique et comme marqu&eacute;e. Je pense aux po&egrave;tes. En particulier &agrave; Keats, &agrave; Rilke, &agrave; Rimbaud, &agrave; Baudelaire. Ce qu'ils font &laquo;&nbsp;ici&nbsp;&raquo; ne peut &ecirc;tre un crit&egrave;re. Eux-m&ecirc;mes ignorent ce qui les pousse, ce qui les fait souffrir et vivre aussi <em>mal</em>. Ces gens-l&agrave; font le d&eacute;sespoir des chr&eacute;tiens temp&eacute;r&eacute;s, car ils ont l'air de jouer les grands r&ocirc;les de notre trag&eacute;die sans se donner aucun mal pour &laquo;&nbsp;intriguer&nbsp;&raquo;. Plac&eacute;s gagnants, mais quel curieux privil&egrave;ge, ils ne sont pas croyants, mais crus.</p>
            
            <p>&Agrave; table. Personne ne mange de moules. Ma m&egrave;re d&eacute;clare qu'elle n'en ach&egrave;tera plus jamais. Je me sens alors pris d'une immense tendresse pour les moules, et plus particuli&egrave;rement pour celles-ci. Revivant l'achat, le retour &agrave; la maison, leur pr&eacute;paration, ce &laquo;&nbsp;nevermore&nbsp;&raquo; me glace d'horreur. &laquo;&nbsp;Donne-m'en tout de m&ecirc;me un peu&nbsp;&raquo;, dis-je &agrave; ma m&egrave;re.</p>
           
            <p>Ce qu'on est, c'est ce qu'on pense involontairement, et qui nous guide au moment o&ugrave; nous nous croyions perdus. Pens&eacute;es-oiseaux.</p>
           
            <p>Pour peu qu'on soit un rien distrait, la journ&eacute;e passe comme une lettre &agrave; la poste. Et nous nous retrouvons dans la position horizontale sans avoir eu le temps de dire ouf. Il suffirait de se voir passer ainsi du jour &agrave; la nuit, dans un mouvement de bascule acc&eacute;l&eacute;r&eacute;, pour comprendre un peu plus nettement ce qui rend notre condition incompr&eacute;hensible.</p>
            
            <p>Noyer le pr&eacute;sent. Nous sommes les poissons de l'air.</p>
            
            <p>Il y a longtemps que je ne sais plus rien confier d'important &agrave; personne. Alors je parle pour &eacute;viter le silence. Je me r&eacute;f&egrave;re &agrave; l'anecdote, toujours accueillie. L'autre encha&icirc;ne, qui pense peut-&ecirc;tre sourdement la m&ecirc;me chose, et l'on se serre la main, souvent lourds de phrases &agrave; hurler, mais &agrave; quoi bon les faire &eacute;clater, celles-l&agrave; justement qui ne sauraient trouver gr&acirc;ce ou r&eacute;ponse. Fatigue de s'expliquer, de se dire et redire, et de profiter de l'amiti&eacute; pour &eacute;taler son mal. Mais c'est dans ce ph&eacute;nom&egrave;ne de pudeur, dans cette interdiction de tout dire &agrave; la seule personne au monde qui peut-&ecirc;tre comprendrait, que si&egrave;ge l'amiti&eacute;. Mais comprendrait quoi ?</p>
            
            <p>Je crois bien que j'ai tout fait &mdash; sans le vouloir &mdash; pour m'&eacute;viter. Pour &eacute;viter ma lucidit&eacute;. J'ai tout tent&eacute; pour d&eacute;pister, pour perdre ce terrible, cet imb&eacute;cile, ce malheureux d'&ecirc;tre l&agrave;, ce Javert d'un mis&eacute;rable, mais rien &agrave; faire pour changer de lieu, et changer de lieu, c'est justement me suivre et le r&eacute;veiller. Pour tuer ce Dieu ou ce Diable que je retrouve p&eacute;riodiquement devant moi et qui ne me demande aucun compte. Son mutisme, voil&agrave; le pire. Aucun reproche, jamais. Aucune louange. Il prend tout indiff&eacute;remment, les bonnes pens&eacute;es comme les mauvaises. Me laisse libre. Et cependant sa pr&eacute;sence m&ecirc;me est une mena
            <p>Go&ucirc;t effr&eacute;n&eacute; de l'&eacute;chec. De la mort. D'une certaine mort. Qui dispose pour un go&ucirc;t effr&eacute;n&eacute; de la vie. Pourvu qu'elle ne me demande rien. Si je joue, j'ai peur de gagner.</p>
           
            <p>La m&eacute;moire est comme le dessus d'une chemin&eacute;e. Pleine de bibelots qu'il sied de ne pas casser, mais qu'on ne voit plus.</p>
           
            <p>On a de l'humour dans la mesure o&ugrave; l'autre ne s'aper&ccedil;oit de rien.</p>
           

            <p LANG="en-US">My dream, which can easily be carried out, would be to write whatever comes to mind on little cards, like visiting cards, all the while forbidding myself to use more than one of them for every such occasion. I would toss the cards into a box and, every fifty years, sort through them. I would assign each a number.</p>
           
            <p LANG="en-US">Making mistakes in English is not important, of course. But will you love a woman whom you are getting ready to love if, as you are entering a restaurant, you hear her say: &ldquo;This one is inferior than the other&rdquo;? Perhaps you will correct her: &ldquo;No, darling, &lsquo;inferior to&rsquo;.&rdquo; &ldquo;Oh, you and all your fine distinctions!&rdquo; The evening will be difficult to get through.</p>
           

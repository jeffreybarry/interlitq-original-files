            <p>Il est aussi invraisemblable d'&ecirc;tre seul que de ne pas l'&ecirc;tre. Notre corps bouche un trou que notre esprit ne cesse d'approfondir. (Un beau jour, le corps est pris de vitesse, et bonsoir la compagnie.)</p>
            
            <p>L'amiti&eacute; commence par la m&eacute;decine. On se pelote : &laquo;&nbsp;O&ugrave; as-tu mal, etc.&nbsp;&raquo; Puis c'est la chirurgie. Enfin on arrive au coeur du sujet, ou de l'objet, et hop, on sectionne. Au suivant de ces messieurs.</p>
           
            <p>Avoir de l'esprit, c'est proprement ne pas savoir ce qu'on va dire dans cinq minutes.</p>
            
            <p>On applaudit. Puis on s'applaudit d'applaudir. &Ccedil;a n'en finit plus. Un tonnerre d'applaudissements est plus difficile &agrave; interrompre qu'un tonnerre de jurons.</p>
            
            <p>Ce qui peut arriver de pire &agrave; Dieu, c'est que l'homme ne mette plus en doute son existence. C'est aussi ce qui peut arriver de pire &agrave; l'homme.</p>
           

            <p LANG="en-US">The art of photography provides a fine symbol. You &ldquo;take&rdquo; the landscape, the phenomenon, or the face. You develop. Yet what needed light and exposure will be rendered only after its surrender, in darkness. </p>
           
            <p LANG="en-US">Nothing more natural than wanting to be loved for oneself. And nothing sillier, since oneself does not exist. Love is always approximate.</p>
            
            <p LANG="en-US">Poem. A man is dying. DYING.  He is transported to the clinic. He is saved. The operation is the poem.</p>
           
            <p LANG="en-US">In order to live, we have to bend over, to rely on, a part of what perpetually passes through us and characterizes us. Why this instead of that? Why not be animals, and continue to be so, since we are indeed animals at times? We side with, that is we endure what seems to suit our personal nature, which is the unknown par excellence yet which ends up hollowing out a rut in all this chaos. Therefore, I will end up calling myself G.P. as before. Living is therefore useless in that it is about pondering what has been found.</p>
          
            <p><SPAN LANG="en-US">There are individuals who inevitably implicate God, without pronouncing His name all that often, and whose existence is terribly angelic and seemingly chosen. I am thinking of poets, especially Keats, Rilke, Rimbaud, and Baudelaire. What they do &ldquo;here&rdquo; cannot be a criterion. They are themselves unaware of what drives them, what makes them suffer and live so </SPAN><SPAN LANG="en-US"><em>badly</em></SPAN><SPAN LANG="en-US">. Such people make temperate Christians despair, for they appear to play leading roles in our tragedy though without bothering whatsoever about &ldquo;scheming.&rdquo; One expects these pure specimens to be winners, yet what an odd privilege this is: they are not believers, but rather the believed.</SPAN></p>
          
            <p LANG="en-US">At the dinner table. No one wants to eat clams. My mother declares that she will never buy them again. I then feel invaded by an immense tenderness for clams, especially these. As I remember our buying them, coming home, and preparing them, this &ldquo;nevermore&rdquo; chills me with horror. &ldquo;Serve me a few, after all,&rdquo; I say to my mother.</p>
           
            <p LANG="en-US">What we are is what we think unwittingly and what guides us at the moment when we thought we were lost. Bird-thoughts.</p>
           
            <p LANG="en-US">If we are just a bit absentminded, daytime goes by without a hitch. And we find ourselves in a horizontal position without having had the time to say &ldquo;whew.&rdquo; It would suffice to watch ourselves thus passing from day to night, tipping over ever faster, in order to understand a little more clearly what makes the human condition incomprehensible.</p>
          
            <p LANG="en-US">Flood the present. We are air fish.</p>
           
            <p LANG="en-US">For a long time now I have forgotten how to confide something important to someone. I thus speak in order to avoid silence. I relate anecdotes, which are always listened to. The other person replies with his own stories, though he is perhaps silently thinking the same thing; and then we shake hands, both of us often burdened with words that we would like to shout. Yet what would be the use of having them explode? They are precisely those words that would be met with neither grace nor response. The weariness of explaining oneself, of talking about and finding faults with oneself, and of taking advantage of friendship in order to make a show of one&rsquo;s suffering. But friendship lies in this phenomenon of self-modesty, in preventing oneself from saying everything to the one person in the world who would perhaps understand. Understand what?</p>
            
            <p LANG="en-US">I think that I have done everything, unwittingly, to avoid myself. To avoid my lucidity. I have tried everything to track down, then to flee from the sights of that terrible, imbecilic, pitiful human being, that Javert in pursuit of a wretched man. But nothing enables me to go elsewhere, for this would mean following myself and awakening him; nor to kill this God or Devil whom I periodically meet up with and who asks me for no explanation. Worst of all is his silence. Never blaming me. Never praising me. He takes everything in stride, both noble thoughts and evil ones. He lets me be free. And yet his very presence is a threat. The threat of his absence.</p>
           
            <p LANG="en-US">A reckless liking for failure. For death. A certain kind of death. That disposes me towards a reckless liking for life. Provided that life asks nothing of me. If I gamble, I am afraid to win.</p>
           
            <p LANG="en-US">Memory is like the mantle of a fireplace. Covered with curios that one must be careful not to break, but that one no longer sees. </p>
          
            <p LANG="en-US">One has a sense of humor insofar as the other person notices nothing.</p>
            
            <p LANG="en-US">Worriers who rent out their seaside villa so as to pass on their worries to others. </p>
           
            <p LANG="en-US">Because life is incomparable, it is impossible to commit suicide.</p>
           
            <p LANG="en-US">Discipline is loving what one loves.</p>
           
            <p LANG="en-US">All those whom we know though do not love are already dead.</p>
            <p LANG="en-US">All of us are already dead for many.</p>

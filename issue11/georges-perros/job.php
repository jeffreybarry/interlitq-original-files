            <p LANG="en-US">Life is a blind woman holding man on a leash.</p>
            
            <p LANG="en-US">It is as unlikely to be alone as not. Our bodies fill the hole that our minds keep deepening. (One fine day, the body is overtaken&mdash;goodnight all!)</p>
           
            <p LANG="en-US">Friendship begins with medicine. We palpate each other: &ldquo;Where does it hurt, etc.&rdquo; Then it&rsquo;s time for surgery. Finally we arrive at the heart of the subject, or the object, and off you go&mdash;we amputate! The next gentleman, please.</p>
            
            <p LANG="en-US">Wittiness strictly means being unaware of what you are going to say in five minutes.</p>
            
            <p LANG="en-US">We applaud. Then we applaud ourselves for applauding. The applause keeps going on. Thunderous applause is more difficult to interrupt than thunderous insulting.</p>
           
            <p LANG="en-US">The worst thing that can happen to God is that man no longer calls His existence into question. This is also the worst thing that can happen to man.</p>
          
            <p LANG="en-US">Idleness, the mother of all vices and the daughter of all virtues.</p>
           
            <p LANG="en-US">Music. You hold your head in your hands in order not to think.</p>
            
            <p LANG="en-US">As soon as a human being becomes aware, he is worked over by death as wood is by a worm.</p>
            
            <p LANG="en-US">I have unwittingly preserved this naivety: when I open a book, I like it to be a book. I expect literature. Otherwise, life suffices&mdash;that is, me and other people. If the purpose of reading is to find yourself back in life, then telephoning your neighbor and spending an evening talking nonsense is just as worthwhile. We all have an idea of what literature is or should be. Some people read to escape. (From what prisons?) Others to learn. (To what ends?) Still others read because it is more worthwhile to spend time with a man&rsquo;s written language than with the spoken language. Hence: I do not detest my concierge, but am fond of Mallarm&eacute;. Both Mallarm&eacute; and my concierge strike me as plying their trades, with the usual drawbacks.</p>
           
            <p LANG="en-US">How to write interesting things to someone who will not fail to find them interesting?</p>
            
            <p LANG="en-US">I sense only absences.</p>
            

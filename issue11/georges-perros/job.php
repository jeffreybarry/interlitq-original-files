            <p LANG="en-US"> Every day, I have the distinct sensation&mdash;the most distinct of all my sensations&mdash;that I have had it. Impossible to right the tiller. Which rights itself. This is a little annoying.</p>
           
            <p><SPAN LANG="en-US">It is probably true that the world is hateful, since so many influential personalities have declared it to be so. Among other thinkers, moralists have never said anything else, as if stating this allowed them to live a day longer. Solitude has always been negative. Living in society leads to suicide. Which by no means prevents us from having friends and from bearing the &ldquo;I love you&rdquo; of the opposite sex. So? Are we able to love for a long time? Be loved for a long time? No. So? We play. But what game? No game has two winners. A nil draw? Yes, nil. So how can we </SPAN><SPAN LANG="en-US"><em>make</em></SPAN><SPAN LANG="en-US"> love?</SPAN></p>
            

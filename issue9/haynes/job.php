                on the back in green, the address in Hereford,<br>
                whom she didn&rsquo;t marry, but his father  instead,<br>
                and was no longer a ladies maid in that big  house in Ludlow.</p>
              <p>And when he died a few years later, and she  was left<br>
                a single mother, owning a whole wool shop<br>
                setting her hips against the brass ruler  along the counter<br>

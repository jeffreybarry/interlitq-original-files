            <br><p><strong>The Ballad of First Cell</strong></p>
            <p>Born in a deepsea  vent. Or carried here <br>
              by meteorite. Or,  take your pick,<br>
              synthesized by  lightning <br>
              in a reducing  atmosphere. </p>
            <p>Algae, that&rsquo;s the  word. <br>
              The first self-replicating molecule <br>
              on Earth. Pulling carbon from organic <br>
              substrates, embroidering </p>
            <p>a million copies of itself, thick <br>
              as autumn leaves on  the Chiltern Ridge<br>
              or the pure gold  bricks <br>
            Sheba sent to Solomon by mule</p>
            <p>and storing genetic data, <br>
              creating the world&rsquo;s first magic:<br>
              photosynthesis <br>
              of atmosphere to oxygen.</p>
            <p>Little Blue-Green, <br>
            dreaming like Billie Holiday<br>
            for who knows how long <br>
            of rhythm,  pattern, form, </p>
           
            <p>like something hoping to be heard <br>
            in a heart cut open. <br>
            Tiny horseman <br>
            of apocalypse.</p>
            <p>&nbsp;</p>
<P><b>The Arrival of Nucleic  Acid</b>
<p>As if from outer  space,<br>
  like  the love which stirs <br>
  the sun and other  stars <br>
  in Dante&rsquo;s universe. </p>
<p>Like light<br>
  shed on quick seas <br>
  with enzymatic properties <br>
  to catalyze and duplicate. </p>
<p>A triple bracelet <br>
  chain-composed <br>
  of phosphate, pentose <br>
  sugar, one nitrogenous base.</p>
 <p>&nbsp;</p> 
 <P><b>Journey of the Cells </b></P>
 <p>Meet cytoskeleton.  A fibre network <br>
   of side-rootlets, mini-ferns&nbsp; <br>
   through cytoplasm, protecting shape <br>
   like a fringe sewn  under the hem</p>
 <p>so Cell can move. Why not call her &ldquo;she&rdquo;, <br>
   watch her wave her longhaired  flagella <br>
   like the  flicker-spray <br>
   of spores on  mould? Her lamellipodia, </p>
 <p>motile fronds<br>
   like clematis or  jasmine, <br>
   power Cell&rsquo;s division of herself<br>
   and all small voyages inside her </p>
 <p>performed by vesicle or organelle. <br>
   But also, like  bison drawn to cross a river,<br>
   with microfilaments at the ready, <br>
   and little cilia</p>
 <p>projecting from  her surface, Cell <br>
   may well migrate. <br>
   Slowly, her front spreads forward, <br>
   retracting, ruffling, bubbling. </p>
 <p>Determined, like an old soul <br>
   moving into heaven <br>
   eager to notate <br>
   a valediction to the  body. </p></td>

            <p><strong>Passing By</strong></p>
            <p>I saw them take  a man from the cross.<br>
              Very sturdy,  with even the rot solid<br>
              they had to  wrench, torture, and ply<br>
              a thorn-torn  flesh from the sky&rsquo;s board.<br>
              Some workers  below, shouldering blindly,<br>
              were shouting to  take it the hell easy<br>
              with such a dead  weight. The careful shroud</p>
            <p>Inserted between  back and bloody beam<br>
              clutched the  descendings of a body<br>
              that angled  about neck, waist, and knees<br>
              macabrely, yet  masked the thighs.<br>
              No one  complained, murmured, or wept:<br>
              those  dismantling the gibbet, younger men,<br>
              were  business-like, or strangely hurried;</p>
            <p>Other faces, in  ambush, could not make room,<br>
              already etched  by grief and elder gloom;<br>
              and of two  women, standing well back,<br>
              only their eyes  glowering in the dusk<br>
              spoke, like  waters, as they followed him<br>
              into the sun&rsquo;s  terse halo, to<br>
              a pit of light  deeper than understanding.</p>
            <p>Closer than all  who watched, a citizen<br>
              stood alert,  fatly blinking, legs apart,<br>
              steadier than a  soldier&rsquo;s challenge or<br>
              the cross: he  also saw without seeing,<br>
              his glance  reflected neither shame nor haste,<br>
              disinterested as  a camel in a death<br>
              that would open  any imagination.</p>
            <p>I was curious to  know what crime the corpse<br>
              had committed,  but no one seemed to care<br>
              about the  bearded, not unbeautiful face,<br>
              only to dispatch  the dead in good order,<br>
              to lay the body  carefully from sight<br>
              with lavings,  myrrh, and linen grave-clothes:<br>
              so I too  committed his face to the earth.</p>
              <p>&nbsp;</p>
<P><strong>Psalm</strong></p>
<p>Break bow and  string<br>
  they must not  express the beat, <br>
  not this time.<br>
  For I have  taught my soul to stand alone<br>
  what then shall  I do,<br>
  what, tell me,  with myself?<br>
  It is a rock,  and all increase<br>
  loneliness.</p>
<p>I have not shut  these ears in vain,<br>
  not this time.<br>
  Can a human tune  speak true<br>
  give eyes, ears,  and a wind-sown field,<br>
  make the barren  bear<br>
  dedicated to an  unhorned God?<br>
  If still the  rock bears<br>
  who has anointed  its head?</p>
<p>I have taught  myself to stand alone<br>
  neither to feed nor  excrete.<br>
  The earth is  full<br>
  it roars to my  ears<br>
  it whips oceans  of sight<br>
  even silence  breeds.<br>
  Dream, despair,<br>
  have eaten the  proud day.</p>
<p>I say to myself,  &ldquo;We are Awake,&rdquo;<br>
  the belly laughs<br>
  teeth grind as  in a sleep.<br>
  Hush, the stars  are unhusked:<br>
  discurtained<br>
  she lies  unveiled<br>
  her skin<br>
  toward desire&rsquo;s  edge<br>
  tenderness<br>
  her lashes dark  spears<br>
  dawn at the hem  of her skirt.<br>
  I say to desire,  &ldquo;Full Tide,&rdquo;<br>
  the quivering  blades<br>
  have reached the  blood of the air<br>
  drained like a  cup.</p>
<p>My soul, who has  anointed you,<br>
  are you still  thirsty like the east winds,<br>
  or sated, ready  to be poured out?<br>
  Whose dark dew  floods you and I do not know it?<br>
  I dream: a  golden sheaf<br>
  a beanstalk  shoots to pierce heaven,<br>
  a mantle of  praise<br>
  suits the  swollen navel.</p>
<p>Is it visible at  last<br>
  the inwrought  face<br>
  formed once for  all<br>
  without  instruments<br>
  out of a unique  ore<br>
  and icy love?</p>
  <p>&nbsp;</p>
  <P><strong>Green</strong></P>    
  <p>Green is our sacred  mountain<br>
    there is a voice  on the Green<br>
    a pueblo people  dances there<br>
    like cars once  around it.<br>
    The skyline  becomes sky again<br>
    awnings flutter  in the wind<br>
    walks plant the  liberated streets<br>
    with randomness.  Shops open<br>
    as coffeehouses  and markets of life.<br>
    All people are  street people<br>
    and hymn  highways that were.<br>
    The tax base  withers away<br>

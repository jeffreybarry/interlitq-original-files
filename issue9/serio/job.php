            <p><strong>Lines</strong></p>
            <P>Once I saw a city here, in this space. <br>
Only square hatches in arrays, numbers with  degrees, modifiers, <br>
but a city was here, people were here.             
            <p>In sweeps a draft and an eraser with razor  edges, <br>
              embraced by plastic shrink-wrap. A cruel  mother <br>
              with a steady hand, it scrubs every crevice  clean,<br>
              leaving only soap scum and rubbery residual  fragments, <br>
              palimpsests all, curled and fetal.<br>
              But the city was here. </p>
            <p>Every cold blast whips bull-like across  gouged concrete, <br>
              cancerous veins with boils, blisters and  surgical scars. <br>
              All is desert, <br>
              grains of ice and sand pellets breaking skin, <br>
              corroding to rust, and there is no air. </p>
            <p>Lines that intersect form maps and splinter, <br>
              like shavings from chop sticks. Street vents  scream bile vapour, <br>
              hot breaths chilled on Cocytus from beneath a  rigid blanket, swaddling dirt. Once<br>
              there was a city here, <br>
              now all disappeared by compartments, <br>
nests made of fabrics and soundtracks.</p></td>

                  <td class="texto2" valign="top"><p><span lang="EN-US">Professor  John Schad studied for his BA at the University of York and his PhD at  the University of Wales. Before moving to Lancaster in 2006 he taught  at Loughborough University. </span></p>
                    <p><span lang="EN-US">Professor  Schad's main areas of research are: critical-creative writing;  modernism; Victorian writing; literary theory; and the relationship  between religion and literature. These interests are reflected in his  major publications - his five authored books: namely, <em>Someone Called Derrida: An Oxford Mystery</em> (Sussex), <em>Arthur Hugh Clough</em> (Northcote House), <em>Queer Fish: Christian Unreason from Darwin to Joyce</em> (Sussex), <em>Victorians in Theory</em> (Manchester UP*), and <em>The Reader in the Dickensian Mirrors</em> (Macmillan); and his four edited books: namely, <em>Dickens Refigured</em> (Manchester UP); <em>Thomas Hardy, A Laodicean</em> (Penguin); <em>Writing the Bodies of Christ</em> (Ashgate); and <em>life.after.theory</em> (Continuum). This last book includes new interviews with Jacques Derrida, Frank Kermode, Toril Moi, and Christopher Norris.</span></p>
                    <p><span lang="EN-US">Professor Schad's most recent book, <em>Someone Called Derrida</em>,  is a novelistic attempt to combine traditional archival scholarship  with memoir, investigative history, detective fiction, and Oxford; it  is, in a sense, a book that explores the space between critical and  creative writing. The opening chapter will be dramatised and performed  on BBC Radio 3's <em>The Verb</em> in October 2009. In this  connection, it is worth noting that Professor Schad is General Editor  for a new series of experimental monographs called critical inventions (<a title="sussex" href="http://www.sussex-academic.co.uk/sa/titles/SS_Critical/_critical.htm" target="_blank">http://www.sussex-academic.co.uk/sa/titles/SS_Critical/_critical.htm</a>).  The series includes titles by Thomas Docherty, Roger Ebbatson, J.Hillis  Miller, Kevin Mills, David Punter, Jean-Michel Rabat&eacute;, and Michael Wood.</span></p>
                    <p><span lang="EN-US">Professor Schad is currently working on a novel called <em>I Am Not Walter Benjamin</em>.  It concerns a man on a post-war council estate near Watford who thinks  or says he is the late Walter Benjamin; this peculiar figure only ever  uses the words of Benjamin. Drawing as it does on the political history  of the estate <em>I Am Not Walter Benjamin</em> is, if you will, a work of social sur-realism. The opening scenes were read and discussed on BBC Radio 3's <em>The Verb</em> in January 2009, and the first chapter will appear in Mark Knight (ed.), <em>Religion, Literature and Imagination</em> (London: Continuum, 2009).</span></p>

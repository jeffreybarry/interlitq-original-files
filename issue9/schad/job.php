            <br><p><em>Introduction: </em></p>
            <p>What follows is a radio-phonic version of the opening of my book, <em>Someone  Called Derrida</em>, which is a real-life murder mystery centred  on the famous French philosopher, Jacques Derrida&rsquo;s complex relationship to the  University of Oxford and the&nbsp; final years of my own father, years overwhelmed by dementia and nightmare. Every factual detail is, as far as I can  tell, correct.&nbsp; The same applies to the  voices you shall hear; they too are exact, verbatim. <strong></strong>My father&rsquo;s words are as transcribed at the time by my mother, and  Derrida&rsquo;s words come directly from a single book of his called <em>The Post Card</em> (1979). </p>
<P><strong>MUSIC:  the opening of </strong><strong>Vaughan</strong><strong> Williams&rsquo;<em> An Oxford Elegy, </em>to be used as a  refrain throughout the play.</strong>
<p>&nbsp;</p>
<p>MYSELF: &nbsp;&nbsp;&nbsp;&nbsp;<br>
  It  is late, and I am reading. The book I  read is by someone called <br>
  Derrida, a strange philosopher. And his book, it is also strange <br>
  because the philosopher,  you see, does not seem to know the person about <br>
  whom he is supposed to  be writing. <br>&nbsp;<br>
  DERRIDA: <em>(Slowly</em>) &nbsp;<br>I  truly believe that I am singing someone who is dead and that I <br>
  <em>did not know.</em><br>&nbsp;<br>
  MYSELF:<br>  Still more strange is that I am  beginning to imagine that this someone is <br>
  my  father. After all, he is dead and the  philosopher did not know him. <br>
  &hellip;  This, I know, is absurd. They are, or  were, so very different - my <br>
  father  being no philosopher But he is definitely dead, and Derrida <br>
  certainly  did not know him. <br>&nbsp;<br>
  DERRIDA: <br>They did not know each other, but &hellip;they  form a couple &hellip;just because of <br>
  that. <br>&nbsp;<br>
  MYSELF:<br> Philosopher  and pastor <br>&nbsp;<br>
  DERRIDA: (Parenthetically)<br> An odd couple. <br>&nbsp;<br>
  MYSELF: <br>It is  true. But I shall point out that they were both born in 1930; both wrote <br>
  censored love letters  from Oxford; and both (here I  hesitate)&hellip;  both <br>
  suffered, or seemed to  have suffered, some kind of childhood calamity in <br>
  the middle of the Second  World War. <br>&nbsp;<br>
  NARRATOR: <br>In  September 1942, someone called Derrida into his study. <br>&nbsp;<br>
  DERRIDA:<br> The  school officer has me come into his office: &lsquo;<em>You</em> are going home, my <br>
  little friend.&rsquo;<br>&nbsp;<br>
  MYSELF<em>: </em><br>The  boy, just twelve years old, is leaving because he is Jewish; he must <br>
  leave school for  home. Exactly one year later, in England, another  boy <br>
  (my father) goes the  other way, leaving home for school. He  is thirteen <br>
  and is beginning at boarding-school  where something happens, <br>
  something quite  terrible; or rather, it <em>may</em> have  happened. &hellip; All I have <br>
  to go on is that, at  around the age of 60, after several years of premature <br>
  dementia my father slowly  disappeared. He  seemed to understand almost <br>
  nothing that we said,  just as we understood almost nothing that he said; <br>
  but then, we did not  really want to understand, since he seemed to describe <br>
  a series of appalling  events &hellip;.Well, perhaps not events. <br>&nbsp;<br>
  DERRIDA: (Gently)<br> Disaster - we have <em>dreamed</em> of it, no? <br>&nbsp;<br>
  MYSELF: <br>Yes &ndash; my father, he may have dreamed of  it. But if so, he was dreaming <br>
  of it every day for the  last five years of his life, and my mother, she heard <br>
  it all, and set it down  even as he spoke. <br>&nbsp;<br>
  FATHER:<br> It was lethal&hellip;. Hanging&hellip; They were kicking  him. Just shut the <br>
  door. He is  murdering me&hellip;. 666&hellip;. I must  telephone. <br>&nbsp;<br>
  MYSELF: <br>The  boy did, finally, telephone. I think he  meant to make the call in 1943, <br>
  but it took him fifty  years to do so, fifty years before he could get through. <br>&nbsp;<br>
  DERRIDA:<br> I tried to call you but it was busy, then no answer, you must <br>
  have gone out. <br>&nbsp;<br>
  MYSELF:<br> When  he did get through it was not just my mother who picked up the<br>
  phone; it was also, I think, the philosopher, for he was no stranger  to <br>
  peculiar phone calls. <br>&nbsp;<br>

  fortune-telling  book that includes thirty-six questions, and each time you <br>
  open  the book you are invited to choose one.  I do not know why, but the <br>
  philosopher  chose:<br>&nbsp;<br>
  DERRIDA:<br><em>Si puer vivet</em> &ndash; &lsquo;If the boy lives.&rsquo; <br>&nbsp;<br>
  MYSELF:<br>The same ancient book includes the answer to this question, but the <br>
  philosopher  is not letting on. To find the answer you  must, therefore, <br>
  take  the last train to Oxford, to that -<br>&nbsp;<br>
  DERRIDA:  <br>Labyrinth between the&hellip;colleges. <br>&nbsp;<br>
  MYSELF:<br>And, once there, you must follow the philosopher to the dead centre of  the <br>
  labyrinth, to somewhere they call the Duke  Humfrey Room.<br>&nbsp;<br>
  DERRIDA:<br> Are you following? <br>&nbsp;<br>
  MYSELF: <br>Perhaps not, but you should. You  owe it to a boy, and perhaps even to a<br>
  philosopher.  So, listen well as he tells your future: <br>&nbsp;<br>
  DERRIDA: <br>One  day I will be dead, and you will come into the Duke Humfrey <br>
  Room to look for the answer in this book. <br>&nbsp;<br>
  MYSELF: <br>The philosopher is now dead. He  died on October the 8th 2004; I <br>
  heard it on the radio, a day later, a day late. <br>&nbsp;<br>
  DERRIDA: <br>The  radio transmits, no one understands.<br>&nbsp;<br>
  MYSELF: <br>But I do understand that the philosopher  is dead. It is, then, time for you <br>
  to go into the Bodleian and look for the  answer. First, though, the oath:<br>&nbsp;<br>
  DERRIDA: <br>&lsquo;I,  hereby undertake not to bring into the Library or kindle therein any <br>
  fire or flame.&rsquo; &hellip; <br>&nbsp;<br>
  [BREAK]  &hellip;&hellip;&hellip;&hellip;&hellip;&hellip;&hellip;&hellip;&hellip;&hellip;&hellip;&hellip;&hellip;&hellip;&hellip;&hellip;&hellip;&hellip;&hellip;&hellip;&hellip;&hellip;&hellip;&hellip;&hellip;.<br>&nbsp;<br>
  MYSELF: <br>The  Duke Humfrey Room is an ancient library, shaped by concentration, <br>
  the  concentration of all the knowing world. <br>&nbsp;<br>
  DERRIDA: <br>Letters,  knowledge wall them up in their crypt. <br>&nbsp;<br>
  MYSELF: <br> And it seemed like a crypt on the day I visited the Room,  and there <br>
  met  a very English Frenchman who had emerged, like a holy father, from <br>
  the  latticed confessional box that they call &lsquo;The Reserve.&rsquo; This kindly<br>
  man was an archivist, and explained that it was he  who, twenty-six years <br>
  before,  had assisted the philosopher. He then showed  me what the <br>
  philosopher  had hand-written in a special copy of his book. It read: <br>&nbsp;<br>
  DERRIDA:<br> To  the soul and body of the Bodleian (&lsquo;&agrave; l&rsquo;&acirc;me et au corps de la <br>
  Bodleian&rsquo;). <br>&nbsp;<br>
  MYSELF: <br>For  some reason, the French archivist had never seen this before, but he <br>
  immediately  got the long-delayed message, the inter-linguistic text. <br>&nbsp;<br>
  ARCHIVIST: <br>I &hellip; <br>&nbsp;<br>
  MYSELF: <br>He  said<br>&nbsp;<br>
  ARCHIVIST: <br>&hellip;am  the <em>corpse</em> of the Bodleian.<br>&nbsp;<br>
  MYSELF:<br> He is right, there is a stiff in Jacques  Derrida&rsquo;s Bodleian; however, it has <br>
  not  yet been finally identified. <br>&nbsp;<br>
  DERRIDA:<br> I  arrived at opening time, just now, still dragging along with me a <br>
  dream:  all around someone in danger of death several doctors. The <br>
  patient  is passive, immobile &hellip; <em>my father</em>. <br>&nbsp;<br>
  MYSELF: <br>If  there is  a body in the Room then, at first glance, it is (the philosopher <br>
  dreams)  -<br>&nbsp;<br>
  DERRIDA:<br> My  father. <br>&nbsp;<br>
  MYSELF: <br> The philosopher&rsquo;s father had died of cancer  at the age of seventy-four, <br>
  and  one day the philosopher would also die of cancer at seventy-four, in <br>
  hospital. The passive and immobile patient at the centre  of this Oxford <br>
  dream  may, then, be the philosopher himself.<br>&nbsp;<br>
  DERRIDA:<br> I intend to go back to Oxford, to take the investigation to its  end. <br>&nbsp;<br>
  MYSELF: <br>The  philosopher, I think, investigates his own death. He  has, you see, <br>
  come  to Oxford in order to die - <br>&nbsp;<br>
  DERRIDA:<br> It would be good if I died tonight, in the  college.<br>&nbsp;<br>
  MYSELF: <br>The college is Balliol, a place where the  thought of the philosopher&rsquo;s death <br>
  had,  in fact, already been entertained. <br>&nbsp;<br>
  DERRIDA: <br>June 6th 1977 - seminar (at Balliol). Afterward, on the lawn, a <br>
  young  student (very handsome) thought he could provoke me and, I think, <br>
  seduce  me by asking why I didn&rsquo;t kill myself. <br>&nbsp;<br>
  MYSELF: <br>If,  two years later, when the philosopher returned to Balliol, he had then <br>
  died  overnight the handsome student may himself have faced some <br>
  awkward  questions.<br>&nbsp;<br>
  NARRATOR:<br> Cue the great Thomas de Quincey:<br>&nbsp;<br>
  DE Q.: (Grandly)<br> Gentlemen, it is a fact that every philosopher of eminence has <br>
  either  been murdered or at least been very near it, &ndash; insomuch that, if a <br>
  man  calls himself a philosopher and never had his life attempted, rest <br>
  assured  there is nothing in him. <br>&nbsp;<br>

  FATHER: <br>Think! Think! &hellip;Think! Think! <br>&nbsp;<br>
  MYSELF: <br>And think-think is exactly what Socrates does  - <br>&nbsp;<br>
  DERRIDA: <br>That devil of a Socrates <br>&nbsp;<br>
  MYSELF: <br>Yes, that perverter of youth, that  philosophical celebrant of the beautiful <br>

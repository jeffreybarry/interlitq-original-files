  MYSELF: <br> And as if <em>he</em> were not enough, there is also - <br>&nbsp;<br>
  DERRIDA: <br>Paul&hellip;the  little brother dead before me. <br>&nbsp;<br>
  FATHER: <br> Poor boy, dead boy. <br>&nbsp;<br>
  MYSELF: <br>But which boy is this now? Which boy are we mourning? The boy erased <br>
  by meningitis? The boy they had called Paul? The boy expelled for being <br>
  a Jew? The thousands of boys that were lost in the  Holocaust? Or, <br>
  simply the boy that was  my father? Or, perhaps, the boy my  father <em>says</em> he <br>
  saw being killed; if  there ever was such a boy, such a never never boy. <br>&nbsp;<br>
  FATHER: <br>He&rsquo;s dead!  That poor little boy&hellip;I saw them bring him out screaming. <br>

  Heidegger, even though  he, Heidegger, had died three years before. <br>&nbsp;<br>
  DERRIDA:<br> On 22 August while typing the words, &lsquo;from  Freud and from <em>Heidegger</em>&rsquo; <br>
  the telephone rings. The operator asks me if I accept a &lsquo;call&rsquo;  from <br>
  Martin Heidegger. <br>&nbsp;<br>
  MYSELF: <br>With  messages coming in from the dead as well as the living, the<br>
  philosopher&rsquo;s switchboard is burdened with interference, with noises  off. <br>
  And these are terrible noises: the noise of -<br>&nbsp;<br>
  DERRIDA: <br>All  the cruelty in the world.<br>&nbsp;<br>
  MYSELF: <br>Even  -<br>&nbsp;<br>
  DERRIDA:<br> The  worst concentration of evil: To the devil with the child, the <br>
  child, the child. <br>&nbsp;<br>
  MYSELF:<br> You  can, perhaps, see why I dream that the philosopher got my father&rsquo;s <br>
  call, my father&rsquo;s 666  call. And I dream it again when the  philosopher <br>
  looks round and asks:<br>&nbsp;<br>
  DERRIDA: <br>Who is he afraid of, this child?<br>&nbsp;<br>
  MYSELF: <br>And his  book, the philosopher&rsquo;s book, it is, stuffed with dead children.<br>&nbsp;<br>
  DERRIDA:<br> For the children, the holocaust has already  begun.<br>&nbsp;<br>
  MYSELF: <br>As  if this, or they, were not enough, there is also -<br>&nbsp;<br>
  DERRIDA: <br>Norbert,  my dead younger brother.<br>&nbsp;<br>

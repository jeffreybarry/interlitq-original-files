            <br><p>1&nbsp;  The Company</p>
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <em>18 May, 1882</em><em>, </em><em>5pm</em></p>
            <p>M&egrave;re Chevillon is at her <em>tisanes</em> again,<br>
              fussing over the Ullman boy. The distant  clink <br>
              of tea-spoons on best china counterpoint <br>
              an evening filled with jackdaw cries, <br>
              the persistent jackass laughter of a plague<br>
              of ducks out on the River Loing that cuts  off<br>
              the sloping garden of our inn. One is  christened<br>
  &lsquo;Caesar&rsquo; and tonight I&rsquo;d happily be his  Brutus.<br>
              Ullman is ill, will die; here perhaps;  we&rsquo;ll have<br>
              a little ceremony in our courtyard.  O&rsquo;Meara,<br>
              the Irishman will speak &ndash;he always does&ndash;<br>
              of wasted youth, the confraternity of <em>plein-air</em><br>
              painters who&rsquo;d found a fresh disciple <br>
              in the recently deceased. I can&rsquo;t be sad.<br>
              TB has done for him and he&rsquo;ll go out<br>
              with the soft Grez light he&rsquo;s mortgaged<br>
              everything to see, mourned by peers <br>
              who value him. And then he&rsquo;ll be replaced.<br>
              They&nbsp;  rise like hoverflies from the riverbank<br>
              brush first; disheveled, hot, smocks<br>
              sticky with willow, they cling to unlikely  spots,<br>
              and hang from palettes by their thumbs,<br>
              clusters of them painting each other<br>
              painting views and the locals, none<br>
              of whom have time to stand and stare.<br>
              They smell of lavender and chew tobacco.<br>
              Who is here then? Foreigners with difficult<br>
              Anglo-Saxon, Swedish names: a Scot<br>
              called Melville, Stott of Oldham,<br>
  &ndash;wherever that may be&ndash; a genius: Carl<br>
              Larsson and a fellow-countryman,<br>
              small and tense with lots of hair<br>
              who thinks he&rsquo;s one as well: Strindberg<br>
              is his name. A Simeon Solomon &ndash;utterly<br>
              improbable&ndash; and my favourite: John Lavery,<br>
              compact, gregarious, with too much charm.<br>
              Listen: the tanned vowels of young men<br>
              gathered over beer tumble up to me;<br>
              gorgeous <em>saltimbanques</em> of laughter<br>
              peel briefly and jag as if mocking me<br>
              for being me and absent from the company.</p>
            <p>&nbsp;</p>
            
            
              <p>2 The Art</p>
            
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <em>May   21st 1883</em><em>, </em><em>4pm</em></p>
            <p>&lsquo;Monsieur le Comte, I challenge you to a  duel!&rsquo;<br>
              His arched eyebrows, smile, accompanying<br>
              snigger of O&rsquo;Meara and the rest suggested <br>
              a joke, a dare, another diversion<br>
              from the job of painting. Yet from the  corner<br>
              of my eye I&rsquo;d spied him at it, assiduous <br>
              for days before the canvas of a rower<br>
              with moustaches kissing hands at ladies<br>
              in&nbsp; <em>bateaux  de plaisance</em> drifting past him. <br>
              The surface splendourof the  oarsman&rsquo;s costume, <br>
              a classic striped affair, replied  vivaciously<br>
              to the girls&rsquo; parasols and the little  sub-plot<br>
              danced among the sparkling greens and  whites.<br>
              Perfect. A day of mischief, sunny <em>far niente</em><br>
              summed up within a four franc gold leaf  frame.<br>
  &lsquo;Well, Comte Henri de Lestrange, will you  play<br>
              our game? My painting versus your  photography?&rsquo;<br>
              I instantly demurred: we could not find<br>
              the dapper rower and the girls were back<br>
              in Paris no doubt. And  then the whole<br>
              thing was to take the moment &lsquo;sur le vif&rsquo;,<br>
              upon the cusp of being there and gone.<br>
              A replay would betray respective arts.<br>
              Or some such stuff. He shrugged,<br>
              produced another painting: &lsquo;This one,<br>
              would be easier, &ldquo;A Grey&nbsp; Summer&rsquo;s Day in Grez&rdquo;.<br>
              You see?&rsquo; It was more posed: a straw hatted<br>
              man &ndash;Lavery himself&ndash; with back to the  spectator <br>
              gazes at the water. Nearby a woman sits<br>
              reading in a campaign chair. Stillness, <br>
              the minnows and the water lillies. Only <br>
              the greyhound, Fred, ears pricked,<br>
              looks down the path and is aware <br>
              of us, of me as I approach. I gave in.<br>
              Misgivings? Plenty. They wished &lsquo;to view <br>
  &ldquo;The Silver Sunbeam&rdquo;&rsquo;as my camera is  called, <br>
              snoop beneath my hood,<br>
  &lsquo;observe the alchemist at work.&rsquo;<br>
              In art they count my aristocracy<br>
              at nought. To them I am a peasant<br>
              drunk on new machinery, an artisan<br>
              before these grandees of oil and gouache. <br>
              Why humour them? Was it the friendliness <br>
              I seemed to find in Lavery&rsquo;s frank face? <br>
              I should have paid attention to the angle <br>
              of the shadow cast by the ruined tower <br>
              next door. It&rsquo;s called <em>La Tour de Ganne</em> <br>
              from Latin &lsquo;gannum&rsquo; meaning trap, <br>
              betrayal, deception and derision.</p>
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<em>9pm</em></p>
            <p>And so I raised him:&nbsp; Karin Berg&ouml;&ouml;,<br>
              Larsson&rsquo;s wife, sat sketching in a chair.<br>
              Lavery was himself, as was the Loing,<br>
              its coolness, light running up<br>
              the underside of leaves, ambulatory,<br>
              mechanical, mocking us at our outdoor<br>
              parlour game. The difficulty was Fred,<br>
              who did not want to play but flopped<br>
              where he should have been alert.&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <br>
              The picture shows him in an ugly<br>
              squat, all that Melville could coax<br>
              while dashing in and out of shot.<br>
              The collodion plate prepared,<br>
              I inserted it, ignored a pinch<br>
              of baited breath from the fist<br>
              of artists looking on then placed my cap <br>
              before the lens, removed it briefly <br>
              and let in the world:<br>
              at this there was some stifled laughter.<br>
              I do not despise the ingenious shutter<br>
              but my cap is my shutter. Sometimes<br>
              I use a book. With both I have succeeded.<br>
              And did so this time. That light can act<br>
              actinically the twinkling of an eye<br>
              is no tax upon the cultivated mind;<br>
              for in this wink light has circled earth <br>
              twice at least and in this trice seen more <br>
              than man can ever see, blanched the picture&rsquo;s<br>
              subject with millions of fresh portions<br>
              that rebound to the lens and through it,<br>
              then nestle on the film. But could I speak<br>
              of this when afterwards they gathered<br>
              round to make&nbsp; inevitable comparisons?<br>
              Why did they chatter so when the photo<br>
              looked at them so frankly? Each splash <br>
              of light surging from the dark, each strand <br>
              of shadow surrounded by a ring of light <br>
              creates the illusion of looking at the one <br>
              who looks at it &ndash;even when no person <br>
              is present in the photograph.<br>
              I was lost in it, silenced, drowning<br>
              in pools of shadow that glanced<br>
              out at me like pupils from the white<br>
              of eyes; Lavery rescued me or rather<br>
              opportunely took centre stage:<br>
              as &ldquo;a young un&rdquo;, he&rsquo;d &ldquo;retouched&rdquo;<br>
              to McNair of Glasgow&rsquo;s <em>Herald</em> <br>
              drawn in a cloud or two &ndash;for skies<br>
  &lsquo;just won&rsquo;t come out&rsquo;&ndash; and rejuvenated&nbsp; <br>
              aging relatives. &ldquo;For photographs so  quickly <br>
              made&rdquo;, he said, &ldquo;you need very ripe  collodion,<br>
              a well corrected lens, short focus<br>
              and a steady hand&rdquo;, at this he grasped<br>
              my hand and held it up, &ldquo; &ndash;like M.  Lestrange&rsquo;s!&ndash;<br>
              beneath the dark hood&rsquo;s tent&rdquo;. His palm <br>
              was soft, soft as his brushes, as the  feathers <br>
              I use to varnish the edges of my negatives. <br>
              He dropped it quickly, then turned <br>
              and fixing me without his usual smile:<br>
  &ldquo;Sodium hyposulfate and acetic acid?&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<br>
              Ancient Egypt  used this solution <br>
              to mummify their dead; its function<br>
              has not changed in several millennia:<br>
              thus, M. le Comte, you embalm<br>
              the living in baths and washes.<br>
              Like a sleuth you track the flux<br>
              of light and try to snare its vital spirit <br>
              in a print. You always fail for here<br>
              is shadow and more shadow,<br>
              a Grez of grey shades, a cave<br>
              in which the colour of the world is lost.<br>
              Yours is a necrophiliac art.&rdquo; At this<br>
              there was a sharp intake of breath.<br>
              My own or from the others&hellip; I wasn&rsquo;t sure.<br>
              I bowed, retired back up the garden<br>
              and for a moment saw myself as they<br>
              must see me: an awkward human tripod half <br>
              cartwheeling,<br>
              the camera&rsquo;s hood flapping forward <br>
              like an elephant&rsquo;s trunk, <br>
              monstrous, most strange.</p>
            <p>&nbsp;</p>
            
            <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<em>Midnight</em></p>
            <p>Sleuth of the dandelion&rsquo;s seed,<br>
              of the sunbeam volatising<br>
              mica on the parapet, sleuth <br>
              of the night-time breeze,<br>
              its taste and touch, <br>
              of the current in the trees,<br>
              tree spit and froth, intricate <br>
              scum on the surface of the Loing, <br>
              sleuth of the chevroned wake <br>
              slipping from the tails<br>
              of swans, of the dark room&rsquo;s tongue&nbsp; <br>
              where only the earth will speak.</p>
            <p>&lsquo;Sleuth&rsquo;: I looked it up: &lsquo;detective,<br>
              a sleuthhound on the track of crime&rsquo;,<br>
              a trapper, a sniffer out; of course;<br>
              but descending from the Norse word,<br>
  &lsquo;sloth&rsquo; which means both trail and trace.</p>
            <p>Sleuth then of the world&rsquo;s<br>
              trace, the silver tracery<br>
              in the&nbsp;  tree stump&rsquo;s bole<br>
              nested by spiders&rsquo; webs;<br>
              trace of a world objectified<br>
              by light, of a person in the lit-up<br>
              world, opening lenses to it,<br>
              accompanying it.</p>
            <p>&lsquo;Sleuth&rsquo;. He meant to hurt me.<br>
              And maybe Lavery too was something<br>
              of the sleuth: what was it in me<br>
              he sensed should be so hurt?<br>
              A strangeness; a nearness;<br>
              of art; of nature? It was when<br>
              he touched me I understood<br>
              his error, possibly his envy:</p>
            <p>unlike painting, those traces<br>
              of the real in photographs <br>
              are not made by human hands<br>
              but by the real encounter of photons<br>
              reflected by the world&rsquo;s things<br>
              and the silvered surface of the film.</p>
            <p>Lavery works with colour, pushes<br>
              little slabs and patches of it,<br>
              kneeds it with scrabbling knuckles,<br>
              thumbs; it is wet and messy;<br>
              like the world but only like,<br>
              like, not traces of the world itself.</p>
            <p>I stand before his paintings and look<br>
              at them. But he must step with his eyes<br>
              into the flat surface of my shady world<br>
              to recreate its depth and understand<br>
              the space that weaves between the objects<br>
              and the people shown there. He must be</p>
            <p>a sleuth, like the maker of the photo,&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <br>
              retouch with his mind the images he sees.<br>
              He hurt me and I let him<br>
              because I wanted so much to smile<br>
              back beneath the dark hood<br>
              of my camera, focus on him<br>
              straw-hatted man, model of mine,&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; <br>
            centre him between my lenses.</p>
            <p>&nbsp;</p>
            <p>This poem draws on ideas presented in Serge Tisseron's wonderful study <em>Le Myst&egrave;re de la Chambre Claire</em> (Paris, Flammarion, 1996)</p></td>

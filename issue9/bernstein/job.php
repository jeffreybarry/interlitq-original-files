            <p><strong>Downstream</strong></p>
            <p>Downstream from all this spilled blood<br>
              Will they find new ways to love the past?<br>
              Will they conclude just as we conclude<br>
              That the past is worth a backward glance<br>
              When it overflows like a bleeding wound?</p>
            <p>Downstream from this fine-grained dust<br>
              That flies in our faces and in our eyes<br>
              Will they recall as we recall<br>
              All that fire has refused to burn?<br>
              Will they find new ways to love the past?</p>
            <p>I walk down a street that has roots<br>
              Where a man anticipates love<br>
              And the events that unfold in pedestrian time<br>
              Are rooted in rituals of a slower kind<br>
              As surely as truth is rooted in truth.</p>
            <p>Downstream from these day-old winds<br>
              Will they tell themselves, as we tell ourselves,<br>
              That the air in their lungs <br>
              Is no deeper than the air in anyone&rsquo;s lungs?<br>
              And with time-honored beliefs, will they honor time?</p>
            <p>Downstream from all this, <br>
              Will they ask themselves, as we ask ourselves,<br>
              What their ancient liberties were, what their future ones  will be?<br>
              Will they find new ways to love the past?<br>
              Will they find new ways to love the past?</p>
              <p>&nbsp;</p>
            <p><strong>What&rsquo;s Left</strong></p>
            <p>What&rsquo;s left of the wind of a hundred years ago<br>
              Blows in fits and starts<br>
              Caught by fortunate ones who turn a corner<br>
              To find an old wind blowing in their faces<br>
              For a moment. Why it happens to blow<br>
              Down my street right now<br>
              Neither I nor anyone else would know.<br>
              The wind of a hundred years ago leaves thousand-year traces.<br>
              And the fingers that made the world out of clay,<br>
              Not even they &mdash;<br>
              Not even they could probably unravel all the gusts and  blasts <br>
              That bring the wind back to its old places.</p>
              <p>&nbsp;</p>
            
            <p><strong>Sweet Grapes</strong></p>
            <p>Precise words are precious things,<br>
              Sweet grapes are a good life.<br>
              A man is subject to many different thirsts<br>
              Not the least of which is the thirst for love<br>
              But foremost among them is the thirst for memory.<br>
              So close your eyes and go to sleep <br>
              And dream about enduring things<br>
              And things that linger mysteriously in the world<br>
              Like propagating waves in deep water.<br>
              Precise words are precious things,<br>
              Sweet grapes are a good life.</p>
              <p>&nbsp;</p>
            <p><strong>I Saw My Lady Weep</strong></p>
            <p>I thought I saw my lady weep<br>
              Glistering tears. I thought I saw her weep <br>
              For the bitterness of our lives. For we labor in mystery<br>
              And we labor in bitterness. We command <br>
              Artificial things to become natural things<br>
              But memory refuses to take them and nature rejects them.<br>
              Fields are deserted and wasted, dwellings are abandoned,  churches are in ruins &mdash;<br>
              The sword remains the first memory we have of existence<br>
              And the blowing of the wind over an open stretch of water.<br>
              I saw my lady weep glistering tears<br>
              For we labor under the yoke of undiscoverable laws<br>
              And there is weariness in the face of chaos.</p>
              <p>&nbsp;</p>

                  <td class="texto2" valign="top"><p>A native of South Carolina, PAUL SCOTT DERRICK has lived in Valencia, Spain, since 1980 and has taught at the University of Valencia, where he is a Senior Lecturer in American literature, since 1989. His main field of interest encompasses Romanticism and American Transcendentalism and their influences on subsequent artistic and intellectual manifestations of the 20th and 21st centuries. </p>
<p>His critical works include <em>Thinking for a Change: Gravity’s Rainbow and Symptoms of the Paradigm Shift in Occidental Culture</em> (1994) and <em>We stand before the secret of the world: Traces along the Pathway of American Transcendentalism </em>(2003). </p>
<p>He has edited and co-translated into Spanish a number of critical editions of works by Ralph Waldo Emerson, Emily Dickinson (Spanish and Catalan), Henry Adams and Sarah Orne Jewett. He is co-editor, with Viorica Patea, of <em>Modernism Revisited: Transgressing Boundaries and Strategies of Renewal in American Poetry</em> (Rodopi, 2007) and is, with Norman Jope and Catherine E. Byfield, a co-editor of <em>The Salt Companion to Richard Berengarten</em> (Salt, 2011). He has, in addition, published English translations of poems by Jorge de Montemayor, Luis Cernuda, Pablo Neruda and Jorge Luis Borges and, with Miguel Teruel, a translation of Richard Berengarten’s <em>Black Light </em>into Spanish (JPM Ediciones, 2012).</p>
<p>His critical essays, translations and poems have appeared in various American and Spanish journals since 1982. He is currently coordinating a series of critical studies and Spanish translations of Emily Dickinson’s fascicles.

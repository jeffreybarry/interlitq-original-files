                  <td class="texto2" valign="top"><p>Sunday (Sunnie) Enessi Ododo   is a poet, dramatist, stage designer and scriptwriter who speaks Ebira,Yoruba,   Hausa and English. Born in Maiduguri, Nigeria, in 1962, he studied Performing Arts and Theatre   Arts at the Universities of Ilorin and Ibadan respectively and has a   PhD in Performing Arts from the University of Ilorin. A senior lecturer   in the Department of Creative Arts at the University of Maiduguri, Nigeria,   he has designed and produced many stage plays. His versatile writings   include several genres: <em>The Revelation </em> (TV drama screened by NTA Yola, Nigeria), 1987; <em>To Return from the   Void</em> (opera), 1998; <em>Vanishing Vapour</em> (dance libretto), 2006; and <em>Hard Choice</em> (drama), 2008. His poems   have appeared in anthologies such as <em>Rising Voices: An Anthology   of New Nigerian Poetry</em>, 1991, and <em>Camouflage: Best of Contemporary   Writing from Nigeria</em>, 2006. His scholarly work has gained considerable   attention, especially his &lsquo;<em>Facekuerade</em> theory&rsquo;, which derives   from the maskless transformational practices of traditional Ebira masquerades   (<em>eku</em>). He has also edited journals like<em> Alore: Ilorin Journal   of the Humanities</em> and <em>The Performer: Ilorin Journal of the Performing   Arts</em>, and his essays have been published in journals in the USA,   the UK and Nigeria, including: &lsquo;Womanhood and Virgo Intacta: Form   and Aesthetic Reconstruction in Ovia-Osese Performance&rsquo; in <em>Ufahamu:   A Journal of African Studies</em> 30 (2/3) 2004; and  &lsquo;&ldquo;<em>Facekuerade</em>&rdquo;:   The Transformational Duality in Ebira-Ekuechi Festival Performance,&rsquo; <em> Cultural Studies</em> 22 (2) 2008. With Duro Oni, he has co-edited <em> Technical Theatre Practice in Nigeria: Trends and Issues</em> (Lagos:   Centre for Black and African Arts and Civilisation &ndash; CBAAC, 2006),   He is on the editorial board of <em>The Perfformio</em>, a University   of Wales onlinejournal for the performing arts, (<a href="http://perfformio.org/default.aspx" target="_blank"><u>http://perfformio.org/default.aspx</u></a>). A member of the Association of Nigerian   Authors (ANA), he is chief judge of ANA literary prizes.&nbsp; He currently   serves as vice-president of the Society of Nigerian Theatre Artists   (SONTA).&nbsp;<br>

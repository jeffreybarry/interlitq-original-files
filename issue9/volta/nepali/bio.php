                  <td class="texto2" valign="top"><p>Hom Paribag was born in 1971 in a mountain village in Nepal, the youngest of nine siblings. At the age of 13, he won first prize for a play about blindness in a competition organised by Radio Nepal, then the only radio station in the country. His play was broadcast nationwide. After high school, he worked as a painter and sign writer. During a brief period of unemployment that coincided with the 1990s movements for democracy in Kathmandu, and while waiting to be enrolled in college, he took up writing seriously, as a poet, print journalist and playwright.</p>
                    <p>His first book, published in 1989 at the age of 19, was the poetry collection <em>Dhoon Napayeka GeetHaru</em> [&lsquo;Songs Without Melodies&rsquo;], followed in 1991 by his biography of a famous Nepalese singer, <em>YatraHaru Narayen GopalKa </em> [&lsquo;The Journeys of Narayen Gopal&rsquo;]. His first published novel <em>Yaad Harayeko Maanchhe</em> [&lsquo;The Lost Memory Man&rsquo;] became an overnight bestseller in 1992. At the same time, he was developing his career as a journalist and editor, editing two successful magazines and writing for national magazines, dailies and weeklies in Kathmandu, all the time continuing to work at his poems.</p>
                    <p>Two further collections followed: <em>Parichaya Tyo</em> [&lsquo;The Identity That&rsquo;] in 1994, and <em>Lukdainan Manaka ByethaaHaru</em> [&lsquo;The Soul&rsquo;s Pain Wont Hide&rsquo;] in 1995. His second novel <em>Samaya Pari</em> (<em>Beyond Time</em>), which is due for publication in London in 2010-11, is set in Nepal, Bihar and Kolkata, India. It is about the plight of the street children of Kathmandu and the sufferings imposed by the caste system in some South Asian countries. Since 1996, Hom Paribag has lived in London.</p>
                    <p>In 2004, he founded the English language publication <em>Society Today Magazine</em>, which is published in hard copy and online (<a href="http://www.society-today.com/">www.society-today.com</a>).</p>

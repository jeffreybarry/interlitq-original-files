          <td height="219" colspan="2" valign="top" class="texto2">  
           
              <p>This  unusual anthology contains ninety-three poems in ninety-three  languages. Ninety-two of these poems are translations of one poem,  the ninety-third.   The anthology, which began in issue 9 of ILQ, with seventy-five languages, is still growing.  </p>
              <p>As Richard  Berengarten writes in his introduction, this project is &ldquo;a celebration  of multiculturalism and diversity&rdquo;. Apart from invoking the pleasure of  learning about different languages, the anthology opens up many  questions. For example: How many languages could this project extend  to? Is translatability a universal feature of language itself? What  does &lsquo;originality&rsquo; actually mean? What difference is there between  writing and translating a poem? And could the fact that these poems  come from all over the world bear out Octavio Paz&rsquo;s claim: &ldquo;For the  first time in our history, we are contemporaries of all humanity&rdquo;? </p>

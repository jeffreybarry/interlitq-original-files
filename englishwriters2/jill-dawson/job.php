<p>When I was in the sixth form we were taken on a school visit to see Ted Hughes at the Yorkshire Playhouse.  I remember sitting in the front row and tittering as the tall man stepped out from the curtain and strode towards the one stool on the stage.   He was not the commanding figure we’d expected.  ‘His flies are undone!’  Vicky Stables whispered beside me.  Hughes didn’t look like a poet should, we girls thought.  We didn’t approve of the dark green raincoat he wore loosely around his shoulders:  the sort a ‘dirty old man’ would wear.  </p>
<p>But then he sat on the stool and began reading.  He had a Yorkshire accent, to our surprise, and a voice of rolling warmth and power.   He began reading <em>The Thought Fox</em> and it was as if someone had turned a key in my back.  ‘I imagine this midnight moment’s forest….’ </p>
<p>I sat up straight.  We’d read the poem many times in our brand new sixth form classrooms at our local comprehensive.   ‘Yeah yeah’, we said, ‘we get it’, when Mr Foggin laboriously explained that it wasn’t just about the real fox but about the writing of the poem, about imagination, memory, creation and recreation.   </p>
<p>‘Two eyes serve a movement that now, and again now, and now, and now….’</p>
<p>Over thirty years later and I can hear Hughes voice, intonation, and the impact of those words on me. He didn’t read in the modern drone that poets like Simon Armitage use.  He practically shouted the line: ‘with a sudden hot sharp stink of fox’.  Vicky Stables jumped out of her skin. She was too mesmerised to titter this time.  </p>
<p>The words had entered ‘the dark hole’ of <em>my</em> head.   Poetry had me in its snare.  You could say I was, perhaps more than other girls from Boston Spa Comprehensive who sat in the front row of the Playhouse that day, a ready candidate.  I was anorexic.  I weighed around six stones and had lately begun to notice a downy hair growing on my forearms.  I was having a relationship with a bullying, needy boy a couple of years older than me, who worked in the carpet store at Thorp Arch Trading estate and bought me giant bars of chocolate to try and ‘feed me up’. But I found it hard to fathom the ways his moods flipped towards me: one minute I was a bitch and worse; the next he begged me to marry him. This young man later told me he was being sexually abused by his older brother at this time. This might have gone some way to explain his frightening behaviour towards me. But I was a girl of sixteen and I didn’t know that then.</p>
<p>Our school trip to see Hughes changed my life.  I wanted to be a writer.  I think I knew this already but in that badly-lit theatre, listening to Hughes murmuring ‘the window is starless still, the clock ticks, the page is printed,’ I knew it with a sense of dread certainty.  I want to make people feel things; I want to enter the dark hole of others’ heads.    The problem was that being a poet, or a writer of any kind, was an impossible, ridiculous ambition for a girl from my background. What did my parents hope for? My mum thought a teacher would be nice.  (She was in awe of teachers.  In my teenage eyes it was a lowly profession). Neither parent had gone to University themselves.  There was a vague hope that I might, but no clear plan for how this miraculous thing would happen.</p>
<p>I had already had a poem published in a women’s magazine, but I didn’t tell teachers or parents that.  I’d been so embarrassed on seeing it there in the newsagent that I shut the pages and left the magazine behind.  The poem was about my anorexia and contained the strange truth I could only articulate as a poem - that if I carried on not eating like this I would surely die.  </p>
<p>Poetry and embarrassment were bedfellows to me.  My Dad had once said to me that he couldn’t see the point of any books that weren’t practical or instructional (our house had an entire set of Encyclopaedias with red hardback covers that were not often disturbed, and many books on gardening and golf, which were).  When I dig around now, trying to find the reason for the source of both the fierce obstinacy and the utter hopelessness about my desire to write, all I can find is a weird feeling of shame. Seeing Hughes – <em>who had an accent like mine</em> – made something shift, made something preposterous at last just a tiny bit possible.  </p>
<p>By the time I was in my mid-twenties I was visiting schools as a published writer too, to offer workshops and readings.  Over about a decade of doing this I visited young offender’s institutes, a school in Chicago for pregnant teenagers, a school in the Chicago projects, libraries, literacy projects, schools in Hackney and Tower Hamlets and South London Comprehensives much tougher than my own.  I often got the impression that lurking in my workshop group was a girl or boy who looked at me exactly the way I had looked at Ted Hughes. Ah, so that’s what a writer sounds like, looks like.  Not so posh, after all.  Not so foreign. Not dead.  Someone like me.</p>
<p>Once, a sixteen year old boy in the Secure Unit I was visiting wrote me a short story and begged me not to read it in class, but afterwards, after I’d left.  ‘I don’t want to see your face when you read it,’ he said, handing me the paper.  I read it on the train going home: the story of a boy in a Secure Unit who is about to go out to sixth form college, after several years inside and who has been having workshops with a visiting writer – a woman, he writes, who reminds him of his long-dead mother.  ‘Mum believed in him, she was the only one, until now.’  I knew this boy was inside the institution for arson.  But he told me in this ‘true story’ what he couldn’t do in class:  his father had killed his mother and then committed suicide when he was seven years old.  He had been in care for a long time; this was not deemed relevant to the arson, committed at fourteen.  So therefore no one had mentioned this in his defence and he simply wanted to tell me about it, so I would think better of him.</p>
<p>The following week, on my last visit, he didn’t want to talk about the story, though we both acknowledged that I had read it.  We talked about the fact that Dwayne (as I’ll call him) was about to leave the Secure Unit and go out in the world, and was thrilled about his place at a college.  We talked about the future, and I asked him how he imagined his life outside.  Different, he suggested, with hesitation. Then more confidently: different.  The officer who had arranged my visits and helped Dwayne with his college application beamed:  <em>Result</em>. Sometimes a young person – especially a troubled or unhappy one - can only picture more of the same.  Nature abhors a vacuum and so does the future. It will grow to fit the shape we hold for it. </p>
<p>Back to <em>The Thought Fox</em> again, and the power of what we can imagine and how it creates reality.  ‘Brilliantly, concentratedly coming about its own business’.  Writers’ visits to schools shouldn’t be a luxury, an add-on, only for well-off schools that can afford them.  The arts are as essential a subject as any we have.  Sometimes a visiting artist or writer, a trip to the theatre, can change lives. I am grateful to Hughes and to poetry for saving mine.</p>



<p><strong>Prayer of Joseph</strong><br><br>


My mountain, <br>
my food and rest, <br>
my angel as both ancient and young child, <br>
my breath. <br><br>

My boat, <br>
my returning angel, <br>
my night light, <br>
my birth. <br><br>

My mother, <br>
my three in one, <br>
my soul's rest, <br>
my word next to word, <br>
paraphrase of the child. <br><br>

My heart, <br>
my umbilical cord, <br>
my woman receiving annunciation, <br>
my announcing angel, <br>
my ave. <br><br>

My pilgrimage of stillness, <br>
my mother of the great gift, <br>
my visitation of Heaven to earth. <br><br>

My engine of harpies, <br>
my pure in face of mockery, <br> 
my feet in the mud, <br>
my sinners' city. <br><br>

My lovable one, <br>
my reversal of thunder, <br>
my listener, serene with hand held up to praise, <br>
my on-side in lonelinesse. <br><br>

My counsel, understanding both the spear that pierces and the knife that circumcises, <br>
my mother in the intimacy of birth, <br>
my world of six daies. <br><br>

My vision transposing gratitude, <br>
my power prudent in the moment, <br>
my Adoration, <br>
my kind of tune. <br><br>




My message which all things hear, <br>
my blood's rejection of fear, <br>
my softness in flight, <br>
my beauty and peace in the shadow of the sepulchre. <br><br>

My mirror of truthfulness, justice and joy, <br>
my throne of eight angels on a seat of wisdom and love, <br>
my scarlet cushion, <br>
my gold and glorious garment. <br><br>

My cause of joy and bliss, <br>
my redemption of yearning, <br>
my breath's vessel, <br>
my exalted food. <br><br>

My fruit, <br>
my swallow, <br>
my vessel of honour and gladness, <br>
my bride, singular vessel of devotion to the best. <br><br>

My rest and sweetness in the inhospitable, <br>
my mystic rose, <br>
my centre, <br>
my tower in the ordinarie. <br><br>

My Heaven, <br>
my night flight, <br>
my woman-man-child, in the cloak of holiness dressed, <br>
my waterfall of blue. <br><br>

My house of gold, <br>
my milkie way, <br>
my bird of paradise in the burning bush, <br>
my Queen, crowned with tenderness. <br><br>

My gate of Heaven, <br>
my church bells, <br>
my veiled in prayer, <br>
my eyes with which I have seen the morning star. <br><br>

My music of angels in the oak and laurel heard, <br>
my health of the sick, <br>
my mother suckling the bright infant when exhausted on a journey, <br>
my refuge of soules. <br><br>

My daughter of Ann and Joachim who met at the Golden Gate, <br>
my birth blood washed away by the serving maid, <br>
my help when naked in the storm, <br>
my land of spices. <br><br>

My throne of bad and good, <br>
my sweet grapes, <br>
my open book, <br>
my something understood. <br><br>

My prophet, <br>
my sorrowing apostle, <br>
my heavenly bliss, <br>
my knife of martyrs, <br>
my carried in procession by Cimabue confessors. <br><br>

My giver of grace in a rocky landscape, <br>
my vision of Saint Bernard, <br>
my calling into being flowered meadows of your own being, <br>
my exquisite under the Holy Paraclete, assumed into Heaven, private. <br><br>

My lady with your feet on the earth, <br>
my lady of the most holy Rosary, <br>
my lamented by apostles Queen of peace, <br>
my throne of lions. <br><br>

My shoulders hunched in extreme grief under the cross, <br>
my humility in blue and black, <br>
my Queen in blue on a flowering meadow. <br><br>

My Mary listening with celebrating angels, <br>
my Mary clothed in the splendour of the Trinity, <br>
my virgin among holy women. <br><br>

My grieving mother, <br>
my woman hooded in white, <br>
my virgin with book and lutenist angel, <br>
my grieving mother. <br><br>

My stronghanded mother, <br>
my mother of the rosary zikr, <br>
my golden and beautiful in mandala of childness, <br>
my Mary, the mother without pinks. <br><br>

My Queen of Heaven, <br>
my grieving mother, <br>
my mother who discovered in the temple, <br>
my virgin in the embrace of prayer. <br><br></p>






Our hero’s father, hurling himself into waves below, <br><br>
a caterpillar flung into my leaky beard. <br><br>
The child is father to the man.    <br><br>
And this one’s gift? To procreate death. <br><br>
A womb black and leathery as midnight bats. <br><br><br><br>


Bull unslaughtered. <br><br>
A double stands in, <br><br>
one tauros looks much like another<br><br>
(<em>videlicet </em>any Iberian corrida). <br><br>
Daedalus, supreme mechanic, asked to contrive<br><br>
a wooden heifer into which Pasiphae<br><br>

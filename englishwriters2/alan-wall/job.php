(his one and only) <br><br>
he throws himself, dim-eyed and doting, into the sea<br><br>
which promptly absorbs his flesh<br><br>
bones blood name<br><br>
henceforth the Aegean<br><br>
introducing him later that day<br><br>
to the artificer’s son. <br><br>
Divers from diverse generations. <br><br>
No shoes required. No crowns. <br><br>
Subaquean protocol alone obtains down here. <br><br><br><br>

Poseidon licks his lips with satisfaction. <br><br>
Snoozes wetly as amniotic tides<br><br>
salute the moon. How fluently the hours<br><br>
proceed. <br><br><br><br>

<strong>5</strong><br><br><br><br>

Years later…decades – the calendar’s occluded here –<br><br>
Theseus in need of a wife<br><br>
(who lost the Amazonian mother of his son<br><br>
Hippolytus) <br><br>
picks up Phaedra, Ariadne’s sister (well, why not?). <br><br>
Gospels tell us <br><br>
best not to let the right hand know<br><br>
what the left is doing. <br><br><br><br>




<strong>6</strong><br><br><br><br>

Dear Phaedra, <br><br>
I hope you are happy with him now. <br><br>
I remember that first night on the boat, how happy I was. Ravished and ravishing. The tide beneath us swelling almost as much as he did. <br><br>
Felt like our dear mother must have done with old Tauros Blanco in his high field, white as the hair on Poseidon’s waves, squeezing and thrashing through her harbour walls. <br><br>
On Naxos that night he led me to the tanglewood<br><br>
only noticing after our third embrace<br><br>
a shrew, its minute neck broken<br><br>
staring so emptily<br><br>
up into the moon’s white face. <br><br>
We stepped apart then<br><br>
as though an invisible cortège<br><br>
must be given passage between us. <br><br>
(Memory like an elephant, haven’t I?) <br><br>
Live here in heaven now with Dionysus my redeemer, as you might have read in the periodicals. A god’s wife. Dinners are grand, believe me. They come from such enormous distances to get here. Some haven’t eaten for a year. And some have suns for eyes. Certain evenings I have to wear shades even to sit at the mahogany table and keep smiling. All in the fast lane, chauffeured along at the speed of light. <br><br>
Some nights when he goes off<br><br>
to the great consult<br><br>
I stare at distant fires<br><br>
a million miles away<br><br>
a million years<br><br>
see mighty shadows<br><br>
(part of this family once) <br><br>
raising their mighty shadow hands to the flames<br><br>
as if to keep warm inside time’s ruins. <br><br>
Tell him that I resent nothing. Except maybe our half-brother. Euthanasia really wasn’t the answer, I’ve come to believe. A vegetarian diet might well have calmed him. Or maybe a beautiful white heifer, sprinkled with the merest hint of Poseidon’s magic salt. A little salted bull’s wife settling beneath him. She could have shouted at midnight almost as loud as he did. Or I did once. <br><br>
Your loving sister, <br><br>
Ariadne<br><br><br><br>

<strong>7</strong><br><br><br><br>

Aphrodite still looking to get even. <br><br>

Half-man half-bull<br><br>
keeping all his ontologies open. <br><br>
Daedalus & Son obliged to join his party: <br><br>
a fellow imprisoned on the isle of ingenuity<br><br>
in a Royal Society of one. <br><br><br><br>

Dark in there, and damp, <br><br>
a cellar in Beirut, say, or a room in the Lubyanka, <br><br>
Polish cattle trucks circa 1943. <br><br>
Took away his liberty, not his know-how. <br><br>
Wings fashioned for both father and son. <br><br>
In the dark hear the clink of makeshift tools. <br><br>
POWs digging through the stalag, as Icarus stands look-out. <br><br>
Half-bird half-man<br><br>

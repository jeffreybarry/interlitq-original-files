drag him to an early death. <br><br><br><br>

Only then does Theseus learn<br><br>
how Phaedra in her fatal misery <br><br>
had lied, turning all Poseidon’s oceans<br><br>
into one large angry womb.     <br><br><br><br>






<strong>8</strong><br><br><br><br>

<em>Poseidon’s Diary</em><br><br><br><br>


evading Cretan ground-to-air artillery, the Minoan flak. <br><br>
Noon next day, swooping over wine-dark seas, <br><br>
Icarus ventures higher even than an eagle dares and<br><br>
SPLASH<br><br>
down he goes in the water<br><br>
highest of all high-divers. <br><br>
Final column of the young man’s life<br><br>
one line of zeroes, dioxide bubbles<br><br>
rising from the swervy blue<br><br>
where a distant waxwing plummets. <br><br>
Time stops. His father’s wings outstretch, hold still. <br><br>
A windhover in heaven. <br><br>
The future now a speeding car windscreen<br><br>
catching the onrush of a sudden storm<br><br>
before wipers flick<br><br>
to synchronise the blinding wet away. <br><br>
The font down there<br><br>
in that big blue baptistery<br><br>
(where Icarus is christened for the second time) <br><br>
big enough to swallow the whole world. <br><br>
Daedalus watches tomorrow drown. <br><br>
How exactly had he ever engineered today? <br><br><br><br>

Poseidon opens that same fluent hole<br><br>
from whence a white bull thrashed out, <br><br>
closes one eye, yawns, <br><br>
briefly contented with his meal. <br><br><br><br>



<strong>2</strong><br><br><br><br>

Minotaur has regular eating habits, <br><br>
according to the Gnomon’s mighty shadow: <br><br>
each nine years seven youths and seven virgins. <br><br>
Proteinaceous, though lacking in roughage. <br><br><br><br>

One by one they’re pointed through<br><br>
vast labyrinth doors carved by Ghiberti. <br><br>
Rite of passage for the catechumens<br><br>
behind bronze gates<br><br>
opening on hell and heaven. <br><br>
One by one taken apart and eaten<br><br>
at leisure, organs sorted<br><br>
according to size piquancy vitamin quotient<br><br>
acidity (lifers tending to the bilious). <br><br><br><br>

Time comes round again, <br><br>
the face of Aegeus in Athens is sad. <br><br>
I’ll go, says his son Theseus -<br><br>
old man so heavy with the tribute demanded<br><br>
it makes a young one grieve –<br><br>
why ever agree to such danegeld? <br><br>

No, no, no. <br><br><br><br>

Dad, I said I’d go. <br><br>
I’ll handle it. <br><br>
So go he goes and handles it<br><br>
the way Michael did for his old man Vito Corleone. <br><br>
Only a question of killing, after all. <br><br><br><br>

<strong>3</strong><br><br><br><br>

Now another curse from Aphrodite<br><br>
(never any shortage there). <br><br>
Over in Crete Ariadne takes one swift look<br><br>
at this Athenian prince, falls<br><br>
entirely as though she were playing her mother, he the big white bull<br><br>
thrashing already inside her. <br><br>
Theseus, her flesh thinks not her mind, <br><br>
designed by Zeus to fit so snug between my thighs. <br><br>
Anything, then, Prinnie. Name your price. <br><br><br><br>

Entry and exit, he replies. <br><br>
Must get in there<br><br>
kill<br><br>
then out again, scrub down, and home. <br><br><br><br>

Here my love’s a little glamour and a clew. <br><br>
With one you kill him, my half-brother, <br><br>
with the other <br><br>
find your way back here to me. <br><br>
The thread’s umbilical. <br><br><br><br>

One condition only, given so great love<br><br>
to betray my flesh and blood<br><br>
for a crew of alien carpetbaggers hunting gold<br><br>
this quisling passion they’ll call it. <br><br>
Little tart, no better than she should be. <br><br><br><br>

Take me with you darling when you go.     <br><br><br><br>

<strong>4</strong><br><br><br><br>

Done. Ariadne sails along with him. <br><br>
On Naxos two days later dumps her. <br><br>
One ancient parish record says she hangs herself<br><br>
using the magical thread of that same clew<br><br>
brought our boy safely through the labyrinth<br><br>
once he’d slaughtered her half-brother. <br><br>
Anything to quell the wandering womb<br><br>
Greeks reckoned made for a woman’s throat <br><br>
without man’s gravity to hold it down. <br><br>
Or bull’s. <br><br>
Back in Greek waters<br><br>
our lads still carousing on deck<br><br>
Signor Hero forgets to change black sail to white<br><br>
as agreed with the old man a lifetime (plus one day) ago<br><br>
in Athens. <br><br><br><br>

Aegeus has stared out from cliff-tops<br><br>
day after day after day<br><br>
sees those black sails at last and knows<br><br>

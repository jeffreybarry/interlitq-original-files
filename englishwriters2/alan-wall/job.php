might navigate a trunk road <br><br>
through the interior of her dark continent. <br><br>
Cue moonshine. White disc of a bull-face waltzing. <br><br>
And Aphrodite’s floating clouds of laughter<br><br>
feathering a full moon. <br><br><br><br>

Outcome on the far side of the equals sign: <br><br>
Minotaur. <br><br>
First bellow announces<br><br>
flawed genealogies<br><br>
miscegenation of species<br><br>
omission from next year’s royal Sandringham photographs. <br><br><br><br>

Daedalus must now construct a labyrinth <br><br>

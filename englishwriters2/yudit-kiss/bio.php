             <p>Yudit Kiss (1956) was born and educated in Budapest, Hungary. She is a development economist, who initially worked on Latin American issues, switching gradually to the problems of Eastern Europe. She carried out research for various academic institutions in Britain, Finland, Germany, Hungary, Mexico, Sweden and Switzerland. She is the author of two monographs and numerous articles, book chapters &amp; research papers on subjects primarily related to the post Cold War transformations of Eastern Europe. She was a recipient of a Leverhulme Research Grant at the Institute of Development Studies (UK) and a Research and Writing grant from the MacArthur Foundation. After having worked in Hungary, Mexico and the United Kingdom, in the early 1990s she moved to Geneva, Switzerland, where she is currently based and works as an independent researcher.</p>

<p>Her articles of general interest on politics, social change and culture have been published, among others, by the <em>Guardian, Lettre Internationale, Nexos, El Nacional, Gazeta Wyborcza, Eurozine</em> and <em>Opendemocracy</em>.</p>

<p>Her literary texts have been published in <em>Lettre Internationale, Himal South Asian</em> and <em>Berfrois</em>.</p>
<p>Her novel, <em>The Summer My Father Died</em>, first published in Hungarian, was translated to Czech and English and was shortlisted for the JQ-Wingate Prize. Her current writing project, supported by a British Society of Authors’ grant, deals with children Holocaust survivors, broken revolutions and exile.</p>




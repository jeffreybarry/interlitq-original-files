<p>Anthony Rudolf, author, literary and art critic, translator and editor, is a distinguished citizen of the 20th century Republic of Letters. In his latest book, <em>Silent Conversations: A Reader's Life</em> he invites us to visit his personal library. Visiting a private library is an intimate act; one enters into a life lived through books and with books - discovering affinities and elections, haphazard encounters and life-long friendships, political convictions and the caprices of taste. We never leave the rooms, packed with books in an ever-growing, organic chaos; still we come across the fundamental questions of our existence: life and death, love, peace and war, politics, nature, memory, identity, family, language, religion and the like. What are books, after all; the crystallised knowledge and experience of our fellow humans – and since Rudolf has certainly read more of them then most of us ever have, he generously shares with us the fruits of his travels amongst the pages. </p>

<p>The presented books include poetry, prose, art criticism, biographies, books about art, even textbooks and books of natural and social sciences. Depending on the subject and the mood of the author, the genre changes from literary essay to diary, from autobiography to a post-modern encyclopaedia, from travel book to memoir. The tone also keeps shifting from lucid literary analysis to leisurely chat, from complex metaphysical meditations to confessions, from dramatic visions of our world to humorous accounts of the vicissitudes of everyday life. </p>

<p>The key landmarks of Rudolf’s literary landscape are French and Russian literature and Jewish worlds. Exploring France and French literature has been Rudolf’s ‘<em>voyage initiatique</em>’ through which he learnt crucial lessons about our world, our place in it and himself, learning self-reflection in the ‘other’. He is as much at home there as in the Anglophone world and is able to map its inner laws, its complicated rituals, its place in the world’s culture, the invisible paths that link Rimbaud to Proust, or, to quote more unusual ones, Victor Hugo to Miles Davis and Marguerite Duras to J. M. Coetzee. Rudolf’s other home is the Jewish world in all its manifestations, including the Torah and klezmer music. The reviewed works of Jewish writing keep returning to the trauma of the Holocaust; how could the immense, rich, diverse pre-WW2 world of European Jewry be destroyed (driven by ideas that germinated in the heart of another rich and sophisticated culture); how such destruction could be survived and eventually commemorated. Russian and Eastern European literature are not homelands in a sense the previous are, but the sections dedicated to them reveal a fascination - mixed with a nearly nostalgic attachment, probably stemming from the author’s origins and interest in a landscape of losses - with a body of work that is profoundly rooted in its specific context, yet keeps addressing mankind’s universal existential questions.</p>

<p>There is another classification of the works by literary genre. Here the main domain is poetry, including poetry in translation, a particularly personal field for Rudolf who has been translating such outstanding poets as Yves Bonnefoy and Claude Vigée and admiring the works of others, like Rilke, to the point of integrating their texts into his personal vocabulary. Beyond fascinating comparisons and intimate knowledge of the works of such different poets as George Seferis and Charles Reznikoff, the author tackles poetry’s profound philosophical queries as well, like the connection between language and identity, experience and words, life and literature. </p>

<p>Most of the described works are contemporary, but the presence of the great old masters is palpable throughout the voyage; Homer’s Odysseus, the eternal traveller is wandering around with us, like Dante, guided by Virgil, and Shakespeare, who even appears on the cover of the exquisitely crafted book. Worthy of Rudolf’s ambitious undertaking, Calcutta-based Seagull publishers produced a beautiful book that is a pleasure to touch and read. </p>

<p>It’s a courageous act to write 700-odd pages full of quotes and references about books today when the rapidly advancing information technology, the emphasis on the image versus the printed or pronounced word threatens to marginalise the Gutenberg galaxy. But once one is lured into this rich and entertaining labyrinth, one discovers the pleasures of travelling through feelings, ideas and theories of different historic periods and cultures with the help of books. Rudolf is a generous and entertaining guide who ushers his readers safely through the changing dimensions of different realities; often leaving our present material world to enter to vanished universes or imaginary countries of dreams, ideas and utopias.  </p>

<p>The text is broken up into short sections, each one an elegant mini-essay that can also be read in a sequence, following the author’s associations. We can move from Pushkin’s Eugene Onegin over to Nabokov and his relationship to language, touch upon <em>saudade</em>, the heart of Portuguese fado and arrive at Puccini’s Tosca. We can discover shared motives in the work of Baudelaire and Paul Celan, we can travel from Hamlet to Borges and on to Peter Brook; raise our glass of wine with Sadegh Hedayat and Primo Levi. </p>

<p>The writing, as always with Rudolf, is elegant, erudite, precise and full of quotes; he invites his authors, dead and living ones, to join in the silent conversation that takes place between him and his reader. After a while, one is not intimidated any longer by the myriads of criss-crossing references many of which remain obscure for most of us, simple mortals, who lack the rich encyclopaedic knowledge Anthony Rudolf possesses. They are like invisible threads in a rich fabric; they keep the piece together, even if we don’t see them. They keep reconnecting people, ideas and motives, reveal parallels, ongoing dialogues between authors and schools of thought that are far apart in time and space. These live connections reveal a coherence of shared feelings, a continuous, organic development of ideas; manifesting the universality of human experience and thinking. A rare, precious gift in our broken, disintegrating, ever more conflictual world.</p>

<p>In the soothing silence of the library, protected from the noise of the ephemerae, we are served delicious, slow food for thought. We can forget about the brutal, hectic events of the outside world and concentrate on the essence. Or rather, we cannot forget, but are given a space for reflection, appreciation and understanding. A chance to feel human connected to other humans thanks to the books we keep writing, reading and sharing with each other. </p>


<p>Anthony Rudolf, <em>Silent Conversations: A Reader's Life</em> (Seagull Books/Chicago University Press, 2013). </p>







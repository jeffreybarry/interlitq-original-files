                  <p><strong>Rodolfo Zagert. </strong>Nació en Eldorado en Misiones, Argentina. En 1951 se traslada con su familia a Buenos Aires, volviendo sin embargo varias veces por largos períodos a Eldorado. Durante todo el año 1961 reside en Canadá.</p>
<p>En 1962 inicia su formación de dibujo y pintura en distintos talleres de Buenos Aires. En 1968 comienza sus estudios universitarios de arte y arquitectura. A los 19 años realiza su primera exposición individual en la mítica Galeria Lirolay en la capital argentina.</p>
<p>De 1972 a 1976 continua, becado, sus estudios en Alemania. Permanece en ese país donde desarrolla su producción artística en el campo de la plástica y la arquitectura, hasta 1991, año en que se traslada a Palma de Mallorca, España.</p>
<p>En 1999 le es concedido el prestigioso premio <em>Ciudad de Palma de Arquitectura</em> por la restauración integral de un palacio del Siglo XVI.</p>
<p>En 2003 resuelve disolver su estudio de arquitectura para dedicarse exclusivamente a las Artes Plásticas.</p>
<p>Desde el año 2006 vive y trabaja alternando entre Palma de Mallorca y Buenos Aires.</p>

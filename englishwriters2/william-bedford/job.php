<p><strong>THE FLITTING</strong><br>
<em>24th July 1841: John Clare</em><br><br><br>


Two wives I have, <br>
iffen folk say no, <br>
law say no, <br>
feeble brain imagines. <br><br> <br>


I’ve walked these miles, <br>
bramble, hazard, <br>
carters’ wiles, <br>
a gypsy crawl<br><br><br>


to find<br>
a home in Mary’s smile. <br>
And still I reckon, <br>
sing love tunes: <br><br><br>


huswife and bedwife, <br>
to bicker over moons; <br>
a harvest supper<br>
wi wooden spoons. <br><br><br>


‘Touched,’ <br>
the neighbours snigger. <br>
I whistle rhythms<br>
nobbut fools chose. <br><br><br>


And walk at night, <br>
nimbling poacher’s ways, <br>
sleep on clover trusses, <br>
chew grass ‘cos<br><br><br>


fish gone, game gone, <br>
no trespasses <br>
allowed <br>
now fields and common closed. <br><br><br>



I chew tobacco too, <br>
then swallow, <br>
no lucifers to light my pipe, <br>
smoke fumes enew. <br><br><br>


Two wives I have, <br>
acrost bare fields of stubble, <br>
no pastoral views, <br>
childern learned to reddle. <br><br><br>


I walk <br>
where wind whips elms: <br>
frozed night, <br>
red gypsy hat for warmth or trouble. <br><br><br>


I walk<br>
where Eden used to be, <br>
haymaking games<br>
a game for saucey drovers. <br><br><br>


Then Patty<br>
says me home. <br>
Sweet Mary died. <br>
And how can I forget? <br><br><br><br><br><br>





<strong>nimbling</strong> moving nimbly <strong>reddle</strong> riddle <strong>enew</strong> enough<br><br><br><br><br><br><br><br><br>
 



<strong>I AM</strong><br>
<em>‘I am, yet what I am, none cares or knows.’*</em><br><br><br>


A taproom weaver show’d me heaven, <br>
Thomson’s Seasons his drunken fun, <br>
my heart lurching like a cart of hay. <br>
I knew he laughed, mocked the lines, <br><br><br>


blank verse or rhyme a thing for hymns<br>
or ballad singers’ cheapest trash, <br>
chapel mornings or taproom brags. <br>
I had to own such verses. I had to sing. <br><br><br>


Teazed the shillings off my father, <br>
and walked to Stamford in summer rain. <br>
The lowering High Street stood deserted, <br>
bookseller and printer away from home. <br><br><br>


‘You never ‘eard o’ Sundays?’ <br>
a crowd of grinning childern clowned. <br>
I stole a day off work. Lost that job. <br>
Then sat in leafy Burghly sun, <br><br><br>


rhyming the blue shine of heaven, <br>
reckless in summer morning sunlight<br>
where skylarks weaved and spelled<br>
their own wild stories. The world to tell. <br><br><br><br><br>





<em>*John Clare</em><br><br><br><br><br><br><br><br><br>
 



<strong>THE GARDINER</strong><br>
<em>John Clare at Burghley House</em><br><br><br>


They’ve put <em>No Trespass</em> signs across the way, <br>
but fox don’t read, hare don’t stay to look, <br>
starnel and redcap up and leave. <br>
‘You’ll work to get a living,’ my old dad say, <br>
‘bow and scrape at gardiner’s ease, <br>
‘cos white stockings are allus grand, <br>
clean neck cloths summat proud.’ <br>
Master of the kitchen garden they call him, <br>
and him as bent as a struck bough, <br>
no matter how low you duck your head. <br><br><br>


 ‘A wauk child grown tall enew to work?’ <br>
he said, grinding the both of us down. <br>
‘How’s tha feel, good feelow now?’ <br>
I feel enclosure fencing me round. <br>
A garden allus cloyed me, <br>
red brick walls and ivy twined round trees, <br>
the gate no gate to heaven. <br>
But swallow words. Turn up for work. <br>
Swinkt till the toiling gets us down, <br>
then run, ‘fore crizzling water freeze. <br><br><br>


O then I knew the fribble games: <br>
fished struttle from the shallow stream, <br>
ran murmering woods and sudden breeze. <br>
In fallow fields and plasky meadows, <br>
chanted Thomson’s words unheard, <br>
no sneering clown, conscieted coxcomb, <br>
leering from the Blue Bell tavern, <br>
enjoying mirth at words I made my own. <br>
A poet’s work is hardest labour, <br>
No Trespass signs across the road. <br><br><br><br>




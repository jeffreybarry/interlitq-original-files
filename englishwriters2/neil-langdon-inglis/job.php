<p>By Neil Langdon Inglis<br>
U.S. General Editor, Interlitq</p>


<p>What kind of person questions that a historical episode actually took place?  Some 15 years ago, I stumbled upon a public lecture by Apollo denialist Ralph René who argued that the lunar landings were trick photography and nothing else.  Afterwards, I pointed out to René that cosmonauts Leonov and co., and indeed the entire Soviet space fraternity, would have monitored the Apollo flights from Baikonur; furthermore, the Soviet regime would not have passed up the propaganda opportunity afforded by such an audacious deception. Not so, Rene replied: the Americans had bought Soviet silence with much-needed grain shipments. </p>

<p>Soviet grain shortages and denialism went hand in hand. In the late 1980s, I had worked for the U.S. Congressional Commission on the Ukraine Famine, which followed on the heels of Robert Conquest's "Harvest of Sorrow" (1987), then the latest in a series of critiques of the Stalinist system which included "The Great Terror" (1968). Conquest's death in August 2015 after a lifetime of intellectual pursuits brought back my first encounter with the grim subject that Conquest had introduced to a disbelieving world: the mass deaths from starvation in Ukraine in the years 1932-1933. </p>

<p>This was, of course, no mere starvation, but a <em>famine</em> artificially, deliberately, and centrally imposed from Moscow to bring a recalcitrant and sovereign nation into line.  A comprehensive lockdown was enforced at gunpoint to seal the Ukrainians’ doom. Prying foreign eyes were kept away, unless they were willing to lie on the regime's behalf. Two who bucked the trend were Sergio Gradenigo, the Italian Consul in Kharkiv, whose dispatches on the famine from his ringside seat I was asked to translate for publication in the Famine Commission's report. The other truth-teller was Malcolm Muggeridge, who would later report on his experiences for the Manchester Guardian.</p>

<p>Exposing the Holodomor earned Western commentators no respect, only abuse. There is a 1970s clip on YouTube in which journalist Alan Brien, a close friend of my parents, attacks Muggeridge in a television interview as a "born defector," a supreme insult among Marxist intellectuals. Muggeridge is stunned into silence on camera; years after the Famine, the barbs still sting, and the hated exile is named and shamed for all to behold. </p>

<p>Men like Consul Gradenigo would typically be accused of peddling fascist propaganda. Other apologists for Stalin have sought to stonewall, blathering that there "is nothing to see here," that grain shortages were unfortunate but a domestic matter (although were they ever purely domestic, if the regime was dependent on Western grain sales, as it plainly was on several occasions). There are scoffers to this day who challenge the death toll estimated in the tens of millions, accusing Conquest of exaggeration, of ignoring "innocent" explanations, and--in the familiar manner of Holocaust denialists--insinuating at some level that the victims got their deserts.</p>

<p>The relentless vilification and silencing of Western investigators created a vacuum in which, in the absence of alternatives, ignorance of the Ukrainian famine became a matter of respectable opinion--of respectable <em>bourgeois</em> opinion. But willful self-delusion was also involved.</p>

<p>An immense effort of the imagination, a dedication to the truth, and imperviousness to criticism were and are required to grasp the unfathomable scale of the horror. Ukraine in 1932-33 was, in Conquest's oft-repeated phrase, akin to "one vast Belsen." As in Germany, the apparatus of oppression brought forth its well-known handmaidens of kangaroo courts and concentration camps. However, the Soviet system added its own finishing touches. In this topsy-turvy world, multitudes starved while grain shipments rotted in railway sidings. Cows were not fattened for the table, but slaughtered and dumped into rivers to conceal them from requisition.  </p>

<p>Any peasant who was *not* starving was presumed to be hoarding grain.  A grieving mother digging a grave for her child was charged with creating an illicit hideaway for grain, even if there was no grain. Then, if she were lucky, she might be permitted to resume the burial rites with a stern reprimand after the mournful contents of her trench had been inspected.  Sporadic and essentially non-existent poverty relief efforts were derided in party organs as a "waste of bread and fish". Traditional sources of succor were also targeted; churches were decommissioned, bells first, while other cherished fixtures and fittings were scooped up and carted away later.</p>

<p>Conquest summarizes the roots of this catastrophe as follows, with careful objectivity and without embellishment:<br>

<blockquote>1. The cause of the famine was the setting of highly excessive grain requisition targets by Stalin and his associates.<br>
2. Ukrainian party leaders made it clear at the start to Stalin and his associates that these targets were highly excessive.<bR>
3. the targets were nevertheless enforced until starvation began.<br>
4. Ukrainian leaders pointed this out to Stalin and his associates and the truth was also made known to him and them by others.<br>
5. the requisitions nonetheless continued.<br></blockquote>

So the assault was on the victims first, then on the truth, and last on those who sought to reveal the truth. Given the coverup, how did Conquest and other researchers get to the bottom of this story?  In fact, there was plenty of material for those who cared to look. It is possible to deduce the body count inferentially from official Soviet population and public health statistics from one year to the next; there were startling bursts of honesty in the press, and not only during the Krushchev interlude; novelists provide other pieces of the puzzle.  </p>

<p>There were further Western eye-witness accounts, furnished not just by Muggeridge and Gradenigo. Alas, the picture conveyed to mass audiences was very different. Pick up copies of Time-Life Magazine issues from the 30s and 40s and you will see pictures of billowing fields of "Ukrainian" grain along with portraits of smiling girls in peasant costumes.  This campaign of falsification was pioneered by Walter Duranty, the NYT columnist and Nobel Prizewinner, who knew the truth but preferred to preserve his influential press contacts in Moscow. It was, to be sure, difficult and dangerous for a Western journalist to report from the USSR, but many did not bother to try. In a book in my collection, featuring a selection of NYT profiles of early 1960s Russia, the American journalist visitors have been thoroughly Potemkinized, with one notable exception--Mike Wallace (later with "60 Minutes") who gives a Soviet official a thorough grilling.</p>

<p>Conquest's critics, I suspect, dislike him not because he shattered the dream of the Communist nirvana, but because he held it up to mockery. Here is his description of agrarian reform, Soviet style, from "Stalin -- Breaker of Nations" (1991).</p>

<p>"As it was, in the villages the party could in fact only rely on some of the local unemployables and psychopaths on the one hand, and some of the teachers on the other; the latter were often educated to the degree of accepting party dogmas, but no higher. They were assisted, or rather led, by the 'twenty-five-thousanders,' sent by the party from the cities and almost all totally ignorant of agriculture, but strong on propaganda and terror."</p>

<p>Fellow travelers limply asserted that agrarian reform was necessary, and had long been perceived to be necessary (as those who finished reading the digression on the subject in "Anna Karenina" will testify). The agriculture sector at the time of the Revolution, unconsolidated although not necessarily inefficient, lacked good information and statistical data. Something had to be done. And yet, as Krushchev pointed out to disbelieving officials in the 1950s, the statistics proved that for all the horror of collectivization and the human catastrophe that came with it, the agriculture sector's performance did not improve in any meaningful sense.  This was, in short, all done for nothing, and paid for at horrendous cost.</p>







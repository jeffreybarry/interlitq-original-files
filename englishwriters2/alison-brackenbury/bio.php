                  <p>Alison Brackenbury was born in 1953 in Lincolnshire, in the English East Midlands.  She grew up in the countryside and is descended from a long line of shepherds. Her first collection of poems, <em>Dreams of Power</em>, published in 1981, when she was 28, won an Eric Gregory Award and was a Poetry Book Society Recommendation. </p>

<p>Since then, she has published eight more collections, all with Carcanet.   Her widely-anthologised poems have won a Cholmondeley Award, given by the Society of Authors for a writer’s entire body of work.  Gillian Clarke, the National Poet of Wales, wrote: ‘Alison Brackenbury loves, lives, hymns and rhymes the natural world and its people like no other poet.’</p>

<p>Despite working for decades in her husband’s family metal finishing business, which made live readings difficult, Alison has managed to do a considerable amount of broadcasting on BBC Radios 3 and 4.  Her feature ‘Singing in the Dark’, on the stubborn survival of traditional song, was singled out by the Radio Times as ‘evocative, amusing and utterly compelling’.  Earlier this year, BBC Radio 4’s arts programme, ‘Front Row’, commissioned her to write a poem about her old enemy, January.</p>

<p>Alison writes reviews for a variety of poetry journals, often covering first collections and pamphlets.  She is particularly enthusiastic about the Internet as a means of bringing poems to a wider audience, and was featured in a recent article in <em>The Independent</em> about poets on Twitter.</p>

<p>Since hanging up her boiler suit in 2012 and retiring from her day job, she has been able to give readings at a wide range of poetry events, including the Ledbury and Aldeburgh Poetry Festivals.  She has also recorded her work for the online Poetry Archive.</p>

<p>Alison’s ninth collection of poems includes poems about hares, history, ‘our heated world’, and the often humorous beginnings of an exploration of age. This new collection will be published in March 2016 by Carcanet.</p>




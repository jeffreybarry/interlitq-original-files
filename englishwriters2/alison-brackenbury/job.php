&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;    <strong>    Breaking the fast </strong><br> <br>


When I am alone, toast is ceremony. <br>
I cut bread thick, (cooking is art), <br>
discover butter, for my cracked skin. <br>
Toast must be hot, butter is chill. <br>
I slide both in the microwave’s loud glow, <br>
wait for the chime, heap strawberries. <br>
But I must eat it where I am, <br>
perfect, alone.  Love’s toast is burned, <br>
brought cold upstairs, with the wrong jam. <br><br><br><br>




&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;                  <strong>   February 26th  </strong><br><br>


Though I read ‘Notes on Stars’, <br>
yet I always forget<br>
the planet, or comet, <br>
until my blank noon. <br>
But our milkman saved me. <br>
I saw on the dawn’s step<br>
the milk, then high Venus, <br>
by her crescent moon. <br><br>

They were a white iris, <br>
as crisp as an eyelash, <br>
a slim curve, a great blaze, <br>
taut blue that too soon<br>
is cars, is dulled daylight. <br>
Look quick, you may catch them, <br>
old Venus, though faded, <br>
her smudged crescent moon. <br><br><br><br>


&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;          <strong>    The gamekeeper's wife </strong><br><br>


The village noted clean washing. <br>
Where did the keeper go? <br>
Under the hawthorns hung his gibbet, <br>
strands of wire to show<br>
shot crows, dulled magpies, squirrels’ plumes. <br>
Torn weasels danced below. <br><br>

When we took the gamekeeper's house, <br>
I learned he had a wife. <br>
I knew women named for flowers. <br>
Rose, Daisy, no man’s wife, <br>
kept house for brothers, grim with pills<br>
trudged past me, shut from life. <br><br>

Her house lay half a mile from lights, <br>
I never saw her face. <br>
The cobwebs held a reeking soot. <br>
They heated the whole place<br>
with paraffin.  We tried it once, <br>
each flame a fierce blue taste. <br><br>

Did the house draughts consume us? <br>
Books cracked me, then instead<br>
I found a boy. Father, retired, <br>
took briefly to his bed. <br>
My mother read there, undisturbed. <br>
Wisely, my sister said<br><br>

they bought a bungalow.  No well<br>
in grass breathed apples’ rot. <br>
No rose from an old garden turned<br>
white petals apricot, <br>
spared by the keeper’s childless wife<br>
one warm hour, while he shot. <br><br><br><br>



 &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;              <strong>         Criticism  </strong><br><br>


‘Good Muses keep hands clean!’ <br><br>

‘Do they?’ she frowns, <br>
sets down the stable fork, <br>
picks up the baby, <br>
welds in the wartime factory<br>
until the siren wails, <br>
notices, in silence, <br>
the whiteness of my nails. <br><br><br><br>




&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;     <strong>   Eight</strong><br><br>


He stayed in Crete.   The family<br>
beckoned him up, one day, to see<br>
a small back room, a curtained niche. <br><br>

A photograph? A daughter dead? <br>
Or slubbed gold heaven, the icon’s head? <br>
Cloth dragged the boards with its rough swish. <br><br>

They held their ground.  They stood in rows, <br>
unlaced, like prisoners’, heels to toes, <br>
eight pairs of paratroopers’ boots. <br><br>

One was bashed by an olive tree, <br>
one bent by rock.  How carefully<br>
they had been eased from each warm foot. <br><br>

One listener asked, ‘Were they for spares?’ <br>
‘No,’ he said firmly.  They stood there<br>
polished in ranks.  It was a shrine. <br><br>

The deaths that were.  The deaths to come-<br>
By sea’s first sigh, the thyme-bees hum. <br>
Eight pairs of boots march down the line. <br><br><br><br>






